CREATE PROCEDURE [dbo].[PermitDumpsterCustomFields]
	@PERMITID AS VARCHAR(36)
AS

SELECT
	cspm.MoreThanOneWeek,
	cspm.NumberOfWeeks,
	cspm.PurposeOfDumpster,
	cspm.OtherAreaToBeOccupied,
	(SELECT TOP 1 cfpli.SVALUE FROM [$(EnerGovDatabase)].dbo.CUSTOMSAVERPERMITMANAGEMENTMS cspmms 
		INNER JOIN [$(EnerGovDatabase)].dbo.CUSTOMFIELDPICKLISTITEM cfpli ON cspmms.CUSTOMFIELDPICKLISTITEMID = cfpli.GCUSTOMFIELDPICKLISTITEM 
		WHERE cspmms.ID = @PERMITID AND cspmms.CUSTOMFIELDPICKLISTITEMID = 'f91f88ad-ff3f-4a4f-9513-2763a07fe622') AS "AreaToBeOccupiedAlley",
	(SELECT TOP 1 cfpli.SVALUE FROM [$(EnerGovDatabase)].dbo.CUSTOMSAVERPERMITMANAGEMENTMS cspmms 
		INNER JOIN [$(EnerGovDatabase)].dbo.CUSTOMFIELDPICKLISTITEM cfpli ON cspmms.CUSTOMFIELDPICKLISTITEMID = cfpli.GCUSTOMFIELDPICKLISTITEM 
		WHERE cspmms.ID = @PERMITID AND cspmms.CUSTOMFIELDPICKLISTITEMID = '1ca9c00c-e9c3-48fe-9173-fb45888f6240') AS "AreaToBeOccupiedBoulevard",
	(SELECT TOP 1 cfpli.SVALUE FROM [$(EnerGovDatabase)].dbo.CUSTOMSAVERPERMITMANAGEMENTMS cspmms 
		INNER JOIN [$(EnerGovDatabase)].dbo.CUSTOMFIELDPICKLISTITEM cfpli ON cspmms.CUSTOMFIELDPICKLISTITEMID = cfpli.GCUSTOMFIELDPICKLISTITEM 
		WHERE cspmms.ID = @PERMITID AND cspmms.CUSTOMFIELDPICKLISTITEMID = 'c8e6918f-57f7-4cc8-bef1-8f46f8603e5f') AS "AreaToBeOccupiedRoadway",
	(SELECT TOP 1 cfpli.SVALUE FROM [$(EnerGovDatabase)].dbo.CUSTOMSAVERPERMITMANAGEMENTMS cspmms 
		INNER JOIN [$(EnerGovDatabase)].dbo.CUSTOMFIELDPICKLISTITEM cfpli ON cspmms.CUSTOMFIELDPICKLISTITEMID = cfpli.GCUSTOMFIELDPICKLISTITEM 
		WHERE cspmms.ID = @PERMITID AND cspmms.CUSTOMFIELDPICKLISTITEMID = '1ca09c75-5cd6-4519-987b-6527c637986d') AS "AreaToBeOccupiedSidewalk",
	(SELECT TOP 1 cfpli.SVALUE FROM [$(EnerGovDatabase)].dbo.CUSTOMSAVERPERMITMANAGEMENTMS cspmms 
		INNER JOIN [$(EnerGovDatabase)].dbo.CUSTOMFIELDPICKLISTITEM cfpli ON cspmms.CUSTOMFIELDPICKLISTITEMID = cfpli.GCUSTOMFIELDPICKLISTITEM 
		WHERE cspmms.ID = @PERMITID AND cspmms.CUSTOMFIELDPICKLISTITEMID = '1ce84320-e569-41d0-80f2-205031793865') AS "AreaToBeOccupiedOther",
	p.PERMITNUMBER
FROM [$(EnerGovDatabase)].dbo.CUSTOMSAVERPERMITMANAGEMENT cspm
INNER JOIN [$(EnerGovDatabase)].dbo.PMPERMIT p ON cspm.ID = p.PMPERMITID
WHERE cspm.ID = @PERMITID

