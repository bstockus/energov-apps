CREATE PROCEDURE [businessmanagementsetup].[USP_BLLICENSETYPE_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
BEGIN
SET NOCOUNT ON;
WITH RAW_DATA AS (
	SELECT
		[dbo].[BLLICENSETYPE].[BLLICENSETYPEID],
		[dbo].[BLLICENSETYPE].[NAME],
		[dbo].[BLLICENSETYPE].[PREFIX],
		[BLLICENSEDEFAULTSTATUS].[NAME] AS DEFAULTSTATUS,
		[BLLICENSEINTERNETDEFAULTSTATUS].[NAME] AS DEFAULTONLINEAPPLYSTATUS,
		[BLLICENSEDEFAULTRENEWSTATUS].[NAME] AS DEFAULTRENEWSTATUS,
		[BLLICENSEDEFAULTWEBRENEWSTATUS].[NAME] AS DEFAULTONLINERENEWSTATUS,
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[BLLICENSETYPE].[NAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[BLLICENSETYPE].[NAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[BLLICENSETYPE]
		LEFT JOIN [dbo].[BLLICENSESTATUS] [BLLICENSEDEFAULTSTATUS] ON [BLLICENSEDEFAULTSTATUS].[BLLICENSESTATUSID] = [dbo].[BLLICENSETYPE].[DEFAULTSTATUSID]
		LEFT JOIN [dbo].[BLLICENSESTATUS] [BLLICENSEINTERNETDEFAULTSTATUS] ON [BLLICENSEINTERNETDEFAULTSTATUS].[BLLICENSESTATUSID] = [dbo].[BLLICENSETYPE].[DEFAULTINTERNETBLLICSTATUSID]
		LEFT JOIN [dbo].[BLLICENSESTATUS] [BLLICENSEDEFAULTRENEWSTATUS] ON [BLLICENSEDEFAULTRENEWSTATUS].[BLLICENSESTATUSID] = [dbo].[BLLICENSETYPE].[DEFAULTRENEWSTATUSID]
		LEFT JOIN [dbo].[BLLICENSESTATUS] [BLLICENSEDEFAULTWEBRENEWSTATUS] ON [BLLICENSEDEFAULTWEBRENEWSTATUS].[BLLICENSESTATUSID] = [dbo].[BLLICENSETYPE].[DEFAULTWEBRENEWSTATUSID]
	WHERE [dbo].[BLLICENSETYPE].[NAME] LIKE '%'+ @SEARCH +'%'
)
SELECT * FROM RAW_DATA
	WHERE
		RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND
		RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
	ORDER BY RowNumber
END