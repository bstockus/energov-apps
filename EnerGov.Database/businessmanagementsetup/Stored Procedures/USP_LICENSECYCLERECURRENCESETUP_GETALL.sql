CREATE PROCEDURE [businessmanagementsetup].[USP_LICENSECYCLERECURRENCESETUP_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[LICENSECYCLERECURRENCESETUP].[LICENSECYCLERECURRENCESETUPID],
	[dbo].[LICENSECYCLERECURRENCESETUP].[NAME],
	[dbo].[LICENSECYCLERECURRENCESETUP].[DESCRIPTION],
	[dbo].[LICENSECYCLERECURRENCESETUP].[RECURRENCEID],
	[dbo].[LICENSECYCLERECURRENCESETUP].[LICENSECYCLETYPEID],
	[dbo].[RECURRENCE].[NAME] AS [RECURRENCENAME],
	[dbo].[LICENSECYCLERECURRENCESETUP].[LASTCHANGEDBY],
	[dbo].[LICENSECYCLERECURRENCESETUP].[LASTCHANGEDON],
	[dbo].[LICENSECYCLERECURRENCESETUP].[ROWVERSION]
FROM [dbo].[LICENSECYCLERECURRENCESETUP]
	LEFT JOIN [dbo].[RECURRENCE] ON [dbo].[LICENSECYCLERECURRENCESETUP].[RECURRENCEID] = [dbo].[RECURRENCE].[RECURRENCEID]
ORDER BY [dbo].[LICENSECYCLERECURRENCESETUP].[NAME] ASC
END