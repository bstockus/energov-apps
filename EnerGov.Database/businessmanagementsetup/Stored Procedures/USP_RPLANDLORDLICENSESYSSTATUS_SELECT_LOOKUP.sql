CREATE PROCEDURE [businessmanagementsetup].[USP_RPLANDLORDLICENSESYSSTATUS_SELECT_LOOKUP]
AS
	SET NOCOUNT ON;
	SELECT 
		[dbo].[RPLANDLORDLICENSESYSSTATUS].[RPLANDLORDLICENSESYSSTATUSID],
		[dbo].[RPLANDLORDLICENSESYSSTATUS].[NAME]
	FROM [dbo].[RPLANDLORDLICENSESYSSTATUS]
	ORDER BY [dbo].[RPLANDLORDLICENSESYSSTATUS].[NAME] ASC