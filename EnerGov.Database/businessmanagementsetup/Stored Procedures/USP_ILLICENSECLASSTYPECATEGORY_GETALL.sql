CREATE PROCEDURE [businessmanagementsetup].[USP_ILLICENSECLASSTYPECATEGORY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[ILLICENSECLASSTYPECATEGORY].[ILLICENSECLASSTYPECATEGORYID],
	[dbo].[ILLICENSECLASSTYPECATEGORY].[NAME],
	[dbo].[ILLICENSECLASSTYPECATEGORY].[DESCRIPTION],
	[dbo].[ILLICENSECLASSTYPECATEGORY].[LASTCHANGEDBY],
	[dbo].[ILLICENSECLASSTYPECATEGORY].[LASTCHANGEDON],
	[dbo].[ILLICENSECLASSTYPECATEGORY].[ROWVERSION]
FROM [dbo].[ILLICENSECLASSTYPECATEGORY]
ORDER BY [dbo].[ILLICENSECLASSTYPECATEGORY].[NAME] ASC

END