CREATE PROCEDURE [businessmanagementsetup].[USP_EXAMSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[EXAMSTATUS].[EXAMSTATUSID],
	[dbo].[EXAMSTATUS].[NAME],
	[dbo].[EXAMSTATUS].[DESCRIPTION],
	[dbo].[EXAMSTATUS].[LASTCHANGEDBY],
	[dbo].[EXAMSTATUS].[LASTCHANGEDON],
	[dbo].[EXAMSTATUS].[ROWVERSION]
FROM 
	[dbo].[EXAMSTATUS]
ORDER BY
	[dbo].[EXAMSTATUS].[NAME]
END