CREATE PROCEDURE [businessmanagementsetup].[USP_RPCONTACTTYPE_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
WITH RAW_DATA AS (
	SELECT
	    [dbo].[RPCONTACTTYPE].[RPCONTACTTYPEID],
		[dbo].[RPCONTACTTYPE].[NAME],		
		[SYSTEMNAME] = [dbo].[RPCONTACTTYPESYSTEM].[NAME],
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[RPCONTACTTYPE].[NAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[RPCONTACTTYPE].[NAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[RPCONTACTTYPE]
	INNER JOIN [dbo].[RPCONTACTTYPESYSTEM] ON [dbo].[RPCONTACTTYPE].[RPCONTACTTYPESYSTEMID] = [dbo].[RPCONTACTTYPESYSTEM].[RPCONTACTTYPESYSTEMID]
	WHERE ([dbo].[RPCONTACTTYPE].[NAME] LIKE '%'+ @SEARCH +'%')
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber