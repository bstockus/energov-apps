CREATE PROCEDURE [businessmanagementsetup].[USP_ILLICENSETYPE_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
WITH RAW_DATA AS (
	SELECT
		[dbo].[ILLICENSETYPE].[ILLICENSETYPEID],
		[dbo].[ILLICENSETYPE].[NAME],
		[dbo].[ILLICENSETYPE].[PREFIX],
		[DEFAULTWEBAPPLYSTATUS].[NAME] AS [DEFAULTWEBAPPLYSTATUS_NAME],
		[DEFAULTRENEWSTATUS].[NAME] AS [DEFAULTRENEWSTATUS_NAME],
		[DEFAULTLICENSESTATUS].[NAME] AS [DEFAULTLICENSESTATUS_NAME],
		[DEFAULTWEBRENEWSTATUS].[NAME] AS [DEFAULTWEBRENEWSTATUS_NAME],
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[ILLICENSETYPE].[NAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[ILLICENSETYPE].[NAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
		FROM [dbo].[ILLICENSETYPE]
		LEFT JOIN [dbo].[ILLICENSESTATUS] AS [DEFAULTWEBAPPLYSTATUS] WITH (NOLOCK) ON [ILLICENSETYPE].[DEFAULTWEBAPPLYSTATUSID] = [DEFAULTWEBAPPLYSTATUS].[ILLICENSESTATUSID]
		LEFT JOIN [dbo].[ILLICENSESTATUS] AS [DEFAULTRENEWSTATUS] WITH (NOLOCK) ON [ILLICENSETYPE].[DEFAULTRENEWSTATUSID] = [DEFAULTRENEWSTATUS].[ILLICENSESTATUSID]
		LEFT JOIN [dbo].[ILLICENSESTATUS] AS [DEFAULTLICENSESTATUS] WITH (NOLOCK) ON [ILLICENSETYPE].[DEFAULTLICENSESTATUSID] = [DEFAULTLICENSESTATUS].[ILLICENSESTATUSID]
		LEFT JOIN [dbo].[ILLICENSESTATUS] AS [DEFAULTWEBRENEWSTATUS] WITH (NOLOCK) ON [ILLICENSETYPE].[DEFAULTWEBRENEWSTATUSID] = [DEFAULTWEBRENEWSTATUS].[ILLICENSESTATUSID]
		WHERE ([dbo].[ILLICENSETYPE].[NAME] LIKE '%'+ @SEARCH +'%')
)
SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber