CREATE PROCEDURE [businessmanagementsetup].[USP_ILLICENSEALLOWEDACTIVITY_GETBYPARENTID]
(
	@ILLICENSETYPEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[ILLICENSEALLOWEDACTIVITY].[ILLICENSEALLOWEDACTIVITYID],
	[dbo].[ILLICENSEALLOWEDACTIVITY].[ILLICENSETYPEID],
	[dbo].[ILLICENSEALLOWEDACTIVITY].[ILLICENSEACTIVITYTYPEID],
	[dbo].[ILLICENSEACTIVITYTYPE].[NAME]
FROM [dbo].[ILLICENSEALLOWEDACTIVITY]
INNER JOIN [dbo].[ILLICENSEACTIVITYTYPE] 
	ON [dbo].[ILLICENSEACTIVITYTYPE].[ILLICENSEACTIVITYTYPEID] = [dbo].[ILLICENSEALLOWEDACTIVITY].[ILLICENSEACTIVITYTYPEID]
WHERE
	[dbo].[ILLICENSEALLOWEDACTIVITY].[ILLICENSETYPEID] = @ILLICENSETYPEID
ORDER BY [dbo].[ILLICENSEACTIVITYTYPE] .[NAME]
END