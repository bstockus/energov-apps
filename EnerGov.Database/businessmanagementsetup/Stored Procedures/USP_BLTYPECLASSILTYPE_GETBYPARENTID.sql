CREATE PROCEDURE [businessmanagementsetup].[USP_BLTYPECLASSILTYPE_GETBYPARENTID]
(
	@BLLICENSETYPEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BLTYPECLASSILTYPE].[BLTYPECLASSILTYPEID],
	[dbo].[BLTYPECLASSILTYPE].[BLLICENSETYPECLASSID],
	[dbo].[BLTYPECLASSILTYPE].[ILLICENSETYPEID],	
	[dbo].[ILLICENSETYPE].[NAME]
	FROM [dbo].[BLTYPECLASSILTYPE]
	INNER JOIN [BLLICENSETYPECLASS] 
		ON [dbo].[BLLICENSETYPECLASS].[BLLICENSETYPECLASSID] = [dbo].[BLTYPECLASSILTYPE].[BLLICENSETYPECLASSID]
	INNER JOIN [dbo].[BLLICENSETYPE]
		ON [dbo].[BLLICENSETYPE].[BLLICENSETYPEID] = [dbo].[BLLICENSETYPECLASS].[BLLICENSETYPEID]
	INNER JOIN [dbo].[ILLICENSETYPE]
		ON [dbo].[ILLICENSETYPE].[ILLICENSETYPEID] = [dbo].[BLTYPECLASSILTYPE].[ILLICENSETYPEID]	
	WHERE
		[dbo].[BLLICENSETYPE].[BLLICENSETYPEID] = @BLLICENSETYPEID
	ORDER BY [dbo].[ILLICENSETYPE].[NAME]
END