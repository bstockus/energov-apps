CREATE PROCEDURE [businessmanagementsetup].[USP_TXBILLPERIOD_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[TXBILLPERIOD].[BILLPERIODID],
	[dbo].[TXBILLPERIOD].[YEAR],
	[dbo].[TXBILLPERIOD].[PERIODNAME],
	[dbo].[TXBILLPERIOD].[STARTDATE],
	[dbo].[TXBILLPERIOD].[ENDDATE],
	[dbo].[TXBILLPERIOD].[DUEDATE],
	[dbo].[TXBILLPERIOD].[DEFAULTGENERATEDDATE],
	[dbo].[TXBILLPERIOD].[RPTPERIODREF],
	[dbo].[TXBILLPERIOD].[LATEDATE],
	[dbo].[TXBILLPERIOD].[COMPOUNDINGINTERESTSTARTDATE],
	[TXBILLPERIODYEAR_YEAR].[YEARNAME] AS [YEARNAME],
	[dbo].[TXBILLPERIOD].[LASTCHANGEDBY],
	[dbo].[TXBILLPERIOD].[LASTCHANGEDON],
	[dbo].[TXBILLPERIOD].[ROWVERSION]
FROM [dbo].[TXBILLPERIOD]
LEFT JOIN [TXBILLPERIODYEAR] TXBILLPERIODYEAR_YEAR WITH (NOLOCK) ON [TXBILLPERIODYEAR_YEAR].[YEAR] = [TXBILLPERIOD].[YEAR]
ORDER BY [dbo].[TXBILLPERIOD].[PERIODNAME] ASC  

END