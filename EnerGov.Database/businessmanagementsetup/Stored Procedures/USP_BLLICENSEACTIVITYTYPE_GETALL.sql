CREATE PROCEDURE [businessmanagementsetup].[USP_BLLICENSEACTIVITYTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BLLICENSEACTIVITYTYPE].[BLLICENSEACTIVITYTYPEID],
	[dbo].[BLLICENSEACTIVITYTYPE].[NAME],
	[dbo].[BLLICENSEACTIVITYTYPE].[DESCRIPTION],
	[dbo].[BLLICENSEACTIVITYTYPE].[CANEDIT],
	[dbo].[BLLICENSEACTIVITYTYPE].[CREATEID],
	[dbo].[BLLICENSEACTIVITYTYPE].[PREFIXID],
	[dbo].[BLLICENSEACTIVITYTYPE].[ALLOWDUPLICATE],
	[dbo].[BLLICENSEACTIVITYTYPE].[CUSTOMFIELDLAYOUTID],
	[dbo].[BLLICENSEACTIVITYTYPE].[LASTCHANGEDBY],
	[dbo].[BLLICENSEACTIVITYTYPE].[LASTCHANGEDON],
	[dbo].[BLLICENSEACTIVITYTYPE].[ROWVERSION]
FROM [dbo].[BLLICENSEACTIVITYTYPE] ORDER BY [dbo].[BLLICENSEACTIVITYTYPE].[NAME] ASC
END