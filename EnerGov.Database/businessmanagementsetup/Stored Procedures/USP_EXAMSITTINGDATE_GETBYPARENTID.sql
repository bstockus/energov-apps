CREATE PROCEDURE [businessmanagementsetup].[USP_EXAMSITTINGDATE_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[EXAMSITTINGDATE].[EXAMSITTINGDATEID],
	[dbo].[EXAMSITTINGDATE].[EXAMOBJECTID],
	[dbo].[EXAMSITTINGDATE].[STARTDATE],
	[dbo].[EXAMSITTINGDATE].[ENDDATE],
	[dbo].[EXAMSITTINGDATE].[CUTOFFDATE]
FROM 
	[dbo].[EXAMSITTINGDATE]
WHERE 
	[dbo].[EXAMSITTINGDATE].[EXAMOBJECTID] = @PARENTID
ORDER BY
	[dbo].[EXAMSITTINGDATE].[STARTDATE]
END