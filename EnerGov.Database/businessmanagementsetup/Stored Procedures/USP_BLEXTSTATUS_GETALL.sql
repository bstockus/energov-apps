CREATE PROCEDURE [businessmanagementsetup].[USP_BLEXTSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BLEXTSTATUS].[BLEXTSTATUSID],
	[dbo].[BLEXTSTATUS].[NAME],
	[dbo].[BLEXTSTATUS].[DESCRIPTION],
	[dbo].[BLEXTSTATUS].[BLEXTSTATUSSYSTEMID],
	[dbo].[BLEXTSTATUS].[LASTCHANGEDBY],
	[dbo].[BLEXTSTATUS].[LASTCHANGEDON],
	[dbo].[BLEXTSTATUS].[ROWVERSION]
FROM [dbo].[BLEXTSTATUS]
ORDER BY [dbo].[BLEXTSTATUS].[NAME] ASC
END