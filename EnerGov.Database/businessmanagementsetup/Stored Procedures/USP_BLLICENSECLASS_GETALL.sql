CREATE PROCEDURE [businessmanagementsetup].[USP_BLLICENSECLASS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BLLICENSECLASS].[BLLICENSECLASSID],
	[dbo].[BLLICENSECLASS].[NAME],
	[dbo].[BLLICENSECLASS].[DESCRIPTION],
	[dbo].[BLLICENSECLASS].[LASTCHANGEDBY],
	[dbo].[BLLICENSECLASS].[LASTCHANGEDON],
	[dbo].[BLLICENSECLASS].[ROWVERSION]
FROM [dbo].[BLLICENSECLASS]
ORDER BY [dbo].[BLLICENSECLASS].[NAME] ASC

END