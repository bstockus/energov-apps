CREATE PROCEDURE [businessmanagementsetup].[USP_EXAMTYPE_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
WITH RAW_DATA AS (
	SELECT
		[dbo].[EXAMTYPE].[EXAMTYPEID],
		[dbo].[EXAMTYPE].[NAME],
		[dbo].[EXAMTYPE].[DESCRIPTION],
		[dbo].[EXAMTYPE].[PREFIX],
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[EXAMTYPE].[NAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[EXAMTYPE].[NAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[EXAMTYPE]
	WHERE ([dbo].[EXAMTYPE].[NAME] LIKE '%'+ @SEARCH +'%') OR
		  ([dbo].[EXAMTYPE].[DESCRIPTION] LIKE '%'+ @SEARCH +'%') OR
		  ([dbo].[EXAMTYPE].[PREFIX] LIKE '%'+ @SEARCH +'%')
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber