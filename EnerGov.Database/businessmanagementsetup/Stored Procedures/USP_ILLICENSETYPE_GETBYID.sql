CREATE PROCEDURE [businessmanagementsetup].[USP_ILLICENSETYPE_GETBYID]      
(      
	@ILLICENSETYPEID CHAR(36)      
)      
AS      
BEGIN      
SET NOCOUNT ON;      
SELECT       
	[dbo].[ILLICENSETYPE].[ILLICENSETYPEID],
	[dbo].[ILLICENSETYPE].[NAME],
	[dbo].[ILLICENSETYPE].[DESCRIPTION],
	[dbo].[ILLICENSETYPE].[KEEPLICENSENUMBER],
	[dbo].[ILLICENSETYPE].[AUTONUMBER],
	[dbo].[ILLICENSETYPE].[CUSTOMFIELDLAYOUTID],
	[dbo].[ILLICENSETYPE].[PREFIX],
	[dbo].[ILLICENSETYPE].[DEFAULTLICENSESTATUSID],
	[dbo].[ILLICENSETYPE].[FRIENDLYNAME],
	[dbo].[ILLICENSETYPE].[DEFAULTWEBAPPLYSTATUSID],
	[dbo].[ILLICENSETYPE].[DEFAULTWEBRENEWSTATUSID],
	[dbo].[ILLICENSETYPE].[ACTIVE],
	[dbo].[ILLICENSETYPE].[USESLICENSEGROUP],
	[dbo].[ILLICENSETYPE].[USESLICENSECLASSTYPES],
	[dbo].[ILLICENSETYPE].[ISRENEWABLE],
	[dbo].[ILLICENSETYPE].[ALLOWWEBSUBMISSION],
	[dbo].[ILLICENSETYPE].[ALLOWRENEWALLICENSENUM],
	[dbo].[ILLICENSETYPE].[ISRECEIPTSTYPE],
	[dbo].[ILLICENSETYPE].[DEFAULTRENEWSTATUSID],
	[dbo].[ILLICENSETYPE].[SETRENEWISSDATETOPRVEXPDATE],
	[dbo].[ILLICENSETYPE].[SETISSUEDATEONRENEWAL],
	[dbo].[ILLICENSETYPE].[CURRENTYEARONRENEWALS],
	[dbo].[ILLICENSETYPE].[ILLICENSESYSTEMTYPEID],
	[dbo].[ILLICENSETYPE].[ALLOWWEBRENEWAL],
	[dbo].[ILLICENSETYPE].[REQUIRELICENSEGROUP],
	[dbo].[ILLICENSETYPE].[REQUIRELICENSECLASSTYPE],
	[dbo].[ILLICENSETYPE].[SKIPPAYMENT],
	[dbo].[ILLICENSETYPE].[LASTCHANGEDBY],
	[dbo].[ILLICENSETYPE].[LASTCHANGEDON],
	[dbo].[ILLICENSETYPE].[ROWVERSION]
FROM [dbo].[ILLICENSETYPE]    
WHERE      
 [dbo].[ILLICENSETYPE].[ILLICENSETYPEID] = @ILLICENSETYPEID  
 
	EXEC [businessmanagementsetup].[USP_ILLICENSETYPELICENSECLASS_GETBYPARENTID] @ILLICENSETYPEID
	EXEC [businessmanagementsetup].[USP_ILLICENSEREQUIREDFIELD_GETBYPARENTID] @ILLICENSETYPEID
	EXEC [businessmanagementsetup].[USP_BLCONTACTTYPEREF_GETBYPARENTID] @ILLICENSETYPEID
    EXEC [businessmanagementsetup].[USP_ATTACHMENTREQFILEREF_GETBYPARENTID] @ILLICENSETYPEID
	EXEC [businessmanagementsetup].[USP_ILLICENSETYPELICENSEGROUP_GETBYPARENTID] @ILLICENSETYPEID
	EXEC [businessmanagementsetup].[USP_ILLICENSEALLOWEDACTIVITY_GETBYPARENTID] @ILLICENSETYPEID
	EXEC [businessmanagementsetup].[USP_ILLICENSETYPECLASSTYPE_GETBYPARENTID] @ILLICENSETYPEID
END