CREATE PROCEDURE [businessmanagementsetup].[USP_EXAMTYPERULE_GETBYPARENTID]
(
	@EXAMTYPEID CHAR(36)
)
AS
BEGIN
SELECT 
	[dbo].[EXAMTYPERULE].[EXAMTYPERULEID],
	[dbo].[EXAMTYPERULE].[EXAMTYPEID],
	[dbo].[EXAMTYPERULE].[EXAMRULEID],
	[dbo].[EXAMRULE].[TEXT],
	[dbo].[EXAMRULE].[EXAMRULEVALUETYPEID],
	[dbo].[EXAMRULEVALUETYPE].[NAME],
	[dbo].[EXAMTYPERULE].[BITVALUE],
	[dbo].[EXAMTYPERULE].[STRINGVALUE],
	[dbo].[EXAMTYPERULE].[INTVALUE]
FROM [dbo].[EXAMTYPERULE]
INNER JOIN [dbo].[EXAMRULE]
ON [dbo].[EXAMRULE].[EXAMRULEID] = [dbo].[EXAMTYPERULE].[EXAMRULEID]	
INNER JOIN [dbo].[EXAMRULEVALUETYPE]
ON [dbo].[EXAMRULEVALUETYPE].[EXAMRULEVALUETYPEID] = [dbo].[EXAMRULE].[EXAMRULEVALUETYPEID]		
WHERE [dbo].[EXAMTYPERULE].[EXAMTYPEID] = @EXAMTYPEID
ORDER BY [dbo].[EXAMRULE].[TEXT]
END