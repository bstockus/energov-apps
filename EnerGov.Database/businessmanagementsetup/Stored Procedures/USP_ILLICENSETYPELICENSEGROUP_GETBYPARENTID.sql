CREATE PROCEDURE [businessmanagementsetup].[USP_ILLICENSETYPELICENSEGROUP_GETBYPARENTID]
(
	@ILLICENSETYPEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
	SELECT 
		[ILLICENSETYPELICENSEGROUP].[ILLICENSEGROUPID],
		[ILLICENSETYPELICENSEGROUP].[ILLICENSETYPEID],
		[ILLICENSEGROUP].[NAME]
	FROM [dbo].[ILLICENSETYPELICENSEGROUP]
		INNER JOIN [dbo].[ILLICENSEGROUP] ON [ILLICENSETYPELICENSEGROUP].[ILLICENSEGROUPID]= [ILLICENSEGROUP].[ILLICENSEGROUPID]
	WHERE [ILLICENSETYPELICENSEGROUP].[ILLICENSETYPEID]=@ILLICENSETYPEID
END