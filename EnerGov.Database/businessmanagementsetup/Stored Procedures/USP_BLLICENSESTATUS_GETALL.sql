CREATE PROCEDURE [businessmanagementsetup].[USP_BLLICENSESTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BLLICENSESTATUS].[BLLICENSESTATUSID],
	[dbo].[BLLICENSESTATUS].[NAME],
	[dbo].[BLLICENSESTATUS].[DESCRIPTION],
	[dbo].[BLLICENSESTATUS].[BLLICENSESTATUSSYSTEMID],
	[dbo].[BLLICENSESTATUS].[LASTCHANGEDBY],
	[dbo].[BLLICENSESTATUS].[LASTCHANGEDON],
	[dbo].[BLLICENSESTATUS].[ROWVERSION]
FROM [dbo].[BLLICENSESTATUS]
ORDER BY [dbo].[BLLICENSESTATUS].[NAME]

END