CREATE PROCEDURE [businessmanagementsetup].[USP_RPPROPERTYACTIVITYTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT		[dbo].[RPPROPERTYACTIVITYTYPE].[RPPROPERTYACTIVITYTYPEID],
			[dbo].[RPPROPERTYACTIVITYTYPE].[NAME],
			[dbo].[RPPROPERTYACTIVITYTYPE].[DESCRIPTION],
			[dbo].[RPPROPERTYACTIVITYTYPE].[CANEDIT],
			[dbo].[RPPROPERTYACTIVITYTYPE].[CREATEID],
			[dbo].[RPPROPERTYACTIVITYTYPE].[PREFIXID],
			[dbo].[RPPROPERTYACTIVITYTYPE].[ALLOWDUPLICATE],
			[dbo].[RPPROPERTYACTIVITYTYPE].[CUSTOMFIELDLAYOUTID],
			[dbo].[RPPROPERTYACTIVITYTYPE].[LASTCHANGEDBY],
			[dbo].[RPPROPERTYACTIVITYTYPE].[LASTCHANGEDON],
			[dbo].[RPPROPERTYACTIVITYTYPE].[ROWVERSION]
FROM		[dbo].[RPPROPERTYACTIVITYTYPE]
ORDER BY	[dbo].[RPPROPERTYACTIVITYTYPE].[NAME]
END