CREATE PROCEDURE [businessmanagementsetup].[USP_BLEINFORMAT_SELECT_LOOKUP]
AS
	SET NOCOUNT ON;
	
	SELECT 
		[dbo].[BLEINFORMAT].[BLEINFORMATID],
		[NAME] = CASE [dbo].[BLEINFORMAT].[BLEINFORMATID] WHEN 0 THEN '' ELSE [dbo].[BLEINFORMAT].[NAME] END
	FROM [dbo].[BLEINFORMAT]
	ORDER BY CASE [dbo].[BLEINFORMAT].[BLEINFORMATID] WHEN 0 THEN '' ELSE [dbo].[BLEINFORMAT].[NAME] END ASC