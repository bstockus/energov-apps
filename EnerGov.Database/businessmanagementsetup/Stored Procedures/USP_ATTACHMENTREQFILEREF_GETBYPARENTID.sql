CREATE PROCEDURE [businessmanagementsetup].[USP_ATTACHMENTREQFILEREF_GETBYPARENTID]
(
@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[ATTACHMENTREQFILEREF].[ATTACHMENTREQFILEREFID],
	[dbo].[ATTACHMENTREQFILEREF].[NAME],
	[dbo].[ATTACHMENTREQFILEREF].[ISREQUIRED],
	[dbo].[ATTACHMENTREQFILEREF].[QUANTITY],
	[dbo].[ATTACHMENTREQFILEREF].[ATTACHMENTGROUPID],
	[dbo].[ATTACHMENTREQFILEREF].[OBJECTCLASSID],
	[dbo].[ATTACHMENTREQFILEREF].[OBJECTTYPEID]
FROM [dbo].[ATTACHMENTREQFILEREF]
WHERE [dbo].[ATTACHMENTREQFILEREF].[OBJECTTYPEID] = @PARENTID
END