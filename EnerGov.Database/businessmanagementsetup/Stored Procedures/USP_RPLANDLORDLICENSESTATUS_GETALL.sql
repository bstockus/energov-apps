CREATE PROCEDURE [businessmanagementsetup].[USP_RPLANDLORDLICENSESTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[RPLANDLORDLICENSESTATUS].[RPLANDLORDLICENSESTATUSID],
	[dbo].[RPLANDLORDLICENSESTATUS].[NAME],
	[dbo].[RPLANDLORDLICENSESTATUS].[DESCRIPTION],
	[dbo].[RPLANDLORDLICENSESTATUS].[RPLANDLORDLICENSESYSSTATUSID],
	[dbo].[RPLANDLORDLICENSESTATUS].[LASTCHANGEDBY],
	[dbo].[RPLANDLORDLICENSESTATUS].[LASTCHANGEDON],
	[dbo].[RPLANDLORDLICENSESTATUS].[ROWVERSION]
FROM [dbo].[RPLANDLORDLICENSESTATUS]
ORDER BY [dbo].[RPLANDLORDLICENSESTATUS].[NAME] ASC

END