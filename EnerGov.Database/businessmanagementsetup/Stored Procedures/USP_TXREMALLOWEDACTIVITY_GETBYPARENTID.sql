CREATE PROCEDURE [businessmanagementsetup].[USP_TXREMALLOWEDACTIVITY_GETBYPARENTID]
(
@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[TXREMALLOWEDACTIVITY].[TXREMALLOWEDACTIVITYID],
	[dbo].[TXREMALLOWEDACTIVITY].[BLLICENSEACTIVITYTYPEID],
	[dbo].[TXREMALLOWEDACTIVITY].[TXREMITTANCETYPEID],
	[dbo].[BLLICENSEACTIVITYTYPE].[NAME]
FROM [dbo].[TXREMALLOWEDACTIVITY]
JOIN [dbo].[BLLICENSEACTIVITYTYPE] WITH (NOLOCK) ON [BLLICENSEACTIVITYTYPE].[BLLICENSEACTIVITYTYPEID] = [TXREMALLOWEDACTIVITY].[BLLICENSEACTIVITYTYPEID]
WHERE [dbo].[TXREMALLOWEDACTIVITY].[TXREMITTANCETYPEID] = @PARENTID
ORDER BY [dbo].[BLLICENSEACTIVITYTYPE].[NAME]
END