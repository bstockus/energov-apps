CREATE PROCEDURE [businessmanagementsetup].[USP_TXRMTTYPERPTPERIOD_GETBYPARENTID]
(
@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[TXRMTTYPERPTPERIOD].[TXRMTTYPERPTPERIODID],
	[dbo].[TXRMTTYPERPTPERIOD].[TXRPTPERIODID],
	[dbo].[TXRMTTYPERPTPERIOD].[TXREMITTANCETYPEID],
	[dbo].[TXRMTTYPERPTPERIOD].[DEFAULTPERIOD],
	[dbo].[TXRPTPERIOD].[NAME],
	[dbo].[TXRPTPERIOD].[DESCRIPTION]
FROM [dbo].[TXRMTTYPERPTPERIOD]
JOIN [dbo].[TXRPTPERIOD] WITH (NOLOCK) ON [TXRPTPERIOD].[TXRPTPERIODID] = [TXRMTTYPERPTPERIOD].[TXRPTPERIODID]
WHERE [dbo].[TXRMTTYPERPTPERIOD].[TXREMITTANCETYPEID] = @PARENTID
ORDER BY [dbo].[TXRPTPERIOD].[NAME]
END