CREATE PROCEDURE [businessmanagementsetup].[USP_ILLICENSEACTIVITYTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[ILLICENSEACTIVITYTYPE].[ILLICENSEACTIVITYTYPEID],
	[dbo].[ILLICENSEACTIVITYTYPE].[NAME],
	[dbo].[ILLICENSEACTIVITYTYPE].[DESCRIPTION],
	[dbo].[ILLICENSEACTIVITYTYPE].[PREFIX],
	[dbo].[ILLICENSEACTIVITYTYPE].[CUSTOMFIELDLAYOUTID],
	[dbo].[ILLICENSEACTIVITYTYPE].[CANEDIT],
	[dbo].[ILLICENSEACTIVITYTYPE].[CREATEID],
	[dbo].[ILLICENSEACTIVITYTYPE].[ALLOWDUPLICATE],
	[dbo].[ILLICENSEACTIVITYTYPE].[LASTCHANGEDBY],
	[dbo].[ILLICENSEACTIVITYTYPE].[LASTCHANGEDON],
	[dbo].[ILLICENSEACTIVITYTYPE].[ROWVERSION]
FROM [dbo].[ILLICENSEACTIVITYTYPE] ORDER BY [dbo].[ILLICENSEACTIVITYTYPE].[NAME] ASC
END