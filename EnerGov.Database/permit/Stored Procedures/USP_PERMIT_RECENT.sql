CREATE PROCEDURE [permit].[USP_PERMIT_RECENT]
(
  @USER_ID AS CHAR(36)
)
AS
BEGIN
	DECLARE @RECENTPERMITLIST AS RecordIDs
	INSERT INTO @RECENTPERMITLIST (RECORDID)

	SELECT TOP 15 RECENTHISTORYPERMIT.PERMITID 
	FROM RECENTHISTORYPERMIT
	WHERE RECENTHISTORYPERMIT.USERID = @USER_ID
	ORDER BY RECENTHISTORYPERMIT.LOGGEDDATETIME DESC

	EXEC [permit].[USP_PMPERMIT_GETBYIDS] @RECENTPERMITLIST, @USER_ID 
END