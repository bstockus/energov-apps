CREATE PROCEDURE [permit].[USP_PMPERMITRENEWALCASE_RECENT]
(
  @USER_ID AS CHAR(36)
)
AS
BEGIN
	DECLARE @RECENTPERMITRENEWALCASEIDLIST AS RecordIDs
	INSERT INTO @RECENTPERMITRENEWALCASEIDLIST (RECORDID)

	SELECT TOP 15 RECENTHISTORYPERMITRENEWALCASE.PERMITRENEWALCASEID 
	FROM RECENTHISTORYPERMITRENEWALCASE
	WHERE RECENTHISTORYPERMITRENEWALCASE.USERID = @USER_ID
	ORDER BY RECENTHISTORYPERMITRENEWALCASE.LOGGEDDATETIME DESC

	EXEC [permit].[USP_PMPERMITRENEWALCASE_GETBYIDS] @RECENTPERMITRENEWALCASEIDLIST, @USER_ID 
END