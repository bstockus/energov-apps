CREATE PROCEDURE [globalsearch].[USP_PROPERTY_SEARCH_SELECT_LOOKUP]
(
    @SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@SORT_FIELD AS NVARCHAR(50)='',
	@IS_ASCENDING AS BIT =0
)
AS
BEGIN
SET NOCOUNT ON;
;WITH RAW_DATA
    AS(            
	SELECT 
		[RPPROPERTY].[RPPROPERTYID],
		[RPPROPERTY].[PROPERTYNUMBER],
		[GLOBALENTITY].[GLOBALENTITYNAME],
		[GLOBALENTITY].[FIRSTNAME],
		[GLOBALENTITY].[LASTNAME],
		[RPPROPERTYTYPE].[NAME] [TYPENAME],
		[RPPROPERTYSTATUS].[NAME] [STATUSNAME],
		(CASE WHEN @IS_ASCENDING =1
			 THEN 
				CASE 
					WHEN @SORT_FIELD = 'propertyNumber' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTY].[PROPERTYNUMBER] ASC)
					WHEN  @SORT_FIELD= 'companyName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[GLOBALENTITY].[GLOBALENTITYNAME] ASC)
					WHEN  @SORT_FIELD= 'firstName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[GLOBALENTITY].[FIRSTNAME] ASC)
					WHEN  @SORT_FIELD= 'lastName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[GLOBALENTITY].[LASTNAME] ASC)
					WHEN  @SORT_FIELD= 'typeName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTYTYPE].[NAME] ASC)
					WHEN  @SORT_FIELD= 'statusName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTYSTATUS].[NAME] ASC)
					ELSE ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTY].[PROPERTYNUMBER]  ASC) 
				END
			 WHEN @IS_ASCENDING=0
			 THEN 
				CASE 
					WHEN @SORT_FIELD= 'propertyNumber' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTY].[PROPERTYNUMBER] DESC)
					WHEN  @SORT_FIELD = 'companyName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[GLOBALENTITY].[GLOBALENTITYNAME] DESC)
					WHEN  @SORT_FIELD = 'firstName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[GLOBALENTITY].[FIRSTNAME] DESC)
					WHEN  @SORT_FIELD = 'lastName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[GLOBALENTITY].[LASTNAME] DESC)
					WHEN  @SORT_FIELD = 'typeName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTYTYPE].[NAME] DESC)
					WHEN  @SORT_FIELD = 'statusName' THEN ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTYSTATUS].[NAME] DESC)
					ELSE ROW_NUMBER() OVER(ORDER BY [dbo].[RPPROPERTY].[PROPERTYNUMBER]  DESC) 
				END
			 END) AS RowNumber,
			 COUNT(1) OVER() AS TotalRows
	FROM 
		[dbo].[RPPROPERTY]
		INNER JOIN [dbo].[RPLANDLORDLICENSE] 
			ON  [RPPROPERTY].[RPLANDLORDLICENSEID] = [RPLANDLORDLICENSE].[RPLANDLORDLICENSEID]
		INNER JOIN [dbo].[GLOBALENTITY]
			ON [RPLANDLORDLICENSE].[GLOBALENTITYID] = [GLOBALENTITY].[GLOBALENTITYID]
		INNER JOIN [dbo].[RPPROPERTYTYPE] WITH (NOLOCK) 
			ON [RPPROPERTY].[RPPROPERTYTYPEID] = [RPPROPERTYTYPE].[RPPROPERTYTYPEID]
		INNER JOIN [dbo].[RPPROPERTYSTATUS] WITH (NOLOCK) 
			ON [RPPROPERTY].[RPPROPERTYSTATUSID] = [RPPROPERTYSTATUS].[RPPROPERTYSTATUSID]
	WHERE	[RPPROPERTY].[PROPERTYNUMBER] LIKE '%'+@SEARCH+'%' 
		OR	[GLOBALENTITY].[GLOBALENTITYNAME] LIKE '%'+@SEARCH+'%' 
		OR  [GLOBALENTITY].[FIRSTNAME] LIKE '%'+@SEARCH+'%' 
		OR  [GLOBALENTITY].[LASTNAME] LIKE '%'+@SEARCH+'%' 
		OR 	[RPPROPERTYTYPE].[NAME] LIKE '%'+@SEARCH+'%' 
		OR  [RPPROPERTYSTATUS].[NAME] LIKE '%'+@SEARCH+'%' 
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber
END