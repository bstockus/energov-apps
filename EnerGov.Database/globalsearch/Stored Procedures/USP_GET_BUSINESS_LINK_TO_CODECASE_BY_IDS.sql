CREATE PROCEDURE [globalsearch].[USP_GET_BUSINESS_LINK_TO_CODECASE_BY_IDS]
	@IDS RECORDIDS READONLY-- BUSINESSIDS
AS
BEGIN
	SELECT DISTINCT RECORDID,
	(CASE WHEN CMCODECASEID IS NULL THEN CAST( 0 AS BIT) ELSE CAST(1 AS BIT) END) AS ISLINKEDTOCODECASE FROM @IDS 
	LEFT JOIN BLGLOBALENTITYEXTENSIONCODECASEXREF ON RECORDID = BLGLOBALENTITYEXTENSIONID
END