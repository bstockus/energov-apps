CREATE PROCEDURE [globalsearch].[USP_GET_CODECASE_LINK_TO_BUSINESS_BY_IDS]
	@IDS RECORDIDS READONLY-- CODECADEIDS
AS
BEGIN
	SELECT DISTINCT RECORDID,
	(CASE WHEN BLGLOBALENTITYEXTENSIONID IS NULL THEN CAST( 0 AS BIT) ELSE CAST(1 AS BIT) END) AS ISLINKEDTOBUSINESS FROM @IDS 
	LEFT JOIN BLGLOBALENTITYEXTENSIONCODECASEXREF ON RECORDID = CMCODECASEID
END