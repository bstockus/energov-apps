CREATE PROCEDURE [examsitting].[USP_EXAMSITTING_RECENT]
(
  @USERID AS CHAR(36)
)
AS
BEGIN
	DECLARE @RECENTEXAMSITTINGLIST AS RecordIDs
	INSERT INTO @RECENTEXAMSITTINGLIST (RECORDID)

	SELECT TOP 15 RECENTHISTORYEXAMSITTING.EXAMSITTINGID 
	FROM RECENTHISTORYEXAMSITTING
	WHERE RECENTHISTORYEXAMSITTING.USERID = @USERID
	ORDER BY RECENTHISTORYEXAMSITTING.LOGGEDDATETIME DESC

	EXEC [examsitting].[USP_RECENTEXAMSITTING_GETBYIDS] @RECENTEXAMSITTINGLIST, @USERID 
END