CREATE PROCEDURE [cashieringsetup].[USP_CAFINANCIALINTEGRATIONSETUP_ACTIVE_EXPORT]

AS

SET NOCOUNT ON;

SELECT	TOP 1 [dbo].[CAFINANCIALINTEGRATIONSETUP].[ENABLEEX]
FROM	[dbo].[CAFINANCIALINTEGRATIONSETUP]
WHERE	[dbo].[CAFINANCIALINTEGRATIONSETUP].[ISACTIVE] = 1
		AND [dbo].[CAFINANCIALINTEGRATIONSETUP].[ENABLEEX] = 1