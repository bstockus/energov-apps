CREATE PROCEDURE [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET]
	@ASSIGNEDUSERIDS AS RECORDIDS READONLY,
	@TODAY AS DATE = NULL,
	@NEXTBUSINESSDAYBEGIN AS DATE = NULL,
	@NEXTBUSINESSDAYEND AS DATE = NULL,
	@OVERDUEDATE AS DATE = NULL
AS
BEGIN
	
DECLARE @PLANMODULE AS TINYINT = 1;
DECLARE @PERMITMODULE AS TINYINT = 2;

SET NOCOUNT ON;

EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_TODAY] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS, @TODAY = @TODAY
EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_NEXT_BUSINESS_DAYS] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS, @NEXTBUSINESSDAYBEGIN = @NEXTBUSINESSDAYBEGIN, @NEXTBUSINESSDAYEND = @NEXTBUSINESSDAYEND
EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_OVERDUE] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS, @OVERDUEDATE = @OVERDUEDATE

EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_ENTITY_TYPE] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS
EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_ENTITY_NUMBER] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS
EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_SUBMITTAL_TYPE] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS
EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_PROJECT] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS
EXEC [reviewcoordinator].[USP_ENTITY_SUBMITTAL_FACETS_GET_LOCATION] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS

END