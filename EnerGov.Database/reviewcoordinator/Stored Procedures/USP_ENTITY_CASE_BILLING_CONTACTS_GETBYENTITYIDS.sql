CREATE PROCEDURE [reviewcoordinator].[USP_ENTITY_CASE_BILLING_CONTACTS_GETBYENTITYIDS]
	@ENTITYIDS RECORDIDS READONLY
AS
BEGIN

SET NOCOUNT ON;
SELECT 
	PMPERMITCONTACT.PMPERMITID ENTITYID,
	GLOBALENTITY.FIRSTNAME,
	GLOBALENTITY.LASTNAME
FROM PMPERMITCONTACT
INNER JOIN @ENTITYIDS on [@ENTITYIDS].RECORDID = PMPERMITCONTACT.PMPERMITID
INNER JOIN GLOBALENTITY on GLOBALENTITY.GLOBALENTITYID = PMPERMITCONTACT.GLOBALENTITYID
WHERE PMPERMITCONTACT.ISBILLING = 1
UNION ALL
SELECT 
	PLPLANCONTACT.PLPLANID ENTITYID,
	GLOBALENTITY.FIRSTNAME,
	GLOBALENTITY.LASTNAME
FROM PLPLANCONTACT
INNER JOIN @ENTITYIDS on [@ENTITYIDS].RECORDID = PLPLANCONTACT.PLPLANID
INNER JOIN GLOBALENTITY on GLOBALENTITY.GLOBALENTITYID = PLPLANCONTACT.GLOBALENTITYID
WHERE PLPLANCONTACT.ISBILLING = 1

END