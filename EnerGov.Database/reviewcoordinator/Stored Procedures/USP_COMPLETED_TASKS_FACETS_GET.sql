CREATE PROCEDURE [reviewcoordinator].[USP_COMPLETED_TASKS_FACETS_GET]
	@ASSIGNEDUSERIDS AS RECORDIDS READONLY,
	@ASSIGNEDTEAMIDS AS RECORDIDS READONLY,
	@OWNEDTEAMIDS AS RECORDIDS READONLY,
	@LASTCOMPLETEDDATE AS DATE = NULL
AS
BEGIN

SET NOCOUNT ON;
DECLARE @facetTypeComplete INT = 1;

EXEC [reviewcoordinator].[USP_INBOX_CARD_FACETS_GET_COMPLETED_BY] @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS, @ASSIGNEDTEAMIDS = @ASSIGNEDTEAMIDS, @LASTCOMPLETEDDATE = @LASTCOMPLETEDDATE
EXEC [reviewcoordinator].[USP_INBOX_CARD_FACETS_GET_TASK_TYPE] @FACETTYPE = @facetTypeComplete, @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS, @ASSIGNEDTEAMIDS = @ASSIGNEDTEAMIDS, @LASTCOMPLETEDDATE = @LASTCOMPLETEDDATE
EXEC [reviewcoordinator].[USP_INBOX_CARD_FACETS_GET_ENTITY_TYPE] @FACETTYPE = @facetTypeComplete, @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS, @ASSIGNEDTEAMIDS = @ASSIGNEDTEAMIDS, @LASTCOMPLETEDDATE = @LASTCOMPLETEDDATE
EXEC [reviewcoordinator].[USP_INBOX_CARD_FACETS_GET_ENTITY_NUMBER] @FACETTYPE = @facetTypeComplete, @ASSIGNEDUSERIDS = @ASSIGNEDUSERIDS, @ASSIGNEDTEAMIDS = @ASSIGNEDTEAMIDS, @LASTCOMPLETEDDATE = @LASTCOMPLETEDDATE

END