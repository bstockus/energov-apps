CREATE PROCEDURE [reviewcoordinator].[USP_ERINTEGRATIONQUEUE_GETBYPLSUBMITTALIDS]
(
	@SUBMITTALIDS AS RECORDIDS READONLY
)
AS
BEGIN
SET NOCOUNT ON;

SELECT 
	ERINTEGRATIONQUEUE.ERINTEGRATIONQUEUEID,
	ERINTEGRATIONQUEUE.ERENTITYPROJECTID,
	ERINTEGRATIONQUEUE.ERENTITYSESSIONID,
	ERINTEGRATIONQUEUE.PROCESSEDDATE,
	ERINTEGRATIONQUEUE.SUCCESS,
	ERINTEGRATIONQUEUE.CREATEDATE,
	ERINTEGRATIONQUEUE.USERID,
	ERINTEGRATIONQUEUE.RESULTLOG,
	ERINTEGRATIONQUEUE.SENTTOBUS,
	ERINTEGRATIONQUEUE.PROCESSTYPE,
	ERENTITYSESSION.PLSUBMITTALID
FROM ERINTEGRATIONQUEUE 
INNER JOIN ERENTITYSESSION ON ERENTITYSESSION.ERENTITYSESSIONID = ERINTEGRATIONQUEUE.ERENTITYSESSIONID
WHERE ERENTITYSESSION.PLSUBMITTALID IN (SELECT RECORDID FROM @SUBMITTALIDS)

END