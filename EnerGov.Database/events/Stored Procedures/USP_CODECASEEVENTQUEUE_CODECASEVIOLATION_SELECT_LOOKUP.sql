CREATE PROCEDURE [events].[USP_CODECASEEVENTQUEUE_CODECASEVIOLATION_SELECT_LOOKUP]
(
	@BATCHSIZE AS INT = 1000
)
AS

DECLARE @RESULT_DATA AS TABLE 
(
	[CODECASEEVENTQUEUEID] BIGINT, 
	[CODECASEEVENTTYPEID] INT,
	[CREATEDDATE] DATETIME,
	[CMCODECASEID] CHAR(36),
	[DESCRIPTION] NVARCHAR(80),
	[CASENUMBER] NVARCHAR(50),
	[EMERGENCY] BIT,
	[OPENEDDATE] DATETIME,
	[CLOSEDDATE] DATETIME,
	[CODECASETYPENAME] NVARCHAR(50),
	[CODECASESTATUSNAME] NVARCHAR(50),
	[SUCCESSFLAG] BIT,
	[FAILUREFLAG] BIT,
	[CANCELLEDFLAG] BIT,
	[ADDRESSLINE1] NVARCHAR(200),
	[ADDRESSLINE2] NVARCHAR(200),
	[ADDRESSLINE3] NVARCHAR(200),
	[STREETTYPE] NVARCHAR(50),
	[PREDIRECTION] NVARCHAR(30),
	[POSTDIRECTION] NVARCHAR(30),
	[UNITORSUITE] NVARCHAR(20),
	[CITY] NVARCHAR(50),
	[COUNTRYTYPE] INT,
	[PROVINCE] NVARCHAR(50),
	[STATE] NVARCHAR(50),
	[POSTALCODE] NVARCHAR(50),
	[COUNTRY] NVARCHAR(50),
	[PARCELNUMBER] NVARCHAR(50),
	[TAXNUMBER] NVARCHAR(50),
	[CMVIOLATIONID] CHAR(36),
	[CORRECTIVEACTION] NVARCHAR(MAX),
	[CITATIONISSUEDATE] DATETIME,
	[COMPLIANCEDATE] DATETIME,
	[RESOLVEDDATE] DATETIME,
	[CODENUMBER] NVARCHAR(50),
	[CODEDESCRIPTION] NVARCHAR(MAX),
	[CODETEXT] NVARCHAR(MAX),
	[CODECATEGORYNAME] NVARCHAR(50),
	[VIOLATIONSTATUSNAME] NVARCHAR(50),
	[VIOLATIONPRIORITYNAME] NVARCHAR(50),
	[LASTCHANGEDON] DATETIME,
	[LASTCHANGEDBY] NVARCHAR(70)
);

WITH RAW_DATA AS
(
	SELECT TOP(@BATCHSIZE)
	[CODECASEEVENTQUEUE].[CODECASEEVENTQUEUEID],
	[CODECASEEVENTQUEUE].[CODECASEEVENTTYPEID],
	[CODECASEEVENTQUEUE].[CREATEDDATE],
	[CMCODECASE].[CMCODECASEID],
	SUBSTRING([CMCODECASE].[DESCRIPTION], 1, 80) AS [DESCRIPTION],
	[CMCODECASE].[CASENUMBER],
	[CMCODECASE].[EMERGENCY],
	[CMCODECASE].[OPENEDDATE],
	[CMCODECASE].[CLOSEDDATE],
	[CMCASETYPE].[NAME] AS [CODECASETYPENAME],
	[CMCODECASESTATUS].[NAME] AS [CODECASESTATUSNAME],
	[CMCODECASESTATUS].[SUCCESSFLAG],
	[CMCODECASESTATUS].[FAILUREFLAG],
	[CMCODECASESTATUS].[CANCELLEDFLAG],
	[MAILINGADDRESS].[ADDRESSLINE1],
	[MAILINGADDRESS].[ADDRESSLINE2],
	[MAILINGADDRESS].[ADDRESSLINE3],
	[MAILINGADDRESS].[STREETTYPE],
	[MAILINGADDRESS].[PREDIRECTION],
	[MAILINGADDRESS].[POSTDIRECTION],
	[MAILINGADDRESS].[UNITORSUITE],
	[MAILINGADDRESS].[CITY],
	[MAILINGADDRESS].[COUNTRYTYPE],
	[MAILINGADDRESS].[PROVINCE],
	[MAILINGADDRESS].[STATE],
	[MAILINGADDRESS].[POSTALCODE],
	[MAILINGADDRESS].[COUNTRY],
	[PARCEL].[PARCELNUMBER],
	[PARCEL].[TAXNUMBER],
	[CMVIOLATION].[CMVIOLATIONID],
	[CMVIOLATION].[CORRECTIVEACTION],
	[CMVIOLATION].[CITATIONISSUEDATE],
	[CMVIOLATION].[COMPLIANCEDATE],
	[CMVIOLATION].[RESOLVEDDATE],
	[CMCODE].[CODENUMBER],
	[CMCODE].[DESCRIPTION] AS [CODEDESCRIPTION],
	[CMCODEREVISION].[CODETEXT],
	[CMCODECATEGORY].[NAME] AS [CODECATEGORYNAME],
	[CMVIOLATIONSTATUS].[NAME] AS [VIOLATIONSTATUSNAME],
	[CMCODEVIOLATIONPRIORITY].[NAME] AS [VIOLATIONPRIORITYNAME],
	[CMCODECASE].[LASTCHANGEDON],
	LTRIM(RTRIM(CONCAT([dbo].[USERS].[FNAME], ' ', [dbo].[USERS].[LNAME]))) AS [LASTCHANGEDBY]

	FROM [dbo].[CODECASEEVENTQUEUE]
	INNER JOIN [dbo].[CMVIOLATION] WITH(NOLOCK) ON [dbo].[CMVIOLATION].[CMVIOLATIONID] = [dbo].[CODECASEEVENTQUEUE].[CMVIOLATIONID]
	INNER JOIN [dbo].[CMCODE] WITH(NOLOCK) ON [dbo].[CMCODE].[CMCODEID] = [dbo].[CMVIOLATION].[CMCODEID]
	INNER JOIN [dbo].[CMCODEREVISION] WITH(NOLOCK) ON [dbo].[CMCODEREVISION].[CMCODEREVISIONID] = [dbo].[CMVIOLATION].[CMCODEREVISIONID]
	INNER JOIN [dbo].[CMVIOLATIONSTATUS] WITH(NOLOCK) ON [dbo].[CMVIOLATIONSTATUS].[CMVIOLATIONSTATUSID] = [dbo].[CMVIOLATION].[CMVIOLATIONSTATUSID]
	LEFT JOIN [dbo].[CMCODECATEGORY] WITH(NOLOCK) ON [dbo].[CMCODECATEGORY].[CMCODECATEGORYID] = [dbo].[CMVIOLATION].[CMCODECATEGORYID]
	LEFT JOIN [dbo].[CMCODEVIOLATIONPRIORITY] WITH(NOLOCK) ON [dbo].[CMCODEVIOLATIONPRIORITY].[CMCODEVIOLATIONPRIORITYID] = [dbo].[CMVIOLATION].[CMCODEVIOLATIONPRIORITYID]
	INNER JOIN [dbo].[CMCODECASE] WITH(NOLOCK) ON [dbo].[CMCODECASE].[CMCODECASEID] = [dbo].[CODECASEEVENTQUEUE].[CMCODECASEID]
	INNER JOIN [dbo].[CMCODECASESTATUS] WITH(NOLOCK) ON [dbo].[CMCODECASESTATUS].[CMCODECASESTATUSID] = [dbo].[CMCODECASE].[CMCODECASESTATUSID]
	LEFT JOIN [dbo].[CMCASETYPE] WITH(NOLOCK) ON [dbo].[CMCASETYPE].[CMCASETYPEID] = [dbo].[CMCODECASE].[CMCASETYPEID]
	LEFT JOIN [dbo].[CMCODECASEADDRESS] WITH(NOLOCK) ON [dbo].[CMCODECASEADDRESS].[CMCODECASEID] = [dbo].[CMCODECASE].[CMCODECASEID] AND [dbo].[CMCODECASEADDRESS].[MAIN] = 1
	LEFT JOIN [dbo].[MAILINGADDRESS] WITH(NOLOCK) ON [dbo].[MAILINGADDRESS].[MAILINGADDRESSID] = [dbo].[CMCODECASEADDRESS].[MAILINGADDRESSID]
	LEFT JOIN [dbo].[CMCODECASEPARCEL] WITH(NOLOCK) ON [dbo].[CMCODECASEPARCEL].[CMCODECASEID] = [dbo].[CMCODECASE].[CMCODECASEID]
	LEFT JOIN [dbo].[PARCEL] WITH(NOLOCK) ON [dbo].[PARCEL].[PARCELID] = [dbo].[CMCODECASEPARCEL].[PARCELID] AND [dbo].[CMCODECASEPARCEL].[PRIMARY] = 1
	LEFT JOIN [dbo].[USERS] WITH(NOLOCK) ON [dbo].[USERS].[SUSERGUID] = [dbo].[CMCODECASE].[LASTCHANGEDBY]
	WHERE [dbo].[CODECASEEVENTQUEUE].[PROCESSEDDATE] IS NULL AND [dbo].[CODECASEEVENTQUEUE].[CMVIOLATIONID] IS NOT NULL 
	ORDER BY [dbo].[CODECASEEVENTQUEUE].[CREATEDDATE]
)
INSERT INTO @RESULT_DATA
SELECT * FROM RAW_DATA

SELECT * FROM @RESULT_DATA

DECLARE @CMCODECASEIDs RecordIDs
INSERT INTO @CMCODECASEIDs(RECORDID)
SELECT DISTINCT [CMCODECASEID] 
FROM @RESULT_DATA

EXEC [events].[USP_EVENTQUEUE_GET_CODECASECONTACT] @UNIQUEIDS = @CMCODECASEIDs