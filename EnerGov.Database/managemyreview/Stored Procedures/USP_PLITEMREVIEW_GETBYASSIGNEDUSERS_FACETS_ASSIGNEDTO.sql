CREATE PROCEDURE [managemyreview].[USP_PLITEMREVIEW_GETBYASSIGNEDUSERS_FACETS_ASSIGNEDTO]
(
	@ITEMREVIEWLIST RecordIDs READONLY
)
AS
BEGIN
SET NOCOUNT ON;

SELECT		ASSIGNEDUSERID AS ID
			, USERS.FNAME AS ASSIGNEDUSERFIRSTNAME
			, USERS.LNAME AS ASSIGNEDUSERLASTNAME
			, COUNT(*) COUNT
FROM		PLITEMREVIEW
INNER JOIN	@ITEMREVIEWLIST ON PLITEMREVIEW.PLITEMREVIEWID = RECORDID
INNER JOIN	USERS ON USERS.SUSERGUID = PLITEMREVIEW.ASSIGNEDUSERID
GROUP BY ASSIGNEDUSERID, USERS.FNAME, USERS.LNAME
ORDER BY COUNT(*) DESC, ASSIGNEDUSERFIRSTNAME, ASSIGNEDUSERLASTNAME

END