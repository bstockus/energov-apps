CREATE PROCEDURE [systemsetup].[USP_CAINTEGRATIONBATCHTYPE_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT		[dbo].[CAINTEGRATIONBATCHTYPE].[CAINTEGRATIONBATCHTYPEID],
				[dbo].[CAINTEGRATIONBATCHTYPE].[NAME]
	FROM		[dbo].[CAINTEGRATIONBATCHTYPE]
	ORDER BY	[dbo].[CAINTEGRATIONBATCHTYPE].[NAME]
END