CREATE PROCEDURE [systemsetup].[USP_WORKFLOWCONDITION_GETBYPARENTID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[WORKFLOWCONDITION].[WORKFLOWCONDITIONID],
	[dbo].[WORKFLOWCONDITION].[WORKFLOWCONDITIONGROUPID],
	[dbo].[WORKFLOWCONDITION].[CLASSNAME],
	[dbo].[WORKFLOWCONDITION].[FRIENDLYCLASSNAME],
	[dbo].[WORKFLOWCONDITION].[PROPERTYNAME],
	[dbo].[WORKFLOWCONDITION].[FRIENDLYPROPERTYNAME],
	[dbo].[WORKFLOWCONDITION].[WORKFLOWCLASSCONDITIONTYPEID],
	[dbo].[WORKFLOWCONDITION].[WORKFLOWPROPERTYCONDTYPEID],
	[dbo].[WORKFLOWCONDITION].[PROPERTYCONDITIONCOMPAREVALUE],
	[dbo].[WORKFLOWCONDITION].[CONDITIONNUMBER],
	[dbo].[WORKFLOWCONDITION].[WORKFLOWCONTROLTYPEID],
	[dbo].[WORKFLOWCONDITION].[CUSTOMFIELDNAME],
	[dbo].[WORKFLOWCONDITION].[CUSTOMFIELDTABLE]
FROM [dbo].[WORKFLOWCONDITION]
JOIN [dbo].[WORKFLOWCONDITIONGROUP]  ON [dbo].[WORKFLOWCONDITION].[WORKFLOWCONDITIONGROUPID] = [dbo].[WORKFLOWCONDITIONGROUP].[WORKFLOWCONDITIONGROUPID]
WHERE [dbo].[WORKFLOWCONDITIONGROUP].[WORKFLOWID] = @WORKFLOWID
ORDER BY [WORKFLOWCONDITION].[WORKFLOWCONDITIONGROUPID] , [WORKFLOWCONDITION].[CONDITIONNUMBER]

END