CREATE PROCEDURE [systemsetup].[USP_ROLES_GETBYID]
(
	@ROLEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[ROLES].[SROLEGUID],
	[dbo].[ROLES].[ID],
	[dbo].[ROLES].[SDESCRIPTION],
	[dbo].[ROLES].[LASTCHANGEDBY],
	[dbo].[ROLES].[LASTCHANGEDON],
	[dbo].[ROLES].[ROWVERSION]
FROM [dbo].[ROLES]
WHERE
	[dbo].[ROLES].[SROLEGUID] = @ROLEID
	
	EXEC [systemsetup].[USP_ROLESROLESSETTING_GETBYUSERROLEID] @ROLEID
	EXEC [systemsetup].[USP_ROLEBILLINGRATEXREF_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_ATTACHMENTGROUPROLEXREF_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_ROLEPAYMENTMETHODXREF_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_COHIDDENCONTACTTYPE_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_ROLESCUSTOMFIELDS_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_ROLEMENUXREF_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_ROLESUBMENUXREF_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_ROLEFORMSXREF_GETBYPARENTID] @ROLEID
	EXEC [systemsetup].[USP_ROLERECORDTYPEXREF_GETBYUSERROLEID] @ROLEID
END