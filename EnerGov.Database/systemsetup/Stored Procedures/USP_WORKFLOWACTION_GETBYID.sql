CREATE PROCEDURE [systemsetup].[USP_WORKFLOWACTION_GETBYID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[WORKFLOWACTION].[WORKFLOWACTIONID],
	[dbo].[WORKFLOWACTION].[WORKFLOWID],
	[dbo].[WORKFLOWACTION].[WORKFLOWACTIONTYPEID],
	[dbo].[WORKFLOWACTION].[ACTIONNAME],
	[dbo].[WORKFLOWACTION].[CUSTOMMESSAGE],
	[dbo].[WORKFLOWACTION].[ACTIONCLASSNAME],
	[dbo].[WORKFLOWACTION].[RUNONLYWHENDIRTY],
	[dbo].[WORKFLOWACTION].[DISABLEQUEUING],
	[dbo].[WORKFLOWACTION].[POSTWORKFLOW],
	0 AS [PRIORITY],
	CONVERT(bit, 0) AS ISQUERYSETUP
FROM [dbo].[WORKFLOWACTION]
WHERE
	[dbo].[WORKFLOWACTION].[WORKFLOWID] = @WORKFLOWID 
END