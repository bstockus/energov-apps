CREATE PROCEDURE [systemsetup].[USP_QUERYACTION_MAILMERGETEMPLATEUSER_GETBYPARENTID]
(
	@QUERYID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
	[dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEUSERID],
	[dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEID],
	[dbo].[USERS].[SUSERGUID],
	[dbo].[USERS].[FNAME],
	[dbo].[USERS].[LNAME],
	[dbo].[USERS].[ID],
	[dbo].[USERS].[EMAIL],
	[dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEUSERTYPEID]
	FROM [dbo].[QUERYACTION]
	INNER JOIN [dbo].[QUERYACTIONMAILMERGETMPL] on [dbo].[QUERYACTION].[QUERYACTIONID] = [dbo].[QUERYACTIONMAILMERGETMPL].[QUERYACTIONID]	
	INNER JOIN [dbo].[MAILMERGETEMPLATEUSER] on [dbo].[QUERYACTIONMAILMERGETMPL].[MAILMERGETEMPLATEID] = [dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEID]
	INNER JOIN [dbo].[USERS] on [dbo].[MAILMERGETEMPLATEUSER].[USERID] = [dbo].[USERS].[SUSERGUID]
	WHERE [dbo].[QUERYACTION].[QUERYID] = @QUERYID

END