CREATE PROCEDURE [systemsetup].[USP_MAILMERGETEMPLATEUSER_GETBYPARENTID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
	[dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEUSERID],
	[dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEID],
	[dbo].[USERS].[SUSERGUID],
	[dbo].[USERS].[FNAME],
	[dbo].[USERS].[LNAME],
	[dbo].[USERS].[ID],
	[dbo].[USERS].[EMAIL],
	[dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEUSERTYPEID]
	FROM [dbo].[WORKFLOWACTION]
	INNER JOIN [dbo].[WORKFLOWACTIONMAILMERGETMPL] on [dbo].[WORKFLOWACTION].[WORKFLOWACTIONID] = [dbo].[WORKFLOWACTIONMAILMERGETMPL].[WORKFLOWACTIONID]	
	INNER JOIN [dbo].[MAILMERGETEMPLATEUSER] on [dbo].[WORKFLOWACTIONMAILMERGETMPL].[MAILMERGETEMPLATEID] = [dbo].[MAILMERGETEMPLATEUSER].[MAILMERGETEMPLATEID]
	INNER JOIN [dbo].[USERS] on [dbo].[MAILMERGETEMPLATEUSER].[USERID] = [dbo].[USERS].[SUSERGUID]
	WHERE [dbo].[WORKFLOWACTION].[WORKFLOWID] = @WORKFLOWID

END