CREATE PROCEDURE [systemsetup].[USP_QUERYACTIONFTP_GETBYPARENTID]
(
	@QUERYID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[QUERYACTIONFTP].[QUERYACTIONFTPID],
	[dbo].[QUERYACTIONFTP].[QUERYACTIONID],
	[dbo].[QUERYACTIONFTP].[FTPSETUPID],
	[dbo].[QUERYACTIONFTP].[SUBPATH],
	[dbo].[QUERYACTIONFTP].[ATTACHMENTGROUPID],
	[dbo].[QUERYACTIONFTP].[FTPFILENAMEOPTIONID]
FROM [dbo].[QUERYACTIONFTP]
INNER JOIN [dbo].[QUERYACTION] ON [dbo].[QUERYACTION].[QUERYACTIONID] = [dbo].[QUERYACTIONFTP].[QUERYACTIONID]
WHERE
	[dbo].[QUERYACTION].[QUERYID] = @QUERYID
END