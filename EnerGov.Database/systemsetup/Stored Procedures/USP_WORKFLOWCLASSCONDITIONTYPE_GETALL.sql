CREATE PROCEDURE [systemsetup].[USP_WORKFLOWCLASSCONDITIONTYPE_GETALL]
AS
BEGIN
	SELECT 
		[dbo].[WORKFLOWCLASSCONDITIONTYPE].WORKFLOWCLASSCONDITIONTYPEID,
		[dbo].[WORKFLOWCLASSCONDITIONTYPE].CLASSCONDITIONTYPENAME
	FROM [dbo].[WORKFLOWCLASSCONDITIONTYPE]
	ORDER BY [dbo].[WORKFLOWCLASSCONDITIONTYPE].WORKFLOWCLASSCONDITIONTYPEID
END