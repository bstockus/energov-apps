CREATE PROCEDURE [systemsetup].[USP_COMAPPEDVALUECERTTYPE_GETALL]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT	[dbo].[COMAPPEDVALUECERTTYPE].[COMAPPEDVALUECERTTYPEID],
			[dbo].[COLICENSECERTIFICATIONTYPE].[NAME],
			[dbo].[COMAPPEDVALUECERTTYPE].[MAPPEDTYPE],
			[dbo].[COMAPPEDVALUECERTTYPE].[MAPPEDCATEGORY],
			[dbo].[COMAPPEDVALUECERTTYPE].[COSIMPLELICCERTTYPEID],
			[dbo].[COMAPPEDVALUECERTTYPE].[LASTCHANGEDBY],
			[dbo].[COMAPPEDVALUECERTTYPE].[LASTCHANGEDON],
			[dbo].[COMAPPEDVALUECERTTYPE].[ROWVERSION]
	FROM [dbo].[COMAPPEDVALUECERTTYPE]
	LEFT JOIN [dbo].[COLICENSECERTIFICATIONTYPE] ON [dbo].[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID] = [dbo].[COMAPPEDVALUECERTTYPE].[COSIMPLELICCERTTYPEID]
	ORDER BY [dbo].[COLICENSECERTIFICATIONTYPE].[NAME]
END