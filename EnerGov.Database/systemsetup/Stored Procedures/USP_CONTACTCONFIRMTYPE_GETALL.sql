CREATE PROCEDURE [systemsetup].[USP_CONTACTCONFIRMTYPE_GETALL]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT		[dbo].[CONTACTCONFIRMTYPE].[CONTACTCONFIRMTYPEID],
				[dbo].[CONTACTCONFIRMTYPE].[LANDMANAGEMENTCONTACTTYPEID],
				[dbo].[CONTACTCONFIRMTYPE].[MODULETYPE],
				[dbo].[CONTACTCONFIRMTYPE].[ISREQUIRE],
				[dbo].[CONTACTCONFIRMTYPE].[HASEDITRIGHT],
				[dbo].[CONTACTCONFIRMTYPE].[CANCREATESUBRECORD],
				[dbo].[CONTACTCONFIRMTYPE].[LASTCHANGEDBY],
				[dbo].[CONTACTCONFIRMTYPE].[LASTCHANGEDON],
				[dbo].[CONTACTCONFIRMTYPE].[ROWVERSION],
				[dbo].[LANDMANAGEMENTCONTACTTYPE].[NAME]
	FROM		[dbo].[CONTACTCONFIRMTYPE]
	JOIN		[dbo].[LANDMANAGEMENTCONTACTTYPE] ON [dbo].[LANDMANAGEMENTCONTACTTYPE].[LANDMANAGEMENTCONTACTTYPEID] = [dbo].[CONTACTCONFIRMTYPE].[LANDMANAGEMENTCONTACTTYPEID]
	ORDER BY	[dbo].[LANDMANAGEMENTCONTACTTYPE].[NAME]
END