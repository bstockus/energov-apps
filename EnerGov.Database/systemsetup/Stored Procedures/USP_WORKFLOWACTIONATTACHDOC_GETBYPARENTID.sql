CREATE PROCEDURE [systemsetup].[USP_WORKFLOWACTIONATTACHDOC_GETBYPARENTID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[WORKFLOWACTIONATTACHDOC].[WORKFLOWACTIONATTACHDOCID],
	[dbo].[WORKFLOWACTIONATTACHDOC].[WORKFLOWACTIONID],
	[dbo].[WORKFLOWACTIONATTACHDOC].[ATTACHMENTGROUPID],
	[dbo].[WORKFLOWACTIONATTACHDOC].[NOTES],
	[dbo].[WORKFLOWACTIONATTACHDOC].[RPTREPORTID]
FROM [dbo].[WORKFLOWACTIONATTACHDOC]
JOIN [dbo].[WORKFLOWACTION] ON [dbo].[WORKFLOWACTIONATTACHDOC].[WORKFLOWACTIONID] = [dbo].[WORKFLOWACTION].[WORKFLOWACTIONID]
WHERE [dbo].[WORKFLOWACTION].[WORKFLOWID] = @WORKFLOWID

END