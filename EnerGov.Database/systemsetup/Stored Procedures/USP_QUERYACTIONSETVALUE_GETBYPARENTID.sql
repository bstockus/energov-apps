CREATE PROCEDURE [systemsetup].[USP_QUERYACTIONSETVALUE_GETBYPARENTID]
(
	@QUERYID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[QUERYACTIONSETVALUE].[QUERYACTIONSETVALUEID],
	[dbo].[QUERYACTIONSETVALUE].[QUERYACTIONID],
	[dbo].[QUERYACTIONSETVALUE].[SETVALUEFIELD],
	[dbo].[QUERYACTIONSETVALUE].[SETVALUEFIELDFRIENDLYNAME],
	[dbo].[QUERYACTIONSETVALUE].[SETVALUEFIELDVALUE],
	[dbo].[QUERYACTIONSETVALUE].[SETVALUEFIELDADJUSTMENTVALUE],
	[dbo].[QUERYACTIONSETVALUE].[SETVALUEWEEKDAYSONLY],
	[dbo].[QUERYACTIONSETVALUE].[SETVALUEUSEPREVIOUSWEEKDAY]
FROM [dbo].[QUERYACTIONSETVALUE]
INNER JOIN [dbo].[QUERYACTION] ON [dbo].[QUERYACTION].[QUERYACTIONID] = [dbo].[QUERYACTIONSETVALUE].[QUERYACTIONID]
WHERE
	[dbo].[QUERYACTION].[QUERYID] = @QUERYID
END