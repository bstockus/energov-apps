CREATE PROCEDURE [systemsetup].[USP_WINDOWSSERVICETASK_GETALL]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT		[dbo].[WINDOWSSERVICETASK].[WINDOWSSERVICETASKID],
				[dbo].[WINDOWSSERVICETASK].[NAME],
				[dbo].[WINDOWSSERVICETASK].[DESCRIPTION],
				[dbo].[WINDOWSSERVICETASK].[THROTTLE],
				[dbo].[WINDOWSSERVICETASK].[ISENABLED],
				[dbo].[WINDOWSSERVICETASK].[JOBSCHEDULEID],
				[dbo].[WINDOWSSERVICETASK].[ISREADY],
				[dbo].[WINDOWSSERVICETASK].[RETRY],
				[dbo].[WINDOWSSERVICETASK].[LASTCHANGEDBY],
				[dbo].[WINDOWSSERVICETASK].[LASTCHANGEDON],
				[dbo].[WINDOWSSERVICETASK].[ROWVERSION]
	FROM  [dbo].[WINDOWSSERVICETASK]
	WHERE [dbo].[WINDOWSSERVICETASK].[ISREADY] = 1
	ORDER BY	[dbo].[WINDOWSSERVICETASK].[WINDOWSSERVICETASKID]
END