CREATE PROCEDURE [systemsetup].[USP_CAPAYMENTMETHOD_SELECT_LOOKUP_FOR_ACCOUNT_TRANSFER_PAYMENT_TYPE]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		[dbo].[CAPAYMENTMETHOD].[CAPAYMENTMETHODID],
		[dbo].[CAPAYMENTMETHOD].[NAME]
	FROM 
		[dbo].[CAPAYMENTMETHOD]
	WHERE
		[dbo].[CAPAYMENTMETHOD].[ISREFUNDTYPE] = 1 AND [dbo].[CAPAYMENTMETHOD].[CAPAYMENTTYPEID] = 4 -- 'Account Transfer' Payment Type
		AND ([dbo].[CAPAYMENTMETHOD].[FEEWAIVER] = 0 OR [dbo].[CAPAYMENTMETHOD].[FEEWAIVER] IS NULL) 
	ORDER BY
		[dbo].[CAPAYMENTMETHOD].[NAME]
END