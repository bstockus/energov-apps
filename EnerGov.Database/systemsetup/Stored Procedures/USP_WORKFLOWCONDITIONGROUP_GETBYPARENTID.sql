CREATE PROCEDURE [systemsetup].[USP_WORKFLOWCONDITIONGROUP_GETBYPARENTID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[WORKFLOWCONDITIONGROUP].[WORKFLOWCONDITIONGROUPID],
	[dbo].[WORKFLOWCONDITIONGROUP].[WORKFLOWID],
	[dbo].[WORKFLOWCONDITIONGROUP].[ISORBASED],
	[dbo].[WORKFLOWCONDITIONGROUP].[GROUPNUMBER],
	[dbo].[WORKFLOWCONDITIONGROUP].[WORKFLOWACTIONID]
FROM [dbo].[WORKFLOWCONDITIONGROUP]
WHERE [dbo].[WORKFLOWCONDITIONGROUP].[WORKFLOWID] = @WORKFLOWID
ORDER BY [WORKFLOWCONDITIONGROUP].[GROUPNUMBER]
END