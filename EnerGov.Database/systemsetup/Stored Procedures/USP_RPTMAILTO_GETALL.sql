CREATE PROCEDURE [systemsetup].[USP_RPTMAILTO_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[RPTMAILTO].[RPTMAILTOID],
	[dbo].[RPTMAILTO].[RPTAUTOMAILID],
	[dbo].[RPTMAILTO].[FIRSTNAME],
	[dbo].[RPTMAILTO].[LASTNAME],
	[dbo].[RPTMAILTO].[GLOBALENTITYNAME],
	[dbo].[RPTMAILTO].[EMAIL]
FROM [dbo].[RPTMAILTO]
ORDER BY [dbo].[RPTMAILTO].[FIRSTNAME],[dbo].[RPTMAILTO].[LASTNAME] ASC
END