CREATE PROCEDURE [systemsetup].[USP_WORKFLOWACTIONFTP_GETBYPARENTID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[WORKFLOWACTIONFTP].[WORKFLOWACTIONFTPID],
	[dbo].[WORKFLOWACTIONFTP].[WORKFLOWACTIONID],
	[dbo].[WORKFLOWACTIONFTP].[FTPSETUPID],
	[dbo].[WORKFLOWACTIONFTP].[SUBPATH],
	[dbo].[WORKFLOWACTIONFTP].[ATTACHMENTGROUPID],
	[dbo].[WORKFLOWACTIONFTP].[FTPFILENAMEOPTIONID]
FROM [dbo].[WORKFLOWACTIONFTP]
INNER JOIN [dbo].[WORKFLOWACTION] ON [dbo].[WORKFLOWACTION].[WORKFLOWACTIONID] = [dbo].[WORKFLOWACTIONFTP].[WORKFLOWACTIONID]
WHERE
	[dbo].[WORKFLOWACTION].[WORKFLOWID] = @WORKFLOWID
END