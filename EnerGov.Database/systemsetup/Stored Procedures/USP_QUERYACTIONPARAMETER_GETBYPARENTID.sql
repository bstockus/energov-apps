CREATE PROCEDURE [systemsetup].[USP_QUERYACTIONPARAMETER_GETBYPARENTID]
(
	@QUERYID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[QUERYACTIONPARAMETER].[QUERYACTIONPARAMETERID],
	[dbo].[QUERYACTIONPARAMETER].[QUERYACTIONID],
	[dbo].[QUERYACTIONPARAMETER].[PARAMETERNAME],
	[dbo].[QUERYACTIONPARAMETER].[PARAMETERVALUE],
	[dbo].[QUERYACTIONPARAMETER].[PARAMETERTYPE]
FROM [dbo].[QUERYACTIONPARAMETER]
JOIN [dbo].[QUERYACTION]  ON [dbo].[QUERYACTIONPARAMETER].[QUERYACTIONID] = [dbo].[QUERYACTION].[QUERYACTIONID]
WHERE [dbo].[QUERYACTION].[QUERYID] = @QUERYID
ORDER BY [dbo].[QUERYACTIONPARAMETER].[PARAMETERNAME]
END