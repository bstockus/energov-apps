CREATE PROCEDURE [systemsetup].[USP_RPTTEXTLIB_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[RPTTEXTLIB].[RPTTEXTLIBID],
	[dbo].[RPTTEXTLIB].[TEXTNAME],
	[dbo].[RPTTEXTLIB].[REPORTTEXT],
	[dbo].[RPTTEXTLIB].[ACTIVE],
	[dbo].[RPTTEXTLIB].[LASTCHANGEDBY],
	[dbo].[RPTTEXTLIB].[LASTCHANGEDON],
	[dbo].[RPTTEXTLIB].[ROWVERSION]
FROM [dbo].[RPTTEXTLIB]
ORDER BY [dbo].[RPTTEXTLIB].[TEXTNAME] ASC
END