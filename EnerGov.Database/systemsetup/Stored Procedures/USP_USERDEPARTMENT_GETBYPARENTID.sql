CREATE PROCEDURE [systemsetup].[USP_USERDEPARTMENT_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
		[dbo].[USERDEPARTMENT].[DEPARTMENTID],
		[dbo].[USERDEPARTMENT].[SUSERGUID],
		[dbo].[DEPARTMENT].[NAME]
	FROM [dbo].[USERDEPARTMENT]
	INNER JOIN [dbo].[DEPARTMENT] ON [dbo].[USERDEPARTMENT].[DEPARTMENTID] = [dbo].[DEPARTMENT].[DEPARTMENTID]
	WHERE [dbo].[USERDEPARTMENT].[SUSERGUID] = @PARENTID  
	ORDER BY [dbo].[DEPARTMENT].[NAME]

END