CREATE PROCEDURE [systemsetup].[USP_ILMAPPEDVALUEGROUP_GETALL]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT	[dbo].[ILMAPPEDVALUEGROUP].[MAPPEDVALUEGROUPID],
			[dbo].[ILLICENSEGROUP].[NAME],
			[dbo].[ILMAPPEDVALUEGROUP].[MAPPEDVALUE],
			[dbo].[ILMAPPEDVALUEGROUP].[ILLICENSEGROUPID],
			[dbo].[ILMAPPEDVALUEGROUP].[LASTCHANGEDBY],
			[dbo].[ILMAPPEDVALUEGROUP].[LASTCHANGEDON],
			[dbo].[ILMAPPEDVALUEGROUP].[ROWVERSION]
	FROM [dbo].[ILMAPPEDVALUEGROUP]
	LEFT JOIN [dbo].[ILLICENSEGROUP] ON [dbo].[ILLICENSEGROUP].[ILLICENSEGROUPID] = [dbo].[ILMAPPEDVALUEGROUP].[ILLICENSEGROUPID]
	ORDER BY [dbo].[ILLICENSEGROUP].[NAME]
END