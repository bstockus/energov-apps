CREATE PROCEDURE [systemsetup].[USP_RPTPRINTTO_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[RPTPRINTTO].[RPTPRINTTOID],
	[dbo].[RPTPRINTTO].[RPTAUTOMAILID],
	[dbo].[RPTPRINTTO].[PRINTERPATH],
	[dbo].[RPTPRINTTO].[DESCRIPTION]
FROM [dbo].[RPTPRINTTO]
ORDER BY [dbo].[RPTPRINTTO].[PRINTERPATH] ASC
END