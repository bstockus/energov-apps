CREATE PROCEDURE [systemsetup].[USP_WORKFLOWACTIONTASKUSER_GETBYPARENTID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT
	[dbo].[WORKFLOWACTIONTASKUSER].[WORKFLOWACTIONTASKUSERID],
	[dbo].[WORKFLOWACTIONTASKUSER].[WORKFLOWACTIONTASKID],
	[dbo].[WORKFLOWACTIONTASKUSER].[USERID],
	[dbo].[USERS].[FNAME],
	[dbo].[USERS].[LNAME]
FROM [dbo].[WORKFLOWACTIONTASKUSER]
INNER JOIN [dbo].[WORKFLOWACTIONTASK] ON [dbo].[WORKFLOWACTIONTASK].[WORKFLOWACTIONTASKID] = [dbo].[WORKFLOWACTIONTASKUSER].[WORKFLOWACTIONTASKID]
INNER JOIN [dbo].[WORKFLOWACTION] ON [dbo].[WORKFLOWACTION].[WORKFLOWACTIONID] = [dbo].[WORKFLOWACTIONTASK].[WORKFLOWACTIONID]
INNER JOIN [dbo].[USERS] on [dbo].[WORKFLOWACTIONTASKUSER].[USERID] = [dbo].[USERS].[SUSERGUID]
WHERE
	[dbo].[WORKFLOWACTION].[WORKFLOWID] = @WORKFLOWID
END