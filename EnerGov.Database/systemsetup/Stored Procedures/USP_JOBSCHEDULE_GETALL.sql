CREATE PROCEDURE [systemsetup].[USP_JOBSCHEDULE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[JOBSCHEDULE].[JOBSCHEDULEID],
	[dbo].[JOBSCHEDULE].[NAME],
	[dbo].[JOBSCHEDULE].[CRONSTRING],
	[dbo].[JOBSCHEDULE].[DESCRIPTION],
	[dbo].[JOBSCHEDULE].[ISCUSTOMCRON],
	[dbo].[JOBSCHEDULE].[RECURRANCE],
	[dbo].[JOBSCHEDULE].[RECURRANCEID],
	[dbo].[JOBSCHEDULE].[RECURRANCESTART],
	[dbo].[JOBSCHEDULE].[RECURRANCEEND],
	[dbo].[JOBSCHEDULE].[LASTCHANGEDBY],
	[dbo].[JOBSCHEDULE].[LASTCHANGEDON],
	[dbo].[JOBSCHEDULE].[ROWVERSION]
FROM [dbo].[JOBSCHEDULE] ORDER BY	[dbo].[JOBSCHEDULE].[NAME] ASC
END