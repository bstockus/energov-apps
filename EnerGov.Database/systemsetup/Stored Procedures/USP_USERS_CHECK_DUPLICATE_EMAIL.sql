CREATE PROCEDURE [systemsetup].[USP_USERS_CHECK_DUPLICATE_EMAIL]
(
	@SUSERGUID CHAR(36),
	@EMAIL NVARCHAR(80)
)
AS
SET NOCOUNT ON;
SELECT	[dbo].[USERS].[SUSERGUID],
		[dbo].[USERS].[EMAIL]				
FROM	[dbo].[USERS] WITH (NOLOCK)
WHERE		
	[dbo].[USERS].[EMAIL] = @EMAIL AND [dbo].[USERS].[SUSERGUID] <> @SUSERGUID