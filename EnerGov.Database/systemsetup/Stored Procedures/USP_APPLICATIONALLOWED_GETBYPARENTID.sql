CREATE PROCEDURE [systemsetup].[USP_APPLICATIONALLOWED_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[APPLICATIONALLOWED].[APPLICATIONALLOWEDID],
	[dbo].[APPLICATIONALLOWED].[USERID],
	[dbo].[APPLICATIONALLOWED].[APPLICATIONID],
	[dbo].[APPLICATIONALLOWED].[APPLICATIONUSERTYPEID],
	[dbo].[APPLICATIONALLOWED].[APPROVED]
FROM [dbo].[APPLICATIONALLOWED]
WHERE
	[dbo].[APPLICATIONALLOWED].[USERID] = @PARENTID  

END