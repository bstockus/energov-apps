CREATE PROCEDURE [systemsetup].[USP_CATRANSACTION_BY_RECEIPTNUMBER]
(
	@RECEIPTNUMBER  NVARCHAR(50)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 	TOP 1
	[dbo].[CATRANSACTION].[RECEIPTNUMBER]
FROM [dbo].[CATRANSACTION]
WHERE
	[dbo].[CATRANSACTION].[RECEIPTNUMBER] = @RECEIPTNUMBER
ORDER BY
	[dbo].[CATRANSACTION].[CATRANSACTIONID]
END