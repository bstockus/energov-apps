CREATE PROCEDURE [systemsetup].[USP_RPTCUSTOMFIELD_GETALL]
AS
BEGIN
SET NOCOUNT ON;

	SELECT [RPTCUSTOMFIELD].[RPTCUSTOMFIELDID],
		   [RPTCUSTOMFIELD].[REPORTID],
		   [RPTCUSTOMFIELD].[CUSTOMFIELDOBJECTID],
		   [CUSTOMFIELDOBJECT].[FKGCUSTOMFIELDLAYOUT],
		   [CUSTOMFIELD].[SFIELDNAME],
		   [RPTCUSTOMFIELD].[REPORTCUSTOMFIELDSECTION],
		   [RPTCUSTOMFIELD].[ORDER]
	
	from [RPTREPORT]
	INNER JOIN [RPTCUSTOMFIELD] on [RPTREPORT].[RPTREPORTID] = [RPTCUSTOMFIELD].[REPORTID]
	INNER JOIN [CUSTOMFIELDOBJECT] on [RPTCUSTOMFIELD].[CUSTOMFIELDOBJECTID] = [CUSTOMFIELDOBJECT].[GCUSTOMFIELD]
	LEFT JOIN [CUSTOMFIELD] on [CUSTOMFIELDOBJECT].[FKGCUSTOMFIELD] = [CUSTOMFIELD].[GCUSTOMFIELD]

	ORDER BY [RPTCUSTOMFIELD].[ORDER]
END