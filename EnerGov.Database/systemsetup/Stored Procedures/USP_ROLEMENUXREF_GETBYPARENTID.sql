CREATE PROCEDURE [systemsetup].[USP_ROLEMENUXREF_GETBYPARENTID]
(
	@ROLEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON; 
SELECT		[dbo].[ROLEMENUXREF].[FKMENUID],
			[dbo].[ROLEMENUXREF].[FKROLEID],			
			[dbo].[MENUS].[SDESCRIPTION],			
			[dbo].[ROLEMENUXREF].[BVISIBLE]
FROM		[dbo].[ROLEMENUXREF]
INNER JOIN	[dbo].[MENUS] ON [dbo].[MENUS].[SMENUGUID] = [dbo].[ROLEMENUXREF].[FKMENUID]
WHERE		[dbo].[ROLEMENUXREF].[FKROLEID] = @ROLEID 
ORDER BY	[dbo].[MENUS].[IORDER]
END