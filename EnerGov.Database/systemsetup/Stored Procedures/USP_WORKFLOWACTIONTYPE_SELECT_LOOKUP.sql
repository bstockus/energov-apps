CREATE PROCEDURE [systemsetup].[USP_WORKFLOWACTIONTYPE_SELECT_LOOKUP]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPEID], 
	[dbo].[WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPENAME]
FROM [dbo].[WORKFLOWACTIONTYPE] 
WHERE [dbo].[WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPEID] <> 11 AND [dbo].[WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPEID] <> 6
ORDER BY [dbo].[WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPENAME]
END