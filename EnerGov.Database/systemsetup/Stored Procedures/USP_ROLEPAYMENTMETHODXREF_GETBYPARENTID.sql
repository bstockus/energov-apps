CREATE PROCEDURE [systemsetup].[USP_ROLEPAYMENTMETHODXREF_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
	SELECT 
		[dbo].[ROLEPAYMENTMETHODXREF].[ROLEPAYMENTMETHODXREFID],
		[dbo].[ROLEPAYMENTMETHODXREF].[SROLEGUID],
		[dbo].[ROLEPAYMENTMETHODXREF].[CAPAYMENTMETHODID],
		[dbo].[ROLEPAYMENTMETHODXREF].[DISABLEDFLAG],
		[dbo].[CAPAYMENTMETHOD].[NAME]
	FROM [dbo].[ROLEPAYMENTMETHODXREF]
	INNER JOIN [dbo].[CAPAYMENTMETHOD] ON [dbo].[CAPAYMENTMETHOD].[CAPAYMENTMETHODID] = [dbo].[ROLEPAYMENTMETHODXREF].[CAPAYMENTMETHODID]
	WHERE
		[dbo].[ROLEPAYMENTMETHODXREF].[SROLEGUID] = @PARENTID
	ORDER BY [dbo].[CAPAYMENTMETHOD].[NAME]
END