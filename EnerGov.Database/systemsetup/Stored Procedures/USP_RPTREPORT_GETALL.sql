CREATE PROCEDURE [systemsetup].[USP_RPTREPORT_GETALL]
AS
BEGIN
SET NOCOUNT ON;

SELECT 
		[dbo].[RPTREPORT].[RPTREPORTID],
		[dbo].[RPTREPORT].[RPTFRMTOBUSOBJMAPID],
		[dbo].[RPTREPORT].[REPORTNAME],		
		[dbo].[RPTREPORT].[FRIENDLYNAME],
		[dbo].[RPTREPORT].[SYSTEMREPORT],
		[dbo].[RPTREPORT].[PROMPTINPUT],
		[dbo].[RPTREPORT].[IDENOBJPROPFRIENDLYNAME],
		[dbo].[RPTREPORT].[IDENTIFYOBJECTPROPERTYTOKEN],
		[dbo].[RPTREPORT].[IDENTIFYOBJECTPROPERTYNAME],
		[dbo].[RPTREPORT].[IDENTIFYOBJECTPROPERTYVALUE],
		[dbo].[RPTREPORT].[MOBILEGOVREPORT],
		[dbo].[RPTREPORT].[ISSUBFOLDER],
		[dbo].[RPTREPORT].[RPTREPORTTYPEID],
		[dbo].[RPTREPORT].[REPORTVIRTUALPATH],
		[dbo].[RPTREPORT].[LASTCHANGEDBY],
		[dbo].[RPTREPORT].[LASTCHANGEDON],
		[dbo].[RPTREPORT].[ROWVERSION],
		CAST(CASE WHEN [dbo].[RPTREPORT].[REPORTPREVIEWIMAGE] IS NULL THEN 0 ELSE 1 END AS BIT) AS HASIMAGEDATA

	FROM [dbo].[RPTREPORT]
	ORDER BY [dbo].[RPTREPORT].[FRIENDLYNAME]

	EXEC [systemsetup].[USP_RPTREPORTATTRIBUTE_GETALL]
	EXEC [systemsetup].[USP_RPTCUSTOMFIELD_GETALL]
END