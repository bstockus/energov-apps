CREATE PROCEDURE [systemsetup].[USP_ROLESUBMENUXREF_GETBYPARENTID]
(
	@ROLEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT		[dbo].[ROLESUBMENUXREF].[FKSUBMENUID],
			[dbo].[ROLESUBMENUXREF].[FKROLEID],			
			[dbo].[SUBMENUS].[FKMENUGUID],
			[dbo].[SUBMENUS].[SDESCRIPTION],
			[dbo].[ROLESUBMENUXREF].[BVISIBLE]
FROM		[dbo].[ROLESUBMENUXREF]
INNER JOIN	[dbo].[SUBMENUS] ON [dbo].[SUBMENUS].[SSUBMENUGUID] = [dbo].[ROLESUBMENUXREF].[FKSUBMENUID]
WHERE		[FKROLEID] = @ROLEID
ORDER BY	[dbo].[SUBMENUS].[IORDER]
END