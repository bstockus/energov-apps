CREATE PROCEDURE [systemsetup].[USP_QUERYACTIONTASKUSER_GETBYPARENTID]
(
	@QUERYID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT
	[dbo].[QUERYACTIONTASKUSER].[QUERYACTIONTASKUSERID],
	[dbo].[QUERYACTIONTASKUSER].[QUERYACTIONTASKID],
	[dbo].[QUERYACTIONTASKUSER].[USERID],
	[dbo].[USERS].[FNAME],
	[dbo].[USERS].[LNAME]
FROM [dbo].[QUERYACTIONTASKUSER]
INNER JOIN [dbo].[QUERYACTIONTASK] ON [dbo].[QUERYACTIONTASK].[QUERYACTIONTASKID] = [dbo].[QUERYACTIONTASKUSER].[QUERYACTIONTASKID]
INNER JOIN [dbo].[QUERYACTION] ON [dbo].[QUERYACTION].[QUERYACTIONID] = [dbo].[QUERYACTIONTASK].[QUERYACTIONID]
INNER JOIN [dbo].[USERS] on [dbo].[QUERYACTIONTASKUSER].[USERID] = [dbo].[USERS].[SUSERGUID]
WHERE
	[dbo].[QUERYACTION].[QUERYID] = @QUERYID
END