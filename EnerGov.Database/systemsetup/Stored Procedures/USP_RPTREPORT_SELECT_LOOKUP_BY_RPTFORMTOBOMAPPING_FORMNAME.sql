CREATE PROCEDURE [systemsetup].[USP_RPTREPORT_SELECT_LOOKUP_BY_RPTFORMTOBOMAPPING_FORMNAME]
(
	@FORMNAME AS [dbo].[RecordNames] READONLY
)
AS
BEGIN
SET NOCOUNT ON;
SELECT  
	[dbo].[RPTREPORT].[RPTREPORTID],
	[dbo].[RPTREPORT].[FRIENDLYNAME],
	[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[FORMNAME]
	FROM [dbo].[RPTREPORT]
	JOIN [dbo].[RPTFORMTOBUSINESSOBJECTMAPPING] ON [dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[RPTFRMTOBUSOBJMAPID] = [dbo].[RPTREPORT].[RPTFRMTOBUSOBJMAPID]
WHERE [dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[FORMNAME] IN (SELECT [Name] FROM @FORMNAME)
ORDER BY [dbo].[RPTREPORT].[FRIENDLYNAME]
END