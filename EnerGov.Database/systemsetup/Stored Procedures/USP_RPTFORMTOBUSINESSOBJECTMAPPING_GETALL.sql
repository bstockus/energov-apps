CREATE PROCEDURE [systemsetup].[USP_RPTFORMTOBUSINESSOBJECTMAPPING_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT		[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[RPTFRMTOBUSOBJMAPID],
				[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[FORMNAME],
				[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[TYPEOFBBUSINESSOBJECT],
				[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[GENRICREPORT],
				[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[CUSTOMFIELDMODULEID],
				[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[SFORMSGUID]
	FROM		[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING]
	WHERE ISNULL([dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[ISHTMLDEPRICATED] , 0) = 0
	ORDER BY	[dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[FORMNAME]
END