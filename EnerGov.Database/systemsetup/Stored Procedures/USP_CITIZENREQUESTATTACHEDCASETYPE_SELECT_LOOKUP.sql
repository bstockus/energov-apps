CREATE PROCEDURE [systemsetup].[USP_CITIZENREQUESTATTACHEDCASETYPE_SELECT_LOOKUP]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CITIZENREQUESTATTACHEDCASETYPE].[REQUESTATTACHEDCASETYPEID],
	[dbo].[CITIZENREQUESTATTACHEDCASETYPE].[REQUESTATTACHEDCASENAME]
FROM 
	[dbo].[CITIZENREQUESTATTACHEDCASETYPE]
ORDER BY 
	[dbo].[CITIZENREQUESTATTACHEDCASETYPE].[REQUESTATTACHEDCASENAME]
END