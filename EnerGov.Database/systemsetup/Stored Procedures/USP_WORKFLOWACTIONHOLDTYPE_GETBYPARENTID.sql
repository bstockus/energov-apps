CREATE PROCEDURE [systemsetup].[USP_WORKFLOWACTIONHOLDTYPE_GETBYPARENTID]
(
	@WORKFLOWID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[WORKFLOWACTIONHOLDTYPE].[WORKFLOWACTIONHOLDTYPEID],
	[dbo].[WORKFLOWACTIONHOLDTYPE].[WORKFLOWACTIONID],
	[dbo].[WORKFLOWACTIONHOLDTYPE].[HOLDSETUPID],
	[dbo].[WORKFLOWACTIONHOLDTYPE].[COMMENTS]
FROM [dbo].[WORKFLOWACTIONHOLDTYPE]
INNER JOIN [dbo].[WORKFLOWACTION] ON [dbo].[WORKFLOWACTION].[WORKFLOWACTIONID] = [dbo].[WORKFLOWACTIONHOLDTYPE].[WORKFLOWACTIONID]
WHERE
	[dbo].[WORKFLOWACTION].[WORKFLOWID] = @WORKFLOWID
END