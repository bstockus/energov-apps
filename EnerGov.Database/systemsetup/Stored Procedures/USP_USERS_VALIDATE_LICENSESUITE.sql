CREATE PROCEDURE [systemsetup].[USP_USERS_VALIDATE_LICENSESUITE]
(
	@LICENSESUITE NVARCHAR(MAX),
	@SYSTEMADMINUSER CHAR(36),
	@SERVICEUSER CHAR(36),
	@ANONYMOUSUSER CHAR(36),
	@FRANKSIDEUSER CHAR(36)
)
AS
SET NOCOUNT ON;
SELECT	COUNT(1)			
FROM	[dbo].[USERS] WITH (NOLOCK)
WHERE		
		[dbo].[USERS].[BACTIVE] = 1 
		AND [dbo].[USERS].[SROLEID] IS NOT NULL		
		AND SUSERGUID <> @SYSTEMADMINUSER 
		AND SUSERGUID <> @SERVICEUSER 
		AND SUSERGUID <> @ANONYMOUSUSER
		AND SUSERGUID <> @FRANKSIDEUSER
		AND LICENSE_SUITE = @LICENSESUITE