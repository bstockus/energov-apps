CREATE PROCEDURE [SystemSettingsManager].[USP_RESOURCE_GETBY_USERID]      
(
@USERID CHAR(36), 
@UNAVAILABLEENDDATE DATE
)
AS    
BEGIN        
SET NOCOUNT ON;
 SELECT     
		[RESOURCE].[RESOURCEID]
  FROM  [dbo].[RESOURCE]       
 WHERE  
		[RESOURCE].[USERID] = @USERID  
	AND [RESOURCE].[UNAVAILABLEENDDATE] >= @UNAVAILABLEENDDATE
END