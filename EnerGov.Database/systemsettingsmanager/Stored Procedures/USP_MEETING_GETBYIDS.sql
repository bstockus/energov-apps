CREATE PROCEDURE [systemsettingsmanager].[USP_MEETING_GETBYIDS]
(
	@RECENTMEETINGLIST RecordIDs READONLY,
	@USERID AS CHAR(36) = NULL
)
AS
BEGIN

SELECT 
	[dbo].[MEETING].[MEETINGID],
	[dbo].[MEETING].[MEETINGTYPEID],
	[dbo].[MEETINGTYPE].[NAME] AS [MEETINGTYPENAME],
	[dbo].[MEETING].[SUBJECT],
	[dbo].[MEETING].[LOCATION],
	[dbo].[MEETING].[COMMENTS],
	[dbo].[MEETING].[STARTDATE],
	[dbo].[MEETING].[ENDDATE],
	[dbo].[MEETING].[ROWVERSION],
	[dbo].[MEETING].[EXCHANGEITEMID],
	[dbo].[MEETING].[SHOWONCALENDAR]
FROM [dbo].[MEETING] 
INNER JOIN @RECENTMEETINGLIST MEETINGLIST ON [dbo].[MEETING].[MEETINGID] = MEETINGLIST.RECORDID
INNER JOIN MEETINGTYPE ON [dbo].[MEETING].[MEETINGTYPEID] = [dbo].[MEETINGTYPE].[MEETINGTYPEID]

END