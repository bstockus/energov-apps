CREATE PROCEDURE [systemsettingsmanager].[USP_TEAMRESOURCE_GETBY_TEAMID]    
@TEAMID CHAR(36)
AS  
BEGIN    
SET NOCOUNT ON;

 SELECT   
		[TEAMRESOURCE].[TEAMRESOURCEID],
		[TEAMRESOURCE].[TEAMID],
		[TEAMRESOURCE].[USERID],
		[TEAMRESOURCE].[RESOURCETYPEID],
		[USERS].[FNAME],
		[USERS].[LNAME],
		[USERS].[PHONE],
		[USERS].[EMAIL],
		[RESOURCETYPE].[NAME]
  FROM  [dbo].[TEAMRESOURCE]
		INNER JOIN [dbo].[USERS] ON [USERS].[SUSERGUID] = [TEAMRESOURCE].[USERID]
		INNER JOIN [dbo].[RESOURCETYPE] WITH (NOLOCK) ON [RESOURCETYPE].[RESOURCETYPEID] = [TEAMRESOURCE].[RESOURCETYPEID]      
 WHERE
[TEAMRESOURCE].[TEAMID] = @TEAMID

EXEC [SystemSettingsManager].[USP_RESOURCE_GETBY_TEAMID] @TEAMID
END