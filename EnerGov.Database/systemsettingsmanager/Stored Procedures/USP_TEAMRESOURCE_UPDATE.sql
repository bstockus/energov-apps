  
CREATE  PROCEDURE [systemsettingsmanager].[USP_TEAMRESOURCE_UPDATE]    
-- Add the parameters for the stored procedure here    
@TEAMRESOURCEID CHAR(36),  
@TEAMID CHAR(36),  
@USERID CHAR(36),  
@RESOURCETYPEID CHAR(36)  
AS    
BEGIN      
 UPDATE[dbo].[TEAMRESOURCE]      
 SET    
[TEAMRESOURCE].[TEAMID] = @TEAMID,  
[TEAMRESOURCE].[USERID] = @USERID,  
[TEAMRESOURCE].[RESOURCETYPEID] = @RESOURCETYPEID  
WHERE   
[TEAMRESOURCE].[TEAMRESOURCEID] = @TEAMRESOURCEID  
END