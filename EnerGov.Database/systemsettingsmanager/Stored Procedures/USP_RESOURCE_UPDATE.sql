CREATE  PROCEDURE [systemsettingsmanager].[USP_RESOURCE_UPDATE]      
@RESOURCEID CHAR(36),  
@USERID CHAR(36),  
@UNAVAILABLESTARTDATE DATETIME,  
@UNAVAILABLEENDDATE DATETIME,  
@REASON VARCHAR(MAX),  
@DELEGATEUSERID CHAR(36)  
AS    
BEGIN        
 UPDATE dbo.[RESOURCE]  
 SET     
[RESOURCE].[USERID] = @USERID,  
[RESOURCE].[UNAVAILABLESTARTDATE] = @UNAVAILABLESTARTDATE,  
[RESOURCE].[UNAVAILABLEENDDATE] = @UNAVAILABLEENDDATE,  
[RESOURCE].[REASON] = @REASON,  
[RESOURCE].[DELEGATEUSERID] = @DELEGATEUSERID  
  FROM  [dbo].[RESOURCE]    
 WHERE  
 [RESOURCE].[RESOURCEID] = @RESOURCEID  
END