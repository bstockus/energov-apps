CREATE PROCEDURE [systemsettingsmanager].[USP_MEETING_SEARCH]
(
	@SEARCH			  AS NVARCHAR(255)	    =	'',
	@PAGE_NUMBER	  AS INT				=	1,
	@PAGE_SIZE		  AS INT				=	10,
	@IS_ASCENDING	  AS BIT				=	1
)
AS

WITH RAW_DATA AS (
	SELECT
			[dbo].[MEETING].[MEETINGID],
			[dbo].[MEETING].[MEETINGTYPEID],
			[dbo].[MEETINGTYPE].[NAME] AS [MEETINGTYPENAME],
			[dbo].[MEETING].[SUBJECT],
			[dbo].[MEETING].[LOCATION],
			[dbo].[MEETING].[COMMENTS],
			[dbo].[MEETING].[STARTDATE],
			[dbo].[MEETING].[ENDDATE],
			CASE @IS_ASCENDING WHEN 1 THEN
				ROW_NUMBER() OVER(ORDER BY [dbo].[MEETING].[SUBJECT])
			ELSE
				ROW_NUMBER() OVER(ORDER BY [dbo].[MEETING].[SUBJECT] DESC)
			END AS RowNumber,
			COUNT(1) OVER() AS TotalRows
	FROM	[dbo].[MEETING]
			INNER JOIN [dbo].[MEETINGTYPE] ON [dbo].[MEETING].[MEETINGTYPEID] = [dbo].[MEETINGTYPE].[MEETINGTYPEID]
	WHERE ([dbo].[MEETING].[SUBJECT] LIKE '%'+ @SEARCH +'%')
)

SELECT		* 
FROM		RAW_DATA
WHERE		RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) 
			AND RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY	RowNumber