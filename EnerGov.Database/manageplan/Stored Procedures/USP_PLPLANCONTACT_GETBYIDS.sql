CREATE PROCEDURE [manageplan].[USP_PLPLANCONTACT_GETBYIDS]
(
 @PLPLANLIST RecordIDs READONLY
)
AS
BEGIN
	SELECT 
		[PLPLANCONTACT].[PLPLANID],
		[PLPLANCONTACT].[GLOBALENTITYID],
		[GLOBALENTITY].[FIRSTNAME],
		[GLOBALENTITY].[LASTNAME],
		[GLOBALENTITY].[GLOBALENTITYNAME],
   		[PLPLANCONTACT].[ISBILLING], 
		[GLOBALENTITY].[TITLE],
		[GLOBALENTITY].[ISACTIVE],
		[LANDMANAGEMENTCONTACTTYPE].[LANDMANAGEMENTCONTACTTYPEID],
		[LANDMANAGEMENTCONTACTTYPE].[NAME]
	FROM [dbo].[PLPLANCONTACT] JOIN  [dbo].[GLOBALENTITY] ON [PLPLANCONTACT].[GLOBALENTITYID] = [GLOBALENTITY].[GLOBALENTITYID]
	INNER JOIN [dbo].[LANDMANAGEMENTCONTACTTYPE] ON [LANDMANAGEMENTCONTACTTYPE].[LANDMANAGEMENTCONTACTTYPEID] = [PLPLANCONTACT].[LANDMANAGEMENTCONTACTTYPEID]
	INNER JOIN @PLPLANLIST [PLPLANLIST] ON [PLPLANCONTACT].[PLPLANID] = [PLPLANLIST].[RECORDID]
END