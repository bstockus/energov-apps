CREATE PROCEDURE [inspectiondashboard].[USP_NAMES_FOR_SELECTED_USERS]
(	
	@RECORDIDs RecordIDs READONLY
)
AS

SET NOCOUNT ON;

SELECT
	[dbo].[USERS].[SUSERGUID],
	[dbo].[USERS].[FNAME],
	[dbo].[USERS].[LNAME]
FROM [dbo].[USERS]
WHERE [dbo].[USERS].[SUSERGUID] IN (SELECT ALL_USERS.RECORDID FROM @RECORDIDs ALL_USERS)
ORDER BY [dbo].[USERS].[SUSERGUID]