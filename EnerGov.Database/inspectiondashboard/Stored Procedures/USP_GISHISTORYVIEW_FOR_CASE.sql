CREATE PROCEDURE [inspectiondashboard].[USP_GISHISTORYVIEW_FOR_CASE]
	@ID CHAR(36)
AS

SET NOCOUNT ON;

SELECT TOP 1
	[dbo].[GISHISTORYVIEW].[ATTRIBUTEFIELD],
	[dbo].[GISHISTORYVIEW].[FEATURELAYER],
	[dbo].[GISHISTORYVIEW].[KEYVALUE]
FROM [dbo].[GISHISTORYVIEW]
WHERE [dbo].[GISHISTORYVIEW].[CASEID] = @ID