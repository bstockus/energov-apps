CREATE PROCEDURE [inspectiondashboard].[USP_TRAVEL_TIME_FOR_SELECTED_USERS]
(	
	@RECORDIDs RecordIDs READONLY
)
AS

SET NOCOUNT ON;

SELECT
	[dbo].[GISZONETOINSPECTORDESIG].[USERID],
	[dbo].[IMINSPECTIONZONE].[DEFAULTTRAVELTIME],
	[dbo].[IMINSPECTIONZONE].[NAME]
FROM [dbo].[GISZONETOINSPECTORDESIG]
INNER JOIN [dbo].[GISZONEEXTERNALVALUE] ON [dbo].[GISZONEEXTERNALVALUE].[GISZONEEXTERNALVALUEID] = [dbo].[GISZONETOINSPECTORDESIG].[GISZONEEXTERNALVALUEID]
INNER JOIN [dbo].[IMINSPECTIONZONE] ON [dbo].[IMINSPECTIONZONE].[IMINSPECTIONZONEID] = [dbo].[GISZONEEXTERNALVALUE].[IMINSPECTIONZONEID]
WHERE [dbo].[GISZONETOINSPECTORDESIG].[USERID] IN (SELECT ALL_USERS.RECORDID FROM @RECORDIDs ALL_USERS)
ORDER BY [dbo].[GISZONETOINSPECTORDESIG].[USERID], [dbo].[IMINSPECTIONZONE].[NAME]