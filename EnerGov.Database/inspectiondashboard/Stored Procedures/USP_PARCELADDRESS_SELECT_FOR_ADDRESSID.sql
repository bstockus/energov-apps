CREATE PROCEDURE [inspectiondashboard].[USP_PARCELADDRESS_SELECT_FOR_ADDRESSID]
	@ID CHAR(36)
AS

SET NOCOUNT ON;

SELECT TOP 1
	[dbo].[PARCELADDRESS].[ADDRESSID], 
	[dbo].[PARCELADDRESS].[ADDRESSLINE1], 
	[dbo].[PARCELADDRESS].[ADDRESSLINE2], 
	[dbo].[PARCELADDRESS].[ADDRESSLINE3], 
	[dbo].[PARCELADDRESS].[CITY], 
	[dbo].[PARCELADDRESS].[STATE], 
	[dbo].[PARCELADDRESS].[POSTALCODE], 
	[dbo].[PARCELADDRESS].[TAXNO], 
	[dbo].[PARCELADDRESS].[STREETTYPE], 
	[dbo].[PARCELADDRESS].[POSTDIRECTION], 
	[dbo].[PARCELADDRESS].[ZONEID], 
	[dbo].[PARCELADDRESS].[DIRECTIONTOPROP], 
	[dbo].[PARCELADDRESS].[PREDIRECTION], 
	[dbo].[PARCELADDRESS].[PARCELID], 
	[dbo].[PARCELADDRESS].[COUNTY], 
	[dbo].[PARCELADDRESS].[COUNTRY], 
	[dbo].[PARCELADDRESS].[COUNTRYTYPE], 
	[dbo].[PARCELADDRESS].[ADDRESSTYPE], 
	[dbo].[PARCELADDRESS].[PARCELNUMBER], 
	[dbo].[PARCELADDRESS].[GISY], 
	[dbo].[PARCELADDRESS].[GISX], 
	[dbo].[PARCELADDRESS].[GISADDRESSID], 
	[dbo].[PARCELADDRESS].[UNITORSUITE], 
	[dbo].[PARCELADDRESS].[PROVINCE], 
	[dbo].[PARCELADDRESS].[RURALROUTE], 
	[dbo].[PARCELADDRESS].[STATION], 
	[dbo].[PARCELADDRESS].[COMPSITE], 
	[dbo].[PARCELADDRESS].[POBOX], 
	[dbo].[PARCELADDRESS].[ATTN], 
	[dbo].[PARCELADDRESS].[GENERALDELIVERY]
FROM [dbo].[PARCELADDRESS]
WHERE [dbo].[PARCELADDRESS].[ADDRESSID] = @ID