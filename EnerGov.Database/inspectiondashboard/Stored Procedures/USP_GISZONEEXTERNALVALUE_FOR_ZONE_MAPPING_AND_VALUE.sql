CREATE PROCEDURE [inspectiondashboard].[USP_GISZONEEXTERNALVALUE_FOR_ZONE_MAPPING_AND_VALUE]
(
	@GISZONEMAPPINGID CHAR(36),
	@GISZONEVALUE NVARCHAR(250)
)
AS

SET NOCOUNT ON;

SELECT TOP 1
	[dbo].[GISZONEEXTERNALVALUE].[GISZONEEXTERNALVALUEID]
FROM [dbo].[GISZONEEXTERNALVALUE]
WHERE 
	[dbo].[GISZONEEXTERNALVALUE].[GISZONEMAPPINGID] = @GISZONEMAPPINGID AND 
	[dbo].[GISZONEEXTERNALVALUE].[VALUE] = @GISZONEVALUE