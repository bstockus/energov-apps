CREATE PROCEDURE [inspectiondashboard].[USP_IMINSPECTIONTYPES_ESTIMATED_MINUTES_FOR_IDS]
(	
	@RECORDIDs RecordIDs READONLY
)
AS

SET NOCOUNT ON;

SELECT
	[dbo].[IMINSPECTIONTYPE].[IMINSPECTIONTYPEID],
	[dbo].[IMINSPECTIONTYPE].[ESTIMATEDMINUTES]
FROM [dbo].[IMINSPECTIONTYPE]
INNER JOIN @RECORDIDs ALL_TYPES ON ALL_TYPES.RECORDID = [dbo].[IMINSPECTIONTYPE].[IMINSPECTIONTYPEID]
ORDER BY [dbo].[IMINSPECTIONTYPE].[IMINSPECTIONTYPEID]