CREATE PROCEDURE [inspectiondashboard].[USP_ACTIVE_INSPECTORS_FOR_INSPECTION_TYPE]
	@ID CHAR(36)
AS

SET NOCOUNT ON;

SELECT 
	[dbo].[IMINSPECTORTYPEUSER].[USERID]
FROM [dbo].[IMINSPECTORTYPEUSER]
INNER JOIN [dbo].[USERS] ON [dbo].[USERS].[SUSERGUID] = [dbo].[IMINSPECTORTYPEUSER].[USERID]
INNER JOIN [dbo].[IMINSPECTORTYPEINSPECTIONTYPE] ON [dbo].[IMINSPECTORTYPEINSPECTIONTYPE].[IMINSPECTORTYPEID] = [dbo].[IMINSPECTORTYPEUSER].[INSPECTORTYPEID]
WHERE [dbo].[USERS].[BACTIVE] = 1 AND [dbo].[IMINSPECTORTYPEINSPECTIONTYPE].[IMINSPECTIONTYPEID] = @ID
ORDER BY [dbo].[IMINSPECTORTYPEUSER].[PRIMARYINSPECTOR] DESC, [dbo].[IMINSPECTORTYPEUSER].[USERID] ASC