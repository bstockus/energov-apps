CREATE PROCEDURE [inspectiondashboard].[USP_IMINSPECTORREF_GET_BY_INSPECTION_IDS]
(	
	@RECORDIDs RecordIDs READONLY
)
AS

SET NOCOUNT ON;

SELECT 
	[dbo].[IMINSPECTORREF].[IMINSPECTORREFID],
	[dbo].[IMINSPECTORREF].[INSPECTIONID], 
	[dbo].[IMINSPECTORREF].[USERID], 
	[dbo].[IMINSPECTORREF].[BPRIMARY]
FROM [dbo].[IMINSPECTORREF]
INNER JOIN @RECORDIDs AS INSPECTIONIDS ON INSPECTIONIDS.RECORDID = [dbo].[IMINSPECTORREF].[INSPECTIONID]