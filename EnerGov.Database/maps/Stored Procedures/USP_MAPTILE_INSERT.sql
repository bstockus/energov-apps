CREATE PROCEDURE [maps].[USP_MAPTILE_INSERT]
(
	@MAPTILEID VARCHAR(36),
	@MAPNAME VARCHAR(35),
	@MAPDESCRIPTION VARCHAR(200),
	@MAPURL VARCHAR(MAX),
	@MAPIMAGE VARCHAR(MAX),
	@USERID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
BEGIN
	DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
	INSERT INTO [dbo].[MAPTILE](
		[MAPTILEID],
		[MAPNAME],
		[MAPDESCRIPTION],
		[MAPURL],
		[MAPIMAGE],
		[USERID],
		[LASTCHANGEDBY],
		[LASTCHANGEDON],
		[ROWVERSION]
	)
	OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
	VALUES
	(
		@MAPTILEID,
		@MAPNAME,
		@MAPDESCRIPTION,
		@MAPURL,
		@MAPIMAGE,
		@USERID,
		@LASTCHANGEDBY,
		@LASTCHANGEDON,
		@ROWVERSION
	)
	SELECT * FROM @OUTPUTTABLE
END