CREATE PROCEDURE [maps].[USP_GISHISTORYQUEUE_INSERT]
(
	@GISHISTORYQUEUEID CHAR(36),
	@MODULENAME VARCHAR(50),
	@CASEID CHAR(36),
	@CASENUMBER VARCHAR (200),
	@CASETYPE CHAR(36),
	@WORKCLASS CHAR(36),
	@APPLICATIONDATE DATETIME,
	@PROJECTID CHAR(36),
	@PROJECTNAME NVARCHAR(100),
	@SPATIALTYPE VARCHAR(50),
	@SPATIALID VARCHAR (150),
	@ISWRITTENTOGIS BIT,
	@CREATEDATE DATETIME,
	@PROCESSEDDATE DATETIME,
	@RESULTLOG NVARCHAR(MAX),
	@RETRYCOUNT INT,
	@SENTTOBUS BIT
)
AS

INSERT INTO [dbo].[GISHISTORYQUEUE](
	[GISHISTORYQUEUEID],
	[MODULENAME],
	[CASEID],
	[CASENUMBER],
	[CASETYPE],
	[WORKCLASS],
	[APPLICATIONDATE],
	[PROJECTID],
	[PROJECTNAME],
	[SPATIALTYPE],
	[SPATIALID],
	[ISWRITTENTOGIS],
	[CREATEDATE],
	[PROCESSEDDATE],
	[RESULTLOG],
	[RETRYCOUNT],
	[SENTTOBUS]
)

VALUES
(
	@GISHISTORYQUEUEID,
	@MODULENAME,
	@CASEID,
	@CASENUMBER,
	@CASETYPE,
	@WORKCLASS,
	@APPLICATIONDATE,
	@PROJECTID,
	@PROJECTNAME,
	@SPATIALTYPE,
	@SPATIALID,
	@ISWRITTENTOGIS,
	@CREATEDATE,
	@PROCESSEDDATE,
	@RESULTLOG,
	@RETRYCOUNT,
	@SENTTOBUS
)