CREATE PROCEDURE [gissetup].[USP_GEOQUERY_SEARCH]
(
	@SEARCH			  AS NVARCHAR(255)	    =	'',
	@PAGE_NUMBER	  AS INT				=	1,
	@PAGE_SIZE		  AS INT				=	10,
	@IS_ASCENDING	  AS BIT				=	1
)
AS

WITH RAW_DATA AS (
	SELECT
			[dbo].[GEOQUERY].[GEOQUERYID],
			[dbo].[GEOQUERY].[QUERYNAME],
			[dbo].[GEOQUERY].[FEATURECLASSNAME],
			[dbo].[GEOQUERY].[FIELDNAME],
			[dbo].[GEOQUERY].[RETURNTYPE],
			[dbo].[GEOQUERY].[RETURNEDFEATURECLASS],
			[dbo].[GEOQUERYTYPE].[QUERYTYPENAME],
			CASE @IS_ASCENDING WHEN 1 THEN
				ROW_NUMBER() OVER(ORDER BY [dbo].[GEOQUERY].[QUERYNAME])
			ELSE
				ROW_NUMBER() OVER(ORDER BY [dbo].[GEOQUERY].[QUERYNAME] DESC)
			END AS RowNumber,
			COUNT(1) OVER() AS TotalRows
	FROM	[dbo].[GEOQUERY]
			INNER JOIN [dbo].[GEOQUERYTYPE] ON [dbo].[GEOQUERYTYPE].[GEOQUERYTYPEID] = [dbo].[GEOQUERY].[GEOQUERYTYPEID]
	WHERE	[dbo].[GEOQUERY].[QUERYNAME] LIKE '%'+ @SEARCH +'%'
)

SELECT		* 
FROM		RAW_DATA
WHERE		RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) 
			AND RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY	RowNumber