CREATE PROCEDURE [gissetup].[USP_GEORULE_LOOKUP_BY_GEORULE_ENTITY]
(
 @GEORULEENTITYCLASSNAME NVARCHAR (250)
)
AS
BEGIN
SET NOCOUNT ON;

SELECT 
	[dbo].[GEORULE].[GEORULEID],
	[dbo].[GEORULE].[RULENAME] 
FROM 
	[dbo].[GEORULE]
	JOIN [dbo].[GEORULEENTITY] ON [dbo].[GEORULE].[GEORULEENTITYID] = [dbo].[GEORULEENTITY].[GEORULEENTITYID]
WHERE 
	[dbo].[GEORULEENTITY].[GEORULEENTITYCLASS] = @GEORULEENTITYCLASSNAME
ORDER BY 
	[dbo].[GEORULE].[RULENAME]

END