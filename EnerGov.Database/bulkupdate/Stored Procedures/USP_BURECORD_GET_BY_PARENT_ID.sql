CREATE PROCEDURE [bulkupdate].[USP_BURECORD_GET_BY_PARENT_ID]
	@PARENTID char(36)
AS
BEGIN

	SELECT 
		[dbo].[BURECORD].[ENTITYID], 
		[dbo].[BURECORD].[ENTITYNUMBER], 
		[dbo].[BURECORD].[PROCESSEDDATE], 
		[dbo].[BURECORD].[RESULTLOG], 
		[dbo].[BURECORD].[SUCCESSFLAG]
	FROM [dbo].[BURECORD]
	WHERE [dbo].[BURECORD].[BUSERVICEID] = @PARENTID

END