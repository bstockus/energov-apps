CREATE PROCEDURE [workflow].[USP_GET_PROFESSIONAL_LICENSE_WORKFLOW_SUMMARY]
	@ID AS CHAR(36)
AS

SET NOCOUNT ON;

SELECT 
	ILLICENSEWFACTIONSTEP.WORKFLOWSTATUSID,
	COUNT(1) AS TOTAL
FROM
	ILLICENSEWFACTIONSTEP
INNER JOIN ILLICENSEWFSTEP ON ILLICENSEWFSTEP.ILLICENSEWFSTEPID = ILLICENSEWFACTIONSTEP.ILLICENSEWFSTEPID
wHERE 
	ILLICENSEWFSTEP.ILLICENSEID = @ID AND ILLICENSEWFACTIONSTEP.WORKFLOWSTATUSID NOT IN (4,3,2,7)
GROUP BY ILLICENSEWFACTIONSTEP.WORKFLOWSTATUSID