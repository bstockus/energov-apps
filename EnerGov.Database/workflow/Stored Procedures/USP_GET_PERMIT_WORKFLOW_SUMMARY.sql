CREATE PROCEDURE [workflow].[USP_GET_PERMIT_WORKFLOW_SUMMARY]
	@ID AS CHAR(36)
AS

SET NOCOUNT ON;

SELECT 
	PMPERMITWFACTIONSTEP.WORKFLOWSTATUSID,
	COUNT(1) AS TOTAL
FROM
	PMPERMITWFACTIONSTEP
INNER JOIN PMPERMITWFSTEP ON PMPERMITWFSTEP.PMPERMITWFSTEPID = PMPERMITWFACTIONSTEP.PMPERMITWFSTEPID
wHERE 
	PMPERMITWFSTEP.PMPERMITID = @ID AND PMPERMITWFACTIONSTEP.WORKFLOWSTATUSID NOT IN (4,3,2,7)
GROUP BY PMPERMITWFACTIONSTEP.WORKFLOWSTATUSID