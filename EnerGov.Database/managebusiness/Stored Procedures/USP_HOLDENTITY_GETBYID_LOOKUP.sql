CREATE PROCEDURE [managebusiness].[USP_HOLDENTITY_GETBYID_LOOKUP]  
(  
 @GLOBALENTITYEXTENSIONID CHAR(36)  
)  
AS  
BEGIN  
SET NOCOUNT ON;  
  
SELECT [BLEXTHOLD].[BLGLOBALENTITYEXTENSIONID],  
  [BLEXTHOLD].[HOLDSETUPID],  
  [BLEXTHOLD].[BLEXTHOLDID],  
  [BLEXTHOLD].[ACTIVE],  
  [USERS].[FNAME],  
  [USERS].[LNAME],  
  [BLEXTHOLD].[CREATEDDATE]    
FROM [BLEXTHOLD]   
  INNER JOIN [USERS] WITH (NOLOCK) ON [BLEXTHOLD].[SUSERGUID]=[USERS].[SUSERGUID]   
WHERE   
 [BLEXTHOLD].[BLGLOBALENTITYEXTENSIONID] = @GLOBALENTITYEXTENSIONID  
END