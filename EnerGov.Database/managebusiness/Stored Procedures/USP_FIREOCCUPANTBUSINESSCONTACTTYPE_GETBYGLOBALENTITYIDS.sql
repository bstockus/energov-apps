CREATE PROCEDURE [managebusiness].[USP_FIREOCCUPANTBUSINESSCONTACTTYPE_GETBYGLOBALENTITYIDS]
(
	@GLOBALENTITYIDS RecordIDs READONLY
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
	BLGLOBALENTITYEXTENSIONCONTACT.BLGLOBALENTITYEXTCONTACTID,
	BLCONTACTTYPE.BLCONTACTTYPEID,
	BLCONTACTTYPE.NAME,
	BLCONTACTTYPESYSTEM.BLCONTACTTYPESYSTEMID
	FROM BLGLOBALENTITYEXTENSIONCONTACT
	INNER JOIN BLCONTACTTYPE ON BLCONTACTTYPE.BLCONTACTTYPEID = BLGLOBALENTITYEXTENSIONCONTACT.BLCONTACTTYPEID
	INNER JOIN BLCONTACTTYPESYSTEM ON BLCONTACTTYPESYSTEM.BLCONTACTTYPESYSTEMID = BLCONTACTTYPE.BLCONTACTTYPESYSTEMID
	WHERE BLGLOBALENTITYEXTENSIONCONTACT.GLOBALENTITYID IN (SELECT RECORDID FROM @GLOBALENTITYIDS)
END