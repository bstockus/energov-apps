CREATE PROCEDURE [managebusiness].[USP_FIREOCCUPANTBUSINESS_GETBYID]
(
	@ID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
	BLGLOBALENTITYEXTENSION.BLGLOBALENTITYEXTENSIONID,
	BLGLOBALENTITYEXTENSION.REGISTRATIONID,
	BLGLOBALENTITYEXTENSION.COMPANYNAME,
	DISTRICT.NAME DISTRICTNAME
	FROM BLGLOBALENTITYEXTENSION
	LEFT OUTER JOIN DISTRICT ON BLGLOBALENTITYEXTENSION.DISTRICTID = DISTRICT.DISTRICTID
	WHERE BLGLOBALENTITYEXTENSIONID = @ID

	EXEC [managebusiness].[USP_FIREOCCUPANTBUSINESSADDRESS_GETBYGLOBALENTITYEXTENSIONID] @ID
	EXEC [managebusiness].[USP_FIREOCCUPANTBUSINESSCONTACT_GETBYGLOBALENTITYEXTENSIONID] @ID

END