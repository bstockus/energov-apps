CREATE PROCEDURE [managebusiness].[USP_BLGLOBALENTITYEXTENSION_GETBYID]  
(@ID AS CHAR(36),
@CURRENTDATE DATETIME)  
AS  
BEGIN  
    DECLARE @RECENTBUSINESSLIST AS RecordIDs;  
    INSERT INTO @RECENTBUSINESSLIST  
    (  
        RECORDID  
    )  
    VALUES  
    (@ID);  
    EXEC [managebusiness].[USP_BLGLOBALENTITYEXTENSION_GETBYIDS] @RECENTBUSINESSLIST,NULL;
	EXEC [managebusiness].[USP_BLGLOBALENTITYEXTENSIONADDRESS_GETALL] @RECENTBUSINESSLIST;
	EXEC [managebusiness].[USP_BUSINESSCONTACT_GETBYID] @ID, 2;
	EXEC [managebusiness].[USP_BUSINESSCONTACTTYPES_GETBYID] @ID
	EXEC [managebusiness].[USP_GETBALANCEDUE_BY_BLGLOBALENTITYEXTENDIONID] @ID,@CURRENTDATE
END;