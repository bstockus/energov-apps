CREATE FUNCTION [managebusiness].[UFN_BLLICENSETYPENAME_GETBYBLLICENSEID]  
(  
 @BLLICENSEID CHAR(36)  
)  
RETURNS NVARCHAR(500)
AS  
BEGIN  

DECLARE @NAME AS NVARCHAR(500)
SET @NAME = (SELECT TOP 1   [BLEXTBUSINESSTYPE].[NAME] FROM [dbo].[BLLICENSEEXTBUSINESSTYPE]  
  INNER JOIN [dbo].[BLEXTBUSINESSTYPE]  ON [BLEXTBUSINESSTYPE].[BLEXTBUSINESSTYPEID]=[BLLICENSEEXTBUSINESSTYPE].[BLEXTBUSINESSTYPEID]   
WHERE   
 [BLLICENSEEXTBUSINESSTYPE].[BLLICENSEID] =  @BLLICENSEID  
 AND [BLLICENSEEXTBUSINESSTYPE].[MAIN] = 1  
 )

	RETURN @NAME
END