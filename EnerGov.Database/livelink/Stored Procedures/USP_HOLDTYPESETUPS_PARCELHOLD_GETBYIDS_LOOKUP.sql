CREATE PROCEDURE [livelink].[USP_HOLDTYPESETUPS_PARCELHOLD_GETBYIDS_LOOKUP]
(
	@PARCELHOLDIDS RECORDIDS READONLY -- @IDS
)
AS
BEGIN
SET NOCOUNT ON;
SELECT  [HOLDTYPESETUPS].[ACTIVE],
		[HOLDTYPESETUPS].[ALERTMESSAGE],
		[HOLDTYPESETUPS].[ALLOWHOLDOVERRIDES],
		[HOLDTYPESETUPS].[ALLOWNEWRENTALPROPERTY],
		[HOLDTYPESETUPS].[ALLOWRENTALPROPERTYINSPECTION],
		[HOLDTYPESETUPS].[BLEXTSTATUSID],
		[HOLDTYPESETUPS].[BLPULLPERMIT],
		[HOLDTYPESETUPS].[BLPULLPLAN],
		[HOLDTYPESETUPS].[BLRENEW],
		[HOLDTYPESETUPS].[BLSTATUS],
		[HOLDTYPESETUPS].[BLWF],
		[HOLDTYPESETUPS].[BNEWLICENSE],
		[HOLDTYPESETUPS].[BRENEWLICENSE],
		[HOLDTYPESETUPS].[BUSINESSINSP],
		[HOLDTYPESETUPS].[DESCRIPTION],
		[HOLDTYPESETUPS].[DISPLAYHOLDMESSAGEONBAR],
		[HOLDTYPESETUPS].[ENTBUSINESSLICRENEWAL],
		[HOLDTYPESETUPS].[ENTCHECKPAY],
		[HOLDTYPESETUPS].[ENTCREDITCARDPAY],
		[HOLDTYPESETUPS].[ENTONLINEINSPECTIONREQUEST],
		[HOLDTYPESETUPS].[ENTONLINEPERMITAPPLICANT],
		[HOLDTYPESETUPS].[ENTONLINEPLANAPPLICANT],
		[HOLDTYPESETUPS].[ENTPROLICRENEWAL],
		[HOLDTYPESETUPS].[HOLDSETUPID],
		[HOLDTYPESETUPS].[HOLDTYPEID],
		[HOLDTYPESETUPS].[ILPULLBL],
		[HOLDTYPESETUPS].[ILPULLPERMIT],
		[HOLDTYPESETUPS].[ILPULLPLAN],
		[HOLDTYPESETUPS].[ILSTATUS],
		[HOLDTYPESETUPS].[ILWF],
		[HOLDTYPESETUPS].[MODULEID],
		[HOLDTYPESETUPS].[NAME],
		[HOLDTYPESETUPS].[PARCELENFORCEMAINPARCELONLY],
		[HOLDTYPESETUPS].[PARCELENFORCEPERM],
		[HOLDTYPESETUPS].[PARCELENFORCEPLAN],
		[HOLDTYPESETUPS].[PARCELENFORCEPROJECT],
		[HOLDTYPESETUPS].[PERMITFINALING],
		[HOLDTYPESETUPS].[PERMITFLAGSTATUS],
		[HOLDTYPESETUPS].[PERMITINSPECTION],
		[HOLDTYPESETUPS].[PERMITISSUANCE],
		[HOLDTYPESETUPS].[PERMITNEW],
		[HOLDTYPESETUPS].[PERMITRENEWALRESTRICTION],
		[HOLDTYPESETUPS].[PERMITWFSUBREVIEW],
		[HOLDTYPESETUPS].[PLANCOMPLETE],
		[HOLDTYPESETUPS].[PLANFLAGSTATUS],
		[HOLDTYPESETUPS].[PLANINSPECTION],
		[HOLDTYPESETUPS].[PLANNEW],
		[HOLDTYPESETUPS].[PLANWFSUBREVIVIEW],
		[HOLDTYPESETUPS].[PLRENEW],
		[HOLDTYPESETUPS].[PROJECTCOMPLETE],
		[HOLDTYPESETUPS].[PROJECTEXTENDHOLDTOCHILD],
		[HOLDTYPESETUPS].[PROJECTFLAGSTATUS],
		[HOLDTYPESETUPS].[PROJECTINSPECTION],
		[HOLDTYPESETUPS].[PROJECTNEWCHILD],
		[HOLDTYPESETUPS].[RESTRICTHOLDRELEASE],
		[HOLDTYPESETUPS].[RPLANDLORDLICENSESTATUSID],
		[HOLDTYPESETUPS].[RPPROPERTYSTATUSID]
FROM 
	[dbo].[HOLDTYPESETUPS] WITH (NOLOCK)
	INNER JOIN [dbo].[PARCELHOLD]
		ON  [HOLDTYPESETUPS].[HOLDSETUPID]=[PARCELHOLD].[HOLDSETUPID] 
WHERE 
	[PARCELHOLD].[PARCELHOLDID]  IN (SELECT [RECORDID] FROM @PARCELHOLDIDS)
	 
END