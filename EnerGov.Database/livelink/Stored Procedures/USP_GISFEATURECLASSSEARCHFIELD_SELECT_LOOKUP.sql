CREATE PROCEDURE [livelink].[USP_GISFEATURECLASSSEARCHFIELD_SELECT_LOOKUP]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
		[GISFEATURECLASSSEARCHFIELD].[GISFEATURECLASSSEARCHFIELDID],
		[GISFEATURECLASSSEARCHFIELD].[GISFEATURECLASSID],
		[GISFEATURECLASSSEARCHFIELD].[NAME],
		[GISFEATURECLASSSEARCHFIELD].[ALIAS]
FROM [dbo].[GISFEATURECLASS] WITH (NOLOCK)
	INNER JOIN [dbo].[GISFEATURECLASSSEARCHFIELD] WITH (NOLOCK) ON [GISFEATURECLASS].[GISFEATURECLASSID] = [GISFEATURECLASSSEARCHFIELD].[GISFEATURECLASSID]
	
END