CREATE PROCEDURE [examrequest].[USP_EXAMREQUEST_RECENT]
(
  @USERID AS CHAR(36)
)
AS
BEGIN
	DECLARE @RECENTEXAMREQUESTLIST AS RecordIDs
	INSERT INTO @RECENTEXAMREQUESTLIST (RECORDID)

	SELECT TOP 15 RECENTHISTORYEXAMREQUEST.EXAMREQUESTID 
	FROM RECENTHISTORYEXAMREQUEST
	WHERE RECENTHISTORYEXAMREQUEST.USERID = @USERID
	ORDER BY RECENTHISTORYEXAMREQUEST.LOGGEDDATETIME DESC

	EXEC [examrequest].[USP_RECENTEXAMREQUEST_GETBYIDS] @RECENTEXAMREQUESTLIST, @USERID 
END