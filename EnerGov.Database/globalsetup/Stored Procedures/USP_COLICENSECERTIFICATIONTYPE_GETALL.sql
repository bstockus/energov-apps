CREATE PROCEDURE [globalsetup].[USP_COLICENSECERTIFICATIONTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID],
	[dbo].[COLICENSECERTIFICATIONTYPE].[NAME],
	[dbo].[COLICENSECERTIFICATIONTYPE].[SUBJECTTOLICENSEVALIDATION],
	[dbo].[COLICENSECERTIFICATIONTYPE].[LASTCHANGEDBY],
	[dbo].[COLICENSECERTIFICATIONTYPE].[LASTCHANGEDON],
	[dbo].[COLICENSECERTIFICATIONTYPE].[ROWVERSION]
FROM [dbo].[COLICENSECERTIFICATIONTYPE]
	ORDER BY [dbo].[COLICENSECERTIFICATIONTYPE].[NAME]

	EXEC [globalsetup].[USP_COLICTYPECLASSREF_GETALL]

	EXEC [globalsetup].[USP_COLICTYPEGROUPREF_GETALL] 
END