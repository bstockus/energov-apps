CREATE PROCEDURE [globalsetup].[USP_PLITEMREVIEWSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PLITEMREVIEWSTATUS].[PLITEMREVIEWSTATUSID],
	[dbo].[PLITEMREVIEWSTATUS].[NAME],
	[dbo].[PLITEMREVIEWSTATUS].[SUCCESSFLAG],
	[dbo].[PLITEMREVIEWSTATUS].[FAILUREFLAG],
	[dbo].[PLITEMREVIEWSTATUS].[SHOWCOMMENTSINCAP],
	[dbo].[PLITEMREVIEWSTATUS].[SHOWRECOMMENDATIONSINCAP],
	[dbo].[PLITEMREVIEWSTATUS].[SHOWCORRECTIONSINCAP],
	[dbo].[PLITEMREVIEWSTATUS].[LASTCHANGEDBY],
	[dbo].[PLITEMREVIEWSTATUS].[LASTCHANGEDON],
	[dbo].[PLITEMREVIEWSTATUS].[ROWVERSION]
FROM [dbo].[PLITEMREVIEWSTATUS]
ORDER BY [dbo].[PLITEMREVIEWSTATUS].[NAME] ASC
END