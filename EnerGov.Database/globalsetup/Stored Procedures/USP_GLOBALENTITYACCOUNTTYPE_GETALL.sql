CREATE PROCEDURE [globalsetup].[USP_GLOBALENTITYACCOUNTTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[GLOBALENTITYACCOUNTTYPE].[GLOBALENTITYACCOUNTTYPEID],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[TYPENAME],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[ISBONDTYPE],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[ISLICENSETYPE],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[ISAUTONUMBER],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[PREFIX],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[DESCRIPTION],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[CHARGECODE],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[ISFEEWAIVERACCOUNT],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[JURISDICTIONID],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[LASTCHANGEDBY],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[LASTCHANGEDON],
	[dbo].[GLOBALENTITYACCOUNTTYPE].[ROWVERSION]
FROM 
	[dbo].[GLOBALENTITYACCOUNTTYPE]
ORDER BY 
	[dbo].[GLOBALENTITYACCOUNTTYPE].[TYPENAME]

EXEC [globalsetup].[USP_GLOBALENTITYACCOUNTTYPEGL_GETALL]

END