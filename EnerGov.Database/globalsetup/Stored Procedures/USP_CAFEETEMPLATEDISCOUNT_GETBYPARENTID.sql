CREATE PROCEDURE [globalsetup].[USP_CAFEETEMPLATEDISCOUNT_GETBYPARENTID]
		@CAFEETEMPLATEID CHAR(36)
AS
BEGIN
SET NOCOUNT ON;

	SELECT	[dbo].[CAFEETEMPLATEDISCOUNT].[CAFEETEMPLATEDISCOUNTID],
			[dbo].[CAFEETEMPLATEDISCOUNT].[CAFEETEMPLATEID],
			[dbo].[CAFEETEMPLATEDISCOUNT].[CADISCOUNTID],
			[dbo].[CADISCOUNT].[NAME],
			[dbo].[CADISCOUNT].[DESCRIPTION],
			[dbo].[CADISCOUNT].[CACOMPUTATIONTYPEID],
			[dbo].[CAFEETEMPLATEDISCOUNT].[APPLYORDER],
			[dbo].[CAFEETEMPLATEDISCOUNT].[APPLYTOTOTAL],		
			[dbo].[CAFEETEMPLATEDISCOUNT].[ISMANUAL],
			[dbo].[CAFEETEMPLATEDISCOUNT].[ISHIDDEN],
			[dbo].[CAFEETEMPLATEDISCOUNT].[ROUNDINGTYPEID],
			[dbo].[CAFEETEMPLATEDISCOUNT].[ROUNDINGVALUE],
			[dbo].[CAFEETEMPLATEDISCOUNT].[CACONDITIONGROUPID],
			[dbo].[CAFEETEMPLATEDISCOUNT].[CAEXPRESSIONID]
	FROM [dbo].[CAFEETEMPLATEDISCOUNT]
	LEFT JOIN [dbo].[CADISCOUNT] ON [dbo].[CADISCOUNT].[CADISCOUNTID] = [dbo].[CAFEETEMPLATEDISCOUNT].[CADISCOUNTID]
	WHERE [dbo].[CAFEETEMPLATEDISCOUNT].[CAFEETEMPLATEID] = @CAFEETEMPLATEID

END