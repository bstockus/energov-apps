CREATE PROCEDURE	[globalsetup].[USP_CAFINANCIALINTEGRATIONSETUP_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT				[dbo].[CAFINANCIALINTEGRATIONSETUP].[CAFINANCIALINTEGRATIONSETUPID],
					[dbo].[CAFINANCIALINTEGRATIONSETUP].[NAME],
					[dbo].[CAFINANCIALINTEGRATIONSETUP].[ISACTIVE]

FROM				[dbo].[CAFINANCIALINTEGRATIONSETUP]
ORDER BY			[dbo].[CAFINANCIALINTEGRATIONSETUP].[NAME]