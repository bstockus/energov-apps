CREATE PROCEDURE [globalsetup].[USP_IMNONCOMPLIANCECATEGORY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[IMNONCOMPLIANCECATEGORY].[IMNONCOMPLIANCECATEGORYID],
	[IMNONCOMPLIANCECATEGORY].[NAME],
	[IMNONCOMPLIANCECATEGORY].[DESCRIPTION],	
	[IMNONCOMPLIANCECATEGORY].[LASTCHANGEDBY],
	[IMNONCOMPLIANCECATEGORY].[LASTCHANGEDON],
	[IMNONCOMPLIANCECATEGORY].[ROWVERSION]
FROM [dbo].[IMNONCOMPLIANCECATEGORY] ORDER BY [IMNONCOMPLIANCECATEGORY].[NAME]

END