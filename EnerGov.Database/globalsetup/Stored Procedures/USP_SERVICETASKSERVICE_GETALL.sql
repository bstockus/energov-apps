CREATE PROCEDURE [globalsetup].[USP_SERVICETASKSERVICE_GETALL]
AS
BEGIN
	SELECT 
		[dbo].[SERVICETASKSERVICE].[SERVICETASKSERVICEID],
		[dbo].[SERVICETASKSERVICE].[SERVICEID],
		[dbo].[SERVICETASKSERVICE].[SERVICETASKID],
		[dbo].[SERVICE].[MACHINENAME],
		[dbo].[SERVICE].[BASEDIRECTORY] ,
		[dbo].[SERVICETASKSERVICE].[INTERVALINMINUTES],
		[dbo].[SERVICETASKSERVICE].[STARTTIME],
		[dbo].[SERVICETASKSERVICE].[ENDTIME],
		[dbo].[SERVICETASKSERVICE].[ISACTIVE],
		[dbo].[SERVICETASKSERVICE].[SERVICETASKID]
	FROM 
		[dbo].[SERVICETASKSERVICE] 
	INNER JOIN 
		[dbo].[SERVICE] ON [dbo].[SERVICE].[SERVICEID] = [dbo].[SERVICETASKSERVICE].[SERVICEID]
	ORDER BY 
		[dbo].[SERVICE].[MACHINENAME] ASC
END