CREATE PROCEDURE [globalsetup].[USP_PLSUBMITTALTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT [dbo].[PLSUBMITTALTYPE].[PLSUBMITTALTYPEID],
	   [dbo].[PLSUBMITTALTYPE].[TYPENAME],
	   [dbo].[PLSUBMITTALTYPE].[PLSUBMITTALSTATUSID],
	   [dbo].[PLSUBMITTALTYPE].[DESCRIPTION],
	   [dbo].[PLSUBMITTALTYPE].[DAYSUNTILDUE],
	   [dbo].[PLSUBMITTALTYPE].[COPYFAILEDITEMREVIEW],
	   [dbo].[PLSUBMITTALTYPE].[RESUMITALDAYSUNTILDUE],
	   [dbo].[PLSUBMITTALTYPE].[LASTCHANGEDON],
	   [dbo].[PLSUBMITTALTYPE].[LASTCHANGEDBY],
	   [dbo].[PLSUBMITTALTYPE].[ROWVERSION]
FROM [dbo].[PLSUBMITTALTYPE]
ORDER BY [dbo].[PLSUBMITTALTYPE].[TYPENAME]	

EXEC [globalsetup].[USP_PLSUBMITTALTYPEITEMREVIEWTYPEX_GETALL]
END