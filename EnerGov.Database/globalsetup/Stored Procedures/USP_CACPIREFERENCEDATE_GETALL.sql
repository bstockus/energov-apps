CREATE PROCEDURE [globalsetup].[USP_CACPIREFERENCEDATE_GETALL]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[CACPIREFERENCEDATE].[CACPIREFERENCEDATEID],
				[dbo].[CACPIREFERENCEDATE].[CAMODULEID],
				[dbo].[CACPIREFERENCEDATE].[DATEOBJECTTYPEID],
				[dbo].[CACPIREFERENCEDATE].[DATEOBJECT],
				[dbo].[CACPIREFERENCEDATE].[DATEOBJECTFRIENDLYNAME],
				[dbo].[CACPIREFERENCEDATE].[ISDEFAULT],
				[dbo].[CACPIREFERENCEDATE].[LASTCHANGEDBY],
				[dbo].[CACPIREFERENCEDATE].[LASTCHANGEDON],
				[dbo].[CACPIREFERENCEDATE].[ROWVERSION],
				[dbo].[CAFEETEMPLATEINPUTTYPE].[NAME]
	FROM		[dbo].[CACPIREFERENCEDATE]
	INNER JOIN  [dbo].[CAFEETEMPLATEINPUTTYPE] ON [dbo].[CAFEETEMPLATEINPUTTYPE].[CAFEETEMPLATEINPUTTYPEID] = [dbo].[CACPIREFERENCEDATE].[DATEOBJECTTYPEID]
	ORDER BY	
				[dbo].[CAFEETEMPLATEINPUTTYPE].[NAME]

END