CREATE PROCEDURE [globalsetup].[USP_BONDSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BONDSTATUS].[BONDSTATUSID],
	[dbo].[BONDSTATUS].[NAME],
	[dbo].[BONDSTATUS].[DESCRIPTION],
	[dbo].[BONDSTATUS].[SUCCESSFLAG],
	[dbo].[BONDSTATUS].[FAILUREFLAG],
	[dbo].[BONDSTATUS].[CANCELLEDFLAG],
	[dbo].[BONDSTATUS].[LASTCHANGEDBY],
	[dbo].[BONDSTATUS].[LASTCHANGEDON],
	[dbo].[BONDSTATUS].[ROWVERSION]
FROM [dbo].[BONDSTATUS]
ORDER BY
	[dbo].[BONDSTATUS].[NAME] ASC
END