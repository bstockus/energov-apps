CREATE PROCEDURE [globalsetup].[USP_CADISCOUNTSETUP_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CADISCOUNTSETUP].[CADISCOUNTSETUPID],
	[dbo].[CADISCOUNTSETUP].[CADISCOUNTID],
	[dbo].[CADISCOUNTSETUP].[CASCHEDULEID],
	[dbo].[CADISCOUNTSETUP].[AMOUNT],
	[dbo].[CADISCOUNTSETUP].[MINIMUMAMOUNT],
	[dbo].[CADISCOUNTSETUP].[MAXIMUMAMOUNT],
	[dbo].[CADISCOUNTSETUP].[COMPUTATIONVALUE],
	[dbo].[CADISCOUNTSETUP].[COMPUTATIONVALUENAME],
	[dbo].[CADISCOUNTSETUP].[ROUNDINGTYPEID],
	[dbo].[CADISCOUNTSETUP].[ROUNDINGVALUE],
	[dbo].[CADISCOUNTSETUP].[ROWVERSION],
	[dbo].[CADISCOUNTSETUP].[LASTCHANGEDBY],
	[dbo].[CADISCOUNTSETUP].[LASTCHANGEDON]
FROM [dbo].[CADISCOUNTSETUP]
WHERE
	[dbo].[CADISCOUNTSETUP].[CADISCOUNTID] = @PARENTID  

END