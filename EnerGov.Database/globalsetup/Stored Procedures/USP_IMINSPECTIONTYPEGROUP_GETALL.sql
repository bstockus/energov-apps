CREATE PROCEDURE [globalsetup].[USP_IMINSPECTIONTYPEGROUP_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMINSPECTIONTYPEGROUP].[IMINSPECTIONTYPEGROUPID],
	[dbo].[IMINSPECTIONTYPEGROUP].[NAME],
	[dbo].[IMINSPECTIONTYPEGROUP].[DESCRIPTION],
	[dbo].[IMINSPECTIONTYPEGROUP].[IVRNUMBER],
	[dbo].[IMINSPECTIONTYPEGROUP].[GROUPLIMITATION],
	[dbo].[IMINSPECTIONTYPEGROUP].[IMINSPECTIONSYSTEMTYPEID],
	[dbo].[IMINSPECTIONTYPEGROUP].[LASTCHANGEDBY],
	[dbo].[IMINSPECTIONTYPEGROUP].[LASTCHANGEDON],
	[dbo].[IMINSPECTIONTYPEGROUP].[ROWVERSION]
FROM [dbo].[IMINSPECTIONTYPEGROUP]
ORDER BY [dbo].[IMINSPECTIONTYPEGROUP].[NAME] ASC
END