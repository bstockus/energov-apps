CREATE PROCEDURE [globalsetup].[USP_LANDMANAGEMENTCONTACTTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[LANDMANAGEMENTCONTACTTYPEID],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[NAME],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[DESCRIPTION],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[LANDMANAGEMENTCONTACTSYSTEMTYP],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[ISVALIDATELIC],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[ISVALIDATEBLLICENSE],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[ISVALIDATEILLICENSE],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[EPORTALSEARCHCRITERIAID],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[LASTCHANGEDBY],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[LASTCHANGEDON],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[ROWVERSION]
FROM [dbo].[LANDMANAGEMENTCONTACTTYPE]
ORDER BY
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[NAME]
END