CREATE PROCEDURE [globalsetup].[USP_IMINSPECTIONCASESTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[IMINSPECTIONCASESTATUS].[IMINSPECTIONCASESTATUSID],
	[IMINSPECTIONCASESTATUS].[NAME],
	[IMINSPECTIONCASESTATUS].[DESCRIPTION],
	[IMINSPECTIONCASESTATUS].[ACTIVE],
	[IMINSPECTIONCASESTATUS].[PAUSE],
	[IMINSPECTIONCASESTATUS].[COMPLETE],
	[IMINSPECTIONCASESTATUS].[LASTCHANGEDBY],
	[IMINSPECTIONCASESTATUS].[LASTCHANGEDON],
	[IMINSPECTIONCASESTATUS].[ROWVERSION]
FROM [dbo].[IMINSPECTIONCASESTATUS] ORDER BY [IMINSPECTIONCASESTATUS].[NAME]

END