CREATE PROCEDURE [globalsetup].[USP_IMINSPECTIONSTATUSENTITY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMINSPECTIONSTATUSENTITY].[IMINSPECTIONSTATUSENTITYID],
	[dbo].[IMINSPECTIONSTATUSENTITY].[NAME],
	[dbo].[IMINSPECTIONSTATUSENTITY].[IMINSPECTIONLINKID],
	[dbo].[IMINSPECTIONSTATUSENTITY].[LASTCHANGEDBY],
	[dbo].[IMINSPECTIONSTATUSENTITY].[LASTCHANGEDON],
	[dbo].[IMINSPECTIONSTATUSENTITY].[ROWVERSION]
FROM [dbo].[IMINSPECTIONSTATUSENTITY]
ORDER BY [dbo].[IMINSPECTIONSTATUSENTITY].[NAME]

EXEC [globalsetup].[USP_IMINSPECTIONSTATUS_GETALL]

END