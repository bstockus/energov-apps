CREATE PROCEDURE [globalsetup].[USP_BONDINTERESTRATE_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BONDINTERESTRATE].[BONDINTERESTRATEID],
	[dbo].[BONDINTERESTRATE].[CAPAYMENTMETHODID],
	[dbo].[BONDINTERESTRATE].[BONDINTERESTSCHEDULEID],
	[dbo].[BONDINTERESTRATE].[STARTDATE],
	[dbo].[BONDINTERESTRATE].[ENDDATE],
	[dbo].[BONDINTERESTRATE].[INTERESTRATE],
	[dbo].[BONDINTERESTRATE].[ACTIVE],
	[dbo].[CAPAYMENTMETHOD].[NAME] AS [PAYMENTMETHODNAME]
FROM [dbo].[BONDINTERESTRATE]
JOIN [dbo].[CAPAYMENTMETHOD] ON [dbo].[CAPAYMENTMETHOD].[CAPAYMENTMETHODID] = [dbo].[BONDINTERESTRATE].[CAPAYMENTMETHODID]
WHERE
	[dbo].[BONDINTERESTRATE].[BONDINTERESTSCHEDULEID] = @PARENTID  
END