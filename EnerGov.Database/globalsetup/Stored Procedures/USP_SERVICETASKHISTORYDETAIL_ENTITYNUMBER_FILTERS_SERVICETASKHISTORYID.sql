CREATE PROCEDURE [globalsetup].[USP_SERVICETASKHISTORYDETAIL_ENTITYNUMBER_FILTERS_SERVICETASKHISTORYID]
(
	@SERVICETASKHISTORYID	CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;

	SELECT DISTINCT	[dbo].[SERVICETASKHISTORYDETAIL].[ENTITYNUMBER]
	FROM 
		[dbo].[SERVICETASKHISTORYDETAIL]
	WHERE
		[dbo].[SERVICETASKHISTORYDETAIL].[SERVICETASKHISTORYID] = @SERVICETASKHISTORYID

END