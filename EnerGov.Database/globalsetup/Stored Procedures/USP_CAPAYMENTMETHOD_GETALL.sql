CREATE PROCEDURE [globalsetup].[USP_CAPAYMENTMETHOD_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CAPAYMENTMETHOD].[CAPAYMENTMETHODID],
	[dbo].[CAPAYMENTMETHOD].[CAPAYMENTTYPEID],
	[dbo].[CAPAYMENTMETHOD].[NAME],
	[dbo].[CAPAYMENTMETHOD].[DESCRIPTION],
	[dbo].[CAPAYMENTMETHOD].[ISACTIVE],
	[dbo].[CAPAYMENTMETHOD].[REQUIRESSUPPLIMENTALDATA],
	[dbo].[CAPAYMENTMETHOD].[REQUIRESCOUNTDOWN],
	[dbo].[CAPAYMENTMETHOD].[ISREFUNDTYPE],
	[dbo].[CAPAYMENTMETHOD].[SUPPLIMENTALDATANAME],
	[dbo].[CAPAYMENTMETHOD].[SUPPLIMENTALDATADESCRIPTION],
	[dbo].[CAPAYMENTMETHOD].[ISSYSTEMMETHOD],
	[dbo].[CAPAYMENTMETHOD].[ISBONDRELEASEMETHOD],
	[dbo].[CAPAYMENTMETHOD].[INCLUDEBONDRELEASEINTILL],
	[dbo].[CAPAYMENTMETHOD].[INCLUDEREFUNDINTILLSESSION],
	[dbo].[CAPAYMENTMETHOD].[FEEWAIVER],
	[dbo].[CAPAYMENTMETHOD].[LASTCHANGEDBY],
	[dbo].[CAPAYMENTMETHOD].[LASTCHANGEDON],
	[dbo].[CAPAYMENTMETHOD].[ROWVERSION],
	[dbo].[CAPAYMENTMETHOD].[TENDERTYPEID]
FROM [dbo].[CAPAYMENTMETHOD]
ORDER BY [dbo].[CAPAYMENTMETHOD].[NAME] ASC
END