CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELDLAYOUTCONTROLTYPE_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
			[dbo].[CUSTOMFIELDLAYOUTCONTROLTYPE].[ICUSTOMFIELDLAYOUTCONTROLTYPE],
			[dbo].[CUSTOMFIELDLAYOUTCONTROLTYPE].[SNAME]
		FROM [dbo].[CUSTOMFIELDLAYOUTCONTROLTYPE]
		ORDER BY [dbo].[CUSTOMFIELDLAYOUTCONTROLTYPE].[SNAME]
-- NOTE: BHASDATA detail is intentionally not loaded here. This value would be derived from helper class based on Control Type
END