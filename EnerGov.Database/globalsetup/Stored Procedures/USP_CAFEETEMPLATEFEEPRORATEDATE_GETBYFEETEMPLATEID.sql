CREATE PROCEDURE [globalsetup].[USP_CAFEETEMPLATEFEEPRORATEDATE_GETBYFEETEMPLATEID]
		@CAFEETEMPLATEID CHAR(36)
AS
BEGIN
SET NOCOUNT ON;

	SELECT	[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[CAFEETEMPLATEFEEPRORATEDATEID],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[CAFEETEMPLATEFEEID],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[STARTOBJECTTYPEID],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[STARTOBJECT],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[STARTOBJECTFRIENDLYNAME],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[ENDOBJECTTYPEID],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[ENDOBJECT],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[ENDOBJECTFRIENDLYNAME],
			[dbo].[CAFEETEMPLATEFEEPRORATEDATE].[DAYSTOPRORATE]
	FROM [dbo].[CAFEETEMPLATEFEEPRORATEDATE]
	INNER JOIN [dbo].[CAFEETEMPLATEFEE] ON [dbo].[CAFEETEMPLATEFEE].[CAFEETEMPLATEFEEID] = [dbo].[CAFEETEMPLATEFEEPRORATEDATE].[CAFEETEMPLATEFEEID]
	WHERE [dbo].[CAFEETEMPLATEFEE].[CAFEETEMPLATEID] = @CAFEETEMPLATEID

END