CREATE PROCEDURE [globalsetup].[USP_IMINSPECTIONCASETYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMINSPECTIONCASETYPE].[IMINSPECTIONCASETYPEID],
	[dbo].[IMINSPECTIONCASETYPE].[NAME],
	[dbo].[IMINSPECTIONCASETYPE].[DESCRIPTION],
	[dbo].[IMINSPECTIONCASETYPE].[IMINSPECTIONTYPEID],
	[dbo].[IMINSPECTIONCASETYPE].[ACTIVE],
	[dbo].[IMINSPECTIONCASETYPE].[RECURRENCELIMITTYPE],
	[dbo].[IMINSPECTIONCASETYPE].[IMINSPECTIONCASESTATUSID],
	[dbo].[IMINSPECTIONCASETYPE].[RECURRENCELIMIT],
	[dbo].[IMINSPECTIONCASETYPE].[PREFIX],
	[dbo].[IMINSPECTIONCASETYPE].[CUSTOMFIELDLAYOUTID],
	[dbo].[IMINSPECTIONCASETYPE].[RECURRENCEID],
	[dbo].[IMINSPECTIONCASETYPE].[ASSIGNINSPECTORVIAGIS],
	[dbo].[IMINSPECTIONCASETYPE].[NOTSCHEDULEINSPECTION],
	[dbo].[IMINSPECTIONCASETYPE].[LASTCHANGEDBY],
	[dbo].[IMINSPECTIONCASETYPE].[LASTCHANGEDON],
	[dbo].[IMINSPECTIONCASETYPE].[ROWVERSION],
	[dbo].[RECURRENCE].[NAME] AS [RECURRENCENAME]
FROM [dbo].[IMINSPECTIONCASETYPE] 
LEFT JOIN [dbo].[RECURRENCE] ON [dbo].[IMINSPECTIONCASETYPE].[RECURRENCEID] = [dbo].[RECURRENCE].[RECURRENCEID]
ORDER BY [dbo].[IMINSPECTIONCASETYPE].[NAME]

END