CREATE PROCEDURE [globalsetup].[USP_SERVICETASKHISTORYACTION_GETBYCRITERIA]
(
	@SERVICETASKHISTORYDETAILID	CHAR(36),
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1,
	@RESULTFILTER AS [dbo].[RecordNames] READONLY,
	@ACTIONTYPEFILTER AS [dbo].[RecordNames] READONLY
)
AS
BEGIN
	SET NOCOUNT ON;
	
		WITH RAW_DATA AS (

SELECT [dbo].[SERVICETASKHISTORYACTION].[SERVICETASKHISTORYACTIONID],
		[dbo].[SERVICETASKHISTORYACTION].[SERVICETASKHISTORYDETAILID],		
		[dbo].[SERVICETASKHISTORYACTION].[STARTTIME],
		[dbo].[SERVICETASKHISTORYACTION].[ENDTIME],
		[dbo].[SERVICETASKHISTORYACTION].[ACTIONTYPE],
		[dbo].[SERVICETASKHISTORYACTION].[ADDITIONALINFO],
		[dbo].[SERVICETASKHISTORYACTION].[RESULT],
		[dbo].[SERVICETASKHISTORYACTION].[ERROR],
	   CASE @IS_ASCENDING WHEN 1 THEN	
		ROW_NUMBER() OVER(ORDER BY [dbo].[SERVICETASKHISTORYACTION].[ENDTIME])
	   ELSE 
	    ROW_NUMBER() OVER(ORDER BY [dbo].[SERVICETASKHISTORYACTION].[ENDTIME] DESC)
	   END AS RowNumber,
	   COUNT(1) OVER() AS TotalRows
	FROM [dbo].[SERVICETASKHISTORYACTION]
	WHERE 
		[dbo].[SERVICETASKHISTORYACTION].[SERVICETASKHISTORYDETAILID] = @SERVICETASKHISTORYDETAILID
	AND
		((SELECT COUNT(1) FROM @RESULTFILTER) = 0 OR COALESCE([SERVICETASKHISTORYACTION].[RESULT],'') IN (SELECT Name FROM @RESULTFILTER))
	AND
		((SELECT COUNT(1) FROM @ACTIONTYPEFILTER) = 0 OR [SERVICETASKHISTORYACTION].[ACTIONTYPE] IN (SELECT Name FROM @ACTIONTYPEFILTER))
	)

SELECT * 
	FROM RAW_DATA
	WHERE
		RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
		RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
	ORDER BY 
		RowNumber
END