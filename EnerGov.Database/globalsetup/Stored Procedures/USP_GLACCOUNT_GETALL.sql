CREATE PROCEDURE [globalsetup].[USP_GLACCOUNT_GETALL]
AS
BEGIN
	SELECT [dbo].[GLACCOUNT].[GLACCOUNTID],
			[dbo].[GLACCOUNT].[NAME],			
			[dbo].[GLACCOUNT].[DESCRIPTION],
			[dbo].[GLACCOUNT].[ACCOUNTNUMBER],
			[dbo].[GLACCOUNT].[ACTIVE],
			[dbo].[GLACCOUNT].[LASTCHANGEDBY],
			[dbo].[GLACCOUNT].[LASTCHANGEDON],
			[dbo].[GLACCOUNT].[ROWVERSION],
			[dbo].[GLACCOUNT].[DYNAMICACCOUNT],
			[dbo].[GLACCOUNT].[DYNAMICACCOUNTSQLFUNCTIONNAME]
	FROM [dbo].[GLACCOUNT] ORDER BY [dbo].[GLACCOUNT].[NAME] ASC
END