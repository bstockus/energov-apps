CREATE PROCEDURE [globalsetup].[USP_OFFICE_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
WITH RAW_DATA AS (
	SELECT
		[dbo].[OFFICE].[OFFICEID],
		[dbo].[OFFICE].[NAME],
		[dbo].[OFFICE].[PHONE],
		[dbo].[OFFICE].[ROWVERSION],
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[OFFICE].[NAME],[dbo].[OFFICE].[PHONE])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[OFFICE].[NAME],[dbo].[OFFICE].[PHONE] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[OFFICE]
	WHERE [dbo].[OFFICE].[NAME] LIKE '%'+ @SEARCH +'%' OR [dbo].[OFFICE].[PHONE] LIKE '%'+ @SEARCH +'%' 
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber