CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELDTABLE_GETBYID]
(
	@CUSTOMFIELDTABLEID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
		[dbo].[CUSTOMFIELDTABLE].[CUSTOMFIELDTABLEID],
		[dbo].[CUSTOMFIELDTABLE].[NAME],
		[dbo].[CUSTOMFIELDTABLE].[REQUIRED],
		[dbo].[CUSTOMFIELDTABLE].[DEFAULTROWNUM],
		[dbo].[CUSTOMFIELDTABLE].[TOOLTIP],
		[dbo].[CUSTOMFIELDTABLE].[SHOWONMOBILE],
		[dbo].[CUSTOMFIELDTABLE].[ISEDITINMOBILE],
		[dbo].[CUSTOMFIELDTABLE].[LASTCHANGEDBY],
		[dbo].[CUSTOMFIELDTABLE].[LASTCHANGEDON],
		[dbo].[CUSTOMFIELDTABLE].[ROWVERSION]
	FROM [dbo].[CUSTOMFIELDTABLE]
	WHERE [dbo].[CUSTOMFIELDTABLE].[CUSTOMFIELDTABLEID] = @CUSTOMFIELDTABLEID

	EXEC [globalsetup].[USP_CUSTOMFIELDTABLECOLUMNREF_GETBYPARENTID] @CUSTOMFIELDTABLEID
	EXEC [globalsetup].[USP_CUSTOMFIELD_GETBYCUSTOMFIELDTABLEID] @CUSTOMFIELDTABLEID
END