CREATE PROCEDURE [globalsetup].[USP_CADISCOUNT_GETBYID]
(
	@CADISCOUNTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CADISCOUNT].[CADISCOUNTID],
	[dbo].[CADISCOUNT].[CACOMPUTATIONTYPEID],
	[dbo].[CADISCOUNT].[NAME],
	[dbo].[CADISCOUNT].[DESCRIPTION],
	[dbo].[CADISCOUNT].[ACTIVE],
	[dbo].[CADISCOUNT].[ROWVERSION],
	[dbo].[CADISCOUNT].[LASTCHANGEDBY],
	[dbo].[CADISCOUNT].[LASTCHANGEDON]
FROM [dbo].[CADISCOUNT]
WHERE
	[dbo].[CADISCOUNT].[CADISCOUNTID] = @CADISCOUNTID  

	EXEC [globalsetup].[USP_CADISCOUNTSETUP_GETBYPARENTID] @CADISCOUNTID
	EXEC [globalsetup].[USP_CAMODULEDISCOUNTXREF_GETBYPARENTID] @CADISCOUNTID
	EXEC [globalsetup].[USP_CADISCOUNTADJUSTABLECALC_GETBYPARENTID] @CADISCOUNTID

END