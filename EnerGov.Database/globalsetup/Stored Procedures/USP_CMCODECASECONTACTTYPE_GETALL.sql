CREATE PROCEDURE [globalsetup].[USP_CMCODECASECONTACTTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CMCODECASECONTACTTYPE].[CMCODECASECONTACTTYPEID],
	[dbo].[CMCODECASECONTACTTYPE].[NAME],
	[dbo].[CMCODECASECONTACTTYPE].[DESCRIPTION],
	[dbo].[CMCODECASECONTACTTYPE].[CMCONTACTTYPESYSTEMID],
	[dbo].[CMCODECASECONTACTTYPE].[LASTCHANGEDBY],
	[dbo].[CMCODECASECONTACTTYPE].[LASTCHANGEDON],
	[dbo].[CMCODECASECONTACTTYPE].[ROWVERSION]
FROM [dbo].[CMCODECASECONTACTTYPE]
	ORDER BY [dbo].[CMCODECASECONTACTTYPE].[NAME]
END