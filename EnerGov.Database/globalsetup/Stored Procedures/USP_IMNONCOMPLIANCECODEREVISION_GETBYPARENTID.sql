CREATE PROCEDURE [globalsetup].[USP_IMNONCOMPLIANCECODEREVISION_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMNONCOMPLIANCECODEREVISION].[IMNONCOMPLIANCECODEREVISIONID],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[IMNONCOMPLIANCECODEID],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[CODETEXT],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[DEFAULTCOMMENTS],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[REVISIONDATE],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[CURRENTREVISION],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[STARTDATE],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[ENDDATE],
	[dbo].[IMNONCOMPLIANCECODEREVISION].[REVISIONNUMBER]
FROM [dbo].[IMNONCOMPLIANCECODEREVISION]
WHERE
	[dbo].[IMNONCOMPLIANCECODEREVISION].[IMNONCOMPLIANCECODEID] = @PARENTID  

END