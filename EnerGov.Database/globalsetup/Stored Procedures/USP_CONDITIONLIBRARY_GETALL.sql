CREATE PROCEDURE [globalsetup].[USP_CONDITIONLIBRARY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CONDITIONLIBRARY].[CONDITIONLIBRARYID],
	[dbo].[CONDITIONLIBRARY].[CONDITIONCATEGORYID],
	[dbo].[CONDITIONLIBRARY].[NAME],
	[dbo].[CONDITIONLIBRARY].[DESCRIPTION],
	[dbo].[CONDITIONLIBRARY].[LASTCHANGEDBY],
	[dbo].[CONDITIONLIBRARY].[LASTCHANGEDON],
	[dbo].[CONDITIONLIBRARY].[ROWVERSION]
FROM [dbo].[CONDITIONLIBRARY]
 ORDER BY [dbo].[CONDITIONLIBRARY].[NAME] ASC
END