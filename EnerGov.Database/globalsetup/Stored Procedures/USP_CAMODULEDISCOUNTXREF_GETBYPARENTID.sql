CREATE PROCEDURE [globalsetup].[USP_CAMODULEDISCOUNTXREF_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CAMODULEDISCOUNTXREF].[CAMODULEDISCOUNTXREFID],
	[dbo].[CAMODULEDISCOUNTXREF].[CAMODULEID],
	[dbo].[CAMODULEDISCOUNTXREF].[CADISCOUNTID]
FROM [dbo].[CAMODULEDISCOUNTXREF]
WHERE
	[dbo].[CAMODULEDISCOUNTXREF].[CADISCOUNTID] = @PARENTID  

END