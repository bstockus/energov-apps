CREATE PROCEDURE [globalsetup].[USP_WFSTEP_GETBYID]
(
	@WFSTEPID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
		[dbo].[WFSTEP].[WFSTEPID],
		[dbo].[WFSTEP].[NAME],
		[dbo].[WFSTEP].[DESCRIPTION],
		[dbo].[WFSTEP].[WFSTEPTYPEID],
		[dbo].[WFSTEP].[DAYSTOCOMPLETE],
		[dbo].[WFSTEP].[WORKFLOWCOMPLETETYPEID],
		[dbo].[WFSTEP].[CMCODEID],
		[dbo].[WFSTEP].[LASTCHANGEDBY],
		[dbo].[WFSTEP].[LASTCHANGEDON],
		[dbo].[WFSTEP].[ROWVERSION],
		[dbo].[CMCODE].[CODENUMBER]
FROM [dbo].[WFSTEP]
LEFT JOIN [dbo].[CMCODE] on [dbo].[CMCODE].[CMCODEID] = [dbo].[WFSTEP].[CMCODEID]
WHERE
	[WFSTEPID] = @WFSTEPID
END