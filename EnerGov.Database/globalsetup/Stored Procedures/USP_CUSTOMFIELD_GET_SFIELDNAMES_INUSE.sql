CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELD_GET_SFIELDNAMES_INUSE]  
(   
 @SFIELDNAMES [dbo].[RecordNames] READONLY
 )  
AS 
BEGIN
SET NOCOUNT ON;  
	SELECT 
		 [CUSTOMFIELD].[GCUSTOMFIELD],
		 [CUSTOMFIELD].[SFIELDNAME]  
	FROM [dbo].[CUSTOMFIELD] 
		WHERE [dbo].[CUSTOMFIELD].[SFIELDNAME] IN (SELECT [NAME] From  @SFIELDNAMES)
END