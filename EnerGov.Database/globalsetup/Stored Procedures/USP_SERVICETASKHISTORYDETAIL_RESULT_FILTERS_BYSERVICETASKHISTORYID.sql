CREATE PROCEDURE [globalsetup].[USP_SERVICETASKHISTORYDETAIL_RESULT_FILTERS_BYSERVICETASKHISTORYID]
(
	@SERVICETASKHISTORYID	CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;

	SELECT DISTINCT	[dbo].[SERVICETASKHISTORYDETAIL].[RESULT]
	FROM 
		[dbo].[SERVICETASKHISTORYDETAIL]
	WHERE
		[dbo].[SERVICETASKHISTORYDETAIL].[SERVICETASKHISTORYID] = @SERVICETASKHISTORYID

END