CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELDMODULES_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT 
	[dbo].[CUSTOMFIELDMODULES].[CUSTOMFIELDMODULEID],
	[dbo].[CUSTOMFIELDMODULES].[MODULENAME],
	[dbo].[CUSTOMFIELDMODULES].[ISHTMLDEPRICATED]
FROM [dbo].[CUSTOMFIELDMODULES]
ORDER BY [dbo].[CUSTOMFIELDMODULES].[MODULENAME]