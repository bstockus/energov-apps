CREATE PROCEDURE [globalsetup].[USP_IMSCHEDULEDTIMEGROUP_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMSCHEDULEDTIMEGROUP].[IMSCHEDULEDTIMEGROUPID],
	[dbo].[IMSCHEDULEDTIMEGROUP].[NAME],
	[dbo].[IMSCHEDULEDTIMEGROUP].[STARTTIME],
	[dbo].[IMSCHEDULEDTIMEGROUP].[ENDTIME],
	[dbo].[IMSCHEDULEDTIMEGROUP].[IVRNUMBER],
	[dbo].[IMSCHEDULEDTIMEGROUP].[LASTCHANGEDBY],
	[dbo].[IMSCHEDULEDTIMEGROUP].[LASTCHANGEDON],
	[dbo].[IMSCHEDULEDTIMEGROUP].[ROWVERSION]
FROM [dbo].[IMSCHEDULEDTIMEGROUP]
ORDER BY
	[dbo].[IMSCHEDULEDTIMEGROUP].[NAME]  
END