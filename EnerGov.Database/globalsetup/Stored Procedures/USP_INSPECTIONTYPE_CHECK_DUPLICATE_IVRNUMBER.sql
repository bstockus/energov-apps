CREATE PROCEDURE [globalsetup].[USP_INSPECTIONTYPE_CHECK_DUPLICATE_IVRNUMBER]
(	
	@INSPECTIONTYPEID AS CHAR(36),
	@IVRNUMBER AS NVARCHAR(5)
)
AS
SELECT
	COUNT(*) AS RecordCount		
FROM 
	[dbo].[IMINSPECTIONTYPE]	
WHERE 
	([dbo].[IMINSPECTIONTYPE].[IMINSPECTIONTYPEID] != @INSPECTIONTYPEID) AND 
	([dbo].[IMINSPECTIONTYPE].[IVRNUMBER] = @IVRNUMBER)