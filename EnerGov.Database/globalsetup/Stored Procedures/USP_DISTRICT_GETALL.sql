CREATE PROCEDURE [globalsetup].[USP_DISTRICT_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[DISTRICT].[DISTRICTID],
	[dbo].[DISTRICT].[NAME],
	[dbo].[DISTRICT].[DESCRIPTION],
	[dbo].[DISTRICT].[LASTCHANGEDBY],
	[dbo].[DISTRICT].[LASTCHANGEDON],
	[dbo].[DISTRICT].[ROWVERSION]
FROM [dbo].[DISTRICT] ORDER BY [dbo].[DISTRICT].[NAME] ASC
END