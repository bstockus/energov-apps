CREATE PROCEDURE [globalsetup].[USP_WFTEMPLATE_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
SET NOCOUNT ON;
WITH RAW_DATA AS (
	SELECT
		[dbo].[WFTEMPLATE].[WFTEMPLATEID],
		[dbo].[WFTEMPLATE].[NAME],
		[dbo].[WFTEMPLATE].[DESCRIPTION],
		[dbo].[WFENTITY].[NAME] as ENTITYNAME,
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[WFTEMPLATE].[NAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[WFTEMPLATE].[NAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[WFTEMPLATE]	
	INNER JOIN [dbo].[WFENTITY] on [dbo].[WFENTITY].[WFENTITYID] = [dbo].[WFTEMPLATE].[WFENTITYID]
	WHERE [dbo].[WFTEMPLATE].[NAME] LIKE '%'+ @SEARCH +'%'
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber