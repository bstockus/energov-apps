CREATE PROCEDURE [globalsetup].[USP_DEPARTMENT_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[DEPARTMENT].[DEPARTMENTID],
	[dbo].[DEPARTMENT].[NAME],
	[dbo].[DEPARTMENT].[LASTCHANGEDBY],
	[dbo].[DEPARTMENT].[LASTCHANGEDON],
	[dbo].[DEPARTMENT].[ROWVERSION]
FROM [dbo].[DEPARTMENT]
	ORDER BY [dbo].[DEPARTMENT].[NAME] ASC
END