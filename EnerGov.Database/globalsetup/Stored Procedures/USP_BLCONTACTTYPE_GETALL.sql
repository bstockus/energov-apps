CREATE PROCEDURE [globalsetup].[USP_BLCONTACTTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BLCONTACTTYPE].[BLCONTACTTYPEID],
	[dbo].[BLCONTACTTYPE].[NAME],
	[dbo].[BLCONTACTTYPE].[DESCRIPTION],
	[dbo].[BLCONTACTTYPE].[BLCONTACTTYPESYSTEMID],
	[dbo].[BLCONTACTTYPE].[REQUIREDVALIDLIC],
	[dbo].[BLCONTACTTYPE].[LASTCHANGEDBY],
	[dbo].[BLCONTACTTYPE].[LASTCHANGEDON],
	[dbo].[BLCONTACTTYPE].[ROWVERSION]
FROM [dbo].[BLCONTACTTYPE]
 ORDER BY [dbo].[BLCONTACTTYPE].[NAME] ASC
END