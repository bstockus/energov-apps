CREATE PROCEDURE [globalsetup].[USP_IMINSPECTIONCATEGORYXREF_GETALL] 
AS
BEGIN
  SET NOCOUNT ON;
  SELECT
    [dbo].[IMINSPECTIONCATEGORYXREF].[IMINSPECTIONCATEGORYXREFID],
    [dbo].[IMINSPECTIONCATEGORYXREF].[IMINSPECTIONCATEGORYID],
    [dbo].[IMINSPECTIONCATEGORYXREF].[IMINSPECTIONTYPEGROUPID],
	[dbo].[IMINSPECTIONTYPEGROUP].[NAME]
  FROM [dbo].[IMINSPECTIONCATEGORYXREF]
  INNER JOIN [dbo].[IMINSPECTIONTYPEGROUP]
    ON [dbo].[IMINSPECTIONTYPEGROUP].[IMINSPECTIONTYPEGROUPID] = [dbo].[IMINSPECTIONCATEGORYXREF].[IMINSPECTIONTYPEGROUPID]
  ORDER BY [dbo].[IMINSPECTIONTYPEGROUP].[NAME]
END