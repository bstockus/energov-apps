CREATE PROCEDURE [globalsetup].[USP_CACOMPOUNDINGINTERESTREFERENCEDATE_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT  [dbo].[CACOMPOUNDINGINTERESTREFERENCEDATE].[CACOMPOUNDINGINTERESTREFERENCEDATEID],
			[dbo].[CACOMPOUNDINGINTERESTREFERENCEDATE].[DATEOBJECTFRIENDLYNAME],
			[dbo].[CACOMPOUNDINGINTERESTREFERENCEDATE].[CAMODULEID]
	FROM [dbo].[CACOMPOUNDINGINTERESTREFERENCEDATE] 
	ORDER BY 
		[dbo].[CACOMPOUNDINGINTERESTREFERENCEDATE].[DATEOBJECTFRIENDLYNAME]
END