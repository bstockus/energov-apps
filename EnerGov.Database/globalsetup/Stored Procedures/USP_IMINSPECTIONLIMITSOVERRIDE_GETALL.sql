CREATE PROCEDURE [globalsetup].[USP_IMINSPECTIONLIMITSOVERRIDE_GETALL]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[IMINSPECTIONLIMITSOVERRIDE].[IMINSPECTIONLIMITSOVERRIDEID],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[IMINSPECTIONTYPEGROUPID],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[IMINSPECTIONCATEGORYID],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[OVERRIDESTARTDATE],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[OVERRIDEENDDATE],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[NEWLIMITVALUE],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[LASTCHANGEDBY],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[LASTCHANGEDON],
				[dbo].[IMINSPECTIONLIMITSOVERRIDE].[ROWVERSION]
	FROM		[dbo].[IMINSPECTIONLIMITSOVERRIDE]
	ORDER BY	[dbo].[IMINSPECTIONLIMITSOVERRIDE].[OVERRIDESTARTDATE] ASC
END