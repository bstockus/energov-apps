CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELDTABLE_CHECK_DUPLICATE_NAME]  
(  
 @CUSTOMFIELDTABLEID CHAR(36),  
 @NAME NVARCHAR(50)  
)  
AS 
BEGIN 
DECLARE @ISCUSTOMFIELDTABLENAMEINUSE BIT = 0;
SET NOCOUNT ON;  
 IF EXISTS(SELECT 1 FROM [dbo].[CUSTOMFIELDTABLE] WITH (NOLOCK)
			WHERE [dbo].[CUSTOMFIELDTABLE].[NAME] = @NAME 
			AND [dbo].[CUSTOMFIELDTABLE].[CUSTOMFIELDTABLEID] <> @CUSTOMFIELDTABLEID
		   )
	BEGIN
		SET @ISCUSTOMFIELDTABLENAMEINUSE = 1
	END

	SELECT @ISCUSTOMFIELDTABLENAMEINUSE
 END