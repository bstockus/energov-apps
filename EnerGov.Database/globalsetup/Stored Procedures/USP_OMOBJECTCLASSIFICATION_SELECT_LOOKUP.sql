CREATE PROCEDURE [globalsetup].[USP_OMOBJECTCLASSIFICATION_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT 
	[dbo].[OMOBJECTCLASSIFICATION].[OMOBJECTCLASSIFICATIONID],
	[dbo].[OMOBJECTCLASSIFICATION].[NAME],
	[dbo].[OMOBJECTTYPECLASS].[OMOBJECTTYPEID]
FROM [dbo].[OMOBJECTCLASSIFICATION] INNER JOIN [dbo].[OMOBJECTTYPECLASS] 
ON [dbo].[OMOBJECTCLASSIFICATION].[OMOBJECTCLASSIFICATIONID] = [dbo].[OMOBJECTTYPECLASS].[OMOBJECTCLASSIFICATIONID]
ORDER BY [dbo].[OMOBJECTCLASSIFICATION].[NAME]