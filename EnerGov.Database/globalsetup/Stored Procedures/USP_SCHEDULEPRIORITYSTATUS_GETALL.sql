CREATE PROCEDURE [globalsetup].[USP_SCHEDULEPRIORITYSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[SCHEDULEPRIORITYSTATUS].[SCHEDULEPRIORITYSTATUSID],
	[dbo].[SCHEDULEPRIORITYSTATUS].[NAME],
	[dbo].[SCHEDULEPRIORITYSTATUS].[SYSTEMPRIORITY],
	[dbo].[SCHEDULEPRIORITYSTATUS].[LASTCHANGEDBY],
	[dbo].[SCHEDULEPRIORITYSTATUS].[LASTCHANGEDON],
	[dbo].[SCHEDULEPRIORITYSTATUS].[ROWVERSION]
FROM [dbo].[SCHEDULEPRIORITYSTATUS]
ORDER BY  [dbo].[SCHEDULEPRIORITYSTATUS].[NAME]		ASC

END