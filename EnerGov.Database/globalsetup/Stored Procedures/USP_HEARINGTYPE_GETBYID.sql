CREATE PROCEDURE [globalsetup].[USP_HEARINGTYPE_GETBYID]
(
	@HEARINGTYPEID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
		[dbo].[HEARINGTYPE].[HEARINGTYPEID],
		[dbo].[HEARINGTYPE].[NAME],
		[dbo].[HEARINGTYPE].[DESCRIPTION],
		[dbo].[HEARINGTYPE].[CUSTOMFIELDLAYOUTID],
		[dbo].[HEARINGTYPE].[DEFAULTSUBJECT],
		[dbo].[HEARINGTYPE].[DEFAULTLOCATION],
		[dbo].[HEARINGTYPE].[DEFAULTCOMMENT],
		[dbo].[HEARINGTYPE].[STIME],
		[dbo].[HEARINGTYPE].[ETIME],
		[dbo].[HEARINGTYPE].[RECURRENCEID],
		[dbo].[HEARINGTYPE].[CONTENTLIMIT],
		[dbo].[HEARINGTYPE].[LIMIT],
		[dbo].[HEARINGTYPE].[DEFAULTSTATUSID],
		[dbo].[HEARINGTYPE].[AVAILINCAP],
		[dbo].[HEARINGTYPE].[AVAILINCAPCONTACTS],
		[dbo].[HEARINGTYPE].[LASTCHANGEDBY],
		[dbo].[HEARINGTYPE].[LASTCHANGEDON],
		[dbo].[HEARINGTYPE].[ROWVERSION],
		[dbo].[RECURRENCE].[NAME] AS [RECURRENCENAME]
	FROM [dbo].[HEARINGTYPE]
	LEFT JOIN [dbo].[RECURRENCE] ON [dbo].[HEARINGTYPE].[RECURRENCEID] = [dbo].[RECURRENCE].[RECURRENCEID]
	WHERE
		[dbo].[HEARINGTYPE].[HEARINGTYPEID] = @HEARINGTYPEID

	EXEC [globalsetup].[USP_HEARINGTYPEDATTENDEE_GETBYPARENTID] @HEARINGTYPEID
	EXEC [globalsetup].[USP_HEARINGCASETYPE_GETBYPARENTID] @HEARINGTYPEID
END