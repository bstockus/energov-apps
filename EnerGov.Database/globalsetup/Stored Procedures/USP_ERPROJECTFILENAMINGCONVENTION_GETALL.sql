CREATE PROCEDURE [globalsetup].[USP_ERPROJECTFILENAMINGCONVENTION_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[ERPROJECTFILENAMINGCONVENTION].[ERPROJECTFILENAMINGCONVENTIONID],
	[dbo].[ERPROJECTFILENAMINGCONVENTION].[NAME],
	[dbo].[ERPROJECTFILENAMINGCONVENTION].[DESCRIPTION]
