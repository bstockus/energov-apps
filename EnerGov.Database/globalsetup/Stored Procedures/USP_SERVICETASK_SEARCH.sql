CREATE PROCEDURE [globalsetup].[USP_SERVICETASK_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
BEGIN
WITH RAW_DATA AS (

SELECT [dbo].[SERVICETASK].[SERVICETASKID],
	   [dbo].[SERVICETASK].[NAME],
	   [dbo].[SERVICETASK].[DESCRIPTION],
	   CASE @IS_ASCENDING WHEN 1 THEN	
		ROW_NUMBER() OVER(ORDER BY [dbo].[SERVICETASK].[NAME])
	   ELSE 
	    ROW_NUMBER() OVER(ORDER BY [dbo].[SERVICETASK].[NAME] DESC)
	   END AS RowNumber,
	   COUNT(1) OVER() AS TotalRows
	FROM [dbo].[SERVICETASK]
	WHERE ([dbo].[SERVICETASK].[NAME] LIKE '%'+@SEARCH+'%' ))

SELECT * 
	FROM RAW_DATA
	WHERE
		RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
		RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
	ORDER BY 
		RowNumber
END