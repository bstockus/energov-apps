CREATE PROCEDURE [globalsetup].[USP_CACPITYPE_GETALL]
AS
BEGIN
	SELECT [dbo].[CACPITYPE].[CACPITYPEID],
		  [dbo].[CACPITYPE].[NAME],
		  [dbo].[CACPITYPE].[DESCRIPTION],
		  [dbo].[CACPITYPE].[CACPICALCULATIONTYPEID],
		  [dbo].[CACPITYPE].[LASTCHANGEDBY],
		  [dbo].[CACPITYPE].[LASTCHANGEDON],
		  [dbo].[CACPITYPE].[ROWVERSION],
		  [dbo].[CACPITYPE].[ROUNDEVERYCPICALCULATION]
	  FROM [dbo].[CACPITYPE] ORDER BY [dbo].[CACPITYPE].[NAME] ASC	
	
	EXEC [globalsetup].[USP_CACPITYPEXREF_GETALL] 

	EXEC [globalsetup].[USP_CACPI_GETALL] 
END