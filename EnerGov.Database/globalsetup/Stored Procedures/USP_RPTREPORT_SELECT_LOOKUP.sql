CREATE PROCEDURE [globalsetup].[USP_RPTREPORT_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT 
	[dbo].[RPTREPORT].[RPTREPORTID],
	CASE RPTREPORTTYPEID WHEN 3 THEN [dbo].[RPTREPORT].[FRIENDLYNAME] 
	ELSE [dbo].[RPTREPORT].[REPORTNAME] END,
	[dbo].[RPTREPORT].[RPTFRMTOBUSOBJMAPID]
FROM [dbo].[RPTREPORT] 
JOIN [dbo].[RPTFORMTOBUSINESSOBJECTMAPPING]
ON [dbo].[RPTREPORT].[RPTFRMTOBUSOBJMAPID] = [dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[RPTFRMTOBUSOBJMAPID]
WHERE [dbo].[RPTFORMTOBUSINESSOBJECTMAPPING].[GENRICREPORT] = 0 
ORDER BY [dbo].[RPTREPORT].[REPORTNAME]