CREATE PROCEDURE [globalsetup].[USP_CAFEETEMPLATE_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@ENTITY AS INT = NULL,
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
SET NOCOUNT ON;
WITH RAW_DATA AS (
	SELECT
		[dbo].[CAFEETEMPLATE].[CAFEETEMPLATEID],
		[dbo].[CAFEETEMPLATE].[CAFEETEMPLATENAME],
		[dbo].[CAFEETEMPLATE].[CAFEETEMPLATEDESCRIPTION],
		[dbo].[CAFEETEMPLATE].[CAENTITYID],
		[dbo].[CAENTITY].[NAME] AS ENTITYNAME,
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[CAFEETEMPLATE].[CAFEETEMPLATENAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[CAFEETEMPLATE].[CAFEETEMPLATENAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[CAFEETEMPLATE]	
	JOIN [dbo].[CAENTITY] ON [dbo].[CAFEETEMPLATE].[CAENTITYID] = [dbo].[CAENTITY].[CAENTITYID]
	WHERE ([dbo].[CAFEETEMPLATE].[CAFEETEMPLATENAME] LIKE '%'+ @SEARCH +'%' OR [dbo].[CAFEETEMPLATE].[CAFEETEMPLATEDESCRIPTION] LIKE '%'+ @SEARCH +'%')
		AND (@ENTITY IS NULL OR @ENTITY = [dbo].[CAFEETEMPLATE].[CAENTITYID])
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber