CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELD_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1,
	@CUSTOMMODULEID AS INT,
	@CUSTOMFIELDTYPEID AS INT
)
AS
WITH RAW_DATA AS (
	SELECT [dbo].[CUSTOMFIELD].[GCUSTOMFIELD],
	[dbo].[CUSTOMFIELD].[FKICUSTOMFIELDTYPE],
	[dbo].[CUSTOMFIELD].[SFIELDNAME],
	[dbo].[CUSTOMFIELD].[SCUSTOMFIELD],
	[dbo].[CUSTOMFIELDLAYOUT].[SNAME],
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[CUSTOMFIELD].[SFIELDNAME], [dbo].[CUSTOMFIELD].[SCUSTOMFIELD])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[CUSTOMFIELD].[SFIELDNAME], [dbo].[CUSTOMFIELD].[SCUSTOMFIELD] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[CUSTOMFIELD]
	    LEFT JOIN [dbo].[CUSTOMFIELDOBJECT] ON [dbo].[CUSTOMFIELD].[GCUSTOMFIELD] = [dbo].[CUSTOMFIELDOBJECT].[FKGCUSTOMFIELD]
		LEFT JOIN [dbo].[CUSTOMFIELDLAYOUT] ON [dbo].[CUSTOMFIELDOBJECT].[FKGCUSTOMFIELDLAYOUT] = [dbo].[CUSTOMFIELDLAYOUT].[GCUSTOMFIELDLAYOUTS]
		LEFT JOIN [dbo].[CUSTOMFIELDMODULES] ON [dbo].[CUSTOMFIELDLAYOUT].[CUSTOMFIELDMODULEID] = [dbo].[CUSTOMFIELDMODULES].[CUSTOMFIELDMODULEID]
		JOIN [dbo].[CUSTOMFIELDTYPE] ON [dbo].[CUSTOMFIELD].[FKICUSTOMFIELDTYPE] = [dbo].[CUSTOMFIELDTYPE].[ICUSTOMFIELDTYPE]
	WHERE ([dbo].[CUSTOMFIELD].[SFIELDNAME] like '%' + @SEARCH +'%' OR [dbo].[CUSTOMFIELD].[SCUSTOMFIELD] LIKE '%'+ @SEARCH +'%' OR [dbo].[CUSTOMFIELDLAYOUT].[SNAME] LIKE '%'+ @SEARCH +'%')
		AND
	[dbo].[CUSTOMFIELDMODULES].[CUSTOMFIELDMODULEID] = @CUSTOMMODULEID AND [dbo].[CUSTOMFIELDTYPE].[ICUSTOMFIELDTYPE] = @CUSTOMFIELDTYPEID
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber