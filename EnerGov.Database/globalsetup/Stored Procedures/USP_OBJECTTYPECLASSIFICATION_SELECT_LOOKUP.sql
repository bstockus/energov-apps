CREATE PROCEDURE [globalsetup].[USP_OBJECTTYPECLASSIFICATION_SELECT_LOOKUP]
AS
BEGIN
SET NOCOUNT ON;
SELECT DISTINCT 
		[dbo].[OMOBJECTTYPE].[OMOBJECTTYPEID],
		[dbo].[OMOBJECTTYPE].[NAME] 
FROM [OMOBJECTTYPE] INNER JOIN [dbo].[OMOBJECTTYPECLASS] ON
[OMOBJECTTYPE].[OMOBJECTTYPEID] =[OMOBJECTTYPECLASS].[OMOBJECTTYPEID]
ORDER BY [dbo].[OMOBJECTTYPE].[NAME]

EXEC [globalsetup].[USP_OMOBJECTCLASSIFICATION_SELECT_LOOKUP] 

END