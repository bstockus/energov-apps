CREATE PROCEDURE [globalsetup].[USP_CAFEECONSTANT_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT		[dbo].[CAFEECONSTANT].[CAFEECONSTANTID],
			[dbo].[CAFEECONSTANT].[NAME],
			[dbo].[CAFEECONSTANT].[DESCRIPTION],
			[dbo].[CAFEECONSTANT].[VALUETYPEID]
FROM		[dbo].[CAFEECONSTANT]
ORDER BY	[dbo].[CAFEECONSTANT].[NAME]