CREATE PROCEDURE [globalsetup].[USP_IMINSPECTIONTYPEGROUP_SELECT_LOOKUP]
AS

SET NOCOUNT ON;
SELECT		[dbo].[IMINSPECTIONTYPEGROUP].[IMINSPECTIONTYPEGROUPID],
			[dbo].[IMINSPECTIONTYPEGROUP].[NAME],
			[dbo].[IMINSPECTIONTYPEGROUP].[GROUPLIMITATION]
FROM		[dbo].[IMINSPECTIONTYPEGROUP]
ORDER BY	[dbo].[IMINSPECTIONTYPEGROUP].[NAME]