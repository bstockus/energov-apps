CREATE PROCEDURE [globalsetup].[USP_HEARINGCASETYPE_GETBYPARENTID]
(
@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[HEARINGCASETYPE].[CASETYPEID],
	[dbo].[HEARINGCASETYPE].[MODULEID],
	[dbo].[HEARINGCASETYPE].[TYPEID],
	[dbo].[HEARINGCASETYPE].[WORKCLASSID],
	[dbo].[HEARINGCASETYPE].[LIMIT],
	[dbo].[HEARINGCASETYPE].[HEARINGTYPEID],
	[dbo].[HEARINGCASETYPE].[MODULENAME],
	[dbo].[HEARINGCASETYPE].[TYPENAME],
	[dbo].[HEARINGCASETYPE].[WORKCLASSNAME]
FROM [dbo].[HEARINGCASETYPE]
WHERE [dbo].[HEARINGCASETYPE].[HEARINGTYPEID] = @PARENTID
ORDER BY [dbo].[HEARINGCASETYPE].[TYPENAME]

END