CREATE PROCEDURE [globalsetup].[USP_GISZONEREVIEWITEMMAPPING_SELECT_LOOKUP]
AS

SET NOCOUNT ON;
SELECT		[dbo].[GISZONEREVIEWITEMMAPPING].[GISZONEREVIEWITEMMAPPINGID],
			[dbo].[GISZONEREVIEWITEMMAPPING].[SOURCENAME]
FROM		[dbo].[GISZONEREVIEWITEMMAPPING]
ORDER BY	[dbo].[GISZONEREVIEWITEMMAPPING].[SOURCENAME]