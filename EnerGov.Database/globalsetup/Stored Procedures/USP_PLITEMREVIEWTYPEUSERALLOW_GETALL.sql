CREATE PROCEDURE [globalsetup].[USP_PLITEMREVIEWTYPEUSERALLOW_GETALL]
AS
BEGIN
	SELECT 
		[dbo].[PLITEMREVIEWTYPEUSERALLOW].[PLITEMREVIEWTYPEUSERALLOWID],
		[dbo].[PLITEMREVIEWTYPEUSERALLOW].[PLITEMREVIEWTYPEID],
		[dbo].[PLITEMREVIEWTYPEUSERALLOW].[USERID],
		[dbo].[USERS].[LNAME] + COALESCE(', ' + [dbo].[USERS].[FNAME], '') [Name]
	FROM 
		[dbo].[PLITEMREVIEWTYPEUSERALLOW] 
	INNER JOIN 
		[dbo].[USERS] ON [dbo].[USERS].[SUSERGUID] = [dbo].[PLITEMREVIEWTYPEUSERALLOW].[USERID]
	ORDER BY 
		[dbo].[USERS].[LNAME],[dbo].[USERS].[FNAME] ASC
END