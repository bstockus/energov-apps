CREATE PROCEDURE [globalsetup].[USP_WFTEMPLATE_GETBYID]
(
	@WFTEMPLATEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
		[dbo].[WFTEMPLATE].[WFTEMPLATEID],
		[dbo].[WFTEMPLATE].[NAME],
		[dbo].[WFTEMPLATE].[DESCRIPTION],
		[dbo].[WFTEMPLATE].[WFENTITYID],
		[dbo].[WFTEMPLATE].[LASTCHANGEDBY],
		[dbo].[WFTEMPLATE].[LASTCHANGEDON],
		[dbo].[WFTEMPLATE].[ROWVERSION]
FROM [dbo].[WFTEMPLATE]
WHERE
	[WFTEMPLATEID] = @WFTEMPLATEID

EXEC [globalsetup].[USP_WFTEMPLATESTEP_GETBYPARENTID] @WFTEMPLATEID
EXEC [globalsetup].[USP_WFTEMPLATESTEPACTION_GETBYPARENTID] @WFTEMPLATEID
END