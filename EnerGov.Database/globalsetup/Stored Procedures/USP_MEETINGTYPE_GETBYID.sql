CREATE PROCEDURE [globalsetup].[USP_MEETINGTYPE_GETBYID]
(
	@MEETINGTYPEID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
		[dbo].[MEETINGTYPE].[MEETINGTYPEID],
		[dbo].[MEETINGTYPE].[NAME],
		[dbo].[MEETINGTYPE].[DESCRIPTION],
		[dbo].[MEETINGTYPE].[CUSTOMFIELDLAYOUTID],
		[dbo].[MEETINGTYPE].[DEFAULTSUBJECT],
		[dbo].[MEETINGTYPE].[DEFAULTLOCATION],
		[dbo].[MEETINGTYPE].[DEFAULTCOMMENT],
		[dbo].[MEETINGTYPE].[STIME],
		[dbo].[MEETINGTYPE].[ETIME],
		[dbo].[MEETINGTYPE].[RECURRENCEID],
		[dbo].[MEETINGTYPE].[CONTENTLIMIT],
		[dbo].[MEETINGTYPE].[LIMIT],
		[dbo].[MEETINGTYPE].[AVAILINCAP],
		[dbo].[MEETINGTYPE].[AVAILINCAPCONTACTS],
		[dbo].[MEETINGTYPE].[LASTCHANGEDBY],
		[dbo].[MEETINGTYPE].[LASTCHANGEDON],
		[dbo].[MEETINGTYPE].[ROWVERSION],
		[dbo].[RECURRENCE].[NAME] AS [RECURRENCENAME]
	FROM [dbo].[MEETINGTYPE]
	LEFT JOIN [dbo].[RECURRENCE] ON [dbo].[MEETINGTYPE].[RECURRENCEID] = [dbo].[RECURRENCE].[RECURRENCEID]
	WHERE
		[dbo].[MEETINGTYPE].[MEETINGTYPEID] = @MEETINGTYPEID

	EXEC [globalsetup].[USP_MEETINGTYPEDATTENDEE_GETBYPARENTID] @MEETINGTYPEID
	EXEC [globalsetup].[USP_MEETINGCASETYPE_GETBYPARENTID] @MEETINGTYPEID
END