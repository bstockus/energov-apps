CREATE PROCEDURE [globalsetup].[USP_ERPROJECTFILECATEGORY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[ERPROJECTFILECATEGORY].[ERPROJECTFILECATEGORYID],
	[dbo].[ERPROJECTFILECATEGORY].[NAME],
	[dbo].[ERPROJECTFILECATEGORY].[DESCRIPTION],
	[dbo].[ERPROJECTFILECATEGORY].[ALLOWEDFILETYPES],
	[dbo].[ERPROJECTFILECATEGORY].[ATTACHMENTGROUPID],
	[dbo].[ERPROJECTFILECATEGORY].[LASTCHANGEDBY],
	[dbo].[ERPROJECTFILECATEGORY].[LASTCHANGEDON],
	[dbo].[ERPROJECTFILECATEGORY].[ROWVERSION],
	COALESCE([dbo].[ERPROJECTFILECATEGORY].[FILENAMEPREFIX],0) AS [FILENAMEPREFIX],
	[dbo].[ERPROJECTFILECATEGORY].[FILENAMEPREFIXCUSTOM],
	COALESCE([dbo].[ERPROJECTFILECATEGORY].[FILENAMEBODY],2) AS [FILENAMEBODY],
	[dbo].[ERPROJECTFILECATEGORY].[FILENAMEBODYCUSTOM],
	COALESCE([dbo].[ERPROJECTFILECATEGORY].[FILENAMESUFFIX],0) AS [FILENAMESUFFIX],
	[dbo].[ERPROJECTFILECATEGORY].[FILENAMESUFFIXCUSTOM]
FROM [dbo].[ERPROJECTFILECATEGORY]
ORDER BY [dbo].[ERPROJECTFILECATEGORY].[NAME] ASC

END