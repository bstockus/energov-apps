CREATE PROCEDURE [globalsetup].[USP_PLITEMREVIEWTYPE_GETALL]
AS
BEGIN
	SELECT [dbo].[PLITEMREVIEWTYPE].[PLITEMREVIEWTYPEID],
		  [dbo].[PLITEMREVIEWTYPE].[NAME],
		  [dbo].[PLITEMREVIEWTYPE].[DESCRIPTION],
		  [dbo].[PLITEMREVIEWTYPE].[DEPARTMENTID],
		  [dbo].[PLITEMREVIEWTYPE].[PLITEMREVIEWSTATUSID],
		  [dbo].[PLITEMREVIEWTYPE].[DAYSUNTILDUE],
		  [dbo].[PLITEMREVIEWTYPE].[RESUMITALDAYSUNTILDUE],
		  [dbo].[PLITEMREVIEWTYPE].[INHERITSUBMITALDUE],
		  [dbo].[PLITEMREVIEWTYPE].[GISZONEREVIEWITEMMAPPINGID],
		  [dbo].[PLITEMREVIEWTYPE].[CODENAME],
		  [dbo].[PLITEMREVIEWTYPE].[CODEURL],
		  [dbo].[PLITEMREVIEWTYPE].[REASSIGNTOWHOCOMPLETEDREVIEW],
		  [dbo].[PLITEMREVIEWTYPE].[HIDERECOMMENDATIONS],
		  [dbo].[PLITEMREVIEWTYPE].[HIDECORRECTIONS],
		  [dbo].[PLITEMREVIEWTYPE].[HIDECONDITIONS],
		  [dbo].[PLITEMREVIEWTYPE].[LASTCHANGEDBY],
		  [dbo].[PLITEMREVIEWTYPE].[LASTCHANGEDON],
		  [dbo].[PLITEMREVIEWTYPE].[ROWVERSION],
		  [dbo].[PLITEMREVIEWTYPE].[ASSIGNTOOBJECT],
		  [dbo].[PLITEMREVIEWTYPE].[TEAMASSIGNEDTOID]
	  FROM [dbo].[PLITEMREVIEWTYPE] ORDER BY [dbo].[PLITEMREVIEWTYPE].[NAME] ASC

	EXEC [globalsetup].[USP_PLITEMREVIEWTYPEUSERALLOW_GETALL]

END