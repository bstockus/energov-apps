CREATE PROCEDURE [globalsetup].[USP_IMCHECKLISTCATEGORY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMCHECKLISTCATEGORY].[IMCHECKLISTCATEGORYID],
	[dbo].[IMCHECKLISTCATEGORY].[NAME],
	[dbo].[IMCHECKLISTCATEGORY].[DESCRIPTION],
	[dbo].[IMCHECKLISTCATEGORY].[LASTCHANGEDBY],
	[dbo].[IMCHECKLISTCATEGORY].[LASTCHANGEDON],
	[dbo].[IMCHECKLISTCATEGORY].[ROWVERSION]
FROM [dbo].[IMCHECKLISTCATEGORY]  ORDER BY [dbo].[IMCHECKLISTCATEGORY].[NAME] ASC
END