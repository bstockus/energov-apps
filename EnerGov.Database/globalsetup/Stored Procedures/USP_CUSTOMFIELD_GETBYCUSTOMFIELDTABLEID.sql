CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELD_GETBYCUSTOMFIELDTABLEID]
(
	@CUSTOMFIELDTABLEID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 	
		[dbo].[CUSTOMFIELD].[GCUSTOMFIELD],
		[dbo].[CUSTOMFIELD].[FKICUSTOMFIELDTYPE],
		[dbo].[CUSTOMFIELD].[SFIELDNAME],
		[dbo].[CUSTOMFIELD].[SCUSTOMFIELD],
		[dbo].[CUSTOMFIELD].[FKCUSTOMFIELDTABLECOLREF],
		[dbo].[CUSTOMFIELD].[COLUMNOPERATOR],
		[dbo].[CUSTOMFIELD].[BISPICKLIST],
		[dbo].[CUSTOMFIELD].[BISWORKSHEET],
		[dbo].[CUSTOMFIELD].[FKGCUSTOMFIELDTEMPLATE],
		[dbo].[CUSTOMFIELD].[FKCUSTOMFIELDTABLE]
	FROM [dbo].[CUSTOMFIELD] 
	INNER JOIN [dbo].[CUSTOMFIELDTABLECOLUMNREF] ON  [dbo].[CUSTOMFIELDTABLECOLUMNREF].[CUSTOMFIELDTABLECOLUMNREFID] = [dbo].[CUSTOMFIELD].[FKCUSTOMFIELDTABLECOLREF]
	WHERE [dbo].[CUSTOMFIELDTABLECOLUMNREF].[CUSTOMFIELDTABLEID] = @CUSTOMFIELDTABLEID
END