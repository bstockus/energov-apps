CREATE PROCEDURE [globalsetup].[USP_BLLICENSECLASS_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT 
	[dbo].[BLLICENSECLASS].[BLLICENSECLASSID],
	[dbo].[BLLICENSECLASS].[NAME],
	[dbo].[BLLICENSETYPECLASS].[BLLICENSETYPEID]
FROM [dbo].[BLLICENSECLASS] INNER JOIN [dbo].[BLLICENSETYPECLASS] 
ON [dbo].[BLLICENSETYPECLASS].[BLLICENSECLASSID] = [dbo].[BLLICENSECLASS].[BLLICENSECLASSID]
ORDER BY [dbo].[BLLICENSECLASS].[NAME]