CREATE PROCEDURE [globalsetup].[USP_CUSTOMFIELDLAYOUT_GETBYID]
(
	@GCUSTOMFIELDLAYOUTS CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
		[GCUSTOMFIELDLAYOUTS],
		[SNAME],
		[CUSTOMFIELDMODULEID],
		[WIDTH],
		[HEIGHT],
		[ISVERSION2],
		[LASTCHANGEDBY],
		[LASTCHANGEDON],
		[ROWVERSION],
		[NUMBEROFCOLUMNS],
		[ISHTMLEXPERIENCE]
	FROM [dbo].[CUSTOMFIELDLAYOUT]
	WHERE
		[GCUSTOMFIELDLAYOUTS] = @GCUSTOMFIELDLAYOUTS  

	EXEC [globalsetup].[USP_CUSTOMFIELDOBJECT_GETBYPARENTID] @GCUSTOMFIELDLAYOUTS
	EXEC [globalsetup].[USP_CUSTOMFIELDPICKLISTITEM_GETBYPARENTID] @GCUSTOMFIELDLAYOUTS
	EXEC [globalsetup].[USP_CUSTOMFIELDWORKSHEETITEM_GETBYPARENTID] @GCUSTOMFIELDLAYOUTS

END