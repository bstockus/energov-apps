CREATE PROCEDURE [globalsetup].[USP_CACPIREFERENCEDATE_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT  [dbo].[CACPIREFERENCEDATE].[CACPIREFERENCEDATEID],
			[dbo].[CACPIREFERENCEDATE].[DATEOBJECTFRIENDLYNAME],
			[dbo].[CACPIREFERENCEDATE].[CAMODULEID],
			[dbo].[CACPIREFERENCEDATE].[ISDEFAULT]
	FROM [dbo].[CACPIREFERENCEDATE] 
	ORDER BY 
		[dbo].[CACPIREFERENCEDATE].[DATEOBJECTFRIENDLYNAME]
END