CREATE PROCEDURE [globalsetup].[USP_CADISCOUNTADJUSTABLECALC_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CADISCOUNTADJUSTABLECALC].[CADISCOUNTADJUSTABLECALCID],
	[dbo].[CADISCOUNTADJUSTABLECALC].[CADISCOUNTID],
	[dbo].[CADISCOUNTADJUSTABLECALC].[CADISCOUNTSETUPID],
	[dbo].[CADISCOUNTADJUSTABLECALC].[LOWERLIMIT],
	[dbo].[CADISCOUNTADJUSTABLECALC].[UPPERLIMIT],
	[dbo].[CADISCOUNTADJUSTABLECALC].[UNLIMITED],
	[dbo].[CADISCOUNTADJUSTABLECALC].[AMOUNT],
	[dbo].[CADISCOUNTADJUSTABLECALC].[ADDITIONALAMOUNT],
	[dbo].[CADISCOUNTADJUSTABLECALC].[ADDITIONALUNIT],
	[dbo].[CADISCOUNTADJUSTABLECALC].[OVER]
FROM [dbo].[CADISCOUNTADJUSTABLECALC]
WHERE
	[dbo].[CADISCOUNTADJUSTABLECALC].[CADISCOUNTID] = @PARENTID  

END