CREATE PROCEDURE [globalsetup].[USP_PLPLANCORRECTIONTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PLPLANCORRECTIONTYPE].[PLPLANCORRECTIONTYPEID],
	[dbo].[PLPLANCORRECTIONTYPE].[NAME],
	[dbo].[PLPLANCORRECTIONTYPE].[DESCRIPTION],
	[dbo].[PLPLANCORRECTIONTYPE].[CORRECTIVEACTION],
	[dbo].[PLPLANCORRECTIONTYPE].[PLCORRECTIONTYPECATEGORYID],
	[dbo].[PLPLANCORRECTIONTYPE].[LASTCHANGEDBY],
	[dbo].[PLPLANCORRECTIONTYPE].[LASTCHANGEDON],
	[dbo].[PLPLANCORRECTIONTYPE].[ROWVERSION]
FROM [dbo].[PLPLANCORRECTIONTYPE]
ORDER BY [dbo].[PLPLANCORRECTIONTYPE].[NAME]
END