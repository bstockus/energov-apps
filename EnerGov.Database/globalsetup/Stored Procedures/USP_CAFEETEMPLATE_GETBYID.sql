CREATE PROCEDURE [globalsetup].[USP_CAFEETEMPLATE_GETBYID]
	@CAFEETEMPLATEID CHAR(36)
AS
BEGIN
SET NOCOUNT ON;
	SELECT 
		[dbo].[CAFEETEMPLATE].[CAFEETEMPLATEID],
		[dbo].[CAFEETEMPLATE].[CAFEETEMPLATENAME],
		[dbo].[CAFEETEMPLATE].[CAFEETEMPLATEDESCRIPTION],
		[dbo].[CAFEETEMPLATE].[CAENTITYID],
		[dbo].[CAFEETEMPLATE].[ROWVERSION],
		[dbo].[CAFEETEMPLATE].[LASTCHANGEDBY],
		[dbo].[CAFEETEMPLATE].[LASTCHANGEDON]
	FROM	[dbo].[CAFEETEMPLATE]
	WHERE
		[dbo].[CAFEETEMPLATE].[CAFEETEMPLATEID] = @CAFEETEMPLATEID  

	--FEE INFO
	EXEC [globalsetup].[USP_CAFEETEMPLATEFEE_GETBYPARENTID] @CAFEETEMPLATEID
	EXEC [globalsetup].[USP_CAFEETEMPLATEFEEPRORATEDATE_GETBYFEETEMPLATEID] @CAFEETEMPLATEID
	EXEC [globalsetup].[USP_CATEMPLATEFEEDISCOUNTXREF_GETBYFEETEMPLATEID] @CAFEETEMPLATEID
	
	--DISCOUNTS INFO
	EXEC [globalsetup].[USP_CAFEETEMPLATEDISCOUNT_GETBYPARENTID] @CAFEETEMPLATEID
	
	--FEE & DISCOUNTS COMMON
	EXEC [globalsetup].[USP_CAFEETEMPLATEINPUT_GETBYFEETEMPLATEID] @CAFEETEMPLATEID
	EXEC [globalsetup].[USP_CAFEETEMPLATEINPUTTRANSLATION_GETBYFEETEMPLATEID] @CAFEETEMPLATEID

	EXEC [globalsetup].[USP_CAEXPRESSIONS_GETBYFEETEMPLATEID] @CAFEETEMPLATEID
	
	EXEC [globalsetup].[USP_CACONDITIONGROUP_GETBYFEETEMPLATEID] @CAFEETEMPLATEID
	EXEC [globalsetup].[USP_CACONDITION_GETBYFEETEMPLATEID] @CAFEETEMPLATEID

	--CHILD FEE TEMPLATES INFO
	EXEC [globalsetup].[USP_CAFEETEMPLATECHILDXREF_GETBYPARENTID] @CAFEETEMPLATEID
	EXEC [globalsetup].[USP_CAFEETEMPLATECHILDXREF_FEES_GETBYPARENTID] @CAFEETEMPLATEID

END