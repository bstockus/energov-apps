CREATE PROCEDURE [globalsetup].[USP_BONDINTADJREASON_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[BONDINTADJREASON].[BONDINTADJREASONID],
	[dbo].[BONDINTADJREASON].[NAME],
	[dbo].[BONDINTADJREASON].[LASTCHANGEDBY],
	[dbo].[BONDINTADJREASON].[LASTCHANGEDON],
	[dbo].[BONDINTADJREASON].[ROWVERSION]
FROM [dbo].[BONDINTADJREASON]
ORDER BY [dbo].[BONDINTADJREASON].[NAME] ASC

END