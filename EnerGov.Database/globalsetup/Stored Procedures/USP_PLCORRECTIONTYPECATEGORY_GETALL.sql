CREATE PROCEDURE [globalsetup].[USP_PLCORRECTIONTYPECATEGORY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PLCORRECTIONTYPECATEGORY].[PLCORRECTIONTYPECATEGORYID],
	[dbo].[PLCORRECTIONTYPECATEGORY].[NAME],
	[dbo].[PLCORRECTIONTYPECATEGORY].[DESCRIPTION],
	[dbo].[PLCORRECTIONTYPECATEGORY].[LASTCHANGEDBY],
	[dbo].[PLCORRECTIONTYPECATEGORY].[LASTCHANGEDON],
	[dbo].[PLCORRECTIONTYPECATEGORY].[ROWVERSION]
FROM 
	[dbo].[PLCORRECTIONTYPECATEGORY]
ORDER BY 
	[dbo].[PLCORRECTIONTYPECATEGORY].[NAME]
END