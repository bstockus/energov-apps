CREATE PROCEDURE [globalsetup].[USP_IMCHECKLIST_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMCHECKLIST].[IMCHECKLISTID],
	[dbo].[IMCHECKLIST].[IMCHECKLISTCATEGORYID],
	[dbo].[IMCHECKLIST].[NAME],
	[dbo].[IMCHECKLIST].[DESCRIPTION],
	[dbo].[IMCHECKLIST].[LASTCHANGEDBY],
	[dbo].[IMCHECKLIST].[LASTCHANGEDON],
	[dbo].[IMCHECKLIST].[ROWVERSION]
FROM 
	[dbo].[IMCHECKLIST]
ORDER BY 
	[dbo].[IMCHECKLIST].[NAME]
END