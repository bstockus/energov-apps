CREATE PROCEDURE [communitydevelopmentsetup].[USP_IPCAPITALPROJECT_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IPCAPITALPROJECT].[IPCAPITALPROJECTID],
	[dbo].[IPCAPITALPROJECT].[NAME],
	[dbo].[IPCAPITALPROJECT].[DESCRIPTION],
	[dbo].[IPCAPITALPROJECT].[ACTIVE],
	[dbo].[IPCAPITALPROJECT].[LASTCHANGEDBY],
	[dbo].[IPCAPITALPROJECT].[LASTCHANGEDON],
	[dbo].[IPCAPITALPROJECT].[ROWVERSION]
FROM [dbo].[IPCAPITALPROJECT] ORDER BY [dbo].[IPCAPITALPROJECT].[NAME]
END