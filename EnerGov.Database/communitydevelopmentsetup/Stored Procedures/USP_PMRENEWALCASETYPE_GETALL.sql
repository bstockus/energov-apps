CREATE PROCEDURE [communitydevelopmentsetup].[USP_PMRENEWALCASETYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PMRENEWALCASETYPE].[PMRENEWALCASETYPEID],
	[dbo].[PMRENEWALCASETYPE].[NAME],
	[dbo].[PMRENEWALCASETYPE].[PREFIX],
	[dbo].[PMRENEWALCASETYPE].[INVOICEDAYSINADVANCE],
	[dbo].[PMRENEWALCASETYPE].[SUCCESSFULLRENEWALSTATUSID],
	[dbo].[PMRENEWALCASETYPE].[FAILEDRENEWALSTATUSID],
	[dbo].[PMRENEWALCASETYPE].[ACTIVE],
	[dbo].[PMRENEWALCASETYPE].[DESCRIPTION],
	[dbo].[PMRENEWALCASETYPE].[RECURRENCEID],
	[dbo].[PMRENEWALCASETYPE].[LASTCHANGEDBY],
	[dbo].[PMRENEWALCASETYPE].[LASTCHANGEDON],
	[dbo].[PMRENEWALCASETYPE].[ROWVERSION],
	[dbo].[RECURRENCE].[NAME] AS [RECURRENCENAME]
FROM [dbo].[PMRENEWALCASETYPE]
INNER JOIN [dbo].[RECURRENCE] ON [dbo].[PMRENEWALCASETYPE].[RECURRENCEID] = [dbo].[RECURRENCE].[RECURRENCEID]
ORDER BY [dbo].[PMRENEWALCASETYPE].[NAME]
END