CREATE PROCEDURE [communitydevelopmentsetup].[USP_IPCASEALLOWEDCONTACTTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IPCASEALLOWEDCONTACTTYPE].[IPCASEALLOWEDCONTACTTYPEID],
	[dbo].[IPCASEALLOWEDCONTACTTYPE].[IPCASETYPEID],
	[dbo].[IPCASEALLOWEDCONTACTTYPE].[CONTACTTYPEID],
	[dbo].[IPCASEALLOWEDCONTACTTYPE].[ISREQUIRED],
	[dbo].[IPCASEALLOWEDCONTACTTYPE].[CONTACTTYPEGROUP],
	[dbo].[LANDMANAGEMENTCONTACTTYPE].[NAME],
	[SYSTEMNAME] = [dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP].[NAME]
FROM [dbo].[IPCASEALLOWEDCONTACTTYPE]
JOIN [dbo].[LANDMANAGEMENTCONTACTTYPE] ON [dbo].[IPCASEALLOWEDCONTACTTYPE].[CONTACTTYPEID] = [dbo].[LANDMANAGEMENTCONTACTTYPE].[LANDMANAGEMENTCONTACTTYPEID]
JOIN [dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP] ON [dbo].[LANDMANAGEMENTCONTACTTYPE].[LANDMANAGEMENTCONTACTSYSTEMTYP] = [dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP].[LANDMANAGEMENTCONTACTSYSTEMTYP]
ORDER BY	[dbo].[LANDMANAGEMENTCONTACTTYPE].[NAME] ASC
END