CREATE PROCEDURE [communitydevelopmentsetup].[USP_PLPLANTYPEGROUP_GETALL]
AS
BEGIN
	SELECT
		[dbo].[PLPLANTYPEGROUP].[PLPLANTYPEGROUPID],
		[dbo].[PLPLANTYPEGROUP].[NAME],		
		[dbo].[PLPLANTYPEGROUP].[DESCRIPTION],
		[dbo].[PLPLANTYPEGROUP].[LASTCHANGEDBY],
		[dbo].[PLPLANTYPEGROUP].[LASTCHANGEDON],
		[dbo].[PLPLANTYPEGROUP].[ROWVERSION]
	FROM  [dbo].[PLPLANTYPEGROUP] ORDER BY [dbo].[PLPLANTYPEGROUP].[NAME] ASC
END