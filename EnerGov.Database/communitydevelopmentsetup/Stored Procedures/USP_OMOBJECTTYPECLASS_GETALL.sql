CREATE PROCEDURE [communitydevelopmentsetup].[USP_OMOBJECTTYPECLASS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[OMOBJECTTYPECLASS].[OMOBJECTTYPECLASSID],
	[dbo].[OMOBJECTTYPECLASS].[OMOBJECTTYPEID],
	[dbo].[OMOBJECTTYPECLASS].[OMOBJECTCLASSIFICATIONID],
	[dbo].[OMOBJECTTYPECLASS].[CUSTOMFIELDLAYOUTID],
	[dbo].[OMOBJECTCLASSIFICATION].[NAME]
FROM [dbo].[OMOBJECTTYPECLASS]
INNER JOIN [dbo].[OMOBJECTCLASSIFICATION] ON [dbo].[OMOBJECTTYPECLASS].[OMOBJECTCLASSIFICATIONID] = 
[dbo].[OMOBJECTCLASSIFICATION].[OMOBJECTCLASSIFICATIONID]
ORDER BY [dbo].[OMOBJECTCLASSIFICATION].[NAME] ASC
END