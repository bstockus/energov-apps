CREATE PROCEDURE [communitydevelopmentsetup].[USP_PRPROJECTTYPEPERMITLIMIT_GETBYPARENTID]
(
	@PRPROJECTTYPEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PRPROJECTTYPEPERMITLIMIT].[PRPROJECTTYPEPERMITLIMITID],
	[dbo].[PRPROJECTTYPEPERMITLIMIT].[PRPROJECTTYPEID],
	[dbo].[PRPROJECTTYPEPERMITLIMIT].[PMPERMITTYPEID],
	[dbo].[PRPROJECTTYPEPERMITLIMIT].[PMPERMITWORKCLASSID],
	[dbo].[PRPROJECTTYPEPERMITLIMIT].[PERMITLIMIT]
FROM [dbo].[PRPROJECTTYPEPERMITLIMIT]
WHERE
	[dbo].[PRPROJECTTYPEPERMITLIMIT].[PRPROJECTTYPEID] = @PRPROJECTTYPEID
END