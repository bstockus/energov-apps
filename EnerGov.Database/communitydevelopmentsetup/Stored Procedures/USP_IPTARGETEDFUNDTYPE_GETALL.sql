CREATE PROCEDURE [communitydevelopmentsetup].[USP_IPTARGETEDFUNDTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT		[dbo].[IPTARGETEDFUNDTYPE].[IPTARGETEDFUNDTYPEID],
			[dbo].[IPTARGETEDFUNDTYPE].[NAME],
			[dbo].[IPTARGETEDFUNDTYPE].[DESCRIPTION],
			[dbo].[IPTARGETEDFUNDTYPE].[ACTIVE],
			[dbo].[IPTARGETEDFUNDTYPE].[LASTCHANGEDBY],
			[dbo].[IPTARGETEDFUNDTYPE].[LASTCHANGEDON],
			[dbo].[IPTARGETEDFUNDTYPE].[ROWVERSION]
FROM		[dbo].[IPTARGETEDFUNDTYPE]
ORDER BY	[dbo].[IPTARGETEDFUNDTYPE].[NAME] ASC

END