CREATE PROCEDURE [communitydevelopmentsetup].[USP_PRPROJECTTYPE_GETBYID]
(
	@PRPROJECTTYPEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PRPROJECTTYPE].[PRPROJECTTYPEID],
	[dbo].[PRPROJECTTYPE].[NAME],
	[dbo].[PRPROJECTTYPE].[DESCRIPTION],
	[dbo].[PRPROJECTTYPE].[CUSTOMFIELDID],
	[dbo].[PRPROJECTTYPE].[CAFEETEMPLATEID],
	[dbo].[PRPROJECTTYPE].[ACTIVE],
	[dbo].[PRPROJECTTYPE].[USECASETYPENUMBERING],
	[dbo].[PRPROJECTTYPE].[LASTCHANGEDBY],
	[dbo].[PRPROJECTTYPE].[LASTCHANGEDON],
	[dbo].[PRPROJECTTYPE].[ROWVERSION]
FROM [dbo].[PRPROJECTTYPE]
WHERE
	[dbo].[PRPROJECTTYPE].[PRPROJECTTYPEID] = @PRPROJECTTYPEID  

EXEC [communitydevelopmentsetup].[USP_PRPROJECTTYPEPERMITLIMIT_GETBYPARENTID] @PRPROJECTTYPEID  
EXEC [communitydevelopmentsetup].[USP_PRPROJECTTYPEPLANORDER_GETBYPARENTID] @PRPROJECTTYPEID

END