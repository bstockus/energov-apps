CREATE PROCEDURE [communitydevelopmentsetup].[USP_OMOBJECTTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[OMOBJECTTYPE].[OMOBJECTTYPEID],
	[dbo].[OMOBJECTTYPE].[PREFIX],
	[dbo].[OMOBJECTTYPE].[NAME],
	[dbo].[OMOBJECTTYPE].[DESCRIPTION],
	[dbo].[OMOBJECTTYPE].[AUTONUMBER],
	[dbo].[OMOBJECTTYPE].[DEFAULTOBJECTSTATUSID],
	[dbo].[OMOBJECTTYPE].[ACTIVE],
	[dbo].[OMOBJECTTYPE].[LASTCHANGEDBY],
	[dbo].[OMOBJECTTYPE].[LASTCHANGEDON],
	[dbo].[OMOBJECTTYPE].[ROWVERSION]
FROM [dbo].[OMOBJECTTYPE]
ORDER BY	[dbo].[OMOBJECTTYPE].[NAME] ASC

EXEC [communitydevelopmentsetup].[USP_OMOBJECTTYPECLASS_GETALL] 

END