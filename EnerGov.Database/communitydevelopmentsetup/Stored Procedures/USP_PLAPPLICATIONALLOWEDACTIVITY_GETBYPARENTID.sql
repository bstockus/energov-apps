CREATE PROCEDURE [communitydevelopmentsetup].[USP_PLAPPLICATIONALLOWEDACTIVITY_GETBYPARENTID]
(
	@PARENTID AS CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PLAPPLICATIONALLOWEDACTIVITY].[PLAPPLICATIONALLOWEDACTIVITYID],
	[dbo].[PLAPPLICATIONALLOWEDACTIVITY].[PLAPPLICATIONTYPEID],
	[dbo].[PLAPPLICATIONALLOWEDACTIVITY].[PLAPPLICATIONACTIVITYTYPEID],
	[dbo].[PLAPPLICATIONACTIVITYTYPE].[NAME]
FROM [dbo].[PLAPPLICATIONALLOWEDACTIVITY]
INNER JOIN [dbo].[PLAPPLICATIONACTIVITYTYPE] 
	ON [dbo].[PLAPPLICATIONACTIVITYTYPE].[PLAPPLICATIONACTIVITYTYPEID] = [dbo].[PLAPPLICATIONALLOWEDACTIVITY].[PLAPPLICATIONACTIVITYTYPEID]
WHERE [dbo].[PLAPPLICATIONALLOWEDACTIVITY].[PLAPPLICATIONTYPEID] = @PARENTID
ORDER BY [dbo].[PLAPPLICATIONACTIVITYTYPE].[NAME]
END