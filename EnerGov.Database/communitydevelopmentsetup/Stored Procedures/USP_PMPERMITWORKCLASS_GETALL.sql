CREATE PROCEDURE [communitydevelopmentsetup].[USP_PMPERMITWORKCLASS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PMPERMITWORKCLASS].[PMPERMITWORKCLASSID],
	[dbo].[PMPERMITWORKCLASS].[NAME],
	[dbo].[PMPERMITWORKCLASS].[DESCRIPTION],
	[dbo].[PMPERMITWORKCLASS].[LASTCHANGEDBY],
	[dbo].[PMPERMITWORKCLASS].[LASTCHANGEDON],
	[dbo].[PMPERMITWORKCLASS].[ROWVERSION]
FROM [dbo].[PMPERMITWORKCLASS] ORDER BY [dbo].[PMPERMITWORKCLASS].[NAME] ASC
END