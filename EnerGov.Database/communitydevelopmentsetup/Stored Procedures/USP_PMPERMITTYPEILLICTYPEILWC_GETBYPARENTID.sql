CREATE PROCEDURE [communitydevelopmentsetup].[USP_PMPERMITTYPEILLICTYPEILWC_GETBYPARENTID]
(
	@PMPERMITTYPEID CHAR(36)
)
AS
BEGIN
SELECT 
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEILLICENSETYPEID],
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEID],
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITWORKCLASSID],	
	[dbo].[PMPERMITTYPEILLICTYPE].[ILLICENSETYPEID],
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEWORKCLASSID],
	[dbo].[ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID],
	[dbo].[ILLICENSECLASSIFICATION].[NAME]
FROM [dbo].[PMPERMITTYPEILLICTYPE]
INNER JOIN [dbo].[PMPERMITTYPEILLICTYPEILWC] 
	ON [dbo].[PMPERMITTYPEILLICTYPEILWC].[PMPERMITTYPEILLICENSETYPEID] = [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEILLICENSETYPEID]
INNER JOIN [dbo].[ILLICENSECLASSIFICATION] 
	ON [dbo].[ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID] = [dbo].[PMPERMITTYPEILLICTYPEILWC].[ILLICENSECLASSIFICATIONID]	
WHERE
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEID] = @PMPERMITTYPEID
ORDER BY [dbo].[ILLICENSECLASSIFICATION].[NAME]
END