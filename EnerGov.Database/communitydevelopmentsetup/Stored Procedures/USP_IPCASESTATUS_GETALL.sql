CREATE PROCEDURE [communitydevelopmentsetup].[USP_IPCASESTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IPCASESTATUS].[IPCASESTATUSID],
	[dbo].[IPCASESTATUS].[NAME],
	[dbo].[IPCASESTATUS].[DESCRIPTION],
	[dbo].[IPCASESTATUS].[APPROVAL],
	[dbo].[IPCASESTATUS].[ACTIVE],
	[dbo].[IPCASESTATUS].[CANCELLED],
	[dbo].[IPCASESTATUS].[LASTCHANGEDBY],
	[dbo].[IPCASESTATUS].[LASTCHANGEDON],
	[dbo].[IPCASESTATUS].[ROWVERSION]
FROM [dbo].[IPCASESTATUS] ORDER BY [dbo].[IPCASESTATUS].[NAME] ASC
END