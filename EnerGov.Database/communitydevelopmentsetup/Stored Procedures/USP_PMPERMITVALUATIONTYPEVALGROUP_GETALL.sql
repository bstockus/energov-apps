CREATE PROCEDURE [communitydevelopmentsetup].[USP_PMPERMITVALUATIONTYPEVALGROUP_GETALL]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[PMPERMITVALTYPEVALGRPID],
				[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[NAME],
				[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[DESCRIPTION],
				[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[EFFECTIVEDATE],
				[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[EXPIRATIONDATE],
				[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[LASTCHANGEDBY],
				[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[LASTCHANGEDON],
				[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[ROWVERSION]
	FROM		[dbo].[PMPERMITVALUATIONTYPEVALGROUP]
	ORDER BY	[dbo].[PMPERMITVALUATIONTYPEVALGROUP].[NAME]

	EXEC [communitydevelopmentsetup].[USP_PMPERMITVALUATIONTYPEVALGROUPX_GETALL]

END