CREATE PROCEDURE [communitydevelopmentsetup].[USP_PRPROJECTSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PRPROJECTSTATUS].[PRPROJECTSTATUSID],
	[dbo].[PRPROJECTSTATUS].[NAME],
	[dbo].[PRPROJECTSTATUS].[DESCRIPTION],
	[dbo].[PRPROJECTSTATUS].[HOLDFLAG],
	[dbo].[PRPROJECTSTATUS].[COMPLETEDFLAG],
	[dbo].[PRPROJECTSTATUS].[CANCELLEDFLAG],
	[dbo].[PRPROJECTSTATUS].[LASTCHANGEDBY],
	[dbo].[PRPROJECTSTATUS].[LASTCHANGEDON],
	[dbo].[PRPROJECTSTATUS].[ROWVERSION]
FROM [dbo].[PRPROJECTSTATUS] ORDER BY [dbo].[PRPROJECTSTATUS].[NAME] ASC

END