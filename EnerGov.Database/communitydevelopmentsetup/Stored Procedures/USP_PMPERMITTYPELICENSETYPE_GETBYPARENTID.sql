CREATE PROCEDURE [communitydevelopmentsetup].[USP_PMPERMITTYPELICENSETYPE_GETBYPARENTID]
(
	@PMPERMITTYPEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PMPERMITTYPELICENSETYPE].[PMPERMITTYPELICENSETYPEID],
	[dbo].[PMPERMITTYPELICENSETYPE].[PMPERMITTYPEID],
	[dbo].[PMPERMITTYPELICENSETYPE].[PMPERMITWORKCLASSID],
	[dbo].[PMPERMITTYPELICENSETYPE].[COSIMPLELICCERTTYPEID],
	[dbo].[PMPERMITTYPELICENSETYPE].[REQUIREALLCERTCLASS],
	[dbo].[COLICENSECERTIFICATIONTYPE].[NAME]
FROM [dbo].[PMPERMITTYPELICENSETYPE]
INNER JOIN [dbo].[COLICENSECERTIFICATIONTYPE]
	ON [dbo].[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID] = [dbo].[PMPERMITTYPELICENSETYPE].[COSIMPLELICCERTTYPEID]
WHERE
	[dbo].[PMPERMITTYPELICENSETYPE].[PMPERMITTYPEID] = @PMPERMITTYPEID  
ORDER BY [dbo].[COLICENSECERTIFICATIONTYPE].[NAME]
END