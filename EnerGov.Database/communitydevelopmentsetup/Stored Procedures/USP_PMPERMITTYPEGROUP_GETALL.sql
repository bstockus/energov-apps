CREATE PROCEDURE [communitydevelopmentsetup].[USP_PMPERMITTYPEGROUP_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PMPERMITTYPEGROUP].[PMPERMITTYPEGROUPID],
	[dbo].[PMPERMITTYPEGROUP].[NAME],
	[dbo].[PMPERMITTYPEGROUP].[DESCRIPTION],
	[dbo].[PMPERMITTYPEGROUP].[LASTCHANGEDBY],
	[dbo].[PMPERMITTYPEGROUP].[LASTCHANGEDON],
	[dbo].[PMPERMITTYPEGROUP].[ROWVERSION]
FROM [dbo].[PMPERMITTYPEGROUP] ORDER BY [dbo].[PMPERMITTYPEGROUP].[NAME] ASC

END