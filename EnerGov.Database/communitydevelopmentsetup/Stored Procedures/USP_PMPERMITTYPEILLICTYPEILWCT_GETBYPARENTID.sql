CREATE PROCEDURE [communitydevelopmentsetup].[USP_PMPERMITTYPEILLICTYPEILWCT_GETBYPARENTID]
(
	@PMPERMITTYPEID CHAR(36)
)
AS
BEGIN
SELECT 
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEILLICENSETYPEID],
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEID],
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITWORKCLASSID],	
	[dbo].[PMPERMITTYPEILLICTYPE].[ILLICENSETYPEID],
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEWORKCLASSID],
	[dbo].[ILLICENSECLASSTYPE].[ILLICENSECLASSTYPEID],
	[dbo].[ILLICENSECLASSTYPE].[NAME]
FROM [dbo].[PMPERMITTYPEILLICTYPE]
INNER JOIN [dbo].[PMPERMITTYPEILLICTYPEILWCT]
	ON [dbo].[PMPERMITTYPEILLICTYPEILWCT].[PMPERMITTYPEILLICENSETYPEID] = [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEILLICENSETYPEID]
INNER JOIN [dbo].[ILLICENSECLASSTYPE] 
	ON [dbo].[ILLICENSECLASSTYPE].[ILLICENSECLASSTYPEID] = [dbo].[PMPERMITTYPEILLICTYPEILWCT].[ILLICENSECLASSIFICATIONTYPEID]	
WHERE
	[dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEID] = @PMPERMITTYPEID
ORDER BY [dbo].[ILLICENSECLASSTYPE].[NAME]
END