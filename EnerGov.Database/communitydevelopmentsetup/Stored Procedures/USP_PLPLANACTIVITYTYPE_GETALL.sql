CREATE PROCEDURE [communitydevelopmentsetup].[USP_PLPLANACTIVITYTYPE_GETALL]
AS
BEGIN
SELECT 
	[dbo].[PLPLANACTIVITYTYPE].[PLPLANACTIVITYTYPEID],
	[dbo].[PLPLANACTIVITYTYPE].[NAME],
	[dbo].[PLPLANACTIVITYTYPE].[DESCRIPTION],
	[dbo].[PLPLANACTIVITYTYPE].[CANEDIT],
	[dbo].[PLPLANACTIVITYTYPE].[CREATEID],
	[dbo].[PLPLANACTIVITYTYPE].[PREFIXID],
	[dbo].[PLPLANACTIVITYTYPE].[ALLOWDUPLICATE],
	[dbo].[PLPLANACTIVITYTYPE].[CUSTOMFIELDID],
	[dbo].[PLPLANACTIVITYTYPE].[LASTCHANGEDBY],
	[dbo].[PLPLANACTIVITYTYPE].[LASTCHANGEDON],
	[dbo].[PLPLANACTIVITYTYPE].[ROWVERSION]
FROM [dbo].[PLPLANACTIVITYTYPE] ORDER BY [dbo].[PLPLANACTIVITYTYPE].[NAME] ASC
END