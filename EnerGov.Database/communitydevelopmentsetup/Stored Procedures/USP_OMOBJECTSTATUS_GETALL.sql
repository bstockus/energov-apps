CREATE PROCEDURE [communitydevelopmentsetup].[USP_OMOBJECTSTATUS_GETALL]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT		[dbo].[OMOBJECTSTATUS].[OMOBJECTSTATUSID],
				[dbo].[OMOBJECTSTATUS].[NAME],
				[dbo].[OMOBJECTSTATUS].[DESCRIPTION],
				[dbo].[OMOBJECTSTATUS].[OMOBJECTSTATUSSYSTEMID],
				[dbo].[OMOBJECTSTATUS].[LASTCHANGEDBY],
				[dbo].[OMOBJECTSTATUS].[LASTCHANGEDON],
				[dbo].[OMOBJECTSTATUS].[ROWVERSION]
	FROM		[dbo].[OMOBJECTSTATUS]
	ORDER BY	[dbo].[OMOBJECTSTATUS].[NAME] ASC
END