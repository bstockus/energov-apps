CREATE PROCEDURE [communitydevelopmentsetup].[USP_IPCONDITIONTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 		[dbo].[IPCONDITIONTYPE].[IPCONDITIONTYPEID],
			[dbo].[IPCONDITIONTYPE].[NAME],
			[dbo].[IPCONDITIONTYPE].[DESCRIPTION],
			[dbo].[IPCONDITIONTYPE].[DEFAULTSTATUSID],
			[dbo].[IPCONDITIONTYPE].[IPCONDITIONCATEGORYID],
			[dbo].[IPCONDITIONTYPE].[CAFEETEMPLATEID],
			[dbo].[IPCONDITIONTYPE].[LASTCHANGEDBY],
			[dbo].[IPCONDITIONTYPE].[LASTCHANGEDON],
			[dbo].[IPCONDITIONTYPE].[ROWVERSION]
FROM		[dbo].[IPCONDITIONTYPE]
ORDER BY	[dbo].[IPCONDITIONTYPE].[NAME] ASC

END