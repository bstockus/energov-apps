CREATE PROCEDURE [communitydevelopmentsetup].[USP_ATTACHMENTREQFILEREF_PLPLANTYPE_GETBYPARENTID]
(
	@PLPLANTYPEID CHAR(36)
)
AS
BEGIN
SELECT 
	[dbo].[ATTACHMENTREQFILEREF].[ATTACHMENTREQFILEREFID],
	[dbo].[ATTACHMENTREQFILEREF].[NAME],
	[dbo].[ATTACHMENTREQFILEREF].[ISREQUIRED],	
	[dbo].[ATTACHMENTREQFILEREF].[QUANTITY],
	[dbo].[ATTACHMENTREQFILEREF].[ATTACHMENTGROUPID],
	[dbo].[ATTACHMENTREQFILEREF].[OBJECTCLASSID],
	[dbo].[ATTACHMENTREQFILEREF].[OBJECTTYPEID]	
FROM [dbo].[ATTACHMENTREQFILEREF]
 INNER JOIN [dbo].[PLPLANTYPEWORKCLASS] ON [dbo].[ATTACHMENTREQFILEREF].[OBJECTTYPEID] = [dbo].[PLPLANTYPEWORKCLASS].[PLPLANTYPEWORKCLASSID]
WHERE
	[dbo].[PLPLANTYPEWORKCLASS].[PLPLANTYPEID] = @PLPLANTYPEID
ORDER BY [dbo].[ATTACHMENTREQFILEREF].[NAME]
END