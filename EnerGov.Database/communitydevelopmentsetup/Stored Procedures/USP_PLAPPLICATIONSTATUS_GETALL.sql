CREATE PROCEDURE [communitydevelopmentsetup].[USP_PLAPPLICATIONSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[PLAPPLICATIONSTATUS].[PLAPPLICATIONSTATUSID],
	[dbo].[PLAPPLICATIONSTATUS].[STATUS],
	[dbo].[PLAPPLICATIONSTATUS].[DESCRIPTION],
	[dbo].[PLAPPLICATIONSTATUS].[SUCCESSFLAG],
	[dbo].[PLAPPLICATIONSTATUS].[LASTCHANGEDBY],
	[dbo].[PLAPPLICATIONSTATUS].[LASTCHANGEDON],
	[dbo].[PLAPPLICATIONSTATUS].[ROWVERSION]
FROM [dbo].[PLAPPLICATIONSTATUS] 
ORDER BY [dbo].[PLAPPLICATIONSTATUS].[STATUS] ASC
END