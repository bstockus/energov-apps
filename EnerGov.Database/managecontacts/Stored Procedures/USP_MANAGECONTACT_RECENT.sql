CREATE PROCEDURE [managecontacts].[USP_MANAGECONTACT_RECENT]
(
  @USER_ID AS CHAR(36)
)
AS
BEGIN
	DECLARE @RECENTCONTACTLIST AS RecordIDs
	INSERT INTO @RECENTCONTACTLIST (RECORDID)

	SELECT TOP 15 [RECENTHISTORYGLOBALENTITY].[GLOBALENTITYID] 
	FROM [dbo].[RECENTHISTORYGLOBALENTITY]
	WHERE [RECENTHISTORYGLOBALENTITY].[USERID] = @USER_ID
	ORDER BY [RECENTHISTORYGLOBALENTITY].[LOGGEDDATETIME] DESC

	EXEC [managecontacts].[USP_MANAGECONTACT_GETBYIDS] @RECENTCONTACTLIST, @USER_ID 
END