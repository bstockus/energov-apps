CREATE FUNCTION [codecase].[UFN_CHECK_CODECASE_HIDDENCONTACT]
(
	@ENTITYID char(36),
	@ROLEID AS CHAR(36)
)
RETURNS BIT
AS
BEGIN
	RETURN CAST(CASE
			WHEN EXISTS (SELECT
			 1
			FROM [dbo].[COHIDDENCONTACTTYPE]
			WHERE SROLEGUID = @ROLEID AND [dbo].[COHIDDENCONTACTTYPE].[CONTACTTYPEID] = @ENTITYID) THEN 1
			ELSE 0
			END AS BIT)
END