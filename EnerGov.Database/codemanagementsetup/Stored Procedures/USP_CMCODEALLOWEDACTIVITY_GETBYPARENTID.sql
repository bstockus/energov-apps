CREATE PROCEDURE [codemanagementsetup].[USP_CMCODEALLOWEDACTIVITY_GETBYPARENTID]
(
@PARENTID AS CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CMCODEALLOWEDACTIVITY].[CMCODEALLOWEDACTIVITYID],
	[dbo].[CMCODEALLOWEDACTIVITY].[CMCASETYPEID],
	[dbo].[CMCODEACTIVITYTYPE].[CMCODEACTIVITYTYPEID] AS [CMCODEACTIVITYTYPEID],
	[dbo].[CMCODEACTIVITYTYPE].[NAME]
FROM [dbo].[CMCODEALLOWEDACTIVITY] INNER JOIN [dbo].[CMCODEACTIVITYTYPE] 
	ON [dbo].[CMCODEACTIVITYTYPE].[CMCODEACTIVITYTYPEID] = [dbo].[CMCODEALLOWEDACTIVITY].[CMCODEACTIVITYTYPEID] 
	WHERE [dbo].[CMCODEALLOWEDACTIVITY].[CMCASETYPEID] = @PARENTID 
	ORDER BY [dbo].[CMCODEACTIVITYTYPE].[NAME] ASC
END