CREATE PROCEDURE [codemanagementsetup].[USP_CMCODECASECONTACTTYPEREF_GETBYPARENTID]
(
@PARENTID AS CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CMCODECASECONTACTTYPEREF].[CONTACTTYPEEXTID],
	[dbo].[CMCODECASECONTACTTYPEREF].[CMCODECASECONTACTTYPEID] AS [CMCODECASECONTACTTYPEID],
	[dbo].[CMCODECASECONTACTTYPEREF].[CONTACTTYPEGROUP],
	[dbo].[CMCODECASECONTACTTYPEREF].[ISREQUIRED],
	[dbo].[CMCODECASECONTACTTYPEREF].[OBJCLASSID],
	[dbo].[CMCODECASECONTACTTYPEREF].[OBJTYPEID],
	[dbo].[CMCODECASECONTACTTYPEREF].[OBJMODULEID],
	[dbo].[CMCODECASECONTACTTYPE].[NAME],
	[dbo].[CMCODECASECONTACTTYPE].[DESCRIPTION]
FROM [dbo].[CMCODECASECONTACTTYPEREF] INNER JOIN [dbo].[CMCODECASECONTACTTYPE]
	ON  [dbo].[CMCODECASECONTACTTYPEREF].[CMCODECASECONTACTTYPEID] = [dbo].[CMCODECASECONTACTTYPE].[CMCODECASECONTACTTYPEID] 
	WHERE [dbo].[CMCODECASECONTACTTYPEREF].[OBJCLASSID] = @PARENTID
	ORDER BY [dbo].[CMCODECASECONTACTTYPE].[NAME] ASC
END