CREATE PROCEDURE [codemanagementsetup].[USP_CMCODEVIOLATIONPRIORITY_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CMCODEVIOLATIONPRIORITY].[CMCODEVIOLATIONPRIORITYID],
	[dbo].[CMCODEVIOLATIONPRIORITY].[NAME],
	[dbo].[CMCODEVIOLATIONPRIORITY].[VIOLATIONPRIORITYSYSTEMTYPEID],
	[dbo].[CMCODEVIOLATIONPRIORITY].[DEFAULTFLAG],
	[dbo].[CMCODEVIOLATIONPRIORITY].[LASTCHANGEDBY],
	[dbo].[CMCODEVIOLATIONPRIORITY].[LASTCHANGEDON],
	[dbo].[CMCODEVIOLATIONPRIORITY].[ROWVERSION]
FROM [dbo].[CMCODEVIOLATIONPRIORITY] ORDER BY [dbo].[CMCODEVIOLATIONPRIORITY].[NAME] ASC

END