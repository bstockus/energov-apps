CREATE PROCEDURE [codemanagementsetup].[USP_CMVIOLATIONSTATUS_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[CMVIOLATIONSTATUS].[CMVIOLATIONSTATUSID],
	[dbo].[CMVIOLATIONSTATUS].[NAME],
	[dbo].[CMVIOLATIONSTATUS].[DESCRIPTION],
	[dbo].[CMVIOLATIONSTATUS].[SUCCESSFLAG],
	[dbo].[CMVIOLATIONSTATUS].[DEFAULTFLAG],
	[dbo].[CMVIOLATIONSTATUS].[FAILUREFLAG],
	[dbo].[CMVIOLATIONSTATUS].[LASTCHANGEDBY],
	[dbo].[CMVIOLATIONSTATUS].[LASTCHANGEDON],
	[dbo].[CMVIOLATIONSTATUS].[ROWVERSION]
FROM [dbo].[CMVIOLATIONSTATUS] ORDER BY [dbo].[CMVIOLATIONSTATUS].[NAME] ASC

END