CREATE PROCEDURE [codemanagementsetup].[USP_CMCODECASESTATUS_GETALL]
AS
BEGIN
SELECT 
	[dbo].[CMCODECASESTATUS].[CMCODECASESTATUSID],
	[dbo].[CMCODECASESTATUS].[NAME],
	[dbo].[CMCODECASESTATUS].[DESCRIPTION],
	[dbo].[CMCODECASESTATUS].[SUCCESSFLAG],
	[dbo].[CMCODECASESTATUS].[FAILUREFLAG],
	[dbo].[CMCODECASESTATUS].[CANCELLEDFLAG],
	[dbo].[CMCODECASESTATUS].[LASTCHANGEDBY],
	[dbo].[CMCODECASESTATUS].[LASTCHANGEDON],
	[dbo].[CMCODECASESTATUS].[ROWVERSION]
FROM [dbo].[CMCODECASESTATUS] ORDER BY [dbo].[CMCODECASESTATUS].[NAME] ASC
END