CREATE PROCEDURE [codemanagementsetup].[USP_CMCODECASEHOLDTYPEREF_GETBYPARENTID]
(
	@CMCASETYPEID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;

	DECLARE @CMCODECASEHOLDTYPEIDS AS RecordIDs
	INSERT INTO @CMCODECASEHOLDTYPEIDS (RECORDID)
	SELECT [dbo].[CMCODECASEHOLDTYPEREF].[CMCODECASEHOLDTYPEID]  FROM [dbo].[CMCODECASEHOLDTYPEREF]
	WHERE [dbo].[CMCODECASEHOLDTYPEREF].[CMCASETYPEID] = @CMCASETYPEID

	EXEC [codemanagementsetup].[USP_CMCODECASEHOLDTYPEREF_GETBYIDS] @CMCODECASEHOLDTYPEIDS
	EXEC [codemanagementsetup].[USP_CMCODECASEHOLDTYPECONTACTTYPEREF_GETBYPARENTIDS] @CMCODECASEHOLDTYPEIDS

END