CREATE PROCEDURE [energovnotifications].[USP_MEETING_GETBYID]
(
	@MEETINGID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;

SELECT 
	[dbo].[MEETING].[MEETINGID],
	[dbo].[MEETING].[MEETINGTYPEID],
	[dbo].[MEETING].[SUBJECT],
	[dbo].[MEETING].[LOCATION],
	[dbo].[MEETING].[COMMENTS],
	[dbo].[MEETING].[STARTDATE],
	[dbo].[MEETING].[ENDDATE],
	[dbo].[MEETING].[LASTCHANGEDBY],
	[dbo].[MEETING].[LASTCHANGEDON],
	[dbo].[MEETING].[ROWVERSION],
	CAST(CASE WHEN [dbo].[MEETINGTYPE].[RECURRENCEID] IS NULL THEN 0 ELSE 1 END AS BIT) AS ISRECURRENCE
FROM [dbo].[MEETING] JOIN [dbo].[MEETINGTYPE] on [dbo].[MEETING].[MEETINGTYPEID] = [dbo].[MEETINGTYPE].[MEETINGTYPEID]
WHERE
	[dbo].[MEETING].[MEETINGID] = @MEETINGID 
	
	EXEC [energovnotifications].[USP_MEETING_ASSOCIATECASES_GETBYID] @MEETINGID
END