CREATE PROCEDURE [metadata].[USP_METADATACLASS_GETBYTYPEFULLNAME]
(@TYPEFULLNAME nvarchar(250))
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
		[dbo].[METADATACLASS].[NAME], 
		[dbo].[METADATACLASS].[TYPEFULLNAME], 
		[dbo].[METADATACLASS].[FRIENDLYNAME]
	FROM [dbo].[METADATACLASS] where [dbo].[METADATACLASS].[TYPEFULLNAME] = @TYPEFULLNAME
END