CREATE PROCEDURE [contactmanager].[USP_CONTACT_CHECK_EXISTING_PARCEL_OWNER]
(
	@IMPNAMEKEY CHAR(300)
)
AS
BEGIN
SET NOCOUNT ON;

DECLARE @id CHAR(36)

SELECT TOP 1 @id = [GLOBALENTITYID]
	FROM [GLOBALENTITY] WITH (NOLOCK)
	WHERE [GLOBALENTITY].[ISACTIVE] = 1 AND ISNULL(@IMPNAMEKEY,'') <> '' AND [GLOBALENTITY].[IMPNAMEKEY] = @IMPNAMEKEY

EXEC [contactmanager].[USP_CONTACT_GETBYID_SUMMARY_LOOKUP] @id

END