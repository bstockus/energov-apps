CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_INSPECTION_LINKEDRECORDS_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
	SELECT DISTINCT			
			[IMINSPECTION].[INSPECTIONNUMBER],
			[IMINSPECTIONTYPE].[NAME],
			[IMINSPECTIONSTATUS].[STATUSNAME],
			[IMINSPECTIONSTATUS].[SCHEDULEDFLAG],
			[IMINSPECTIONSTATUS].[WAITFLAG],
			[IMINSPECTIONSTATUS].[INDICATESSUCCESS],
			[IMINSPECTIONSTATUS].[REINSPECTIONFLAG],
			[IMINSPECTIONSTATUS].[PARTIALPASS],
			[IMINSPECTIONSTATUS].[INVIOLATIONFLAG],
			[IMINSPECTIONSTATUS].[EXTEND_EXPIRE_DATE],
			[IMINSPECTIONSTATUS].[CANCELLEDFLAG],
			[IMINSPECTIONSTATUS].[OUTOFVIOLATIONFLAG],
			[IMINSPECTION].[IMINSPECTIONID]
	FROM [IMINSPECTIONPARCEL]
	INNER JOIN [IMINSPECTION] ON [IMINSPECTION].[IMINSPECTIONID] = [IMINSPECTIONPARCEL].[IMINSPECTIONID]
	INNER JOIN [IMINSPECTIONTYPE] ON [IMINSPECTIONTYPE].[IMINSPECTIONTYPEID] = [IMINSPECTION].[IMINSPECTIONTYPEID]	
	INNER JOIN [IMINSPECTIONSTATUS] ON [IMINSPECTIONSTATUS].[IMINSPECTIONSTATUSID] = [IMINSPECTION].[IMINSPECTIONSTATUSID]	
	WHERE 
		[IMINSPECTIONPARCEL].[PARCELID] = @PARENTID
END