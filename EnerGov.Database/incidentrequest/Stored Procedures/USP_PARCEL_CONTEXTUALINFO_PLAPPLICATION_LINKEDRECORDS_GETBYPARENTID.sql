CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_PLAPPLICATION_LINKEDRECORDS_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
	SELECT DISTINCT			
			[PLAPPLICATION].[APPNUMBER],
			[PLAPPLICATIONTYPE].[APPLICATIONTYPENAME],
			[PLAPPLICATIONSTATUS].[STATUS],
			[PLAPPLICATIONSTATUS].[SUCCESSFLAG]						
	FROM [PLAPPLICATIONPARCEL]	
	INNER JOIN [PLAPPLICATION] ON [PLAPPLICATION].[PLAPPLICATIONID] = [PLAPPLICATIONPARCEL].[PLAPPLICATIONID]
	INNER JOIN [PLAPPLICATIONTYPE] ON [PLAPPLICATIONTYPE].[PLAPPLICATIONTYPEID] = [PLAPPLICATION].[PLAPPLICATIONTYPEID]	
	INNER JOIN [PLAPPLICATIONSTATUS] ON [PLAPPLICATIONSTATUS].[PLAPPLICATIONSTATUSID] = [PLAPPLICATION].[PLAPPLICATIONSTATUSID]
	WHERE 
		[PLAPPLICATIONPARCEL].[PARCELID] = @PARENTID
END