CREATE PROCEDURE [incidentrequest].[USP_CONTACT_APPLICATION_LINKEDRECORDS_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[PLAPPLICATION].[APPNUMBER] CASENUMBER,
	[PLAPPLICATIONTYPE].[APPLICATIONTYPENAME] CASETYPE,
	[PLAPPLICATIONSTATUS].[STATUS],
	[PLAPPLICATIONSTATUS].[SUCCESSFLAG]
FROM [PLAPPLICATION]
INNER JOIN [PLAPPLICATIONCONTACT]
ON [PLAPPLICATIONCONTACT].[PLAPPLICATIONID] = [PLAPPLICATION].[PLAPPLICATIONID]
INNER JOIN [PLAPPLICATIONTYPE] WITH (NOLOCK)
ON [PLAPPLICATIONTYPE].[PLAPPLICATIONTYPEID] = [PLAPPLICATION].[PLAPPLICATIONTYPEID]
INNER JOIN [PLAPPLICATIONSTATUS] WITH (NOLOCK)
ON [PLAPPLICATIONSTATUS].[PLAPPLICATIONSTATUSID] = [PLAPPLICATION].[PLAPPLICATIONSTATUSID]
WHERE
	[PLAPPLICATIONCONTACT].[GLOBALENTITYID] = @PARENTID
END