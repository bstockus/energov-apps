CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CITIZENREQUEST_LINKEDRECORDS_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
	SELECT DISTINCT			
			[CITIZENREQUEST].[REQUESTNUMBER],
			[CITIZENREQUESTTYPE].[NAME],
			[CITIZENREQUESTSTATUS].[STATUS],
			[CITIZENREQUESTSTATUS].[RESOLVEDFLAG]						
	FROM [CITIZENREQUESTPARCEL]
	INNER JOIN [CITIZENREQUEST] ON [CITIZENREQUEST].[CITIZENREQUESTID] = [CITIZENREQUESTPARCEL].[CITIZENREQUESTID]
	INNER JOIN [CITIZENREQUESTTYPE] ON [CITIZENREQUESTTYPE].[CITIZENREQUESTTYPEID] = [CITIZENREQUEST].[CITIZENREQUESTTYPEID]	
	INNER JOIN [CITIZENREQUESTSTATUS] ON [CITIZENREQUESTSTATUS].[CITIZENREQUESTSTATUSID] = [CITIZENREQUEST].[CITIZENREQUESTSTATUSID]
	WHERE 
		[CITIZENREQUESTPARCEL].[PARCELID] = @PARENTID
END