CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_PROJECT_LINKEDRECORDS_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
	SELECT DISTINCT			
			[PRPROJECT].[PROJECTNUMBER],
			[PRPROJECTTYPE].[NAME],
			[PRPROJECT].[NAME],
			[PRPROJECTSTATUS].[NAME],
			[PRPROJECTSTATUS].[CANCELLEDFLAG],
			[PRPROJECTSTATUS].[COMPLETEDFLAG],
			[PRPROJECTSTATUS].[HOLDFLAG]					
	FROM [PRPROJECTPARCEL]	
	INNER JOIN [PRPROJECT] ON [PRPROJECT].[PRPROJECTID] = [PRPROJECTPARCEL].[PRPROJECTID]
	INNER JOIN [PRPROJECTTYPE] ON [PRPROJECTTYPE].[PRPROJECTTYPEID] = [PRPROJECT].[PRPROJECTTYPEID]	
	INNER JOIN [PRPROJECTSTATUS] ON [PRPROJECTSTATUS].[PRPROJECTSTATUSID] = [PRPROJECT].[PRPROJECTSTATUSID]	
	WHERE 
		[PRPROJECTPARCEL].[PARCELID] = @PARENTID
END