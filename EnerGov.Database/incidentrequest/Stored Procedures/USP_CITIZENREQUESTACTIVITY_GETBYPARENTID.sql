CREATE PROCEDURE [incidentrequest].[USP_CITIZENREQUESTACTIVITY_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 	
	[CITIZENREQUESTACTIVITY].[CITIZENREQUESTACTIVITYNAME],
	[CITIZENREQUESTACTIVITYTYPE].[NAME],	
	[CITIZENREQUESTACTIVITY].[CITIZENREQUESTACTIVITYCOMMENTS],	
	[CITIZENREQUESTACTIVITY].[CREATEDON]
FROM [dbo].[CITIZENREQUESTACTIVITY]
LEFT JOIN CITIZENREQUESTACTIVITYTYPE ON CITIZENREQUESTACTIVITYTYPE.CITIZENREQUESTACTIVITYTYPEID = CITIZENREQUESTACTIVITY.CITIZENREQUESTACTIVITYTYPEID
WHERE
	[CITIZENREQUESTID] = @PARENTID  
END