CREATE PROCEDURE [incidentrequest].[USP_CONTACT_CUSTOMFIELD_GETBYPARENTID] 
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
	SELECT 
		[CUSTOMSAVERSYSTEMSETUP].* 
	FROM [GLOBALENTITY]
	INNER JOIN [CUSTOMSAVERSYSTEMSETUP]
	ON [CUSTOMSAVERSYSTEMSETUP].ID = [GLOBALENTITY].GLOBALENTITYID
	WHERE [GLOBALENTITY].GLOBALENTITYID = @PARENTID
END