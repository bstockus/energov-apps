CREATE PROCEDURE [incidentrequest].[USP_CITIZENREQUESTPARCEL_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[CITIZENREQUESTPARCEL].[MAIN],
	[PARCEL].[PARCELNUMBER],
	[PARCEL].[PARCELID]
FROM [dbo].[CITIZENREQUESTPARCEL]
INNER JOIN PARCEL ON [PARCEL].PARCELID = [CITIZENREQUESTPARCEL].PARCELID
WHERE
	[CITIZENREQUESTID] = @PARENTID  

END