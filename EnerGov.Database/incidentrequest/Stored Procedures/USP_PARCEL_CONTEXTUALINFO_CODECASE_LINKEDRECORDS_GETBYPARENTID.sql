CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CODECASE_LINKEDRECORDS_GETBYPARENTID] 
	@PARCELID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
	SELECT DISTINCT			
			[CMCODECASE].[CASENUMBER],
			[CMCASETYPE].[NAME],
			[CMCODECASESTATUS].[NAME],
			[CMCODECASESTATUS].[FAILUREFLAG],
			[CMCODECASESTATUS].[SUCCESSFLAG],
			[CMCODECASESTATUS].[CANCELLEDFLAG],
			[CMCODECASE].[CMCODECASEID]
	FROM [CMCODECASEPARCEL]	
	INNER JOIN [CMCODECASE] ON [CMCODECASE].[CMCODECASEID] = [CMCODECASEPARCEL].[CMCODECASEID]
	INNER JOIN [CMCASETYPE] ON [CMCASETYPE].[CMCASETYPEID] = [CMCODECASE].[CMCASETYPEID]	
	INNER JOIN [CMCODECASESTATUS] ON [CMCODECASESTATUS].[CMCODECASESTATUSID] = [CMCODECASE].[CMCODECASESTATUSID]	
	WHERE 
		[CMCODECASEPARCEL].[PARCELID] = @PARCELID
END