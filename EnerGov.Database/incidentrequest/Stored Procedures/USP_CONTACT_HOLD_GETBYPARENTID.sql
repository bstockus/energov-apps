CREATE PROCEDURE [incidentrequest].[USP_CONTACT_HOLD_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[GLOBALENTITYHOLD].[CREATEDDATE],
	[GLOBALENTITYHOLD].[EFFECTIVEENDDATE],
	[GLOBALENTITYHOLD].[COMMENTS],
	[GLOBALENTITYHOLD].[ACTIVE],
	[HOLDTYPESETUPS].[NAME]	TYPENAME
FROM [GLOBALENTITYHOLD]
INNER JOIN [HOLDTYPESETUPS] WITH (NOLOCK)
ON [HOLDTYPESETUPS].HOLDSETUPID = [GLOBALENTITYHOLD].[HOLDSETUPID]
WHERE
	[GLOBALENTITYHOLD].[GLOBALENTITYID] = @PARENTID
END