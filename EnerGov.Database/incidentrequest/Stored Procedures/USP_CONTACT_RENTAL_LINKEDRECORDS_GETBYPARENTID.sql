CREATE PROCEDURE [incidentrequest].[USP_CONTACT_RENTAL_LINKEDRECORDS_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[RPPROPERTY].[PROPERTYNUMBER],
	[RPPROPERTYTYPE].[NAME] PROPERTYTYPE,
	[RPPROPERTYSTATUS].[NAME] [STATUS],
	[RPPROPERTYSYSSTATUS].[NAME] [SYSTEMSTATUS]
FROM [RPPROPERTY]
INNER JOIN [RPLANDLORDLICENSE]
ON [RPLANDLORDLICENSE].[RPLANDLORDLICENSEID] = RPPROPERTY.[RPLANDLORDLICENSEID]
INNER JOIN [RPPROPERTYTYPE] WITH (NOLOCK)
ON [RPPROPERTYTYPE].[RPPROPERTYTYPEID] = [RPPROPERTY].[RPPROPERTYTYPEID]
INNER JOIN [RPPROPERTYSTATUS] WITH (NOLOCK)
ON [RPPROPERTY].[RPPROPERTYSTATUSID] = [RPPROPERTYSTATUS].[RPPROPERTYSTATUSID]
INNER JOIN [RPPROPERTYSYSSTATUS] WITH (NOLOCK)
ON [RPPROPERTYSTATUS].RPPROPERTYSYSSTATUSID = [RPPROPERTYSYSSTATUS].RPPROPERTYSYSSTATUSID
WHERE
	[RPLANDLORDLICENSE].[GLOBALENTITYID] = @PARENTID
END