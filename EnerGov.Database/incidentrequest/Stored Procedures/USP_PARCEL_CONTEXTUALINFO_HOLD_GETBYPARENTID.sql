CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_HOLD_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
 SELECT 
	[PARCELHOLD].[COMMENTS],
	[PARCELHOLD].[CREATEDDATE],
	[PARCELHOLD].[EFFECTIVEENDDATE],
	[HOLDTYPE].[HOLDTYPENAME],
	[PARCELHOLD].[ACTIVE]
FROM  [dbo].[PARCELHOLD]
LEFT JOIN [HOLDTYPESETUPS] ON [HOLDTYPESETUPS].[HOLDSETUPID] = [PARCELHOLD].[HOLDSETUPID]	
LEFT JOIN [HOLDTYPE] ON [HOLDTYPE].[HOLDTYPEID] = [HOLDTYPESETUPS].[HOLDTYPEID]
WHERE 
    PARCELID = @PARENTID
END