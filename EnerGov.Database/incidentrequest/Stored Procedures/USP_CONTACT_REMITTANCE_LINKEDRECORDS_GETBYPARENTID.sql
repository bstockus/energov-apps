CREATE PROCEDURE [incidentrequest].[USP_CONTACT_REMITTANCE_LINKEDRECORDS_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[TXREMITTANCEACCOUNT].[REMITTANCEACCOUNTNUMBER],
	[TXREMITTANCETYPE].[NAME] REMITTANCETYPE,
	[TXREMITSTATUS].[NAME] [STATUS],
	[TXREMITSTATUSSYSTEM].[NAME] [SYSTEMSTATUS]
FROM [TXREMITTANCEACCOUNT]
INNER JOIN [BLGLOBALENTITYEXTENSION]
ON [BLGLOBALENTITYEXTENSION].BLGLOBALENTITYEXTENSIONID = [TXREMITTANCEACCOUNT].BLGLOBALENTITYEXTENSIONID
INNER JOIN [TXREMITTANCETYPE] WITH (NOLOCK)
ON [TXREMITTANCEACCOUNT].[TXREMITTANCETYPEID] = [TXREMITTANCETYPE].[TXREMITTANCETYPEID]
INNER JOIN [TXREMITSTATUS] WITH (NOLOCK)
ON [TXREMITTANCEACCOUNT].[TXREMITTANCESTATUSID] = [TXREMITSTATUS].[TXREMITSTATUSID]
INNER JOIN [TXREMITSTATUSSYSTEM]
ON [TXREMITSTATUS].[TXREMITSTATUSSYSTEMID] = [TXREMITSTATUSSYSTEM].[TXREMITSTATUSSYSTEMID]
WHERE
	[BLGLOBALENTITYEXTENSION].[GLOBALENTITYID] = @PARENTID
END