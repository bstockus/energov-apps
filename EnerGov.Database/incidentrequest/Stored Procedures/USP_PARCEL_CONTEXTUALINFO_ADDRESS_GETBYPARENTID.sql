CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_ADDRESS_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
 SELECT 
	[PARCELADDRESS].[ADDRESSLINE1],
	[PARCELADDRESS].[PREDIRECTION],
	[PARCELADDRESS].[ADDRESSLINE2],
	[PARCELADDRESS].[STREETTYPE],
	[PARCELADDRESS].[POSTDIRECTION],
	[PARCELADDRESS].[UNITORSUITE],
	[PARCELADDRESS].[POBOX],
	[PARCELADDRESS].[CITY],
	[PARCELADDRESS].[STATE],
	[PARCELADDRESS].[POSTALCODE],
	[PARCELADDRESS].[COUNTY],
	[PARCELADDRESS].[ADDRESSLINE3],
	[PARCELADDRESS].[PARCELNUMBER],
	[PARCELADDRESS].[ADDRESSTYPE]
	FROM  [dbo].[PARCELADDRESS]
WHERE 
    PARCELID = @PARENTID
END