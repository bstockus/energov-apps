CREATE PROCEDURE [incidentrequest].[USP_CONTACT_PROJECT_LINKEDRECORDS_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[PRPROJECT].[PROJECTNUMBER],
	[PRPROJECTTYPE].[NAME] PROJECTTYPE,
	[PRPROJECTSTATUS].[NAME] [STATUS],
	[PRPROJECTSTATUS].[CANCELLEDFLAG],
	[PRPROJECTSTATUS].[COMPLETEDFLAG],
	[PRPROJECTSTATUS].[HOLDFLAG]
FROM [PRPROJECT]
INNER JOIN [PRPROJECTCONTACT]
ON [PRPROJECTCONTACT].[PRPROJECTID] = [PRPROJECT].[PRPROJECTID]
INNER JOIN [PRPROJECTTYPE] WITH (NOLOCK)
ON [PRPROJECTTYPE].[PRPROJECTTYPEID] = [PRPROJECT].[PRPROJECTTYPEID]
INNER JOIN [PRPROJECTSTATUS] WITH (NOLOCK)
ON [PRPROJECTSTATUS].[PRPROJECTSTATUSID] = [PRPROJECT].[PRPROJECTSTATUSID]
WHERE
	[PRPROJECTCONTACT].[GLOBALENTITYID] = @PARENTID
END