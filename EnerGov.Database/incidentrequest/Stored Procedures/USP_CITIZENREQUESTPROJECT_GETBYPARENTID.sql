CREATE PROCEDURE [incidentrequest].[USP_CITIZENREQUESTPROJECT_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 	
	[PRPROJECT].[PROJECTNUMBER],
	[PRPROJECT].[NAME],
	[PRPROJECTTYPE].[NAME],
	[PRPROJECTSTATUS].[NAME]
FROM [dbo].[PRPROJECTCITIZENREQUEST]
    LEFT JOIN PRPROJECT ON [PRPROJECT].[PRPROJECTID] = [PRPROJECTCITIZENREQUEST].[PRPROJECTID]
	LEFT JOIN PRPROJECTTYPE ON [PRPROJECTTYPE].[PRPROJECTTYPEID] = [PRPROJECT].[PRPROJECTTYPEID]
	LEFT JOIN PRPROJECTSTATUS ON PRPROJECTSTATUS.[PRPROJECTSTATUSID] = [PRPROJECT].[PRPROJECTSTATUSID]	
WHERE
	[CITIZENREQUESTID] = @PARENTID  
END