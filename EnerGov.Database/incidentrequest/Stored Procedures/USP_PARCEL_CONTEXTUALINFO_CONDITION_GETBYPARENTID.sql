CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CONDITION_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
 SELECT 
	[CONDITION].[NAME],
	[CONDITIONCATEGORY].[NAME],
	[CONDITION].[DESCRIPTION],
	[CONDITION].[ISENABLE],
	[CONDITION].[COMMENTS],
	[CONDITION].[ORIGINOBJECT]
FROM  [dbo].[PARCELCONDITION]
LEFT JOIN [CONDITION] ON [CONDITION].[CONDITIONID] = [PARCELCONDITION].[CONDITIONID]	
LEFT JOIN [CONDITIONCATEGORY] ON [CONDITIONCATEGORY].[CONDITIONCATEGORYID] = [CONDITION].[CONDITIONCATEGORYID]
WHERE 
    PARCELID = @PARENTID
END