CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CONTACT_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
 SELECT 
	[GLOBALENTITY].[GLOBALENTITYNAME],
	[GLOBALENTITY].[FIRSTNAME],
	[GLOBALENTITY].[LASTNAME],
	[PARCELCONTACTTYPE].[NAME],
	[GLOBALENTITY].[GLOBALENTITYID]
FROM  [dbo].[PARCELCONTACT]
LEFT JOIN [GLOBALENTITY] ON [GLOBALENTITY].[GLOBALENTITYID] = [PARCELCONTACT].[GLOBALENTITYID]	
LEFT JOIN [PARCELCONTACTTYPE] ON [PARCELCONTACTTYPE].[PARCELCONTACTTYPEID] = [PARCELCONTACT].[PARCELCONTACTTYPEID]	
WHERE 
    PARCELID = @PARENTID
END