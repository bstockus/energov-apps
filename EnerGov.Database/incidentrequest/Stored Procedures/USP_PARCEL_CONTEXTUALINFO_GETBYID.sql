CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_GETBYID] 
	@PARCELID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
 SELECT 
	[PARCEL].[PARCELID],
	[PARCEL].[PARCELNUMBER],	
	[PARCEL].[TOWNSHIP],
	[PARCEL].[STATUSID],	
	[PARCEL].[ADDITION],
	[PARCEL].[LOT],	
	[PARCEL].[SECTION],
	[PARCEL].[RANGE],	
	[PARCEL].[BLOCK],
	[PARCEL].[ACTIVE],	
	[PARCEL].[TRACT],
	[PARCEL].[TAXNUMBER],	
	[PARCEL].[SUBDIVISION],
	[PARCEL].[LEGALDESCRIPTION],	
	[PARCEL].[JURISDICTION],
	[PARCEL].[DIRECTIONS],
	[IMINSPECTIONZONE].[NAME],
	[PARCEL].[NAME1],	
	[PARCEL].[NAME2],
	[PARCEL].[ADDRESSLINE1],
	[PARCEL].[ADDRESSLINE2],
	[PARCEL].[CITY],
	[PARCEL].[STATE],
	[PARCEL].[POSTALCODE],
	[PARCEL].[GISX],
	[PARCEL].[GISY]
FROM  [dbo].[PARCEL]
LEFT JOIN [IMINSPECTIONZONE] ON [IMINSPECTIONZONE].[IMINSPECTIONZONEID] = [PARCEL].[INSPECTIONZONEID]	
WHERE 
    PARCELID = @PARCELID

EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_HOLD_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_ADDRESS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CONTACT_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CONDITION_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_PERMIT_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_PLAN_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_PROJECT_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_BLLICENSE_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CITIZENREQUEST_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_CODECASE_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_ILLICENSE_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_INSPECTION_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_PLAPPLICATION_LINKEDRECORDS_GETBYPARENTID] @PARCELID
EXEC [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_BUSINESS_LINKEDRECORDS_GETBYPARENTID] @PARCELID
END