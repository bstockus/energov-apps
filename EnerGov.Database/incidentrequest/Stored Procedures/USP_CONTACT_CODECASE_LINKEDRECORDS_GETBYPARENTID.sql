CREATE PROCEDURE [incidentrequest].[USP_CONTACT_CODECASE_LINKEDRECORDS_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[CMCODECASE].[CASENUMBER],
	[CMCASETYPE].[NAME] CASETYPE,
	[CMCODECASESTATUS].[NAME] [STATUS],
	[CMCODECASESTATUS].[CANCELLEDFLAG],
	[CMCODECASESTATUS].[SUCCESSFLAG],
	[CMCODECASESTATUS].[FAILUREFLAG],
	[CMCODECASE].[CMCODECASEID]
FROM [CMCODECASE]
INNER JOIN [CMCODECASECONTACT]
ON [CMCODECASECONTACT].[CMCODECASEID] = [CMCODECASE].[CMCODECASEID]
INNER JOIN [CMCASETYPE] WITH (NOLOCK)
ON [CMCASETYPE].[CMCASETYPEID] = [CMCODECASE].[CMCASETYPEID]
INNER JOIN [CMCODECASESTATUS] WITH (NOLOCK)
ON [CMCODECASESTATUS].[CMCODECASESTATUSID] = [CMCODECASE].[CMCODECASESTATUSID]
WHERE
	[CMCODECASECONTACT].[GLOBALENTITYID] = @PARENTID
END