CREATE PROCEDURE [incidentrequest].[USP_PARCELADDRESS_INFO_GETBYID]
(
	@ADDRESSID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		ADDRESSID,
		ADDRESSLINE1,
		ADDRESSLINE2,
		ADDRESSLINE3,
		ADDRESSTYPE,
		UNITORSUITE,
		ATTN,
		CITY,
		STATE,
		STATION,
		POBOX,
		POSTALCODE,
		COMPSITE,
		POSTDIRECTION,
		PREDIRECTION,
		PROVINCE,
		RURALROUTE,
		COUNTY,
		COUNTRY,
		COUNTRYTYPE,
		PARCELID,
		PARCELNUMBER,
		GENERALDELIVERY
	FROM [PARCELADDRESS]
	WHERE [PARCELADDRESS].[ADDRESSID] = @ADDRESSID

END