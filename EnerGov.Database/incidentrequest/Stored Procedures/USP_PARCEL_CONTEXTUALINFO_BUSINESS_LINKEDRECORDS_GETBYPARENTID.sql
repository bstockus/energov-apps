CREATE PROCEDURE [incidentrequest].[USP_PARCEL_CONTEXTUALINFO_BUSINESS_LINKEDRECORDS_GETBYPARENTID] 
	@PARENTID char(36)
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;
   
	SELECT DISTINCT			
			[BLGLOBALENTITYEXTENSION].[COMPANYNAME],
			[BLGLOBALENTITYEXTENSION].[REGISTRATIONID],
			[BLGLOBALENTITYEXTENSION].[DBA],
			[BLEXTCOMPANYTYPE].[NAME],
			[BLEXTSTATUS].[NAME],
			[BLGLOBALENTITYEXTENSION].[OPENDATE],
			[BLGLOBALENTITYEXTENSION].[CLOSEDATE],
			[BLEXTSTATUSSYSTEM].[NAME]
	FROM [BLGLOBALENTITYEXTENSIONPARCEL]	
	INNER JOIN [BLGLOBALENTITYEXTENSION] ON [BLGLOBALENTITYEXTENSION].[BLGLOBALENTITYEXTENSIONID] = [BLGLOBALENTITYEXTENSIONPARCEL].[BLGLOBALENTITYEXTENSIONID]
	INNER JOIN [BLEXTCOMPANYTYPE] ON [BLEXTCOMPANYTYPE].[BLEXTCOMPANYTYPEID] = [BLGLOBALENTITYEXTENSION].[BLEXTCOMPANYTYPEID]
	INNER JOIN [BLEXTSTATUS] ON BLEXTSTATUS.[BLEXTSTATUSID] = [BLGLOBALENTITYEXTENSION].[BLEXTSTATUSID]
	INNER JOIN [BLEXTSTATUSSYSTEM] ON [BLEXTSTATUSSYSTEM].[BLEXTSTATUSSYSTEMID] =[BLEXTSTATUS].[BLEXTSTATUSSYSTEMID]
	WHERE 
		[BLGLOBALENTITYEXTENSIONPARCEL].[PARCELID] = @PARENTID
END