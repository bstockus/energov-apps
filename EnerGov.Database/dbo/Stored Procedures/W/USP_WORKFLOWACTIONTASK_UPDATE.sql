CREATE PROCEDURE [dbo].[USP_WORKFLOWACTIONTASK_UPDATE]
(
	@WORKFLOWACTIONTASKID CHAR(36),
	@WORKFLOWACTIONID CHAR(36),
	@TASKSUBJECT NVARCHAR (MAX),
    @TASKTEXT NVARCHAR (MAX),
    @STARTDATE NVARCHAR (MAX),
    @DUEDATE NVARCHAR (MAX),
    @STARTTIME NVARCHAR (MAX),
    @DUETIME NVARCHAR (MAX),
    @STARTDATEOFFSETDAYS INT,
    @DUEDATEOFFSETDAYS INT,
    @TASKPRIORITYID INT,
    @SHOWONCALENDAR BIT,
    @ASSIGNEDTO NVARCHAR (MAX)
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWACTIONTASKID]  char(36))
UPDATE [dbo].[WORKFLOWACTIONTASK] SET
	[WORKFLOWACTIONID] = @WORKFLOWACTIONID,
	[TASKSUBJECT] = @TASKSUBJECT,
    [TASKTEXT] = @TASKTEXT,
    [STARTDATE] = @STARTDATE,
    [DUEDATE] = @DUEDATE,
    [STARTTIME] = @STARTTIME,
    [DUETIME] = @DUETIME,
    [STARTDATEOFFSETDAYS] = @STARTDATEOFFSETDAYS,
    [DUEDATEOFFSETDAYS] = @DUEDATEOFFSETDAYS,
    [TASKPRIORITYID] = @TASKPRIORITYID,
    [SHOWONCALENDAR] = @SHOWONCALENDAR,
    [ASSIGNEDTO] = @ASSIGNEDTO
OUTPUT inserted.[WORKFLOWACTIONTASKID] INTO @OUTPUTTABLE
WHERE
	[WORKFLOWACTIONTASKID] = @WORKFLOWACTIONTASKID  
SELECT * FROM @OUTPUTTABLE