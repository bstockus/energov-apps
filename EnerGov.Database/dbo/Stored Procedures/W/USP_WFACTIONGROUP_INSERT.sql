CREATE PROCEDURE [dbo].[USP_WFACTIONGROUP_INSERT]
	@WFACTIONGROUPID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@WFENTITYID INT,
	@WFSTEPTYPEID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[WFACTIONGROUP](
	[WFACTIONGROUPID],
	[NAME],
	[DESCRIPTION],
	[WFENTITYID],
	[WFSTEPTYPEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION] 
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@WFACTIONGROUPID,
	@NAME,
	@DESCRIPTION,
	@WFENTITYID,
	@WFSTEPTYPEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE