CREATE PROCEDURE [dbo].[USP_WFACTIONGROUPACTIONXREF_UPDATE]
(
	@WFACTIONGROUPACTIONXREFID CHAR(36),
	@PRIORITYORDER INT,
	@SORTORDER INT,
	@AUTOFILL BIT,
	@AUTORECEIVE BIT
)
AS

UPDATE [dbo].[WFACTIONGROUPACTIONXREF] SET
	[PRIORITYORDER] = @PRIORITYORDER,
	[SORTORDER] = @SORTORDER,
	[AUTOFILL] = @AUTOFILL,
	[AUTORECEIVE] = @AUTORECEIVE

WHERE
	[WFACTIONGROUPACTIONXREFID] = @WFACTIONGROUPACTIONXREFID