CREATE PROCEDURE [dbo].[USP_WFSTEP_INSERT]
	@WFSTEPID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@WFSTEPTYPEID INT,
	@ICON VARBINARY(MAX),
	@DAYSTOCOMPLETE INT,
	@WORKFLOWCOMPLETETYPEID INT,
	@CODEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[WFSTEP](
	[WFSTEPID],
	[NAME],
	[DESCRIPTION],
	[WFSTEPTYPEID],
	[ICON],
	[DAYSTOCOMPLETE],
	[WORKFLOWCOMPLETETYPEID],
	[CMCODEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION] 
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@WFSTEPID,
	@NAME,
	@DESCRIPTION,
	@WFSTEPTYPEID,
	@ICON,
	@DAYSTOCOMPLETE,
	@WORKFLOWCOMPLETETYPEID,
	@CODEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE