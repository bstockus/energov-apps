CREATE PROCEDURE [dbo].[USP_WORKFLOWACTIONFTP_UPDATE]
(
	@WORKFLOWACTIONFTPID CHAR(36),
	@WORKFLOWACTIONID CHAR(36),
	@FTPSETUPID CHAR(36),
	@SUBPATH NVARCHAR(255),
	@ATTACHMENTGROUPID CHAR(36),
	@FTPFILENAMEOPTIONID INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWACTIONFTPID]  char(36))
UPDATE [dbo].[WORKFLOWACTIONFTP] SET
	[WORKFLOWACTIONID] = @WORKFLOWACTIONID,
	[FTPSETUPID] = @FTPSETUPID,
	[SUBPATH] = @SUBPATH,
	[ATTACHMENTGROUPID] = @ATTACHMENTGROUPID,
	[FTPFILENAMEOPTIONID] = @FTPFILENAMEOPTIONID
OUTPUT inserted.[WORKFLOWACTIONFTPID] INTO @OUTPUTTABLE
WHERE
	[WORKFLOWACTIONFTPID] = @WORKFLOWACTIONFTPID AND 
	[WORKFLOWACTIONID]= @WORKFLOWACTIONID

SELECT * FROM @OUTPUTTABLE