CREATE PROCEDURE [dbo].[USP_WFTEMPLATE_INSERT]
	@WFTEMPLATEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@WFENTITYID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[WFTEMPLATE](
	[WFTEMPLATEID],
	[NAME],
	[DESCRIPTION],
	[WFENTITYID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION] 
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@WFTEMPLATEID,
	@NAME,
	@DESCRIPTION,
	@WFENTITYID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE