CREATE PROCEDURE [dbo].[USP_WORKFLOWACTIONHOLDTYPE_DELETE_BYPARENTID]
(
    @WORKFLOWID CHAR(36),
    @WORKFLOWACTIONID CHAR(36)
    
)
AS
DELETE [dbo].[WORKFLOWACTIONHOLDTYPE] FROM [dbo].[WORKFLOWACTIONHOLDTYPE]
INNER JOIN [dbo].[WORKFLOWACTION] ON [dbo].[WORKFLOWACTIONHOLDTYPE].[WORKFLOWACTIONID] =  [dbo].[WORKFLOWACTION].[WORKFLOWACTIONID]
WHERE
    (@WORKFLOWID IS NULL OR [dbo].[WORKFLOWACTION].[WORKFLOWID]= @WORKFLOWID) 
    AND (@WORKFLOWACTIONID IS NULL OR [dbo].[WORKFLOWACTIONHOLDTYPE].[WORKFLOWACTIONID] = @WORKFLOWACTIONID)