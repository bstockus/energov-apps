CREATE PROCEDURE [dbo].[USP_WORKFLOWACTIONFTP_INSERT]
(
	@WORKFLOWACTIONFTPID CHAR(36),
	@WORKFLOWACTIONID CHAR(36),
	@FTPSETUPID CHAR(36),
	@SUBPATH NVARCHAR(255),
	@ATTACHMENTGROUPID CHAR(36),
	@FTPFILENAMEOPTIONID INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWACTIONFTPID]  char(36))
INSERT INTO [dbo].[WORKFLOWACTIONFTP](
	[WORKFLOWACTIONFTPID],
	[WORKFLOWACTIONID],
	[FTPSETUPID],
	[SUBPATH],
	[ATTACHMENTGROUPID],
	[FTPFILENAMEOPTIONID]
)
OUTPUT inserted.[WORKFLOWACTIONFTPID] INTO @OUTPUTTABLE
VALUES
(
	@WORKFLOWACTIONFTPID,
	@WORKFLOWACTIONID,
	@FTPSETUPID,
	@SUBPATH,
	@ATTACHMENTGROUPID,
	@FTPFILENAMEOPTIONID
)
SELECT * FROM @OUTPUTTABLE