CREATE PROCEDURE [dbo].[USP_WORKFLOWACTIONTASK_INSERT]
(
	@WORKFLOWACTIONTASKID CHAR(36),
	@WORKFLOWACTIONID CHAR(36),
	@TASKSUBJECT NVARCHAR (MAX),
    @TASKTEXT NVARCHAR (MAX),
    @STARTDATE NVARCHAR (MAX),
    @DUEDATE NVARCHAR (MAX),
    @STARTTIME NVARCHAR (MAX),
    @DUETIME NVARCHAR (MAX),
    @STARTDATEOFFSETDAYS INT,
    @DUEDATEOFFSETDAYS INT,
    @TASKPRIORITYID INT,
    @SHOWONCALENDAR BIT,
    @ASSIGNEDTO NVARCHAR (MAX)
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWACTIONTASKID]  char(36))
INSERT INTO [dbo].[WORKFLOWACTIONTASK](
    [WORKFLOWACTIONTASKID],
    [WORKFLOWACTIONID],
    [TASKSUBJECT],
    [TASKTEXT],
    [STARTDATE],
    [DUEDATE],
    [STARTTIME],
    [DUETIME],
    [STARTDATEOFFSETDAYS],
    [DUEDATEOFFSETDAYS],
    [TASKPRIORITYID],
    [SHOWONCALENDAR],
    [ASSIGNEDTO]
)
OUTPUT inserted.[WORKFLOWACTIONTASKID] INTO @OUTPUTTABLE
VALUES
(
    @WORKFLOWACTIONTASKID,
	@WORKFLOWACTIONID,
	@TASKSUBJECT,
    @TASKTEXT,
    @STARTDATE,
    @DUEDATE,
    @STARTTIME,
    @DUETIME,
    @STARTDATEOFFSETDAYS,
    @DUEDATEOFFSETDAYS,
    @TASKPRIORITYID,
    @SHOWONCALENDAR,
    @ASSIGNEDTO
)
SELECT * FROM @OUTPUTTABLE