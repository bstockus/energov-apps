CREATE PROCEDURE [dbo].[USP_WORKFLOWCONDITION_INSERT]
(
	@WORKFLOWCONDITIONID CHAR(36),
	@WORKFLOWCONDITIONGROUPID CHAR(36),
	@CLASSNAME NVARCHAR(MAX),
	@FRIENDLYCLASSNAME NVARCHAR(MAX),
	@PROPERTYNAME NVARCHAR(MAX),
	@FRIENDLYPROPERTYNAME NVARCHAR(MAX),
	@WORKFLOWCLASSCONDITIONTYPEID INT,
	@WORKFLOWPROPERTYCONDTYPEID INT,
	@PROPERTYCONDITIONCOMPAREVALUE NVARCHAR(MAX),
	@CONDITIONNUMBER INT,
	@WORKFLOWCONTROLTYPEID INT,
	@CUSTOMFIELDNAME NVARCHAR(MAX),
	@CUSTOMFIELDTABLE NVARCHAR(MAX)
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWCONDITIONID]  char(36))
INSERT INTO [dbo].[WORKFLOWCONDITION](
	[WORKFLOWCONDITIONID],
	[WORKFLOWCONDITIONGROUPID],
	[CLASSNAME],
	[FRIENDLYCLASSNAME],
	[PROPERTYNAME],
	[FRIENDLYPROPERTYNAME],
	[WORKFLOWCLASSCONDITIONTYPEID],
	[WORKFLOWPROPERTYCONDTYPEID],
	[PROPERTYCONDITIONCOMPAREVALUE],
	[CONDITIONNUMBER],
	[WORKFLOWCONTROLTYPEID],
	[CUSTOMFIELDNAME],
	[CUSTOMFIELDTABLE]
)
OUTPUT inserted.[WORKFLOWCONDITIONID] INTO @OUTPUTTABLE
VALUES
(
	@WORKFLOWCONDITIONID,
	@WORKFLOWCONDITIONGROUPID,
	@CLASSNAME,
	@FRIENDLYCLASSNAME,
	@PROPERTYNAME,
	@FRIENDLYPROPERTYNAME,
	@WORKFLOWCLASSCONDITIONTYPEID,
	@WORKFLOWPROPERTYCONDTYPEID,
	@PROPERTYCONDITIONCOMPAREVALUE,
	@CONDITIONNUMBER,
	@WORKFLOWCONTROLTYPEID,
	@CUSTOMFIELDNAME,
	@CUSTOMFIELDTABLE
)
SELECT * FROM @OUTPUTTABLE