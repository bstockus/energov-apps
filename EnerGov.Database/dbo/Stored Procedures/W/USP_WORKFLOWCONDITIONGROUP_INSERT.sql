CREATE PROCEDURE [dbo].[USP_WORKFLOWCONDITIONGROUP_INSERT]
(
	@WORKFLOWCONDITIONGROUPID CHAR(36),
	@WORKFLOWID CHAR(36),
	@ISORBASED BIT,
	@GROUPNUMBER INT,
	@WORKFLOWACTIONID CHAR(36)
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWCONDITIONGROUPID]  char(36))
INSERT INTO [dbo].[WORKFLOWCONDITIONGROUP](
	[WORKFLOWCONDITIONGROUPID],
	[WORKFLOWID],
	[ISORBASED],
	[GROUPNUMBER],
	[WORKFLOWACTIONID]
)
OUTPUT inserted.[WORKFLOWCONDITIONGROUPID] INTO @OUTPUTTABLE
VALUES
(
	@WORKFLOWCONDITIONGROUPID,
	@WORKFLOWID,
	@ISORBASED,
	@GROUPNUMBER,
	@WORKFLOWACTIONID
)
SELECT * FROM @OUTPUTTABLE