CREATE PROCEDURE [dbo].[USP_WFACTIONGROUP_DELETE]
(
	@WFACTIONGROUPID CHAR(36),
	@ROWVERSION INT
)
AS

SET NOCOUNT ON;

EXEC [globalsetup].[USP_WFACTIONGROUPACTIONXREF_DELETE_BYPARENTID] @WFACTIONGROUPID

SET NOCOUNT OFF;

DELETE FROM [dbo].[WFACTIONGROUP]
WHERE
	[WFACTIONGROUPID] = @WFACTIONGROUPID AND 
	[ROWVERSION]= @ROWVERSION