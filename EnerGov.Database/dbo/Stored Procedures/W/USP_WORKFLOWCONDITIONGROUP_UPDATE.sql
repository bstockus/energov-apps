CREATE PROCEDURE [dbo].[USP_WORKFLOWCONDITIONGROUP_UPDATE]
(
	@WORKFLOWCONDITIONGROUPID CHAR(36),
	@WORKFLOWID CHAR(36),
	@ISORBASED BIT,
	@GROUPNUMBER INT,
	@WORKFLOWACTIONID CHAR(36)
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWCONDITIONGROUPID]  char(36))
UPDATE [dbo].[WORKFLOWCONDITIONGROUP] SET
	[WORKFLOWID] = @WORKFLOWID,
	[ISORBASED] = @ISORBASED,
	[GROUPNUMBER] = @GROUPNUMBER,
	[WORKFLOWACTIONID] = @WORKFLOWACTIONID
OUTPUT inserted.[WORKFLOWCONDITIONGROUPID] INTO @OUTPUTTABLE
WHERE
	[WORKFLOWCONDITIONGROUPID] = @WORKFLOWCONDITIONGROUPID AND 
	[WORKFLOWID]= @WORKFLOWID

SELECT * FROM @OUTPUTTABLE