CREATE PROCEDURE [dbo].[USP_WORKFLOWACTION_UPDATE]
(
	@WORKFLOWACTIONID CHAR(36),
	@ACTIONNAME NVARCHAR(50),
	@CUSTOMMESSAGE NVARCHAR(MAX),
	@ACTIONCLASSNAME NVARCHAR(MAX),
	@RUNONLYWHENDIRTY BIT,
	@DISABLEQUEUING BIT,
	@POSTWORKFLOW BIT	
)
AS
UPDATE [dbo].[WORKFLOWACTION] SET
	[ACTIONNAME] = @ACTIONNAME,
	[CUSTOMMESSAGE] = @CUSTOMMESSAGE,
	[ACTIONCLASSNAME] = @ACTIONCLASSNAME,
	[RUNONLYWHENDIRTY] = @RUNONLYWHENDIRTY,
	[DISABLEQUEUING] = @DISABLEQUEUING,
	[POSTWORKFLOW] = @POSTWORKFLOW
WHERE [WORKFLOWACTIONID] = @WORKFLOWACTIONID