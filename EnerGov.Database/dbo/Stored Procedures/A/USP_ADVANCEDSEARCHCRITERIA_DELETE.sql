CREATE PROCEDURE [dbo].[USP_ADVANCEDSEARCHCRITERIA_DELETE]
(
	@ADVANCEDSEARCHCRITERIAID CHAR(36)
)
AS

EXEC USP_ADVANCEDSEARCHCRITERIASEARCHFIELD_DELETEBYPARENTID @ADVANCEDSEARCHCRITERIAID

EXEC USP_ADVANCEDSEARCHOUTPUTFIELD_DELETEBYPARENTID @ADVANCEDSEARCHCRITERIAID

EXEC USP_ADVANCEDSEARCHSORTFIELD_DELETEBYPARENTID @ADVANCEDSEARCHCRITERIAID

DELETE FROM [dbo].[ADVANCEDSEARCHCRITERIA]
WHERE
	[ADVANCEDSEARCHCRITERIAID] = @ADVANCEDSEARCHCRITERIAID