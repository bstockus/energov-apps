CREATE PROCEDURE [dbo].[USP_ADDRESSLAYOUTSETUP_UPDATE]
(
	@ADDRESSLAYOUTSETUPID CHAR(36),
	@COUNTRYID INT,
	@KEYTEXTBLOCKNAME VARCHAR(100),
	@KEYCONTROLNAME VARCHAR(100),
	@KEYBINDINGPROPERTYNAME VARCHAR(100),
	@DEFAULTLABEL VARCHAR(100),
	@NEWLABEL VARCHAR(100),
	@DEFAULTTABINDEX INT,
	@NEWTABINDEX INT,
	@ISHIDE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[ADDRESSLAYOUTSETUP] SET
	[COUNTRYID] = @COUNTRYID,
	[KEYTEXTBLOCKNAME] = @KEYTEXTBLOCKNAME,
	[KEYCONTROLNAME] = @KEYCONTROLNAME,
	[KEYBINDINGPROPERTYNAME] = @KEYBINDINGPROPERTYNAME,
	[DEFAULTLABEL] = @DEFAULTLABEL,
	[NEWLABEL] = @NEWLABEL,
	[DEFAULTTABINDEX] = @DEFAULTTABINDEX,
	[NEWTABINDEX] = @NEWTABINDEX,
	[ISHIDE] = @ISHIDE,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[ADDRESSLAYOUTSETUPID] = @ADDRESSLAYOUTSETUPID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE