CREATE PROCEDURE [dbo].[ADDERPORTALALLOWEDUSER]
-- Add the parameters for the stored procedure here
@APPLICATIONALLOWEDID char(36),
@USERID char(36)
AS
BEGIN	
	DECLARE @APPLICATIONUSERTYPEID char(36)
	DECLARE @APPLICATIONID int
	SELECT @APPLICATIONUSERTYPEID = APPLICATIONUSERTYPEID FROM APPLICATIONUSERTYPE WHERE TYPEID = 1 AND APPLICATIONID = 2	
	SET @APPLICATIONID = 2
	INSERT INTO APPLICATIONALLOWED 	
	(
	APPLICATIONALLOWEDID,
	USERID,
	APPLICATIONID,
	APPLICATIONUSERTYPEID,
	APPROVED	
	)
	VALUES(
	@APPLICATIONALLOWEDID,
	@USERID,
	@APPLICATIONID,
	@APPLICATIONUSERTYPEID,
	0
	)		
END
