(
	@ATTACHMENTGROUPTCMXREFID CHAR(36),
	@ATTACHMENTGROUPID CHAR(36),
	@ROWVERSION INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[ATTACHMENTGROUPTCMXREF](
	[ATTACHMENTGROUPTCMXREFID],
	[ATTACHMENTGROUPID],
	[ROWVERSION],
	[LASTCHANGEDBY],
	[LASTCHANGEDON]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@ATTACHMENTGROUPTCMXREFID,
	@ATTACHMENTGROUPID,
	@ROWVERSION,
	@LASTCHANGEDBY,
	@LASTCHANGEDON
)
SELECT * FROM @OUTPUTTABLE