CREATE PROCEDURE [dbo].[USP_APPLICATIONROUTEMAPPING_GETALL]
AS
BEGIN 
	SET NOCOUNT ON;
	SELECT 
		[dbo].[APPLICATIONROUTEMAPPING].[APPLICATION],
		[dbo].[APPLICATIONROUTEMAPPING].[ROUTE],
		[dbo].[APPLICATIONROUTEMAPPING].[PERMISSIONTYPE],
		[dbo].[APPLICATIONROUTEMAPPING].[FORMID],
		[dbo].[APPLICATIONROUTEMAPPING].[REQUIRESTYPECHECK]
	 FROM [dbo].[APPLICATIONROUTEMAPPING] ORDER BY [APPLICATION], [ROUTE]
END