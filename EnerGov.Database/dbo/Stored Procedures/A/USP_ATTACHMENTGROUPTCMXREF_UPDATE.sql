CREATE PROCEDURE [dbo].[USP_ATTACHMENTGROUPTCMXREF_UPDATE]
(
	@ATTACHMENTGROUPTCMXREFID CHAR(36),
	@ATTACHMENTGROUPID CHAR(36),
	@ROWVERSION INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[ATTACHMENTGROUPTCMXREF] SET
	[ATTACHMENTGROUPID] = @ATTACHMENTGROUPID,
	[ROWVERSION] = @ROWVERSION + 1,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[ATTACHMENTGROUPTCMXREFID] = @ATTACHMENTGROUPTCMXREFID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE