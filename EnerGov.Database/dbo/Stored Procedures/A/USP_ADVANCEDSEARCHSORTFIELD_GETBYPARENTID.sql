CREATE PROCEDURE [dbo].[USP_ADVANCEDSEARCHSORTFIELD_GETBYPARENTID]
(
	@SEARCHCRITERIAID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[ADVANCEDSEARCHSORTFIELDID],
	[SEARCHCRITERIAID],
	[FIELDID],
	[CUSTOMFIELDID],
	[DESCENDING]
FROM [dbo].[ADVANCEDSEARCHSORTFIELD]
WHERE
	[SEARCHCRITERIAID] = @SEARCHCRITERIAID  

END