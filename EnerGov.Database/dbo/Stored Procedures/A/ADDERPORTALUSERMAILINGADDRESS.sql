	@MAILINGADDRESSID,
	NULL,
	@ADDRESSLINE1,
	@ADDRESSLINE2,
	@UNITORSUITE,
	@STREETTYPE,
	@CITY,
	@COUNTY,
	@STATE,
	@POSTALCODE,
	@COUNTRYTYPE,
	@LASTCHANGEDON,
	@LASTCHANGEDBY,
	1	
	)		
END
