CREATE PROCEDURE [dbo].[USP_ADVANCEDSEARCHOUTPUTFIELD_GETBYPARENTID]
(
	@SEARCHCRITERIAID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[ADVANCEDSEARCHOUTPUTFIELDID],
	[SEARCHCRITERIAID],
	[FIELDID],
	[CUSTOMFIELDID],
	[ORDER],
	[CALCULATETOTAL],
	[CUSTOMFIELDTABLEID]
FROM [dbo].[ADVANCEDSEARCHOUTPUTFIELD]
WHERE
	[SEARCHCRITERIAID] = @SEARCHCRITERIAID  

END