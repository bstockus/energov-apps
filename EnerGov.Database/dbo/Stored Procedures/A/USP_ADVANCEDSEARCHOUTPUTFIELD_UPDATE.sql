CREATE PROCEDURE [dbo].[USP_ADVANCEDSEARCHOUTPUTFIELD_UPDATE]
(
	@ADVANCEDSEARCHOUTPUTFIELDID CHAR(36),
	@SEARCHCRITERIAID CHAR(36),
	@FIELDID INT,
	@CUSTOMFIELDID CHAR(36),
	@ORDER INT,
	@CALCULATETOTAL BIT,
	@CUSTOMFIELDTABLEID CHAR(36)
)
AS
UPDATE [dbo].[ADVANCEDSEARCHOUTPUTFIELD] SET
	[ADVANCEDSEARCHOUTPUTFIELDID] = @ADVANCEDSEARCHOUTPUTFIELDID,
	[SEARCHCRITERIAID] = @SEARCHCRITERIAID,
	[FIELDID] = @FIELDID,
	[CUSTOMFIELDID] = @CUSTOMFIELDID,
	[ORDER] = @ORDER,
	[CALCULATETOTAL] = @CALCULATETOTAL,
	[CUSTOMFIELDTABLEID] = @CUSTOMFIELDTABLEID

WHERE
	[ADVANCEDSEARCHOUTPUTFIELDID] = @ADVANCEDSEARCHOUTPUTFIELDID