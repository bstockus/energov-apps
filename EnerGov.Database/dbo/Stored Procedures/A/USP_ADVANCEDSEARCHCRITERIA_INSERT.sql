CREATE PROCEDURE [dbo].[USP_ADVANCEDSEARCHCRITERIA_INSERT]
(
	@ADVANCEDSEARCHCRITERIAID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@USERID CHAR(36),
	@SHARED BIT,
	@SEARCHMODULE INT,
	@PAGESIZE INT,
	@PAGENUMBER INT,
	@CREATEDON DATETIME,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@CHILDMODULEPATH NVARCHAR(100)
)
AS
INSERT INTO [dbo].[ADVANCEDSEARCHCRITERIA](
	[ADVANCEDSEARCHCRITERIAID],
	[NAME],
	[DESCRIPTION],
	[USERID],
	[SHARED],
	[SEARCHMODULE],
	[PAGESIZE],
	[PAGENUMBER],
	[CREATEDON],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION],
	[CHILDMODULEPATH]
)
OUTPUT inserted.[ROWVERSION]
VALUES
(
	@ADVANCEDSEARCHCRITERIAID,
	@NAME,
	@DESCRIPTION,
	@USERID,
	@SHARED,
	@SEARCHMODULE,
	@PAGESIZE,
	@PAGENUMBER,
	@CREATEDON,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION,
	@CHILDMODULEPATH
)