CREATE PROCEDURE [dbo].[USP_ILLICENSESTATUS_INSERT]
(
	@ILLICENSESTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@ILLICENSESTATUSSYSTEMID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[ILLICENSESTATUS](
	[ILLICENSESTATUSID],
	[NAME],
	[DESCRIPTION],
	[ILLICENSESTATUSSYSTEMID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@ILLICENSESTATUSID,
	@NAME,
	@DESCRIPTION,
	@ILLICENSESTATUSSYSTEMID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE