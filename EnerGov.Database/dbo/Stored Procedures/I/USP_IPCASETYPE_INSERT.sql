CREATE PROCEDURE [dbo].[USP_IPCASETYPE_INSERT]
(
	@IPCASETYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@PREFIX NVARCHAR(10),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@DEFAULTSTATUSID CHAR(36),
	@DAYSTOCASEAPPROVALEXPIRE INT,
	@ACTIVE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IPCASETYPE](
	[IPCASETYPEID],
	[NAME],
	[DESCRIPTION],
	[PREFIX],
	[CUSTOMFIELDLAYOUTID],
	[DEFAULTSTATUSID],
	[DAYSTOCASEAPPROVALEXPIRE],
	[ACTIVE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IPCASETYPEID,
	@NAME,
	@DESCRIPTION,
	@PREFIX,
	@CUSTOMFIELDLAYOUTID,
	@DEFAULTSTATUSID,
	@DAYSTOCASEAPPROVALEXPIRE,
	@ACTIVE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE