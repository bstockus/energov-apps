	@NAME,
	@DESCRIPTION,
	@PREFIX,
	@CUSTOMFIELDLAYOUTID,
	@CANEDIT,
	@CREATEID,
	@ALLOWDUPLICATE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
