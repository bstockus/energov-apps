CREATE PROCEDURE [dbo].[USP_IPCONDITIONTYPE_INSERT]
(
	@IPCONDITIONTYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@DEFAULTSTATUSID CHAR(36),
	@IPCONDITIONCATEGORYID CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IPCONDITIONTYPE](
	[IPCONDITIONTYPEID],
	[NAME],
	[DESCRIPTION],
	[DEFAULTSTATUSID],
	[IPCONDITIONCATEGORYID],
	[CAFEETEMPLATEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IPCONDITIONTYPEID,
	@NAME,
	@DESCRIPTION,
	@DEFAULTSTATUSID,
	@IPCONDITIONCATEGORYID,
	@CAFEETEMPLATEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE