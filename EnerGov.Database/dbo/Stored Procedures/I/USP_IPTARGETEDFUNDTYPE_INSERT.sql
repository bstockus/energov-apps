CREATE PROCEDURE [dbo].[USP_IPTARGETEDFUNDTYPE_INSERT]
(
	@IPTARGETEDFUNDTYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@ACTIVE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IPTARGETEDFUNDTYPE](
	[IPTARGETEDFUNDTYPEID],
	[NAME],
	[DESCRIPTION],
	[ACTIVE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IPTARGETEDFUNDTYPEID,
	@NAME,
	@DESCRIPTION,
	@ACTIVE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE