CREATE PROCEDURE [dbo].[USP_ILMAPPEDVALUECLASS_INSERT]
(
	@MAPPEDVALUEID CHAR(36),
	@MAPPEDVALUE VARCHAR(50),
	@ILLICENSECLASSIFICATIONID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[ILMAPPEDVALUECLASS](
	[MAPPEDVALUEID],
	[MAPPEDVALUE],
	[ILLICENSECLASSIFICATIONID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@MAPPEDVALUEID,
	@MAPPEDVALUE,
	@ILLICENSECLASSIFICATIONID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE