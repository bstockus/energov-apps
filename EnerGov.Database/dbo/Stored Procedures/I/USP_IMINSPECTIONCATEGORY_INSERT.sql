CREATE PROCEDURE [dbo].[USP_IMINSPECTIONCATEGORY_INSERT]
(
	@IMINSPECTIONCATEGORYID CHAR(36),
	@NAME NVARCHAR(100),
	@CATEGORYLIMIT INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IMINSPECTIONCATEGORY](
	[IMINSPECTIONCATEGORYID],
	[NAME],
	[CATEGORYLIMIT],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT [inserted].[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IMINSPECTIONCATEGORYID,
	@NAME,
	@CATEGORYLIMIT,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE