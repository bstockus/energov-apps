CREATE PROCEDURE [dbo].[USP_IPCASETYPE_UPDATE]
(
	@IPCASETYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@PREFIX NVARCHAR(10),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@DEFAULTSTATUSID CHAR(36),
	@DAYSTOCASEAPPROVALEXPIRE INT,
	@ACTIVE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[IPCASETYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[PREFIX] = @PREFIX,
	[CUSTOMFIELDLAYOUTID] = @CUSTOMFIELDLAYOUTID,
	[DEFAULTSTATUSID] = @DEFAULTSTATUSID,
	[DAYSTOCASEAPPROVALEXPIRE] = @DAYSTOCASEAPPROVALEXPIRE,
	[ACTIVE] = @ACTIVE,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[IPCASETYPEID] = @IPCASETYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE