CREATE PROCEDURE [dbo].[USP_IMINSPECTIONCASESTATUS_INSERT]
(
	@IMINSPECTIONCASESTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@ACTIVE BIT,
	@PAUSE BIT,
	@COMPLETE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IMINSPECTIONCASESTATUS](
	[IMINSPECTIONCASESTATUSID],
	[NAME],
	[DESCRIPTION],
	[ACTIVE],
	[PAUSE],
	[COMPLETE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IMINSPECTIONCASESTATUSID,
	@NAME,
	@DESCRIPTION,
	@ACTIVE,
	@PAUSE,
	@COMPLETE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE