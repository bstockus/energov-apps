CREATE PROCEDURE [dbo].[USP_IPCASESTATUS_INSERT]
(
	@IPCASESTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@APPROVAL BIT,
	@ACTIVE BIT,
	@CANCELLED BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IPCASESTATUS](
	[IPCASESTATUSID],
	[NAME],
	[DESCRIPTION],
	[APPROVAL],
	[ACTIVE],
	[CANCELLED],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IPCASESTATUSID,
	@NAME,
	@DESCRIPTION,
	@APPROVAL,
	@ACTIVE,
	@CANCELLED,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE