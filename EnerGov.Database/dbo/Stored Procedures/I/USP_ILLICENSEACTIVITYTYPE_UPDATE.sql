CREATE PROCEDURE [dbo].[USP_ILLICENSEACTIVITYTYPE_UPDATE]
(
	@ILLICENSEACTIVITYTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@PREFIX NVARCHAR(20),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@CANEDIT BIT,
	@CREATEID BIT,
	@ALLOWDUPLICATE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[ILLICENSEACTIVITYTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[PREFIX] = @PREFIX,
	[CUSTOMFIELDLAYOUTID] = @CUSTOMFIELDLAYOUTID,
	[CANEDIT] = @CANEDIT,
	[CREATEID] = @CREATEID,
	[ALLOWDUPLICATE] = @ALLOWDUPLICATE,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[ILLICENSEACTIVITYTYPEID] = @ILLICENSEACTIVITYTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE