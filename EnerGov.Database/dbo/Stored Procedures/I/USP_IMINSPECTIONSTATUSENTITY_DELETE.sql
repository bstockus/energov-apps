CREATE PROCEDURE [dbo].[USP_IMINSPECTIONSTATUSENTITY_DELETE]
(
	@IMINSPECTIONSTATUSENTITYID CHAR(36),
	@ROWVERSION INT
)
AS

SET NOCOUNT ON;
EXEC [dbo].[USP_IMINSPECTIONSTATUS_DELETE_BYPARENTID] @IMINSPECTIONSTATUSENTITYID
SET NOCOUNT OFF;

DELETE FROM [dbo].[IMINSPECTIONSTATUSENTITY]
WHERE
	[IMINSPECTIONSTATUSENTITYID] = @IMINSPECTIONSTATUSENTITYID  AND	  
	[ROWVERSION]= @ROWVERSION