CREATE PROCEDURE [dbo].[USP_ILMAPPEDVALUECLASS_UPDATE]
(
	@MAPPEDVALUEID CHAR(36),
	@MAPPEDVALUE VARCHAR(50),
	@ILLICENSECLASSIFICATIONID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[ILMAPPEDVALUECLASS] SET
	[MAPPEDVALUE] = @MAPPEDVALUE,
	[ILLICENSECLASSIFICATIONID] = @ILLICENSECLASSIFICATIONID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[MAPPEDVALUEID] = @MAPPEDVALUEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE