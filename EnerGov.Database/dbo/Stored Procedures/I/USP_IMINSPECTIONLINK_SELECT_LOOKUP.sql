CREATE PROCEDURE [dbo].[USP_IMINSPECTIONLINK_SELECT_LOOKUP]
AS
  SET NOCOUNT ON;
  SELECT
    [dbo].[IMINSPECTIONLINK].[IMINSPECTIONLINKID],
    [dbo].[IMINSPECTIONLINK].[NAME]
  FROM [dbo].[IMINSPECTIONLINK]
  WHERE [dbo].[IMINSPECTIONLINK].[IMINSPECTIONLINKID] <> 15 -- IMINSPECTIONLINKID 15 is 'Fire Occupancy'
  ORDER BY CASE
    WHEN [dbo].[IMINSPECTIONLINK].[IMINSPECTIONLINKID] = 0 THEN '' -- IMINSPECTIONLINKID 0 is 'NONE'
    ELSE  [dbo].[IMINSPECTIONLINK].[NAME]
  END