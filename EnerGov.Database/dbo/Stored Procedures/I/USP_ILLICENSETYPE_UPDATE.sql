CREATE PROCEDURE [dbo].[USP_ILLICENSETYPE_UPDATE]
(
	@ILLICENSETYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@KEEPLICENSENUMBER BIT,
	@AUTONUMBER BIT,
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@PREFIX NVARCHAR(10),
	@DEFAULTLICENSESTATUSID CHAR(36),
	@FRIENDLYNAME NVARCHAR(100),
	@DEFAULTWEBAPPLYSTATUSID CHAR(36),
	@DEFAULTWEBRENEWSTATUSID CHAR(36),
	@ACTIVE BIT,
	@USESLICENSEGROUP BIT,
	@USESLICENSECLASSTYPES BIT,
	@ISRENEWABLE BIT,
	@ALLOWWEBSUBMISSION BIT,
	@ALLOWRENEWALLICENSENUM BIT,
	@ISRECEIPTSTYPE BIT,
	@DEFAULTRENEWSTATUSID CHAR(36),
	@SETRENEWISSDATETOPRVEXPDATE BIT,
	@SETISSUEDATEONRENEWAL BIT,
	@CURRENTYEARONRENEWALS BIT,
	@ILLICENSESYSTEMTYPEID INT,
	@ALLOWWEBRENEWAL BIT,
	@REQUIRELICENSEGROUP BIT,
	@REQUIRELICENSECLASSTYPE BIT,
	@SKIPPAYMENT BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[ILLICENSETYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[KEEPLICENSENUMBER] = @KEEPLICENSENUMBER,
	[AUTONUMBER] = @AUTONUMBER,
	[CUSTOMFIELDLAYOUTID] = @CUSTOMFIELDLAYOUTID,
	[PREFIX] = @PREFIX,
	[DEFAULTLICENSESTATUSID] = @DEFAULTLICENSESTATUSID,
	[FRIENDLYNAME] = @FRIENDLYNAME,
	[DEFAULTWEBAPPLYSTATUSID] = @DEFAULTWEBAPPLYSTATUSID,
	[DEFAULTWEBRENEWSTATUSID] = @DEFAULTWEBRENEWSTATUSID,
	[ACTIVE] = @ACTIVE,
	[USESLICENSEGROUP] = @USESLICENSEGROUP,
	[USESLICENSECLASSTYPES] = @USESLICENSECLASSTYPES,
	[ISRENEWABLE] = @ISRENEWABLE,
	[ALLOWWEBSUBMISSION] = @ALLOWWEBSUBMISSION,
	[ALLOWRENEWALLICENSENUM] = @ALLOWRENEWALLICENSENUM,
	[ISRECEIPTSTYPE] = @ISRECEIPTSTYPE,
	[DEFAULTRENEWSTATUSID] = @DEFAULTRENEWSTATUSID,
	[SETRENEWISSDATETOPRVEXPDATE] = @SETRENEWISSDATETOPRVEXPDATE,
	[SETISSUEDATEONRENEWAL] = @SETISSUEDATEONRENEWAL,
	[CURRENTYEARONRENEWALS] = @CURRENTYEARONRENEWALS,
	[ILLICENSESYSTEMTYPEID] = @ILLICENSESYSTEMTYPEID,
	[ALLOWWEBRENEWAL] = @ALLOWWEBRENEWAL,
	[REQUIRELICENSEGROUP] = @REQUIRELICENSEGROUP,
	[REQUIRELICENSECLASSTYPE] = @REQUIRELICENSECLASSTYPE,
	[SKIPPAYMENT] = @SKIPPAYMENT,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[ILLICENSETYPEID] = @ILLICENSETYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE