CREATE PROCEDURE [dbo].[USP_IMINSPECTIONZONE_INSERT]
(
	@IMINSPECTIONZONEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@DEFAULTTRAVELTIME DECIMAL(18,4),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IMINSPECTIONZONE](
	[IMINSPECTIONZONEID],
	[NAME],
	[DESCRIPTION],
	[DEFAULTTRAVELTIME],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IMINSPECTIONZONEID,
	@NAME,
	@DESCRIPTION,
	@DEFAULTTRAVELTIME,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE