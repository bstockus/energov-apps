
/****** Object:  StoredProcedure [dbo].[rpt_IP_SR_Impact_Case_Detail_Report_Contact]    Script Date: 01/08/2014 15:58:53 ******/
CREATE PROCEDURE [dbo].[rpt_IP_SR_Impact_Case_Detail_Report_Contact]
@IPCASEID AS VARCHAR(36)
AS
SELECT	IPCASECONTACT.GLOBALENTITYID, 
		LANDMANAGEMENTCONTACTTYPE.NAME AS CONTACTTYPE, 
		GLOBALENTITY.GLOBALENTITYNAME, GLOBALENTITY.FIRSTNAME, GLOBALENTITY.LASTNAME, GLOBALENTITY.BUSINESSPHONE, GLOBALENTITY.HOMEPHONE, GLOBALENTITY.MOBILEPHONE,
		MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.ADDRESSLINE2, MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, MAILINGADDRESS.POSTALCODE, 
		MAILINGADDRESS.POSTDIRECTION, MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.STREETTYPE, MAILINGADDRESS.ADDRESSTYPE, MAILINGADDRESS.UNITORSUITE
		

FROM	IPCASECONTACT 
		INNER JOIN LANDMANAGEMENTCONTACTTYPE ON IPCASECONTACT.CONTACTTYPEID = LANDMANAGEMENTCONTACTTYPE.LANDMANAGEMENTCONTACTTYPEID 
		INNER JOIN GLOBALENTITY ON IPCASECONTACT.GLOBALENTITYID = GLOBALENTITY.GLOBALENTITYID 
		LEFT OUTER JOIN GLOBALENTITYMAILINGADDRESS ON GLOBALENTITYMAILINGADDRESS.GLOBALENTITYID = GLOBALENTITY.GLOBALENTITYID
		LEFT OUTER JOIN MAILINGADDRESS ON MAILINGADDRESS.MAILINGADDRESSID = GLOBALENTITYMAILINGADDRESS.MAILINGADDRESSID 

WHERE IPCASECONTACT.IPCASEID = @IPCASEID

