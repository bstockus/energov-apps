CREATE PROCEDURE [dbo].[USP_IMSCHEDULEDTIMEGROUP_INSERT]
(
	@IMSCHEDULEDTIMEGROUPID CHAR(36),
	@NAME NVARCHAR(50),
	@STARTTIME DATETIME,
	@ENDTIME DATETIME,
	@IVRNUMBER INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IMSCHEDULEDTIMEGROUP](
	[IMSCHEDULEDTIMEGROUPID],
	[NAME],
	[STARTTIME],
	[ENDTIME],
	[IVRNUMBER],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IMSCHEDULEDTIMEGROUPID,
	@NAME,
	@STARTTIME,
	@ENDTIME,
	@IVRNUMBER,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE