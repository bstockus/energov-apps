



CREATE PROCEDURE [dbo].[rpt_IM_Inspection_Listing_Report_By_Scheduled_Start_Date_Address]
@INSPECTIONID AS VARCHAR(36)
AS
SELECT     MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.ADDRESSLINE2, MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, 
                      MAILINGADDRESS.POSTALCODE, MAILINGADDRESS.POSTDIRECTION, MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.STREETTYPE, 
                      MAILINGADDRESS.UNITORSUITE
FROM         IMINSPECTIONADDRESS INNER JOIN
                      MAILINGADDRESS ON IMINSPECTIONADDRESS.MAILINGADDRESSID = MAILINGADDRESS.MAILINGADDRESSID
WHERE IMINSPECTIONADDRESS.IMINSPECTIONID = @INSPECTIONID AND IMINSPECTIONADDRESS.MAIN = 'TRUE'



