CREATE PROCEDURE [dbo].[USP_ILMAPPEDVALUEGROUP_INSERT]
(
	@MAPPEDVALUEGROUPID CHAR(36),
	@MAPPEDVALUE VARCHAR(50),
	@ILLICENSEGROUPID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[ILMAPPEDVALUEGROUP](
	[MAPPEDVALUEGROUPID],
	[MAPPEDVALUE],
	[ILLICENSEGROUPID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@MAPPEDVALUEGROUPID,
	@MAPPEDVALUE,
	@ILLICENSEGROUPID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE