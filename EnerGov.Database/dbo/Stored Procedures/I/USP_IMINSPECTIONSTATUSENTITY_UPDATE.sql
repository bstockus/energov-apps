CREATE PROCEDURE [dbo].[USP_IMINSPECTIONSTATUSENTITY_UPDATE]
(
	@IMINSPECTIONSTATUSENTITYID CHAR(36),
	@NAME NVARCHAR(255),
	@IMINSPECTIONLINKID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[IMINSPECTIONSTATUSENTITY] SET
	[NAME] = @NAME,
	[IMINSPECTIONLINKID] = @IMINSPECTIONLINKID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
	OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[IMINSPECTIONSTATUSENTITYID] = @IMINSPECTIONSTATUSENTITYID AND 
	[ROWVERSION]= @ROWVERSION
		
SELECT * FROM @OUTPUTTABLE