CREATE PROCEDURE [dbo].[USP_IMINSPECTIONTYPECHKLSTXREF_UPDATE]
(
	@IMINSPECTIONTYPECHKLSTXREFID CHAR(36),
	@IMCHECKLISTID CHAR(36),
	@IMINSPECTIONTYPEID CHAR(36),
	@SORTORDER INT,
	@AUTOADD BIT
)
AS

UPDATE [dbo].[IMINSPECTIONTYPECHKLSTXREF] SET
	[IMCHECKLISTID] = @IMCHECKLISTID,
	[IMINSPECTIONTYPEID] = @IMINSPECTIONTYPEID,
	[SORTORDER] = @SORTORDER,
	[AUTOADD] = @AUTOADD

WHERE
	[IMINSPECTIONTYPECHKLSTXREFID] = @IMINSPECTIONTYPECHKLSTXREFID