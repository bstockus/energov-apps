CREATE PROCEDURE [dbo].[USP_IPASSESSMENTMILESTONE_INSERT]
(
	@IPASSESSMENTMILESTONEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@IPMILESTONETYPEID INT,
	@ACTIVE BIT,
	@CONFIGUREFIELDID CHAR(36),
	@MODULEID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IPASSESSMENTMILESTONE](
	[IPASSESSMENTMILESTONEID],
	[NAME],
	[DESCRIPTION],
	[IPMILESTONETYPEID],
	[ACTIVE],
	[CONFIGUREFIELDID],
	[MODULEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IPASSESSMENTMILESTONEID,
	@NAME,
	@DESCRIPTION,
	@IPMILESTONETYPEID,
	@ACTIVE,
	@CONFIGUREFIELDID,
	@MODULEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE