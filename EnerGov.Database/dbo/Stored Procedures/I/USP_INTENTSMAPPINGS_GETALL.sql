CREATE PROCEDURE [dbo].[USP_INTENTSMAPPINGS_GETALL]
AS
BEGIN 
	SET NOCOUNT ON;
	SELECT 
		[dbo].[INTENTSMAPPINGS].[APPLICATIONID],
		[dbo].[INTENTSMAPPINGS].[MIMETYPE],
		[dbo].[INTENTSMAPPINGS].[ACTION],
		[dbo].[INTENTSMAPPINGS].[CATEGORY],
		[dbo].[INTENTSMAPPINGS].[PRODUCT],
		[dbo].[INTENTSMAPPINGS].[SUBMENUID],
		[dbo].[INTENTSMAPPINGS].[SUBMENUORDER],
		[dbo].[INTENTSMAPPINGS].[FORMID]
	 FROM [dbo].[INTENTSMAPPINGS] ORDER BY [APPLICATIONID], [MIMETYPE], [ACTION], [CATEGORY]
END