CREATE PROCEDURE [dbo].[USP_IMNONCOMPLIANCECODE_UPDATE]
(
	@IMNONCOMPLIANCECODEID CHAR(36),
	@IMNONCOMPLIANCECATEGORYID CHAR(36),
	@DEFAULTRESPONSIBLEPARTYID INT,
	@DEFAULTNONCOMPLIANCERISKID INT,
	@COMPLIANCEDAYSUNTILDUE INT,
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(2000),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[IMNONCOMPLIANCECODE] SET
	[IMNONCOMPLIANCECATEGORYID] = @IMNONCOMPLIANCECATEGORYID,
	[DEFAULTRESPONSIBLEPARTYID] = @DEFAULTRESPONSIBLEPARTYID,
	[DEFAULTNONCOMPLIANCERISKID] = @DEFAULTNONCOMPLIANCERISKID,
	[COMPLIANCEDAYSUNTILDUE] = @COMPLIANCEDAYSUNTILDUE,
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[IMNONCOMPLIANCECODEID] = @IMNONCOMPLIANCECODEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE