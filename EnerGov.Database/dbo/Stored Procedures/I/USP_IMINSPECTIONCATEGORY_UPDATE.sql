CREATE PROCEDURE [dbo].[USP_IMINSPECTIONCATEGORY_UPDATE]
(
	@IMINSPECTIONCATEGORYID CHAR(36),
	@NAME NVARCHAR(100),
	@CATEGORYLIMIT INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[IMINSPECTIONCATEGORY] SET
	[NAME] = @NAME,
	[CATEGORYLIMIT] = @CATEGORYLIMIT,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT [inserted].[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[dbo].[IMINSPECTIONCATEGORY].[IMINSPECTIONCATEGORYID] = @IMINSPECTIONCATEGORYID AND 
	[dbo].[IMINSPECTIONCATEGORY].[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE