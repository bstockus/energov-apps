CREATE PROCEDURE [dbo].[USP_IMINSPECTIONCASETYPE_INSERT]
(
	@IMINSPECTIONCASETYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@IMINSPECTIONTYPEID CHAR(36),
	@ACTIVE BIT,
	@RECURRENCELIMITTYPE INT,
	@IMINSPECTIONCASESTATUSID CHAR(36),
	@RECURRENCELIMIT INT,
	@PREFIX NVARCHAR(10),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@RECURRENCEID CHAR(36),
	@ASSIGNINSPECTORVIAGIS BIT,
	@NOTSCHEDULEINSPECTION BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IMINSPECTIONCASETYPE](
	[IMINSPECTIONCASETYPEID],
	[NAME],
	[DESCRIPTION],
	[IMINSPECTIONTYPEID],
	[ACTIVE],
	[RECURRENCELIMITTYPE],
	[IMINSPECTIONCASESTATUSID],
	[RECURRENCELIMIT],
	[PREFIX],
	[CUSTOMFIELDLAYOUTID],
	[RECURRENCEID],
	[ASSIGNINSPECTORVIAGIS],
	[NOTSCHEDULEINSPECTION],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IMINSPECTIONCASETYPEID,
	@NAME,
	@DESCRIPTION,
	@IMINSPECTIONTYPEID,
	@ACTIVE,
	@RECURRENCELIMITTYPE,
	@IMINSPECTIONCASESTATUSID,
	@RECURRENCELIMIT,
	@PREFIX,
	@CUSTOMFIELDLAYOUTID,
	@RECURRENCEID,
	@ASSIGNINSPECTORVIAGIS,
	@NOTSCHEDULEINSPECTION,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE