CREATE PROCEDURE [dbo].[USP_IPCONDITIONSTATUS_INSERT]
(
	@IPCONDITIONSTATUSID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@IPCONDITIONSYSTEMSTATUSID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IPCONDITIONSTATUS](
	[IPCONDITIONSTATUSID],
	[NAME],
	[DESCRIPTION],
	[IPCONDITIONSYSTEMSTATUSID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IPCONDITIONSTATUSID,
	@NAME,
	@DESCRIPTION,
	@IPCONDITIONSYSTEMSTATUSID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE