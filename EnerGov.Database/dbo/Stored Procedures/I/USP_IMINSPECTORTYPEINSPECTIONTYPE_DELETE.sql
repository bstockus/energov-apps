CREATE PROCEDURE [dbo].[USP_IMINSPECTORTYPEINSPECTIONTYPE_DELETE]
(
@IMINSPECTORTYPEID CHAR(36),
@IMINSPECTIONTYPEID CHAR(36)
)
AS
DELETE FROM [dbo].[IMINSPECTORTYPEINSPECTIONTYPE]
WHERE
	[dbo].[IMINSPECTORTYPEINSPECTIONTYPE].[IMINSPECTORTYPEID] = @IMINSPECTORTYPEID AND 
	[dbo].[IMINSPECTORTYPEINSPECTIONTYPE].[IMINSPECTIONTYPEID] = @IMINSPECTIONTYPEID