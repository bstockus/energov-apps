

/****** Object:  StoredProcedure [dbo].[rpt_IP_SR_Impact_Case_Detail_Report_Address]    Script Date: 01/08/2014 15:58:53 ******/
CREATE PROCEDURE [dbo].[rpt_IP_SR_Impact_Case_Detail_Report_Address]
@IPCASEID AS VARCHAR(36)
AS
SELECT	MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.ADDRESSLINE2, MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, MAILINGADDRESS.POSTALCODE, 
		MAILINGADDRESS.POSTDIRECTION, MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.STREETTYPE, MAILINGADDRESS.PARCELNUMBER, MAILINGADDRESS.UNITORSUITE, IPCASEADDRESS.MAILINGADDRESSID, IPCASEADDRESS.MAIN

FROM	IPCASEADDRESS 
		INNER JOIN MAILINGADDRESS ON IPCASEADDRESS.MAILINGADDRESSID = MAILINGADDRESS.MAILINGADDRESSID

WHERE	IPCASEADDRESS.IPCASEID = @IPCASEID

