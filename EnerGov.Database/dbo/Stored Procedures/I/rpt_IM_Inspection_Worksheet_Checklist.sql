

CREATE PROCEDURE [dbo].[rpt_IM_Inspection_Worksheet_Checklist]
@INSPECTIONID AS VARCHAR(36)
AS
SELECT IMCHECKLIST.NAME AS ChecklistItem, IMINSPECTIONCHECKLISTXREF.PASSED AS Passed, IMINSPECTIONCHECKLISTXREF.COMMENTS AS Comments
FROM IMCHECKLIST 
INNER JOIN IMINSPECTIONCHECKLISTXREF ON IMCHECKLIST.IMCHECKLISTID = IMINSPECTIONCHECKLISTXREF.IMCHECKLISTID
WHERE IMINSPECTIONCHECKLISTXREF.IMINSPECTIONID = @INSPECTIONID
ORDER BY IMINSPECTIONCHECKLISTXREF.ORDERNUM

