CREATE PROCEDURE [dbo].[USP_IMINSPECTIONCALHOLIDAY_UPDATE]
(
	@IMINSPECTIONCALHOLIDAYID CHAR(36),
	@IMINSPECTIONCALENDARID CHAR(36),
	@NAME NVARCHAR(100),
	@HOLIDAYDATE DATETIME
)
AS

UPDATE [dbo].[IMINSPECTIONCALHOLIDAY] SET
	[IMINSPECTIONCALENDARID] = @IMINSPECTIONCALENDARID,
	[NAME] = @NAME,
	[HOLIDAYDATE] = @HOLIDAYDATE

WHERE
	[IMINSPECTIONCALHOLIDAYID] = @IMINSPECTIONCALHOLIDAYID