CREATE PROCEDURE [dbo].[USP_IMINSPECTIONSTATUS_INSERT]
(
	@IMINSPECTIONSTATUSID CHAR(36),
	@STATUSNAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@INDICATESSUCCESS BIT,
	@IMINSPECTIONSTATUSENTITYID CHAR(36),
	@REINSPECTIONFLAG BIT,
	@INVIOLATIONFLAG BIT,
	@OUTOFVIOLATIONFLAG BIT,
	@EXTEND_EXPIRE_DATE BIT,
	@SCHEDULEDFLAG BIT,
	@WAITFLAG BIT,
	@PARTIALPASS BIT,
	@CANCELLEDFLAG BIT,
	@DESCRIPTION_SPANISH NVARCHAR(MAX),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[IMINSPECTIONSTATUS](
	[IMINSPECTIONSTATUSID],
	[STATUSNAME],
	[DESCRIPTION],
	[INDICATESSUCCESS],
	[IMINSPECTIONSTATUSENTITYID],
	[REINSPECTIONFLAG],
	[INVIOLATIONFLAG],
	[OUTOFVIOLATIONFLAG],
	[EXTEND_EXPIRE_DATE],
	[SCHEDULEDFLAG],
	[WAITFLAG],
	[PARTIALPASS],
	[CANCELLEDFLAG],
	[DESCRIPTION_SPANISH],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@IMINSPECTIONSTATUSID,
	@STATUSNAME,
	@DESCRIPTION,
	@INDICATESSUCCESS,
	@IMINSPECTIONSTATUSENTITYID,
	@REINSPECTIONFLAG,
	@INVIOLATIONFLAG,
	@OUTOFVIOLATIONFLAG,
	@EXTEND_EXPIRE_DATE,
	@SCHEDULEDFLAG,
	@WAITFLAG,
	@PARTIALPASS,
	@CANCELLEDFLAG,
	@DESCRIPTION_SPANISH,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE