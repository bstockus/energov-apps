
/****** Object:  StoredProcedure [dbo].[rpt_IP_SR_Impact_Case_Detail_Report_Note]    Script Date: 01/08/2014 15:58:53 ******/
CREATE PROCEDURE [dbo].[rpt_IP_SR_Impact_Case_Detail_Report_Note]
@IPCASEID AS VARCHAR(36)
AS

SELECT	IPCASENOTE.IPCASENOTEID, IPCASENOTE.TEXT, IPCASENOTE.CREATEDBY, IPCASENOTE.CREATEDDATE, 
		USERS.FNAME, USERS.LNAME

FROM	IPCASENOTE 
		INNER JOIN USERS ON IPCASENOTE.CREATEDBY = USERS.SUSERGUID

WHERE	IPCASENOTE.IPCASEID = @IPCASEID

