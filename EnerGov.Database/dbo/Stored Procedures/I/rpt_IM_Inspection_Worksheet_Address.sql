

CREATE PROCEDURE [dbo].[rpt_IM_Inspection_Worksheet_Address]
@INSPECTIONID AS VARCHAR(36)
AS
SELECT MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.ADDRESSLINE2, MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, 
       MAILINGADDRESS.POSTALCODE, MAILINGADDRESS.POSTDIRECTION, MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.STREETTYPE, 
       MAILINGADDRESS.UNITORSUITE
FROM MAILINGADDRESS 
INNER JOIN IMINSPECTIONADDRESS ON MAILINGADDRESS.MAILINGADDRESSID = IMINSPECTIONADDRESS.MAILINGADDRESSID
WHERE IMINSPECTIONADDRESS.IMINSPECTIONID = @INSPECTIONID 
AND IMINSPECTIONADDRESS.MAIN = 'TRUE'

