CREATE PROCEDURE [dbo].[USP_IPCONDITIONTYPE_UPDATE]
(
	@IPCONDITIONTYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@DEFAULTSTATUSID CHAR(36),
	@IPCONDITIONCATEGORYID CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[IPCONDITIONTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[DEFAULTSTATUSID] = @DEFAULTSTATUSID,
	[IPCONDITIONCATEGORYID] = @IPCONDITIONCATEGORYID,
	[CAFEETEMPLATEID] = @CAFEETEMPLATEID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[IPCONDITIONTYPEID] = @IPCONDITIONTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE