CREATE PROCEDURE [dbo].[USP_IMCHECKLISTCATEGORY_INSERT]
(
	@IMCHECKLISTCATEGORYID CHAR(36),
	@NAME NVARCHAR(255),
	@DESCRIPTION NVARCHAR(MAX),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS

INSERT INTO [dbo].[IMCHECKLISTCATEGORY](
	[IMCHECKLISTCATEGORYID],
	[NAME],
	[DESCRIPTION],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)

VALUES
(
	@IMCHECKLISTCATEGORYID,
	@NAME,
	@DESCRIPTION,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)