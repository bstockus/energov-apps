CREATE PROCEDURE [dbo].[USP_INVOICEEVENTQUEUE_UPDATE]
(
	@INVOICEEVENTQUEUEID AS BIGINT,
	@EVENTSTATUSID INT
)
AS  
BEGIN  
	SET NOCOUNT ON;
	UPDATE [dbo].[INVOICEEVENTQUEUE]
	SET 
		PROCESSEDDATE = GETDATE(),
		EVENTSTATUSID = @EVENTSTATUSID
	WHERE [dbo].[INVOICEEVENTQUEUE].[INVOICEEVENTQUEUEID] = @INVOICEEVENTQUEUEID
END