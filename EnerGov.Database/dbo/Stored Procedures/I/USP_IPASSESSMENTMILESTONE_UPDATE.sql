CREATE PROCEDURE [dbo].[USP_IPASSESSMENTMILESTONE_UPDATE]
(
	@IPASSESSMENTMILESTONEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@IPMILESTONETYPEID INT,
	@ACTIVE BIT,
	@CONFIGUREFIELDID CHAR(36),
	@MODULEID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[IPASSESSMENTMILESTONE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[IPMILESTONETYPEID] = @IPMILESTONETYPEID,
	[ACTIVE] = @ACTIVE,
	[CONFIGUREFIELDID] = @CONFIGUREFIELDID,
	[MODULEID] = @MODULEID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[IPASSESSMENTMILESTONEID] = @IPASSESSMENTMILESTONEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE