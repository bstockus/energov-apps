CREATE PROCEDURE [dbo].[USP_QUERYACTION_INSERT]
(
	@QUERYACTIONID CHAR(36),
	@QUERYID CHAR(36),
	@QUERYACTIONTYPEID INT,
	@ACTIONNAME NVARCHAR(50),
	@CUSTOMMESSAGE NVARCHAR(MAX),
	@ACTIONCLASSNAME NVARCHAR(MAX),
	@PRIORITY INT
)
AS
INSERT INTO [dbo].[QUERYACTION](
	[QUERYACTIONID],
	[QUERYID],
	[QUERYACTIONTYPEID],
	[ACTIONNAME],
	[CUSTOMMESSAGE],
	[ACTIONCLASSNAME],
	[PRIORITY]
)

VALUES
(
	@QUERYACTIONID,
	@QUERYID,
	@QUERYACTIONTYPEID,
	@ACTIONNAME,
	@CUSTOMMESSAGE,
	@ACTIONCLASSNAME,
	@PRIORITY
)