CREATE PROCEDURE [dbo].[USP_QUERYACTIONFTP_DELETE_BYPARENTID]
(
    @QUERYID CHAR(36),
    @QUERYACTIONID CHAR(36)
)
AS
DELETE [dbo].[QUERYACTIONFTP] FROM [dbo].[QUERYACTIONFTP]
INNER JOIN [dbo].[QUERYACTION] ON [dbo].[QUERYACTIONFTP].[QUERYACTIONID] =  [dbo].[QUERYACTION].[QUERYACTIONID]
WHERE
	(@QUERYID IS NULL OR [dbo].[QUERYACTION].[QUERYID]= @QUERYID) 
    AND (@QUERYACTIONID IS NULL OR [dbo].[QUERYACTIONFTP].[QUERYACTIONID] = @QUERYACTIONID)