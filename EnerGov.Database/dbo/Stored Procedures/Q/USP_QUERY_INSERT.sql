CREATE PROCEDURE [dbo].[USP_QUERY_INSERT]
(
	@QUERYID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@PRIORITY INT,
	@ISENABLED BIT,
	@MODULEID INT,
	@QUERYBUILDERID CHAR(36),
	@LASTCHANGEDON DATETIME,
	@LASTCHANGEDBY CHAR(36),
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[QUERY](
	[QUERYID],
	[NAME],
	[DESCRIPTION],
	[PRIORITY],
	[ISENABLED],
	[MODULEID],
	[QUERYBUILDERID],
	[LASTCHANGEDON],
	[LASTCHANGEDBY],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@QUERYID,
	@NAME,
	@DESCRIPTION,
	@PRIORITY,
	@ISENABLED,
	@MODULEID,
	@QUERYBUILDERID,
	@LASTCHANGEDON,
	@LASTCHANGEDBY,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE