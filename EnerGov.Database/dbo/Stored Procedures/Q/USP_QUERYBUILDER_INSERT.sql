CREATE PROCEDURE [dbo].[USP_QUERYBUILDER_INSERT]
(	
	@QUERYBUILDERID CHAR(36),
	@NAME NVARCHAR(100),	
	@DESCRIPTION NVARCHAR(MAX),
	@ROOTCLASSNAME NVARCHAR(MAX), 
	@QUERYMODULEID INT,
	@QUERY NVARCHAR(MAX),
	@ISCUSTOM BIT,
	@LASTCHANGEDON DATETIME,
	@LASTCHANGEDBY CHAR(36),	
	@ROWVERSION INT
)
AS

INSERT INTO [dbo].[QUERYBUILDER](
	[QUERYBUILDERID],
	[NAME],
	[DESCRIPTION],
	[ROOTCLASSNAME],
	[QUERYMODULEID],
	[QUERY],
	[ISCUSTOM],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
VALUES(
	@QUERYBUILDERID,
	@NAME,
	@DESCRIPTION,
	@ROOTCLASSNAME,
	@QUERYMODULEID,
	@QUERY,
	@ISCUSTOM,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)