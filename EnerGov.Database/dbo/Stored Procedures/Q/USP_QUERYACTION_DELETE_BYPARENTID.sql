CREATE PROCEDURE [dbo].[USP_QUERYACTION_DELETE_BYPARENTID]
	(
	@PARENTID CHAR(36)
)
AS

EXEC [dbo].[USP_MAILMERGETEMPLATEUSER_DELETE_BYPARENTID] NULL, @PARENTID -- delete mail merge users
EXEC [dbo].[USP_QUERYACTIONMAILMERGETMPL_DELETE_BYPARENTID] NULL, @PARENTID -- delete mail merge templates
EXEC [dbo].[USP_QUERYACTIONATTACHDOC_DELETE_BYPARENTID] @PARENTID, NULL -- delete attach documents
EXEC [dbo].[USP_QUERYACTIONFTP_DELETE_BYPARENTID] @PARENTID,NULL
EXEC [dbo].[USP_QUERYACTIONPARAMETER_DELETE_BYPARENTID] @PARENTID,NULL --delete fees
EXEC [dbo].[USP_QUERYACTIONHOLDTYPE_DELETE_BYPARENTID] @PARENTID,NULL
EXEC [dbo].[USP_QUERYACTIONTASKUSER_DELETE_BYPARENTID] null, @PARENTID, null
EXEC [dbo].[USP_QUERYACTIONTASK_DELETE_BYPARENTID] @PARENTID, null
EXEC [dbo].[USP_QUERYACTIONSETVALUE_DELETE_BYPARENTID] @PARENTID, NULL

DELETE FROM [dbo].[QUERYACTION]
WHERE [QUERYID] = @PARENTID