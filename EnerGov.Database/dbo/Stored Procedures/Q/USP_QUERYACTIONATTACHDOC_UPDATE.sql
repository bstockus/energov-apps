CREATE PROCEDURE [dbo].[USP_QUERYACTIONATTACHDOC_UPDATE]
(
	@QUERYACTIONATTACHDOCID CHAR(36),
	@QUERYACTIONID CHAR(36),
	@ATTACHMENTGROUPID CHAR(36) = NULL,
	@NOTES NVARCHAR(MAX),
	@RPTREPORTID CHAR(36)
)
AS

UPDATE [dbo].[QUERYACTIONATTACHDOC] SET
	[QUERYACTIONID] = @QUERYACTIONID,
	[ATTACHMENTGROUPID] = @ATTACHMENTGROUPID,
	[NOTES] = @NOTES,
	[RPTREPORTID] = @RPTREPORTID

WHERE
	[QUERYACTIONATTACHDOCID] = @QUERYACTIONATTACHDOCID