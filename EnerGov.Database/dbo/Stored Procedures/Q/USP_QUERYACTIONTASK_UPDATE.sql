CREATE PROCEDURE [dbo].[USP_QUERYACTIONTASK_UPDATE]
(
	@QUERYACTIONTASKID CHAR(36),
	@QUERYACTIONID CHAR(36),
	@TASKSUBJECT NVARCHAR(MAX),
	@TASKTEXT NVARCHAR(MAX),
	@STARTDATE NVARCHAR(MAX),
	@DUEDATE NVARCHAR(MAX),
	@STARTTIME NVARCHAR(MAX),
	@DUETIME NVARCHAR(MAX),
	@STARTDATEOFFSETDAYS INT,
	@DUEDATEOFFSETDAYS INT,
	@TASKPRIORITYID INT,
	@SHOWONCALENDAR BIT,
	@ASSIGNEDTO NVARCHAR(MAX)
)
AS
DECLARE @OUTPUTTABLE as TABLE([QUERYACTIONTASKID]  char(36))
UPDATE [dbo].[QUERYACTIONTASK] SET
	[QUERYACTIONID] = @QUERYACTIONID,
	[TASKSUBJECT] = @TASKSUBJECT,
	[TASKTEXT] = @TASKTEXT,
	[STARTDATE] = @STARTDATE,
	[DUEDATE] = @DUEDATE,
	[STARTTIME] = @STARTTIME,
	[DUETIME] = @DUETIME,
	[STARTDATEOFFSETDAYS] = @STARTDATEOFFSETDAYS,
	[DUEDATEOFFSETDAYS] = @DUEDATEOFFSETDAYS,
	[TASKPRIORITYID] = @TASKPRIORITYID,
	[SHOWONCALENDAR] = @SHOWONCALENDAR,
	[ASSIGNEDTO] = @ASSIGNEDTO
OUTPUT inserted.[QUERYACTIONTASKID] INTO @OUTPUTTABLE
WHERE
	[QUERYACTIONTASKID] = @QUERYACTIONTASKID  
SELECT * FROM @OUTPUTTABLE