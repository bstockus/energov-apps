CREATE PROCEDURE [dbo].[USP_QUERYACTIONHOLDTYPE_UPDATE]
(
	@QUERYACTIONHOLDTYPEID CHAR(36),
	@QUERYACTIONID CHAR(36),
	@HOLDSETUPID CHAR(36),
	@COMMENTS NVARCHAR(MAX)
)
AS
DECLARE @OUTPUTTABLE as TABLE([WORKFLOWACTIONHOLDTYPEID]  char(36))
UPDATE [dbo].[QUERYACTIONHOLDTYPE] SET
	[QUERYACTIONID] = @QUERYACTIONID,
	[HOLDSETUPID] = @HOLDSETUPID,
	[COMMENTS] = @COMMENTS
OUTPUT inserted.[QUERYACTIONHOLDTYPEID] INTO @OUTPUTTABLE
WHERE
	[QUERYACTIONHOLDTYPEID] = @QUERYACTIONHOLDTYPEID AND 
	[QUERYACTIONID]= @QUERYACTIONID

SELECT * FROM @OUTPUTTABLE