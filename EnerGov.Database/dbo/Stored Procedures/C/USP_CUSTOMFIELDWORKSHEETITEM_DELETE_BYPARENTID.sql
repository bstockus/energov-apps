CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDWORKSHEETITEM_DELETE_BYPARENTID]
(
	@RECORDIDs RecordIDs READONLY
)
AS
DELETE FROM [dbo].[CUSTOMFIELDWORKSHEETITEM]
WHERE
	[FKGCUSTOMFIELDWORKSHEET] IN (
	SELECT 
		GCUSTOMFIELDWORKSHEET 
	FROM [dbo].[CUSTOMFIELDWORKSHEET] 
	INNER JOIN 	@RECORDIDs AS DELETED_CUSTOM_FIELDS ON DELETED_CUSTOM_FIELDS.RECORDID = [dbo].[CUSTOMFIELDWORKSHEET].[FKGCUSTOMFIELD])