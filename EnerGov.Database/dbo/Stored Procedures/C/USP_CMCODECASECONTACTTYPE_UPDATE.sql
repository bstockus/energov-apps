CREATE PROCEDURE [dbo].[USP_CMCODECASECONTACTTYPE_UPDATE]
(
	@CMCODECASECONTACTTYPEID CHAR(36),
	@NAME VARCHAR(50),
	@DESCRIPTION VARCHAR(MAX),
	@CMCONTACTTYPESYSTEMID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CMCODECASECONTACTTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[CMCONTACTTYPESYSTEMID] = @CMCONTACTTYPESYSTEMID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CMCODECASECONTACTTYPEID] = @CMCODECASECONTACTTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE