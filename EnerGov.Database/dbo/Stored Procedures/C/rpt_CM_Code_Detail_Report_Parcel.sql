


CREATE PROCEDURE [dbo].[rpt_CM_Code_Detail_Report_Parcel]
@CODECASEID AS VARCHAR(36)
AS
SELECT PARCEL.PARCELNUMBER, CMCODECASEPARCEL.[PRIMARY] AS MAIN, CMCODECASEPARCEL.PARCELID
FROM CMCODECASEPARCEL 
INNER JOIN PARCEL ON CMCODECASEPARCEL.PARCELID = PARCEL.PARCELID
WHERE CMCODECASEPARCEL.CMCODECASEID = @CODECASEID


