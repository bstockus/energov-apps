CREATE PROCEDURE [dbo].[USP_CUSTOMEVENTQUEUE_UPDATE]
(
@EVENTQUEUEID AS BIGINT,
@EVENTSTATUSID INT
)
AS  
BEGIN  
	SET NOCOUNT ON;
	UPDATE [dbo].[CUSTOMEVENTQUEUE]
	SET 
		PROCESSEDDATE = GETDATE(),
		EVENTSTATUSID = @EVENTSTATUSID
	WHERE [dbo].[CUSTOMEVENTQUEUE].[CUSTOMEVENTQUEUEID] = @EVENTQUEUEID

END