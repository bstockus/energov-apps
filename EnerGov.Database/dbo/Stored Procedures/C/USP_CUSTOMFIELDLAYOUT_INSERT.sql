CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDLAYOUT_INSERT]
(
	@GCUSTOMFIELDLAYOUTS CHAR(36),
	@SNAME VARCHAR(50),
	@CUSTOMFIELDMODULEID INT,
	@WIDTH INT,
	@HEIGHT INT,
	@ISVERSION2 BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@NUMBEROFCOLUMNS INT,
	@ISHTMLEXPERIENCE BIT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CUSTOMFIELDLAYOUT](
	[GCUSTOMFIELDLAYOUTS],
	[SNAME],
	[CUSTOMFIELDMODULEID],
	[WIDTH],
	[HEIGHT],
	[ISVERSION2],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION],
	[NUMBEROFCOLUMNS],
	[ISHTMLEXPERIENCE]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@GCUSTOMFIELDLAYOUTS,
	@SNAME,
	@CUSTOMFIELDMODULEID,
	@WIDTH,
	@HEIGHT,
	@ISVERSION2,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION,
	@NUMBEROFCOLUMNS,
	@ISHTMLEXPERIENCE
)
SELECT * FROM @OUTPUTTABLE