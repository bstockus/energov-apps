CREATE PROCEDURE [dbo].[USP_CAFEEDELETEREASON_INSERT]
(
	@CAFEEDELETEREASONID CHAR(36),
	@REASONNAME NVARCHAR(20),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CAFEEDELETEREASON](
	[CAFEEDELETEREASONID],
	[REASONNAME],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@CAFEEDELETEREASONID,
	@REASONNAME,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE