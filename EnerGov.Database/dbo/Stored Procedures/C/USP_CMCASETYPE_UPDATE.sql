CREATE PROCEDURE [dbo].[USP_CMCASETYPE_UPDATE]
(
	@CMCASETYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@CUSTOMFIELDID CHAR(36),
	@WFTEMPLATEID CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@ACTIVE BIT,
	@DEFAULTUSER CHAR(36),
	@DEFAULTSTATUS CHAR(36),
	@DESCRIPTION NVARCHAR(MAX),
	@PREFIX NVARCHAR(10),
	@USECASETYPENUMBERING BIT,
	@ONLINECUSTOMFIELDLAYOUTID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CMCASETYPE] SET
	[NAME] = @NAME,
	[CUSTOMFIELDID] = @CUSTOMFIELDID,
	[WFTEMPLATEID] = @WFTEMPLATEID,
	[CAFEETEMPLATEID] = @CAFEETEMPLATEID,
	[ACTIVE] = @ACTIVE,
	[DEFAULTUSER] = @DEFAULTUSER,
	[DEFAULTSTATUS] = @DEFAULTSTATUS,
	[DESCRIPTION] = @DESCRIPTION,
	[PREFIX] = @PREFIX,
	[USECASETYPENUMBERING] = @USECASETYPENUMBERING,
	[ONLINECUSTOMFIELDLAYOUTID] = @ONLINECUSTOMFIELDLAYOUTID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CMCASETYPEID] = @CMCASETYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE