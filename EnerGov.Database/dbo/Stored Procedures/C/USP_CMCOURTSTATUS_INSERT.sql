CREATE PROCEDURE [dbo].[USP_CMCOURTSTATUS_INSERT]
(
	@CMCOURTSTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(255),
	@ISCLOSED BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CMCOURTSTATUS](
	[CMCOURTSTATUSID],
	[NAME],
	[DESCRIPTION],
	[ISCLOSED],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@CMCOURTSTATUSID,
	@NAME,
	@DESCRIPTION,
	@ISCLOSED,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE