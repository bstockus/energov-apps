

CREATE PROCEDURE [dbo].[rpt_CA_Invoice_Billing_Contact]
@GLOBALENTITYID AS VARCHAR(36)
AS
SELECT GLOBALENTITY.GLOBALENTITYNAME, GLOBALENTITY.FIRSTNAME+' '+GLOBALENTITY.LASTNAME AS GLOBALENTITYFLNAME, MAILINGADDRESS.ADDRESSTYPE, 
       MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.ADDRESSLINE2, MAILINGADDRESS.STREETTYPE, 
       MAILINGADDRESS.POSTDIRECTION, MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.UNITORSUITE, 
       MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, MAILINGADDRESS.POSTALCODE
FROM GLOBALENTITY 
LEFT OUTER JOIN GLOBALENTITYMAILINGADDRESS ON GLOBALENTITY.GLOBALENTITYID = GLOBALENTITYMAILINGADDRESS.GLOBALENTITYID 
LEFT OUTER JOIN MAILINGADDRESS ON GLOBALENTITYMAILINGADDRESS.MAILINGADDRESSID = MAILINGADDRESS.MAILINGADDRESSID
WHERE GLOBALENTITY.GLOBALENTITYID = @GLOBALENTITYID 


