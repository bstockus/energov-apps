CREATE PROCEDURE [dbo].[USP_CAFEE_UPDATE]
(
	@CAFEEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@ACTIVE BIT,
	@ROWVERSION INT,
	@LASTCHANGEDON DATETIME,
	@LASTCHANGEDBY CHAR(36),
	@CACOMPUTATIONTYPEID INT,
	@NOTES NVARCHAR(MAX),
	@ISTIMETRACKING BIT,
	@ISTAXFEE BIT,
	@ISCPIFEE BIT,
	@ISPRORATEFEE BIT,
	@ISARFEE BIT,
	@ARCREDITACCOUNTID CHAR(36),
	@ARDEBITACCOUNTID CHAR(36),
	@ARFEECODE NVARCHAR(100),
	@SETTLEMENTCODE NVARCHAR(100),
	@APPLYPRORATIONONRENEWAL BIT,
	@ISSUBJECTTOEXTERNALINVOICE BIT,
	@DAYSUNTILEXTERNALINVOICEEXPORT INT,
	@ISEXEMPTFROMFEEWAIVER BIT,
	@JURISDICTIONID CHAR(36),
	@ISCOMPOUNDINGINTERESTFEE BIT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CAFEE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[ACTIVE] = @ACTIVE,
	[ROWVERSION] = @ROWVERSION + 1,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[CACOMPUTATIONTYPEID] = @CACOMPUTATIONTYPEID,
	[NOTES] = @NOTES,
	[ISTIMETRACKING] = @ISTIMETRACKING,
	[ISTAXFEE] = @ISTAXFEE,
	[ISCPIFEE] = @ISCPIFEE,
	[ISPRORATEFEE] = @ISPRORATEFEE,
	[ISARFEE] = @ISARFEE,
	[ARCREDITACCOUNTID] = @ARCREDITACCOUNTID,
	[ARDEBITACCOUNTID] = @ARDEBITACCOUNTID,
	[ARFEECODE] = @ARFEECODE,
	[SETTLEMENTCODE] = @SETTLEMENTCODE,
	[APPLYPRORATIONONRENEWAL] = @APPLYPRORATIONONRENEWAL,
	[ISSUBJECTTOEXTERNALINVOICE] = @ISSUBJECTTOEXTERNALINVOICE,
	[DAYSUNTILEXTERNALINVOICEEXPORT] = @DAYSUNTILEXTERNALINVOICEEXPORT,
	[ISEXEMPTFROMFEEWAIVER] = @ISEXEMPTFROMFEEWAIVER,
	[JURISDICTIONID] = @JURISDICTIONID,
	[ISCOMPOUNDINGINTERESTFEE] = @ISCOMPOUNDINGINTERESTFEE
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CAFEEID] = @CAFEEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE