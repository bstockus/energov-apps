CREATE PROCEDURE [dbo].[USP_CAPRORATESCHEDULERATES_INSERT]
(
	@CAPRORATESCHEDULERATESID CHAR(36),
	@CAPRORATESCHEDULEID CHAR(36),
	@STARTDATE DATETIME,
	@ENDDATE DATETIME,
	@RATES DECIMAL(20,4)
)
AS

INSERT INTO [dbo].[CAPRORATESCHEDULERATES](
	[CAPRORATESCHEDULERATESID],
	[CAPRORATESCHEDULEID],
	[STARTDATE],
	[ENDDATE],
	[RATES]
)

VALUES
(
	@CAPRORATESCHEDULERATESID,
	@CAPRORATESCHEDULEID,
	@STARTDATE,
	@ENDDATE,
	@RATES
)