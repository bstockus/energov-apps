CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDTEMPLATE_UPDATE]
(
	@GCUSTOMFIELDTEMPLATE CHAR(36),
	@FKCUSTOMFIELDLAYOUTCONTROLTYPE INT,
	@TEMPLATENAME NVARCHAR(50),
	@SDEFAULTVALUE NVARCHAR(50),
	@SFIELDTIP NVARCHAR(MAX),
	@ISACTIVE BIT,
	@BALLOWMULTIPLESELECTIONS BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CUSTOMFIELDTEMPLATE] SET
	[FKCUSTOMFIELDLAYOUTCONTROLTYPE] = @FKCUSTOMFIELDLAYOUTCONTROLTYPE,
	[TEMPLATENAME] = @TEMPLATENAME,
	[SDEFAULTVALUE] = @SDEFAULTVALUE,
	[SFIELDTIP] = @SFIELDTIP,
	[ISACTIVE] = @ISACTIVE,
	[BALLOWMULTIPLESELECTIONS] = @BALLOWMULTIPLESELECTIONS,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[GCUSTOMFIELDTEMPLATE] = @GCUSTOMFIELDTEMPLATE AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE