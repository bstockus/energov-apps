CREATE PROCEDURE [dbo].[USP_CMCODECASEHOLDTYPEREF_UPDATE]
(
	@CMCODECASEHOLDTYPEID CHAR(36),
	@CMCASETYPEID CHAR(36),
	@HOLDSETUPID CHAR(36)
)
AS
BEGIN

UPDATE [dbo].[CMCODECASEHOLDTYPEREF] SET
	[CMCASETYPEID] = @CMCASETYPEID,
	[HOLDSETUPID] = @HOLDSETUPID
WHERE
	[CMCODECASEHOLDTYPEID] = @CMCODECASEHOLDTYPEID  

END