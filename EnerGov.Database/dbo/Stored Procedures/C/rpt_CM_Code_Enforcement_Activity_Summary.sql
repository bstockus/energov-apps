


CREATE PROCEDURE [dbo].[rpt_CM_Code_Enforcement_Activity_Summary]
@STARTDATE AS DATETIME,
@ENDDATE AS DATETIME
AS
SET @ENDDATE = DATEADD(S,-1,DATEADD(D,1,@ENDDATE))
SELECT     CMCODECASE.CASENUMBER AS CaseNumber, CMCODECASESTATUS.NAME AS CaseStatus, CMCODECASE.CMCODECASEID,
					CMCODECASE.OPENEDDATE AS OpenedDate, CMCODECASE.CLOSEDDATE AS ClosedDate, YEAR(CMCODECASE.OPENEDDATE) AS OpenYear, YEAR(CMCODECASE.CLOSEDDATE) AS CloseYear
FROM         CMCODECASE INNER JOIN
                      CMCASETYPE ON CMCODECASE.CMCASETYPEID = CMCASETYPE.CMCASETYPEID INNER JOIN
                      CMCODECASESTATUS ON CMCODECASE.CMCODECASESTATUSID = CMCODECASESTATUS.CMCODECASESTATUSID
WHERE CMCASETYPE.NAME = 'Code Enforcement'


