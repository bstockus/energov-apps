CREATE PROCEDURE [dbo].[USP_CACPITYPE_UPDATE]
(
	@CACPITYPEID CHAR(36),
	@NAME NVARCHAR(255),
	@DESCRIPTION NVARCHAR(500),
	@CACPICALCULATIONTYPEID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@ROUNDEVERYCPICALCULATION BIT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CACPITYPE] SET	
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[CACPICALCULATIONTYPEID] = @CACPICALCULATIONTYPEID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1,
	[ROUNDEVERYCPICALCULATION] = @ROUNDEVERYCPICALCULATION
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CACPITYPEID] = @CACPITYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE