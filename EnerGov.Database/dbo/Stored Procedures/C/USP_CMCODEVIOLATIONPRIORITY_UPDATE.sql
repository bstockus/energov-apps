CREATE PROCEDURE [dbo].[USP_CMCODEVIOLATIONPRIORITY_UPDATE]
(
	@CMCODEVIOLATIONPRIORITYID CHAR(36),
	@NAME NVARCHAR(100),
	@VIOLATIONPRIORITYSYSTEMTYPEID INT,
	@DEFAULTFLAG BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CMCODEVIOLATIONPRIORITY] SET
	[NAME] = @NAME,
	[VIOLATIONPRIORITYSYSTEMTYPEID] = @VIOLATIONPRIORITYSYSTEMTYPEID,
	[DEFAULTFLAG] = @DEFAULTFLAG,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CMCODEVIOLATIONPRIORITYID] = @CMCODEVIOLATIONPRIORITYID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE