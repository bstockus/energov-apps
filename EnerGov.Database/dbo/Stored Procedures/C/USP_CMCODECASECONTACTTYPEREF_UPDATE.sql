CREATE PROCEDURE [dbo].[USP_CMCODECASECONTACTTYPEREF_UPDATE]
(
	@CONTACTTYPEEXTID CHAR(36),
	@CMCODECASECONTACTTYPEID CHAR(36),
	@CONTACTTYPEGROUP INT,
	@ISREQUIRED BIT,
	@OBJCLASSID CHAR(36),
	@OBJTYPEID CHAR(36),
	@OBJMODULEID INT
)
AS

UPDATE [dbo].[CMCODECASECONTACTTYPEREF] SET
	[CMCODECASECONTACTTYPEID] = @CMCODECASECONTACTTYPEID,
	[CONTACTTYPEGROUP] = @CONTACTTYPEGROUP,
	[ISREQUIRED] = @ISREQUIRED,
	[OBJCLASSID] = @OBJCLASSID,
	[OBJTYPEID] = @OBJTYPEID,
	[OBJMODULEID] = @OBJMODULEID

WHERE
	[CONTACTTYPEEXTID] = @CONTACTTYPEEXTID