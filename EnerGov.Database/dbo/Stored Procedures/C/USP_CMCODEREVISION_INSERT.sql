CREATE PROCEDURE [dbo].[USP_CMCODEREVISION_INSERT]
(
	@CMCODEREVISIONID CHAR(36),
	@CMCODEID CHAR(36),
	@CODETEXT NVARCHAR(MAX),
	@REVISIONDATE DATETIME,
	@CURRENTREVISION BIT,
	@STARTDATE DATETIME,
	@ENDDATE DATETIME,
	@REVISIONNUMBER INT
)
AS

INSERT INTO [dbo].[CMCODEREVISION](
	[CMCODEREVISIONID],
	[CMCODEID],
	[CODETEXT],
	[REVISIONDATE],
	[CURRENTREVISION],
	[STARTDATE],
	[ENDDATE],
	[REVISIONNUMBER]
)

VALUES
(
	@CMCODEREVISIONID,
	@CMCODEID,
	@CODETEXT,
	@REVISIONDATE,
	@CURRENTREVISION,
	@STARTDATE,
	@ENDDATE,
	@REVISIONNUMBER
)