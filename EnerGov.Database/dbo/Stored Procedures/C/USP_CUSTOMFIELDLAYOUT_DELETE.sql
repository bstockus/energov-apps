CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDLAYOUT_DELETE]
(
	@GCUSTOMFIELDLAYOUTS CHAR(36),
	@ROWVERSION INT
)
AS

SET NOCOUNT ON

EXEC [dbo].[USP_CUSTOMFIELDOBJECT_DELETE_BYPARENTID] @GCUSTOMFIELDLAYOUTS

SET NOCOUNT OFF

DELETE FROM [dbo].[CUSTOMFIELDLAYOUT]
WHERE
	[GCUSTOMFIELDLAYOUTS] = @GCUSTOMFIELDLAYOUTS AND 
	[ROWVERSION]= @ROWVERSION