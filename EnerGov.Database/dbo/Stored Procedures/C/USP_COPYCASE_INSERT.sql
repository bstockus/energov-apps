CREATE PROCEDURE [dbo].[USP_COPYCASE_INSERT]
	@PARENID CHAR(36), 
	@COPYCASEIDS [RecordIDs] READONLY,
	@MODULEID int,
	@USERID CHAR(36),
	@LASTCHANGEDON datetime
AS
	INSERT INTO COPYCASE(PARENTID, COPYCASEID, MODULEID, LASTCHANGEDBY, LASTCHANGEDON) SELECT @PARENID, RECORDID, @MODULEID, @USERID, @LASTCHANGEDON from @COPYCASEIDS
RETURN 0