CREATE PROCEDURE [dbo].[USP_CMCITATIONTYPE_INSERT]
(
	@CMCITATIONTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@PREFIX NVARCHAR(10),
	@DEFAULTCITATIONSTATUSID CHAR(36),
	@ACTIVE BIT,
	@AUTONUMBER BIT,
	@TYPESPECIFICAUTONUMBER BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CMCITATIONTYPE](
	[CMCITATIONTYPEID],
	[NAME],
	[DESCRIPTION],
	[PREFIX],
	[DEFAULTCITATIONSTATUSID],
	[ACTIVE],
	[AUTONUMBER],
	[TYPESPECIFICAUTONUMBER],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@CMCITATIONTYPEID,
	@NAME,
	@DESCRIPTION,
	@PREFIX,
	@DEFAULTCITATIONSTATUSID,
	@ACTIVE,
	@AUTONUMBER,
	@TYPESPECIFICAUTONUMBER,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE