CREATE PROCEDURE [dbo].[USP_CMCODEREVISION_UPDATE]
(
	@CMCODEREVISIONID CHAR(36),
	@CMCODEID CHAR(36),
	@CODETEXT NVARCHAR(MAX),
	@REVISIONDATE DATETIME,
	@CURRENTREVISION BIT,
	@STARTDATE DATETIME,
	@ENDDATE DATETIME,
	@REVISIONNUMBER INT
)
AS

UPDATE [dbo].[CMCODEREVISION] SET
	[CMCODEID] = @CMCODEID,
	[CODETEXT] = @CODETEXT,
	[REVISIONDATE] = @REVISIONDATE,
	[CURRENTREVISION] = @CURRENTREVISION,
	[STARTDATE] = @STARTDATE,
	[ENDDATE] = @ENDDATE,
	[REVISIONNUMBER] = @REVISIONNUMBER

WHERE
	[CMCODEREVISIONID] = @CMCODEREVISIONID