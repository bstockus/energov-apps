CREATE PROCEDURE [dbo].[USP_CADISCOUNT_INSERT]
(
	@CADISCOUNTID CHAR(36),
	@CACOMPUTATIONTYPEID INT,
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@ACTIVE BIT,
	@ROWVERSION INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CADISCOUNT](
	[CADISCOUNTID],
	[CACOMPUTATIONTYPEID],
	[NAME],
	[DESCRIPTION],
	[ACTIVE],
	[ROWVERSION],
	[LASTCHANGEDBY],
	[LASTCHANGEDON]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@CADISCOUNTID,
	@CACOMPUTATIONTYPEID,
	@NAME,
	@DESCRIPTION,
	@ACTIVE,
	@ROWVERSION,
	@LASTCHANGEDBY,
	@LASTCHANGEDON
)
SELECT * FROM @OUTPUTTABLE