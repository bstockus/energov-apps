CREATE PROCEDURE [dbo].[USP_CATEMPLATEFEEDISCOUNTXREF_DELETE_BY_FEETEMPLATEID]
(
@CAFEETEMPLATEID CHAR(36)
)
AS
DELETE	[dbo].[CATEMPLATEFEEDISCOUNTXREF] FROM [dbo].[CATEMPLATEFEEDISCOUNTXREF]
JOIN	[dbo].[CAFEETEMPLATEFEE] ON [dbo].[CATEMPLATEFEEDISCOUNTXREF].[CAFEETEMPLATEFEEID] = [dbo].[CAFEETEMPLATEFEE].[CAFEETEMPLATEFEEID]
WHERE	[dbo].[CAFEETEMPLATEFEE].[CAFEETEMPLATEID] = @CAFEETEMPLATEID