

CREATE PROCEDURE [dbo].[rpt_CR_SR_Citizen_Request_Detailed_Report_Address]
@REQUESTID AS VARCHAR(36)
AS
-- exec rpt_CR_SR_Citizen_Request_Detailed_Report_Address '7fb3de92-f94c-439c-8a11-6df3e72173e1'
SELECT MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.ADDRESSLINE2, MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, MAILINGADDRESS.POSTALCODE, 
       MAILINGADDRESS.POSTDIRECTION, MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.STREETTYPE, MAILINGADDRESS.PARCELNUMBER, MAILINGADDRESS.UNITORSUITE, CITIZENREQUESTADDRESS.MAILINGADDRESSID, CITIZENREQUESTADDRESS.MAIN
FROM CITIZENREQUESTADDRESS 
INNER JOIN MAILINGADDRESS ON CITIZENREQUESTADDRESS.MAILINGADDRESSID = MAILINGADDRESS.MAILINGADDRESSID
WHERE CITIZENREQUESTADDRESS.CITIZENREQUESTID = @REQUESTID

