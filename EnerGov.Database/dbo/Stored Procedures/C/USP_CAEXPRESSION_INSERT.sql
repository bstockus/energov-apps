CREATE PROCEDURE [dbo].[USP_CAEXPRESSION_INSERT]
(
	@CAEXPRESSIONID CHAR(36),
	@CAEXPRESSIONTYPE INT,
	@RETURNTYPE INT,
	@CONSTANTVALUE NVARCHAR(MAX),
	@VARIABLETYPEID INT,
	@VARIABLEINPUTNAME NVARCHAR(100),
	@VARIABLEINPUTID CHAR(36),
	@PARENTEXPRESSIONID CHAR(36),
	@PARAMETERINDEX INT
)
AS

INSERT INTO [dbo].[CAEXPRESSION](
	[CAEXPRESSIONID],
	[CAEXPRESSIONTYPE],
	[RETURNTYPE],
	[CONSTANTVALUE],
	[VARIABLETYPEID],
	[VARIABLEINPUTNAME],
	[VARIABLEINPUTID],
	[PARENTEXPRESSIONID],
	[PARAMETERINDEX]
)

VALUES
(
	@CAEXPRESSIONID,
	@CAEXPRESSIONTYPE,
	@RETURNTYPE,
	@CONSTANTVALUE,
	@VARIABLETYPEID,
	@VARIABLEINPUTNAME,
	@VARIABLEINPUTID,
	@PARENTEXPRESSIONID,
	@PARAMETERINDEX
)