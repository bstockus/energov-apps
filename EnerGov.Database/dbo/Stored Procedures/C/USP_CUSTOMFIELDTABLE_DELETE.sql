CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDTABLE_DELETE]
(
	@CUSTOMFIELDTABLEID CHAR(36),
	@ROWVERSION INT
)
AS

SET NOCOUNT ON;  

EXEC [dbo].[USP_CUSTOMFIELD_DELETEBY_CUSTOMFIELDTABLEID] @CUSTOMFIELDTABLEID
EXEC [dbo].[USP_CUSTOMFIELDTABLECOLUMNREF_DELETE_BYPARENTID] @CUSTOMFIELDTABLEID

SET NOCOUNT OFF;  

DELETE FROM [dbo].[CUSTOMFIELDTABLE]
WHERE
	[CUSTOMFIELDTABLEID] = @CUSTOMFIELDTABLEID AND 
	[ROWVERSION]= @ROWVERSION