CREATE PROCEDURE [dbo].[USP_CMCODEVIOLATIONPRIORITY_INSERT]
(
	@CMCODEVIOLATIONPRIORITYID CHAR(36),
	@NAME NVARCHAR(100),
	@VIOLATIONPRIORITYSYSTEMTYPEID INT,
	@DEFAULTFLAG BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CMCODEVIOLATIONPRIORITY](
	[CMCODEVIOLATIONPRIORITYID],
	[NAME],
	[VIOLATIONPRIORITYSYSTEMTYPEID],
	[DEFAULTFLAG],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@CMCODEVIOLATIONPRIORITYID,
	@NAME,
	@VIOLATIONPRIORITYSYSTEMTYPEID,
	@DEFAULTFLAG,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE