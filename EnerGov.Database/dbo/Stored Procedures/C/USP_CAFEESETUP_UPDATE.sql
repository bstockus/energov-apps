CREATE PROCEDURE [dbo].[USP_CAFEESETUP_UPDATE]
(
	@CAFEESETUPID CHAR(36),
	@CAFEEID CHAR(36),
	@CASCHEDULEID CHAR(36),
	@AMOUNT MONEY,
	@MINIMUMAMOUNT MONEY,
	@MAXIMUMAMOUNT MONEY,
	@COMPUTATIONVALUE DECIMAL(20,4),
	@COMPUTATIONVALUENAME NVARCHAR(50),
	@ROUNDINGTYPEID INT,
	@ROUNDINGVALUE DECIMAL(20,4),
	@ROWVERSION INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@CAPRORATESCHEDULEID CHAR(36),
	@CAPRORATIONMODELID INT,
	@CACPITYPEID CHAR(36),
	@CPIORIGINALFEEDATE DATETIME,
	@CAFEECOMPOUNDINGFREQUENCYTYPEID INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CAFEESETUP] SET
	[CAFEEID] = @CAFEEID,
	[CASCHEDULEID] = @CASCHEDULEID,
	[AMOUNT] = @AMOUNT,
	[MINIMUMAMOUNT] = @MINIMUMAMOUNT,
	[MAXIMUMAMOUNT] = @MAXIMUMAMOUNT,
	[COMPUTATIONVALUE] = @COMPUTATIONVALUE,
	[COMPUTATIONVALUENAME] = @COMPUTATIONVALUENAME,
	[ROUNDINGTYPEID] = @ROUNDINGTYPEID,
	[ROUNDINGVALUE] = @ROUNDINGVALUE,
	[ROWVERSION] = @ROWVERSION + 1,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[CAPRORATESCHEDULEID] = @CAPRORATESCHEDULEID,
	[CAPRORATIONMODELID] = @CAPRORATIONMODELID,
	[CACPITYPEID] = @CACPITYPEID,
	[CPIORIGINALFEEDATE] = @CPIORIGINALFEEDATE,
	[CAFEECOMPOUNDINGFREQUENCYTYPEID] = @CAFEECOMPOUNDINGFREQUENCYTYPEID
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CAFEESETUPID] = @CAFEESETUPID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE