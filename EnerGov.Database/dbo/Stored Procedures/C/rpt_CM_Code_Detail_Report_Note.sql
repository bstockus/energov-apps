


CREATE PROCEDURE [dbo].[rpt_CM_Code_Detail_Report_Note]
@CODECASEID AS VARCHAR(36)
AS
SELECT CMCODECASENOTE.CMCODECASENOTEID, CMCODECASENOTE.TEXT, CMCODECASENOTE.CREATEDBY, CMCODECASENOTE.CREATEDDATE, USERS.FNAME, USERS.LNAME
FROM CMCODECASENOTE 
INNER JOIN USERS ON CMCODECASENOTE.CREATEDBY = USERS.SUSERGUID
WHERE CMCODECASENOTE.CMCODECASEID = @CODECASEID



