CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDTABLECOLUMNREF_DELETE]
(
@CUSTOMFIELDTABLECOLUMNREFID CHAR(36)
)
AS

SET NOCOUNT ON

EXEC [dbo].[USP_CUSTOMFIELD_DELETEBY_CUSTOMFIELDTABLECOLUMNREFID] @CUSTOMFIELDTABLECOLUMNREFID

SET NOCOUNT OFF

DELETE FROM [dbo].[CUSTOMFIELDTABLECOLUMNREF]
WHERE
	[CUSTOMFIELDTABLECOLUMNREFID] = @CUSTOMFIELDTABLECOLUMNREFID