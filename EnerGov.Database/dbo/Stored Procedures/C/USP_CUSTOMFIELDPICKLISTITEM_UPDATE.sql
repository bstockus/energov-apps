CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDPICKLISTITEM_UPDATE]
(
	@GCUSTOMFIELDPICKLISTITEM CHAR(36),
	@FKGCUSTOMFIELDPICKLIST CHAR(36),
	@SVALUE VARCHAR(50),
	@IORDER INT,
	@ISRETIRE BIT,
	@FKCUSTOMFIELDTEMPLATEITEM CHAR(36)
)
AS

UPDATE [dbo].[CUSTOMFIELDPICKLISTITEM] SET
	[FKGCUSTOMFIELDPICKLIST] = @FKGCUSTOMFIELDPICKLIST,
	[SVALUE] = @SVALUE,
	[IORDER] = @IORDER,
	[ISRETIRE] = @ISRETIRE,
	[FKCUSTOMFIELDTEMPLATEITEM] = @FKCUSTOMFIELDTEMPLATEITEM

WHERE
	[GCUSTOMFIELDPICKLISTITEM] = @GCUSTOMFIELDPICKLISTITEM