CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDTABLE_INSERT]
(
	@CUSTOMFIELDTABLEID CHAR(36),
	@NAME NVARCHAR(50),
	@REQUIRED BIT,
	@DEFAULTROWNUM INT,
	@TOOLTIP VARCHAR(MAX),
	@SHOWONMOBILE BIT,
	@ISEDITINMOBILE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CUSTOMFIELDTABLE](
	[CUSTOMFIELDTABLEID],
	[NAME],
	[REQUIRED],
	[DEFAULTROWNUM],
	[TOOLTIP],
	[SHOWONMOBILE],
	[ISEDITINMOBILE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@CUSTOMFIELDTABLEID,
	@NAME,
	@REQUIRED,
	@DEFAULTROWNUM,
	@TOOLTIP,
	@SHOWONMOBILE,
	@ISEDITINMOBILE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE