


CREATE PROCEDURE [dbo].[rpt_CR_Citizen_Request_Detailed_Report_Note]
@REQUESTID AS VARCHAR(36)
AS
-- Note
SELECT CITIZENREQUESTNOTE.CITIZENREQUESTID AS REQUESTNOTEID, CITIZENREQUESTNOTE.TEXT, CITIZENREQUESTNOTE.CREATEDBY, CITIZENREQUESTNOTE.CREATEDDATE, USERS.FNAME + ' '+  USERS.LNAME AS NAME
FROM CITIZENREQUESTNOTE 
INNER JOIN USERS ON CITIZENREQUESTNOTE.CREATEDBY = USERS.SUSERGUID
WHERE CITIZENREQUESTNOTE.CITIZENREQUESTID = @REQUESTID


