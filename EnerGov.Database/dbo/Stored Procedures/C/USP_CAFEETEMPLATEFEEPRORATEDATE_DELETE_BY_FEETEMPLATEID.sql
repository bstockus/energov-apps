CREATE PROCEDURE [dbo].[USP_CAFEETEMPLATEFEEPRORATEDATE_DELETE_BY_FEETEMPLATEID]
(
@CAFEETEMPLATEID CHAR(36)
)
AS
DELETE [dbo].[CAFEETEMPLATEFEEPRORATEDATE] FROM [dbo].[CAFEETEMPLATEFEEPRORATEDATE]
JOIN [dbo].[CAFEETEMPLATEFEE] ON [dbo].[CAFEETEMPLATEFEEPRORATEDATE].[CAFEETEMPLATEFEEID] = [dbo].[CAFEETEMPLATEFEE].[CAFEETEMPLATEFEEID]
WHERE	[dbo].[CAFEETEMPLATEFEE].[CAFEETEMPLATEID] = @CAFEETEMPLATEID