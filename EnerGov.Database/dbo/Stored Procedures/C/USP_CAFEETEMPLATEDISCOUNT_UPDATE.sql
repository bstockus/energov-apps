CREATE PROCEDURE [dbo].[USP_CAFEETEMPLATEDISCOUNT_UPDATE]
(
	@CAFEETEMPLATEDISCOUNTID CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@CADISCOUNTID CHAR(36),
	@ISMANUAL BIT,
	@ISHIDDEN BIT,
	@APPLYORDER INT,
	@APPLYTOTOTAL BIT,
	@ROUNDINGTYPEID INT,
	@ROUNDINGVALUE DECIMAL(20,4),
	@CACONDITIONGROUPID CHAR(36),
	@CAEXPRESSIONID CHAR(36)
)
AS

UPDATE [dbo].[CAFEETEMPLATEDISCOUNT] SET
	[CAFEETEMPLATEID] = @CAFEETEMPLATEID,
	[CADISCOUNTID] = @CADISCOUNTID,
	[ISMANUAL] = @ISMANUAL,
	[ISHIDDEN] = @ISHIDDEN,
	[APPLYORDER] = @APPLYORDER,
	[APPLYTOTOTAL] = @APPLYTOTOTAL,
	[ROUNDINGTYPEID] = @ROUNDINGTYPEID,
	[ROUNDINGVALUE] = @ROUNDINGVALUE,
	[CACONDITIONGROUPID] = @CACONDITIONGROUPID,
	[CAEXPRESSIONID] = @CAEXPRESSIONID

WHERE
	[CAFEETEMPLATEDISCOUNTID] = @CAFEETEMPLATEDISCOUNTID