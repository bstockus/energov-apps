CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDCOLUMNTEMPLATE_UPDATE]
(
	@CUSTOMFIELDCOLUMNTEMPLATEID CHAR(36),
	@FKCUSTOMFIELDLAYOUTCONTROLTYPE INT,
	@GCUSTOMFIELD CHAR(36),
	@SCOLUMNNAME NVARCHAR(50),
	@SDESCRIPTION NVARCHAR(50),
	@SFIELDTIP NVARCHAR(MAX),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CUSTOMFIELDCOLUMNTEMPLATE] SET
	[FKCUSTOMFIELDLAYOUTCONTROLTYPE] = @FKCUSTOMFIELDLAYOUTCONTROLTYPE,
	[GCUSTOMFIELD] = @GCUSTOMFIELD,
	[SCOLUMNNAME] = @SCOLUMNNAME,
	[SDESCRIPTION] = @SDESCRIPTION,
	[SFIELDTIP] = @SFIELDTIP,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CUSTOMFIELDCOLUMNTEMPLATEID] = @CUSTOMFIELDCOLUMNTEMPLATEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE