CREATE PROCEDURE [dbo].[USP_CAEXPRESSION_UPDATE]
(
	@CAEXPRESSIONID CHAR(36),
	@CAEXPRESSIONTYPE INT,
	@RETURNTYPE INT,
	@CONSTANTVALUE NVARCHAR(MAX),
	@VARIABLETYPEID INT,
	@VARIABLEINPUTNAME NVARCHAR(100),
	@VARIABLEINPUTID CHAR(36),
	@PARENTEXPRESSIONID CHAR(36),
	@PARAMETERINDEX INT
)
AS

UPDATE [dbo].[CAEXPRESSION] SET
	[CAEXPRESSIONTYPE] = @CAEXPRESSIONTYPE,
	[RETURNTYPE] = @RETURNTYPE,
	[CONSTANTVALUE] = @CONSTANTVALUE,
	[VARIABLETYPEID] = @VARIABLETYPEID,
	[VARIABLEINPUTNAME] = @VARIABLEINPUTNAME,
	[VARIABLEINPUTID] = @VARIABLEINPUTID,
	[PARENTEXPRESSIONID] = @PARENTEXPRESSIONID,
	[PARAMETERINDEX] = @PARAMETERINDEX

WHERE
	[CAEXPRESSIONID] = @CAEXPRESSIONID