CREATE PROCEDURE [dbo].[USP_CUSTOMFIELD_INSERT]
(
	@GCUSTOMFIELD CHAR(36),
	@FKICUSTOMFIELDTYPE INT,
	@SFIELDNAME NVARCHAR(50),
	@SCUSTOMFIELD NVARCHAR(50),
	@BISPICKLIST BIT,
	@BISWORKSHEET BIT,
	@FKGCUSTOMFIELDTEMPLATE CHAR(36),
	@FKCUSTOMFIELDTABLE CHAR(36),
	@FKCUSTOMFIELDTABLECOLREF CHAR(36),
	@COLUMNOPERATOR INT
)
AS

INSERT INTO [dbo].[CUSTOMFIELD](
	[GCUSTOMFIELD],
	[FKICUSTOMFIELDTYPE],
	[SFIELDNAME],
	[SCUSTOMFIELD],
	[BISPICKLIST],
	[BISWORKSHEET],
	[FKGCUSTOMFIELDTEMPLATE],
	[FKCUSTOMFIELDTABLE],
	[FKCUSTOMFIELDTABLECOLREF],
	[COLUMNOPERATOR]
)

VALUES
(
	@GCUSTOMFIELD,
	@FKICUSTOMFIELDTYPE,
	@SFIELDNAME,
	@SCUSTOMFIELD,
	@BISPICKLIST,
	@BISWORKSHEET,
	@FKGCUSTOMFIELDTEMPLATE,
	@FKCUSTOMFIELDTABLE,
	@FKCUSTOMFIELDTABLECOLREF,
	@COLUMNOPERATOR
)