CREATE PROCEDURE [dbo].[USP_CACPI_INSERT]
(
	@CACPIID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(255),
	@STARTDATE DATETIME,
	@ENDDATE DATETIME,
	@CPIVALUE DECIMAL(20,4)
)
AS

INSERT INTO [dbo].[CACPI](
	[CACPIID],
	[NAME],
	[DESCRIPTION],
	[STARTDATE],
	[ENDDATE],
	[CPIVALUE]
)
VALUES
(
	@CACPIID,
	@NAME,
	@DESCRIPTION,
	@STARTDATE,
	@ENDDATE,
	@CPIVALUE
)