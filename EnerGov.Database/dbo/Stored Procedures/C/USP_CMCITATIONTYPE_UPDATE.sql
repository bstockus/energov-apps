CREATE PROCEDURE [dbo].[USP_CMCITATIONTYPE_UPDATE]
(
	@CMCITATIONTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@PREFIX NVARCHAR(10),
	@DEFAULTCITATIONSTATUSID CHAR(36),
	@ACTIVE BIT,
	@AUTONUMBER BIT,
	@TYPESPECIFICAUTONUMBER BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CMCITATIONTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[PREFIX] = @PREFIX,
	[DEFAULTCITATIONSTATUSID] = @DEFAULTCITATIONSTATUSID,
	[ACTIVE] = @ACTIVE,
	[AUTONUMBER] = @AUTONUMBER,
	[TYPESPECIFICAUTONUMBER] = @TYPESPECIFICAUTONUMBER,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[CMCITATIONTYPEID] = @CMCITATIONTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE