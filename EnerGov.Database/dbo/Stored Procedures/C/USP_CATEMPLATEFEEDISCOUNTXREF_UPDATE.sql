CREATE PROCEDURE [dbo].[USP_CATEMPLATEFEEDISCOUNTXREF_UPDATE]
(
	@CATEMPLATEFEEDISCOUNTXREFID CHAR(36),
	@CAFEETEMPLATEFEEID CHAR(36),
	@CAFEETEMPLATEDISCOUNTID CHAR(36),
	@SORTORDER INT
)
AS

UPDATE [dbo].[CATEMPLATEFEEDISCOUNTXREF] SET
	[CAFEETEMPLATEFEEID] = @CAFEETEMPLATEFEEID,
	[CAFEETEMPLATEDISCOUNTID] = @CAFEETEMPLATEDISCOUNTID,
	[SORTORDER] = @SORTORDER

WHERE
	[CATEMPLATEFEEDISCOUNTXREFID] = @CATEMPLATEFEEDISCOUNTXREFID