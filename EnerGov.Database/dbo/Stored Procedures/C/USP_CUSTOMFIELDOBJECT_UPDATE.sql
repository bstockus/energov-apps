CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDOBJECT_UPDATE]
(
	@GCUSTOMFIELD CHAR(36),
	@FKGCUSTOMFIELDLAYOUT CHAR(36),
	@FKCUSTOMFIELDLAYOUTCONTROLTYPE INT,
	@SLABEL NVARCHAR(50),
	@FLEFTPOS NUMERIC(29,15),
	@FTOPPOS NUMERIC(29,15),
	@FHEIGHT NUMERIC(29,15),
	@FWIDTH NUMERIC(29,15),
	@BISINTABCONTROL BIT,
	@IPOSITION INT,
	@FKGCUSTOMFIELD CHAR(36),
	@FKGPARENTCONTROL CHAR(36),
	@TABINDEX INT,
	@LABELDATA NVARCHAR(MAX),
	@SDEFAULTVALUE NVARCHAR(50),
	@SFIELDTIP NVARCHAR(MAX),
	@BISREQUIRED BIT,
	@FORMULA NVARCHAR(MAX),
	@ISLABELSURPRESSED BIT,
	@CUSTOMIMAGE VARBINARY(MAX),
	@HYPERLINKDATA NVARCHAR(MAX),
	@COLUMNHEADERNAME NVARCHAR(50),
	@SPANACROSSCOLUMN BIT,
	@BFOOTER BIT,
	@COLUMNWIDTHS NVARCHAR(MAX),
	@NUMBEROFCOLUMNS INT
)
AS

UPDATE [dbo].[CUSTOMFIELDOBJECT] SET
	[FKGCUSTOMFIELDLAYOUT] = @FKGCUSTOMFIELDLAYOUT,
	[FKCUSTOMFIELDLAYOUTCONTROLTYPE] = @FKCUSTOMFIELDLAYOUTCONTROLTYPE,
	[SLABEL] = @SLABEL,
	[FLEFTPOS] = @FLEFTPOS,
	[FTOPPOS] = @FTOPPOS,
	[FHEIGHT] = @FHEIGHT,
	[FWIDTH] = @FWIDTH,
	[BISINTABCONTROL] = @BISINTABCONTROL,
	[IPOSITION] = @IPOSITION,
	[FKGCUSTOMFIELD] = @FKGCUSTOMFIELD,
	[FKGPARENTCONTROL] = @FKGPARENTCONTROL,
	[TABINDEX] = @TABINDEX,
	[LABELDATA] = @LABELDATA,
	[SDEFAULTVALUE] = @SDEFAULTVALUE,
	[SFIELDTIP] = @SFIELDTIP,
	[BISREQUIRED] = @BISREQUIRED,
	[FORMULA] = @FORMULA,
	[ISLABELSURPRESSED] = @ISLABELSURPRESSED,
	[CUSTOMIMAGE] = @CUSTOMIMAGE,
	[HYPERLINKDATA] = @HYPERLINKDATA,
	[COLUMNHEADERNAME] = @COLUMNHEADERNAME,
	[SPANACROSSCOLUMN] = @SPANACROSSCOLUMN,
	[BFOOTER] = @BFOOTER,
	[COLUMNWIDTHS] = @COLUMNWIDTHS,
	[NUMBEROFCOLUMNS] = @NUMBEROFCOLUMNS

WHERE
	[GCUSTOMFIELD] = @GCUSTOMFIELD