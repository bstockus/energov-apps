CREATE PROCEDURE [dbo].[USP_CMCODE_INSERT]
(
	@CMCODEID CHAR(36),
	@DESCRIPTION NVARCHAR(MAX),
	@CODENUMBER NVARCHAR(50),
	@CMCODECATEGORYID CHAR(36),
	@CORRECTIVEACTION NVARCHAR(MAX),
	@WFACTIONGROUPID CHAR(36),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@COMPLIANCEDAYSUNTILDUEHIGH INT,
	@CAFEETEMPLATEID CHAR(36),
	@COMPLIANCEDAYSUNTILDUEMED INT,
	@COMPLIANCEDAYSUNTILDUELOW INT,
	@DEFAULTVIOLATIONPRIORITYID CHAR(36),
	@DEFAULTCMCOURTSTATUSID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CMCODE](
	[CMCODEID],
	[DESCRIPTION],
	[CODENUMBER],
	[CMCODECATEGORYID],
	[CORRECTIVEACTION],
	[WFACTIONGROUPID],
	[CUSTOMFIELDLAYOUTID],
	[COMPLIANCEDAYSUNTILDUEHIGH],
	[CAFEETEMPLATEID],
	[COMPLIANCEDAYSUNTILDUEMED],
	[COMPLIANCEDAYSUNTILDUELOW],
	[DEFAULTVIOLATIONPRIORITYID],
	[DEFAULTCMCOURTSTATUSID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@CMCODEID,
	@DESCRIPTION,
	@CODENUMBER,
	@CMCODECATEGORYID,
	@CORRECTIVEACTION,
	@WFACTIONGROUPID,
	@CUSTOMFIELDLAYOUTID,
	@COMPLIANCEDAYSUNTILDUEHIGH,
	@CAFEETEMPLATEID,
	@COMPLIANCEDAYSUNTILDUEMED,
	@COMPLIANCEDAYSUNTILDUELOW,
	@DEFAULTVIOLATIONPRIORITYID,
	@DEFAULTCMCOURTSTATUSID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE