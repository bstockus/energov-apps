CREATE PROCEDURE [dbo].[USP_CODECASEEVENTQUEUE_UPDATE]
(
@EVENTQUEUEID AS BIGINT,
@EVENTSTATUSID INT
)
AS  
BEGIN  
	SET NOCOUNT ON;
	UPDATE [dbo].[CODECASEEVENTQUEUE]
	SET 
		PROCESSEDDATE = GETDATE(),
		EVENTSTATUSID = @EVENTSTATUSID
	WHERE [dbo].[CODECASEEVENTQUEUE].[CODECASEEVENTQUEUEID] = @EVENTQUEUEID

END