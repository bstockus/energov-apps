	@FKCUSTOMFIELDLAYOUTCONTROLTYPE INT,
	@GCUSTOMFIELD CHAR(36),
	@SCOLUMNNAME NVARCHAR(50),
	@SDESCRIPTION NVARCHAR(50),
	@SFIELDTIP NVARCHAR(MAX),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[CUSTOMFIELDCOLUMNTEMPLATE](
	[CUSTOMFIELDCOLUMNTEMPLATEID],
	[FKCUSTOMFIELDLAYOUTCONTROLTYPE],
	[GCUSTOMFIELD],
	[SCOLUMNNAME],
	[SDESCRIPTION],
	[SFIELDTIP],
