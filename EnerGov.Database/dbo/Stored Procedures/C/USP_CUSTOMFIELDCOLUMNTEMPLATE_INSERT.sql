	@FKCUSTOMFIELDLAYOUTCONTROLTYPE,
	@GCUSTOMFIELD,
	@SCOLUMNNAME,
	@SDESCRIPTION,
	@SFIELDTIP,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE