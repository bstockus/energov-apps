CREATE PROCEDURE [dbo].[USP_CUSTOMFIELDLAYOUT_UPDATE]
(
	@GCUSTOMFIELDLAYOUTS CHAR(36),
	@SNAME VARCHAR(50),
	@CUSTOMFIELDMODULEID INT,
	@WIDTH INT,
	@HEIGHT INT,
	@ISVERSION2 BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@NUMBEROFCOLUMNS INT,
	@ISHTMLEXPERIENCE BIT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[CUSTOMFIELDLAYOUT] SET
	[SNAME] = @SNAME,
	[CUSTOMFIELDMODULEID] = @CUSTOMFIELDMODULEID,
	[WIDTH] = @WIDTH,
	[HEIGHT] = @HEIGHT,
	[ISVERSION2] = @ISVERSION2,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1,
	[NUMBEROFCOLUMNS] = @NUMBEROFCOLUMNS,
	[ISHTMLEXPERIENCE] = @ISHTMLEXPERIENCE
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[GCUSTOMFIELDLAYOUTS] = @GCUSTOMFIELDLAYOUTS AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE