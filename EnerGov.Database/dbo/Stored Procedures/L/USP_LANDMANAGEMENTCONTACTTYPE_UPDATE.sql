CREATE PROCEDURE [dbo].[USP_LANDMANAGEMENTCONTACTTYPE_UPDATE]
(
	@LANDMANAGEMENTCONTACTTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@LANDMANAGEMENTCONTACTSYSTEMTYP INT,
	@ISVALIDATELIC BIT,
	@ISVALIDATEBLLICENSE BIT,
	@ISVALIDATEILLICENSE BIT,
	@EPORTALSEARCHCRITERIAID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[LANDMANAGEMENTCONTACTTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[LANDMANAGEMENTCONTACTSYSTEMTYP] = @LANDMANAGEMENTCONTACTSYSTEMTYP,
	[ISVALIDATELIC] = @ISVALIDATELIC,
	[ISVALIDATEBLLICENSE] = @ISVALIDATEBLLICENSE,
	[ISVALIDATEILLICENSE] = @ISVALIDATEILLICENSE,
	[EPORTALSEARCHCRITERIAID] = @EPORTALSEARCHCRITERIAID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[LANDMANAGEMENTCONTACTTYPEID] = @LANDMANAGEMENTCONTACTTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE