CREATE PROCEDURE [dbo].[DELETEPLANPARCELPROJCONDITION]
	-- Add the parameters for the stored procedure here
	@ConditionID char(36)	
	AS
	BEGIN		
		DELETE FROM PLPLANCONDITION WHERE CONDITIONID = @ConditionID
		DELETE FROM PARCELCONDITION WHERE CONDITIONID = @ConditionID
		DELETE FROM PRPROJECTCONDITION WHERE CONDITIONID = @ConditionID
	END
