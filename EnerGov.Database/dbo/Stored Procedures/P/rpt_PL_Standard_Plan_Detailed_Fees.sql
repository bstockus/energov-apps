
CREATE PROCEDURE rpt_PL_Standard_Plan_Detailed_Fees 
@PLPLANID VARCHAR(36)
AS

SELECT PMF.PLPLANID
     , CF.FEENAME, CF.COMPUTEDAMOUNT

FROM PLPLANFEE PMF
INNER JOIN CACOMPUTEDFEE CF ON PMF.CACOMPUTEDFEEID = CF.CACOMPUTEDFEEID AND CF.CASTATUSID NOT IN (5,10) --VOID, DELETED
WHERE PMF.PLPLANID = @PLPLANID
