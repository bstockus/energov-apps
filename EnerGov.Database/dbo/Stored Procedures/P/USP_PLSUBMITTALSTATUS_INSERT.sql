CREATE PROCEDURE [dbo].[USP_PLSUBMITTALSTATUS_INSERT]
(
	@PLSUBMITTALSTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@DESCRIPTIONSPANISH NVARCHAR(MAX),
	@SUCCESSFLAG BIT,
	@FAILUREFLAG BIT,
	@NOTREQUIREDFLAG BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PLSUBMITTALSTATUS](
	[PLSUBMITTALSTATUSID],
	[NAME],
	[DESCRIPTION],
	[DESCRIPTION_SPANISH],
	[SUCCESSFLAG],
	[FAILUREFLAG],
	[NOTREQUIREDFLAG],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PLSUBMITTALSTATUSID,
	@NAME,
	@DESCRIPTION,
	@DESCRIPTIONSPANISH,
	@SUCCESSFLAG,
	@FAILUREFLAG,
	@NOTREQUIREDFLAG,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE