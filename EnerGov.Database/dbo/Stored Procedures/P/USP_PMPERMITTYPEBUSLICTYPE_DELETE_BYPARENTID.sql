CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEBUSLICTYPE_DELETE_BYPARENTID]  
(  
@PERMITTYPEID CHAR(36),
@PERMITTYPEWORKCLASSID CHAR(36)
)  
AS

-- Delete Business license classification type for (permittype,permittypeworkclass)
EXEC [dbo].[USP_PMPERMITTYPEBUSLICTYPEBUSWC_DELETE_BYPARENTID] @PERMITTYPEID, @PERMITTYPEWORKCLASSID, null

DELETE [dbo].[PMPERMITTYPEBUSLICTYPE] FROM [dbo].[PMPERMITTYPEBUSLICTYPE]
INNER JOIN [dbo].[PMPERMITTYPEWORKCLASS] ON [dbo].[PMPERMITTYPEBUSLICTYPE].[PMPERMITTYPEWORKCLASSID] = [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEWORKCLASSID]
WHERE
	[dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEID] = @PERMITTYPEID AND (@PERMITTYPEWORKCLASSID IS NULL OR [dbo].[PMPERMITTYPEBUSLICTYPE].[PMPERMITTYPEWORKCLASSID] = @PERMITTYPEWORKCLASSID)