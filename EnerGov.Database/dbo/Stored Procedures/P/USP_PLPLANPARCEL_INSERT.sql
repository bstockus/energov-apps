CREATE PROCEDURE [dbo].[USP_PLPLANPARCEL_INSERT]
(
	@PLPLANID CHAR(36),
	@PARCELID CHAR(36),
	@MAIN BIT,
	@PARCELSPLITPROCESS BIT
)
AS
BEGIN
INSERT INTO [dbo].[PLPLANPARCEL]
		(
			[PLPLANPARCELID],
			[PLPLANID],
			[PARCELID],
			[MAIN],
			[PARCELSPLITPROCESS]
		)
		VALUES
		(
			NEWID(),
			@PLPLANID,
			@PARCELID,
			@MAIN,
			@PARCELSPLITPROCESS
		)
END