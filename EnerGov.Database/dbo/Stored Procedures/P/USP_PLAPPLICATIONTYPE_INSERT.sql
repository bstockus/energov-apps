CREATE PROCEDURE [dbo].[USP_PLAPPLICATIONTYPE_INSERT]
(
	@PLAPPLICATIONTYPEID CHAR(36),
	@APPLICATIONTYPENAME NVARCHAR(255),
	@DESCRIPTION NVARCHAR(MAX),
	@CUSTOMFIELDID CHAR(36),
	@DEFAULTAPPLICATIONSTATUSID CHAR(36),
	@PREFIX NVARCHAR(20),
	@ISAUTONUMBER BIT,
	@DEFAULTPERMITTYPE CHAR(36),
	@DEFAULTPLANTYPE CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PLAPPLICATIONTYPE](
	[PLAPPLICATIONTYPEID],
	[APPLICATIONTYPENAME],
	[DESCRIPTION],
	[CUSTOMFIELDID],
	[DEFAULTAPPLICATIONSTATUSID],
	[PREFIX],
	[ISAUTONUMBER],
	[DEFAULTPERMITTYPE],
	[DEFAULTPLANTYPE],
	[CAFEETEMPLATEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PLAPPLICATIONTYPEID,
	@APPLICATIONTYPENAME,
	@DESCRIPTION,
	@CUSTOMFIELDID,
	@DEFAULTAPPLICATIONSTATUSID,
	@PREFIX,
	@ISAUTONUMBER,
	@DEFAULTPERMITTYPE,
	@DEFAULTPLANTYPE,
	@CAFEETEMPLATEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE