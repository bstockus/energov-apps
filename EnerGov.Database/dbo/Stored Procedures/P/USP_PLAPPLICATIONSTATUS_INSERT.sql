CREATE PROCEDURE [dbo].[USP_PLAPPLICATIONSTATUS_INSERT]
(
	@PLAPPLICATIONSTATUSID CHAR(36),
	@STATUS NVARCHAR(255),
	@DESCRIPTION NVARCHAR(255),
	@SUCCESSFLAG BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PLAPPLICATIONSTATUS](
	[PLAPPLICATIONSTATUSID],
	[STATUS],
	[DESCRIPTION],
	[SUCCESSFLAG],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PLAPPLICATIONSTATUSID,
	@STATUS,
	@DESCRIPTION,
	@SUCCESSFLAG,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE