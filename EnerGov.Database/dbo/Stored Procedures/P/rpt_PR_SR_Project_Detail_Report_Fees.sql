

CREATE PROCEDURE [dbo].[rpt_PR_SR_Project_Detail_Report_Fees]
@PRPROJECTID AS VARCHAR(36)
AS
SELECT	CACOMPUTEDFEE.FEENAME, CACOMPUTEDFEE.COMPUTEDAMOUNT, CAINVOICE.INVOICENUMBER, CAINVOICE.INVOICEDATE, CAINVOICE.CAINVOICEID, 
		CAINVOICEFEE.CACOMPUTEDFEEID, CASTATUS.NAME AS INVOICESTATUS, CASTATUS_1.NAME AS COMPUTEDFEESTATUS, CASTATUS_2.NAME AS [TRANSACTIONFEE STATUS], 
		CATRANSACTIONSTATUS.NAME AS TRANSACTIONSTATUS, CATRANSACTIONFEE.PAIDAMOUNT, CATRANSACTION.RECEIPTNUMBER
FROM	PRPROJECTFEE
INNER JOIN CACOMPUTEDFEE ON PRPROJECTFEE.CACOMPUTEDFEEID = CACOMPUTEDFEE.CACOMPUTEDFEEID 
INNER JOIN CASTATUS AS CASTATUS_1 ON CACOMPUTEDFEE.CASTATUSID = CASTATUS_1.CASTATUSID AND (CASTATUS_1.CASTATUSID <> 5) 
LEFT OUTER JOIN CATRANSACTIONFEE ON CATRANSACTIONFEE.CACOMPUTEDFEEID = CACOMPUTEDFEE.CACOMPUTEDFEEID
LEFT OUTER JOIN CATRANSACTION ON CATRANSACTION.CATRANSACTIONID = CATRANSACTIONFEE.CATRANSACTIONID 
LEFT OUTER JOIN CATRANSACTIONSTATUS ON CATRANSACTION.CATRANSACTIONSTATUSID = CATRANSACTIONSTATUS.CATRANSACTIONSTATUSID AND CATRANSACTIONSTATUS.CATRANSACTIONSTATUSID NOT IN (2,3) 
LEFT OUTER JOIN CASTATUS AS CASTATUS_2 ON CATRANSACTIONFEE.CASTATUSID = CASTATUS_2.CASTATUSID 
LEFT OUTER JOIN CAINVOICEFEE ON CACOMPUTEDFEE.CACOMPUTEDFEEID = CAINVOICEFEE.CACOMPUTEDFEEID 
LEFT OUTER JOIN CAINVOICE ON CAINVOICEFEE.CAINVOICEID = CAINVOICE.CAINVOICEID 
LEFT OUTER JOIN CASTATUS ON CAINVOICE.CASTATUSID = CASTATUS.CASTATUSID AND (CASTATUS.CASTATUSID <> 5)
WHERE (PRPROJECTFEE.PRPROJECTID = @PRPROJECTID)
