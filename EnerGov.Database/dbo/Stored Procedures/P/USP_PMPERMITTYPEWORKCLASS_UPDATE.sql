CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEWORKCLASS_UPDATE]
(
	@PMPERMITTYPEWORKCLASSID CHAR(36),
	@PMPERMITTYPEID CHAR(36),
	@PMPERMITWORKCLASSID CHAR(36),
	@WFTEMPLATEID CHAR(36),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@ONLINECUSTOMFIELDLAYOUTID CHAR(36),
	@INTERNETFLAG BIT,
	@PMPERMITCAPAPPLICATIONTYPEID INT,
	@ISCAPADDRESSREQUIRED BIT,
	@RENEWALFEETEMPLATEID CHAR(36),
	@PLREVIEWITEMCAPVISIBILITYID INT,
	@FILESETID CHAR(36),
	@REQUIREALLCERTTYPE BIT,
	@DEFAULTCASEASSIGNEDTO CHAR(36),
	@VALIDATECERTIFICATIONSFORALLCONTACTS BIT,
	@VALIDATEPROFLICENSESFORALLCONTACTS BIT
)
AS

UPDATE [dbo].[PMPERMITTYPEWORKCLASS] SET
	[PMPERMITTYPEID] = @PMPERMITTYPEID,
	[PMPERMITWORKCLASSID] = @PMPERMITWORKCLASSID,
	[WFTEMPLATEID] = @WFTEMPLATEID,
	[CUSTOMFIELDLAYOUTID] = @CUSTOMFIELDLAYOUTID,
	[CAFEETEMPLATEID] = @CAFEETEMPLATEID,
	[ONLINECUSTOMFIELDLAYOUTID] = @ONLINECUSTOMFIELDLAYOUTID,
	[INTERNETFLAG] = @INTERNETFLAG,
	[PMPERMITCAPAPPLICATIONTYPEID] = @PMPERMITCAPAPPLICATIONTYPEID,
	[ISCAPADDRESSREQUIRED] = @ISCAPADDRESSREQUIRED,
	[RENEWALFEETEMPLATEID] = @RENEWALFEETEMPLATEID,
	[PLREVIEWITEMCAPVISIBILITYID] = @PLREVIEWITEMCAPVISIBILITYID,
	[FILESETID] = @FILESETID,
	[REQUIREALLCERTTYPE] = @REQUIREALLCERTTYPE,
	[DEFAULTCASEASSIGNEDTO] = @DEFAULTCASEASSIGNEDTO,
	[VALIDATECERTIFICATIONSFORALLCONTACTS] = @VALIDATECERTIFICATIONSFORALLCONTACTS,
	[VALIDATEPROFLICENSESFORALLCONTACTS] = @VALIDATEPROFLICENSESFORALLCONTACTS

WHERE
	[PMPERMITTYPEWORKCLASSID] = @PMPERMITTYPEWORKCLASSID