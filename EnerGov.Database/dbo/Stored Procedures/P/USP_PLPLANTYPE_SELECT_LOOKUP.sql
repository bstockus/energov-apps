CREATE PROCEDURE [dbo].[USP_PLPLANTYPE_SELECT_LOOKUP]
(
	@ACTIVEONLY BIT = 1
)
AS
SET NOCOUNT ON;
SELECT 
	[dbo].[PLPLANTYPE].[PLPLANTYPEID],
	[dbo].[PLPLANTYPE].[PLANNAME]
FROM [dbo].[PLPLANTYPE]
WHERE [dbo].[PLPLANTYPE].[ACTIVE] = @ACTIVEONLY OR @ACTIVEONLY = 0
ORDER BY [dbo].[PLPLANTYPE].[PLANNAME]