CREATE PROCEDURE [dbo].[USP_PLPLANACTIVITYTYPE_INSERT]
(
	@PLPLANACTIVITYTYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@CANEDIT BIT,
	@CREATEID BIT,
	@PREFIXID NVARCHAR(40),
	@ALLOWDUPLICATE BIT,
	@CUSTOMFIELDID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PLPLANACTIVITYTYPE](
	[PLPLANACTIVITYTYPEID],
	[NAME],
	[DESCRIPTION],
	[CANEDIT],
	[CREATEID],
	[PREFIXID],
	[ALLOWDUPLICATE],
	[CUSTOMFIELDID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PLPLANACTIVITYTYPEID,
	@NAME,
	@DESCRIPTION,
	@CANEDIT,
	@CREATEID,
	@PREFIXID,
	@ALLOWDUPLICATE,
	@CUSTOMFIELDID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE