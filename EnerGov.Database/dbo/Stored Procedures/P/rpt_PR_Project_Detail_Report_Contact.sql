

CREATE PROCEDURE [dbo].[rpt_PR_Project_Detail_Report_Contact]
@PROJECTID AS VARCHAR(36)
AS
SELECT     GLOBALENTITY.FIRSTNAME, GLOBALENTITY.LASTNAME, MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.ADDRESSLINE2, 
                      MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, MAILINGADDRESS.POSTALCODE, MAILINGADDRESS.POSTDIRECTION, 
                      MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.STREETTYPE, MAILINGADDRESS.UNITORSUITE, LANDMANAGEMENTCONTACTTYPE.NAME AS ContactType, 
                      GLOBALENTITY.BUSINESSPHONE, GLOBALENTITY.HOMEPHONE, GLOBALENTITY.MOBILEPHONE
FROM         LANDMANAGEMENTCONTACTTYPE INNER JOIN
                      PRPROJECTCONTACT INNER JOIN
                      GLOBALENTITY ON PRPROJECTCONTACT.GLOBALENTITYID = GLOBALENTITY.GLOBALENTITYID ON 
                      LANDMANAGEMENTCONTACTTYPE.LANDMANAGEMENTCONTACTTYPEID = PRPROJECTCONTACT.LANDMANAGEMENTCONTACTTYPEID LEFT OUTER JOIN
                      MAILINGADDRESS INNER JOIN
                      GLOBALENTITYMAILINGADDRESS ON MAILINGADDRESS.MAILINGADDRESSID = GLOBALENTITYMAILINGADDRESS.MAILINGADDRESSID ON 
                      GLOBALENTITY.GLOBALENTITYID = GLOBALENTITYMAILINGADDRESS.GLOBALENTITYID
WHERE PRPROJECTCONTACT.PRPROJECTID = @PROJECTID

