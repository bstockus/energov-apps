
CREATE PROCEDURE rpt_PM_Permit_Detailed_Dept_B_Fees 
@PMPERMITID VARCHAR(36)
AS
SELECT PMF.PMPERMITID, CF.FEENAME, CF.COMPUTEDAMOUNT
FROM PMPERMITFEE PMF
INNER JOIN CACOMPUTEDFEE CF ON PMF.CACOMPUTEDFEEID = CF.CACOMPUTEDFEEID AND CF.CASTATUSID NOT IN (5,10) --VOID, DELETED
WHERE PMF.PMPERMITID = @PMPERMITID
