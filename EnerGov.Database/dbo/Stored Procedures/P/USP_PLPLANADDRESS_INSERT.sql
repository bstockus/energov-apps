CREATE PROCEDURE [dbo].[USP_PLPLANADDRESS_INSERT]
(
	@PLPLANID CHAR(36),
	@MAILINGADDRESSID CHAR(36),
	@MAIN BIT
)
AS
BEGIN
INSERT INTO [dbo].[PLPLANADDRESS]
		(
			[PLPLANADDRESSID],
			[PLPLANID],
			[MAIN],
			[MAILINGADDRESSID]
		)
		VALUES
		(
			NEWID(),
			@PLPLANID,
			@MAIN,
			@MAILINGADDRESSID
		)
END