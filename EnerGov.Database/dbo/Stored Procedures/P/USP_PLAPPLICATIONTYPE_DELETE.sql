CREATE PROCEDURE [dbo].[USP_PLAPPLICATIONTYPE_DELETE]
(
	@PLAPPLICATIONTYPEID CHAR(36),
	@ROWVERSION INT
)
AS

SET NOCOUNT ON;

EXEC USP_PLCONTACTTYPEREF_DELETE_BYPARENTID @PLAPPLICATIONTYPEID
EXEC USP_PLAPPLICATIONALLOWEDACTIVITY_DELETE_BYPARENTID @PLAPPLICATIONTYPEID

SET NOCOUNT OFF;

DELETE FROM [dbo].[PLAPPLICATIONTYPE]
WHERE
	[PLAPPLICATIONTYPEID] = @PLAPPLICATIONTYPEID AND 
	[ROWVERSION]= @ROWVERSION