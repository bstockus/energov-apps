CREATE PROCEDURE [dbo].[USP_PLITEMREVIEWSTATUS_INSERT]
(
	@PLITEMREVIEWSTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@SUCCESSFLAG BIT,
	@FAILUREFLAG BIT,
	@SHOWCOMMENTSINCAP BIT,
	@SHOWRECOMMENDATIONSINCAP BIT,
	@SHOWCORRECTIONSINCAP BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PLITEMREVIEWSTATUS](
	[PLITEMREVIEWSTATUSID],
	[NAME],
	[SUCCESSFLAG],
	[FAILUREFLAG],
	[SHOWCOMMENTSINCAP],
	[SHOWRECOMMENDATIONSINCAP],
	[SHOWCORRECTIONSINCAP],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PLITEMREVIEWSTATUSID,
	@NAME,
	@SUCCESSFLAG,
	@FAILUREFLAG,
	@SHOWCOMMENTSINCAP,
	@SHOWRECOMMENDATIONSINCAP,
	@SHOWCORRECTIONSINCAP,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE