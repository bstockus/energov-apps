CREATE PROCEDURE [dbo].[USP_PMPERMITVALUATIONTYPEVALGROUP_DELETE]
(
	@PMPERMITVALTYPEVALGRPID CHAR(36),
	@ROWVERSION INT
)
AS
BEGIN

	SET NOCOUNT ON;

	EXEC [dbo].[USP_PMPERMITVALUATIONTYPEVALGROUPX_DELETE_BYPARENTID] @PMPERMITVALTYPEVALGRPID

	SET NOCOUNT OFF;

	DELETE FROM [dbo].[PMPERMITVALUATIONTYPEVALGROUP]
	WHERE
		[PMPERMITVALTYPEVALGRPID] = @PMPERMITVALTYPEVALGRPID AND 
		[ROWVERSION]= @ROWVERSION
END