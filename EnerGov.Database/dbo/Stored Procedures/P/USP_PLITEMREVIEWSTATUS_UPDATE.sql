CREATE PROCEDURE [dbo].[USP_PLITEMREVIEWSTATUS_UPDATE]
(
	@PLITEMREVIEWSTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@SUCCESSFLAG BIT,
	@FAILUREFLAG BIT,
	@SHOWCOMMENTSINCAP BIT,
	@SHOWRECOMMENDATIONSINCAP BIT,
	@SHOWCORRECTIONSINCAP BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[PLITEMREVIEWSTATUS] SET
	[NAME] = @NAME,
	[SUCCESSFLAG] = @SUCCESSFLAG,
	[FAILUREFLAG] = @FAILUREFLAG,
	[SHOWCOMMENTSINCAP] = @SHOWCOMMENTSINCAP,
	[SHOWRECOMMENDATIONSINCAP] = @SHOWRECOMMENDATIONSINCAP,
	[SHOWCORRECTIONSINCAP] = @SHOWCORRECTIONSINCAP,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[PLITEMREVIEWSTATUSID] = @PLITEMREVIEWSTATUSID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE