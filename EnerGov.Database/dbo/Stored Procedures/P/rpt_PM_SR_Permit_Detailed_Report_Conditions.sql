

/****** Object:  StoredProcedure [dbo].[rpt_PM_SR_Permit_Detailed_Report_CONDITIONS]    Script Date: 1/16/2014 1:36:48 PM ******/
CREATE PROCEDURE [dbo].[rpt_PM_SR_Permit_Detailed_Report_Conditions]
@PMPERMITID AS VARCHAR(36)
AS

SELECT	CONDITIONCATEGORY.NAME AS CONDITIONCATEGORY, CONDITION.NAME AS CONDITION, CONDITION.COMMENTS, 
		PMPERMITCONDITION.CREATEDON, PMPERMITCONDITION.ISSATISFIED 
		
FROM	PMPERMITCONDITION 
		INNER JOIN CONDITION ON PMPERMITCONDITION.CONDITIONID = CONDITION.CONDITIONID 
		INNER JOIN CONDITIONCATEGORY ON CONDITION.CONDITIONCATEGORYID = CONDITIONCATEGORY.CONDITIONCATEGORYID   
WHERE	PMPERMITCONDITION.PMPERMITID = @PMPERMITID

