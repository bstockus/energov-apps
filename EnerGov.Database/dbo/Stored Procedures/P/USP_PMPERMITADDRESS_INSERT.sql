CREATE PROCEDURE [dbo].[USP_PMPERMITADDRESS_INSERT]
(
	@PMPERMITID CHAR(36),
	@MAILINGADDRESSID CHAR(36),
	@MAIN BIT
)
AS
BEGIN
INSERT INTO [dbo].[PMPERMITADDRESS]
		(
			[PMPERMITADDRESSID],
			[PMPERMITID],
			[MAIN],
			[MAILINGADDRESSID]
		)
		VALUES
		(
			NEWID(),
			@PMPERMITID,
			@MAIN,
			@MAILINGADDRESSID
		)
END