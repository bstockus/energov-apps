


CREATE PROCEDURE [dbo].[RPT_PM_PERMIT_BONDS_ISSUED_REPORTS]
@STARTDATE AS DATETIME,
@ENDDATE AS DATETIME
AS
SET @ENDDATE = DATEADD(S,-1,DATEADD(D,1,@ENDDATE))
SELECT PMPERMIT.PERMITNUMBER, BOND.BONDNUMBER, BONDTYPE.NAME AS BONDTYPE, BOND.AMOUNT, BOND.BONDDATE AS ISSUEDATE, 
       BOND.BONDEXPIRATIONDATE AS EXPIRATIONDATE, BOND.BONDRELEASEDATE AS RELEASEDATE, BONDSTATUS.NAME AS STATUS,
       BOND.PRINCIPALID, BOND.SURETYID, BOND.OBLIGEEID, BOND.BONDID
FROM PMPERMITBOND 
INNER JOIN PMPERMIT ON PMPERMITBOND.PMPERMITID = PMPERMIT.PMPERMITID 
INNER JOIN BOND ON PMPERMITBOND.BONDID = BOND.BONDID 
INNER JOIN BONDTYPE ON BOND.BONDTYPEID = BONDTYPE.BONDTYPEID 
INNER JOIN BONDSTATUS ON BOND.BONDSTATUSID = BONDSTATUS.BONDSTATUSID
WHERE BOND.BONDDATE BETWEEN @STARTDATE AND @ENDDATE


