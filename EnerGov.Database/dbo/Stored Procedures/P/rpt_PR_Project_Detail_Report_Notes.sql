

CREATE PROCEDURE [dbo].[rpt_PR_Project_Detail_Report_Notes]
@PROJECTID AS VARCHAR(36)
AS
SELECT     USERS.FNAME, USERS.LNAME, PRPROJECTNOTE.TEXT, PRPROJECTNOTE.CREATEDDATE, PRPROJECTNOTE.PRPROJECTNOTEID
FROM         PRPROJECTNOTE INNER JOIN
                      USERS ON PRPROJECTNOTE.CREATEDBY = USERS.SUSERGUID
WHERE PRPROJECTNOTE.PRPROJECTID = @PROJECTID

