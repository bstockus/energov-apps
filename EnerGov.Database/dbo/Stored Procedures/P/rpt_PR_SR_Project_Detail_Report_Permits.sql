

CREATE PROCEDURE [dbo].[rpt_PR_SR_Project_Detail_Report_Permits]
@PRPROJECTID AS VARCHAR(36)
AS
SELECT	PMPERMIT.APPLYDATE, PMPERMIT.EXPIREDATE, PMPERMIT.ISSUEDATE, PMPERMIT.FINALIZEDATE, PMPERMITWORKCLASS.NAME AS WorkClass, 
		PMPERMITSTATUS.NAME AS Status, PMPERMITTYPE.NAME AS PermitType, PMPERMIT.PERMITNUMBER
FROM	PMPERMIT 
INNER JOIN PMPERMITTYPE ON PMPERMIT.PMPERMITTYPEID = PMPERMITTYPE.PMPERMITTYPEID 
INNER JOIN PMPERMITWORKCLASS ON PMPERMIT.PMPERMITWORKCLASSID = PMPERMITWORKCLASS.PMPERMITWORKCLASSID 
INNER JOIN PMPERMITSTATUS ON PMPERMIT.PMPERMITSTATUSID = PMPERMITSTATUS.PMPERMITSTATUSID 
INNER JOIN PRPROJECTPERMIT ON PMPERMIT.PMPERMITID = PRPROJECTPERMIT.PMPERMITID
WHERE PRPROJECTPERMIT.PRPROJECTID = @PRPROJECTID
