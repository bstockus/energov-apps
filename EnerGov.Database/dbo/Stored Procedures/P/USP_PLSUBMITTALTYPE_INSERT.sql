CREATE PROCEDURE [dbo].[USP_PLSUBMITTALTYPE_INSERT]
(
 @PLSUBMITTALTYPEID CHAR(36),
 @TYPENAME NVARCHAR(50),
 @DESCRIPTION NVARCHAR(MAX),
 @DAYSUNTILDUE INT,
 @PLSUBMITTALSTATUSID CHAR(36),
 @RESUBMITALDAYSUNTILDUE INT,
 @COPYFAILEDITEMREVIEW BIT,
 @DESCRIPTION_SPANISH NVARCHAR(MAX) = NULL,
 @LASTCHANGEDBY CHAR(36),
 @LASTCHANGEDON DATETIME,
 @ROWVERSION INT	
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)

INSERT INTO [dbo].[PLSUBMITTALTYPE]
(
 [PLSUBMITTALTYPEID],
 [TYPENAME],
 [DESCRIPTION],
 [DAYSUNTILDUE],
 [PLSUBMITTALSTATUSID],
 [RESUMITALDAYSUNTILDUE],
 [COPYFAILEDITEMREVIEW],
 [DESCRIPTION_SPANISH],
 [LASTCHANGEDBY],
 [LASTCHANGEDON],
 [ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
 @PLSUBMITTALTYPEID,
 @TYPENAME,
 @DESCRIPTION,
 @DAYSUNTILDUE,
 @PLSUBMITTALSTATUSID,
 @RESUBMITALDAYSUNTILDUE,
 @COPYFAILEDITEMREVIEW,
 @DESCRIPTION_SPANISH,
 @LASTCHANGEDBY,
 @LASTCHANGEDON,
 @ROWVERSION
)
SELECT * FROM @OUTPUTTABLE