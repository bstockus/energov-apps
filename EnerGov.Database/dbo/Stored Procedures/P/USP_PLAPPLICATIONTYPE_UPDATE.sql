CREATE PROCEDURE [dbo].[USP_PLAPPLICATIONTYPE_UPDATE]
(
	@PLAPPLICATIONTYPEID CHAR(36),
	@APPLICATIONTYPENAME NVARCHAR(255),
	@DESCRIPTION NVARCHAR(MAX),
	@CUSTOMFIELDID CHAR(36),
	@DEFAULTAPPLICATIONSTATUSID CHAR(36),
	@PREFIX NVARCHAR(20),
	@ISAUTONUMBER BIT,
	@DEFAULTPERMITTYPE CHAR(36),
	@DEFAULTPLANTYPE CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[PLAPPLICATIONTYPE] SET
	[APPLICATIONTYPENAME] = @APPLICATIONTYPENAME,
	[DESCRIPTION] = @DESCRIPTION,
	[CUSTOMFIELDID] = @CUSTOMFIELDID,
	[DEFAULTAPPLICATIONSTATUSID] = @DEFAULTAPPLICATIONSTATUSID,
	[PREFIX] = @PREFIX,
	[ISAUTONUMBER] = @ISAUTONUMBER,
	[DEFAULTPERMITTYPE] = @DEFAULTPERMITTYPE,
	[DEFAULTPLANTYPE] = @DEFAULTPLANTYPE,
	[CAFEETEMPLATEID] = @CAFEETEMPLATEID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[PLAPPLICATIONTYPEID] = @PLAPPLICATIONTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE