




CREATE PROCEDURE [dbo].[rpt_PL_Plan_Detail_Report_Note]
@PLANID AS VARCHAR(36)
AS
SELECT PLPLANNOTE.PLPLANNOTEID, PLPLANNOTE.TEXT, PLPLANNOTE.CREATEDBY, PLPLANNOTE.CREATEDDATE, USERS.FNAME, USERS.LNAME
FROM PLPLANNOTE 
INNER JOIN USERS ON PLPLANNOTE.CREATEDBY = USERS.SUSERGUID
WHERE PLPLANNOTE.PLPLANID = @PLANID




