

CREATE PROCEDURE [dbo].[RPT_PM_PERMIT_ISSUANCE_AND_VALUATION_BY_DISTRICT]
@STARTDATE AS DATETIME,
@ENDDATE AS DATETIME
AS
SET @ENDDATE = DATEADD(S,-1,DATEADD(D,1,@ENDDATE))

SELECT DISTRICT.NAME AS District, PMPERMITTYPE.NAME AS PermitType, PMPERMITWORKCLASS.NAME AS PermitWorkClass, 
       PMPERMIT.PMPERMITID, PMPERMIT.VALUE, PMPERMIT.ISSUEDATE
FROM PMPERMIT 
INNER JOIN PMPERMITTYPE ON PMPERMIT.PMPERMITTYPEID = PMPERMITTYPE.PMPERMITTYPEID
INNER JOIN DISTRICT ON PMPERMIT.DISTRICTID = DISTRICT.DISTRICTID
INNER JOIN PMPERMITWORKCLASS ON PMPERMIT.PMPERMITWORKCLASSID = PMPERMITWORKCLASS.PMPERMITWORKCLASSID
WHERE PMPERMIT.ISSUEDATE BETWEEN @STARTDATE AND @ENDDATE


