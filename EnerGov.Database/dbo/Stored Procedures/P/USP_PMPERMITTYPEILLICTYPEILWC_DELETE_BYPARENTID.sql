CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEILLICTYPEILWC_DELETE_BYPARENTID]  
(  
@PERMITTYPEID CHAR(36),
@PERMITTYPEWORKCLASSID CHAR(36),
@PMPERMITTYPEILLICENSETYPEID CHAR(36)
)  
AS  
DELETE [dbo].[PMPERMITTYPEILLICTYPEILWC] FROM [dbo].[PMPERMITTYPEILLICTYPEILWC]
INNER JOIN [dbo].[PMPERMITTYPEILLICTYPE] ON [dbo].[PMPERMITTYPEILLICTYPEILWC].[PMPERMITTYPEILLICENSETYPEID] = [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEILLICENSETYPEID]
INNER JOIN [dbo].[PMPERMITTYPEWORKCLASS] ON [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEWORKCLASSID] = [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEWORKCLASSID]
WHERE
	(@PMPERMITTYPEILLICENSETYPEID IS NULL OR [dbo].[PMPERMITTYPEILLICTYPEILWC].[PMPERMITTYPEILLICENSETYPEID] = @PMPERMITTYPEILLICENSETYPEID)
	AND (@PERMITTYPEID IS NULL OR [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEID] = @PERMITTYPEID )
	AND (@PERMITTYPEWORKCLASSID IS NULL OR [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEWORKCLASSID] = @PERMITTYPEWORKCLASSID)