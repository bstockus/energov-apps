CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEILLICTYPEILWCT_DELETE_BYPARENTID]  
(  
@PERMITTYPEID CHAR(36),
@PERMITTYPEWORKCLASSID CHAR(36),
@PMPERMITTYPEILLICENSETYPEID CHAR(36)
)  
AS  
DELETE [dbo].[PMPERMITTYPEILLICTYPEILWCT] FROM [dbo].[PMPERMITTYPEILLICTYPEILWCT]
INNER JOIN [dbo].[PMPERMITTYPEILLICTYPE] ON [dbo].[PMPERMITTYPEILLICTYPEILWCT].[PMPERMITTYPEILLICENSETYPEID] = [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEILLICENSETYPEID]
INNER JOIN [dbo].[PMPERMITTYPEWORKCLASS] ON [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEWORKCLASSID] = [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEWORKCLASSID]
WHERE
	(@PMPERMITTYPEILLICENSETYPEID IS NULL OR [dbo].[PMPERMITTYPEILLICTYPEILWCT].[PMPERMITTYPEILLICENSETYPEID] = @PMPERMITTYPEILLICENSETYPEID)
	AND (@PERMITTYPEID IS NULL OR [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEID] = @PERMITTYPEID)
	AND (@PERMITTYPEWORKCLASSID IS NULL OR [dbo].[PMPERMITTYPEILLICTYPE].[PMPERMITTYPEWORKCLASSID] = @PERMITTYPEWORKCLASSID)