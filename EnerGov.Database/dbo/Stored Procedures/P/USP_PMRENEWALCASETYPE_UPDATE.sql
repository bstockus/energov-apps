CREATE PROCEDURE [dbo].[USP_PMRENEWALCASETYPE_UPDATE]
(
	@PMRENEWALCASETYPEID CHAR(36),
	@NAME NVARCHAR(MAX),
	@PREFIX NVARCHAR(MAX),
	@INVOICEDAYSINADVANCE INT,
	@SUCCESSFULLRENEWALSTATUSID CHAR(36),
	@FAILEDRENEWALSTATUSID CHAR(36),
	@ACTIVE BIT,
	@DESCRIPTION NVARCHAR(MAX),
	@RECURRENCEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[PMRENEWALCASETYPE] SET
	[NAME] = @NAME,
	[PREFIX] = @PREFIX,
	[INVOICEDAYSINADVANCE] = @INVOICEDAYSINADVANCE,
	[SUCCESSFULLRENEWALSTATUSID] = @SUCCESSFULLRENEWALSTATUSID,
	[FAILEDRENEWALSTATUSID] = @FAILEDRENEWALSTATUSID,
	[ACTIVE] = @ACTIVE,
	[DESCRIPTION] = @DESCRIPTION,
	[RECURRENCEID] = @RECURRENCEID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[PMRENEWALCASETYPEID] = @PMRENEWALCASETYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE