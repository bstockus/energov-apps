CREATE PROCEDURE [dbo].[USP_PRPROJECTTYPEPLANORDER_UPDATE]
(
	@PRPROJECTTYPEPLANORDERID CHAR(36),
	@PRPROJECTTYPEID CHAR(36),
	@PLPLANTYPEID CHAR(36),
	@PLPLANWORKCLASSID CHAR(36),
	@PLANORDER INT,
	@AUTOADD BIT
)
AS

UPDATE [dbo].[PRPROJECTTYPEPLANORDER] SET
	[PRPROJECTTYPEID] = @PRPROJECTTYPEID,
	[PLPLANTYPEID] = @PLPLANTYPEID,
	[PLPLANWORKCLASSID] = @PLPLANWORKCLASSID,
	[PLANORDER] = @PLANORDER,
	[AUTOADD] = @AUTOADD

WHERE
	[PRPROJECTTYPEPLANORDERID] = @PRPROJECTTYPEPLANORDERID