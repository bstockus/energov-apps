CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEWORKCLASS_SELECT_LOOKUP]
(
	@ACTIVEONLY BIT = 1
)
AS
BEGIN
SET NOCOUNT ON;

SELECT
	[dbo].[PMPERMITTYPE].[PMPERMITTYPEID],
	[dbo].[PMPERMITTYPE].[NAME]
FROM [dbo].[PMPERMITTYPE]
WHERE [dbo].[PMPERMITTYPE].[ACTIVE] = @ACTIVEONLY OR @ACTIVEONLY = 0
ORDER BY [dbo].[PMPERMITTYPE].[NAME]

EXEC [dbo].[USP_PMPERMITWORKCLASS_SELECT_LOOKUP] @ACTIVEONLY

END