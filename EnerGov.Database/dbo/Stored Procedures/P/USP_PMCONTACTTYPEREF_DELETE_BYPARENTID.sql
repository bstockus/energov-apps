CREATE PROCEDURE [dbo].[USP_PMCONTACTTYPEREF_DELETE_BYPARENTID]  
(  
@PERMITTYPEID CHAR(36),
@PERMITTYPEWORKCLASSID CHAR(36)
)  
AS  
DELETE [dbo].[PMCONTACTTYPEREF] FROM [dbo].[PMCONTACTTYPEREF]
INNER JOIN [dbo].[PMPERMITTYPEWORKCLASS] ON [dbo].[PMCONTACTTYPEREF].[OBJTYPEID] = [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEWORKCLASSID]
WHERE
	[dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEID] = @PERMITTYPEID AND (@PERMITTYPEWORKCLASSID IS NULL OR [dbo].[PMCONTACTTYPEREF].[OBJTYPEID] = @PERMITTYPEWORKCLASSID)