CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEILLICTYPE_UPDATE]
(
	@PMPERMITTYPEILLICENSETYPEID CHAR(36),
	@PMPERMITTYPEID CHAR(36),
	@PMPERMITWORKCLASSID CHAR(36),
	@ILLICENSETYPEID CHAR(36),
	@PMPERMITTYPEWORKCLASSID CHAR(36)
)
AS

UPDATE [dbo].[PMPERMITTYPEILLICTYPE] SET
	[PMPERMITTYPEID] = @PMPERMITTYPEID,
	[PMPERMITWORKCLASSID] = @PMPERMITWORKCLASSID,
	[ILLICENSETYPEID] = @ILLICENSETYPEID,
	[PMPERMITTYPEWORKCLASSID] = @PMPERMITTYPEWORKCLASSID

WHERE
	[PMPERMITTYPEILLICENSETYPEID] = @PMPERMITTYPEILLICENSETYPEID