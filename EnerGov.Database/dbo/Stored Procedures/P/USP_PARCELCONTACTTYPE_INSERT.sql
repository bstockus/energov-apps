CREATE PROCEDURE [dbo].[USP_PARCELCONTACTTYPE_INSERT]
(
	@PARCELCONTACTTYPEID CHAR(36),
	@NAME VARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@PARCELCONTACTSYSTEMTYPEID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PARCELCONTACTTYPE](
	[PARCELCONTACTTYPEID],
	[NAME],
	[DESCRIPTION],
	[PARCELCONTACTSYSTEMTYPEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PARCELCONTACTTYPEID,
	@NAME,
	@DESCRIPTION,
	@PARCELCONTACTSYSTEMTYPEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE