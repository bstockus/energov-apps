(
	@PMRENEWALCASETYPEID,
	@NAME,
	@PREFIX,
	@INVOICEDAYSINADVANCE,
	@SUCCESSFULLRENEWALSTATUSID,
	@FAILEDRENEWALSTATUSID,
	@ACTIVE,
	@DESCRIPTION,
	@RECURRENCEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
