(
	@PMRENEWALCASETYPEID CHAR(36),
	@NAME NVARCHAR(MAX),
	@PREFIX NVARCHAR(MAX),
	@INVOICEDAYSINADVANCE INT,
	@SUCCESSFULLRENEWALSTATUSID CHAR(36),
	@FAILEDRENEWALSTATUSID CHAR(36),
