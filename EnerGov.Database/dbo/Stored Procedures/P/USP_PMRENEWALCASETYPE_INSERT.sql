	@DESCRIPTION NVARCHAR(MAX),
	@RECURRENCEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PMRENEWALCASETYPE](
	[PMRENEWALCASETYPEID],
	[NAME],
	[PREFIX],
	[INVOICEDAYSINADVANCE],
	[SUCCESSFULLRENEWALSTATUSID],
	[FAILEDRENEWALSTATUSID],
	[ACTIVE],
	[DESCRIPTION],
	[RECURRENCEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
