

--[dbo].[RPT_PL_PLAN_PLANNING_ACTIVITY_REPORT] '01/01/2012 00:00:00'

CREATE PROCEDURE [dbo].[rpt_PL_PlanCasesByUser_Expired]
@STARTDATE DATETIME,
@ENDDATE DATETIME

AS

BEGIN

SELECT PLPlan.PLPLANID, PLPlanStatus.NAME, PLPlanType.PLANNAME, Users.ID, PLPlan.[ExpireDATE]

 FROM PLPLAN
 INNER JOIN PLPLANTYPE ON PLPLAN.PLPLANTYPEID = PLPLANTYPE.PLPLANTYPEID
 INNER JOIN PLPLANSTATUS ON PLPLAN.PLPLANSTATUSID = PLPLANSTATUS.PLPLANSTATUSID
 INNER JOIN USERS ON PLPLAN.ASSIGNEDTO = USERS.SUSERGUID


WHERE PLPlan.[EXPIREDATE] BETWEEN @STARTDATE AND @ENDDATE 


END



