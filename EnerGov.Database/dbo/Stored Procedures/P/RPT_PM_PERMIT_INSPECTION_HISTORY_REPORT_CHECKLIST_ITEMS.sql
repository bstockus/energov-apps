



CREATE PROCEDURE [dbo].[RPT_PM_PERMIT_INSPECTION_HISTORY_REPORT_CHECKLIST_ITEMS]
@INSPECTIONID AS VARCHAR(36)
AS
SELECT     IMCHECKLIST.NAME AS [CHECKLIST ITEM], IMINSPECTIONCHECKLISTXREF.PASSED, IMINSPECTIONCHECKLISTXREF.COMMENTS, IMINSPECTIONCHECKLISTXREF.IMCHECKLISTID
FROM         IMCHECKLIST INNER JOIN
                      IMINSPECTIONCHECKLISTXREF ON IMCHECKLIST.IMCHECKLISTID = IMINSPECTIONCHECKLISTXREF.IMCHECKLISTID
WHERE IMINSPECTIONCHECKLISTXREF.IMINSPECTIONID = @INSPECTIONID
ORDER BY IMINSPECTIONCHECKLISTXREF.ORDERNUM



