CREATE PROCEDURE [dbo].[USP_PMPERMITVALUATIONTYPEVALGROUP_INSERT]
(
	@PMPERMITVALTYPEVALGRPID CHAR(36),
	@NAME NVARCHAR(255),
	@DESCRIPTION NVARCHAR(MAX),
	@EFFECTIVEDATE DATETIME,
	@EXPIRATIONDATE DATETIME,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PMPERMITVALUATIONTYPEVALGROUP]
(
	[PMPERMITVALTYPEVALGRPID],
	[NAME],
	[DESCRIPTION],
	[EFFECTIVEDATE],
	[EXPIRATIONDATE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PMPERMITVALTYPEVALGRPID,
	@NAME,
	@DESCRIPTION,
	@EFFECTIVEDATE,
	@EXPIRATIONDATE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE