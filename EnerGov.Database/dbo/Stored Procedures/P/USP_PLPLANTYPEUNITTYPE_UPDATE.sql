CREATE PROCEDURE [dbo].[USP_PLPLANTYPEUNITTYPE_UPDATE]
(
	@PLPLANTYPEUNITTYPEID CHAR(36),
	@PLPLANTYPEID CHAR(36),
	@PLPLANWORKCLASSID CHAR(36),
	@IPUNITTYPEID CHAR(36),
	@ISREQUIRED BIT,
	@UNITTYPEGROUP INT
)
AS

UPDATE [dbo].[PLPLANTYPEUNITTYPE] SET
	[PLPLANTYPEID] = @PLPLANTYPEID,
	[PLPLANWORKCLASSID] = @PLPLANWORKCLASSID,
	[IPUNITTYPEID] = @IPUNITTYPEID,
	[ISREQUIRED] = @ISREQUIRED,
	[UNITTYPEGROUP] = @UNITTYPEGROUP

WHERE
	[PLPLANTYPEUNITTYPEID] = @PLPLANTYPEUNITTYPEID