CREATE PROCEDURE [dbo].[USP_PLITEMREVIEWTYPE_INSERT]
(
	@PLITEMREVIEWTYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@DEPARTMENTID CHAR(36),
	@PLITEMREVIEWSTATUSID CHAR(36),
	@DAYSUNTILDUE INT,
	@RESUMITALDAYSUNTILDUE INT,
	@INHERITSUBMITALDUE BIT,
	@GISZONEREVIEWITEMMAPPINGID CHAR(36),
	@CODENAME NVARCHAR(100),
	@CODEURL NVARCHAR(250),
	@REASSIGNTOWHOCOMPLETEDREVIEW BIT,
	@HIDERECOMMENDATIONS BIT,
	@HIDECORRECTIONS BIT,
	@HIDECONDITIONS BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@ASSIGNTOOBJECT INT,
	@TEAMASSIGNEDTOID CHAR(36)
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PLITEMREVIEWTYPE](
	[PLITEMREVIEWTYPEID],
	[NAME],
	[DESCRIPTION],
	[DEPARTMENTID],
	[PLITEMREVIEWSTATUSID],
	[DAYSUNTILDUE],
	[RESUMITALDAYSUNTILDUE],
	[INHERITSUBMITALDUE],
	[GISZONEREVIEWITEMMAPPINGID],
	[CODENAME],
	[CODEURL],
	[REASSIGNTOWHOCOMPLETEDREVIEW],
	[HIDERECOMMENDATIONS],
	[HIDECORRECTIONS],
	[HIDECONDITIONS],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION],
	[ASSIGNTOOBJECT],
	[TEAMASSIGNEDTOID]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PLITEMREVIEWTYPEID,
	@NAME,
	@DESCRIPTION,
	@DEPARTMENTID,
	@PLITEMREVIEWSTATUSID,
	@DAYSUNTILDUE,
	@RESUMITALDAYSUNTILDUE,
	@INHERITSUBMITALDUE,
	@GISZONEREVIEWITEMMAPPINGID,
	@CODENAME,
	@CODEURL,
	@REASSIGNTOWHOCOMPLETEDREVIEW,
	@HIDERECOMMENDATIONS,
	@HIDECORRECTIONS,
	@HIDECONDITIONS,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION,
	@ASSIGNTOOBJECT,
	@TEAMASSIGNEDTOID
)
SELECT * FROM @OUTPUTTABLE