CREATE PROCEDURE [dbo].[USP_PLPLANSTATUS_INSERT]
(
	@PLPLANSTATUSID CHAR(36),
	@NAME NVARCHAR(100),
	@SUCCESSFLAG BIT,
	@HOLDFLAG BIT,
	@FAILUREFLAG BIT,
	@CANCELLEDFLAG BIT,
	@DESCRIPTION NVARCHAR(MAX),
	@DESCRIPTION_SPANISH NVARCHAR(MAX),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PLPLANSTATUS](
	[PLPLANSTATUSID],
	[NAME],
	[SUCCESSFLAG],
	[HOLDFLAG],
	[FAILUREFLAG],
	[CANCELLEDFLAG],
	[DESCRIPTION],
	[DESCRIPTION_SPANISH],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PLPLANSTATUSID,
	@NAME,
	@SUCCESSFLAG,
	@HOLDFLAG,
	@FAILUREFLAG,
	@CANCELLEDFLAG,
	@DESCRIPTION,
	@DESCRIPTION_SPANISH,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE