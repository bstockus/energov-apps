CREATE PROCEDURE [dbo].[USP_PRPROJECTTYPEPERMITLIMIT_UPDATE]
(
	@PRPROJECTTYPEPERMITLIMITID CHAR(36),
	@PRPROJECTTYPEID CHAR(36),
	@PMPERMITTYPEID CHAR(36),
	@PMPERMITWORKCLASSID CHAR(36),
	@PERMITLIMIT INT
)
AS

UPDATE [dbo].[PRPROJECTTYPEPERMITLIMIT] SET
	[PRPROJECTTYPEID] = @PRPROJECTTYPEID,
	[PMPERMITTYPEID] = @PMPERMITTYPEID,
	[PMPERMITWORKCLASSID] = @PMPERMITWORKCLASSID,
	[PERMITLIMIT] = @PERMITLIMIT

WHERE
	[PRPROJECTTYPEPERMITLIMITID] = @PRPROJECTTYPEPERMITLIMITID