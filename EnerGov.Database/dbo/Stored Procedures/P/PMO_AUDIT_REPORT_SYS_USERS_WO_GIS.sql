



CREATE PROCEDURE [dbo].[PMO_AUDIT_REPORT_SYS_USERS_WO_GIS]
AS

SELECT     USERS.ID AS LOGIN, USERS.FNAME, USERS.LNAME, ROLES.ID AS ROLE, USERS.GMAPID
FROM         USERS INNER JOIN
                      ROLES ON USERS.SROLEID = ROLES.SROLEGUID
WHERE (USERS.GMAPID IS NULL or USERS.GMAPID='') AND USERS.ID not in ('ANONYMOUS','EnerGovService','FSide')

