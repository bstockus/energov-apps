CREATE PROCEDURE [dbo].[USP_PLPLANCORRECTIONTYPE_UPDATE]
(
	@PLPLANCORRECTIONTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@CORRECTIVEACTION NVARCHAR(MAX),
	@PLCORRECTIONTYPECATEGORYID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[PLPLANCORRECTIONTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[CORRECTIVEACTION] = @CORRECTIVEACTION,
	[PLCORRECTIONTYPECATEGORYID] = @PLCORRECTIONTYPECATEGORYID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[PLPLANCORRECTIONTYPEID] = @PLPLANCORRECTIONTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE