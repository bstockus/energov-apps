


CREATE PROCEDURE [dbo].[PMO_AUDIT_REPORT_SYS_IOS_COMPFEES_NOT_USING_DEFAULT]
AS
SELECT     WORKFLOW.WORKFLOWNAME AS IO_NAME, WORKFLOW.ISENABLED, WORKFLOWACTION.ACTIONNAME AS IO_ACTION_NAME, WORKFLOWACTIONTYPE.WORKFLOWACTIONTYPENAME, 
                      CAFEETEMPLATE.CAFEETEMPLATENAME
FROM         WORKFLOW INNER JOIN
                      WORKFLOWACTION ON WORKFLOW.WORKFLOWID = WORKFLOWACTION.WORKFLOWID INNER JOIN
                      WORKFLOWACTIONTYPE ON WORKFLOWACTION.WORKFLOWACTIONTYPEID = WORKFLOWACTIONTYPE.WORKFLOWACTIONTYPEID INNER JOIN
                      WORKFLOWACTIONPARAMETER ON WORKFLOWACTION.WORKFLOWACTIONID = WORKFLOWACTIONPARAMETER.WORKFLOWACTIONID INNER JOIN
                      CAFEETEMPLATE ON WORKFLOWACTIONPARAMETER.PARAMETERVALUE = CAFEETEMPLATE.CAFEETEMPLATEID
WHERE     (WORKFLOWACTIONTYPE.WORKFLOWACTIONTYPENAME = 'Compute Fees') AND (WORKFLOW.ISENABLED = 1)

