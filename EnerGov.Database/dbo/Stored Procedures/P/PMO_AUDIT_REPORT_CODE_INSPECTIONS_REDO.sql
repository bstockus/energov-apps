

CREATE PROCEDURE [dbo].[PMO_AUDIT_REPORT_CODE_INSPECTIONS_REDO]
AS


SELECT     CMCASETYPE.NAME AS CODE_CASE_TYPE, WFTEMPLATE.NAME AS WF_TEMPLATE, WFACTION.NAME AS WF_ACTION_INSP_NAME, WFACTION.ALLOWREDO
FROM         WFTEMPLATESTEP 
INNER JOIN WFTEMPLATE ON WFTEMPLATESTEP.WFTEMPLATEID = WFTEMPLATE.WFTEMPLATEID 
INNER JOIN WFTEMPLATESTEPACTION ON WFTEMPLATESTEP.WFTEMPLATESTEPID = WFTEMPLATESTEPACTION.WFTEMPLATESTEPID 
INNER JOIN WFACTION ON WFTEMPLATESTEPACTION.WFACTIONID = WFACTION.WFACTIONID
INNER JOIN IMINSPECTIONTYPE ON WFACTION.IMINSPECTIONTYPEID = IMINSPECTIONTYPE.IMINSPECTIONTYPEID 
INNER JOIN CMCASETYPE ON WFTEMPLATE.WFTEMPLATEID = CMCASETYPE.WFTEMPLATEID
WHERE WFACTION.ALLOWREDO='TRUE'

