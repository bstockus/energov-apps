

CREATE PROCEDURE [dbo].[rpt_PL_SR_Plan_Detail_Report_Object]
@PLPLANID VARCHAR(36)
AS
SELECT	PLPLANOBJECT.PLPLANID,
		OMOBJECT.OMOBJECTID, OMOBJECT.OBJECTNUMBER, OMOBJECT.INSTALLDATE, OMOBJECT.OPERATIONSTARTDATE, OMOBJECT.OPERATIONENDDATE,
		OMOBJECTTYPE.NAME OBJECTTYPE, OMOBJECTCLASSIFICATION.NAME OBJECTCLASS, OMOBJECTSTATUS.NAME OBJECTSTATUS

FROM	PLPLANOBJECT 
INNER JOIN OMOBJECT ON PLPLANOBJECT.OMOBJECTID = OMOBJECT.OMOBJECTID
INNER JOIN OMOBJECTTYPE ON OMOBJECT.OMOBJECTTYPEID = OMOBJECTTYPE.OMOBJECTTYPEID
INNER JOIN OMOBJECTCLASSIFICATION ON OMOBJECT.OMOBJECTCLASSIFICATIONID = OMOBJECTCLASSIFICATION.OMOBJECTCLASSIFICATIONID
INNER JOIN OMOBJECTSTATUS ON OMOBJECT.OMOBJECTSTATUSID = OMOBJECTSTATUS.OMOBJECTSTATUSID
WHERE	PLPLANOBJECT.PLPLANID = @PLPLANID

