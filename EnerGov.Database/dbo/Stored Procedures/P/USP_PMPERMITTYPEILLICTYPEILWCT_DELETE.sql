CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEILLICTYPEILWCT_DELETE]
(
@PMPERMITTYPEILLICENSETYPEID CHAR(36),
@ILLICENSECLASSIFICATIONTYPEID CHAR(36)
)
AS
DELETE FROM [dbo].[PMPERMITTYPEILLICTYPEILWCT]
WHERE
	[PMPERMITTYPEILLICENSETYPEID] = @PMPERMITTYPEILLICENSETYPEID AND 
	[ILLICENSECLASSIFICATIONTYPEID] = @ILLICENSECLASSIFICATIONTYPEID