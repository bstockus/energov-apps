CREATE PROCEDURE [dbo].[USP_PLITEMREVIEWTYPE_UPDATE]
(
	@PLITEMREVIEWTYPEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@DEPARTMENTID CHAR(36),
	@PLITEMREVIEWSTATUSID CHAR(36),
	@DAYSUNTILDUE INT,
	@RESUMITALDAYSUNTILDUE INT,
	@INHERITSUBMITALDUE BIT,
	@GISZONEREVIEWITEMMAPPINGID CHAR(36),
	@CODENAME NVARCHAR(100),
	@CODEURL NVARCHAR(250),
	@REASSIGNTOWHOCOMPLETEDREVIEW BIT,
	@HIDERECOMMENDATIONS BIT,
	@HIDECORRECTIONS BIT,
	@HIDECONDITIONS BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@ASSIGNTOOBJECT INT,
	@TEAMASSIGNEDTOID CHAR(36)
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[PLITEMREVIEWTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[DEPARTMENTID] = @DEPARTMENTID,
	[PLITEMREVIEWSTATUSID] = @PLITEMREVIEWSTATUSID,
	[DAYSUNTILDUE] = @DAYSUNTILDUE,
	[RESUMITALDAYSUNTILDUE] = @RESUMITALDAYSUNTILDUE,
	[INHERITSUBMITALDUE] = @INHERITSUBMITALDUE,
	[GISZONEREVIEWITEMMAPPINGID] = @GISZONEREVIEWITEMMAPPINGID,
	[CODENAME] = @CODENAME,
	[CODEURL] = @CODEURL,
	[REASSIGNTOWHOCOMPLETEDREVIEW] = @REASSIGNTOWHOCOMPLETEDREVIEW,
	[HIDERECOMMENDATIONS] = @HIDERECOMMENDATIONS,
	[HIDECORRECTIONS] = @HIDECORRECTIONS,
	[HIDECONDITIONS] = @HIDECONDITIONS,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1,
	[ASSIGNTOOBJECT] = @ASSIGNTOOBJECT,
	[TEAMASSIGNEDTOID] = @TEAMASSIGNEDTOID
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[PLITEMREVIEWTYPEID] = @PLITEMREVIEWTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE