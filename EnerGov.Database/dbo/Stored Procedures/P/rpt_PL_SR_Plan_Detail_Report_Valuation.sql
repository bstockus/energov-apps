

CREATE PROCEDURE [dbo].[rpt_PL_SR_Plan_Detail_Report_Valuation]
@PLPLANID AS VARCHAR(36)
AS
SELECT PMPERMITVALUATIONTYPE.NAME AS ValuationType, PMPERMITVALUATIONGROUP.NAME AS ValuationGroup, PLPLANVALUATION.SQUAREFEET, PLPLANVALUATION.VALUE
FROM PMPERMITVALUATIONTYPEVALGROUPX 
INNER JOIN PMPERMITVALUATIONTYPE ON PMPERMITVALUATIONTYPEVALGROUPX.PMPERMITVALUATIONTYPEID = PMPERMITVALUATIONTYPE.PMPERMITVALUATIONTYPEID 
INNER JOIN PMPERMITVALUATIONGROUP ON PMPERMITVALUATIONTYPEVALGROUPX.PMPERMITVALUATIONGROUPID = PMPERMITVALUATIONGROUP.PMPERMITVALUATIONGROUPID 
INNER JOIN PMPERMITVALUATIONTYPEVALGROUP ON PMPERMITVALUATIONTYPEVALGROUPX.PMPERMITVALTYPEVALGRPID = PMPERMITVALUATIONTYPEVALGROUP.PMPERMITVALTYPEVALGRPID 
INNER JOIN PLPLANVALUATION ON PMPERMITVALUATIONTYPEVALGROUPX.PMPERMITVALUATIONTYPEVALGRPXID = PLPLANVALUATION.PMPERMITVALTYPEVALGROUPXID
WHERE PLPLANVALUATION.PLPLANID = @PLPLANID

