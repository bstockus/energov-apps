



CREATE PROCEDURE [dbo].[PMO_AUDIT_REPORT_SYS_CF_COMBOBOXES_WITHOUT_ITEMS]
AS

SELECT     CUSTOMFIELDOBJECT.SLABEL AS CF_NAME, CUSTOMFIELDLAYOUT.SNAME AS CF_LAYOUT
FROM         CUSTOMFIELD INNER JOIN
                      CUSTOMFIELDPICKLIST ON CUSTOMFIELD.GCUSTOMFIELD = CUSTOMFIELDPICKLIST.FKGCUSTOMFIELD INNER JOIN
                      CUSTOMFIELDOBJECT ON CUSTOMFIELD.GCUSTOMFIELD = CUSTOMFIELDOBJECT.FKGCUSTOMFIELD INNER JOIN
                      CUSTOMFIELDLAYOUT ON CUSTOMFIELDOBJECT.FKGCUSTOMFIELDLAYOUT = CUSTOMFIELDLAYOUT.GCUSTOMFIELDLAYOUTS
WHERE     (CUSTOMFIELDPICKLIST.GCUSTOMFIELDPICKLIST NOT IN
                          (SELECT     FKGCUSTOMFIELDPICKLIST
                            FROM          CUSTOMFIELDPICKLISTITEM))

