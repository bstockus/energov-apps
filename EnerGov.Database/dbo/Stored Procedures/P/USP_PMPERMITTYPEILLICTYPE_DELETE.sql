CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEILLICTYPE_DELETE]
(
@PMPERMITTYPEILLICENSETYPEID CHAR(36)
)
AS

-- Delete Professional license classification for (permittype,permittypeworkclass)
EXEC [dbo].[USP_PMPERMITTYPEILLICTYPEILWC_DELETE_BYPARENTID] null, null, @PMPERMITTYPEILLICENSETYPEID

-- Delete Professional license classification type for (permittype,permittypeworkclass)
EXEC [dbo].[USP_PMPERMITTYPEILLICTYPEILWCT_DELETE_BYPARENTID] null, null, @PMPERMITTYPEILLICENSETYPEID

DELETE FROM [dbo].[PMPERMITTYPEILLICTYPE]
WHERE
	[PMPERMITTYPEILLICENSETYPEID] = @PMPERMITTYPEILLICENSETYPEID