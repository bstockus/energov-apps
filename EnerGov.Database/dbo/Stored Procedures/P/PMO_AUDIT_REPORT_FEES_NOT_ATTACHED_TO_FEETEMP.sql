

CREATE PROCEDURE [dbo].[PMO_AUDIT_REPORT_FEES_NOT_ATTACHED_TO_FEETEMP]
AS

SELECT CAFEE.NAME AS FEE_NAME, CAMODULE.NAME AS FEE_MODULE
FROM CAMODULE 
INNER JOIN CAMODULEFEEXREF ON CAMODULE.CAMODULEID = CAMODULEFEEXREF.CAMODULEID 
RIGHT OUTER JOIN CAFEE ON CAMODULEFEEXREF.CAFEEID = CAFEE.CAFEEID
WHERE (CAMODULE.NAME IS NULL OR CAMODULE.NAME <> 'CASHIER')  
AND (CAFEE.CAFEEID NOT IN (SELECT CAFEEID FROM  CAFEETEMPLATEFEE))

