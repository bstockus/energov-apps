



CREATE PROCEDURE [dbo].[PMO_AUDIT_REPORT_PL_NOISSUED_STATUS]
AS

SELECT     ILLICENSESTATUS.NAME AS LIC_STATUS, ILLICENSESTATUS.DESCRIPTION AS LIC_STATUS_DESC, ILLICENSESTATUSSYSTEM.NAME AS LIC_SYSTEM_STATUS
FROM         ILLICENSESTATUS INNER JOIN
                      ILLICENSESTATUSSYSTEM ON ILLICENSESTATUS.ILLICENSESTATUSSYSTEMID = ILLICENSESTATUSSYSTEM.ILLICENSESTATUSSYSTEMID
WHERE ILLICENSESTATUSSYSTEM.NAME='Issued'

