CREATE PROCEDURE [dbo].[USP_PRPROJECTTYPE_INSERT]
(
	@PRPROJECTTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@CUSTOMFIELDID CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@ACTIVE BIT,
	@USECASETYPENUMBERING BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[PRPROJECTTYPE](
	[PRPROJECTTYPEID],
	[NAME],
	[DESCRIPTION],
	[CUSTOMFIELDID],
	[CAFEETEMPLATEID],
	[ACTIVE],
	[USECASETYPENUMBERING],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@PRPROJECTTYPEID,
	@NAME,
	@DESCRIPTION,
	@CUSTOMFIELDID,
	@CAFEETEMPLATEID,
	@ACTIVE,
	@USECASETYPENUMBERING,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE