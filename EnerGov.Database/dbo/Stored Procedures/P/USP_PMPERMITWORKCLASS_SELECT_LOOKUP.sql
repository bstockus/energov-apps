CREATE PROCEDURE [dbo].[USP_PMPERMITWORKCLASS_SELECT_LOOKUP]
(
	@ACTIVEONLY BIT = 1
)
AS
SET NOCOUNT ON;

SELECT 
	[dbo].[PMPERMITWORKCLASS].[PMPERMITWORKCLASSID],
	[dbo].[PMPERMITWORKCLASS].[NAME],
	[dbo].[PMPERMITTYPE].[PMPERMITTYPEID]
FROM [dbo].[PMPERMITWORKCLASS]
JOIN [dbo].[PMPERMITTYPEWORKCLASS] ON [dbo].[PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITWORKCLASSID]
JOIN [dbo].[PMPERMITTYPE] ON [dbo].[PMPERMITTYPEWORKCLASS].[PMPERMITTYPEID] = [dbo].[PMPERMITTYPE].[PMPERMITTYPEID]
WHERE [dbo].[PMPERMITTYPE].[ACTIVE] = @ACTIVEONLY OR @ACTIVEONLY = 0
ORDER BY [dbo].[PMPERMITWORKCLASS].[NAME]