CREATE PROCEDURE [dbo].[USP_PMPERMITTYPEILLICTYPEILWC_DELETE]
(
@PMPERMITTYPEILLICENSETYPEID CHAR(36),
@ILLICENSECLASSIFICATIONID CHAR(36)
)
AS
DELETE FROM [dbo].[PMPERMITTYPEILLICTYPEILWC]
WHERE
	[PMPERMITTYPEILLICENSETYPEID] = @PMPERMITTYPEILLICENSETYPEID AND 
	[ILLICENSECLASSIFICATIONID] = @ILLICENSECLASSIFICATIONID