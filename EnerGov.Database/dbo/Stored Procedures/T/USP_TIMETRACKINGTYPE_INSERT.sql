CREATE PROCEDURE [dbo].[USP_TIMETRACKINGTYPE_INSERT]
(
	@TIMETRACKINGTYPEID CHAR(36),
	@NAME NVARCHAR(150),
	@DESCRIPTION NVARCHAR(MAX),
	@BILLABLE BIT,
	@MODULEID INT,
	@ACTIVE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[TIMETRACKINGTYPE](
	[TIMETRACKINGTYPEID],
	[NAME],
	[DESCRIPTION],
	[BILLABLE],
	[MODULEID],
	[ACTIVE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@TIMETRACKINGTYPEID,
	@NAME,
	@DESCRIPTION,
	@BILLABLE,
	@MODULEID,
	@ACTIVE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE