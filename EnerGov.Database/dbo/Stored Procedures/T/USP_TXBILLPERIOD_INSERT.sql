	@YEAR,
	@PERIODNAME,
	@STARTDATE,
	@ENDDATE,
	@DUEDATE,
	@DEFAULTGENERATEDDATE,
	@RPTPERIODREF,
	@LATEDATE,
	@COMPOUNDINGINTERESTSTARTDATE,
