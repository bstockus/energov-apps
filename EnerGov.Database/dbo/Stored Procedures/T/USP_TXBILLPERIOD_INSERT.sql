	@COMPOUNDINGINTERESTSTARTDATE DATETIME,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[TXBILLPERIOD](
	[BILLPERIODID],
	[YEAR],
	[PERIODNAME],
	[STARTDATE],
	[ENDDATE],
	[DUEDATE],
	[DEFAULTGENERATEDDATE],
	[RPTPERIODREF],
