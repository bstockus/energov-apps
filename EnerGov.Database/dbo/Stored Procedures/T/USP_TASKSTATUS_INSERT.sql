CREATE PROCEDURE [dbo].[USP_TASKSTATUS_INSERT]
(
	@TASKSTATUSID INT,
	@TASKSTATUSNAME NVARCHAR(30),
	@ISCOMPLETED BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[TASKSTATUS](
	[TASKSTATUSID],
	[TASKSTATUSNAME],
	[ISCOMPLETED],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@TASKSTATUSID,
	@TASKSTATUSNAME,
	@ISCOMPLETED,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE