CREATE PROCEDURE [dbo].[USP_TXRMTCONTACTTYPEREF_UPDATE]
(
	@CONTACTTYPEEXTID CHAR(36),
	@TXREMITTANCETYPEID CHAR(36),
	@BLCONTACTTYPEID CHAR(36),
	@CONTACTTYPEGROUPID INT,
	@ISREQUIRED BIT
)
AS

UPDATE [dbo].[TXRMTCONTACTTYPEREF] SET
	[TXREMITTANCETYPEID] = @TXREMITTANCETYPEID,
	[BLCONTACTTYPEID] = @BLCONTACTTYPEID,
	[CONTACTTYPEGROUPID] = @CONTACTTYPEGROUPID,
	[ISREQUIRED] = @ISREQUIRED

WHERE
	[CONTACTTYPEEXTID] = @CONTACTTYPEEXTID