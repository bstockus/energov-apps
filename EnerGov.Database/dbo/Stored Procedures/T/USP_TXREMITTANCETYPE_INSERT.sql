CREATE PROCEDURE [dbo].[USP_TXREMITTANCETYPE_INSERT]
(
	@TXREMITTANCETYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(250),
	@ACTIVE BIT,
	@DEFAULTSTATUSID CHAR(36),
	@PREFIX NVARCHAR(20),
	@MULTIPLIERRATE DECIMAL(18,0),
	@CAFEETEMPLATEID CHAR(36),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@ONLINECUSTOMFIELDLAYOUTID CHAR(36),
	@INTERNETFLAG BIT,
	@INTERNETAPPLYTYPE INT,
	@DEFAULTGENERATEDATE DATETIME,
	@FRIENDLYNAME NVARCHAR(50),
	@ENABLEEXEMPTIONS BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[TXREMITTANCETYPE](
	[TXREMITTANCETYPEID],
	[NAME],
	[DESCRIPTION],
	[ACTIVE],
	[DEFAULTSTATUSID],
	[PREFIX],
	[MULTIPLIERRATE],
	[CAFEETEMPLATEID],
	[CUSTOMFIELDLAYOUTID],
	[ONLINECUSTOMFIELDLAYOUTID],
	[INTERNETFLAG],
	[INTERNETAPPLYTYPE],
	[DEFAULTGENERATEDATE],
	[FRIENDLYNAME],
	[ENABLEEXEMPTIONS],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@TXREMITTANCETYPEID,
	@NAME,
	@DESCRIPTION,
	@ACTIVE,
	@DEFAULTSTATUSID,
	@PREFIX,
	@MULTIPLIERRATE,
	@CAFEETEMPLATEID,
	@CUSTOMFIELDLAYOUTID,
	@ONLINECUSTOMFIELDLAYOUTID,
	@INTERNETFLAG,
	@INTERNETAPPLYTYPE,
	@DEFAULTGENERATEDATE,
	@FRIENDLYNAME,
	@ENABLEEXEMPTIONS,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE