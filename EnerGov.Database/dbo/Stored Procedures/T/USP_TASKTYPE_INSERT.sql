CREATE PROCEDURE [dbo].[USP_TASKTYPE_INSERT]
(
	@TASKTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@ACTIVE BIT,
	@ISDEFAULT BIT,
	@ISGLOBAL BIT,
	@DAYSUNTILDUE INT,
	@TASKTEXT NVARCHAR(MAX),
	@DEFAULTSUBJECT NVARCHAR(200),
	@TASKSTATUSID INT,
	@TASKPRIORITYID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[TASKTYPE](
	[TASKTYPEID],
	[NAME],
	[DESCRIPTION],
	[ACTIVE],
	[ISDEFAULT],
	[ISGLOBAL],
	[DAYSUNTILDUE],
	[TASKTEXT],
	[DEFAULTSUBJECT],
	[TASKSTATUSID],
	[TASKPRIORITYID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@TASKTYPEID,
	@NAME,
	@DESCRIPTION,
	@ACTIVE,
	@ISDEFAULT,
	@ISGLOBAL,
	@DAYSUNTILDUE,
	@TASKTEXT,
	@DEFAULTSUBJECT,
	@TASKSTATUSID,
	@TASKPRIORITYID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE