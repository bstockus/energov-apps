CREATE PROCEDURE [dbo].[USP_OFFICEHOURS_INSERT]
(
	@OFFICEHOURSID CHAR(36),
	@WORKDAYID INT,
	@OFFICEID CHAR(36),
	@STARTTIME DATETIME,
	@ENDTIME DATETIME,
	@ISWORKINGDAY BIT
)
AS

INSERT INTO [dbo].[OFFICEHOURS](
	[OFFICEHOURSID],
	[WORKDAYID],
	[OFFICEID],
	[STARTTIME],
	[ENDTIME],
	[ISWORKINGDAY]
)

VALUES
(
	@OFFICEHOURSID,
	@WORKDAYID,
	@OFFICEID,
	@STARTTIME,
	@ENDTIME,
	@ISWORKINGDAY
)