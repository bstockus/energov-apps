CREATE PROCEDURE [dbo].[USP_OMOBJECTTYPE_DELETE]
(
	@OMOBJECTTYPEID CHAR(36),
	@ROWVERSION INT
)
AS
BEGIN
SET NOCOUNT ON

EXEC [USP_OMOBJECTTYPECLASS_DELETE_BYPARENTID] @OMOBJECTTYPEID

SET NOCOUNT OFF

DELETE FROM [dbo].[OMOBJECTTYPE]
WHERE
	[OMOBJECTTYPEID] = @OMOBJECTTYPEID AND 
	[ROWVERSION]= @ROWVERSION
END