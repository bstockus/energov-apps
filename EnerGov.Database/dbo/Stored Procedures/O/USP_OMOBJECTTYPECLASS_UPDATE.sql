CREATE PROCEDURE [dbo].[USP_OMOBJECTTYPECLASS_UPDATE]
(
	@OMOBJECTTYPECLASSID CHAR(36),
	@OMOBJECTTYPEID CHAR(36),
	@OMOBJECTCLASSIFICATIONID CHAR(36),
	@CUSTOMFIELDLAYOUTID CHAR(36)
)
AS

UPDATE [dbo].[OMOBJECTTYPECLASS] SET
	[OMOBJECTTYPEID] = @OMOBJECTTYPEID,
	[OMOBJECTCLASSIFICATIONID] = @OMOBJECTCLASSIFICATIONID,
	[CUSTOMFIELDLAYOUTID] = @CUSTOMFIELDLAYOUTID

WHERE
	[OMOBJECTTYPECLASSID] = @OMOBJECTTYPECLASSID