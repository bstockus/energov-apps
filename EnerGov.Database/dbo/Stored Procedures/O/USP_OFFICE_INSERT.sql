CREATE PROCEDURE [dbo].[USP_OFFICE_INSERT]
(
	@OFFICEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX),
	@DISTRICTID CHAR(36),
	@PHONE NVARCHAR(50),
	@FAX NVARCHAR(50),
	@TYLERFINANCIALOFFICENAME NVARCHAR(100),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@JURISDICTIONID CHAR(36)
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[OFFICE](
	[OFFICEID],
	[NAME],
	[DESCRIPTION],
	[DISTRICTID],
	[PHONE],
	[FAX],
	[TYLERFINANCIALOFFICENAME],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION],
	[JURISDICTIONID]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@OFFICEID,
	@NAME,
	@DESCRIPTION,
	@DISTRICTID,
	@PHONE,
	@FAX,
	@TYLERFINANCIALOFFICENAME,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION,
	@JURISDICTIONID
)
SELECT * FROM @OUTPUTTABLE