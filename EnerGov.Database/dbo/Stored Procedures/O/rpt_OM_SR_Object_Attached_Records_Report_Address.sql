

/****** OBJECT:  StoredProcedure [dbo].[rpt_OM_Object_Attached_Records_Report_Address]    Script Date: 02/25/2011 15:58:53 ******/
CREATE PROCEDURE rpt_OM_SR_Object_Attached_Records_Report_Address
@OMOBJECTID AS VARCHAR(36)
AS
SELECT	MAILINGADDRESS.ADDRESSLINE1, MAILINGADDRESS.ADDRESSLINE2, MAILINGADDRESS.ADDRESSLINE3, MAILINGADDRESS.CITY, MAILINGADDRESS.STATE, 
		MAILINGADDRESS.POSTALCODE, MAILINGADDRESS.POSTDIRECTION, MAILINGADDRESS.PREDIRECTION, MAILINGADDRESS.STREETTYPE, 
		MAILINGADDRESS.PARCELNUMBER, MAILINGADDRESS.UNITORSUITE, 
		OMOBJECTADDRESS.MAILINGADDRESSID, OMOBJECTADDRESS.MAIN

FROM	OMOBJECTADDRESS 
		INNER JOIN MAILINGADDRESS ON OMOBJECTADDRESS.MAILINGADDRESSID = MAILINGADDRESS.MAILINGADDRESSID
WHERE	OMOBJECTADDRESS.OMOBJECTID = @OMOBJECTID

