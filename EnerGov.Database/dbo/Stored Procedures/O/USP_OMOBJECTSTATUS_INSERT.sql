CREATE PROCEDURE [dbo].[USP_OMOBJECTSTATUS_INSERT]
(
	@OMOBJECTSTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@OMOBJECTSTATUSSYSTEMID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[OMOBJECTSTATUS](
	[OMOBJECTSTATUSID],
	[NAME],
	[DESCRIPTION],
	[OMOBJECTSTATUSSYSTEMID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@OMOBJECTSTATUSID,
	@NAME,
	@DESCRIPTION,
	@OMOBJECTSTATUSSYSTEMID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE