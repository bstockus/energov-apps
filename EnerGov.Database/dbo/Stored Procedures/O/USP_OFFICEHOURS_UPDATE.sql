CREATE PROCEDURE [dbo].[USP_OFFICEHOURS_UPDATE]
(
	@OFFICEHOURSID CHAR(36),
	@WORKDAYID INT,
	@OFFICEID CHAR(36),
	@STARTTIME DATETIME,
	@ENDTIME DATETIME,
	@ISWORKINGDAY BIT
)
AS

UPDATE [dbo].[OFFICEHOURS] SET
	[WORKDAYID] = @WORKDAYID,
	[OFFICEID] = @OFFICEID,
	[STARTTIME] = @STARTTIME,
	[ENDTIME] = @ENDTIME,
	[ISWORKINGDAY] = @ISWORKINGDAY

WHERE
	[OFFICEHOURSID] = @OFFICEHOURSID