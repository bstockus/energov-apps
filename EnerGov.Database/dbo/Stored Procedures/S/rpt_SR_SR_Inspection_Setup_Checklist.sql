
CREATE PROCEDURE [dbo].[rpt_SR_SR_Inspection_Setup_Checklist]
@IMINSPECTIONTYPEID AS VARCHAR(36)
AS
SELECT IMINSPECTIONTYPECHKLSTXREF.SORTORDER AS ChecklistSortOrder, IMINSPECTIONTYPECHKLSTXREF.AUTOADD AS ChecklistAutoAdd, 
       IMCHECKLIST.NAME AS ChecklistItem, IMCHECKLIST.DESCRIPTION AS ChecklistDescription, IMCHECKLISTCATEGORY.NAME AS ChecklistCategory
FROM IMINSPECTIONTYPECHKLSTXREF 
INNER JOIN IMCHECKLIST ON IMINSPECTIONTYPECHKLSTXREF.IMCHECKLISTID = IMCHECKLIST.IMCHECKLISTID 
INNER JOIN IMCHECKLISTCATEGORY ON IMCHECKLIST.IMCHECKLISTCATEGORYID = IMCHECKLISTCATEGORY.IMCHECKLISTCATEGORYID
WHERE IMINSPECTIONTYPECHKLSTXREF.IMINSPECTIONTYPEID = @IMINSPECTIONTYPEID

