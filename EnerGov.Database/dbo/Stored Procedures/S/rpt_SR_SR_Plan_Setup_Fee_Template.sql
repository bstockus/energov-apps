

CREATE PROCEDURE [dbo].[rpt_SR_SR_Plan_Setup_Fee_Template]
@PLPLANTYPEID AS VARCHAR(36)
AS
SELECT CAFEETEMPLATE.CAFEETEMPLATENAME AS FeeTemplate, CAFEETEMPLATEFEE.FEEORDER AS FeeOrder, CAFEETEMPLATEFEE.FEEPRIORITY AS FeePriority, 
       CAFEETEMPLATEFEE.FEENAME AS FeeName, CAFEETEMPLATEFEE.ISMANUAL AS ManuallyAdded, CAFEETEMPLATEFEE.ISHIDDEN AS IsHidden, 
       CAFEETEMPLATEFEE.ISONLINE AS IsOnline
FROM CAFEETEMPLATE 
INNER JOIN CAFEETEMPLATEFEE ON CAFEETEMPLATE.CAFEETEMPLATEID = CAFEETEMPLATEFEE.CAFEETEMPLATEID 
INNER JOIN PLPLANTYPEWORKCLASS ON CAFEETEMPLATE.CAFEETEMPLATEID = PLPLANTYPEWORKCLASS.CAFEETEMPLATEID
WHERE PLPLANTYPEWORKCLASS.PLPLANTYPEID = @PLPLANTYPEID

