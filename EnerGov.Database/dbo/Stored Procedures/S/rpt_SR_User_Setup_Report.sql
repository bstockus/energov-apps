
CREATE PROCEDURE [dbo].[rpt_SR_User_Setup_Report]
AS
SELECT     USERS.ID AS UserID, USERS.FNAME AS FirstName, USERS.LNAME AS LastName, USERS.PHONE AS Phone, USERS.EMAIL AS Email, USERS.COMPANY AS Company,
                       USERS.MIDDLENAME AS MiddleName, USERS.TITLE AS Title, USERS.CREDENTIALS AS Credentials, USERS.SUSERGUID, ROLES.SROLEGUID, ROLES.ID AS RoleID
FROM         USERS INNER JOIN
                      ROLES ON USERS.SROLEID = ROLES.SROLEGUID
