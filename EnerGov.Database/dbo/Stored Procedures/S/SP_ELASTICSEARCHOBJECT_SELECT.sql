CREATE PROCEDURE [dbo].[SP_ELASTICSEARCHOBJECT_SELECT] (
	@RESERVED_BY CHAR(36)
) AS
BEGIN
	SELECT
		[ELASTICSEARCHOBJECTID]
		,[OBJECTID]
		,[OBJECTCLASSNAME] 
		,[ROWVERSION]
		,[CREATEDATE]
		,[PROCESSEDDATE]
		,[OBJECTACTION] 
		,[INDEXNAME]
		,[RESERVEDBY]
		,[RESERVEDDATE]
	FROM [ELASTICSEARCHOBJECT]
	WHERE [RESERVEDBY] = @RESERVED_BY
	AND [PROCESSEDDATE] IS NULL;
END