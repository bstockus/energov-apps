
CREATE PROCEDURE [dbo].[rpt_SR_Permit_Setup_Allowed_Permit_Activity]
@PMPERMITTYPEID AS VARCHAR(36)
AS
SELECT     PMPERMITACTIVITYTYPE.NAME AS PermitActivityType, CUSTOMFIELDLAYOUT.SNAME AS CustomFieldLayout, PMPERMITACTIVITYTYPE.DESCRIPTION, 
                      PMPERMITACTIVITYTYPE.CANEDIT, PMPERMITACTIVITYTYPE.PREFIXID, PMPERMITACTIVITYTYPE.ALLOWDUPLICATE, PMPERMITACTIVITYTYPE.CREATEID
FROM         PMPERMITACTIVITYTYPE INNER JOIN
                      PMPERMITALLOWEDACTIVITY ON 
                      PMPERMITACTIVITYTYPE.PMPERMITACTIVITYTYPEID = PMPERMITALLOWEDACTIVITY.PMPERMITACTIVITYTYPEID LEFT OUTER JOIN
                      CUSTOMFIELDLAYOUT ON PMPERMITACTIVITYTYPE.CUSTOMFIELDID = CUSTOMFIELDLAYOUT.GCUSTOMFIELDLAYOUTS
WHERE PMPERMITALLOWEDACTIVITY.PMPERMITTYPEID = @PMPERMITTYPEID
