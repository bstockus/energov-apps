
CREATE PROCEDURE [dbo].[rpt_SR_Permit_Setup_Workclass_Certifications]
@PMPERMITTYPEID AS VARCHAR(36)
AS
SELECT     PMPERMITWORKCLASS.NAME AS WorkClass, ILLICENSECLASSIFICATION.NAME AS Classification
FROM         PMPERMITTYPEWORKCLASS INNER JOIN
                      PMPERMITWORKCLASS ON PMPERMITTYPEWORKCLASS.PMPERMITWORKCLASSID = PMPERMITWORKCLASS.PMPERMITWORKCLASSID INNER JOIN
                      PMPERMITTYPELICENSETYPE ON PMPERMITWORKCLASS.PMPERMITWORKCLASSID = PMPERMITTYPELICENSETYPE.PMPERMITWORKCLASSID INNER JOIN
                      PMWCCERTCLASS ON PMPERMITTYPELICENSETYPE.PMPERMITTYPELICENSETYPEID = PMWCCERTCLASS.PMPERMITTYPELICENSETYPEID INNER JOIN
                      ILLICENSECLASSIFICATION ON PMWCCERTCLASS.ILLICENSECLASSIFICATIONID = ILLICENSECLASSIFICATION.ILLICENSECLASSIFICATIONID
WHERE PMPERMITTYPEWORKCLASS.PMPERMITTYPEID = @PMPERMITTYPEID
