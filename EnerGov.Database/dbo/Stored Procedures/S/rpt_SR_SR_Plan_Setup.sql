

CREATE PROCEDURE [dbo].[rpt_SR_SR_Plan_Setup]
AS
SELECT PLPLANTYPE.PLPLANTYPEID, PLPLANTYPE.PLANNAME AS PlanType, PLPLANTYPE.PREFIX, PLPLANTYPE.DAYSTOEXPIRE, 
       PLPLANTYPE.DAYSTOPLANAPPROVALEXPIRE, PLPLANTYPE.ACTIVE, PLPLANTYPE.VALUATION, PLPLANTYPE.SQUAREFEET, PLPLANTYPE.UNLIMITED, 
       PLPLANTYPE.ALLOWINTERNETSUBMISSION, PLPLANSTATUS.NAME AS Status, PLPLANTYPE.EXPIRABLE, USERS.FNAME AS AssignedToFName, 
       USERS.LNAME AS AssignedToLName, PLPLANTYPE.SHOWINLICENSING, PLPLANTYPE.ALLOWCOMPLETEWITHOPENINVOICE, PLPLANTYPE.USINGCLOCK, 
       PLPLANTYPE.CLOCKLIMITEDDAYS, PLPLANTYPE.TABLENAME, PLPLANTYPE.REPORTNAME, USERS_1.FNAME AS DEFAULTFNAME, USERS_1.LNAME AS DEFAULTLNAME
FROM PLPLANTYPE 
LEFT OUTER JOIN PLPLANSTATUS ON PLPLANTYPE.DEFAULTSTATUS = PLPLANSTATUS.PLPLANSTATUSID
LEFT OUTER JOIN USERS AS USERS_1 ON PLPLANTYPE.DEFAULTUSER = USERS_1.SUSERGUID 
LEFT OUTER JOIN USERS ON PLPLANTYPE.ASSIGNEDUSER = USERS.SUSERGUID 

