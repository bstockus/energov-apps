
CREATE PROCEDURE [dbo].[rpt_SR_SR_Code_Setup_Allowed_Code_Activity]

@CMCASETYPEID AS VARCHAR(36)

AS

SELECT	CMCODEALLOWEDACTIVITY.CMCASETYPEID,
		CUSTOMFIELDLAYOUT.SNAME AS CustomFieldLayout, 
		CMCODEACTIVITYTYPE.NAME AS CodeActivityType, CMCODEACTIVITYTYPE.DESCRIPTION, CMCODEACTIVITYTYPE.CANEDIT, CMCODEACTIVITYTYPE.CREATEID, 
		CMCODEACTIVITYTYPE.PREFIXID, CMCODEACTIVITYTYPE.ALLOWDUPLICATE

FROM	CMCODEALLOWEDACTIVITY 
		INNER JOIN CMCODEACTIVITYTYPE ON CMCODEALLOWEDACTIVITY.CMCODEACTIVITYTYPEID = CMCODEACTIVITYTYPE.CMCODEACTIVITYTYPEID 
		LEFT OUTER JOIN CUSTOMFIELDLAYOUT ON CMCODEACTIVITYTYPE.CUSTOMFIELDID = CUSTOMFIELDLAYOUT.GCUSTOMFIELDLAYOUTS
		WHERE CMCODEALLOWEDACTIVITY.CMCASETYPEID = @CMCASETYPEID
