

CREATE PROCEDURE [dbo].[rpt_SR_SR_Plan_Setup_Allowed_Plan_Activity]
@PLPLANTYPEID AS VARCHAR(36)
AS
SELECT PLPLANACTIVITYTYPE.NAME AS PlanActivityType, CUSTOMFIELDLAYOUT.SNAME AS CustomFieldLayout, PLPLANACTIVITYTYPE.DESCRIPTION, 
       PLPLANACTIVITYTYPE.CANEDIT, PLPLANACTIVITYTYPE.PREFIXID, PLPLANACTIVITYTYPE.ALLOWDUPLICATE, PLPLANACTIVITYTYPE.CREATEID
FROM PLPLANACTIVITYTYPE 
INNER JOIN PLPLANALLOWEDACTIVITY ON PLPLANACTIVITYTYPE.PLPLANACTIVITYTYPEID = PLPLANALLOWEDACTIVITY.PLPLANACTIVITYTYPEID 
LEFT OUTER JOIN CUSTOMFIELDLAYOUT ON PLPLANACTIVITYTYPE.CUSTOMFIELDID = CUSTOMFIELDLAYOUT.GCUSTOMFIELDLAYOUTS
WHERE PLPLANALLOWEDACTIVITY.PLPLANTYPEID = @PLPLANTYPEID

