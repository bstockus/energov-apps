CREATE PROCEDURE SEARCHCODEINSPECTORLISTBYTYPE
@InspectionTypeId char(36),
@SearchText varchar(500)
AS
BEGIN
SELECT     DISTINCT USERS.SUSERGUID, USERS.FNAME, USERS.LNAME
FROM         USERS INNER JOIN
					  IMINSPECTORTYPEUSER ON USERS.SUSERGUID = IMINSPECTORTYPEUSER.USERID INNER JOIN
					  IMINSPECTORTYPEINSPECTIONTYPE ON IMINSPECTORTYPEUSER.INSPECTORTYPEID = IMINSPECTORTYPEINSPECTIONTYPE.IMINSPECTORTYPEID
WHERE IMINSPECTORTYPEINSPECTIONTYPE.IMINSPECTIONTYPEID = @InspectionTypeId AND (FNAME like @SearchText or LNAME like @SearchText)
END
