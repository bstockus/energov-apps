CREATE PROCEDURE [dbo].[USP_JOBSCHEDULE_UPDATE]
(
	@JOBSCHEDULEID CHAR(36),
	@NAME NVARCHAR(50),
	@CRONSTRING NVARCHAR(300),
	@DESCRIPTION VARCHAR(MAX),
	@ISCUSTOMCRON BIT,
	@RECURRANCE INT,
	@RECURRANCEID INT,
	@RECURRANCESTART INT,
	@RECURRANCEEND INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[JOBSCHEDULE] SET
	[NAME] = @NAME,
	[CRONSTRING] = @CRONSTRING,
	[DESCRIPTION] = @DESCRIPTION,
	[ISCUSTOMCRON] = @ISCUSTOMCRON,
	[RECURRANCE] = @RECURRANCE,
	[RECURRANCEID] = @RECURRANCEID,
	[RECURRANCESTART] = @RECURRANCESTART,
	[RECURRANCEEND] = @RECURRANCEEND,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[JOBSCHEDULEID] = @JOBSCHEDULEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE