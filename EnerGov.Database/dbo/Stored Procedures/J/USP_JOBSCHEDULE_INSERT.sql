CREATE PROCEDURE [dbo].[USP_JOBSCHEDULE_INSERT]
(
	@JOBSCHEDULEID CHAR(36),
	@NAME NVARCHAR(50),
	@CRONSTRING NVARCHAR(300),
	@DESCRIPTION VARCHAR(MAX),
	@ISCUSTOMCRON BIT,
	@RECURRANCE INT,
	@RECURRANCEID INT,
	@RECURRANCESTART INT,
	@RECURRANCEEND INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[JOBSCHEDULE](
	[JOBSCHEDULEID],
	[NAME],
	[CRONSTRING],
	[DESCRIPTION],
	[ISCUSTOMCRON],
	[RECURRANCE],
	[RECURRANCEID],
	[RECURRANCESTART],
	[RECURRANCEEND],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@JOBSCHEDULEID,
	@NAME,
	@CRONSTRING,
	@DESCRIPTION,
	@ISCUSTOMCRON,
	@RECURRANCE,
	@RECURRANCEID,
	@RECURRANCESTART,
	@RECURRANCEEND,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE