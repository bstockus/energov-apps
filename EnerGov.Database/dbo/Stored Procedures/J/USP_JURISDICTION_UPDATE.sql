CREATE PROCEDURE [dbo].[USP_JURISDICTION_UPDATE]
(
	@JURISDICTIONID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(255),
	@CAFINANCIALINTEGRATIONSETUPID CHAR(36) = NULL,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[JURISDICTION] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[CAFINANCIALINTEGRATIONSETUPID] = @CAFINANCIALINTEGRATIONSETUPID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[JURISDICTIONID] = @JURISDICTIONID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE