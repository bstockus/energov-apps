CREATE PROCEDURE [dbo].[USP_JURISDICTION_INSERT]
(
	@JURISDICTIONID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(255),
	@CAFINANCIALINTEGRATIONSETUPID CHAR(36) = NULL,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[JURISDICTION](
	[JURISDICTIONID],
	[NAME],
	[DESCRIPTION],
	[CAFINANCIALINTEGRATIONSETUPID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@JURISDICTIONID,
	@NAME,
	@DESCRIPTION,
	@CAFINANCIALINTEGRATIONSETUPID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE