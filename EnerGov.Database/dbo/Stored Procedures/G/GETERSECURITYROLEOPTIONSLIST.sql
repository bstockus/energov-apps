
CREATE PROCEDURE [dbo].[GETERSECURITYROLEOPTIONSLIST]
-- Add the parameters for the stored procedure here
@RoleID char(36)	
AS
BEGIN
	SELECT	DISTINCT ROLESSETTING.NAME,
			ROLESROLESSETTING.BITVALUE AS ALLOW
	FROM	ROLESSETTING		
	INNER JOIN ROLESROLESSETTING ON ROLESROLESSETTING.ROLESSETTINGID = ROLESSETTING.ROLESSETTINGID
	WHERE BITVALUE IS NOT NULL AND ROLESROLESSETTING.ROLEID = @RoleID		
END	
