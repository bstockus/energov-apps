CREATE PROCEDURE [dbo].[USP_GET_WORKFLOWACTIONFTP_FOR_ATTACHMENT]
(
@ATTACHMENTGROUPID CHAR(36),
@WORKFLOWACTIONID CHAR(36)
)
AS
BEGIN
	SELECT WORKFLOWACTIONFTP.WORKFLOWACTIONFTPID ACTIONFTPID, WORKFLOWACTIONFTP.FTPFILENAMEOPTIONID
	FROM WORKFLOWACTIONFTP 
	WHERE WORKFLOWACTIONFTP.ATTACHMENTGROUPID = @ATTACHMENTGROUPID
	AND WORKFLOWACTIONFTP.WORKFLOWACTIONID = @WORKFLOWACTIONID
END