
CREATE PROCEDURE [dbo].[GETMOBILECODECASECONTACTLIST]
	@CodeCaseId char(36)
	AS
	BEGIN
	SELECT	DISTINCT CMCODECASECONTACT.CMCODECASECONTACTID,
					GLOBALENTITY.GLOBALENTITYID,
					GLOBALENTITY.FIRSTNAME,	
					GLOBALENTITY.LASTNAME,	
					GLOBALENTITY.GLOBALENTITYNAME AS COMPANY,
					GLOBALENTITY.BUSINESSPHONE,
					GLOBALENTITY.HOMEPHONE,
					GLOBALENTITY.MOBILEPHONE,
					GLOBALENTITY.OTHERPHONE,
					GLOBALENTITY.EMAIL,
					CMCODECASECONTACTTYPE.CMCODECASECONTACTTYPEID,
					CMCODECASECONTACTTYPE.NAME AS CONTACTTYPE,
					CMCODECASECONTACT.ISBILLING
			FROM	CMCODECASECONTACT
			INNER JOIN GLOBALENTITY ON GLOBALENTITY.GLOBALENTITYID = CMCODECASECONTACT.GLOBALENTITYID
			INNER JOIN CMCODECASECONTACTTYPE ON CMCODECASECONTACTTYPE.CMCODECASECONTACTTYPEID = CMCODECASECONTACT.CMCODECASECONTACTTYPEID 
			WHERE CMCODECASECONTACT.CMCODECASEID = @CodeCaseId	ORDER BY GLOBALENTITY.FIRSTNAME		
END

