
CREATE PROCEDURE GETMOBILECODECASEATTACHLIST
@parentID char(36)
AS
BEGIN
SELECT [ATTACHMENTID]
	  ,[FILENAME]
	  ,[FILEPATH]
	  ,[NOTES]
	  ,[PARENTID]
	  ,[ADDEDON]
	  ,[ADDEDBY]
	  ,ATTACHMENT.[ATTACHMENTGROUPID]
	  ,ATTACHMENTGROUP.NAME
  FROM [ATTACHMENT]
  LEFT JOIN ATTACHMENTGROUP ON ATTACHMENT.ATTACHMENTGROUPID = ATTACHMENTGROUP.ATTACHMENTGROUPID
   WHERE PARENTID = @parentID	ORDER BY ADDEDON
END
