CREATE PROCEDURE [dbo].[GETERITEMREVIEWTIMEENTRYLIST]
-- Add the parameters for the stored procedure here
@ItemReviewID char(36),
@ModuleID int	
AS
BEGIN	
	SELECT DISTINCT TIMETRACKING.*,
			TIMETRACKINGTYPE.NAME AS TIMETRACKINGTYPENAME,
			USERS.FNAME AS LOGUSERFIRSTNAME,
			USERS.LNAME AS LOGUSERLASTNAME,
			@ModuleID AS MODULEID
	FROM TIMETRACKING
	INNER JOIN TIMETRACKINGTYPE ON TIMETRACKINGTYPE.TIMETRACKINGTYPEID = TIMETRACKING.TIMETRACKINGTYPEID
	INNER JOIN USERS ON USERS.SUSERGUID = TIMETRACKING.LOGUSERID	
	WHERE SECONDARYRECORDTYPEID IS NOT NULL AND 
			SECONDARYRECORDTYPEID = 1 AND 
			SECONDARYRECORDID IS NOT NULL AND
			SECONDARYRECORDID = @ItemReviewID
	ORDER BY TIMETRACKING.LOGDATE DESC
END