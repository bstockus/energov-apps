	[GISLLMAPPINGID],
	[GISLLMAPPINGFIELD],
	[GISLLDATASOURCE],
	[ISIDENTIFIER],
	[COLUMNORPARAMETER],
	[MAPPINGORDER],
	[DELIMITER],
	[DEFAULTVALUE],
	[USELIKEFORGIS],
	[RELATIONSHIP]
)

VALUES
(
	@GISLLMAPPINGID,
	@GISLLMAPPINGFIELD,
	@GISLLDATASOURCE,
	@ISIDENTIFIER,
	@COLUMNORPARAMETER,
	@MAPPINGORDER,
	@DELIMITER,
	@DEFAULTVALUE,
	@USELIKEFORGIS,
	@RELATIONSHIP
)