
CREATE PROCEDURE [GISVALIDATION_LIVELINKMAPPINGS]
AS
  BEGIN
      -- SET NOCOUNT ON added to prevent extra result sets from
      -- interfering with SELECT statements.
      SET NOCOUNT ON;

      --Live Link Mappings
      SELECT GISLLMAPPINGFIELD.TABLENAME,
             GISLLMAPPINGFIELD.COLUMNNAME,
             GISLLDATASOURCE.ARCGISENDPOINT,
             GISLLDATASOURCE.ARCGISLAYER,
             GISLLMAPPING.COLUMNORPARAMETER,
             GISLLMAPPING.ISIDENTIFIER
      FROM   GISLLMAPPING
             INNER JOIN GISLLDATASOURCE
                     ON GISLLMAPPING.GISLLDATASOURCE =
                        GISLLDATASOURCE.GISLLDATASOURCEID
             INNER JOIN GISLLMAPPINGFIELD
                     ON GISLLMAPPING.GISLLMAPPINGFIELD =
                        GISLLMAPPINGFIELD.GISLLMAPPINGFIELDID
      WHERE  GISLLDATASOURCE.ISPARCELLAYER = 1
              OR GISLLDATASOURCE.ISADDRESSLAYER = 1
      ORDER  BY TABLENAME,
                COLUMNNAME
  END
