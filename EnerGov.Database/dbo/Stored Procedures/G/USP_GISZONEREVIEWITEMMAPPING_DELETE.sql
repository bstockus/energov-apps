CREATE PROCEDURE [dbo].[USP_GISZONEREVIEWITEMMAPPING_DELETE]
(
	@GISZONEREVIEWITEMMAPPINGID CHAR(36),
	@ROWVERSION INT
)
AS
BEGIN
SET NOCOUNT ON

EXEC [USP_GISZONETOREVIEWITEMDESIG_DELETE_BYPARENTID] @GISZONEREVIEWITEMMAPPINGID
EXEC [USP_GISZONEREVIEWITEMVALUE_DELETE_BYPARENTID] @GISZONEREVIEWITEMMAPPINGID

SET NOCOUNT OFF

DELETE FROM [dbo].[GISZONEREVIEWITEMMAPPING]
WHERE
	[GISZONEREVIEWITEMMAPPINGID] = @GISZONEREVIEWITEMMAPPINGID AND 
	[ROWVERSION]= @ROWVERSION

END