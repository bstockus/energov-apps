CREATE PROCEDURE [dbo].[USP_GISFEATURECLASS_UPDATE]
(
	@GISFEATURECLASSID CHAR(36),
	@LAYERNUMBER INT,
	@ATTRIBUTEFIELD VARCHAR(50),
	@LAYERURL VARCHAR(200),
	@LAYERNAME VARCHAR(50),
	@ALIAS VARCHAR(50),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[GISFEATURECLASS] SET
	[LAYERNUMBER] = @LAYERNUMBER,
	[ATTRIBUTEFIELD] = @ATTRIBUTEFIELD,
	[LAYERURL] = @LAYERURL,
	[LAYERNAME] = @LAYERNAME,
	[ALIAS] = @ALIAS,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[GISFEATURECLASSID] = @GISFEATURECLASSID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE