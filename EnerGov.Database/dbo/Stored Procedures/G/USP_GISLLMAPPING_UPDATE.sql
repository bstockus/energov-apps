CREATE PROCEDURE [dbo].[USP_GISLLMAPPING_UPDATE]
(
	@GISLLMAPPINGID CHAR(36),
	@GISLLMAPPINGFIELD CHAR(36),
	@GISLLDATASOURCE CHAR(36),
	@ISIDENTIFIER BIT,
	@COLUMNORPARAMETER NVARCHAR(250),
	@MAPPINGORDER INT,
	@DELIMITER NVARCHAR(3),
	@DEFAULTVALUE NVARCHAR(MAX),
	@USELIKEFORGIS BIT,
	@RELATIONSHIP NVARCHAR(250)
)
AS

UPDATE [dbo].[GISLLMAPPING] SET
	[GISLLMAPPINGID] = @GISLLMAPPINGID,
	[GISLLMAPPINGFIELD] = @GISLLMAPPINGFIELD,
	[GISLLDATASOURCE] = @GISLLDATASOURCE,
	[ISIDENTIFIER] = @ISIDENTIFIER,
	[COLUMNORPARAMETER] = @COLUMNORPARAMETER,
	[MAPPINGORDER] = @MAPPINGORDER,
	[DELIMITER] = @DELIMITER,
	[DEFAULTVALUE] = @DEFAULTVALUE,
	[USELIKEFORGIS] = @USELIKEFORGIS,
	[RELATIONSHIP] = @RELATIONSHIP
WHERE
	[GISLLMAPPINGID] = @GISLLMAPPINGID