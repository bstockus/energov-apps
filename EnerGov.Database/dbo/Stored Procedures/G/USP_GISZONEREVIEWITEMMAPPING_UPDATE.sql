CREATE PROCEDURE [dbo].[USP_GISZONEREVIEWITEMMAPPING_UPDATE]
(
	@GISZONEREVIEWITEMMAPPINGID CHAR(36),
	@SOURCENAME VARCHAR(100),
	@ARCGISURL VARCHAR(250),
	@ARCGISLAYERNAME VARCHAR(250),
	@ARCGISFIELDNAME VARCHAR(250),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[GISZONEREVIEWITEMMAPPING] SET
	[SOURCENAME] = @SOURCENAME,
	[ARCGISURL] = @ARCGISURL,
	[ARCGISLAYERNAME] = @ARCGISLAYERNAME,
	[ARCGISFIELDNAME] = @ARCGISFIELDNAME,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[GISZONEREVIEWITEMMAPPINGID] = @GISZONEREVIEWITEMMAPPINGID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE