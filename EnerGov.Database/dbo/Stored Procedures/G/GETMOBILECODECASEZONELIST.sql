CREATE PROCEDURE GETMOBILECODECASEZONELIST
@CodeCaseId char(36)
AS
BEGIN
SELECT	DISTINCT CMCODECASEID,CMCODECASEZONEID,CMCODECASEZONE.ZONEID,CMCODECASEZONE.MAIN, ZONE.ZONECODE,ZONE.NAME, ZONE.DESCRIPTION
		FROM CMCODECASEZONE
		INNER JOIN ZONE ON CMCODECASEZONE.ZONEID  = ZONE.ZONEID
		WHERE CMCODECASEZONE.CMCODECASEID = @CodeCaseId	ORDER BY ZONE.NAME
END
