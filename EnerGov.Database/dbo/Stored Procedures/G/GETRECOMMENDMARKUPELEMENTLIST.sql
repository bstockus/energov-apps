

CREATE PROCEDURE [dbo].[GETRECOMMENDMARKUPELEMENTLIST]
-- Add the parameters for the stored procedure here
@RecommendationID char(36)	
AS
BEGIN	
	SELECT	DISTINCT ERRECOMMENDATIONELEMENT.ERRECOMMENDATIONELEMENTID,  
			ERRECOMMENDATIONELEMENT.PLPLANRECOMMENDATIONID,
			ERRECOMMENDATIONELEMENT.PAGENUMBER,			
			ERRECOMMENDATIONELEMENT.ELEMENTNUMBER,			
			ERRECOMMENDATIONELEMENT.COMMENTS,
			ERRECOMMENDATIONELEMENT.ELEMENTID			
	FROM ERRECOMMENDATIONELEMENT			
	WHERE (ERRECOMMENDATIONELEMENT.PLPLANRECOMMENDATIONID = @RecommendationID)	
	ORDER BY ERRECOMMENDATIONELEMENT.PAGENUMBER, ERRECOMMENDATIONELEMENT.ELEMENTNUMBER    
END
