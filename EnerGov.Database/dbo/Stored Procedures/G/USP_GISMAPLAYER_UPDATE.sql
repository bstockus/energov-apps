CREATE PROCEDURE [dbo].[USP_GISMAPLAYER_UPDATE]
(
	@GISMAPLAYERID CHAR(36),
	@LAYERURL VARCHAR(500),
	@DESCRIPTION VARCHAR(MAX),
	@NAME NVARCHAR(100),
	@LEGENDURL VARCHAR(500),
	@IS_KOOP_ENDPOINT BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[GISMAPLAYER] SET
	[LAYERURL] = @LAYERURL,
	[DESCRIPTION] = @DESCRIPTION,
	[NAME] = @NAME,
	[LEGENDURL] = @LEGENDURL,
	[IS_KOOP_ENDPOINT] = @IS_KOOP_ENDPOINT,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[GISMAPLAYERID] = @GISMAPLAYERID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE