CREATE PROCEDURE [dbo].[USP_GISZONEREVIEWITEMVALUE_UPDATE]
(
	@GISZONEREVIEWITEMVALUEID CHAR(36),
	@GISZONEREVIEWITEMMAPPINGID CHAR(36),
	@VALUE VARCHAR(250),
	@PLITEMREVIEWZONEID CHAR(36)
)
AS

UPDATE [dbo].[GISZONEREVIEWITEMVALUE] SET
	[GISZONEREVIEWITEMMAPPINGID] = @GISZONEREVIEWITEMMAPPINGID,
	[VALUE] = @VALUE,
	[PLITEMREVIEWZONEID] = @PLITEMREVIEWZONEID

WHERE
	[GISZONEREVIEWITEMVALUEID] = @GISZONEREVIEWITEMVALUEID