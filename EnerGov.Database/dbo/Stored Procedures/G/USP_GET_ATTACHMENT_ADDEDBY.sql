CREATE PROCEDURE [dbo].[USP_GET_ATTACHMENT_ADDEDBY]
	@ATTACHMENTID as varchar(36)
AS
BEGIN
	SELECT TOP 1 [USERS].[LNAME] + ', ' + [USERS].[FNAME] AS USERNAME
	FROM [ATTACHMENT]
	JOIN [USERS] ON [ATTACHMENT].[ADDEDBY] = [USERS].[SUSERGUID]
	WHERE [ATTACHMENT].[ATTACHMENTID] = @ATTACHMENTID
	   OR [ATTACHMENT].[TCMDOCID] = @ATTACHMENTID
END