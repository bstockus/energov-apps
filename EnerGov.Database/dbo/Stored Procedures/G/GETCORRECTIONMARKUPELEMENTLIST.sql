
CREATE PROCEDURE [dbo].[GETCORRECTIONMARKUPELEMENTLIST]
-- Add the parameters for the stored procedure here
@CorrectionID char(36)	
AS
BEGIN	
	SELECT	DISTINCT ERMARKUPELEMENT.ERMARKUPELEMENTID,  
			ERMARKUPELEMENT.PLPLANCORRECTIONID,
			ERMARKUPELEMENT.PAGENUMBER,			
			ERMARKUPELEMENT.ELEMENTNUMBER,
			ERMARKUPELEMENT.COMMENTS,
			ERMARKUPELEMENT.ELEMENTID			
	FROM ERMARKUPELEMENT			
	WHERE (ERMARKUPELEMENT.PLPLANCORRECTIONID = @CorrectionID)	
	ORDER BY ERMARKUPELEMENT.PAGENUMBER, ERMARKUPELEMENT.ELEMENTNUMBER    
END
