CREATE PROCEDURE GETPARCELMAPPINGDATA
	-- Add the parameters for the stored procedure here
	@gisDatasourceID char(36)
	AS
	BEGIN	
SELECT	DISTINCT COLUMNORPARAMETER,TABLENAME,COLUMNNAME,GISLLMAPPING.DEFAULTVALUE FROM GISLLDATASOURCE, GISLLMAPPING, GISLLMAPPINGFIELD where GISLLDATASOURCE.GISLLDATASOURCEID = @gisDatasourceID
 and GISLLMAPPING.[GISLLMAPPINGFIELD] = GISLLMAPPINGFIELD.[GISLLMAPPINGFIELDID] AND GISLLDATASOURCE.[GISLLDATASOURCEID] =GISLLMAPPING.GISLLDATASOURCE
END

