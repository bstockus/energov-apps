CREATE PROCEDURE [dbo].[USP_GEOFIELDMAPPINGTRANSLATE_DELETE_BYGEOFIELDMAPPINGID]
(
	@GEOFIELDMAPPINGID CHAR(36)
)
AS
DELETE		[dbo].[GEOFIELDMAPPINGTRANSLATE] FROM [dbo].[GEOFIELDMAPPINGTRANSLATE]
INNER JOIN	[dbo].[GEOFIELDMAPPING]	ON [dbo].[GEOFIELDMAPPING].[GEOFIELDMAPPINGID] = [dbo].[GEOFIELDMAPPINGTRANSLATE].[GEOFIELDMAPPINGID]
WHERE		[dbo].[GEOFIELDMAPPING].[GEOFIELDMAPPINGID] = @GEOFIELDMAPPINGID