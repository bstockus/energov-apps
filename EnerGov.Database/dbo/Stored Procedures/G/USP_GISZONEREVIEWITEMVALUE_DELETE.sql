CREATE PROCEDURE [dbo].[USP_GISZONEREVIEWITEMVALUE_DELETE]
(
@GISZONEREVIEWITEMVALUEID CHAR(36)
)
AS

DECLARE @GISZONEREVIEWITEMMAPPINGID CHAR(36)

SELECT @GISZONEREVIEWITEMMAPPINGID = [GISZONEREVIEWITEMMAPPINGID]
FROM   [dbo].[GISZONEREVIEWITEMVALUE] WHERE [GISZONEREVIEWITEMVALUEID] = @GISZONEREVIEWITEMVALUEID

EXEC [USP_GISZONETOREVIEWITEMDESIG_DELETE_BYPARENTID] @GISZONEREVIEWITEMMAPPINGID


DELETE FROM [dbo].[GISZONEREVIEWITEMVALUE]
WHERE
	[GISZONEREVIEWITEMVALUEID] = @GISZONEREVIEWITEMVALUEID