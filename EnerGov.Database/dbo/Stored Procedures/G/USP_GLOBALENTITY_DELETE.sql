CREATE PROCEDURE [dbo].[USP_GLOBALENTITY_DELETE]
(
	@GLOBALENTITYID CHAR(36),
	@ROWVERSION INT
)
AS
SET NOCOUNT ON;

EXEC [dbo].[USP_PMPERMITCONTACT_DELETE_BYPARENTID] @GLOBALENTITYID
EXEC [dbo].[USP_PMPERMITCONTACT_DELETE_BYPARENTID] @GLOBALENTITYID

SET NOCOUNT OFF;

DELETE FROM [dbo].[GLOBALENTITY]
WHERE
	[GLOBALENTITYID] = @GLOBALENTITYID AND 
	[ROWVERSION]= @ROWVERSION