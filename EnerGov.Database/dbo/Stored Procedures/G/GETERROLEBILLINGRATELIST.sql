
CREATE PROCEDURE [dbo].[GETERROLEBILLINGRATELIST]
-- Add the parameters for the stored procedure here
@RoleID char(36)	
AS
BEGIN	
	SELECT DISTINCT ROLEBILLINGRATEXREF.BILLINGRATEID,
		BILLINGRATE.NAME,
		BILLINGRATE.AMOUNT,
		BILLINGRATE.ACTIVE,
		ROLEBILLINGRATEXREF.ISDEFAULT
	FROM ROLEBILLINGRATEXREF
	INNER JOIN BILLINGRATE ON BILLINGRATE.BILLINGRATEID = ROLEBILLINGRATEXREF.BILLINGRATEID	
	WHERE ROLEBILLINGRATEXREF.ROLEID = @RoleID AND BILLINGRATE.ACTIVE = 1
	ORDER BY BILLINGRATE.NAME
END
