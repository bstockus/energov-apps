CREATE PROCEDURE [dbo].[USP_GISZONEREVIEWITEMMAPPING_INSERT]
(
	@GISZONEREVIEWITEMMAPPINGID CHAR(36),
	@SOURCENAME VARCHAR(100),
	@ARCGISURL VARCHAR(250),
	@ARCGISLAYERNAME VARCHAR(250),
	@ARCGISFIELDNAME VARCHAR(250),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[GISZONEREVIEWITEMMAPPING](
	[GISZONEREVIEWITEMMAPPINGID],
	[SOURCENAME],
	[ARCGISURL],
	[ARCGISLAYERNAME],
	[ARCGISFIELDNAME],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@GISZONEREVIEWITEMMAPPINGID,
	@SOURCENAME,
	@ARCGISURL,
	@ARCGISLAYERNAME,
	@ARCGISFIELDNAME,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE