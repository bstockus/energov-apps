CREATE PROCEDURE [dbo].[USP_GLOBALENTITYACCOUNTTYPE_UPDATE]
(
	@GLOBALENTITYACCOUNTTYPEID CHAR(36),
	@TYPENAME NVARCHAR(30),
	@ISBONDTYPE BIT,
	@ISLICENSETYPE BIT,
	@ISAUTONUMBER BIT,
	@PREFIX NVARCHAR(10),
	@DESCRIPTION NVARCHAR(MAX),
	@CHARGECODE NVARCHAR(100),
	@ISFEEWAIVERACCOUNT BIT,
	@JURISDICTIONID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[GLOBALENTITYACCOUNTTYPE] SET
	[TYPENAME] = @TYPENAME,
	[ISBONDTYPE] = @ISBONDTYPE,
	[ISLICENSETYPE] = @ISLICENSETYPE,
	[ISAUTONUMBER] = @ISAUTONUMBER,
	[PREFIX] = @PREFIX,
	[DESCRIPTION] = @DESCRIPTION,
	[CHARGECODE] = @CHARGECODE,
	[ISFEEWAIVERACCOUNT] = @ISFEEWAIVERACCOUNT,
	[JURISDICTIONID] = @JURISDICTIONID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[GLOBALENTITYACCOUNTTYPEID] = @GLOBALENTITYACCOUNTTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE