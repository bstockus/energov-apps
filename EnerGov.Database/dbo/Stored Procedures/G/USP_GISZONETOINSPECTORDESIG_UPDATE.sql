CREATE PROCEDURE [dbo].[USP_GISZONETOINSPECTORDESIG_UPDATE]
(
	@GISZONETOINSPECTORDESIGID CHAR(36),
	@GISZONEEXTERNALVALUEID CHAR(36),
	@USERID CHAR(36)
)
AS

UPDATE [dbo].[GISZONETOINSPECTORDESIG] SET
	[GISZONEEXTERNALVALUEID] = @GISZONEEXTERNALVALUEID,
	[USERID] = @USERID

WHERE
	[GISZONETOINSPECTORDESIGID] = @GISZONETOINSPECTORDESIGID