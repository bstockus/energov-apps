CREATE PROCEDURE [dbo].[USP_GISZONETOREVIEWITEMDESIG_UPDATE]
(
	@GISZONETOREVIEWITEMDESIGID CHAR(36),
	@GISZONEREVIEWITEMVALUEID CHAR(36),
	@USERID CHAR(36),
	@PLITEMREVIEWTYPEID CHAR(36)	
)
AS
UPDATE [dbo].[GISZONETOREVIEWITEMDESIG] SET
	[GISZONEREVIEWITEMVALUEID] = @GISZONEREVIEWITEMVALUEID,
	[USERID] = @USERID,
	[PLITEMREVIEWTYPEID] = @PLITEMREVIEWTYPEID	
WHERE
	[GISZONETOREVIEWITEMDESIGID] = @GISZONETOREVIEWITEMDESIGID