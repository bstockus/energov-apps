CREATE PROCEDURE [dbo].[USP_GISZONEEXTERNALVALUE_UPDATE]
(
	@GISZONEEXTERNALVALUEID CHAR(36),
	@GISZONEMAPPINGID CHAR(36),
	@VALUE VARCHAR(250),
	@IMINSPECTIONZONEID CHAR(36)
)
AS

UPDATE [dbo].[GISZONEEXTERNALVALUE] SET
	[GISZONEMAPPINGID] = @GISZONEMAPPINGID,
	[VALUE] = @VALUE,
	[IMINSPECTIONZONEID] = @IMINSPECTIONZONEID

WHERE
	[GISZONEEXTERNALVALUEID] = @GISZONEEXTERNALVALUEID