CREATE PROCEDURE [dbo].[USP_MAILINGADDRESSTYPE_SELECT_LOOKUP]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[MAILINGADDRESSTYPE].[MAILINGADDRESSTYPEID],
	[dbo].[MAILINGADDRESSTYPE].[MAILINGADDRESSTYPENAME] AS [NAME]
FROM [dbo].[MAILINGADDRESSTYPE]
ORDER BY [dbo].[MAILINGADDRESSTYPE].[MAILINGADDRESSTYPENAME] 
END