CREATE PROCEDURE [dbo].[USP_MEETINGTYPE_DELETE]
(
	@MEETINGTYPEID CHAR(36),
	@ROWVERSION INT
)
AS

SET NOCOUNT ON;

EXEC [dbo].[USP_MEETINGTYPEDATTENDEE_DELETE_BYPARENTID] @MEETINGTYPEID
EXEC [dbo].[USP_MEETINGCASETYPE_DELETE_BYPARENTID] @MEETINGTYPEID
EXEC [dbo].[USP_WFACTION_MEETINGTYPE_DELETE_BYPARENTID] @MEETINGTYPEID

SET NOCOUNT OFF;

DELETE FROM [dbo].[MEETINGTYPE]
WHERE
	[MEETINGTYPEID] = @MEETINGTYPEID AND 
	[ROWVERSION]= @ROWVERSION