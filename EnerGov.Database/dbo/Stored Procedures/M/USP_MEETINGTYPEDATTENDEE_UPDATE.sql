CREATE PROCEDURE [dbo].[USP_MEETINGTYPEDATTENDEE_UPDATE]
(
	@MEETINGTYPEDATTENDEEID CHAR(36),
	@MEETINGTYPEID CHAR(36),
	@ATTENDEEID CHAR(36),
	@ISUSER BIT,
	@ISCONTACT BIT
)
AS

UPDATE [dbo].[MEETINGTYPEDATTENDEE] SET
	[MEETINGTYPEID] = @MEETINGTYPEID,
	[ATTENDEEID] = @ATTENDEEID,
	[ISUSER] = @ISUSER,
	[ISCONTACT] = @ISCONTACT

WHERE
	[MEETINGTYPEDATTENDEEID] = @MEETINGTYPEDATTENDEEID