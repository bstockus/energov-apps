CREATE PROCEDURE [dbo].[USP_MAILMERGETEMPLATE_INSERT]
(
	@MAILMERGETEMPLATEID CHAR(36),
	@SENDTO NVARCHAR(MAX),
	@SENDFROM NVARCHAR(MAX),
	@CCTO NVARCHAR(MAX),
	@BCCTO NVARCHAR(MAX),
	@SUBJECT NVARCHAR(MAX),
	@BODY NVARCHAR(MAX),
	@ISHTML BIT,
	@RPTREPORTID  CHAR(36)
)
AS
DECLARE @OUTPUTTABLE AS TABLE([MAILMERGETEMPLATEID] CHAR(36))
INSERT INTO [dbo].[MAILMERGETEMPLATE](
	[MAILMERGETEMPLATEID],
	[SENDTO],
	[SENDFROM],
	[CCTO],
	[BCCTO],
	[SUBJECT],
	[BODY],
	[ISHTML],
	[RPTREPORTID]
)
OUTPUT inserted.[MAILMERGETEMPLATEID] into @OUTPUTTABLE
VALUES(
	@MAILMERGETEMPLATEID,
	@SENDTO,
	@SENDFROM,
	@CCTO,
	@BCCTO,
	@SUBJECT,
	@BODY,
	@ISHTML,
	@RPTREPORTID
)
SELECT * FROM @OUTPUTTABLE