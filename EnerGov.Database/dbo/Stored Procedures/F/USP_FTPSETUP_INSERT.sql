CREATE PROCEDURE [dbo].[USP_FTPSETUP_INSERT]
(
	@FTPSETUPID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(250),
	@URL NVARCHAR(250),
	@USERNAME NVARCHAR(100),
	@PASSWORD NVARCHAR(100),
	@FTPTYPEID INT,
	@PORT INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[FTPSETUP](
	[FTPSETUPID],
	[NAME],
	[DESCRIPTION],
	[URL],
	[USERNAME],
	[PASSWORD],
	[FTPTYPEID],
	[PORT],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@FTPSETUPID,
	@NAME,
	@DESCRIPTION,
	@URL,
	@USERNAME,
	@PASSWORD,
	@FTPTYPEID,
	@PORT,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE