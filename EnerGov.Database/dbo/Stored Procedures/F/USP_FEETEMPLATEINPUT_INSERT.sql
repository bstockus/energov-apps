CREATE PROCEDURE [dbo].[USP_FEETEMPLATEINPUT_INSERT]
(
	@INPUTID CHAR(36),
	@PARENTID CHAR(36),
	@INPUTTYPEID INT,
	@INPUTVALUE NVARCHAR(50),
	@INPUTFRIENDLYNAME NVARCHAR(MAX),
	@ISDISPLAY BIT,
	@CUSTOMFIELDTABLE NVARCHAR(MAX),
	@CLASSNAME NVARCHAR(MAX),
	@ISFEEINPUT BIT
)
AS

IF @ISFEEINPUT = 1
	BEGIN
		INSERT INTO [dbo].[CAFEETEMPLATEFEEINPUT](
			[CAFEETEMPLATEFEEINPUTID],
			[CAFEETEMPLATEFEEID],
			[CAFEETEMPLATEINPUTTYPEID],
			[INPUTVALUE],
			[INPUTFRIENDLYNAME],
			[ISDISPLAY],
			[CUSTOMFIELDTABLE],
			[CLASSNAME]
		)
		VALUES
		(
			@INPUTID,
			@PARENTID,
			@INPUTTYPEID,
			@INPUTVALUE,
			@INPUTFRIENDLYNAME,
			@ISDISPLAY,
			@CUSTOMFIELDTABLE,
			@CLASSNAME
		)
	END
ELSE
	BEGIN
		INSERT INTO [dbo].[CAFEETEMPLATEDISCOUNTINPUT](
			[CAFEETEMPLATEDISCOUNTINPUTID],
			[CAFEETEMPLATEDISCOUNTID],
			[CAFEETEMPLATEINPUTTYPEID],
			[INPUTVALUE],
			[INPUTFRIENDLYNAME],
			[ISDISPLAY],
			[CLASSNAME]
		)
		VALUES
		(
			@INPUTID,
			@PARENTID,
			@INPUTTYPEID,
			@INPUTVALUE,
			@INPUTFRIENDLYNAME,
			@ISDISPLAY,
			@CLASSNAME
		)
	END