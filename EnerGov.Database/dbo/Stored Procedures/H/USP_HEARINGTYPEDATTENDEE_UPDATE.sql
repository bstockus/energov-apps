CREATE PROCEDURE [dbo].[USP_HEARINGTYPEDATTENDEE_UPDATE]
(
	@HEARINGTYPEDATTENDEEID CHAR(36),
	@HEARINGTYPEID CHAR(36),
	@ATTENDEEID CHAR(36),
	@ISUSER BIT,
	@ISCONTACT BIT
)
AS

UPDATE [dbo].[HEARINGTYPEDATTENDEE] SET
	[HEARINGTYPEID] = @HEARINGTYPEID,
	[ATTENDEEID] = @ATTENDEEID,
	[ISUSER] = @ISUSER,
	[ISCONTACT] = @ISCONTACT

WHERE
	[HEARINGTYPEDATTENDEEID] = @HEARINGTYPEDATTENDEEID