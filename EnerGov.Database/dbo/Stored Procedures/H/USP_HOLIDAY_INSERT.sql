CREATE PROCEDURE [dbo].[USP_HOLIDAY_INSERT]
(
	@HOLIDAYID CHAR(36),
	@NAME NVARCHAR(100),
	@HOLIDAYDATE DATETIME,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[HOLIDAY](
	[HOLIDAYID],
	[NAME],
	[HOLIDAYDATE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@HOLIDAYID,
	@NAME,
	@HOLIDAYDATE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE