CREATE PROCEDURE [dbo].[USP_BONDINTERESTSCHEDULE_UPDATE]
(
	@BONDINTERESTSCHEDULEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(500),
	@ISCOMPOUNDINTEREST BIT,
	@INTERESTMETHODID INT,
	@CAFEEID CHAR(36),
	@CHARGECODE NVARCHAR(100),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@JURISDICTIONID CHAR(36)
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[BONDINTERESTSCHEDULE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[ISCOMPOUNDINTEREST] = @ISCOMPOUNDINTEREST,
	[INTERESTMETHODID] = @INTERESTMETHODID,
	[CAFEEID] = @CAFEEID,
	[CHARGECODE] = @CHARGECODE,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1,
	[JURISDICTIONID] = @JURISDICTIONID
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[BONDINTERESTSCHEDULEID] = @BONDINTERESTSCHEDULEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE