CREATE PROCEDURE [dbo].[USP_BLCONTACTTYPE_INSERT]
(
	@BLCONTACTTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@BLCONTACTTYPESYSTEMID CHAR(36),
	@REQUIREDVALIDLIC BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[BLCONTACTTYPE](
	[BLCONTACTTYPEID],
	[NAME],
	[DESCRIPTION],
	[BLCONTACTTYPESYSTEMID],
	[REQUIREDVALIDLIC],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@BLCONTACTTYPEID,
	@NAME,
	@DESCRIPTION,
	@BLCONTACTTYPESYSTEMID,
	@REQUIREDVALIDLIC,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE