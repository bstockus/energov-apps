CREATE PROCEDURE [dbo].[USP_BUSINESSLICENSEEVENTQUEUE_UPDATE]
(
@EVENTQUEUEID AS BIGINT,
@EVENTSTATUSID INT
)
AS  
BEGIN  
	SET NOCOUNT ON;
	UPDATE [dbo].[BUSINESSLICENSEEVENTQUEUE]
	SET 
		PROCESSEDDATE = GETDATE(),
		EVENTSTATUSID = @EVENTSTATUSID
	WHERE [dbo].[BUSINESSLICENSEEVENTQUEUE].[BUSINESSLICENSEEVENTQUEUEID] = @EVENTQUEUEID

END