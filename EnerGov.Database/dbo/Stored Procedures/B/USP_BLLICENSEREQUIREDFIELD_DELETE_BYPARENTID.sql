CREATE PROCEDURE [dbo].[USP_BLLICENSEREQUIREDFIELD_DELETE_BYPARENTID]  
(  
@BLLICENSETYPEID CHAR(36)='',
@BLLICENSETYPECLASSID CHAR(36)=NULL
)  
AS  
DELETE [dbo].[BLLICENSEREQUIREDFIELD] FROM [dbo].[BLLICENSEREQUIREDFIELD]
INNER JOIN [dbo].[BLLICENSETYPECLASS] ON [dbo].[BLLICENSEREQUIREDFIELD].[BLLICENSETYPECLASSID] = [dbo].[BLLICENSETYPECLASS].[BLLICENSETYPECLASSID]
WHERE
[dbo].[BLLICENSETYPECLASS].[BLLICENSETYPEID] = @BLLICENSETYPEID 
AND (@BLLICENSETYPECLASSID IS NULL OR [dbo].[BLLICENSETYPECLASS].[BLLICENSECLASSID] = @BLLICENSETYPECLASSID)