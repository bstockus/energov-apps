CREATE PROCEDURE [dbo].[USP_BLEXTSTATUS_INSERT]
(
	@BLEXTSTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@BLEXTSTATUSSYSTEMID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[BLEXTSTATUS](
	[BLEXTSTATUSID],
	[NAME],
	[DESCRIPTION],
	[BLEXTSTATUSSYSTEMID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@BLEXTSTATUSID,
	@NAME,
	@DESCRIPTION,
	@BLEXTSTATUSSYSTEMID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE