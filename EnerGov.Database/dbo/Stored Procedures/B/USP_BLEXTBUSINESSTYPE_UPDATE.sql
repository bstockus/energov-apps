CREATE PROCEDURE [dbo].[USP_BLEXTBUSINESSTYPE_UPDATE]
(
	@BLEXTBUSINESSTYPEID CHAR(36),
	@CODENUMBER NVARCHAR(10),
	@NAME NVARCHAR(250),
	@DESCRIPTION NVARCHAR(MAX),
	@BLEXTBUSINESSCATEGORYID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[BLEXTBUSINESSTYPE] SET
	[CODENUMBER] = @CODENUMBER,
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[BLEXTBUSINESSCATEGORYID] = @BLEXTBUSINESSCATEGORYID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[BLEXTBUSINESSTYPEID] = @BLEXTBUSINESSTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE