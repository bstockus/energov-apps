CREATE PROCEDURE [dbo].[USP_BLTYPECLASSILTYPE_DELETE]
(
	@BLTYPECLASSILTYPEID CHAR(36)
)
AS
BEGIN
-- Delete Professional license classification.
EXEC [dbo].[USP_BLTYPECLASSILCLASS_DELETE_BYPARENTID] null, null, @BLTYPECLASSILTYPEID  

	DELETE FROM [dbo].[BLTYPECLASSILTYPE]
	WHERE
		[BLTYPECLASSILTYPEID] = @BLTYPECLASSILTYPEID
END