CREATE PROCEDURE [dbo].[USP_BLTYPECLASSILTYPE_UPDATE]
(
	@BLTYPECLASSILTYPEID CHAR(36),
	@BLLICENSETYPECLASSID CHAR(36),
	@ILLICENSETYPEID CHAR(36)
)
AS

UPDATE [dbo].[BLTYPECLASSILTYPE] SET
	[BLLICENSETYPECLASSID] = @BLLICENSETYPECLASSID,
	[ILLICENSETYPEID] = @ILLICENSETYPEID

WHERE
	[BLTYPECLASSILTYPEID] = @BLTYPECLASSILTYPEID