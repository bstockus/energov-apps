CREATE PROCEDURE [dbo].[USP_BONDTYPE_INSERT]
(
	@BONDTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@DEFAULTAMOUNT MONEY,
	@HASTRANSACTION BIT,
	@DEFAULTSTATUSID CHAR(36),
	@PREFIX NVARCHAR(10),
	@DAYSTOEXPIRE INT,
	@ACTIVE BIT,
	@AUTONUMBER BIT,
	@ISUSERELEASEINTEREST BIT,
	@DAYSPRIORTOACCRUAL INT,
	@BONDINTERESTSCHEDULEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[BONDTYPE](
	[BONDTYPEID],
	[NAME],
	[DESCRIPTION],
	[DEFAULTAMOUNT],
	[HASTRANSACTION],
	[DEFAULTSTATUSID],
	[PREFIX],
	[DAYSTOEXPIRE],
	[ACTIVE],
	[AUTONUMBER],
	[ISUSERELEASEINTEREST],
	[DAYSPRIORTOACCRUAL],
	[BONDINTERESTSCHEDULEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@BONDTYPEID,
	@NAME,
	@DESCRIPTION,
	@DEFAULTAMOUNT,
	@HASTRANSACTION,
	@DEFAULTSTATUSID,
	@PREFIX,
	@DAYSTOEXPIRE,
	@ACTIVE,
	@AUTONUMBER,
	@ISUSERELEASEINTEREST,
	@DAYSPRIORTOACCRUAL,
	@BONDINTERESTSCHEDULEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE