CREATE PROCEDURE [dbo].[USP_BLLICENSETYPECLASS_DELETE_BYPARENTID]
(
	@PARENTID CHAR(36)
)
AS

-- Delete Required Fields for (BusinessLicenseType, BusinessLicenseTypeClassification)
EXEC [dbo].[USP_BLLICENSEREQUIREDFIELD_DELETE_BYPARENTID] @PARENTID, NULL

EXEC [dbo].[USP_BLTYPECLASSILTYPE_DELETE_BYPARENTID] @PARENTID,null

EXEC [dbo].[USP_ATTACHMENTREQFILEREF_DELETE_BYPARENTID] @PARENTID,NULL

EXEC [dbo].[USP_BLCONTACTTYPEREF_DELETE_BYPARENTID] @PARENTID

DELETE FROM [dbo].[BLLICENSETYPECLASS]
WHERE
	[BLLICENSETYPEID] = @PARENTID