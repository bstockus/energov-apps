CREATE PROCEDURE [dbo].[USP_BONDINTERESTSCHEDULE_INSERT]
(
	@BONDINTERESTSCHEDULEID CHAR(36),
	@NAME NVARCHAR(100),
	@DESCRIPTION NVARCHAR(500),
	@ISCOMPOUNDINTEREST BIT,
	@INTERESTMETHODID INT,
	@CAFEEID CHAR(36),
	@CHARGECODE NVARCHAR(100),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@JURISDICTIONID CHAR(36)
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[BONDINTERESTSCHEDULE](
	[BONDINTERESTSCHEDULEID],
	[NAME],
	[DESCRIPTION],
	[ISCOMPOUNDINTEREST],
	[INTERESTMETHODID],
	[CAFEEID],
	[CHARGECODE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION],
	[JURISDICTIONID]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@BONDINTERESTSCHEDULEID,
	@NAME,
	@DESCRIPTION,
	@ISCOMPOUNDINTEREST,
	@INTERESTMETHODID,
	@CAFEEID,
	@CHARGECODE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION,
	@JURISDICTIONID
)
SELECT * FROM @OUTPUTTABLE