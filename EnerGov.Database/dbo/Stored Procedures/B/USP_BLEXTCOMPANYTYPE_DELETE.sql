CREATE PROCEDURE [dbo].[USP_BLEXTCOMPANYTYPE_DELETE]
(
	@BLEXTCOMPANYTYPEID CHAR(36),
	@ROWVERSION INT
)
AS

SET NOCOUNT ON;

EXEC [dbo].[USP_BLCONTACTTYPEREF_DELETE_BYPARENTID] @BLEXTCOMPANYTYPEID
EXEC [dbo].[USP_ATTACHMENTREQFILEREF_DELETE_BYPARENTID] @BLEXTCOMPANYTYPEID, NULL

SET NOCOUNT OFF;

DELETE FROM [dbo].[BLEXTCOMPANYTYPE]
WHERE
	[BLEXTCOMPANYTYPEID] = @BLEXTCOMPANYTYPEID AND 
	[ROWVERSION]= @ROWVERSION