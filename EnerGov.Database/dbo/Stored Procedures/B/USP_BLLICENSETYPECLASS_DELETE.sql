CREATE PROCEDURE [dbo].[USP_BLLICENSETYPECLASS_DELETE]  
(  
 @BLLICENSETYPECLASSID CHAR(36)  
)  
AS   
BEGIN  

DECLARE @BLLICENSETYPEID CHAR(36), @BLLICENSECLASSID CHAR(36)

SELECT @BLLICENSETYPEID = [BLLICENSETYPEID], @BLLICENSECLASSID = [BLLICENSECLASSID] 
	FROM [dbo].[BLLICENSETYPECLASS] WHERE BLLICENSETYPECLASSID  = @BLLICENSETYPECLASSID

-- Delete Professional license type
EXEC [dbo].[USP_BLTYPECLASSILTYPE_DELETE_BYPARENTID] @BLLICENSETYPEID, @BLLICENSECLASSID

-- Delete Required Fields for (BusinessLicenseType, BusinessLicenseTypeClassification)
EXEC [dbo].[USP_BLLICENSEREQUIREDFIELD_DELETE_BYPARENTID] @BLLICENSETYPEID, @BLLICENSECLASSID

-- Delete Online File types for (BusinessLicenseType, BusinessLicenseTypeClassification)
EXEC [USP_ATTACHMENTREQFILEREF_DELETE_BYPARENTID] @BLLICENSETYPEID, @BLLICENSECLASSID
  
DELETE FROM [dbo].[BLLICENSETYPECLASS]  
WHERE  
 [BLLICENSETYPECLASSID] = @BLLICENSETYPECLASSID     
END