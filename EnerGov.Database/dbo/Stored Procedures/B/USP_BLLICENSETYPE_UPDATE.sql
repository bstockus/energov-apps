CREATE PROCEDURE [dbo].[USP_BLLICENSETYPE_UPDATE]
(
	@BLLICENSETYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@ACTIVE BIT,
	@DEFAULTSTATUSID CHAR(36),
	@PREFIX NVARCHAR(10),
	@ISRECEIPTSTYPE BIT,
	@ISRENEWABLE BIT,
	@DEFAULTINTERNETBLLICSTATUSID CHAR(36),
	@ALLOWINTERNETSUBMISSION BIT,
	@ALLOWRENEWALLICENSENUM BIT,
	@DEFAULTRENEWSTATUSID CHAR(36),
	@SETRENEWISSDATETOPRVEXPDATE BIT,
	@SETISSUEDATEONRENEWAL BIT,
	@USEACTUALRECEIPTSASEST BIT,
	@ANNUALIZERECONCILERECEIPTS BIT,
	@CURRENTYEARONRENEWALS BIT,
	@CANRENEWONLINE BIT,
	@DEFAULTWEBRENEWSTATUSID CHAR(36),
	@MANAGEBUSINESSTYPECODES BIT,
	@REQUIREBUSINESSTYPECODES BIT,
	@REPORTRECEIPTSBYBUSINESSTYPECODE BIT,
	@SKIPPAYMENT BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT,
	@BLLICENSETYPEMODULEID INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[BLLICENSETYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[ACTIVE] = @ACTIVE,
	[DEFAULTSTATUSID] = @DEFAULTSTATUSID,
	[PREFIX] = @PREFIX,
	[ISRECEIPTSTYPE] = @ISRECEIPTSTYPE,
	[ISRENEWABLE] = @ISRENEWABLE,
	[DEFAULTINTERNETBLLICSTATUSID] = @DEFAULTINTERNETBLLICSTATUSID,
	[ALLOWINTERNETSUBMISSION] = @ALLOWINTERNETSUBMISSION,
	[ALLOWRENEWALLICENSENUM] = @ALLOWRENEWALLICENSENUM,
	[DEFAULTRENEWSTATUSID] = @DEFAULTRENEWSTATUSID,
	[SETRENEWISSDATETOPRVEXPDATE] = @SETRENEWISSDATETOPRVEXPDATE,
	[SETISSUEDATEONRENEWAL] = @SETISSUEDATEONRENEWAL,
	[USEACTUALRECEIPTSASEST] = @USEACTUALRECEIPTSASEST,
	[ANNUALIZERECONCILERECEIPTS] = @ANNUALIZERECONCILERECEIPTS,
	[CURRENTYEARONRENEWALS] = @CURRENTYEARONRENEWALS,
	[CANRENEWONLINE] = @CANRENEWONLINE,
	[DEFAULTWEBRENEWSTATUSID] = @DEFAULTWEBRENEWSTATUSID,
	[MANAGEBUSINESSTYPECODES] = @MANAGEBUSINESSTYPECODES,
	[REQUIREBUSINESSTYPECODES] = @REQUIREBUSINESSTYPECODES,
	[REPORTRECEIPTSBYBUSINESSTYPECODE] = @REPORTRECEIPTSBYBUSINESSTYPECODE,
	[SKIPPAYMENT] = @SKIPPAYMENT,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1,
	[BLLICENSETYPEMODULEID] = @BLLICENSETYPEMODULEID

OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[BLLICENSETYPEID] = @BLLICENSETYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE