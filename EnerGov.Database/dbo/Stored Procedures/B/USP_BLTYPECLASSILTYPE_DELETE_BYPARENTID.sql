CREATE PROCEDURE [dbo].[USP_BLTYPECLASSILTYPE_DELETE_BYPARENTID]  
(  
	@BLLICENSETYPEID CHAR(36),
	@BLLICENSECLASSID CHAR(36)
)  
AS 
BEGIN
-- Delete Professional license classification
EXEC [dbo].[USP_BLTYPECLASSILCLASS_DELETE_BYPARENTID] @BLLICENSETYPEID, @BLLICENSECLASSID, null

	DELETE [dbo].[BLTYPECLASSILTYPE] FROM [dbo].[BLTYPECLASSILTYPE]
	INNER JOIN [BLLICENSETYPECLASS]   
	ON [dbo].[BLLICENSETYPECLASS].[BLLICENSETYPECLASSID] = [dbo].[BLTYPECLASSILTYPE].[BLLICENSETYPECLASSID]
	WHERE
		[dbo].[BLLICENSETYPECLASS].[BLLICENSETYPEID] = @BLLICENSETYPEID AND (@BLLICENSECLASSID IS NULL OR [dbo].[BLLICENSETYPECLASS].[BLLICENSECLASSID] = @BLLICENSECLASSID)
END