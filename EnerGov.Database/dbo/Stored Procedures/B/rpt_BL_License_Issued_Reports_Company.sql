



CREATE PROCEDURE [dbo].[rpt_BL_License_Issued_Reports_Company]
@GLOBALENTITYEXTENSIONID AS VARCHAR(36)
AS
SELECT GLOBALENTITY.GLOBALENTITYNAME AS CompanyName
FROM BLGLOBALENTITYEXTENSION 
INNER JOIN GLOBALENTITY ON BLGLOBALENTITYEXTENSION.GLOBALENTITYID = GLOBALENTITY.GLOBALENTITYID
WHERE BLGLOBALENTITYEXTENSION.BLGLOBALENTITYEXTENSIONID = @GLOBALENTITYEXTENSIONID



