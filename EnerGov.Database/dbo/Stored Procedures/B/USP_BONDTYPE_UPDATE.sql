CREATE PROCEDURE [dbo].[USP_BONDTYPE_UPDATE]
(
	@BONDTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@DEFAULTAMOUNT MONEY,
	@HASTRANSACTION BIT,
	@DEFAULTSTATUSID CHAR(36),
	@PREFIX NVARCHAR(10),
	@DAYSTOEXPIRE INT,
	@ACTIVE BIT,
	@AUTONUMBER BIT,
	@ISUSERELEASEINTEREST BIT,
	@DAYSPRIORTOACCRUAL INT,
	@BONDINTERESTSCHEDULEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[BONDTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[DEFAULTAMOUNT] = @DEFAULTAMOUNT,
	[HASTRANSACTION] = @HASTRANSACTION,
	[DEFAULTSTATUSID] = @DEFAULTSTATUSID,
	[PREFIX] = @PREFIX,
	[DAYSTOEXPIRE] = @DAYSTOEXPIRE,
	[ACTIVE] = @ACTIVE,
	[AUTONUMBER] = @AUTONUMBER,
	[ISUSERELEASEINTEREST] = @ISUSERELEASEINTEREST,
	[DAYSPRIORTOACCRUAL] = @DAYSPRIORTOACCRUAL,
	[BONDINTERESTSCHEDULEID] = @BONDINTERESTSCHEDULEID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[BONDTYPEID] = @BONDTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE