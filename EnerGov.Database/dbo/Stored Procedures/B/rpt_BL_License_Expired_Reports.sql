

CREATE PROCEDURE [dbo].[rpt_BL_License_Expired_Reports]
@STARTDATE AS DATETIME,
@ENDDATE AS DATETIME
AS
SET @ENDDATE = DATEADD(S,-1,DATEADD(D,1,@ENDDATE))
SELECT BLLICENSE.LICENSENUMBER AS LicenseNumber, BLLICENSETYPE.NAME AS LicenseType, BLLICENSESTATUS.NAME AS LicenseStatus, 
       BLLICENSE.ISSUEDDATE AS IssuedDate, BLLICENSE.EXPIRATIONDATE AS ExpirationDate, BLLICENSE.APPLIEDDATE AS AppliedDate, 
       BLLICENSE.TAXYEAR AS TaxYear, BLLICENSE.BLGLOBALENTITYEXTENSIONID, BLLICENSECLASS.NAME AS Classification, DISTRICT.NAME AS District
FROM BLLICENSE 
INNER JOIN BLLICENSETYPE ON BLLICENSE.BLLICENSETYPEID = BLLICENSETYPE.BLLICENSETYPEID 
INNER JOIN BLLICENSECLASS ON BLLICENSE.BLLICENSECLASSID = BLLICENSECLASS.BLLICENSECLASSID 
INNER JOIN BLLICENSESTATUS ON BLLICENSE.BLLICENSESTATUSID = BLLICENSESTATUS.BLLICENSESTATUSID 
INNER JOIN DISTRICT ON BLLICENSE.DISTRICTID = DISTRICT.DISTRICTID
WHERE BLLICENSE.EXPIRATIONDATE BETWEEN @STARTDATE AND @ENDDATE

