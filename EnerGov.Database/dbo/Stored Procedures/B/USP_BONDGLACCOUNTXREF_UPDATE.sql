CREATE PROCEDURE [dbo].[USP_BONDGLACCOUNTXREF_UPDATE]
(
	@BONDGLACCOUNTXREFID CHAR(36),
	@BONDINTERESTSCHEDULEID CHAR(36),
	@DEBITGLACCOUNTID CHAR(36),
	@CREDITGLACCOUNTID CHAR(36),
	@CAPAYMENTMETHODID CHAR(36),
	@PERCENTAGE DECIMAL(20,4)
)
AS

UPDATE [dbo].[BONDGLACCOUNTXREF] SET
	[BONDINTERESTSCHEDULEID] = @BONDINTERESTSCHEDULEID,
	[DEBITGLACCOUNTID] = @DEBITGLACCOUNTID,
	[CREDITGLACCOUNTID] = @CREDITGLACCOUNTID,
	[CAPAYMENTMETHODID] = @CAPAYMENTMETHODID,
	[PERCENTAGE] = @PERCENTAGE

WHERE
	[BONDGLACCOUNTXREFID] = @BONDGLACCOUNTXREFID