CREATE PROCEDURE [dbo].[USP_ROLEPAYMENTMETHODXREF_UPDATE]
(
	@ROLEPAYMENTMETHODXREFID CHAR(36),
	@SROLEGUID CHAR(36),
	@CAPAYMENTMETHODID CHAR(36),
	@DISABLEDFLAG BIT
)
AS

UPDATE [dbo].[ROLEPAYMENTMETHODXREF] SET
	[ROLEPAYMENTMETHODXREFID] = @ROLEPAYMENTMETHODXREFID,
	[SROLEGUID] = @SROLEGUID,
	[CAPAYMENTMETHODID] = @CAPAYMENTMETHODID,
	[DISABLEDFLAG] = @DISABLEDFLAG

WHERE
	[ROLEPAYMENTMETHODXREFID] = @ROLEPAYMENTMETHODXREFID