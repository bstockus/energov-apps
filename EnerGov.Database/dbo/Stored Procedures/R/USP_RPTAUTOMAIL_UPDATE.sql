CREATE PROCEDURE [dbo].[USP_RPTAUTOMAIL_UPDATE]
(
	@RPTAUTOMAILID CHAR(36),
	@SESSIONNAME VARCHAR(255),
	@RPTDATERANGEID INT,
	@DAYNUMDATE INT,
	@RPTREPORTID CHAR(36),
	@RECURRENCEID CHAR(36),
	@DESCRIPTION VARCHAR(MAX),
	@LASTRECURRENCE DATETIME,
	@NEXTRECURRENCE DATETIME,
	@CUSTOMSUBJECT NVARCHAR(MAX),
	@CUSTOMBODY NVARCHAR(MAX),
	@FTPSETUPID CHAR(36),
	@SUBPATH NVARCHAR(255),
	@LASTCHANGEDON DATETIME,
	@LASTCHANGEDBY CHAR(36),
	@ROWVERSION INT,
	@RPTREPORTFILETYPEID INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[RPTAUTOMAIL] SET
	[SESSIONNAME] = @SESSIONNAME,
	[RPTDATERANGEID] = @RPTDATERANGEID,
	[DAYNUMDATE] = @DAYNUMDATE,
	[RPTREPORTID] = @RPTREPORTID,
	[RECURRENCEID] = @RECURRENCEID,
	[DESCRIPTION] = @DESCRIPTION,
	[LASTRECURRENCE] = @LASTRECURRENCE,
	[NEXTRECURRENCE] = @NEXTRECURRENCE,
	[CUSTOMSUBJECT] = @CUSTOMSUBJECT,
	[CUSTOMBODY] = @CUSTOMBODY,
	[FTPSETUPID] = @FTPSETUPID,
	[SUBPATH] = @SUBPATH,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[ROWVERSION] = @ROWVERSION + 1,
	[RPTREPORTFILETYPEID] = @RPTREPORTFILETYPEID
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[RPTAUTOMAILID] = @RPTAUTOMAILID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE