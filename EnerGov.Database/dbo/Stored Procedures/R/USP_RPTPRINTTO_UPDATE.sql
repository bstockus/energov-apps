CREATE PROCEDURE [dbo].[USP_RPTPRINTTO_UPDATE]
(
	@RPTPRINTTOID CHAR(36),
	@RPTAUTOMAILID CHAR(36),
	@PRINTERPATH  NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX)
)
AS

UPDATE [dbo].[RPTPRINTTO] SET
	[RPTAUTOMAILID] = @RPTAUTOMAILID,
	[PRINTERPATH] = @PRINTERPATH,
	[DESCRIPTION] = @DESCRIPTION

WHERE
	[RPTPRINTTOID] = @RPTPRINTTOID