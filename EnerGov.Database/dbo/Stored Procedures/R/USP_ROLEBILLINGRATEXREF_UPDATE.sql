CREATE PROCEDURE [dbo].[USP_ROLEBILLINGRATEXREF_UPDATE]
(
	@ROLEBILLINGRATEXREFID CHAR(36),
	@ROLEID CHAR(36),
	@BILLINGRATEID CHAR(36),
	@ISDEFAULT BIT
)
AS
	UPDATE [dbo].[ROLEBILLINGRATEXREF] SET
		[ROLEID] = @ROLEID,
		[BILLINGRATEID] = @BILLINGRATEID,
		[ISDEFAULT] = @ISDEFAULT
	WHERE
		[ROLEBILLINGRATEXREFID] = @ROLEBILLINGRATEXREFID