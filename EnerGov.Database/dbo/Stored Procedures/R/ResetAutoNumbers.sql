CREATE PROCEDURE [dbo].[ResetAutoNumbers]
	@autoClass NVARCHAR (250)
AS
	IF @autoClass IS NULL OR @autoClass = ''
	BEGIN
		DECLARE @MONTH INT, @DAY INT
		SET @MONTH = MONTH(GETDATE())
		SET @DAY = DAY(GETDATE())
		UPDATE AUTONUMBERSETTINGS SET NEXTVALUE = 1 WHERE USERESET = 1 AND @MONTH = 1 AND @DAY = 1
	END
	ELSE
	BEGIN 
		UPDATE AUTONUMBERSETTINGS SET NEXTVALUE = 1 WHERE CLASSNAME = @autoClass
	END

RETURN 0