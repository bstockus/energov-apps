CREATE PROCEDURE [dbo].[USP_ROLERECORDTYPEXREF_SELECT_BY_ROLE_ID]
	@ROLEID CHAR(36)
AS

SET NOCOUNT ON;
SELECT 
	[dbo].[ROLERECORDTYPEXREF].[MODULERECORDTYPEID], 
	[dbo].[ROLERECORDTYPEXREF].[RECORDTYPEID], 
	[dbo].[ROLERECORDTYPEXREF].[VISIBLE], 
	[dbo].[ROLERECORDTYPEXREF].[ALLOWADD], 
	[dbo].[ROLERECORDTYPEXREF].[ALLOWUPDATE], 
	[dbo].[ROLERECORDTYPEXREF].[ALLOWDELETE] 
FROM [dbo].[ROLERECORDTYPEXREF]
WHERE [dbo].[ROLERECORDTYPEXREF].[ROLEID] = @ROLEID
ORDER BY [dbo].[ROLERECORDTYPEXREF].[MODULERECORDTYPEID], [dbo].[ROLERECORDTYPEXREF].[RECORDTYPEID]