CREATE PROCEDURE [dbo].[USP_ROLESROLESSETTING_GETBYROLEID]
(
  @ROLEID AS CHAR(36)
)
AS
BEGIN

SELECT [dbo].ROLESSETTING.[NAME], [dbo].ROLESROLESSETTING.BITVALUE, [dbo].ROLESROLESSETTING.STRINGVALUE, [dbo].ROLESROLESSETTING.INTVALUE 
FROM [dbo].ROLESROLESSETTING
JOIN [dbo].ROLESSETTING ON [dbo].ROLESROLESSETTING.ROLESSETTINGID = [dbo].ROLESSETTING.ROLESSETTINGID
WHERE [dbo].ROLESROLESSETTING.ROLEID = @ROLEID

END