CREATE PROCEDURE [dbo].[USP_RPCONTACTTYPE_INSERT]
(
	@RPCONTACTTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@RPCONTACTTYPESYSTEMID INT,
	@REQUIREDVALIDLIC BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[RPCONTACTTYPE](
	[RPCONTACTTYPEID],
	[NAME],
	[DESCRIPTION],
	[RPCONTACTTYPESYSTEMID],
	[REQUIREDVALIDLIC],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@RPCONTACTTYPEID,
	@NAME,
	@DESCRIPTION,
	@RPCONTACTTYPESYSTEMID,
	@REQUIREDVALIDLIC,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE