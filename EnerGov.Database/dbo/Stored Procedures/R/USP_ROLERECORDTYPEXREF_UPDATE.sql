CREATE PROCEDURE [dbo].[USP_ROLERECORDTYPEXREF_UPDATE]
(
	@ROLERECORDTYPEXREFID CHAR(36),
	@ROLEID CHAR(36),
	@MODULERECORDTYPEID INT,
	@RECORDTYPEID CHAR(36),
	@VISIBLE BIT,
	@ALLOWADD BIT,
	@ALLOWUPDATE BIT,
	@ALLOWDELETE BIT
)
AS

UPDATE [dbo].[ROLERECORDTYPEXREF] SET
	[ROLEID] = @ROLEID,
	[MODULERECORDTYPEID] = @MODULERECORDTYPEID,
	[RECORDTYPEID] = @RECORDTYPEID,
	[VISIBLE] = @VISIBLE,
	[ALLOWADD] = @ALLOWADD,
	[ALLOWUPDATE] = @ALLOWUPDATE,
	[ALLOWDELETE] = @ALLOWDELETE

WHERE
	[ROLERECORDTYPEXREFID] = @ROLERECORDTYPEXREFID