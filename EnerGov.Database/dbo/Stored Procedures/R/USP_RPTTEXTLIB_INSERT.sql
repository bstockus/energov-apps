CREATE PROCEDURE [dbo].[USP_RPTTEXTLIB_INSERT]
(
	@RPTTEXTLIBID CHAR(36),
	@TEXTNAME NVARCHAR(50),
	@REPORTTEXT NVARCHAR(4000),
	@ACTIVE BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[RPTTEXTLIB](
	[RPTTEXTLIBID],
	[TEXTNAME],
	[REPORTTEXT],
	[ACTIVE],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@RPTTEXTLIBID,
	@TEXTNAME,
	@REPORTTEXT,
	@ACTIVE,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE