CREATE PROCEDURE [dbo].[USP_RPLANDLORDLICENSETYPE_UPDATE]
(
	@RPLANDLORDLICENSETYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@FRIENDLYNAME NVARCHAR(100),
	@PREFIX NVARCHAR(10),
	@DEFAULTSTATUSID CHAR(36),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@CAFEETEMPLATEID CHAR(36),
	@ACTIVE BIT,
	@BLEINFORMATID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[RPLANDLORDLICENSETYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[FRIENDLYNAME] = @FRIENDLYNAME,
	[PREFIX] = @PREFIX,
	[DEFAULTSTATUSID] = @DEFAULTSTATUSID,
	[CUSTOMFIELDLAYOUTID] = @CUSTOMFIELDLAYOUTID,
	[CAFEETEMPLATEID] = @CAFEETEMPLATEID,
	[ACTIVE] = @ACTIVE,
	[BLEINFORMATID] = @BLEINFORMATID,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[RPLANDLORDLICENSETYPEID] = @RPLANDLORDLICENSETYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE