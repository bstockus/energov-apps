CREATE PROCEDURE [dbo].[USP_RPLANDLORDLICENSESTATUS_INSERT]
(
	@RPLANDLORDLICENSESTATUSID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@RPLANDLORDLICENSESYSSTATUSID INT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[RPLANDLORDLICENSESTATUS](
	[RPLANDLORDLICENSESTATUSID],
	[NAME],
	[DESCRIPTION],
	[RPLANDLORDLICENSESYSSTATUSID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@RPLANDLORDLICENSESTATUSID,
	@NAME,
	@DESCRIPTION,
	@RPLANDLORDLICENSESYSSTATUSID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE