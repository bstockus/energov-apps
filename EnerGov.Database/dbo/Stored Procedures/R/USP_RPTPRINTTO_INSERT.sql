CREATE PROCEDURE [dbo].[USP_RPTPRINTTO_INSERT]
(
	@RPTPRINTTOID CHAR(36),
	@RPTAUTOMAILID CHAR(36),
	@PRINTERPATH  NVARCHAR(100),
	@DESCRIPTION NVARCHAR(MAX)
)
AS

INSERT INTO [dbo].[RPTPRINTTO](
	[RPTPRINTTOID],
	[RPTAUTOMAILID],
	[PRINTERPATH],
	[DESCRIPTION]
)

VALUES
(
	@RPTPRINTTOID,
	@RPTAUTOMAILID,
	@PRINTERPATH,
	@DESCRIPTION
)