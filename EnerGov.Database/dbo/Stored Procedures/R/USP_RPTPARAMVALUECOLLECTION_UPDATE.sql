CREATE PROCEDURE [dbo].[USP_RPTPARAMVALUECOLLECTION_UPDATE]
(
	@ROWIDNUM CHAR(36),
	@RPTPARAMVALUECOLLECTIONID CHAR(36),
	@RPTREPORTID CHAR(36),
	@PARAMNAME NVARCHAR(255),
	@PARAMVALUE NVARCHAR(255),
	@ISMULTI BIT,
	@CREATEDATE DATETIME,
	@ISREQUIRED BIT
)
AS

DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)

UPDATE [dbo].[RPTPARAMVALUECOLLECTION] SET
	[RPTPARAMVALUECOLLECTIONID] = @RPTPARAMVALUECOLLECTIONID,
	[RPTREPORTID] = @RPTREPORTID,
	[PARAMNAME] = @PARAMNAME,
	[PARAMVALUE] = @PARAMVALUE,
	[ISMULTI] = @ISMULTI,
	[CREATEDATE] = @CREATEDATE,
	[ISREQUIRED] = @ISREQUIRED
OUTPUT 1 INTO @OUTPUTTABLE  -- Added this because we need to return somthing after updating any data and we dont have row number colum in this table.
WHERE
	[ROWIDNUM] = @ROWIDNUM  
SELECT * FROM @OUTPUTTABLE