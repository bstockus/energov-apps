CREATE PROCEDURE [dbo].[USP_RPTREPORTATTRIBUTE_UPDATE]
(
	@RPTREPORTATTRIBUTEID CHAR(36),
	@RPTREPORTID CHAR(36),
	@RPTPARAMTYPEID INT,
	@BUSINESSOBJECTPROPERTYNAME NVARCHAR(255),
	@PROPERTYFRIENDLYNAME NVARCHAR(255),
	@PROPERTYTOKEN NVARCHAR(255),
	@ISMULTI BIT,
	@PARAMETERNAME NVARCHAR(255),
	@CHILDPARAMETERNAME NVARCHAR(255),
	@PARAMETERORDER INT,
	@ISSTARTDATE BIT,
	@ISREQUIRED BIT,
	@VALUESOURCE NVARCHAR(4000)
)
AS

UPDATE [dbo].[RPTREPORTATTRIBUTE] SET
	[RPTREPORTID] = @RPTREPORTID,
	[RPTPARAMTYPEID] = @RPTPARAMTYPEID,
	[BUSINESSOBJECTPROPERTYNAME] = @BUSINESSOBJECTPROPERTYNAME,
	[PROPERTYFRIENDLYNAME] = @PROPERTYFRIENDLYNAME,
	[PROPERTYTOKEN] = @PROPERTYTOKEN,
	[ISMULTI] = @ISMULTI,
	[PARAMETERNAME] = @PARAMETERNAME,
	[CHILDPARAMETERNAME] = @CHILDPARAMETERNAME,
	[PARAMETERORDER] = @PARAMETERORDER,
	[ISSTARTDATE] = @ISSTARTDATE,
	[ISREQUIRED] = @ISREQUIRED,
	[VALUESOURCE] = @VALUESOURCE

WHERE
	[RPTREPORTATTRIBUTEID] = @RPTREPORTATTRIBUTEID