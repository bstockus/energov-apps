CREATE PROCEDURE [dbo].[USP_ROLESROLESSETTING_UPDATE]
(
	@ROLESSETTINGID CHAR(36),
	@ROLEID CHAR(36),
	@BITVALUE BIT,
	@STRINGVALUE NVARCHAR(150),
	@INTVALUE INT
)
AS

UPDATE [dbo].[ROLESROLESSETTING] SET
	[BITVALUE] = @BITVALUE,
	[STRINGVALUE] = @STRINGVALUE,
	[INTVALUE] = @INTVALUE

WHERE
	[ROLESSETTINGID] = @ROLESSETTINGID AND 
	[ROLEID] = @ROLEID