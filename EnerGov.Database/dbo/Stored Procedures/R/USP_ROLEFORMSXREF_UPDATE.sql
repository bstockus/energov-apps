CREATE PROCEDURE [dbo].[USP_ROLEFORMSXREF_UPDATE]
(
	@FKROLEID CHAR(36),
	@FKFORMSID CHAR(36),
	@BVISIBLE BIT,
	@BALLOWADD BIT,
	@BALLOWUPDATE BIT,
	@BALLOWDELETE BIT
)
AS

UPDATE [dbo].[ROLEFORMSXREF] SET
	[BVISIBLE] = @BVISIBLE,
	[BALLOWADD] = @BALLOWADD,
	[BALLOWUPDATE] = @BALLOWUPDATE,
	[BALLOWDELETE] = @BALLOWDELETE

WHERE
	[FKROLEID] = @FKROLEID AND 
	[FKFORMSID] = @FKFORMSID