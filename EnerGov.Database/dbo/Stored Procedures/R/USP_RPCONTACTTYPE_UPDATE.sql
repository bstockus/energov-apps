CREATE PROCEDURE [dbo].[USP_RPCONTACTTYPE_UPDATE]
(
	@RPCONTACTTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@RPCONTACTTYPESYSTEMID INT,
	@REQUIREDVALIDLIC BIT,
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
UPDATE [dbo].[RPCONTACTTYPE] SET
	[NAME] = @NAME,
	[DESCRIPTION] = @DESCRIPTION,
	[RPCONTACTTYPESYSTEMID] = @RPCONTACTTYPESYSTEMID,
	[REQUIREDVALIDLIC] = @REQUIREDVALIDLIC,
	[LASTCHANGEDBY] = @LASTCHANGEDBY,
	[LASTCHANGEDON] = @LASTCHANGEDON,
	[ROWVERSION] = @ROWVERSION + 1
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
WHERE
	[RPCONTACTTYPEID] = @RPCONTACTTYPEID AND 
	[ROWVERSION]= @ROWVERSION

SELECT * FROM @OUTPUTTABLE