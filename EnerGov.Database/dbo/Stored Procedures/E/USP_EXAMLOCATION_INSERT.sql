CREATE PROCEDURE [dbo].[USP_EXAMLOCATION_INSERT]
(
	@EXAMLOCATIONID CHAR(36),
	@NAME NVARCHAR(50),
	@DESCRIPTION NVARCHAR(MAX),
	@PAPERBASEDSEATS INT,
	@PCBASEDSEATS INT,
	@SITTINGDATEAVAILABILITYID INT,
	@ACTIVE BIT,
	@PROMPTLOCATIONDETAILS BIT,
	@RECURRENCEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[EXAMLOCATION](
	[EXAMLOCATIONID],
	[NAME],
	[DESCRIPTION],
	[PAPERBASEDSEATS],
	[PCBASEDSEATS],
	[SITTINGDATEAVAILABILITYID],
	[ACTIVE],
	[PROMPTLOCATIONDETAILS],
	[RECURRENCEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@EXAMLOCATIONID,
	@NAME,
	@DESCRIPTION,
	@PAPERBASEDSEATS,
	@PCBASEDSEATS,
	@SITTINGDATEAVAILABILITYID,
	@ACTIVE,
	@PROMPTLOCATIONDETAILS,
	@RECURRENCEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE