CREATE PROCEDURE [dbo].[USP_EXAMTYPE_INSERT]
(
	@EXAMTYPEID CHAR(36),
	@NAME NVARCHAR(50),
	@FRIENDLYNAME NVARCHAR(150),
	@DESCRIPTION NVARCHAR(MAX),
	@EXAMFORMATID INT,
	@DEFAULTEXAMSTATUSID CHAR(36),
	@DEFAULTEXAMLOCATIONID CHAR(36),
	@CUSTOMFIELDLAYOUTID CHAR(36),
	@PREFIX NVARCHAR(20),
	@DAYSUNTILEXPIRE INT,
	@ACTIVE BIT,
	@UNLIMITEDEXPIRATION BIT,
	@SITTINGDATEAVAILABILITYID INT,
	@RECURRENCEID CHAR(36),
	@LASTCHANGEDBY CHAR(36),
	@LASTCHANGEDON DATETIME,
	@ROWVERSION INT
)
AS
DECLARE @OUTPUTTABLE as TABLE([ROWVERSION]  int)
INSERT INTO [dbo].[EXAMTYPE](
	[EXAMTYPEID],
	[NAME],
	[FRIENDLYNAME],
	[DESCRIPTION],
	[EXAMFORMATID],
	[DEFAULTEXAMSTATUSID],
	[DEFAULTEXAMLOCATIONID],
	[CUSTOMFIELDLAYOUTID],
	[PREFIX],
	[DAYSUNTILEXPIRE],
	[ACTIVE],
	[UNLIMITEDEXPIRATION],
	[SITTINGDATEAVAILABILITYID],
	[RECURRENCEID],
	[LASTCHANGEDBY],
	[LASTCHANGEDON],
	[ROWVERSION]
)
OUTPUT inserted.[ROWVERSION] INTO @OUTPUTTABLE
VALUES
(
	@EXAMTYPEID,
	@NAME,
	@FRIENDLYNAME,
	@DESCRIPTION,
	@EXAMFORMATID,
	@DEFAULTEXAMSTATUSID,
	@DEFAULTEXAMLOCATIONID,
	@CUSTOMFIELDLAYOUTID,
	@PREFIX,
	@DAYSUNTILEXPIRE,
	@ACTIVE,
	@UNLIMITEDEXPIRATION,
	@SITTINGDATEAVAILABILITYID,
	@RECURRENCEID,
	@LASTCHANGEDBY,
	@LASTCHANGEDON,
	@ROWVERSION
)
SELECT * FROM @OUTPUTTABLE