CREATE PROCEDURE [dbo].[USP_EXAMLOCATION_DELETE]
(
	@EXAMLOCATIONID CHAR(36),
	@ROWVERSION INT
)
AS
SET NOCOUNT ON;

EXEC [dbo].[USP_EXAMLOCTIMESLOT_DELETE_BYPARENTID] @EXAMLOCATIONID
EXEC [dbo].[USP_EXAMSITTINGDATE_DELETE_BYPARENTID] @EXAMLOCATIONID

SET NOCOUNT OFF;
DELETE FROM [dbo].[EXAMLOCATION]
WHERE
	[EXAMLOCATIONID] = @EXAMLOCATIONID AND 
	[ROWVERSION] = @ROWVERSION