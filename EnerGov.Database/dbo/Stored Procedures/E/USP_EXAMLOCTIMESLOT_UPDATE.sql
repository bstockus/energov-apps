CREATE PROCEDURE [dbo].[USP_EXAMLOCTIMESLOT_UPDATE]
(
	@EXAMLOCTIMESLOTID CHAR(36),
	@EXAMTIMESLOTID CHAR(36),
	@EXAMLOCATIONID CHAR(36),
	@ISDEFAULT BIT
)
AS

UPDATE [dbo].[EXAMLOCTIMESLOT] SET
	[EXAMTIMESLOTID] = @EXAMTIMESLOTID,
	[EXAMLOCATIONID] = @EXAMLOCATIONID,
	[ISDEFAULT] = @ISDEFAULT

WHERE
	[EXAMLOCTIMESLOTID] = @EXAMLOCTIMESLOTID