CREATE PROCEDURE [dbo].[USP_EXAMTYPERULE_UPDATE]
(
	@EXAMTYPERULEID CHAR(36),
	@EXAMTYPEID CHAR(36),
	@EXAMRULEID CHAR(36),
	@BITVALUE BIT,
	@STRINGVALUE NVARCHAR(500),
	@INTVALUE INT
)
AS

UPDATE [dbo].[EXAMTYPERULE] SET
	[EXAMTYPEID] = @EXAMTYPEID,
	[EXAMRULEID] = @EXAMRULEID,
	[BITVALUE] = @BITVALUE,
	[STRINGVALUE] = @STRINGVALUE,
	[INTVALUE] = @INTVALUE

WHERE
	[EXAMTYPERULEID] = @EXAMTYPERULEID