CREATE TABLE [dbo].[PLPLANADDRESS] (
    [PLPLANADDRESSID]  CHAR (36) NOT NULL,
    [PLPLANID]         CHAR (36) NOT NULL,
    [MAILINGADDRESSID] CHAR (36) NOT NULL,
    [MAIN]             BIT       NOT NULL,
    CONSTRAINT [PK_PLPlanAddress] PRIMARY KEY CLUSTERED ([PLPLANADDRESSID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PLPlanAddress_Address] FOREIGN KEY ([MAILINGADDRESSID]) REFERENCES [dbo].[MAILINGADDRESS] ([MAILINGADDRESSID]),
    CONSTRAINT [FK_PLPlanAddress_PLPlan] FOREIGN KEY ([PLPLANID]) REFERENCES [dbo].[PLPLAN] ([PLPLANID])
);


GO
CREATE NONCLUSTERED INDEX [IMPORT1]
    ON [dbo].[PLPLANADDRESS]([MAILINGADDRESSID] ASC) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IMPORT2]
    ON [dbo].[PLPLANADDRESS]([PLPLANID] ASC) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IX_PLPLANADDRESS_MAIN]
    ON [dbo].[PLPLANADDRESS]([MAIN] ASC, [PLPLANID] ASC)
    INCLUDE([PLPLANADDRESSID], [MAILINGADDRESSID]);

