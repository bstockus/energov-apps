CREATE TABLE [dbo].[PMPERMITPARCEL] (
    [PMPERMITPARCELID]   CHAR (36) NOT NULL,
    [PMPERMITID]         CHAR (36) NOT NULL,
    [PARCELID]           CHAR (36) NOT NULL,
    [MAIN]               BIT       NOT NULL,
    [PARCELSPLITPROCESS] BIT       CONSTRAINT [DF_PMPERMITPARCEL_PARCELSPLITPROCESS] DEFAULT ((0)) NULL,
    CONSTRAINT [PK_PMPermitParcel] PRIMARY KEY CLUSTERED ([PMPERMITPARCELID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_PMPermitParcel_Parcel] FOREIGN KEY ([PARCELID]) REFERENCES [dbo].[PARCEL] ([PARCELID]),
    CONSTRAINT [FK_PMPermitParcel_PMPermit] FOREIGN KEY ([PMPERMITID]) REFERENCES [dbo].[PMPERMIT] ([PMPERMITID])
);


GO
CREATE NONCLUSTERED INDEX [IMPORT1]
    ON [dbo].[PMPERMITPARCEL]([PMPERMITID] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [IMPORT2]
    ON [dbo].[PMPERMITPARCEL]([PARCELID] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [NCIDX_PMPERMITPARCEL_MAIN_INCL]
    ON [dbo].[PMPERMITPARCEL]([MAIN] ASC)
    INCLUDE([PMPERMITID], [PARCELID]) WITH (FILLFACTOR = 90, PAD_INDEX = ON);

