CREATE TABLE [dbo].[PERFLOG] (
    [PERFLOGID] CHAR (36)       NOT NULL,
    [USERID]    CHAR (36)       NULL,
    [MESSAGE]   NVARCHAR (4000) NOT NULL,
    [DURATION]  DECIMAL (18, 4) NOT NULL,
    [LOGDATE]   DATETIME        NOT NULL,
    CONSTRAINT [PK_PERFLOG] PRIMARY KEY NONCLUSTERED ([PERFLOGID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PERFLOG_USERS] FOREIGN KEY ([USERID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);

