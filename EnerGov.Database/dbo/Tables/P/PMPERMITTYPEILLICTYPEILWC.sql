CREATE TABLE [dbo].[PMPERMITTYPEILLICTYPEILWC] (
    [PMPERMITTYPEILLICENSETYPEID] CHAR (36) NOT NULL,
    [ILLICENSECLASSIFICATIONID]   CHAR (36) NOT NULL,
    CONSTRAINT [PK_PMTTYPEILLICTYPEILWC] PRIMARY KEY CLUSTERED ([PMPERMITTYPEILLICENSETYPEID] ASC, [ILLICENSECLASSIFICATIONID] ASC) WITH (FILLFACTOR = 90),
