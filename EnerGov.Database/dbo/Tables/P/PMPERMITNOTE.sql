CREATE TABLE [dbo].[PMPERMITNOTE] (
    [PMPERMITNOTEID] CHAR (36)      NOT NULL,
    [PMPERMITID]     CHAR (36)      NOT NULL,
    [TEXT]           NVARCHAR (MAX) NULL,
    [CREATEDBY]      CHAR (36)      NOT NULL,
    [CREATEDDATE]    DATETIME       NOT NULL,
    [TITLE]          NVARCHAR (50)  CONSTRAINT [DF_PMPERMITNOTE_TITLE] DEFAULT ('') NULL,
    [PIN]            BIT            CONSTRAINT [DF_PMPERMITNOTE_PIN] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_PMPermitNote] PRIMARY KEY CLUSTERED ([PMPERMITNOTEID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_PMPermitNote_PMPermit] FOREIGN KEY ([PMPERMITID]) REFERENCES [dbo].[PMPERMIT] ([PMPERMITID]),
    CONSTRAINT [FK_PMPermitNote_Users] FOREIGN KEY ([CREATEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [PMPERMITNOTE_PERMIT]
    ON [dbo].[PMPERMITNOTE]([PMPERMITID] ASC) WITH (FILLFACTOR = 80);

