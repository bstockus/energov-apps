CREATE TABLE [dbo].[PMPERMITHOLD] (
    [PMPERMITHOLDID]     CHAR (36)      NOT NULL,
    [PMPERMITID]         CHAR (36)      NOT NULL,
    [HOLDSETUPID]        CHAR (36)      NULL,
    [ORIGIN]             CHAR (36)      NULL,
    [ORIGINNUMBER]       NVARCHAR (150) NULL,
    [SUSERGUID]          CHAR (36)      NULL,
    [COMMENTS]           NVARCHAR (MAX) NULL,
    [CREATEDDATE]        DATETIME       NULL,
    [EFFECTIVEENDDATE]   DATETIME       NULL,
    [ACTIVE]             BIT            CONSTRAINT [DF_PMPermitHold_Active] DEFAULT ((1)) NOT NULL,
    [HOLDSEVERITYID]     INT            DEFAULT ((1)) NOT NULL,
    [COPYTOSUBRECORD]    BIT            CONSTRAINT [DF_PMPermitHold_COPYTOSUBRECORD] DEFAULT ((1)) NOT NULL,
    [ORIGINCMCODECASEID] CHAR (36)      NULL,
    CONSTRAINT [PK_PMPermitHold] PRIMARY KEY CLUSTERED ([PMPERMITHOLDID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PMPermitHold_Hold] FOREIGN KEY ([HOLDSETUPID]) REFERENCES [dbo].[HOLDTYPESETUPS] ([HOLDSETUPID]),
    CONSTRAINT [FK_PMPermitHold_HoldSeverity] FOREIGN KEY ([HOLDSEVERITYID]) REFERENCES [dbo].[HOLDSEVERITY] ([HOLDSEVERITYID]),
    CONSTRAINT [FK_PMPermitHold_OriginCodeCase] FOREIGN KEY ([ORIGINCMCODECASEID]) REFERENCES [dbo].[CMCODECASE] ([CMCODECASEID]),
    CONSTRAINT [FK_PMPermitHold_PMPermit] FOREIGN KEY ([PMPERMITID]) REFERENCES [dbo].[PMPERMIT] ([PMPERMITID]),
    CONSTRAINT [FK_PMPermitHold_User] FOREIGN KEY ([SUSERGUID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_PMPERMITHOLD_PMPERMITID]
    ON [dbo].[PMPERMITHOLD]([PMPERMITID] ASC)
    INCLUDE([PMPERMITHOLDID]) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IX_PMPERMITHOLD_ORIGINCMCODECASEID]
    ON [dbo].[PMPERMITHOLD]([ORIGINCMCODECASEID] ASC)
    INCLUDE([PMPERMITHOLDID]);

