CREATE TABLE [dbo].[PLAPPLICATIONZONE] (
    [PLAPPLICATIONZONEID] CHAR (36) NOT NULL,
    [PLAPPLICATIONID]     CHAR (36) NOT NULL,
    [ZONEID]              CHAR (36) NOT NULL,
    [MAIN]                BIT       NOT NULL,
    CONSTRAINT [PK_PLApplicationZone] PRIMARY KEY CLUSTERED ([PLAPPLICATIONZONEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PLApplicationZone_PLApplication] FOREIGN KEY ([PLAPPLICATIONID]) REFERENCES [dbo].[PLAPPLICATION] ([PLAPPLICATIONID]),
    CONSTRAINT [FK_PLApplicationZone_Zone] FOREIGN KEY ([ZONEID]) REFERENCES [dbo].[ZONE] ([ZONEID])
);


GO
CREATE NONCLUSTERED INDEX [PLAPPLICATIONZONE_APPLICATION]
    ON [dbo].[PLAPPLICATIONZONE]([PLAPPLICATIONID] ASC);

