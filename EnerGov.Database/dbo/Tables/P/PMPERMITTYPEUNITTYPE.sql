CREATE TABLE [dbo].[PMPERMITTYPEUNITTYPE] (
    [PMPERMITTYPEIPUNITTYPEID] CHAR (36) NOT NULL,
    [PMPERMITTYPEID]           CHAR (36) NOT NULL,
    [PMPERMITWORKCLASSID]      CHAR (36) NULL,
    [IPUNITTYPEID]             CHAR (36) NOT NULL,
    [ISREQUIRED]               BIT       NOT NULL,
    [UNITTYPEGROUP]            INT       NULL,
    CONSTRAINT [PK_PMPERMITTYPEUNITTYPE] PRIMARY KEY CLUSTERED ([PMPERMITTYPEIPUNITTYPEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PMUNITTYPE_IPUNITTYPE] FOREIGN KEY ([IPUNITTYPEID]) REFERENCES [dbo].[IPUNITTYPE] ([IPUNITTYPEID]),
    CONSTRAINT [FK_PMUNITTYPE_PMPERMITTYPE] FOREIGN KEY ([PMPERMITTYPEID]) REFERENCES [dbo].[PMPERMITTYPE] ([PMPERMITTYPEID]),
    CONSTRAINT [FK_PMUNITTYPE_PMWORKCLASS] FOREIGN KEY ([PMPERMITWORKCLASSID]) REFERENCES [dbo].[PMPERMITWORKCLASS] ([PMPERMITWORKCLASSID])
);


GO
CREATE TRIGGER [dbo].[TG_PMPERMITTYPEUNITTYPE_DELETE]  
    ON  [dbo].[PMPERMITTYPEUNITTYPE]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[PMPERMITTYPE].[PMPERMITTYPEID],
		[PMPERMITTYPE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
		'Permit Type Allowed Unit Type Deleted',
        '',
        '',       
		'Permit Type (' + [PMPERMITTYPE].[NAME] + '), Work Class Name (' + ISNULL([PMPERMITWORKCLASS].[NAME],'[none]') +'), Allowed Unit Type (' + [IPUNITTYPE].[NAME] +')',
		'4976A4A9-20E8-4B8F-997D-CE244B540104',
		3,
		0,
		[IPUNITTYPE].[NAME]
    FROM [deleted]
	INNER JOIN [PMPERMITTYPE] ON [PMPERMITTYPE].[PMPERMITTYPEID] = [deleted].[PMPERMITTYPEID]
	INNER JOIN [IPUNITTYPE] WITH (NOLOCK) ON [IPUNITTYPE].[IPUNITTYPEID] = [deleted].[IPUNITTYPEID]
	LEFT JOIN [PMPERMITWORKCLASS] WITH (NOLOCK) ON [PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [deleted].[PMPERMITWORKCLASSID]
	
END
GO
