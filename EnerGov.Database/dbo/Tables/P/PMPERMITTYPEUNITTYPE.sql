CREATE TRIGGER [dbo].[TG_PMPERMITTYPEUNITTYPE_UPDATE]
   ON  [dbo].[PMPERMITTYPEUNITTYPE] 
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
			[PMPERMITTYPE].[PMPERMITTYPEID],
			[PMPERMITTYPE].[ROWVERSION],
			GETUTCDATE(),
			[PMPERMITTYPE].[LASTCHANGEDBY],
			'Required Flag',
			CASE [deleted].[ISREQUIRED] WHEN 1 THEN 'Yes' WHEN 0 THEN 'No' END,
			CASE [inserted].[ISREQUIRED] WHEN 1 THEN 'Yes' WHEN 0 THEN 'No' END,
			'Permit Type (' + [PMPERMITTYPE].[NAME] + '), Work Class Name (' + ISNULL([PMPERMITWORKCLASS].[NAME],'[none]') +'), Allowed Unit Type (' + [IPUNITTYPE].[NAME] +')',
			'4976A4A9-20E8-4B8F-997D-CE244B540104',
			2,
			0,
			[IPUNITTYPE].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[PMPERMITTYPEIPUNITTYPEID] = [inserted].[PMPERMITTYPEIPUNITTYPEID]
			INNER JOIN [PMPERMITTYPE] ON [PMPERMITTYPE].[PMPERMITTYPEID] = [inserted].[PMPERMITTYPEID]			
			INNER JOIN [IPUNITTYPE] WITH (NOLOCK) ON [IPUNITTYPE].[IPUNITTYPEID] = [inserted].[IPUNITTYPEID]			
			LEFT JOIN [PMPERMITWORKCLASS] WITH (NOLOCK) ON [PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [inserted].[PMPERMITWORKCLASSID]
	WHERE	[deleted].[ISREQUIRED] <> [inserted].[ISREQUIRED]
	UNION ALL
	SELECT 
			[PMPERMITTYPE].[PMPERMITTYPEID],
			[PMPERMITTYPE].[ROWVERSION],
			GETUTCDATE(),
			[PMPERMITTYPE].[LASTCHANGEDBY],
			'Unit Type Set',
			ISNULL(CONVERT(NVARCHAR(MAX), [deleted].[UNITTYPEGROUP]),'[none]'),
			ISNULL(CONVERT(NVARCHAR(MAX), [inserted].[UNITTYPEGROUP]),'[none]'),			
			'Permit Type (' + [PMPERMITTYPE].[NAME] + '), Work Class Name (' + ISNULL([PMPERMITWORKCLASS].[NAME],'[none]') +'), Allowed Unit Type (' + [IPUNITTYPE].[NAME] +')',
			'4976A4A9-20E8-4B8F-997D-CE244B540104',
			2,
			0,
			[IPUNITTYPE].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[PMPERMITTYPEIPUNITTYPEID] = [inserted].[PMPERMITTYPEIPUNITTYPEID]
			INNER JOIN [PMPERMITTYPE] ON [PMPERMITTYPE].[PMPERMITTYPEID] = [inserted].[PMPERMITTYPEID]			
			INNER JOIN [IPUNITTYPE] WITH (NOLOCK) ON [IPUNITTYPE].[IPUNITTYPEID] = [inserted].[IPUNITTYPEID]			
			LEFT JOIN [PMPERMITWORKCLASS] WITH (NOLOCK) ON [PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [inserted].[PMPERMITWORKCLASSID]
	WHERE	ISNULL([deleted].[UNITTYPEGROUP],'') <> ISNULL([inserted].[UNITTYPEGROUP],'')
	
END