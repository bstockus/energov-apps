CREATE TRIGGER [dbo].[TG_PMPERMITTYPEILLICTYPEILWCT_INSERT]
   ON  [dbo].[PMPERMITTYPEILLICTYPEILWCT]
   AFTER INSERT
AS 
BEGIN	
	SET NOCOUNT ON;	
	INSERT INTO [HISTORYSYSTEMSETUP]
    (
        [ID],
        [ROWVERSION],
        [CHANGEDON],
        [CHANGEDBY],
        [FIELDNAME],
        [OLDVALUE],
        [NEWVALUE],
        [ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
        [PMPERMITTYPE].[PMPERMITTYPEID], 
        [PMPERMITTYPE].[ROWVERSION],
        GETUTCDATE(),
        [PMPERMITTYPE].[LASTCHANGEDBY],	
        'Permit Type Professional License Classification Type Added',
        '',
        '',       
		'Permit Type (' + [PMPERMITTYPE].[NAME] + '), Work Class Name (' + [PMPERMITWORKCLASS].[NAME] + '), Professional License Type (' + [ILLICENSETYPE].[NAME] + '), Professional License Classification Type (' + [ILLICENSECLASSTYPE].[NAME] + ')',
		'4976A4A9-20E8-4B8F-997D-CE244B540104',
		1,
		0,
		[ILLICENSECLASSTYPE].[NAME]
    FROM [inserted]
	INNER JOIN [PMPERMITTYPEILLICTYPE] ON [PMPERMITTYPEILLICTYPE].[PMPERMITTYPEILLICENSETYPEID] = [inserted].[PMPERMITTYPEILLICENSETYPEID]
	INNER JOIN [ILLICENSECLASSTYPE] WITH (NOLOCK) ON [ILLICENSECLASSTYPE].[ILLICENSECLASSTYPEID] = [inserted].[ILLICENSECLASSIFICATIONTYPEID]
	INNER JOIN [PMPERMITTYPE] ON [PMPERMITTYPE].[PMPERMITTYPEID] = [PMPERMITTYPEILLICTYPE].[PMPERMITTYPEID]
	INNER JOIN [PMPERMITWORKCLASS] WITH (NOLOCK) ON [PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [PMPERMITTYPEILLICTYPE].[PMPERMITWORKCLASSID]
	INNER JOIN [ILLICENSETYPE] WITH (NOLOCK) ON [ILLICENSETYPE].[ILLICENSETYPEID] = [PMPERMITTYPEILLICTYPE].[ILLICENSETYPEID]
END
