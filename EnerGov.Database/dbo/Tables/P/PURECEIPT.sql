    CONSTRAINT [FK_PURcptInspBy_Users] FOREIGN KEY ([INSPECTEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID]),
    CONSTRAINT [FK_PURcptLCB_Users] FOREIGN KEY ([LASTCHANGEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID]),
    CONSTRAINT [FK_PURcptRcvdBy_Users] FOREIGN KEY ([RECEIVEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID]),
    CONSTRAINT [FK_PURcptShi_GECompany] FOREIGN KEY ([SHIPPER]) REFERENCES [dbo].[GLOBALENTITY] ([GLOBALENTITYID]),
    CONSTRAINT [FK_PURcptVendor_GECompany] FOREIGN KEY ([VENDOR]) REFERENCES [dbo].[GLOBALENTITY] ([GLOBALENTITYID])
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [UX_PUReceiptNo]
    ON [dbo].[PURECEIPT]([RECEIPTNO] ASC) WITH (FILLFACTOR = 90);

