CREATE TABLE [dbo].[PURECEIPT] (
    [PURECEIPTID]        CHAR (36)     NOT NULL,
    [PURECEIPTTYPEID]    CHAR (36)     NOT NULL,
    [RECEIPTNO]          VARCHAR (250) NOT NULL,
    [RECEIVEDDATE]       DATETIME      NOT NULL,
    [CREATEDDATE]        DATETIME      NOT NULL,
