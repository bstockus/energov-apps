CREATE TABLE [dbo].[PLPLANALLOWEDACTIVITY] (
    [PLPLANALLOWEDACTIVITYID] CHAR (36) NOT NULL,
    [PLPLANTYPEID]            CHAR (36) NOT NULL,
    [PLPLANACTIVITYTYPEID]    CHAR (36) NOT NULL,
    CONSTRAINT [PK_PLPlanAllowedActivity] PRIMARY KEY CLUSTERED ([PLPLANALLOWEDACTIVITYID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PLPlanAllowedActivity_PLPlanActivityType] FOREIGN KEY ([PLPLANACTIVITYTYPEID]) REFERENCES [dbo].[PLPLANACTIVITYTYPE] ([PLPLANACTIVITYTYPEID]),
    CONSTRAINT [FK_PLPlanAllowedActivity_PLPlanType] FOREIGN KEY ([PLPLANTYPEID]) REFERENCES [dbo].[PLPLANTYPE] ([PLPLANTYPEID])
);


GO
CREATE TRIGGER [dbo].[TG_PLPLANALLOWEDACTIVITY_INSERT]
   ON  [dbo].[PLPLANALLOWEDACTIVITY]
   AFTER INSERT
AS 
BEGIN	
	SET NOCOUNT ON;	
	INSERT INTO [HISTORYSYSTEMSETUP]
    (
        [ID],
        [ROWVERSION],
        [CHANGEDON],
        [CHANGEDBY],
        [FIELDNAME],
        [OLDVALUE],
        [NEWVALUE],
        [ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
        [PLPLANTYPE].[PLPLANTYPEID], 
        [PLPLANTYPE].[ROWVERSION],
        GETUTCDATE(),
        [PLPLANTYPE].[LASTCHANGEDBY],	
        'Plan Type Allowed Plan Activity Added',
        '',
        '',       
		'Plan Type (' + [PLPLANTYPE].[PLANNAME] + '), Activity Type (' + [PLPLANACTIVITYTYPE].[NAME] +')',
		'DEC0149B-EF4A-4E0F-AA4A-E622B90C1545',
		1,
		0,
		[PLPLANACTIVITYTYPE].[NAME]
    FROM [inserted]
	INNER JOIN [PLPLANTYPE] ON [PLPLANTYPE].[PLPLANTYPEID] = [inserted].[PLPLANTYPEID]
	INNER JOIN [PLPLANACTIVITYTYPE] WITH (NOLOCK) ON [PLPLANACTIVITYTYPE].[PLPLANACTIVITYTYPEID] = [inserted].[PLPLANACTIVITYTYPEID]
END
GO
CREATE TRIGGER [dbo].[TG_PLPLANALLOWEDACTIVITY_DELETE]  
    ON  [dbo].[PLPLANALLOWEDACTIVITY]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[PLPLANTYPE].[PLPLANTYPEID],
		[PLPLANTYPE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
		'Plan Type Allowed Plan Activity Deleted',
        '',
        '',       
		'Plan Type (' + [PLPLANTYPE].[PLANNAME] + '), Activity Type (' + [PLPLANACTIVITYTYPE].[NAME] +')',
		'DEC0149B-EF4A-4E0F-AA4A-E622B90C1545',
		3,
		0,
		[PLPLANACTIVITYTYPE].[NAME]
    FROM [deleted]
	INNER JOIN [PLPLANTYPE] ON [PLPLANTYPE].[PLPLANTYPEID] = [deleted].[PLPLANTYPEID]
	INNER JOIN [PLPLANACTIVITYTYPE] WITH (NOLOCK) ON [PLPLANACTIVITYTYPE].[PLPLANACTIVITYTYPEID] = [deleted].[PLPLANACTIVITYTYPEID]
END