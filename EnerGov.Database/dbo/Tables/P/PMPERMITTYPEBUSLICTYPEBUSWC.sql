    [PMPERMITTYPEBUSLICTYPEID] CHAR (36) NOT NULL,
    [BLLICENSECLASSID]         CHAR (36) NOT NULL,
    CONSTRAINT [PK_PMPERMITTYPEBUSLICTYPEILWC] PRIMARY KEY CLUSTERED ([PMPERMITTYPEBUSLICTYPEID] ASC, [BLLICENSECLASSID] ASC) WITH (FILLFACTOR = 90),
