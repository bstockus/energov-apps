GO
CREATE TRIGGER [dbo].[TG_PMPERMITTYPEBUSLICTYPEBUSWC_INSERT]
   ON  [dbo].[PMPERMITTYPEBUSLICTYPEBUSWC]
   AFTER INSERT
AS 
BEGIN	
	SET NOCOUNT ON;	
	INSERT INTO [HISTORYSYSTEMSETUP]
    (
        [ID],
        [ROWVERSION],
        [CHANGEDON],
        [CHANGEDBY],
        [FIELDNAME],
        [OLDVALUE],
        [NEWVALUE],
        [ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
        [PMPERMITTYPE].[PMPERMITTYPEID], 
        [PMPERMITTYPE].[ROWVERSION],
        GETUTCDATE(),
        [PMPERMITTYPE].[LASTCHANGEDBY],	
        'Permit Type Business License Classification Added',
        '',
        '',       
		'Permit Type (' + [PMPERMITTYPE].[NAME] + '), Work Class Name (' + [PMPERMITWORKCLASS].[NAME] +'), Business License Type (' + [BLLICENSETYPE].[NAME] +'), Business License Classification (' + [BLLICENSECLASS].[NAME] +')','4976A4A9-20E8-4B8F-997D-CE244B540104',
		1,
		0,
		[BLLICENSECLASS].[NAME]

	FROM [inserted]	
 	INNER JOIN [BLLICENSECLASS] WITH (NOLOCK) ON [BLLICENSECLASS].[BLLICENSECLASSID] = [inserted].[BLLICENSECLASSID]	
	INNER JOIN [PMPERMITTYPEBUSLICTYPE] ON [PMPERMITTYPEBUSLICTYPE].[PMPERMITTYPEBUSLICTYPEID] =  [inserted].[PMPERMITTYPEBUSLICTYPEID]
	INNER JOIN [BLLICENSETYPE] WITH (NOLOCK) ON [BLLICENSETYPE].[BLLICENSETYPEID] =  [PMPERMITTYPEBUSLICTYPE].[BLLICENSETYPEID]
	INNER JOIN [PMPERMITTYPE] ON [PMPERMITTYPE].[PMPERMITTYPEID] =  [PMPERMITTYPEBUSLICTYPE].[PMPERMITTYPEID]
	LEFT JOIN [PMPERMITWORKCLASS] WITH (NOLOCK) ON [PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [PMPERMITTYPEBUSLICTYPE].[PMPERMITWORKCLASSID] 
END
GO
CREATE TRIGGER [dbo].[TG_PMPERMITTYPEBUSLICTYPEBUSWC_DELETE]  
    ON  [dbo].[PMPERMITTYPEBUSLICTYPEBUSWC]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
