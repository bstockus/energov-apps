    CONSTRAINT [FK_PMBUSLIC_PMPERMBUSLIC] FOREIGN KEY ([PMPERMITTYPEBUSLICTYPEID]) REFERENCES [dbo].[PMPERMITTYPEBUSLICTYPE] ([PMPERMITTYPEBUSLICTYPEID])
