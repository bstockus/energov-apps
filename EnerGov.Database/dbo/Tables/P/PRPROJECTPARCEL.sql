CREATE TABLE [dbo].[PRPROJECTPARCEL] (
    [PRPROJECTPARCELID] CHAR (36) NOT NULL,
    [PARCELID]          CHAR (36) NOT NULL,
    [PRPROJECTID]       CHAR (36) NOT NULL,
    [MAIN]              BIT       NOT NULL,
    CONSTRAINT [PK_PRProjectParcel] PRIMARY KEY CLUSTERED ([PRPROJECTPARCELID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PRProjectParcel_Parcel] FOREIGN KEY ([PARCELID]) REFERENCES [dbo].[PARCEL] ([PARCELID]),
    CONSTRAINT [FK_PRProjectParcel_PRProject] FOREIGN KEY ([PRPROJECTID]) REFERENCES [dbo].[PRPROJECT] ([PRPROJECTID])
);


GO
CREATE NONCLUSTERED INDEX [IDX_PRPROJECTPARCEL_PROJECTID_MAIN]
    ON [dbo].[PRPROJECTPARCEL]([PRPROJECTID] ASC, [MAIN] ASC)
    INCLUDE([PARCELID]);

