CREATE TRIGGER [TG_PLAPPLICATIONTYPE_UPDATE] ON PLAPPLICATIONTYPE
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )

	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Name',
			[deleted].[APPLICATIONTYPENAME],
			[inserted].[APPLICATIONTYPENAME],
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	WHERE	[deleted].[APPLICATIONTYPENAME] <> [inserted].[APPLICATIONTYPENAME]
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Description',
			ISNULL([deleted].[DESCRIPTION],'[none]'),
			ISNULL([inserted].[DESCRIPTION],'[none]'),
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	WHERE	ISNULL([deleted].[DESCRIPTION], '') <> ISNULL([inserted].[DESCRIPTION], '')
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Custom Field Layout',
			ISNULL([DELETED_LAYOUT].[SNAME],'[none]'),
			ISNULL([INSERTED_LAYOUT].[SNAME],'[none]'),
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	LEFT OUTER JOIN CUSTOMFIELDLAYOUT INSERTED_LAYOUT WITH (NOLOCK) ON INSERTED_LAYOUT.GCUSTOMFIELDLAYOUTS = [inserted].CUSTOMFIELDID
	LEFT OUTER JOIN CUSTOMFIELDLAYOUT DELETED_LAYOUT WITH (NOLOCK) ON DELETED_LAYOUT.GCUSTOMFIELDLAYOUTS = [deleted].CUSTOMFIELDID
	WHERE	ISNULL([inserted].[CUSTOMFIELDID],'') <> ISNULL([deleted].[CUSTOMFIELDID],'')
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Default Status',
			ISNULL([DELETED_STATUS].[STATUS],'[none]'),
			ISNULL([INSERTED_STATUS].[STATUS],'[none]'),
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	LEFT OUTER JOIN PLAPPLICATIONSTATUS INSERTED_STATUS WITH (NOLOCK) ON INSERTED_STATUS.PLAPPLICATIONSTATUSID = [inserted].DEFAULTAPPLICATIONSTATUSID
	LEFT OUTER JOIN PLAPPLICATIONSTATUS DELETED_STATUS WITH (NOLOCK) ON DELETED_STATUS.PLAPPLICATIONSTATUSID = [deleted].DEFAULTAPPLICATIONSTATUSID
	WHERE	ISNULL([inserted].[DEFAULTAPPLICATIONSTATUSID],'') <> ISNULL([deleted].[DEFAULTAPPLICATIONSTATUSID],'')	
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Prefix',
			ISNULL([deleted].[PREFIX],'[none]'),
			ISNULL([inserted].[PREFIX],'[none]'),
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	WHERE	ISNULL([deleted].[PREFIX], '') <> ISNULL([inserted].[PREFIX], '')
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Auto Create Application ID Flag',
			CASE WHEN [deleted].[ISAUTONUMBER] = 1 THEN 'Yes' ELSE 'No' END,
			CASE WHEN [inserted].[ISAUTONUMBER] = 1 THEN 'Yes' ELSE 'No' END,
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	WHERE	[deleted].[ISAUTONUMBER] <> [inserted].[ISAUTONUMBER]
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Default Permit Type',
			ISNULL([DELETED_PMPERMITTYPE].[NAME],'[none]'),
			ISNULL([INSERTED_PMPERMITTYPE].[NAME],'[none]'),
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	LEFT OUTER JOIN PMPERMITTYPE INSERTED_PMPERMITTYPE WITH (NOLOCK) ON INSERTED_PMPERMITTYPE.PMPERMITTYPEID = [inserted].[DEFAULTPERMITTYPE]
	LEFT OUTER JOIN PMPERMITTYPE DELETED_PMPERMITTYPE WITH (NOLOCK) ON DELETED_PMPERMITTYPE.PMPERMITTYPEID = [deleted].[DEFAULTPERMITTYPE]
	WHERE	ISNULL([inserted].[DEFAULTPERMITTYPE],'') <> ISNULL([deleted].[DEFAULTPERMITTYPE],'')	
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Default Plan Type',
			ISNULL([DELETED_PLPLANTYPE].[PLANNAME],'[none]'),
			ISNULL([INSERTED_PLPLANTYPE].[PLANNAME],'[none]'),
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	LEFT OUTER JOIN PLPLANTYPE INSERTED_PLPLANTYPE WITH (NOLOCK) ON INSERTED_PLPLANTYPE.PLPLANTYPEID = [inserted].[DEFAULTPLANTYPE]
	LEFT OUTER JOIN PLPLANTYPE DELETED_PLPLANTYPE WITH (NOLOCK) ON DELETED_PLPLANTYPE.PLPLANTYPEID = [deleted].[DEFAULTPLANTYPE]
	WHERE	ISNULL([inserted].[DEFAULTPLANTYPE],'') <> ISNULL([deleted].[DEFAULTPLANTYPE],'')		
	UNION ALL
	SELECT
			[inserted].[PLAPPLICATIONTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Fee Template',
			ISNULL([DELETED_CAFEETEMPLATE].[CAFEETEMPLATENAME],'[none]'),
			ISNULL([INSERTED_CAFEETEMPLATE].[CAFEETEMPLATENAME],'[none]'),
			'Application Type (' + [inserted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			2,
			1,
			[inserted].[APPLICATIONTYPENAME]
	FROM	[deleted]
	INNER JOIN [inserted] ON [deleted].PLAPPLICATIONTYPEID = [inserted].PLAPPLICATIONTYPEID
	LEFT OUTER JOIN CAFEETEMPLATE INSERTED_CAFEETEMPLATE WITH (NOLOCK) ON INSERTED_CAFEETEMPLATE.CAFEETEMPLATEID = [inserted].CAFEETEMPLATEID
	LEFT OUTER JOIN CAFEETEMPLATE DELETED_CAFEETEMPLATE WITH (NOLOCK) ON DELETED_CAFEETEMPLATE.CAFEETEMPLATEID = [deleted].CAFEETEMPLATEID
	WHERE	ISNULL([inserted].[CAFEETEMPLATEID],'') <> ISNULL([deleted].[CAFEETEMPLATEID],'')	
END