CREATE TABLE [dbo].[PLAPPLICATIONTYPE] (
    [PLAPPLICATIONTYPEID]        CHAR (36)      NOT NULL,
    [APPLICATIONTYPENAME]        NVARCHAR (255) NOT NULL,
    [DESCRIPTION]                NVARCHAR (MAX) NULL,
    [CUSTOMFIELDID]              CHAR (36)      NULL,
    [DEFAULTAPPLICATIONSTATUSID] CHAR (36)      NULL,
    [PREFIX]                     NVARCHAR (10)  NULL,
    [ISAUTONUMBER]               BIT            CONSTRAINT [DF_PLApplicationType_IsAutoNumber] DEFAULT ((0)) NOT NULL,
    [DEFAULTPERMITTYPE]          CHAR (36)      NULL,
    [DEFAULTPLANTYPE]            CHAR (36)      NULL,
    [CAFEETEMPLATEID]            CHAR (36)      NULL,
    [LASTCHANGEDBY]              CHAR (36)      NULL,
    [LASTCHANGEDON]              DATETIME       CONSTRAINT [DF_PLApplicationType_LastChangedOn] DEFAULT (getutcdate()) NOT NULL,
    [ROWVERSION]                 INT            CONSTRAINT [DF_PLApplicationType_RowVersion] DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_PLApplicationType] PRIMARY KEY CLUSTERED ([PLAPPLICATIONTYPEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_AppType_PermitType] FOREIGN KEY ([DEFAULTPERMITTYPE]) REFERENCES [dbo].[PMPERMITTYPE] ([PMPERMITTYPEID]),
    CONSTRAINT [FK_AppType_PlanType] FOREIGN KEY ([DEFAULTPLANTYPE]) REFERENCES [dbo].[PLPLANTYPE] ([PLPLANTYPEID]),
    CONSTRAINT [FK_PLApplicationType_CustomFieldLayout] FOREIGN KEY ([CUSTOMFIELDID]) REFERENCES [dbo].[CUSTOMFIELDLAYOUT] ([GCUSTOMFIELDLAYOUTS]),
    CONSTRAINT [FK_PLApplicationType_PLApplicationStatus] FOREIGN KEY ([DEFAULTAPPLICATIONSTATUSID]) REFERENCES [dbo].[PLAPPLICATIONSTATUS] ([PLAPPLICATIONSTATUSID]),
    CONSTRAINT [FK_PLAPPLICATIONTYPES_USERS] FOREIGN KEY ([LASTCHANGEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID]),
    CONSTRAINT [FK_PLAppType_FeeTemplate] FOREIGN KEY ([CAFEETEMPLATEID]) REFERENCES [dbo].[CAFEETEMPLATE] ([CAFEETEMPLATEID])
);


GO

CREATE TRIGGER [TG_PLAPPLICATIONTYPE_DELETE] ON PLAPPLICATIONTYPE
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )

	SELECT
			[deleted].[PLAPPLICATIONTYPEID],
			[deleted].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Application Type Deleted',
			'',
			'',
			'Application Type (' + [deleted].[APPLICATIONTYPENAME] + ')',
			'0C9FECFF-F1B4-4A19-A89B-F7B414BC8D46',
			3,
			1,
			[deleted].[APPLICATIONTYPENAME]
	FROM	[deleted]
END
GO

