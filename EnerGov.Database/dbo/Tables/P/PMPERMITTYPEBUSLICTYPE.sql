CREATE TABLE [dbo].[PMPERMITTYPEBUSLICTYPE] (
    [PMPERMITTYPEBUSLICTYPEID] CHAR (36) NOT NULL,
    [PMPERMITTYPEID]           CHAR (36) NOT NULL,
    [PMPERMITWORKCLASSID]      CHAR (36) NOT NULL,
    [BLLICENSETYPEID]          CHAR (36) NOT NULL,
    [PMPERMITTYPEWORKCLASSID]  CHAR (36) NOT NULL,
    CONSTRAINT [PK_PMPermitTypeBusLicenseType] PRIMARY KEY CLUSTERED ([PMPERMITTYPEBUSLICTYPEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PMTypeBusLicType_ILLicType] FOREIGN KEY ([BLLICENSETYPEID]) REFERENCES [dbo].[BLLICENSETYPE] ([BLLICENSETYPEID]),
    CONSTRAINT [FK_PMTypeBusLicType_PMType] FOREIGN KEY ([PMPERMITTYPEID]) REFERENCES [dbo].[PMPERMITTYPE] ([PMPERMITTYPEID]),
    CONSTRAINT [FK_PMTypeBusLicType_PMWC] FOREIGN KEY ([PMPERMITWORKCLASSID]) REFERENCES [dbo].[PMPERMITWORKCLASS] ([PMPERMITWORKCLASSID]),
    CONSTRAINT [FK_PMTypeBusLicType_PMWCTY] FOREIGN KEY ([PMPERMITTYPEWORKCLASSID]) REFERENCES [dbo].[PMPERMITTYPEWORKCLASS] ([PMPERMITTYPEWORKCLASSID])
);


GO
CREATE NONCLUSTERED INDEX [IX_PMPERMITTYPEBUSLICTYPE_BLLICENSETYPEID]
    ON [dbo].[PMPERMITTYPEBUSLICTYPE]([BLLICENSETYPEID] ASC);


GO

CREATE TRIGGER [dbo].[TG_PMPERMITTYPEBUSLICTYPE_INSERT]
   ON  [dbo].[PMPERMITTYPEBUSLICTYPE]
   AFTER INSERT
AS 
BEGIN	
	SET NOCOUNT ON;	
	INSERT INTO [HISTORYSYSTEMSETUP]
    (
        [ID],
        [ROWVERSION],
        [CHANGEDON],
        [CHANGEDBY],
        [FIELDNAME],
        [OLDVALUE],
        [NEWVALUE],
        [ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
        [PMPERMITTYPE].[PMPERMITTYPEID], 
        [PMPERMITTYPE].[ROWVERSION],
        GETUTCDATE(),
        [PMPERMITTYPE].[LASTCHANGEDBY],	
        'Permit Type Business License Type Added',
        '',
        '',       
		'Permit Type (' + [PMPERMITTYPE].[NAME] + '), Work Class Name (' + [PMPERMITWORKCLASS].[NAME] +'), Business License Type (' + [BLLICENSETYPE].[NAME] +')',
		'4976A4A9-20E8-4B8F-997D-CE244B540104',
		1,
		0,
		[BLLICENSETYPE].[NAME]
    FROM [inserted]
	INNER JOIN [PMPERMITTYPE]  ON [PMPERMITTYPE].[PMPERMITTYPEID] = [inserted].[PMPERMITTYPEID]
	INNER JOIN [BLLICENSETYPE] WITH (NOLOCK) ON [BLLICENSETYPE].[BLLICENSETYPEID] = [inserted].[BLLICENSETYPEID]
	LEFT JOIN [PMPERMITWORKCLASS] WITH (NOLOCK) ON [PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [inserted].[PMPERMITWORKCLASSID]
END
GO
CREATE TRIGGER [dbo].[TG_PMPERMITTYPEBUSLICTYPE_DELETE]  
    ON  [dbo].[PMPERMITTYPEBUSLICTYPE]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[PMPERMITTYPE].[PMPERMITTYPEID],
		[PMPERMITTYPE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
		'Permit Type Business License Type Deleted',
        '',
        '',       
		'Permit Type (' + [PMPERMITTYPE].[NAME] + '), Work Class Name (' + [PMPERMITWORKCLASS].[NAME] +'), Business License Type (' + [BLLICENSETYPE].[NAME] +')',
		'4976A4A9-20E8-4B8F-997D-CE244B540104',
		3,
		0,
		[BLLICENSETYPE].[NAME]
    FROM [deleted]
	INNER JOIN [PMPERMITTYPE]  ON [PMPERMITTYPE].[PMPERMITTYPEID] = [deleted].[PMPERMITTYPEID]
	INNER JOIN [BLLICENSETYPE] WITH (NOLOCK) ON [BLLICENSETYPE].[BLLICENSETYPEID] = [deleted].[BLLICENSETYPEID]
	LEFT JOIN [PMPERMITWORKCLASS] WITH (NOLOCK) ON [PMPERMITWORKCLASS].[PMPERMITWORKCLASSID] = [deleted].[PMPERMITWORKCLASSID]
END