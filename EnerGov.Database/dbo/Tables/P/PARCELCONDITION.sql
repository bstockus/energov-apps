CREATE TABLE [dbo].[PARCELCONDITION] (
    [PARCELCONDITIONID] CHAR (36)   NOT NULL,
    [PARCELID]          CHAR (36)   NOT NULL,
    [CONDITIONID]       CHAR (36)   NOT NULL,
    [CONDITIONSCOPEID]  INT         NOT NULL,
    [CREATEDON]         DATETIME    NULL,
    [ORDER]             VARCHAR (5) NULL,
    [CREATEDBY]         CHAR (36)   NULL,
    CONSTRAINT [PK_ParcelCondition] PRIMARY KEY CLUSTERED ([PARCELCONDITIONID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_ParcelCondition_Condition] FOREIGN KEY ([CONDITIONID]) REFERENCES [dbo].[CONDITION] ([CONDITIONID]),
    CONSTRAINT [FK_ParcelCondition_ConditionScope] FOREIGN KEY ([CONDITIONSCOPEID]) REFERENCES [dbo].[CONDITIONSCOPE] ([CONDITIONSCOPEID]),
    CONSTRAINT [FK_ParcelCondition_Parcel] FOREIGN KEY ([PARCELID]) REFERENCES [dbo].[PARCEL] ([PARCELID]),
    CONSTRAINT [FK_PARCELCONDITION_USERS] FOREIGN KEY ([CREATEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_PARCELCONDITION_PARCELID]
    ON [dbo].[PARCELCONDITION]([PARCELID] ASC) WITH (FILLFACTOR = 90);

