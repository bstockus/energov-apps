CREATE TRIGGER [dbo].[TG_PRPROJECTTYPEPERMITLIMIT_UPDATE] on [dbo].[PRPROJECTTYPEPERMITLIMIT]   
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )

	SELECT 
			[PRPROJECTTYPE].[PRPROJECTTYPEID],
			[PRPROJECTTYPE].[ROWVERSION],
			GETUTCDATE(),
			[PRPROJECTTYPE].[LASTCHANGEDBY],
			'Permit Type',
			[PMPERMITTYPE_DELETED].[NAME],
			[PMPERMITTYPE_INSERTED].[NAME],
			'Project Type (' + [PRPROJECTTYPE].[NAME] + '), Permit Limit (' + [PMPERMITTYPE_INSERTED].[NAME] + ')',
			'3524A7D9-D7E6-44C1-A320-828311F3E4C6',
			2,
			0,
			[PMPERMITTYPE_INSERTED].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[PRPROJECTTYPEPERMITLIMITID] = [inserted].[PRPROJECTTYPEPERMITLIMITID]
			INNER JOIN [PRPROJECTTYPE] ON [PRPROJECTTYPE].[PRPROJECTTYPEID] = [inserted].[PRPROJECTTYPEID]
			INNER JOIN PMPERMITTYPE PMPERMITTYPE_DELETED WITH (NOLOCK) ON [PMPERMITTYPE_DELETED].[PMPERMITTYPEID] = [deleted].[PMPERMITTYPEID]
			INNER JOIN PMPERMITTYPE PMPERMITTYPE_INSERTED WITH (NOLOCK) ON [PMPERMITTYPE_INSERTED].[PMPERMITTYPEID] = [inserted].[PMPERMITTYPEID]
	WHERE	[deleted].[PMPERMITTYPEID] <> [inserted].[PMPERMITTYPEID]
	UNION ALL

	SELECT 
			[PRPROJECTTYPE].[PRPROJECTTYPEID],
			[PRPROJECTTYPE].[ROWVERSION],
			GETUTCDATE(),
			[PRPROJECTTYPE].[LASTCHANGEDBY],
			'Permit Work Class',
			[PMPERMITWORKCLASS_DELETED].[NAME],
			[PMPERMITWORKCLASS_INSERTED].[NAME],
			'Project Type (' + [PRPROJECTTYPE].[NAME] + '), Permit Limit (' + [PMPERMITTYPE].[NAME] + ')',
			'3524A7D9-D7E6-44C1-A320-828311F3E4C6',
			2,
			0,
			[PMPERMITTYPE].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[PRPROJECTTYPEPERMITLIMITID] = [inserted].[PRPROJECTTYPEPERMITLIMITID]
			INNER JOIN [PRPROJECTTYPE] ON [PRPROJECTTYPE].[PRPROJECTTYPEID] = [inserted].[PRPROJECTTYPEID]
			INNER JOIN PMPERMITWORKCLASS PMPERMITWORKCLASS_DELETED WITH (NOLOCK) ON [PMPERMITWORKCLASS_DELETED].[PMPERMITWORKCLASSID] = [deleted].[PMPERMITWORKCLASSID]
			INNER JOIN PMPERMITWORKCLASS PMPERMITWORKCLASS_INSERTED WITH (NOLOCK) ON [PMPERMITWORKCLASS_INSERTED].[PMPERMITWORKCLASSID] = [inserted].[PMPERMITWORKCLASSID]
			INNER JOIN PMPERMITTYPE PMPERMITTYPE WITH (NOLOCK) ON [PMPERMITTYPE].[PMPERMITTYPEID] = [inserted].[PMPERMITTYPEID]
	WHERE	[deleted].[PMPERMITWORKCLASSID] <> [inserted].[PMPERMITWORKCLASSID]
	UNION ALL

	SELECT 
			[PRPROJECTTYPE].[PRPROJECTTYPEID],
			[PRPROJECTTYPE].[ROWVERSION],
			GETUTCDATE(),
			[PRPROJECTTYPE].[LASTCHANGEDBY],
			'Permit Limit',
			CAST([deleted].[PERMITLIMIT] AS NVARCHAR(MAX)),
			CAST([inserted].[PERMITLIMIT] AS NVARCHAR(MAX)),
			'Project Type (' + [PRPROJECTTYPE].[NAME] + '), Permit Limit (' + [PMPERMITTYPE].[NAME] + ')',
			'3524A7D9-D7E6-44C1-A320-828311F3E4C6',
			2,
			0,
			[PMPERMITTYPE].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[PRPROJECTTYPEPERMITLIMITID] = [inserted].[PRPROJECTTYPEPERMITLIMITID]
			INNER JOIN [PRPROJECTTYPE] ON [PRPROJECTTYPE].[PRPROJECTTYPEID] = [inserted].[PRPROJECTTYPEID]
			INNER JOIN PMPERMITTYPE PMPERMITTYPE WITH (NOLOCK) ON [PMPERMITTYPE].[PMPERMITTYPEID] = [inserted].[PMPERMITTYPEID]
	WHERE	[deleted].[PERMITLIMIT] <> [inserted].[PERMITLIMIT]
