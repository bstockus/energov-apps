CREATE TRIGGER [dbo].[TG_PRPROJECTTYPEPERMITLIMIT_DELETE]
   ON  [dbo].[PRPROJECTTYPEPERMITLIMIT]
   AFTER DELETE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
        [PRPROJECTTYPE].[PRPROJECTTYPEID],
        [PRPROJECTTYPE].[ROWVERSION],
        GETUTCDATE(),
        (SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
        'Project Type Permit Limit Deleted',
        '',
        '',
        'Project Type (' + [PRPROJECTTYPE].[NAME] + '), Permit Limit (' + [PMPERMITTYPE].[NAME] + ')',
		'3524A7D9-D7E6-44C1-A320-828311F3E4C6',
		3,
		0,
		[PMPERMITTYPE].[NAME]
    FROM [deleted]
	INNER JOIN [PRPROJECTTYPE] ON [PRPROJECTTYPE].[PRPROJECTTYPEID] = [deleted].[PRPROJECTTYPEID]
	INNER JOIN PMPERMITTYPE PMPERMITTYPE WITH (NOLOCK) ON [PMPERMITTYPE].[PMPERMITTYPEID] = [deleted].[PMPERMITTYPEID]
END
