CREATE TABLE [dbo].[PRPROJECTTYPEPERMITLIMIT] (
    [PRPROJECTTYPEPERMITLIMITID] CHAR (36) NOT NULL,
    [PRPROJECTTYPEID]            CHAR (36) NOT NULL,
    [PMPERMITTYPEID]             CHAR (36) NOT NULL,
    [PMPERMITWORKCLASSID]        CHAR (36) NOT NULL,
    [PERMITLIMIT]                INT       NOT NULL,
    CONSTRAINT [PK_PRProjectTypePermitLimit] PRIMARY KEY CLUSTERED ([PRPROJECTTYPEPERMITLIMITID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_PRProjectTypePermitLimit_PMPermitType] FOREIGN KEY ([PMPERMITTYPEID]) REFERENCES [dbo].[PMPERMITTYPE] ([PMPERMITTYPEID]),
    CONSTRAINT [FK_PRProjectTypePermitLimit_PMPermitWorkClass] FOREIGN KEY ([PMPERMITWORKCLASSID]) REFERENCES [dbo].[PMPERMITWORKCLASS] ([PMPERMITWORKCLASSID]),
    CONSTRAINT [FK_PRProjectTypePermitLimit_PRProjectType] FOREIGN KEY ([PRPROJECTTYPEID]) REFERENCES [dbo].[PRPROJECTTYPE] ([PRPROJECTTYPEID])
);


GO
