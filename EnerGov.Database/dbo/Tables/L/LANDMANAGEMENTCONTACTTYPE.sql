CREATE TABLE [dbo].[LANDMANAGEMENTCONTACTTYPE] (
    [LANDMANAGEMENTCONTACTTYPEID]    CHAR (36)      NOT NULL,
    [NAME]                           NVARCHAR (50)  NOT NULL,
    [DESCRIPTION]                    NVARCHAR (MAX) NULL,
    [LANDMANAGEMENTCONTACTSYSTEMTYP] INT            CONSTRAINT [DF_LandManagementContactType_LandManagementContactSystemTypeID] DEFAULT ((0)) NOT NULL,
