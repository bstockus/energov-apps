    [ISVALIDATEILLICENSE]            BIT            DEFAULT ((0)) NOT NULL,
    [EPORTALSEARCHCRITERIAID]        INT            NULL,
    [LASTCHANGEDBY]                  CHAR (36)      NULL,
    [LASTCHANGEDON]                  DATETIME       CONSTRAINT [DF_LANDMANAGEMENTCONTACTTYPE_LastChangedOn] DEFAULT (getutcdate()) NOT NULL,
