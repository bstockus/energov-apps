    CONSTRAINT [FK_LandManagementContactType_LandManagementContactSystemType] FOREIGN KEY ([LANDMANAGEMENTCONTACTSYSTEMTYP]) REFERENCES [dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP] ([LANDMANAGEMENTCONTACTSYSTEMTYP]),
    CONSTRAINT [FK_LANDTYPE_EPORTALCRITERIA] FOREIGN KEY ([EPORTALSEARCHCRITERIAID]) REFERENCES [dbo].[EPORTALSEARCHCRITERIA] ([EPORTALSEARCHCRITERIAID])
);


GO
