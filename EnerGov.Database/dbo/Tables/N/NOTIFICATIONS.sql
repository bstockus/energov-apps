CREATE TABLE [dbo].[NOTIFICATIONS] (
    [NOTIFICATIONSID] CHAR (36)      NOT NULL,
    [SUBJECT]         NVARCHAR (MAX) NOT NULL,
    [BODY]            NVARCHAR (MAX) NOT NULL,
    [UNIQUERECORDID]  CHAR (36)      NULL,
    [FORMID]          CHAR (36)      NULL,
    CONSTRAINT [PK_Notifications] PRIMARY KEY CLUSTERED ([NOTIFICATIONSID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_Notifications_Forms] FOREIGN KEY ([FORMID]) REFERENCES [dbo].[FORMS] ([SFORMSGUID])
);

