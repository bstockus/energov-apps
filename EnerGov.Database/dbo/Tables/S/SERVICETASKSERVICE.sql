CREATE TABLE [dbo].[SERVICETASKSERVICE] (
    [SERVICETASKSERVICEID] CHAR (36)       NOT NULL,
    [SERVICEID]            CHAR (36)       NOT NULL,
    [SERVICETASKID]        INT             NOT NULL,
    [ISACTIVE]             BIT             NOT NULL,
    [INTERVALINMINUTES]    INT             NOT NULL,
    [STARTTIME]            NVARCHAR (1000) NOT NULL,
    [ENDTIME]              NVARCHAR (1000) NOT NULL,
    CONSTRAINT [PK_SERVICETASKSERVICE] PRIMARY KEY CLUSTERED ([SERVICETASKSERVICEID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_SERVICETASKSERVICE_SRV] FOREIGN KEY ([SERVICEID]) REFERENCES [dbo].[SERVICE] ([SERVICEID]),
    CONSTRAINT [FK_SERVICETASKSERVICE_TSK] FOREIGN KEY ([SERVICETASKID]) REFERENCES [dbo].[SERVICETASK] ([SERVICETASKID])
);


GO
CREATE NONCLUSTERED INDEX [IX_SERVICETASKSERVICE_SRV]
    ON [dbo].[SERVICETASKSERVICE]([SERVICEID] ASC) WITH (FILLFACTOR = 90);

