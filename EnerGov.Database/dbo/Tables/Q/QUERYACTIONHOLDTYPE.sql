CREATE TABLE [dbo].[QUERYACTIONHOLDTYPE] (
    [QUERYACTIONHOLDTYPEID] CHAR (36)      NOT NULL,
    [QUERYACTIONID]         CHAR (36)      NOT NULL,
    [HOLDSETUPID]           CHAR (36)      NOT NULL,
    [COMMENTS]              NVARCHAR (MAX) NULL,
    CONSTRAINT [PK_QUERYACTIONHOLDTYPE] PRIMARY KEY CLUSTERED ([QUERYACTIONHOLDTYPEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_QUERYACTIONHOLDTYPE_HOLDTYPESETUPS] FOREIGN KEY ([HOLDSETUPID]) REFERENCES [dbo].[HOLDTYPESETUPS] ([HOLDSETUPID]),
    CONSTRAINT [FK_QUERYACTIONHOLDTYPE_QUERYACTION] FOREIGN KEY ([QUERYACTIONID]) REFERENCES [dbo].[QUERYACTION] ([QUERYACTIONID])
);


GO
CREATE NONCLUSTERED INDEX [QUERYACTIONHOLDTYPE_HOLDSETUPID]
    ON [dbo].[QUERYACTIONHOLDTYPE]([HOLDSETUPID] ASC);


GO
CREATE NONCLUSTERED INDEX [QUERYACTIONHOLDTYPE_QUERYACTIONID]
    ON [dbo].[QUERYACTIONHOLDTYPE]([QUERYACTIONID] ASC);


GO

CREATE TRIGGER [dbo].[TG_QUERYACTIONHOLDTYPE_UPDATE]
   ON  [dbo].[QUERYACTIONHOLDTYPE]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )	
	SELECT
			[QUERY].[QUERYID], 
			[QUERY].[ROWVERSION],
			GETUTCDATE(),
			[QUERY].[LASTCHANGEDBY],
			'Hold Type',
			ISNULL(DELETEHOLDTYPESETUPS.[NAME],'[none]'),
			ISNULL(INSERTHOLDTYPESETUPS.[NAME],'[none]'),
			'Intelligent Query Action (' + [QUERY].[NAME] + '), Action (' + [QUERYACTION].[ACTIONNAME] +'), Action Type (' + [QUERYACTIONTYPE].[NAME] + ')',
			'C08776DA-046E-4E9B-9DE4-4CC2005A98CD',
			2,
			0,
			[QUERYACTION].[ACTIONNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[QUERYACTIONID] = [inserted].[QUERYACTIONID]
			INNER JOIN [QUERYACTION] ON [QUERYACTION].[QUERYACTIONID] = [inserted].[QUERYACTIONID]
			INNER JOIN [QUERY] ON [QUERY].[QUERYID] = [QUERYACTION].[QUERYID]
			INNER JOIN [QUERYACTIONTYPE] WITH (NOLOCK) ON [QUERYACTIONTYPE].[QUERYACTIONTYPEID] = [QUERYACTION].[QUERYACTIONTYPEID]
			INNER JOIN [HOLDTYPESETUPS] AS DELETEHOLDTYPESETUPS WITH (NOLOCK) ON DELETEHOLDTYPESETUPS.[HOLDSETUPID] = [deleted].[HOLDSETUPID]
			INNER JOIN [HOLDTYPESETUPS] AS INSERTHOLDTYPESETUPS WITH (NOLOCK) ON INSERTHOLDTYPESETUPS.[HOLDSETUPID] = [inserted].[HOLDSETUPID]
	WHERE	[deleted].[HOLDSETUPID] <> [inserted].[HOLDSETUPID]

	UNION ALL
	SELECT
			[QUERY].[QUERYID], 
			[QUERY].[ROWVERSION],
			GETUTCDATE(),
			[QUERY].[LASTCHANGEDBY],
			'Notes',
			ISNULL([deleted].[COMMENTS],'[none]'),
			ISNULL([inserted].[COMMENTS],'[none]'),
			'Intelligent Query Action (' + [QUERY].[NAME] + '), Action (' + [QUERYACTION].[ACTIONNAME] +'), Action Type (' + [QUERYACTIONTYPE].[NAME] + ')',
			'C08776DA-046E-4E9B-9DE4-4CC2005A98CD',
			2,
			0,
			[QUERYACTION].[ACTIONNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[QUERYACTIONID] = [inserted].[QUERYACTIONID]
			INNER JOIN [QUERYACTION] ON [QUERYACTION].[QUERYACTIONID] = [inserted].[QUERYACTIONID]
			INNER JOIN [QUERY] ON [QUERY].[QUERYID] = [QUERYACTION].[QUERYID]
			INNER JOIN [QUERYACTIONTYPE] WITH (NOLOCK) ON [QUERYACTIONTYPE].[QUERYACTIONTYPEID] = [QUERYACTION].[QUERYACTIONTYPEID]
	WHERE	ISNULL([deleted].[COMMENTS],'') <> ISNULL([inserted].[COMMENTS],'')

END