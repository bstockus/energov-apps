CREATE TABLE [dbo].[QUERYACTIONPARAMETER] (
    [QUERYACTIONPARAMETERID] CHAR (36)      NOT NULL,
    [QUERYACTIONID]          CHAR (36)      NOT NULL,
    [PARAMETERNAME]          NVARCHAR (MAX) NOT NULL,
    [PARAMETERVALUE]         NVARCHAR (MAX) NOT NULL,
    [PARAMETERTYPE]          NVARCHAR (MAX) NOT NULL,
    CONSTRAINT [PK_QUERYACTIONPARAMETER] PRIMARY KEY CLUSTERED ([QUERYACTIONPARAMETERID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_QUERYACTIONPARAMETER_QA] FOREIGN KEY ([QUERYACTIONID]) REFERENCES [dbo].[QUERYACTION] ([QUERYACTIONID])
);


GO
CREATE NONCLUSTERED INDEX [QUERYACTIONPARAMETER_QUERYACTIONID]
    ON [dbo].[QUERYACTIONPARAMETER]([QUERYACTIONID] ASC);


GO

CREATE TRIGGER [dbo].[TG_QUERYACTIONPARAMETER_INSERT]
   ON  [dbo].[QUERYACTIONPARAMETER]
   AFTER INSERT
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )	
	SELECT
			[QUERY].[QUERYID], 
			[QUERY].[ROWVERSION],
			GETUTCDATE(),
			[QUERY].[LASTCHANGEDBY],
			'On Fee Template Added',
			'(Default)',
			[CAFEETEMPLATE].[CAFEETEMPLATENAME],
			'Intelligent Query Action (' + [QUERY].[NAME] + '), Action (' + [QUERYACTION].[ACTIONNAME] +'), Action Type (' + [QUERYACTIONTYPE].[NAME] + ')',
			'C08776DA-046E-4E9B-9DE4-4CC2005A98CD',
			1,
			0,
			[QUERYACTION].[ACTIONNAME]
	FROM	[inserted]
			INNER JOIN [QUERYACTION] ON [QUERYACTION].[QUERYACTIONID] = [inserted].[QUERYACTIONID]
			INNER JOIN [QUERY] ON [QUERY].[QUERYID] = [QUERYACTION].[QUERYID]
			INNER JOIN [QUERYACTIONTYPE] WITH (NOLOCK) ON [QUERYACTIONTYPE].[QUERYACTIONTYPEID] = [QUERYACTION].[QUERYACTIONTYPEID]
			INNER JOIN [CAFEETEMPLATE] WITH (NOLOCK) ON [CAFEETEMPLATE].[CAFEETEMPLATEID] = [inserted].[PARAMETERVALUE]
	WHERE [inserted].[PARAMETERNAME] = 'FeeTemplateID'

END
GO

CREATE TRIGGER [dbo].[TG_QUERYACTIONPARAMETER_UPDATE]
   ON  [dbo].[QUERYACTIONPARAMETER]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )	
	SELECT
			[QUERY].[QUERYID], 
			[QUERY].[ROWVERSION],
			GETUTCDATE(),
			[QUERY].[LASTCHANGEDBY],
			CASE  WHEN INSERTCAFEETEMPLATE.[CAFEETEMPLATEID] IS NULL THEN [inserted].[PARAMETERNAME] ELSE 'On Fee Template' END,
			CASE  WHEN DELETECAFEETEMPLATE.[CAFEETEMPLATEID] IS NULL THEN [deleted].[PARAMETERVALUE] ELSE DELETECAFEETEMPLATE.[CAFEETEMPLATENAME] END,
			CASE  WHEN INSERTCAFEETEMPLATE.[CAFEETEMPLATEID] IS NULL THEN [inserted].[PARAMETERVALUE] ELSE INSERTCAFEETEMPLATE.[CAFEETEMPLATENAME] END,
			'Intelligent Query Action (' + [QUERY].[NAME] + '), Action (' + [QUERYACTION].[ACTIONNAME] +'), Action Type (' + [QUERYACTIONTYPE].[NAME] + ')',
			'C08776DA-046E-4E9B-9DE4-4CC2005A98CD',
			2,
			0,
			[QUERYACTION].[ACTIONNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[QUERYACTIONID] = [inserted].[QUERYACTIONID]
			INNER JOIN [QUERYACTION] ON [QUERYACTION].[QUERYACTIONID] = [inserted].[QUERYACTIONID]
			INNER JOIN [QUERY] ON [QUERY].[QUERYID] = [QUERYACTION].[QUERYID]
			INNER JOIN [QUERYACTIONTYPE] WITH (NOLOCK) ON [QUERYACTIONTYPE].[QUERYACTIONTYPEID] = [QUERYACTION].[QUERYACTIONTYPEID]
			LEFT JOIN [CAFEETEMPLATE]  AS DELETECAFEETEMPLATE WITH (NOLOCK) ON DELETECAFEETEMPLATE.[CAFEETEMPLATEID] = [deleted].[PARAMETERVALUE]
			LEFT JOIN [CAFEETEMPLATE]  AS INSERTCAFEETEMPLATE WITH (NOLOCK) ON INSERTCAFEETEMPLATE.[CAFEETEMPLATEID] = [inserted].[PARAMETERVALUE]
	WHERE	[deleted].[PARAMETERVALUE] <> [inserted].[PARAMETERVALUE]

END
GO

CREATE TRIGGER [dbo].[TG_QUERYACTIONPARAMETER_DELETE]
   ON  [dbo].[QUERYACTIONPARAMETER]
   AFTER DELETE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )	
	SELECT
			[QUERY].[QUERYID], 
			[QUERY].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'On Fee Template Deleted',
			[CAFEETEMPLATE].[CAFEETEMPLATENAME],
			'(Default)',
			'Intelligent Query Action (' + [QUERY].[NAME] + '), Action (' + [QUERYACTION].[ACTIONNAME] +'), Action Type (' + [QUERYACTIONTYPE].[NAME] + ')',
			'C08776DA-046E-4E9B-9DE4-4CC2005A98CD',
			3,
			0,
			[QUERYACTION].[ACTIONNAME]
	FROM	[deleted]
			INNER JOIN [QUERYACTION] ON [QUERYACTION].[QUERYACTIONID] = [deleted].[QUERYACTIONID]
			INNER JOIN [QUERY] ON [QUERY].[QUERYID] = [QUERYACTION].[QUERYID]
			INNER JOIN [QUERYACTIONTYPE] WITH (NOLOCK) ON [QUERYACTIONTYPE].[QUERYACTIONTYPEID] = [QUERYACTION].[QUERYACTIONTYPEID]
			INNER JOIN [CAFEETEMPLATE] WITH (NOLOCK) ON [CAFEETEMPLATE].[CAFEETEMPLATEID] = [deleted].[PARAMETERVALUE]
	WHERE 	[deleted].[PARAMETERNAME] = 'FeeTemplateID'

END