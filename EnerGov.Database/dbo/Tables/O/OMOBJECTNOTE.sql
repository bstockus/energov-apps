CREATE TABLE [dbo].[OMOBJECTNOTE] (
    [OMOBJECTNOTEID] CHAR (36)      NOT NULL,
    [OMOBJECTID]     CHAR (36)      NOT NULL,
    [TEXT]           NVARCHAR (MAX) NULL,
    [CREATEDBY]      CHAR (36)      NOT NULL,
    [CREATEDDATE]    DATETIME       NOT NULL,
    [TITLE]          NVARCHAR (50)  CONSTRAINT [DF_OMOBJECTNOTE_TITLE] DEFAULT ('') NULL,
    [PIN]            BIT            CONSTRAINT [DF_OMOBJECTNOTE_PIN] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_OMOBJECTNOTE] PRIMARY KEY CLUSTERED ([OMOBJECTNOTEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_OMOBJECTNOTE_OMOBJECT] FOREIGN KEY ([OMOBJECTID]) REFERENCES [dbo].[OMOBJECT] ([OMOBJECTID]),
    CONSTRAINT [FK_OMOBJECTNOTE_USERS] FOREIGN KEY ([CREATEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [OMOBJECTNOTE_OBJECT]
    ON [dbo].[OMOBJECTNOTE]([OMOBJECTID] ASC);

