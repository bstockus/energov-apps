CREATE TABLE [dbo].[OMOBJECTADDRESS] (
    [OMOBJECTADDRESSID] CHAR (36) NOT NULL,
    [OMOBJECTID]        CHAR (36) NOT NULL,
    [MAILINGADDRESSID]  CHAR (36) NOT NULL,
    [MAIN]              BIT       NOT NULL,
    CONSTRAINT [PK_OMOBJECTADDRESS] PRIMARY KEY CLUSTERED ([OMOBJECTADDRESSID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_OMOBJECTADDRESS_MAILINGADDR] FOREIGN KEY ([MAILINGADDRESSID]) REFERENCES [dbo].[MAILINGADDRESS] ([MAILINGADDRESSID]),
    CONSTRAINT [FK_OMOBJECTADDRESS_OMOBJECT] FOREIGN KEY ([OMOBJECTID]) REFERENCES [dbo].[OMOBJECT] ([OMOBJECTID])
);


GO
CREATE NONCLUSTERED INDEX [OMOBJECTADDRESS_IX_OBJECT]
    ON [dbo].[OMOBJECTADDRESS]([OMOBJECTID] ASC, [MAILINGADDRESSID] ASC);

