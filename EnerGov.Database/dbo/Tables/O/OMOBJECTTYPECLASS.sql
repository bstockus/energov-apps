CREATE TRIGGER [TG_OMOBJECTTYPECLASS_DELETE] ON [OMOBJECTTYPECLASS]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[OMOBJECTTYPE].[OMOBJECTTYPEID],
			[OMOBJECTTYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Object Type Classification Deleted',
			'',
			'',
			'Object Type (' + [OMOBJECTTYPE].[NAME] + '), Classification (' + [OMOBJECTCLASSIFICATION].[NAME] + ')',
			'95F19699-6655-4399-ACC3-CF4EE9043E4F',
			3,
			0,
			[OMOBJECTCLASSIFICATION].[NAME]
	FROM	[deleted]	
	INNER JOIN [OMOBJECTTYPE] ON [deleted].[OMOBJECTTYPEID] = [OMOBJECTTYPE].[OMOBJECTTYPEID]
	INNER JOIN [OMOBJECTCLASSIFICATION] WITH (NOLOCK) ON [deleted].[OMOBJECTCLASSIFICATIONID] = OMOBJECTCLASSIFICATION.[OMOBJECTCLASSIFICATIONID]
END