CREATE TABLE [dbo].[OMOBJECTTYPECLASS] (
    [OMOBJECTTYPECLASSID]      CHAR (36) NOT NULL,
    [OMOBJECTTYPEID]           CHAR (36) NOT NULL,
    [OMOBJECTCLASSIFICATIONID] CHAR (36) NOT NULL,
    [CUSTOMFIELDLAYOUTID]      CHAR (36) NULL,
    CONSTRAINT [PK_OMOBJECTTYPECLASS] PRIMARY KEY CLUSTERED ([OMOBJECTTYPECLASSID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_OBJCLASS_CUSTOMFIELDLAY] FOREIGN KEY ([CUSTOMFIELDLAYOUTID]) REFERENCES [dbo].[CUSTOMFIELDLAYOUT] ([GCUSTOMFIELDLAYOUTS]),
    CONSTRAINT [FK_OMOBJECTTYPECLASS_OMOBJECTC] FOREIGN KEY ([OMOBJECTCLASSIFICATIONID]) REFERENCES [dbo].[OMOBJECTCLASSIFICATION] ([OMOBJECTCLASSIFICATIONID]),
    CONSTRAINT [FK_OMOBJECTTYPECLASS_OMOBJECTT] FOREIGN KEY ([OMOBJECTTYPEID]) REFERENCES [dbo].[OMOBJECTTYPE] ([OMOBJECTTYPEID])
);


GO
CREATE NONCLUSTERED INDEX [OMOBJECTTYPECLASS_IX_OMOBJECTTYPEID]
    ON [dbo].[OMOBJECTTYPECLASS]([OMOBJECTTYPEID] ASC);


GO

CREATE TRIGGER [dbo].[TG_OMOBJECTTYPECLASS_UPDATE] ON  [dbo].[OMOBJECTTYPECLASS]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;

	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT
			[OMOBJECTTYPE].[OMOBJECTTYPEID],
			[OMOBJECTTYPE].[ROWVERSION],
			GETUTCDATE(),
			[OMOBJECTTYPE].[LASTCHANGEDBY],
			'Custom Field',
			ISNULL([CUSTOMFIELDLAYOUT_DELETED].[SNAME],'[none]'),
			ISNULL([CUSTOMFIELDLAYOUT_INSERTED].[SNAME],'[none]'),
			'Object Type (' + [OMOBJECTTYPE].[NAME] + '), Classification (' + [OMOBJECTCLASSIFICATION].[NAME] + ')',
			'95F19699-6655-4399-ACC3-CF4EE9043E4F',
			2,
			0,
			[OMOBJECTCLASSIFICATION].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[OMOBJECTTYPEID] = [inserted].[OMOBJECTTYPEID]
			INNER JOIN [OMOBJECTTYPE] ON [inserted].[OMOBJECTTYPEID] = [OMOBJECTTYPE].[OMOBJECTTYPEID]
			INNER JOIN [OMOBJECTCLASSIFICATION] WITH (NOLOCK) ON [deleted].[OMOBJECTCLASSIFICATIONID] = OMOBJECTCLASSIFICATION.[OMOBJECTCLASSIFICATIONID]
			LEFT JOIN [CUSTOMFIELDLAYOUT] CUSTOMFIELDLAYOUT_DELETED WITH (NOLOCK) ON [CUSTOMFIELDLAYOUT_DELETED].[GCUSTOMFIELDLAYOUTS] = [deleted].[CUSTOMFIELDLAYOUTID]
			LEFT JOIN [CUSTOMFIELDLAYOUT] CUSTOMFIELDLAYOUT_INSERTED WITH (NOLOCK) ON [CUSTOMFIELDLAYOUT_INSERTED].[GCUSTOMFIELDLAYOUTS] = [inserted].[CUSTOMFIELDLAYOUTID]
	WHERE	ISNULL([deleted].[CUSTOMFIELDLAYOUTID], '') <> ISNULL([inserted].[CUSTOMFIELDLAYOUTID], '')
END
GO
