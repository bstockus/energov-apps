CREATE TABLE [dbo].[BLGLOBALENTITYEXTENSIONADDRESS] (
    [BLGLOBALENTITYEXTADDRESSID] CHAR (36) NOT NULL,
    [BLGLOBALENTITYEXTENSIONID]  CHAR (36) NOT NULL,
