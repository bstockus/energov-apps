    [MAIN]                       BIT       DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_BLGlobalEntityExtensionAddress] PRIMARY KEY CLUSTERED ([BLGLOBALENTITYEXTADDRESSID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_BLGlobalEntityExtensionAddress_BLGlobalEntityExtension] FOREIGN KEY ([BLGLOBALENTITYEXTENSIONID]) REFERENCES [dbo].[BLGLOBALENTITYEXTENSION] ([BLGLOBALENTITYEXTENSIONID]),
    CONSTRAINT [FK_BLGlobalEntityExtensionAddress_MailingAddress] FOREIGN KEY ([MAILINGADDRESSID]) REFERENCES [dbo].[MAILINGADDRESS] ([MAILINGADDRESSID])
);


GO
