CREATE TABLE [dbo].[BLTYPECLASSILCLASS] (
    [BLTYPECLASSILCLASSID]      CHAR (36) NOT NULL,
    [BLTYPECLASSILTYPEID]       CHAR (36) NOT NULL,
    [ILLICENSECLASSIFICATIONID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_BLTYPECLASSILCLASS] PRIMARY KEY CLUSTERED ([BLTYPECLASSILCLASSID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_BLTYPECLASSILCLASS_BLTCILCL] FOREIGN KEY ([BLTYPECLASSILTYPEID]) REFERENCES [dbo].[BLTYPECLASSILTYPE] ([BLTYPECLASSILTYPEID]),
    CONSTRAINT [FK_BLTYPECLASSILCLASS_ILCL] FOREIGN KEY ([ILLICENSECLASSIFICATIONID]) REFERENCES [dbo].[ILLICENSECLASSIFICATION] ([ILLICENSECLASSIFICATIONID])
);


GO
CREATE NONCLUSTERED INDEX [IX_BLTYPECLASSILCLASS_BLTYPECLASSILTYPEID]
    ON [dbo].[BLTYPECLASSILCLASS]([BLTYPECLASSILTYPEID] ASC);


GO

CREATE TRIGGER [dbo].[TG_BLTYPECLASSILCLASS_INSERT] ON  [dbo].[BLTYPECLASSILCLASS]
   AFTER INSERT
AS 
BEGIN	
	SET NOCOUNT ON;	
	INSERT INTO [HISTORYSYSTEMSETUP]
    (
        [ID],
        [ROWVERSION],
        [CHANGEDON],
        [CHANGEDBY],
        [FIELDNAME],
        [OLDVALUE],
        [NEWVALUE],
        [ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
        [BLLICENSETYPE].[BLLICENSETYPEID], 
        [BLLICENSETYPE].[ROWVERSION],
        GETUTCDATE(),
        [BLLICENSETYPE].[LASTCHANGEDBY],	
        'Business License Type Professional License Classification Added',
        '',
        '',       
		'Business License Type (' + [BLLICENSETYPE].[NAME] + '), Professional License Classification Name (' + [ILLICENSECLASSIFICATION].[NAME] +')',
		'8C9D2A3A-2F18-45CE-B059-33A98C1EA148',
		1,
		0,
		[ILLICENSECLASSIFICATION].[NAME]
    FROM [inserted]
	INNER JOIN [BLTYPECLASSILTYPE] ON [BLTYPECLASSILTYPE].[BLTYPECLASSILTYPEID] = [inserted].[BLTYPECLASSILTYPEID]	
	INNER JOIN [BLLICENSETYPECLASS] ON [BLLICENSETYPECLASS].[BLLICENSETYPECLASSID] = [BLTYPECLASSILTYPE].[BLLICENSETYPECLASSID]
	INNER JOIN [BLLICENSETYPE] ON [BLLICENSETYPE].[BLLICENSETYPEID] = [BLLICENSETYPECLASS].[BLLICENSETYPEID]
	INNER JOIN [ILLICENSECLASSIFICATION] WITH (NOLOCK) ON [ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID] = [inserted].[ILLICENSECLASSIFICATIONID]
END
GO

CREATE TRIGGER [dbo].[TG_BLTYPECLASSILCLASS_DELETE]  ON  [dbo].[BLTYPECLASSILCLASS]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[BLLICENSETYPE].[BLLICENSETYPEID],
		[BLLICENSETYPE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
		'Business License Type Professional License Classification Deleted',
        '',
        '',       
		'Business License Type (' + [BLLICENSETYPE].[NAME] + '), Professional License Classification Name (' + [ILLICENSECLASSIFICATION].[NAME] +')',
		'8C9D2A3A-2F18-45CE-B059-33A98C1EA148',
		3,
		0,
		[ILLICENSECLASSIFICATION].[NAME]
    FROM [deleted]
	INNER JOIN [BLTYPECLASSILTYPE] ON [BLTYPECLASSILTYPE].[BLTYPECLASSILTYPEID] = [deleted].[BLTYPECLASSILTYPEID]	
	INNER JOIN [BLLICENSETYPECLASS] ON [BLLICENSETYPECLASS].[BLLICENSETYPECLASSID] = [BLTYPECLASSILTYPE].[BLLICENSETYPECLASSID]
	INNER JOIN [BLLICENSETYPE] ON [BLLICENSETYPE].[BLLICENSETYPEID] = [BLLICENSETYPECLASS].[BLLICENSETYPEID]
	INNER JOIN [ILLICENSECLASSIFICATION] WITH (NOLOCK) ON [ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID] = [deleted].[ILLICENSECLASSIFICATIONID]
END