CREATE TABLE [dbo].[BUCONTACT] (
    [BUCONTACTID]   CHAR (36) NOT NULL,
    [BUSERVICEID]   CHAR (36) NOT NULL,
    [CONTACTID]     CHAR (36) NOT NULL,
    [CONTACTTYPEID] CHAR (36) NOT NULL,
    [ISBILLING]     BIT       NOT NULL,
    CONSTRAINT [PK_BUCONTACT] PRIMARY KEY CLUSTERED ([BUCONTACTID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_BUCONTACT_BUSERVICE] FOREIGN KEY ([BUSERVICEID]) REFERENCES [dbo].[BUSERVICE] ([BUSERVICEID]),
    CONSTRAINT [FK_BUCONTACT_GLOBALENTITY] FOREIGN KEY ([CONTACTID]) REFERENCES [dbo].[GLOBALENTITY] ([GLOBALENTITYID])
);

