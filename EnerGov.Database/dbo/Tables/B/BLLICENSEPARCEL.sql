CREATE TABLE [dbo].[BLLICENSEPARCEL] (
    [BLLICENSEPARCELID] CHAR (36) NOT NULL,
    [BLLICENSEID]       CHAR (36) NOT NULL,
    [PARCELID]          CHAR (36) NOT NULL,
    [MAIN]              BIT       NOT NULL,
    CONSTRAINT [PK_BLLicenseParcel] PRIMARY KEY CLUSTERED ([BLLICENSEPARCELID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_BLLicenseParcel_BLLicense] FOREIGN KEY ([BLLICENSEID]) REFERENCES [dbo].[BLLICENSE] ([BLLICENSEID]),
    CONSTRAINT [FK_BLLicenseParcel_Parcel] FOREIGN KEY ([PARCELID]) REFERENCES [dbo].[PARCEL] ([PARCELID])
);


GO
CREATE NONCLUSTERED INDEX [IX_BLLICENSEPARCEL_LICENSE]
    ON [dbo].[BLLICENSEPARCEL]([BLLICENSEID] ASC, [PARCELID] ASC, [MAIN] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [IMPORT1]
    ON [dbo].[BLLICENSEPARCEL]([BLLICENSEID] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [IMPORT2]
    ON [dbo].[BLLICENSEPARCEL]([PARCELID] ASC) WITH (FILLFACTOR = 80);

