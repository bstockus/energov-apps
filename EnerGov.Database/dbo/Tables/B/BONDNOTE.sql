CREATE TABLE [dbo].[BONDNOTE] (
    [BONDNOTEID] CHAR (36)      NOT NULL,
    [BONDID]     CHAR (36)      NOT NULL,
    [TEXT]       NVARCHAR (MAX) NOT NULL,
    [CREATEDBY]  CHAR (36)      NULL,
    [CREATEDATE] DATETIME       NULL,
    [TITLE]      VARCHAR (50)   CONSTRAINT [DF_BONDNOTE_TITLE] DEFAULT ('') NULL,
    [PIN]        BIT            CONSTRAINT [DF_BONDNOTE_PIN] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_BondNote] PRIMARY KEY CLUSTERED ([BONDNOTEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_BondNote_Bond] FOREIGN KEY ([BONDID]) REFERENCES [dbo].[BOND] ([BONDID]),
    CONSTRAINT [FK_BondNote_Users] FOREIGN KEY ([CREATEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);

