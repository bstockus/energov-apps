CREATE TABLE [dbo].[BUSERVICE] (
    [BUSERVICEID]   CHAR (36)     NOT NULL,
    [BATCHNUMBER]   NVARCHAR (50) NOT NULL,
    [ENTITY]        INT           NOT NULL,
    [SERVICESTATUS] INT           NOT NULL,
    [CREATEDATE]    DATETIME      NOT NULL,
    [CREATEDBY]     CHAR (36)     NOT NULL,
    [SCHEDULEDDATE] DATETIME      NULL,
    CONSTRAINT [PK_BUSERVICE] PRIMARY KEY CLUSTERED ([BUSERVICEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_BUSERVICE_USERS] FOREIGN KEY ([CREATEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);

