CREATE TABLE [dbo].[BLEXTBOND] (
    [BLEXTBONDID]               CHAR (36) NOT NULL,
    [BLGLOBALENTITYEXTENSIONID] CHAR (36) NOT NULL,
    [BONDID]                    CHAR (36) NOT NULL,
    [CREATEDBY]                 CHAR (36) NULL,
    [CREATEDATE]                DATETIME  NULL,
    CONSTRAINT [PK_BLEXTBOND] PRIMARY KEY CLUSTERED ([BLEXTBONDID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_BLEXTBOND_BOND] FOREIGN KEY ([BONDID]) REFERENCES [dbo].[BOND] ([BONDID]),
    CONSTRAINT [FK_BLEXTBOND_ILLICENSE] FOREIGN KEY ([BLGLOBALENTITYEXTENSIONID]) REFERENCES [dbo].[BLGLOBALENTITYEXTENSION] ([BLGLOBALENTITYEXTENSIONID]),
    CONSTRAINT [FK_BLEXTBOND_USERS] FOREIGN KEY ([CREATEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [NCIDX1_BLEXTBOND_BONDID]
    ON [dbo].[BLEXTBOND]([BONDID] ASC)
    INCLUDE([BLGLOBALENTITYEXTENSIONID]);

