CREATE TABLE [dbo].[BLLICENSECONTACT] (
    [BLLICENSECONTACTID] CHAR (36) NOT NULL,
    [BLLICENSEID]        CHAR (36) NOT NULL,
    [GLOBALENTITYID]     CHAR (36) NULL,
    [BLCONTACTTYPEID]    CHAR (36) NOT NULL,
    [ISBILLING]          BIT       NOT NULL,
    CONSTRAINT [PK_BLLicenseContact] PRIMARY KEY CLUSTERED ([BLLICENSECONTACTID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_BLLicContact_BLType] FOREIGN KEY ([BLCONTACTTYPEID]) REFERENCES [dbo].[BLCONTACTTYPE] ([BLCONTACTTYPEID]),
    CONSTRAINT [FK_BLLicContact_GlobalEnt] FOREIGN KEY ([GLOBALENTITYID]) REFERENCES [dbo].[GLOBALENTITY] ([GLOBALENTITYID]),
    CONSTRAINT [FK_BLLicenseContact_BLLicense] FOREIGN KEY ([BLLICENSEID]) REFERENCES [dbo].[BLLICENSE] ([BLLICENSEID])
);


GO
CREATE NONCLUSTERED INDEX [BLLICENSECONTACT_ENTITY]
    ON [dbo].[BLLICENSECONTACT]([GLOBALENTITYID] ASC, [ISBILLING] ASC, [BLLICENSEID] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [IX_BLLICENSECONTACT_LIC]
    ON [dbo].[BLLICENSECONTACT]([BLLICENSEID] ASC, [GLOBALENTITYID] ASC, [BLCONTACTTYPEID] ASC, [ISBILLING] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [NCIDX_BLLICENSECONTACT_GLOBALENTITYID]
    ON [dbo].[BLLICENSECONTACT]([GLOBALENTITYID] ASC) WITH (FILLFACTOR = 90, PAD_INDEX = ON);

