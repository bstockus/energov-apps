CREATE TABLE [dbo].[BLCONTACTTYPEREF] (
    [CONTACTTYPEEXTID]           CHAR (36) NOT NULL,
    [BLCONTACTTYPEID]            CHAR (36) NOT NULL,
    [CONTACTTYPEGROUP]           INT       NULL,
    [ISREQUIRED]                 BIT       CONSTRAINT [DF_BLCONTACTTYPEREF_REQ] DEFAULT ((0)) NOT NULL,
    [OBJCLASSID]                 CHAR (36) NULL,
    [OBJTYPEID]                  CHAR (36) NULL,
    [OBJMODULEID]                INT       NOT NULL,
    [ISDEFAULTONLINECONTACTTYPE] BIT       DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_BLCONTACTTYPEREF] PRIMARY KEY CLUSTERED ([CONTACTTYPEEXTID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_BLCONTACTTYPEREF_COTYPE] FOREIGN KEY ([BLCONTACTTYPEID]) REFERENCES [dbo].[BLCONTACTTYPE] ([BLCONTACTTYPEID])
);


GO
CREATE NONCLUSTERED INDEX [BLCONTACTTYPEREF_IX_OBJTYPEID]
    ON [dbo].[BLCONTACTTYPEREF]([OBJTYPEID] ASC);


