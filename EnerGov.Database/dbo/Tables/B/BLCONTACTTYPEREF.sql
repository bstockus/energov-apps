CREATE TRIGGER [dbo].[TG_BLCONTACTTYPEREF_DELETE] ON [dbo].[BLCONTACTTYPEREF]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )

	SELECT
			[BLEXTCOMPANYTYPE].[BLEXTCOMPANYTYPEID],
			[BLEXTCOMPANYTYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Business Company Type Contact Type Deleted',
			'',
			'',
			'Business Company Type (' + [BLEXTCOMPANYTYPE].[NAME] + '),  Contact Type (' + [BLCONTACTTYPE].[NAME] + ')',
			'AD2A1266-BD91-418D-94E0-D99B930A57D0',
			3,
			0,
			[BLCONTACTTYPE].[NAME]
	FROM	[deleted]
			JOIN [BLCONTACTTYPE] ON [BLCONTACTTYPE].[BLCONTACTTYPEID] = [deleted].[BLCONTACTTYPEID]
			JOIN [BLEXTCOMPANYTYPE] ON [BLEXTCOMPANYTYPE].[BLEXTCOMPANYTYPEID] = [deleted].[OBJTYPEID]
	UNION ALL
	SELECT DISTINCT
			[BLLICENSETYPE].[BLLICENSETYPEID],
			[BLLICENSETYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Business License Type Contact Type Deleted',
			'',
			'',
			'Business License Type (' + [BLLICENSETYPE].[NAME] + '), Contact Type (' + [BLCONTACTTYPE].[NAME] + ')',
			'8C9D2A3A-2F18-45CE-B059-33A98C1EA148',
			3,
			0,
			[BLCONTACTTYPE].[NAME]
	FROM	[deleted]	
		LEFT JOIN [dbo].[BLLICENSETYPECLASS] ON [deleted].[OBJTYPEID] = [BLLICENSETYPEID] 
		INNER JOIN [dbo].[BLLICENSETYPE] ON [BLLICENSETYPE].[BLLICENSETYPEID] = [BLLICENSETYPECLASS].[BLLICENSETYPEID]
		LEFT JOIN [dbo].[BLLICENSECLASS] ON [deleted].[OBJCLASSID] = [BLLICENSECLASS].[BLLICENSECLASSID]
		INNER JOIN [dbo].[BLCONTACTTYPE] WITH (NOLOCK) ON [deleted].[BLCONTACTTYPEID] = [BLCONTACTTYPE].[BLCONTACTTYPEID]
	UNION ALL
	SELECT DISTINCT
			[ILLICENSETYPE].[ILLICENSETYPEID],
			[ILLICENSETYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Professional License Type Contact Type Deleted',
			'',
			'',
			'Professional License Type (' + [ILLICENSETYPE].[NAME] + '), Contact Type (' + [BLCONTACTTYPE].[NAME] + ')',
			'67978C16-D720-4F8E-8120-E00FBB732A77',
			3,
			0,
			[BLCONTACTTYPE].[NAME]
	FROM	[deleted]	
		INNER JOIN [dbo].[ILLICENSETYPE] ON [ILLICENSETYPE].[ILLICENSETYPEID] = [deleted].[OBJTYPEID]
		LEFT JOIN [dbo].[ILLICENSECLASSIFICATION] ON [deleted].[OBJCLASSID] = [ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID]
		INNER JOIN [dbo].[BLCONTACTTYPE] WITH (NOLOCK) ON [deleted].[BLCONTACTTYPEID] = [BLCONTACTTYPE].[BLCONTACTTYPEID]
