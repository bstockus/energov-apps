CREATE TABLE [dbo].[BURECORD] (
    [BURECORDID]    CHAR (36)      NOT NULL,
    [BUSERVICEID]   CHAR (36)      NOT NULL,
    [ENTITYID]      CHAR (36)      NOT NULL,
    [ENTITYNUMBER]  NVARCHAR (100) NOT NULL,
    [PROCESSEDDATE] DATETIME       NULL,
    [RESULTLOG]     NVARCHAR (MAX) NULL,
    [SUCCESSFLAG]   BIT            NULL,
    [SENTTOBUS]     BIT            NULL,
    CONSTRAINT [PK_BURECORD] PRIMARY KEY CLUSTERED ([BURECORDID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_BURECORD_BUSERVICE] FOREIGN KEY ([BUSERVICEID]) REFERENCES [dbo].[BUSERVICE] ([BUSERVICEID])
);


GO
CREATE NONCLUSTERED INDEX [IDX_BURECORD_BUSERVICEID]
    ON [dbo].[BURECORD]([BUSERVICEID] ASC)
    INCLUDE([PROCESSEDDATE]);

