CREATE TRIGGER [dbo].[TG_GISLLTRANSLATION_UPDATE] ON  [dbo].[GISLLTRANSLATION]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	
	SELECT 
			[GISLLDATASOURCE].[GISLLDATASOURCEID],
			[GISLLDATASOURCE].[ROWVERSION],
			GETUTCDATE(),
			[GISLLDATASOURCE].[LASTCHANGEDBY],
			'EnerGov Value',
			ISNULL([deleted].[ENERGOVVALUE],'[none]'),
			ISNULL([inserted].[ENERGOVVALUE],'[none]'),
			'Live Link (' + ISNULL([GISLLDATASOURCE].[ARCGISLAYER],'[none]') + '), Translation (' + ISNULL([inserted].[ENERGOVVALUE],'[none]') + ')',
			'9B9CF7DC-8C6A-444A-B5B7-F50664942297',
			2,
			0,
			ISNULL([inserted].[ENERGOVVALUE],'[none]')
	FROM	[deleted] JOIN [inserted] ON [deleted].[GISLLTRANSLATIONID] = [inserted].[GISLLTRANSLATIONID]
			JOIN	[GISLLMAPPING] AS [GISLLMAPPING] WITH (NOLOCK) ON [GISLLMAPPING].[GISLLMAPPINGID] = [inserted].[GISLLMAPPING]
			JOIN	[GISLLDATASOURCE] AS [GISLLDATASOURCE] WITH (NOLOCK) ON [GISLLDATASOURCE].[GISLLDATASOURCEID] = [GISLLMAPPING].[GISLLDATASOURCE]			
	WHERE	ISNULL([deleted].[ENERGOVVALUE],'') <> ISNULL([inserted].[ENERGOVVALUE],'')
	UNION ALL

	SELECT 
			[GISLLDATASOURCE].[GISLLDATASOURCEID],
			[GISLLDATASOURCE].[ROWVERSION],
			GETUTCDATE(),
			[GISLLDATASOURCE].[LASTCHANGEDBY],
			'External Value',
			ISNULL([deleted].[EXTERNALVALUE],'[none]'),
			ISNULL([inserted].[EXTERNALVALUE],'[none]'),
			'Live Link (' + ISNULL([GISLLDATASOURCE].[ARCGISLAYER],'[none]') + '), Translation (' + ISNULL([inserted].[ENERGOVVALUE],'[none]') + ')',
			'9B9CF7DC-8C6A-444A-B5B7-F50664942297',
			2,
			0,
			ISNULL([inserted].[ENERGOVVALUE],'[none]')
	FROM	[deleted] JOIN [inserted] ON [deleted].[GISLLTRANSLATIONID] = [inserted].[GISLLTRANSLATIONID]
			JOIN	[GISLLMAPPING] AS [GISLLMAPPING] WITH (NOLOCK) ON [GISLLMAPPING].[GISLLMAPPINGID] = [inserted].[GISLLMAPPING]
			JOIN	[GISLLDATASOURCE] AS [GISLLDATASOURCE] WITH (NOLOCK) ON [GISLLDATASOURCE].[GISLLDATASOURCEID] = [GISLLMAPPING].[GISLLDATASOURCE]			
	WHERE	ISNULL([deleted].[EXTERNALVALUE],'') <> ISNULL([inserted].[EXTERNALVALUE],'')
END