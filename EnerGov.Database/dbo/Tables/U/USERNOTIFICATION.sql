CREATE TABLE [dbo].[USERNOTIFICATION] (
    [USERID]          CHAR (36) NOT NULL,
    [NOTIFICATIONSID] CHAR (36) NOT NULL,
    [DATECREATED]     DATETIME  NOT NULL,
    [DATEREAD]        DATETIME  NULL,
    CONSTRAINT [PK_UserNotification] PRIMARY KEY CLUSTERED ([USERID] ASC, [NOTIFICATIONSID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_UserNotification_Notificati] FOREIGN KEY ([NOTIFICATIONSID]) REFERENCES [dbo].[NOTIFICATIONS] ([NOTIFICATIONSID]),
    CONSTRAINT [FK_UserNotification_Users] FOREIGN KEY ([USERID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_USERNOTIFICATION_DATEREAD]
    ON [dbo].[USERNOTIFICATION]([USERID] ASC, [DATEREAD] ASC);

