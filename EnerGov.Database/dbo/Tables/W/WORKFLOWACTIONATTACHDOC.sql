CREATE TABLE [dbo].[WORKFLOWACTIONATTACHDOC] (
    [WORKFLOWACTIONATTACHDOCID] CHAR (36)      NOT NULL,
    [WORKFLOWACTIONID]          CHAR (36)      NOT NULL,
    [ATTACHMENTGROUPID]         CHAR (36)      NULL,
    [NOTES]                     NVARCHAR (MAX) NOT NULL,
    [RPTREPORTID]               CHAR (36)      NOT NULL,
    CONSTRAINT [PK_WORKFLOWACTIONATTACHDOC] PRIMARY KEY CLUSTERED ([WORKFLOWACTIONATTACHDOCID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_WORKFLOWACTIONATTACHDOC_AG] FOREIGN KEY ([ATTACHMENTGROUPID]) REFERENCES [dbo].[ATTACHMENTGROUP] ([ATTACHMENTGROUPID]),
    CONSTRAINT [FK_WORKFLOWACTIONATTACHDOC_RP] FOREIGN KEY ([RPTREPORTID]) REFERENCES [dbo].[RPTREPORT] ([RPTREPORTID]),
    CONSTRAINT [FK_WORKFLOWACTIONATTACHDOC_WA] FOREIGN KEY ([WORKFLOWACTIONID]) REFERENCES [dbo].[WORKFLOWACTION] ([WORKFLOWACTIONID])
);


GO
CREATE NONCLUSTERED INDEX [WORKFLOWACTIONATTACHDOC_ATTACHMENTGROUPID]
    ON [dbo].[WORKFLOWACTIONATTACHDOC]([ATTACHMENTGROUPID] ASC);


GO
CREATE NONCLUSTERED INDEX [WORKFLOWACTIONATTACHDOC_RPTREPORTID]
    ON [dbo].[WORKFLOWACTIONATTACHDOC]([RPTREPORTID] ASC);


GO
CREATE NONCLUSTERED INDEX [WORKFLOWACTIONATTACHDOC_WORKFLOWACTIONID]
    ON [dbo].[WORKFLOWACTIONATTACHDOC]([WORKFLOWACTIONID] ASC);


GO

CREATE TRIGGER [dbo].[TG_WORKFLOWACTIONATTACHDOC_UPDATE]
   ON  [dbo].[WORKFLOWACTIONATTACHDOC]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )	
	SELECT
			[WORKFLOW].[WORKFLOWID], 
			[WORKFLOW].[ROWVERSION],
			GETUTCDATE(),
			[WORKFLOW].[LASTCHANGEDBY],
			'Attachment Group',
			ISNULL(DELETEATTACHMENTGROUP.[NAME],'[none]'),
			ISNULL(INSERTATTACHMENTGROUP.[NAME],'[none]'),
			'Intelligent Object (' + [WORKFLOW].[WORKFLOWNAME] + '), Action (' + [WORKFLOWACTION].[ACTIONNAME] +'), Action Type (' + [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPENAME] + ')',
			'F053A756-19BE-4A42-A70D-185D5B01C31A',
			2,
			0,
			[WORKFLOWACTION].[ACTIONNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOWACTION] ON [WORKFLOWACTION].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOW] ON [WORKFLOW].[WORKFLOWID] = [WORKFLOWACTION].[WORKFLOWID]
			INNER JOIN [WORKFLOWACTIONTYPE] WITH (NOLOCK) ON [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPEID] = [WORKFLOWACTION].[WORKFLOWACTIONTYPEID]
			LEFT JOIN [ATTACHMENTGROUP] AS DELETEATTACHMENTGROUP WITH (NOLOCK) ON DELETEATTACHMENTGROUP.[ATTACHMENTGROUPID] = [deleted].[ATTACHMENTGROUPID]
			LEFT JOIN [ATTACHMENTGROUP] AS INSERTATTACHMENTGROUP WITH (NOLOCK) ON INSERTATTACHMENTGROUP.[ATTACHMENTGROUPID] = [inserted].[ATTACHMENTGROUPID]
	WHERE	ISNULL([deleted].[ATTACHMENTGROUPID],'') <> ISNULL([inserted].[ATTACHMENTGROUPID],'')

	UNION ALL
	SELECT
			[WORKFLOW].[WORKFLOWID], 
			[WORKFLOW].[ROWVERSION],
			GETUTCDATE(),
			[WORKFLOW].[LASTCHANGEDBY],
			'Notes',
			[deleted].[NOTES],
			[inserted].[NOTES],
			'Intelligent Object (' + [WORKFLOW].[WORKFLOWNAME] + '), Action (' + [WORKFLOWACTION].[ACTIONNAME] +'), Action Type (' + [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPENAME] + ')',
			'F053A756-19BE-4A42-A70D-185D5B01C31A',
			2,
			0,
			[WORKFLOWACTION].[ACTIONNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOWACTION] ON [WORKFLOWACTION].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOW] ON [WORKFLOW].[WORKFLOWID] = [WORKFLOWACTION].[WORKFLOWID]
			INNER JOIN [WORKFLOWACTIONTYPE] WITH (NOLOCK) ON [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPEID] = [WORKFLOWACTION].[WORKFLOWACTIONTYPEID]
	WHERE	[deleted].[NOTES] <> [inserted].[NOTES]

	UNION ALL
	SELECT
			[WORKFLOW].[WORKFLOWID], 
			[WORKFLOW].[ROWVERSION],
			GETUTCDATE(),
			[WORKFLOW].[LASTCHANGEDBY],
			'Attachment',
			ISNULL(DELETERPTREPORT.[FRIENDLYNAME],'[none]'),
			ISNULL(INSERTRPTREPORT.[FRIENDLYNAME],'[none]'),
			'Intelligent Object (' + [WORKFLOW].[WORKFLOWNAME] + '), Action (' + [WORKFLOWACTION].[ACTIONNAME] +'), Action Type (' + [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPENAME] + ')',
			'F053A756-19BE-4A42-A70D-185D5B01C31A',
			2,
			0,
			[WORKFLOWACTION].[ACTIONNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOWACTION] ON [WORKFLOWACTION].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOW] ON [WORKFLOW].[WORKFLOWID] = [WORKFLOWACTION].[WORKFLOWID]
			INNER JOIN [WORKFLOWACTIONTYPE] WITH (NOLOCK) ON [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPEID] = [WORKFLOWACTION].[WORKFLOWACTIONTYPEID]
			INNER JOIN [RPTREPORT] AS DELETERPTREPORT WITH (NOLOCK) ON DELETERPTREPORT.[RPTREPORTID] = [deleted].[RPTREPORTID]
			INNER JOIN [RPTREPORT] AS INSERTRPTREPORT WITH (NOLOCK) ON INSERTRPTREPORT.[RPTREPORTID] = [inserted].[RPTREPORTID]
	WHERE	[deleted].[RPTREPORTID] <> [inserted].[RPTREPORTID]
END