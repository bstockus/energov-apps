CREATE TABLE [dbo].[WORKFLOWACTIONSOCIALNETWORK] (
    [WORKFLOWACTIONSOCIALNETWORKID] VARCHAR (36)   NOT NULL,
    [WORKFLOWACTIONID]              CHAR (36)      NOT NULL,
    [TEXT]                          NVARCHAR (MAX) NOT NULL,
    CONSTRAINT [PK_WorkflowActionSocialNetwork] PRIMARY KEY CLUSTERED ([WORKFLOWACTIONSOCIALNETWORKID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_WSocialNetwork_Action] FOREIGN KEY ([WORKFLOWACTIONID]) REFERENCES [dbo].[WORKFLOWACTION] ([WORKFLOWACTIONID])
);


GO
CREATE NONCLUSTERED INDEX [WORKFLOWACTIONSOCIALNETWORK_WORKFLOWACTIONID]
    ON [dbo].[WORKFLOWACTIONSOCIALNETWORK]([WORKFLOWACTIONID] ASC);


GO

CREATE TRIGGER [dbo].[TG_WORKFLOWACTIONSOCIALNETWORK_UPDATE]
   ON  [dbo].[WORKFLOWACTIONSOCIALNETWORK]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )	
	SELECT
			[WORKFLOW].[WORKFLOWID], 
			[WORKFLOW].[ROWVERSION],
			GETUTCDATE(),
			[WORKFLOW].[LASTCHANGEDBY],
			'Body',
			[deleted].[TEXT],
			[inserted].[TEXT],
			'Intelligent Object (' + [WORKFLOW].[WORKFLOWNAME] + '), Action (' + [WORKFLOWACTION].[ACTIONNAME] +'), Action Type (' + [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPENAME] + ')',
			'F053A756-19BE-4A42-A70D-185D5B01C31A',
			2,
			0,
			[WORKFLOWACTION].[ACTIONNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOWACTION] ON [WORKFLOWACTION].[WORKFLOWACTIONID] = [inserted].[WORKFLOWACTIONID]
			INNER JOIN [WORKFLOW] ON [WORKFLOW].[WORKFLOWID] = [WORKFLOWACTION].[WORKFLOWID]
			INNER JOIN [WORKFLOWACTIONTYPE] WITH (NOLOCK) ON [WORKFLOWACTIONTYPE].[WORKFLOWACTIONTYPEID] = [WORKFLOWACTION].[WORKFLOWACTIONTYPEID]
	WHERE	[deleted].[TEXT] <> [inserted].[TEXT]
END