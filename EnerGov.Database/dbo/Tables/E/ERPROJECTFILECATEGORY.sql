			'Name',
			[deleted].[NAME],
			[inserted].[NAME],
			'Project File Category (' + [inserted].[NAME] + ')',
			'386549BC-81BD-4C29-84BF-E5E161547688',
			2,
			1,
			[inserted].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[ERPROJECTFILECATEGORYID] = [inserted].[ERPROJECTFILECATEGORYID]
	WHERE	[deleted].[NAME] <> [inserted].[NAME]	
	UNION ALL
	SELECT
			[inserted].[ERPROJECTFILECATEGORYID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Instructions',
			ISNULL([deleted].[DESCRIPTION],'[none]'),
			ISNULL([inserted].[DESCRIPTION],'[none]'),
			'Project File Category (' + [inserted].[NAME] + ')',
			'386549BC-81BD-4C29-84BF-E5E161547688',
			2,
			1,
			[inserted].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[ERPROJECTFILECATEGORYID] = [inserted].[ERPROJECTFILECATEGORYID]
	WHERE	ISNULL([deleted].[DESCRIPTION], '') <> ISNULL([inserted].[DESCRIPTION], '')
	UNION ALL
	SELECT
			[inserted].[ERPROJECTFILECATEGORYID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
