CREATE TRIGGER [TG_ERPROJECTFILECATEGORY_DELETE] ON [ERPROJECTFILECATEGORY]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )

	SELECT
			[deleted].[ERPROJECTFILECATEGORYID],
			[deleted].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Project File Category Deleted',
			'',
			'',
			'Project File Category (' + [deleted].[NAME] + ')',
			'386549BC-81BD-4C29-84BF-E5E161547688',
			3,
			1,
			[deleted].[NAME]
	FROM	[deleted]
END
