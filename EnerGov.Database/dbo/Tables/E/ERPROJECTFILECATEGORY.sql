			ISNULL([deleted].[ALLOWEDFILETYPES],'[none]'),
			ISNULL([inserted].[ALLOWEDFILETYPES],'[none]'),
			'Project File Category (' + [inserted].[NAME] + ')',
			'386549BC-81BD-4C29-84BF-E5E161547688',
			2,
			1,
			[inserted].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[ERPROJECTFILECATEGORYID] = [inserted].[ERPROJECTFILECATEGORYID]
	WHERE	ISNULL([deleted].[ALLOWEDFILETYPES], '') <> ISNULL([inserted].[ALLOWEDFILETYPES], '')
	UNION ALL
	SELECT
