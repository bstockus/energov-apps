    CONSTRAINT [FK_ERPROJECTFILECATEGORY_FILENAMEPREFIX] FOREIGN KEY ([FILENAMEPREFIX]) REFERENCES [dbo].[ERPROJECTFILENAMINGCONVENTION] ([ERPROJECTFILENAMINGCONVENTIONID]),
    CONSTRAINT [FK_ERPROJECTFILECATEGORY_FILENAMESUFFIX] FOREIGN KEY ([FILENAMESUFFIX]) REFERENCES [dbo].[ERPROJECTFILENAMINGCONVENTION] ([ERPROJECTFILENAMINGCONVENTIONID])
);


GO
