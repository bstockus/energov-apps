CREATE TABLE [dbo].[ERPROJECTFILECATEGORY] (
    [ERPROJECTFILECATEGORYID] CHAR (36)      NOT NULL,
    [NAME]                    NVARCHAR (50)  NOT NULL,
    [DESCRIPTION]             NVARCHAR (MAX) NULL,
    [ALLOWEDFILETYPES]        NVARCHAR (MAX) NULL,
    [ATTACHMENTGROUPID]       CHAR (36)      NULL,
    [LASTCHANGEDBY]           CHAR (36)      NULL,
    [LASTCHANGEDON]           DATETIME       CONSTRAINT [DF_ERPROJECTFILECATEGORY_LastChangedOn] DEFAULT (getutcdate()) NOT NULL,
    [ROWVERSION]              INT            CONSTRAINT [DF_ERPROJECTFILECATEGORY_RowVersion] DEFAULT ((1)) NOT NULL,
    [FILENAMEPREFIX]          INT            NULL,
    [FILENAMEPREFIXCUSTOM]    NVARCHAR (20)  NULL,
    [FILENAMEBODY]            INT            NULL,
    [FILENAMEBODYCUSTOM]      NVARCHAR (50)  NULL,
    [FILENAMESUFFIX]          INT            NULL,
