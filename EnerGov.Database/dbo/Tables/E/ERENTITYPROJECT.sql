CREATE TABLE [dbo].[ERENTITYPROJECT] (
    [ERENTITYPROJECTID] VARCHAR (36)   NOT NULL,
    [PROJECTNAME]       VARCHAR (100)  NULL,
    [PLPLANID]          CHAR (36)      NULL,
    [PMPERMITID]        CHAR (36)      NULL,
    [APIRESPONSE]       NVARCHAR (MAX) NULL,
    CONSTRAINT [PK_ERENTITYPROJECT] PRIMARY KEY NONCLUSTERED ([ERENTITYPROJECTID] ASC),
    CONSTRAINT [FK_ERENTITYPROJECT_PLPLAN] FOREIGN KEY ([PLPLANID]) REFERENCES [dbo].[PLPLAN] ([PLPLANID]),
    CONSTRAINT [FK_ERENTITYPROJECT_PMPERMIT] FOREIGN KEY ([PMPERMITID]) REFERENCES [dbo].[PMPERMIT] ([PMPERMITID])
);


GO
CREATE NONCLUSTERED INDEX [IX_PLPLANID]
    ON [dbo].[ERENTITYPROJECT]([PLPLANID] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_PMPERMITID]
    ON [dbo].[ERENTITYPROJECT]([PMPERMITID] ASC);

