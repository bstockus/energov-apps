	INNER JOIN [CUSTOMFIELD] WITH (NOLOCK) ON [CUSTOMFIELD].[GCUSTOMFIELD] = [inserted].[CUSTOMFIELDID]
END
GO

CREATE TRIGGER [TG_ROLESCUSTOMFIELDS_DELETE] ON [ROLESCUSTOMFIELDS]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[ROLES].[SROLEGUID],
			[ROLES].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'User Role - Custom Field Deleted',
			'',
			'',
			'User Role (' + [ROLES].[ID] + '), Custom Field (' + [CUSTOMFIELD].[SFIELDNAME] + ')',
			'801F270F-912F-420A-91D6-82EBC3F351F3',
			3,
			0,
			[CUSTOMFIELD].[SFIELDNAME]
	FROM	[deleted]	
	INNER JOIN [ROLES] ON [deleted].[ROLEID] = [ROLES].[SROLEGUID]	
	INNER JOIN [CUSTOMFIELD] WITH (NOLOCK) ON [CUSTOMFIELD].[GCUSTOMFIELD] = [deleted].[CUSTOMFIELDID]

END