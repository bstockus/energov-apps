CREATE TABLE [dbo].[ROLESUBMENUXREF] (
    [FKROLEID]    CHAR (36) NOT NULL,
    [FKSUBMENUID] CHAR (36) NOT NULL,
    [BVISIBLE]    BIT       NOT NULL,
    CONSTRAINT [PK_UserSubMenuXRef] PRIMARY KEY CLUSTERED ([FKROLEID] ASC, [FKSUBMENUID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_RoleSubMenuXRef_Roles] FOREIGN KEY ([FKROLEID]) REFERENCES [dbo].[ROLES] ([SROLEGUID]),
    CONSTRAINT [FK_RoleSubMenuXRef_SubMenus] FOREIGN KEY ([FKSUBMENUID]) REFERENCES [dbo].[SUBMENUS] ([SSUBMENUGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_ROLESUBMENUXREF_FKSUBMENUID]
    ON [dbo].[ROLESUBMENUXREF]([FKSUBMENUID] ASC);


GO
CREATE NONCLUSTERED INDEX [ROLESUBMENUXREF_IX_FKROLEID]
    ON [dbo].[ROLESUBMENUXREF]([FKROLEID] ASC);


GO


CREATE TRIGGER [dbo].[TG_ROLESUBMENUXREF_UPDATE] ON  [dbo].[ROLESUBMENUXREF]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
			
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	
	SELECT 
			[ROLES].[SROLEGUID],
			[ROLES].[ROWVERSION],
			GETUTCDATE(),
			[ROLES].[LASTCHANGEDBY],
			'Visible Flag',			
			CASE [deleted].[BVISIBLE] WHEN 1 THEN 'Yes' ELSE 'No' END,
			CASE [inserted].[BVISIBLE] WHEN 1 THEN 'Yes' ELSE 'No' END,
			'User Role (' + [ROLES].[ID] + '), Submenu (' + [SUBMENUS].[SDESCRIPTION] +')',			
			'801F270F-912F-420A-91D6-82EBC3F351F3',
			2,
			0,
			[SUBMENUS].[SDESCRIPTION]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[FKROLEID] = [inserted].[FKROLEID]			
			INNER JOIN [ROLES] ON [inserted].[FKROLEID] = [ROLES].[SROLEGUID]		
			INNER JOIN [SUBMENUS] ON [inserted].[FKSUBMENUID] = [SUBMENUS].[SSUBMENUGUID]
	WHERE	[deleted].[BVISIBLE] <> [inserted].[BVISIBLE]	

END
GO

CREATE TRIGGER [TG_ROLESUBMENUXREF_INSERT] ON [ROLESUBMENUXREF]
	AFTER INSERT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
			[ROLES].[SROLEGUID],
			[ROLES].[ROWVERSION],
			GETUTCDATE(),
			[ROLES].[LASTCHANGEDBY],
			'User Role - Submenu Added',
			'',
			'',
			'User Role (' + [ROLES].[ID] + '), Submenu (' + [SUBMENUS].[SDESCRIPTION] + ')',
			'801F270F-912F-420A-91D6-82EBC3F351F3',
			1,
			0,
			[SUBMENUS].[SDESCRIPTION]
	FROM	[inserted]
	INNER JOIN [ROLES] ON [inserted].[FKROLEID] = [ROLES].[SROLEGUID]
	INNER JOIN [SUBMENUS] WITH (NOLOCK) ON [SUBMENUS].[SSUBMENUGUID] = [inserted].[FKSUBMENUID]
END