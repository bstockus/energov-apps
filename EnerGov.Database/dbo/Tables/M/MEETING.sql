CREATE TABLE [dbo].[MEETING] (
    [MEETINGID]      CHAR (36)      NOT NULL,
    [MEETINGTYPEID]  CHAR (36)      NOT NULL,
    [SUBJECT]        NVARCHAR (MAX) NOT NULL,
    [LOCATION]       NVARCHAR (MAX) NOT NULL,
    [COMMENTS]       NVARCHAR (MAX) NULL,
    [STARTDATE]      DATETIME       NOT NULL,
    [ENDDATE]        DATETIME       NOT NULL,
    [LASTCHANGEDBY]  CHAR (36)      NULL,
    [LASTCHANGEDON]  DATETIME       CONSTRAINT [DF_MEETING_LASTCHANGEDON] DEFAULT (getutcdate()) NOT NULL,
    [ROWVERSION]     INT            NOT NULL,
    [EXCHANGEITEMID] VARCHAR (200)  NULL,
    [SHOWONCALENDAR] BIT            DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_Meetings] PRIMARY KEY CLUSTERED ([MEETINGID] ASC),
    CONSTRAINT [FK_Meeting_MeetingType] FOREIGN KEY ([MEETINGTYPEID]) REFERENCES [dbo].[MEETINGTYPE] ([MEETINGTYPEID])
);


GO
CREATE NONCLUSTERED INDEX [IX_MEETING_DATE]
    ON [dbo].[MEETING]([SHOWONCALENDAR] ASC, [STARTDATE] ASC, [ENDDATE] ASC)
    INCLUDE([MEETINGID]);


GO
CREATE NONCLUSTERED INDEX [IX_MEETING_ID_TYPE]
    ON [dbo].[MEETING]([MEETINGID] ASC, [MEETINGTYPEID] ASC)
    INCLUDE([STARTDATE], [COMMENTS]);


GO


CREATE TRIGGER [TG_MEETING_INSERTUPDATE_ELASTIC_PLAN] ON  [MEETING]
   AFTER INSERT, UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[P].[PLPLANID] ,
        'EnerGovBusiness.PlanManagement.Plan' ,
        [P].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [MEETINGXREF] AS [M] WITH (NOLOCK) ON [M].[MEETINGID] = [Inserted].[MEETINGID]
	JOIN [PLPLANWFACTIONSTEP] AS [A] WITH (NOLOCK) ON [A].[PLPLANWFACTIONSTEPID] = [M].[OBJECTID]
	JOIN [PLPLANWFSTEP] AS [S] WITH (NOLOCK) ON [S].[PLPLANWFSTEPID] = [A].[PLPLANWFSTEPID]
	JOIN [PLPLAN] AS [P] WITH (NOLOCK) ON [P].[PLPLANID] = [S].[PLPLANID]

END
GO


