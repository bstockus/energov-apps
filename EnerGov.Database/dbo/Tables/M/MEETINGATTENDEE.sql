CREATE TABLE [dbo].[MEETINGATTENDEE] (
    [MEETINGATTENDEEID] CHAR (36) NOT NULL,
    [HEARINGID]         CHAR (36) NOT NULL,
    [ATTENDEEID]        CHAR (36) NOT NULL,
    [ISUSER]            BIT       CONSTRAINT [DF_MeetingAttendee_IsUser] DEFAULT ((0)) NOT NULL,
    [ISGLOBALENTITY]    BIT       CONSTRAINT [DF_MeetingAttendee_IsGlobalEntity] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_MeetingAttendee] PRIMARY KEY CLUSTERED ([MEETINGATTENDEEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_MeetingAttendee_Meeting] FOREIGN KEY ([HEARINGID]) REFERENCES [dbo].[MEETING] ([MEETINGID])
);


GO
CREATE NONCLUSTERED INDEX [IX_MEETINGATTENDEE_ID_ATTENDEE]
    ON [dbo].[MEETINGATTENDEE]([ATTENDEEID] ASC)
    INCLUDE([HEARINGID]);

