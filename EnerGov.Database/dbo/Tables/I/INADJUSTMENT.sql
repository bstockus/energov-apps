CREATE TABLE [dbo].[INADJUSTMENT] (
    [INADJUSTMENTID]      CHAR (36)       NOT NULL,
    [ADJUSTINGOBJECTID]   CHAR (36)       NULL,
    [ADJUSTINGOBJECTTYPE] CHAR (36)       NULL,
    [ADJUSTINGOBJECTLINE] INT             NULL,
    [GLACCOUNTIDTODEBIT]  CHAR (36)       NULL,
    [GLACCOUNTIDTOCREDIT] CHAR (36)       NULL,
    [FISCALPERIODID]      CHAR (36)       NULL,
    [QUANTITY]            NUMERIC (38, 5) NOT NULL,
    [COST]                MONEY           NOT NULL,
    [DATEENTERED]         DATETIME        NULL,
    [UNITCOST]            MONEY           NOT NULL,
    [LASTCHANGEDBY]       CHAR (36)       NULL,
    [LASTCHANGEDDATE]     DATETIME        NULL,
    [INADJUSTMENTTYPEID]  CHAR (36)       NOT NULL,
    [INSTOREITEMID]       CHAR (36)       NOT NULL,
    CONSTRAINT [PK_INAdjustment] PRIMARY KEY CLUSTERED ([INADJUSTMENTID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_INAdjustment_FiscalPeriod] FOREIGN KEY ([FISCALPERIODID]) REFERENCES [dbo].[FISCALPERIOD] ([FISCALPERIODID]),
    CONSTRAINT [FK_INAdjustment_GLAccount] FOREIGN KEY ([GLACCOUNTIDTOCREDIT]) REFERENCES [dbo].[GLACCOUNT] ([GLACCOUNTID]),
    CONSTRAINT [FK_INAdjustment_GLAccount1] FOREIGN KEY ([GLACCOUNTIDTODEBIT]) REFERENCES [dbo].[GLACCOUNT] ([GLACCOUNTID]),
    CONSTRAINT [FK_INAdjustment_INAdjustmentTy] FOREIGN KEY ([INADJUSTMENTTYPEID]) REFERENCES [dbo].[INADJUSTMENTTYPE] ([INADJUSTMENTTYPEID]),
    CONSTRAINT [FK_INAdjustment_INStoreItem] FOREIGN KEY ([INSTOREITEMID]) REFERENCES [dbo].[INSTOREITEM] ([INSTOREITEMID]),
    CONSTRAINT [FK_INAdjustment_Users] FOREIGN KEY ([LASTCHANGEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);

