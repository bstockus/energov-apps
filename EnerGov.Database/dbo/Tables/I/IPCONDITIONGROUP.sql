CREATE TABLE [dbo].[IPCONDITIONGROUP] (
    [IPCONDITIONGROUPID] CHAR (36)      NOT NULL,
    [GROUPNAME]          NVARCHAR (100) NOT NULL,
    [IPCASEID]           CHAR (36)      NOT NULL,
    [CREATEDDATE]        DATETIME       NOT NULL,
