CREATE TABLE [dbo].[ITEMTYPE] (
    [ITEMTYPEID]    CHAR (36)     NOT NULL,
    [NAME]          VARCHAR (50)  NULL,
    [DESCRIPTION]   VARCHAR (MAX) NOT NULL,
    [CUSTOMFIELDID] CHAR (36)     NULL,
    CONSTRAINT [PK_ItemType] PRIMARY KEY CLUSTERED ([ITEMTYPEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_ItemType_CustomFieldLayout] FOREIGN KEY ([CUSTOMFIELDID]) REFERENCES [dbo].[CUSTOMFIELDLAYOUT] ([GCUSTOMFIELDLAYOUTS])
);

