CREATE TABLE [dbo].[ILLICENSEALLOWEDACTIVITY] (
    [ILLICENSEALLOWEDACTIVITYID] CHAR (36) NOT NULL,
    [ILLICENSETYPEID]            CHAR (36) NOT NULL,
    [ILLICENSEACTIVITYTYPEID]    CHAR (36) NOT NULL,
    CONSTRAINT [PK_ILLICENSEALLOWEDACTIVITY] PRIMARY KEY CLUSTERED ([ILLICENSEALLOWEDACTIVITYID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_ILLICALLOWACT_ILLICACTTYPE] FOREIGN KEY ([ILLICENSEACTIVITYTYPEID]) REFERENCES [dbo].[ILLICENSEACTIVITYTYPE] ([ILLICENSEACTIVITYTYPEID]),
    CONSTRAINT [FK_ILLICALLOWACT_ILLICTYPE] FOREIGN KEY ([ILLICENSETYPEID]) REFERENCES [dbo].[ILLICENSETYPE] ([ILLICENSETYPEID])
);


GO
CREATE NONCLUSTERED INDEX [IX_ILLICENSEALLOWEDACTIVITY_ILLICENSETYPEID]
    ON [dbo].[ILLICENSEALLOWEDACTIVITY]([ILLICENSETYPEID] ASC);


GO

CREATE TRIGGER [dbo].[TG_ILLICENSEALLOWEDACTIVITY_DELETE]  ON  [dbo].[ILLICENSEALLOWEDACTIVITY]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[ILLICENSETYPE].[ILLICENSETYPEID],
		[ILLICENSETYPE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
		'Professional License Type Allowed Professional License Activity Deleted',
        '',
        '',       
		'Professional License Type (' + [ILLICENSETYPE].[NAME] + '), Allowed Professional License Activity Type (' + [ILLICENSEACTIVITYTYPE].[NAME] +')',
		'67978C16-D720-4F8E-8120-E00FBB732A77',
		3,
		0,
		[ILLICENSEACTIVITYTYPE].[NAME]
    FROM [deleted]
	INNER JOIN [ILLICENSETYPE] ON [ILLICENSETYPE].[ILLICENSETYPEID] = [deleted].[ILLICENSETYPEID]
	INNER JOIN [ILLICENSEACTIVITYTYPE] WITH (NOLOCK) ON [ILLICENSEACTIVITYTYPE].[ILLICENSEACTIVITYTYPEID] = [deleted].[ILLICENSEACTIVITYTYPEID]
END
GO

