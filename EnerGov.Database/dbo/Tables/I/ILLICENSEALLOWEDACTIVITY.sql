CREATE TRIGGER [dbo].[TG_ILLICENSEALLOWEDACTIVITY_UPDATE]   ON  [dbo].[ILLICENSEALLOWEDACTIVITY] 
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
			[ILLICENSETYPE].[ILLICENSETYPEID], 
			[ILLICENSETYPE].[ROWVERSION],
			GETUTCDATE(),
			[ILLICENSETYPE].[LASTCHANGEDBY],	
			'Activity Name',			
			[ILLICENSEACTIVITYTYPE_DELETED].[NAME] ,
			[ILLICENSEACTIVITYTYPE_INSERTED].[NAME] ,
			'Professional License Type (' + [ILLICENSETYPE].[NAME] + '), Allowed Professional License Activity Type (' + [ILLICENSEACTIVITYTYPE_INSERTED].[NAME] +')',
			'67978C16-D720-4F8E-8120-E00FBB732A77',
			2,
			0,
			[ILLICENSEACTIVITYTYPE_INSERTED].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[ILLICENSEALLOWEDACTIVITYID] = [inserted].[ILLICENSEALLOWEDACTIVITYID]			
			INNER JOIN [ILLICENSETYPE] ON [ILLICENSETYPE].[ILLICENSETYPEID] = [deleted].[ILLICENSETYPEID]
			INNER JOIN [ILLICENSEACTIVITYTYPE] [ILLICENSEACTIVITYTYPE_DELETED]WITH (NOLOCK) ON [ILLICENSEACTIVITYTYPE_DELETED].[ILLICENSEACTIVITYTYPEID] = [deleted].[ILLICENSEACTIVITYTYPEID]
			INNER JOIN [ILLICENSEACTIVITYTYPE] [ILLICENSEACTIVITYTYPE_INSERTED] WITH (NOLOCK) ON [ILLICENSEACTIVITYTYPE_INSERTED].[ILLICENSEACTIVITYTYPEID] = [inserted].[ILLICENSEACTIVITYTYPEID]
	WHERE	[deleted].[ILLICENSEACTIVITYTYPEID] <>[inserted].[ILLICENSEACTIVITYTYPEID]
END