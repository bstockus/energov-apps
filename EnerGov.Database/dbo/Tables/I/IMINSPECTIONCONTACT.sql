CREATE TABLE [dbo].[IMINSPECTIONCONTACT] (
    [IMINSPECTIONCONTACTID] CHAR (36) NOT NULL,
    [IMINSPECTIONID]        CHAR (36) NOT NULL,
    [GLOBALENTITYID]        CHAR (36) NOT NULL,
    [CONTACTTYPEID]         CHAR (36) NULL,
    [ISBILLING]             BIT       NOT NULL,
    CONSTRAINT [PK_IMINSPECTIONCONTACT] PRIMARY KEY NONCLUSTERED ([IMINSPECTIONCONTACTID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_INSPCONTACT_GLOBALENTITY] FOREIGN KEY ([GLOBALENTITYID]) REFERENCES [dbo].[GLOBALENTITY] ([GLOBALENTITYID]),
    CONSTRAINT [FK_INSPCONTACT_INSP] FOREIGN KEY ([IMINSPECTIONID]) REFERENCES [dbo].[IMINSPECTION] ([IMINSPECTIONID])
);


GO
CREATE NONCLUSTERED INDEX [IX_IMINSPECTION_CONTACT]
    ON [dbo].[IMINSPECTIONCONTACT]([GLOBALENTITYID] ASC, [IMINSPECTIONID] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [IX_IMINSPECTIONCONTACT_IMINSPECTIONID]
    ON [dbo].[IMINSPECTIONCONTACT]([IMINSPECTIONID] ASC);

