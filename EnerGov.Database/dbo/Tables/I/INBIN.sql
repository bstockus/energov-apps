CREATE TABLE [dbo].[INBIN] (
    [INBINID]       CHAR (36)       NOT NULL,
    [NAME]          NVARCHAR (50)   NOT NULL,
    [DESCRIPTION]   NVARCHAR (MAX)  NULL,
    [INSTOREID]     CHAR (36)       NOT NULL,
    [INBINTYPEID]   CHAR (36)       NOT NULL,
    [AMASSETID]     CHAR (36)       NULL,
    [CAPACITY]      DECIMAL (38, 5) NULL,
    [UNITOFMEASURE] CHAR (36)       NULL,
    [BINNO]         VARCHAR (50)    NOT NULL,
    CONSTRAINT [PK_INBin] PRIMARY KEY CLUSTERED ([INBINID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_INBin_AMAsset] FOREIGN KEY ([AMASSETID]) REFERENCES [dbo].[AMASSET] ([AMASSETID]),
    CONSTRAINT [FK_INBin_INBinType] FOREIGN KEY ([INBINTYPEID]) REFERENCES [dbo].[INBINTYPE] ([INBINTYPEID]),
    CONSTRAINT [FK_INBin_INStore] FOREIGN KEY ([INSTOREID]) REFERENCES [dbo].[INSTORE] ([INSTOREID]),
    CONSTRAINT [FK_INBin_UnitOfMeasure] FOREIGN KEY ([UNITOFMEASURE]) REFERENCES [dbo].[UNITOFMEASURE] ([UNITOFMEASUREID])
);

