CREATE TABLE [dbo].[ILLICENSEPARCEL] (
    [ILLICENSEPARCELID] CHAR (36) NOT NULL,
    [ILLICENSEID]       CHAR (36) NOT NULL,
    [PARCELID]          CHAR (36) NOT NULL,
    [MAIN]              BIT       NOT NULL,
    CONSTRAINT [PK_ILLICENSEPARCEL] PRIMARY KEY CLUSTERED ([ILLICENSEPARCELID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_ILLICENSEPARCEL_ILLICENSE] FOREIGN KEY ([ILLICENSEID]) REFERENCES [dbo].[ILLICENSE] ([ILLICENSEID]),
    CONSTRAINT [FK_ILLICENSEPARCEL_PARCEL] FOREIGN KEY ([PARCELID]) REFERENCES [dbo].[PARCEL] ([PARCELID])
);


GO
CREATE NONCLUSTERED INDEX [IX_ILLICENSEPARCEL_LICENSE]
    ON [dbo].[ILLICENSEPARCEL]([ILLICENSEID] ASC, [PARCELID] ASC, [MAIN] ASC) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IMPORT1]
    ON [dbo].[ILLICENSEPARCEL]([ILLICENSEID] ASC) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IMPORT2]
    ON [dbo].[ILLICENSEPARCEL]([PARCELID] ASC) WITH (FILLFACTOR = 90);

