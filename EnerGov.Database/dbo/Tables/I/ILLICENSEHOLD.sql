CREATE TABLE [dbo].[ILLICENSEHOLD] (
    [ILLICENSEHOLDID]    CHAR (36)      NOT NULL,
    [ILLICENSEID]        CHAR (36)      NOT NULL,
    [HOLDSETUPID]        CHAR (36)      NULL,
    [COMMENTS]           NVARCHAR (MAX) NULL,
    [SUSERGUID]          CHAR (36)      NULL,
    [CREATEDDATE]        DATETIME       NULL,
    [ORIGIN]             CHAR (36)      NULL,
    [ORIGINNUMBER]       NVARCHAR (150) NULL,
    [EFFECTIVEENDDATE]   DATETIME       NULL,
    [ACTIVE]             BIT            NOT NULL,
    [HOLDSEVERITYID]     INT            DEFAULT ((1)) NOT NULL,
    [ORIGINCMCODECASEID] CHAR (36)      NULL,
    CONSTRAINT [PK_ILLicenseHold] PRIMARY KEY CLUSTERED ([ILLICENSEHOLDID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_ILLicenseHold_Hold] FOREIGN KEY ([HOLDSETUPID]) REFERENCES [dbo].[HOLDTYPESETUPS] ([HOLDSETUPID]),
    CONSTRAINT [FK_ILLicenseHold_HoldSeverity] FOREIGN KEY ([HOLDSEVERITYID]) REFERENCES [dbo].[HOLDSEVERITY] ([HOLDSEVERITYID]),
    CONSTRAINT [FK_ILLicenseHold_ILLicense] FOREIGN KEY ([ILLICENSEID]) REFERENCES [dbo].[ILLICENSE] ([ILLICENSEID]),
    CONSTRAINT [FK_ILLicenseHold_OrginCodeCase] FOREIGN KEY ([ORIGINCMCODECASEID]) REFERENCES [dbo].[CMCODECASE] ([CMCODECASEID]),
    CONSTRAINT [FK_ILLicenseHold_User] FOREIGN KEY ([SUSERGUID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_ILLICENSEHOLD_ILLICENSEID]
    ON [dbo].[ILLICENSEHOLD]([ILLICENSEID] ASC)
    INCLUDE([ILLICENSEHOLDID]) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IX_ILLICENSEHOLD_ORIGINCMCODECASEID]
    ON [dbo].[ILLICENSEHOLD]([ORIGINCMCODECASEID] ASC)
    INCLUDE([ILLICENSEHOLDID]);

