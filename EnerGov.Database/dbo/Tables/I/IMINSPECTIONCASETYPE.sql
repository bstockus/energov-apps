	WHERE	ISNULL([deleted].[IMINSPECTIONCASESTATUSID], '') <> ISNULL([inserted].[IMINSPECTIONCASESTATUSID], '')

	UNION ALL
	SELECT
			[inserted].[IMINSPECTIONCASETYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Recurrence Limit',
			CONVERT(NVARCHAR(MAX), [deleted].[RECURRENCELIMIT]),
			CONVERT(NVARCHAR(MAX), [inserted].[RECURRENCELIMIT]),
			'Inspection Case Type (' + [inserted].[NAME] + ')',
			'4E9A143E-94BA-4B18-9427-1379BC7B54F4',
			2,
