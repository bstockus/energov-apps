CREATE TABLE [dbo].[IMINSPECTIONCASETYPE] (
    [IMINSPECTIONCASETYPEID]   CHAR (36)      NOT NULL,
    [NAME]                     NVARCHAR (50)  NOT NULL,
    [DESCRIPTION]              NVARCHAR (MAX) NULL,
    [IMINSPECTIONTYPEID]       CHAR (36)      NOT NULL,
    [ACTIVE]                   BIT            CONSTRAINT [DF_IMINSPECTIONCASETYPE_ACTIVE] DEFAULT ((0)) NOT NULL,
    [RECURRENCELIMITTYPE]      INT            NOT NULL,
    [IMINSPECTIONCASESTATUSID] CHAR (36)      NULL,
    [RECURRENCELIMIT]          INT            NOT NULL,
    [PREFIX]                   NVARCHAR (10)  NULL,
    [CUSTOMFIELDLAYOUTID]      CHAR (36)      NULL,
    [RECURRENCEID]             CHAR (36)      NULL,
    [ASSIGNINSPECTORVIAGIS]    BIT            DEFAULT ((0)) NOT NULL,
    [NOTSCHEDULEINSPECTION]    BIT            DEFAULT ((0)) NOT NULL,
    [LASTCHANGEDBY]            CHAR (36)      NULL,
    [LASTCHANGEDON]            DATETIME       CONSTRAINT [DF_IMINSPECTIONCASETYPE_LastChangedOn] DEFAULT (getutcdate()) NOT NULL,
    [ROWVERSION]               INT            CONSTRAINT [DF_IMINSPECTIONCASETYPE_RowVersion] DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_IMInspectionCaseType] PRIMARY KEY CLUSTERED ([IMINSPECTIONCASETYPEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_IMINSPECTIONCASETYPE_CUSTOMFIELDLAYOUT] FOREIGN KEY ([CUSTOMFIELDLAYOUTID]) REFERENCES [dbo].[CUSTOMFIELDLAYOUT] ([GCUSTOMFIELDLAYOUTS]),
    CONSTRAINT [FK_IMInspectionCaseType_IMInsp] FOREIGN KEY ([IMINSPECTIONTYPEID]) REFERENCES [dbo].[IMINSPECTIONTYPE] ([IMINSPECTIONTYPEID]),
    CONSTRAINT [FK_IMINSPECTIONCASETYPE_REC] FOREIGN KEY ([RECURRENCEID]) REFERENCES [dbo].[RECURRENCE] ([RECURRENCEID]),
    CONSTRAINT [FK_IMINSPECTIONCASETYPE_STATUS] FOREIGN KEY ([IMINSPECTIONCASESTATUSID]) REFERENCES [dbo].[IMINSPECTIONCASESTATUS] ([IMINSPECTIONCASESTATUSID])
);


GO
CREATE NONCLUSTERED INDEX [IMINSPECTIONCASETYPE_IX_QUERY]
    ON [dbo].[IMINSPECTIONCASETYPE]([IMINSPECTIONCASETYPEID] ASC, [NAME] ASC);


