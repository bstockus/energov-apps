			'4E9A143E-94BA-4B18-9427-1379BC7B54F4',
			2,
			1,
			[inserted].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[IMINSPECTIONCASETYPEID] = [inserted].[IMINSPECTIONCASETYPEID]
			INNER JOIN [IMINSPECTIONTYPE] IMINSPECTIONTYPE_DELETED WITH (NOLOCK) ON [deleted].[IMINSPECTIONTYPEID] = IMINSPECTIONTYPE_DELETED.[IMINSPECTIONTYPEID]
			INNER JOIN [IMINSPECTIONTYPE] IMINSPECTIONTYPE_INSERTED WITH (NOLOCK) ON [inserted].[IMINSPECTIONTYPEID] = IMINSPECTIONTYPE_INSERTED.[IMINSPECTIONTYPEID]
	WHERE	[deleted].[IMINSPECTIONTYPEID] <> [inserted].[IMINSPECTIONTYPEID]

	UNION ALL
	SELECT
			[inserted].[IMINSPECTIONCASETYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Active Flag',
			CASE [deleted].[ACTIVE] WHEN 1 THEN 'Yes' WHEN 0 THEN 'No' END,
