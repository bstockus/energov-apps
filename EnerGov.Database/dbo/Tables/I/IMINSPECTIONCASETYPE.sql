			'Inspection Case Type (' + [inserted].[NAME] + ')',
			'4E9A143E-94BA-4B18-9427-1379BC7B54F4',
			2,
			1,
			[inserted].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[IMINSPECTIONCASETYPEID] = [inserted].[IMINSPECTIONCASETYPEID]
	WHERE	[deleted].[ACTIVE] <> [inserted].[ACTIVE]

	UNION ALL
	SELECT
			[inserted].[IMINSPECTIONCASETYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Recurrence Limit Type',
			CASE [deleted].[RECURRENCELIMITTYPE] WHEN 1 THEN 'Number Of Recurrences' WHEN 2 THEN 'Number Of Days' WHEN 3 THEN 'Unlimited Recurrences' END,
			CASE [inserted].[RECURRENCELIMITTYPE] WHEN 1 THEN 'Number Of Recurrences' WHEN 2 THEN 'Number Of Days' WHEN 3 THEN 'Unlimited Recurrences' END,
			'Inspection Case Type (' + [inserted].[NAME] + ')',
			'4E9A143E-94BA-4B18-9427-1379BC7B54F4',
