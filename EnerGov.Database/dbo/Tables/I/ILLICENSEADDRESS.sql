CREATE TABLE [dbo].[ILLICENSEADDRESS] (
    [ILLICENSEADDRESSID] CHAR (36) NOT NULL,
    [ILLICENSEID]        CHAR (36) NOT NULL,
    [MAILINGADDRESSID]   CHAR (36) NOT NULL,
    [MAIN]               BIT       NOT NULL,
    CONSTRAINT [PK_ILLICENSEADDRESS] PRIMARY KEY CLUSTERED ([ILLICENSEADDRESSID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_ILLICADDR_ILLIC] FOREIGN KEY ([ILLICENSEID]) REFERENCES [dbo].[ILLICENSE] ([ILLICENSEID]),
    CONSTRAINT [FK_ILLICENSEADDR_MAILLADDR] FOREIGN KEY ([MAILINGADDRESSID]) REFERENCES [dbo].[MAILINGADDRESS] ([MAILINGADDRESSID])
);


GO
CREATE NONCLUSTERED INDEX [IX_ILLICADD_LICENSE]
    ON [dbo].[ILLICENSEADDRESS]([ILLICENSEID] ASC, [MAILINGADDRESSID] ASC, [MAIN] ASC) WITH (FILLFACTOR = 90);

