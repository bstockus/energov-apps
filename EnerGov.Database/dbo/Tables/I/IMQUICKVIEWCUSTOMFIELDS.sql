    [IMQUICKVIEWCUSTOMFIELDID] CHAR (36) NOT NULL,
    [CUSTOMFIELDID]            CHAR (36) NOT NULL,
    [SHOWINGRID]               BIT       NOT NULL,
    [MODULEID]                 INT       NOT NULL,
    [IORDER]                   INT       NOT NULL,
    [LASTCHANGEDBY]            CHAR (36) NULL,
