CREATE TABLE [dbo].[IMINSPECTORTYPEINSPECTIONTYPE] (
    [IMINSPECTORTYPEID]  CHAR (36) NOT NULL,
    [IMINSPECTIONTYPEID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_IMInspectorTypeInspectionType] PRIMARY KEY CLUSTERED ([IMINSPECTORTYPEID] ASC, [IMINSPECTIONTYPEID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_InspectorInspectType_Type] FOREIGN KEY ([IMINSPECTIONTYPEID]) REFERENCES [dbo].[IMINSPECTIONTYPE] ([IMINSPECTIONTYPEID]),
    CONSTRAINT [FK_InspectorInspectType_Type2] FOREIGN KEY ([IMINSPECTORTYPEID]) REFERENCES [dbo].[IMINSPECTORTYPE] ([IMINSPECTORTYPEID])
);


GO

CREATE TRIGGER [TG_IMINSPECTORTYPEINSPECTIONTYPE_DELETE] ON IMINSPECTORTYPEINSPECTIONTYPE
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )

	SELECT
			[IMINSPECTORTYPE].[IMINSPECTORTYPEID],
			[IMINSPECTORTYPE].[ROWVERSION],
			GETUTCDATE(),
			[IMINSPECTORTYPE].[LASTCHANGEDBY],
			'Inspector Type Inspection Type Deleted',
			'',
			'',
			'Inspector Type (' + [IMINSPECTORTYPE].[SNAME] + '), Inspection Type (' + [IMINSPECTIONTYPE_DELETED].[NAME] + ')',
			'C145443E-4168-4AEA-BD6F-056366BE2652',
			3,
			0,
			[IMINSPECTIONTYPE_DELETED].[NAME]
	FROM	[deleted]
	INNER JOIN [IMINSPECTORTYPE] ON [deleted].[IMINSPECTORTYPEID] = [IMINSPECTORTYPE].[IMINSPECTORTYPEID]	
	LEFT JOIN [IMINSPECTIONTYPE] IMINSPECTIONTYPE_DELETED WITH (NOLOCK) ON [deleted].[IMINSPECTIONTYPEID] = [IMINSPECTIONTYPE_DELETED].[IMINSPECTIONTYPEID]	
END
GO

