CREATE TABLE [dbo].[IMINSPECTIONPARCEL] (
    [INSPECTIONPARCELID] CHAR (36) NOT NULL,
    [PARCELID]           CHAR (36) NOT NULL,
    [IMINSPECTIONID]     CHAR (36) NOT NULL,
    [MAIN]               BIT       NOT NULL,
    CONSTRAINT [PK_IMInspectionParcel] PRIMARY KEY CLUSTERED ([INSPECTIONPARCELID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_InspectionParcel_Insp] FOREIGN KEY ([IMINSPECTIONID]) REFERENCES [dbo].[IMINSPECTION] ([IMINSPECTIONID]),
    CONSTRAINT [FK_InspectionParcel_Par] FOREIGN KEY ([PARCELID]) REFERENCES [dbo].[PARCEL] ([PARCELID])
);


GO
CREATE NONCLUSTERED INDEX [NCIDX_IMINSPECTIONPARCEL_PARCELID_INCL]
    ON [dbo].[IMINSPECTIONPARCEL]([PARCELID] ASC)
    INCLUDE([IMINSPECTIONID]) WITH (FILLFACTOR = 90, PAD_INDEX = ON);


GO
CREATE NONCLUSTERED INDEX [NCIDX_IMINSPECTIONPARCEL_MAIN_INCL]
    ON [dbo].[IMINSPECTIONPARCEL]([IMINSPECTIONID] ASC, [PARCELID] ASC, [MAIN] ASC) WITH (FILLFACTOR = 90, PAD_INDEX = ON);

