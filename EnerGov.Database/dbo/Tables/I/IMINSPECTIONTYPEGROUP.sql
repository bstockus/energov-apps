CREATE TABLE [dbo].[IMINSPECTIONTYPEGROUP] (
    [IMINSPECTIONTYPEGROUPID]  CHAR (36)      NOT NULL,
    [NAME]                     NVARCHAR (50)  NOT NULL,
    [DESCRIPTION]              NVARCHAR (MAX) NULL,
    [IVRNUMBER]                INT            NOT NULL,
    [GROUPLIMITATION]          INT            DEFAULT ((0)) NOT NULL,
    [IMINSPECTIONSYSTEMTYPEID] INT            DEFAULT ((1)) NULL,
    [LASTCHANGEDBY]            CHAR (36)      NULL,
    [LASTCHANGEDON]            DATETIME       CONSTRAINT [DF_IMINSPECTIONTYPEGROUP_LastChangedOn] DEFAULT (getutcdate()) NOT NULL,
    [ROWVERSION]               INT            CONSTRAINT [DF_IMINSPECTIONTYPEGROUP_RowVersion] DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_IMInspectionTypeGroup] PRIMARY KEY CLUSTERED ([IMINSPECTIONTYPEGROUPID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_IMINSPECTIONTYPEGROUP_IMINSPECTIONSYSTEMTYPE] FOREIGN KEY ([IMINSPECTIONSYSTEMTYPEID]) REFERENCES [dbo].[IMINSPECTIONSYSTEMTYPE] ([IMINSPECTIONSYSTEMTYPEID])
);


GO
