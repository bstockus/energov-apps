CREATE TABLE [dbo].[TEAMRESOURCE] (
    [TEAMRESOURCEID] CHAR (36) NOT NULL,
    [TEAMID]         CHAR (36) NOT NULL,
    [USERID]         CHAR (36) NOT NULL,
    [RESOURCETYPEID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_TEAMRESOURCE] PRIMARY KEY NONCLUSTERED ([TEAMRESOURCEID] ASC),
    CONSTRAINT [FK_TEAMRESOURCE_RESOURCETYPE] FOREIGN KEY ([RESOURCETYPEID]) REFERENCES [dbo].[RESOURCETYPE] ([RESOURCETYPEID]),
    CONSTRAINT [FK_TEAMRESOURCE_TEAM] FOREIGN KEY ([TEAMID]) REFERENCES [dbo].[TEAM] ([TEAMID]),
    CONSTRAINT [FK_TEAMRESOURCE_USER] FOREIGN KEY ([USERID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_TEAMRESOURCE]
    ON [dbo].[TEAMRESOURCE]([TEAMID] ASC, [USERID] ASC, [RESOURCETYPEID] ASC);

