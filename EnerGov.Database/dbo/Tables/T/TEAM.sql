CREATE TABLE [dbo].[TEAM] (
    [TEAMID]    CHAR (36)     NOT NULL,
    [NAME]      VARCHAR (200) NOT NULL,
    [USERID]    CHAR (36)     NOT NULL,
    [TEAMIMAGE] VARCHAR (MAX) NULL,
    CONSTRAINT [PK_TEAM] PRIMARY KEY NONCLUSTERED ([TEAMID] ASC),
    CONSTRAINT [FK_TEAM_USER] FOREIGN KEY ([USERID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_TEAM_MANAGER]
    ON [dbo].[TEAM]([USERID] ASC, [TEAMID] ASC);

