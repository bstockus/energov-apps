CREATE TABLE [dbo].[TEMPLATEDOC] (
    [TEMPLATEDOCID]    CHAR (36)       NOT NULL,
    [TEMPLATENAME]     VARCHAR (255)   NULL,
    [TEMPLATE]         VARBINARY (MAX) NULL,
    [TEMPLATEOBJECTID] CHAR (36)       NOT NULL,
    [TEMPLATEIMAGE]    VARBINARY (MAX) NULL,
    [DESCRIPTION]      VARCHAR (MAX)   NULL,
    [ACTIVE]           BIT             NULL,
    CONSTRAINT [PK_TemplateDoc] PRIMARY KEY CLUSTERED ([TEMPLATEDOCID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_TemplateDoc_Obj] FOREIGN KEY ([TEMPLATEOBJECTID]) REFERENCES [dbo].[TEMPLATEOBJECT] ([TEMPLATEOBJECTID])
);

