CREATE TABLE [dbo].[TXRMTTYPERPTPERIOD] (
    [TXRMTTYPERPTPERIODID] CHAR (36) NOT NULL,
    [TXRPTPERIODID]        CHAR (36) NOT NULL,
    [TXREMITTANCETYPEID]   CHAR (36) NOT NULL,
    [DEFAULTPERIOD]        BIT       DEFAULT ((0)) NOT NULL,
    PRIMARY KEY CLUSTERED ([TXRMTTYPERPTPERIODID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_TXREMITTANCETYPEID] FOREIGN KEY ([TXREMITTANCETYPEID]) REFERENCES [dbo].[TXREMITTANCETYPE] ([TXREMITTANCETYPEID]),
    CONSTRAINT [FK_TXRPTPERIODID] FOREIGN KEY ([TXRPTPERIODID]) REFERENCES [dbo].[TXRPTPERIOD] ([TXRPTPERIODID])
);


GO
CREATE NONCLUSTERED INDEX [TXRMTTYPERPTPERIOD_IX_TXRMTTYPERPTPERIODID]
    ON [dbo].[TXRMTTYPERPTPERIOD]([TXRMTTYPERPTPERIODID] ASC);


GO

CREATE TRIGGER [TG_TXRMTTYPERPTPERIOD_INSERT] ON [TXRMTTYPERPTPERIOD]
	AFTER INSERT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
			[TXREMITTANCETYPE].[TXREMITTANCETYPEID],
			[TXREMITTANCETYPE].[ROWVERSION],
			GETUTCDATE(),
			[TXREMITTANCETYPE].[LASTCHANGEDBY],
			'Tax Remittance Type Allowed Report Period Added',
			'',
			'',
			'Tax Remittance Type (' + [TXREMITTANCETYPE].[NAME] + '), Allowed Report Period ('+ [TXRPTPERIOD].[NAME] + ')',
			'41E1FC7D-1D43-499B-B40B-7AE70E72A922',
			1,
			0,
			[TXRPTPERIOD].[NAME]
	FROM	[inserted]
	INNER JOIN [TXREMITTANCETYPE] ON [inserted].[TXREMITTANCETYPEID] = [TXREMITTANCETYPE].[TXREMITTANCETYPEID]
	INNER JOIN [TXRPTPERIOD] ON [inserted].[TXRPTPERIODID] = [TXRPTPERIOD].[TXRPTPERIODID]
END
GO

CREATE TRIGGER [TG_TXRMTTYPERPTPERIOD_UPDATE] ON [TXRMTTYPERPTPERIOD]
	AFTER UPDATE
AS
BEGIN
 INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT
			[TXREMITTANCETYPE].[TXREMITTANCETYPEID],
			[TXREMITTANCETYPE].[ROWVERSION],
			GETUTCDATE(),
			[TXREMITTANCETYPE].[LASTCHANGEDBY],
			'Is Default Flag',
			CASE [deleted].[DEFAULTPERIOD] WHEN 1 THEN 'Yes' WHEN 0 THEN 'No' END,
			CASE [inserted].[DEFAULTPERIOD] WHEN 1 THEN 'Yes' WHEN 0 THEN 'No' END,
			'Tax Remittance Type (' + [TXREMITTANCETYPE].[NAME] + '), Allowed Report Period ('+ [TXRPTPERIOD].[NAME] + ')',
			'41E1FC7D-1D43-499B-B40B-7AE70E72A922',
			2,
			0,
			[TXRPTPERIOD].[NAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[TXREMITTANCETYPEID] = [inserted].[TXREMITTANCETYPEID]
			INNER JOIN [TXREMITTANCETYPE] ON [inserted].[TXREMITTANCETYPEID] = [TXREMITTANCETYPE].[TXREMITTANCETYPEID]
			INNER JOIN [TXRPTPERIOD] WITH (NOLOCK) ON [inserted].[TXRPTPERIODID] = [TXRPTPERIOD].[TXRPTPERIODID]
	WHERE	[deleted].[DEFAULTPERIOD] <> [inserted].[DEFAULTPERIOD]
END
GO

CREATE TRIGGER [TG_TXRMTTYPERPTPERIOD_DELETE] ON [TXRMTTYPERPTPERIOD]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[TXREMITTANCETYPE].[TXREMITTANCETYPEID],
			[TXREMITTANCETYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Tax Remittance Type Allowed Report Period Deleted',
			'',
			'',
			'Tax Remittance Type (' + [TXREMITTANCETYPE].[NAME] + '), Allowed Report Period ('+ [TXRPTPERIOD].[NAME] + ')',
			'41E1FC7D-1D43-499B-B40B-7AE70E72A922',
			3,
			0,
			[TXRPTPERIOD].[NAME]
	FROM	[deleted]	
	INNER JOIN [TXREMITTANCETYPE] ON [deleted].[TXREMITTANCETYPEID] = [TXREMITTANCETYPE].[TXREMITTANCETYPEID]
	INNER JOIN [TXRPTPERIOD]  WITH (NOLOCK) ON [deleted].[TXRPTPERIODID] = [TXRPTPERIOD].[TXRPTPERIODID]
END