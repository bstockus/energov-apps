CREATE TABLE [dbo].[TASKUSER] (
    [TASKUSERID] CHAR (36) NOT NULL,
    [TASKID]     CHAR (36) NOT NULL,
    [USERID]     CHAR (36) NOT NULL,
    CONSTRAINT [PK_TaskUser] PRIMARY KEY CLUSTERED ([TASKUSERID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_TaskUser_Task] FOREIGN KEY ([TASKID]) REFERENCES [dbo].[TASK] ([TASKID]),
    CONSTRAINT [FK_TaskUser_Users] FOREIGN KEY ([USERID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [UQ_TaskUser]
    ON [dbo].[TASKUSER]([USERID] ASC, [TASKID] ASC) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [TASK_USER_TASK]
    ON [dbo].[TASKUSER]([TASKID] ASC);

