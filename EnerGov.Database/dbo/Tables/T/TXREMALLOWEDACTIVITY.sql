CREATE TABLE [dbo].[TXREMALLOWEDACTIVITY] (
    [TXREMALLOWEDACTIVITYID]  CHAR (36) NOT NULL,
    [TXREMITTANCETYPEID]      CHAR (36) NOT NULL,
    [BLLICENSEACTIVITYTYPEID] CHAR (36) NOT NULL,
    PRIMARY KEY CLUSTERED ([TXREMALLOWEDACTIVITYID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_ACTIVITYBLACTIVITYTYPE] FOREIGN KEY ([BLLICENSEACTIVITYTYPEID]) REFERENCES [dbo].[BLLICENSEACTIVITYTYPE] ([BLLICENSEACTIVITYTYPEID]),
    CONSTRAINT [FK_ACTIVITYTAXREMTYPE] FOREIGN KEY ([TXREMITTANCETYPEID]) REFERENCES [dbo].[TXREMITTANCETYPE] ([TXREMITTANCETYPEID])
);


GO
CREATE NONCLUSTERED INDEX [TXREMALLOWEDACTIVITY_IX_TXREMALLOWEDACTIVITYID]
    ON [dbo].[TXREMALLOWEDACTIVITY]([TXREMALLOWEDACTIVITYID] ASC);


GO

CREATE TRIGGER [TG_TXREMALLOWEDACTIVITY_DELETE] ON [TXREMALLOWEDACTIVITY]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[TXREMITTANCETYPE].[TXREMITTANCETYPEID],
			[TXREMITTANCETYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Tax Remittance Type Allowed Activity Type Deleted',
			'',
			'',
			'Tax Remittance Type (' + [TXREMITTANCETYPE].[NAME] + '), Allowed Activity Type ('+ [BLLICENSEACTIVITYTYPE].[NAME] + ')',
			'41E1FC7D-1D43-499B-B40B-7AE70E72A922',
			3,
			0,
			[BLLICENSEACTIVITYTYPE].[NAME]
	FROM	[deleted]	
	INNER JOIN [TXREMITTANCETYPE] ON [deleted].[TXREMITTANCETYPEID] = [TXREMITTANCETYPE].[TXREMITTANCETYPEID]
	INNER JOIN [BLLICENSEACTIVITYTYPE] WITH (NOLOCK) ON [deleted].BLLICENSEACTIVITYTYPEID = [BLLICENSEACTIVITYTYPE].[BLLICENSEACTIVITYTYPEID]
END
GO

CREATE TRIGGER [TG_TXREMALLOWEDACTIVITY_INSERT] ON [TXREMALLOWEDACTIVITY]
	AFTER INSERT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
			[TXREMITTANCETYPE].[TXREMITTANCETYPEID],
			[TXREMITTANCETYPE].[ROWVERSION],
			GETUTCDATE(),
			[TXREMITTANCETYPE].[LASTCHANGEDBY],
			'Tax Remittance Type Allowed Activity Type Added',
			'',
			'',
			'Tax Remittance Type (' + [TXREMITTANCETYPE].[NAME] + '), Allowed Activity Type ('+ [BLLICENSEACTIVITYTYPE].[NAME] + ')',
			'41E1FC7D-1D43-499B-B40B-7AE70E72A922',
			1,
			0,
			[BLLICENSEACTIVITYTYPE].[NAME]
	FROM	[inserted]
	INNER JOIN [TXREMITTANCETYPE] ON [inserted].[TXREMITTANCETYPEID] = [TXREMITTANCETYPE].[TXREMITTANCETYPEID]
	INNER JOIN [BLLICENSEACTIVITYTYPE] WITH (NOLOCK) ON [inserted].BLLICENSEACTIVITYTYPEID = [BLLICENSEACTIVITYTYPE].[BLLICENSEACTIVITYTYPEID]
END