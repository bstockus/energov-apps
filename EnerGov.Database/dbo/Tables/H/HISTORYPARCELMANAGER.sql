CREATE TABLE [dbo].[HISTORYPARCELMANAGER] (
    [HISTORYPARCELMANAGERID] INT            IDENTITY (1, 1) NOT NULL,
    [ID]                     CHAR (36)      NOT NULL,
    [ROWVERSION]             INT            NOT NULL,
    [CHANGEDON]              DATETIME       NOT NULL,
    [CHANGEDBY]              CHAR (36)      NOT NULL,
    [FIELDNAME]              NVARCHAR (250) NOT NULL,
    [OLDVALUE]               NVARCHAR (MAX) NOT NULL,
    [NEWVALUE]               NVARCHAR (MAX) NOT NULL,
    [ADDITIONALINFO]         NVARCHAR (MAX) NOT NULL,
    CONSTRAINT [PK_HistoryParcelManagerID] PRIMARY KEY CLUSTERED ([HISTORYPARCELMANAGERID] ASC),
    CONSTRAINT [HistoryParcelManager_Users] FOREIGN KEY ([CHANGEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IDX_HParcel_ChangedOn]
    ON [dbo].[HISTORYPARCELMANAGER]([ID] ASC, [CHANGEDON] ASC, [HISTORYPARCELMANAGERID] ASC, [CHANGEDBY] ASC, [FIELDNAME] ASC)
    INCLUDE([ROWVERSION], [OLDVALUE], [NEWVALUE], [ADDITIONALINFO]);


GO
CREATE NONCLUSTERED INDEX [HistoryParcelManager_ID]
    ON [dbo].[HISTORYPARCELMANAGER]([ID] ASC, [ROWVERSION] ASC);

