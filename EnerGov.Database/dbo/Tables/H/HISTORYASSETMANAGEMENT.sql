CREATE TABLE [dbo].[HISTORYASSETMANAGEMENT] (
    [HISTORYASSETMANAGEMENTID] INT            IDENTITY (1, 1) NOT NULL,
    [ID]                       CHAR (36)      NOT NULL,
    [ROWVERSION]               INT            NOT NULL,
    [CHANGEDON]                DATETIME       NOT NULL,
    [CHANGEDBY]                CHAR (36)      NOT NULL,
    [FIELDNAME]                NVARCHAR (250) NOT NULL,
    [OLDVALUE]                 NVARCHAR (MAX) NOT NULL,
    [NEWVALUE]                 NVARCHAR (MAX) NOT NULL,
    [ADDITIONALINFO]           NVARCHAR (MAX) NOT NULL,
    CONSTRAINT [PK_HistoryAssetManagementID] PRIMARY KEY CLUSTERED ([HISTORYASSETMANAGEMENTID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [HistoryAssetManagement_Users] FOREIGN KEY ([CHANGEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [HistoryAssetManagement_ID]
    ON [dbo].[HISTORYASSETMANAGEMENT]([ID] ASC, [ROWVERSION] ASC) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IDX_HAM_ChangedOn]
    ON [dbo].[HISTORYASSETMANAGEMENT]([ID] ASC, [CHANGEDON] ASC, [HISTORYASSETMANAGEMENTID] ASC, [CHANGEDBY] ASC, [FIELDNAME] ASC)
    INCLUDE([ROWVERSION], [OLDVALUE], [NEWVALUE], [ADDITIONALINFO]) WITH (FILLFACTOR = 90);

