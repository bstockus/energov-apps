CREATE TABLE [dbo].[HEARINGXREF] (
    [OBJECTID]  CHAR (36)      NOT NULL,
    [HEARINGID] CHAR (36)      NOT NULL,
    [OBJECTMSG] NVARCHAR (255) NULL,
    CONSTRAINT [PK_HearingXRef] PRIMARY KEY CLUSTERED ([OBJECTID] ASC, [HEARINGID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_HearingXRef_Hearing] FOREIGN KEY ([HEARINGID]) REFERENCES [dbo].[HEARING] ([HEARINGID])
);


GO
CREATE NONCLUSTERED INDEX [IX_HEARXREF_PARENT_METNG]
    ON [dbo].[HEARINGXREF]([OBJECTID] ASC, [HEARINGID] ASC) WITH (FILLFACTOR = 90);

