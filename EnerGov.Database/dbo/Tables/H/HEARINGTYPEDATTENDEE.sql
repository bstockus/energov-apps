CREATE TABLE [dbo].[HEARINGTYPEDATTENDEE] (
    [HEARINGTYPEDATTENDEEID] CHAR (36) NOT NULL,
    [HEARINGTYPEID]          CHAR (36) NULL,
    [ATTENDEEID]             CHAR (36) NULL,
    [ISUSER]                 BIT       NULL,
    [ISCONTACT]              BIT       NULL,
    CONSTRAINT [PK_HEARINGTYPEDATTENDEE] PRIMARY KEY CLUSTERED ([HEARINGTYPEDATTENDEEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_HEARINGTYPEDATTENDEE_TYP] FOREIGN KEY ([HEARINGTYPEID]) REFERENCES [dbo].[HEARINGTYPE] ([HEARINGTYPEID])
);


GO
CREATE NONCLUSTERED INDEX [HEARINGTYPEDATTENDEE_IX_HEARINGTYPEID]
    ON [dbo].[HEARINGTYPEDATTENDEE]([HEARINGTYPEID] ASC);


GO
CREATE TRIGGER [TG_HEARINGTYPEDATTENDEE_INSERT] ON [HEARINGTYPEDATTENDEE]
	AFTER INSERT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
			[HEARINGTYPE].[HEARINGTYPEID],
			[HEARINGTYPE].[ROWVERSION],
			GETUTCDATE(),
			[HEARINGTYPE].[LASTCHANGEDBY],
			'Hearing Type ' + CASE WHEN [inserted].[ISUSER] = 1 THEN 'User ' ELSE 'Contact ' END + 'Added',
			'',
			'',
			'Hearing Type (' + [HEARINGTYPE].[NAME] + '), '+ CASE WHEN [inserted].[ISUSER] = 1 THEN 'User (' + [dbo].[USERS].[LNAME] + COALESCE(', ' + [dbo].[USERS].[FNAME], '') ELSE 'Contact (' + [dbo].[GLOBALENTITY].[LASTNAME]+ ', '+ [dbo].[GLOBALENTITY].[FIRSTNAME] END + ')',
			'D2EF914D-EACD-4AA2-AF1B-6B2E30AE074B',
			1,
			0,
			CASE WHEN [inserted].[ISUSER] = 1 THEN [dbo].[USERS].[LNAME] + COALESCE(', ' + [dbo].[USERS].[FNAME], '') ELSE [dbo].[GLOBALENTITY].[LASTNAME]+ ', '+ [dbo].[GLOBALENTITY].[FIRSTNAME] END
	FROM	[inserted]
	INNER JOIN HEARINGTYPE ON [inserted].[HEARINGTYPEID] = [HEARINGTYPE].[HEARINGTYPEID]
	LEFT JOIN USERS  WITH (NOLOCK) ON [inserted].[ATTENDEEID] = [USERS].[SUSERGUID]
	LEFT JOIN GLOBALENTITY  WITH (NOLOCK) ON [inserted].[ATTENDEEID] = [GLOBALENTITY].[GLOBALENTITYID]
END
GO

CREATE TRIGGER [TG_HEARINGTYPEDATTENDEE_DELETE] ON [HEARINGTYPEDATTENDEE]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[HEARINGTYPE].[HEARINGTYPEID],
			[HEARINGTYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Hearing Type ' + CASE WHEN [deleted].[ISUSER] = 1 THEN 'User ' ELSE 'Contact ' END + 'Deleted',
			'',
			'',
			'Hearing Type (' + [HEARINGTYPE].[NAME] + '), '+ CASE WHEN [deleted].[ISUSER] = 1 THEN 'User (' + [dbo].[USERS].[LNAME] + COALESCE(', ' + [dbo].[USERS].[FNAME], '') ELSE 'Contact (' + [dbo].[GLOBALENTITY].[LASTNAME]+ ', '+ [dbo].[GLOBALENTITY].[FIRSTNAME] END + ')',
			'D2EF914D-EACD-4AA2-AF1B-6B2E30AE074B',
			3,
			0,
			CASE WHEN [deleted].[ISUSER] = 1 THEN [dbo].[USERS].[LNAME] + COALESCE(', ' + [dbo].[USERS].[FNAME], '') ELSE [dbo].[GLOBALENTITY].[LASTNAME]+ ', '+ [dbo].[GLOBALENTITY].[FIRSTNAME] END
	FROM	[deleted]	
	INNER JOIN HEARINGTYPE ON [deleted].[HEARINGTYPEID] = [HEARINGTYPE].[HEARINGTYPEID]
	LEFT JOIN USERS  WITH (NOLOCK) ON [deleted].[ATTENDEEID] = [USERS].[SUSERGUID]
	LEFT JOIN GLOBALENTITY  WITH (NOLOCK) ON [deleted].[ATTENDEEID] = [GLOBALENTITY].[GLOBALENTITYID]
END