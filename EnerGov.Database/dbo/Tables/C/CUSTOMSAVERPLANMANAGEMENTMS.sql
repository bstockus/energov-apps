    CONSTRAINT [PlanManagementMS_PlanManagemen] FOREIGN KEY ([ID]) REFERENCES [dbo].[CUSTOMSAVERPLANMANAGEMENT] ([ID])
);


GO
CREATE NONCLUSTERED INDEX [PlanManagementMS_ID]
    ON [dbo].[CUSTOMSAVERPLANMANAGEMENTMS]([ID] ASC) WITH (FILLFACTOR = 90);


GO

CREATE TRIGGER [TG_CUSTOMSAVERPLANMANAGEMENTMS_UPDATE_ELASTIC] ON  CUSTOMSAVERPLANMANAGEMENTMS
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[P].[PLPLANID] ,
        'EnerGovBusiness.PlanManagement.Plan' ,
        [P].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [PLPLAN] AS [P] WITH (NOLOCK) ON [P].[PLPLANID] = [Inserted].[ID];

END