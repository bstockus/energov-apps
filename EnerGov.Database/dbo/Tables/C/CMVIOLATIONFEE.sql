CREATE TABLE [dbo].[CMVIOLATIONFEE] (
    [CMVIOLATIONFEEID] CHAR (36) NOT NULL,
    [CMVIOLATIONID]    CHAR (36) NOT NULL,
    [CACOMPUTEDFEEID]  CHAR (36) NOT NULL,
    [CREATEDON]        DATETIME  NOT NULL,
    CONSTRAINT [PK_CMViolationFee] PRIMARY KEY NONCLUSTERED ([CMVIOLATIONFEEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CMViolationFee_ComputedFee] FOREIGN KEY ([CACOMPUTEDFEEID]) REFERENCES [dbo].[CACOMPUTEDFEE] ([CACOMPUTEDFEEID]),
    CONSTRAINT [FK_CMViolationFee_Violation] FOREIGN KEY ([CMVIOLATIONID]) REFERENCES [dbo].[CMVIOLATION] ([CMVIOLATIONID])
);


GO
CREATE NONCLUSTERED INDEX [IX_CMViolationFee_Violation]
    ON [dbo].[CMVIOLATIONFEE]([CMVIOLATIONID] ASC) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IX_CMVIOLATIONFEE_CACOMPUTEDFEEID_CMVIOLATIONID]
    ON [dbo].[CMVIOLATIONFEE]([CACOMPUTEDFEEID] ASC)
    INCLUDE([CMVIOLATIONID]);

