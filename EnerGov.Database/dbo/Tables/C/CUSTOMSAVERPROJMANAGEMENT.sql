CREATE TABLE [dbo].[CUSTOMSAVERPROJMANAGEMENT] (
    [ID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_ProjectManagement] PRIMARY KEY NONCLUSTERED ([ID] ASC) WITH (FILLFACTOR = 90)
);


GO

CREATE TRIGGER [TG_CUSTOMSAVERPROJMANAGEMENT_UPDATE_ELASTIC] ON  CUSTOMSAVERPROJMANAGEMENT
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[P].[PRPROJECTID] ,
        'EnerGovBusiness.ProjectManagement.Project' ,
        [P].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [PRPROJECT] AS [P] WITH (NOLOCK) ON [P].[PRPROJECTID] = [Inserted].[ID];

END
