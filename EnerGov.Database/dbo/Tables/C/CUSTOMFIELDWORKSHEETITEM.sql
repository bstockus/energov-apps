CREATE TABLE [dbo].[CUSTOMFIELDWORKSHEETITEM] (
    [GCUSTOMFIELDWORKSHEETITEM] CHAR (36)    NOT NULL,
    [FKGCUSTOMFIELDWORKSHEET]   CHAR (36)    NOT NULL,
    [SVALUE]                    VARCHAR (50) NOT NULL,
    [IORDER]                    INT          NOT NULL,
    CONSTRAINT [PK_CUSTOMFIELDWORKSHEETITEM] PRIMARY KEY CLUSTERED ([GCUSTOMFIELDWORKSHEETITEM] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CUSTFLDWSITEM_CUSTFLDWS] FOREIGN KEY ([FKGCUSTOMFIELDWORKSHEET]) REFERENCES [dbo].[CUSTOMFIELDWORKSHEET] ([GCUSTOMFIELDWORKSHEET])
);


GO
