CREATE TRIGGER [dbo].[TG_CUSTOMFIELDWORKSHEETITEM_UPDATE]
   ON  [dbo].[CUSTOMFIELDWORKSHEETITEM] 
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
        [CUSTOMFIELDLAYOUT].[GCUSTOMFIELDLAYOUTS], 
        [CUSTOMFIELDLAYOUT].[ROWVERSION],
        GETUTCDATE(),
        [CUSTOMFIELDLAYOUT].[LASTCHANGEDBY],	
        'Value',
        [deleted].[SVALUE],
        [inserted].[SVALUE],       
		'Custom Field Layout (' + [CUSTOMFIELDLAYOUT].[SNAME] + '), Field (' + [CUSTOMFIELD].[SCUSTOMFIELD] +'), Item (' + [inserted].[SVALUE] +')',
        'CCD23415-9668-462D-AFCC-3794BBAB9ADB',
		2,
		0,
		[inserted].[SVALUE]
    FROM [deleted]
    INNER JOIN [inserted] ON [inserted].[GCUSTOMFIELDWORKSHEETITEM] = [deleted].[GCUSTOMFIELDWORKSHEETITEM] 
    INNER JOIN [CUSTOMFIELDWORKSHEET] ON [CUSTOMFIELDWORKSHEET].[GCUSTOMFIELDWORKSHEET] = [inserted].[FKGCUSTOMFIELDWORKSHEET]
	INNER JOIN [CUSTOMFIELD] ON [CUSTOMFIELD].[GCUSTOMFIELD] = [CUSTOMFIELDWORKSHEET].[FKGCUSTOMFIELD]
    INNER JOIN [CUSTOMFIELDOBJECT] ON [CUSTOMFIELDOBJECT].[GCUSTOMFIELD] = [CUSTOMFIELD].[GCUSTOMFIELD]
	INNER JOIN [CUSTOMFIELDLAYOUT] ON [CUSTOMFIELDLAYOUT].[GCUSTOMFIELDLAYOUTS] = [CUSTOMFIELDOBJECT].[FKGCUSTOMFIELDLAYOUT]
    WHERE [deleted].[SVALUE] <> [inserted].[SVALUE]
    UNION ALL
	SELECT 
        [CUSTOMFIELDLAYOUT].[GCUSTOMFIELDLAYOUTS], 
        [CUSTOMFIELDLAYOUT].[ROWVERSION],
        GETUTCDATE(),
        [CUSTOMFIELDLAYOUT].[LASTCHANGEDBY],	
        'Order',
        CAST([deleted].[IORDER] AS NVARCHAR(MAX)),
        CAST([inserted].[IORDER] AS NVARCHAR(MAX)),
		'Custom Field Layout (' + [CUSTOMFIELDLAYOUT].[SNAME] + '), Field (' + [CUSTOMFIELD].[SCUSTOMFIELD] +'), Item (' + [inserted].[SVALUE] +')',
        'CCD23415-9668-462D-AFCC-3794BBAB9ADB',
		2,
		0,
		[inserted].[SVALUE]
    FROM [deleted]
    INNER JOIN [inserted] ON [inserted].[GCUSTOMFIELDWORKSHEETITEM] = [deleted].[GCUSTOMFIELDWORKSHEETITEM] 
    INNER JOIN [CUSTOMFIELDWORKSHEET] ON [CUSTOMFIELDWORKSHEET].[GCUSTOMFIELDWORKSHEET] = [inserted].[FKGCUSTOMFIELDWORKSHEET]
	INNER JOIN [CUSTOMFIELD] ON [CUSTOMFIELD].[GCUSTOMFIELD] = [CUSTOMFIELDWORKSHEET].[FKGCUSTOMFIELD]
    INNER JOIN [CUSTOMFIELDOBJECT] ON [CUSTOMFIELDOBJECT].[GCUSTOMFIELD] = [CUSTOMFIELD].[GCUSTOMFIELD]
	INNER JOIN [CUSTOMFIELDLAYOUT] ON [CUSTOMFIELDLAYOUT].[GCUSTOMFIELDLAYOUTS] = [CUSTOMFIELDOBJECT].[FKGCUSTOMFIELDLAYOUT]
    WHERE [deleted].[IORDER] <> [inserted].[IORDER]
END