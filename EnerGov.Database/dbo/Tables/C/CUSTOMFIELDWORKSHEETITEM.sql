    ON [dbo].[CUSTOMFIELDWORKSHEETITEM]([FKGCUSTOMFIELDWORKSHEET] ASC);


GO

CREATE TRIGGER [dbo].[TG_CUSTOMFIELDWORKSHEETITEM_DELETE]  
    ON  [dbo].[CUSTOMFIELDWORKSHEETITEM]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
        [CUSTOMFIELDLAYOUT].[GCUSTOMFIELDLAYOUTS], 
        [CUSTOMFIELDLAYOUT].[ROWVERSION],
        GETUTCDATE(),
        [CUSTOMFIELDLAYOUT].[LASTCHANGEDBY],	
        'Custom Field Layout Worksheet Item Deleted',
        '',
        '',       
		'Custom Field Layout (' + [CUSTOMFIELDLAYOUT].[SNAME] + '), Field (' + [CUSTOMFIELD].[SCUSTOMFIELD] +'), Item (' + [deleted].[SVALUE] +')',
        'CCD23415-9668-462D-AFCC-3794BBAB9ADB',
		3,
		0,
		[deleted].[SVALUE]
    FROM [deleted]
    INNER JOIN [CUSTOMFIELDWORKSHEET] ON [CUSTOMFIELDWORKSHEET].[GCUSTOMFIELDWORKSHEET] = [deleted].[FKGCUSTOMFIELDWORKSHEET]
	INNER JOIN [CUSTOMFIELD] ON [CUSTOMFIELD].[GCUSTOMFIELD] = [CUSTOMFIELDWORKSHEET].[FKGCUSTOMFIELD]
    INNER JOIN [CUSTOMFIELDOBJECT] ON [CUSTOMFIELDOBJECT].[GCUSTOMFIELD] = [CUSTOMFIELD].[GCUSTOMFIELD]
	INNER JOIN [CUSTOMFIELDLAYOUT] ON [CUSTOMFIELDLAYOUT].[GCUSTOMFIELDLAYOUTS] = [CUSTOMFIELDOBJECT].[FKGCUSTOMFIELDLAYOUT]
	
END
GO

