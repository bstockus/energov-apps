CREATE TABLE [dbo].[CAINVOICEGROUP] (
    [CAINVOICEGROUPID]   CHAR (36)      NOT NULL,
    [INVOICEGROUPNUMBER] NVARCHAR (255) NOT NULL,
    [CREATEDATE]         DATETIME       NOT NULL,
    [USERID]             CHAR (36)      NOT NULL,
    CONSTRAINT [PK_CAINVOICEGROUP] PRIMARY KEY NONCLUSTERED ([CAINVOICEGROUPID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CAINVOICEGROUP_USERS] FOREIGN KEY ([USERID]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [IX_CAINVOICEGROUP_NUMBER]
    ON [dbo].[CAINVOICEGROUP]([INVOICEGROUPNUMBER] ASC) WITH (FILLFACTOR = 90);

