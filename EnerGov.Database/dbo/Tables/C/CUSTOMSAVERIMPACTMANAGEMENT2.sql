CREATE TABLE [dbo].[CUSTOMSAVERIMPACTMANAGEMENT2] (
    [ID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_CUSTOMSAVERIMPACT2] PRIMARY KEY CLUSTERED ([ID] ASC) WITH (FILLFACTOR = 90)
);


GO

CREATE TRIGGER [TG_CUSTOMSAVERIMPACTMANAGEMENT2_UPDATE_ELASTIC] ON CUSTOMSAVERIMPACTMANAGEMENT2
	AFTER UPDATE
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID(),
		[ImpactCase].[IPCASEID],
		'EnerGovBusiness.ImpactManagement.ImpactCase',
		[ImpactCase].[ROWVERSION],
		GETDATE(),
		NULL,
		2,
		(SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [IPCASE] AS [ImpactCase] WITH (NOLOCK) ON [ImpactCase].[IPCASEID] = [Inserted].[ID];
END
