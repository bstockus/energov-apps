CREATE TABLE [dbo].[CAFEETEMPLATEFEEINPUT] (
    [CAFEETEMPLATEFEEINPUTID]  CHAR (36)      NOT NULL,
    [CAFEETEMPLATEFEEID]       CHAR (36)      NOT NULL,
    [CAFEETEMPLATEINPUTTYPEID] INT            CONSTRAINT [DF_CAFeeTemplFeeInput_Type] DEFAULT ((1)) NOT NULL,
    [INPUTVALUE]               NVARCHAR (50)  NULL,
    [INPUTFRIENDLYNAME]        NVARCHAR (MAX) NULL,
    [ISDISPLAY]                BIT            CONSTRAINT [DF_CAFeeTemplFeeInput_IsDisplay] DEFAULT ((0)) NOT NULL,
    [CUSTOMFIELDTABLE]         NVARCHAR (MAX) NULL,
    [CLASSNAME]                NVARCHAR (MAX) NULL,
    CONSTRAINT [PK_CAFeeTemplateFeeInput] PRIMARY KEY CLUSTERED ([CAFEETEMPLATEFEEINPUTID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_CAFeeTemplFeeInput_Fee] FOREIGN KEY ([CAFEETEMPLATEFEEID]) REFERENCES [dbo].[CAFEETEMPLATEFEE] ([CAFEETEMPLATEFEEID]),
    CONSTRAINT [FK_CAFeeTemplFeeInput_Type] FOREIGN KEY ([CAFEETEMPLATEINPUTTYPEID]) REFERENCES [dbo].[CAFEETEMPLATEINPUTTYPE] ([CAFEETEMPLATEINPUTTYPEID])
);


GO
CREATE NONCLUSTERED INDEX [IX_CAFEETEMPLATEFEEINPUT_CAFEETEMPLATEFEEID]
    ON [dbo].[CAFEETEMPLATEFEEINPUT]([CAFEETEMPLATEFEEID] ASC);


GO

CREATE TRIGGER [dbo].[TG_CAFEETEMPLATEFEEINPUT_DELETE]  ON  [dbo].[CAFEETEMPLATEFEEINPUT]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[CAFEETEMPLATE].[CAFEETEMPLATEID],
		[CAFEETEMPLATE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
		'Fee Template, Fee/Child Template, Fee Template Input Deleted',
        '',
        '',       
		'Fee Template (' + [CAFEETEMPLATE].[CAFEETEMPLATENAME] + '), Fee/Child Template (' + [CAFEETEMPLATEFEE].[FEENAME] +'), Fee Template Input ('+ [deleted].[INPUTFRIENDLYNAME] +' )',
		'CCA2295B-072A-494B-9C11-91424F59BC16',
		3,
		0,
		[CAFEETEMPLATE].[CAFEETEMPLATENAME]
    FROM [deleted]
	INNER JOIN [dbo].[CAFEETEMPLATEFEE] ON [deleted].[CAFEETEMPLATEFEEID] = [CAFEETEMPLATEFEE].[CAFEETEMPLATEFEEID] 
	INNER JOIN [dbo].[CAFEETEMPLATE] ON [CAFEETEMPLATE].[CAFEETEMPLATEID] = [CAFEETEMPLATEFEE].[CAFEETEMPLATEID]	
END
GO

