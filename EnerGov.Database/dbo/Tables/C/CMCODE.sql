CREATE TRIGGER [dbo].[TG_CMCODE_DELETE] 
   ON  [dbo].[CMCODE]
   AFTER DELETE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )SELECT
			[deleted].[CMCODEID],
			[deleted].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Code Deleted',
			'',
			'',
			'Code (' + [deleted].[CODENUMBER] + ')',
			'3FAFB6FE-439F-48B0-978D-748BE8F5D52C',
			3,
			1,
			[deleted].[CODENUMBER]
	FROM	[deleted]
END