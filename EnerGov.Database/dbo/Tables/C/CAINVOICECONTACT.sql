CREATE TABLE [dbo].[CAINVOICECONTACT] (
    [CAINVOICECONTACTID] CHAR (36) NOT NULL,
    [CAINVOICEID]        CHAR (36) NOT NULL,
    [GLOBALENTITYID]     CHAR (36) NOT NULL,
    [ISACTIVE]           BIT       DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_CAINVOICECONTACT] PRIMARY KEY NONCLUSTERED ([CAINVOICECONTACTID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_CAINVCONTACT_CONTACT] FOREIGN KEY ([GLOBALENTITYID]) REFERENCES [dbo].[GLOBALENTITY] ([GLOBALENTITYID]),
    CONSTRAINT [FK_CAINVCONTACT_INVOICE] FOREIGN KEY ([CAINVOICEID]) REFERENCES [dbo].[CAINVOICE] ([CAINVOICEID])
);


GO
CREATE NONCLUSTERED INDEX [IX_CAINVOICECONTACT_GLBENT]
    ON [dbo].[CAINVOICECONTACT]([GLOBALENTITYID] ASC, [ISACTIVE] ASC)
    INCLUDE([CAINVOICEID]) WITH (FILLFACTOR = 80);


GO
CREATE NONCLUSTERED INDEX [NCIDX_CAINVOICECONTACT_CAINVOICEID]
    ON [dbo].[CAINVOICECONTACT]([CAINVOICEID] ASC) WITH (FILLFACTOR = 90, PAD_INDEX = ON);

