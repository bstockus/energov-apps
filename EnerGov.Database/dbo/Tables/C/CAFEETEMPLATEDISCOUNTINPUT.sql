CREATE TABLE [dbo].[CAFEETEMPLATEDISCOUNTINPUT] (
    [CAFEETEMPLATEDISCOUNTINPUTID] CHAR (36)      NOT NULL,
    [CAFEETEMPLATEDISCOUNTID]      CHAR (36)      NOT NULL,
    [CAFEETEMPLATEINPUTTYPEID]     INT            NOT NULL,
    [INPUTVALUE]                   NVARCHAR (50)  NOT NULL,
    [INPUTFRIENDLYNAME]            NVARCHAR (MAX) NOT NULL,
    [ISDISPLAY]                    BIT            CONSTRAINT [DF_CAFeeTemplateDiscountInput_IsDisplay] DEFAULT ((0)) NOT NULL,
    [CLASSNAME]                    NVARCHAR (MAX) NULL,
    CONSTRAINT [PK_CAFeeTemplDisctInputID] PRIMARY KEY CLUSTERED ([CAFEETEMPLATEDISCOUNTINPUTID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CAFeeTmplDisctInput_Disct] FOREIGN KEY ([CAFEETEMPLATEDISCOUNTID]) REFERENCES [dbo].[CAFEETEMPLATEDISCOUNT] ([CAFEETEMPLATEDISCOUNTID]),
    CONSTRAINT [FK_CAFeeTmplDisctInput_Type] FOREIGN KEY ([CAFEETEMPLATEINPUTTYPEID]) REFERENCES [dbo].[CAFEETEMPLATEINPUTTYPE] ([CAFEETEMPLATEINPUTTYPEID])
);


GO
CREATE NONCLUSTERED INDEX [IX_CAFEETEMPLATEDISCOUNTINPUT_CAFEETEMPLATEDISCOUNTID]
    ON [dbo].[CAFEETEMPLATEDISCOUNTINPUT]([CAFEETEMPLATEDISCOUNTID] ASC);


GO

CREATE TRIGGER [dbo].[TG_CAFEETEMPLATEDISCOUNTINPUT_DELETE]  ON  [dbo].[CAFEETEMPLATEDISCOUNTINPUT]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[CAFEETEMPLATE].[CAFEETEMPLATEID],
		[CAFEETEMPLATE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
        'Fee Template, Fee Template Discount, Fee Template Discount Input Deleted',
        '',
        '',       
		'Fee Template (' + [CAFEETEMPLATE].[CAFEETEMPLATENAME] + '), Fee Template Discount (' + [CADISCOUNT].[NAME] +'), Fee Template Discount Input ('+ [deleted].[INPUTFRIENDLYNAME] +' )',
		'CCA2295B-072A-494B-9C11-91424F59BC16',
		3,
		0,
		[CAFEETEMPLATE].[CAFEETEMPLATENAME]
    FROM [deleted]
	INNER JOIN [dbo].[CAFEETEMPLATEDISCOUNT] ON [deleted].[CAFEETEMPLATEDISCOUNTID] = [CAFEETEMPLATEDISCOUNT].[CAFEETEMPLATEDISCOUNTID]
	INNER JOIN [dbo].[CADISCOUNT] ON [CADISCOUNT].[CADISCOUNTID] = [CAFEETEMPLATEDISCOUNT].[CADISCOUNTID]
	INNER JOIN [dbo].[CAFEETEMPLATE] ON [CAFEETEMPLATEDISCOUNT].[CAFEETEMPLATEID] = [CAFEETEMPLATE].[CAFEETEMPLATEID]
END
GO

