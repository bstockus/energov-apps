CREATE TABLE [dbo].[CAFEEINPUTTRANSLATION] (
    [CAFEEINPUTTRANSLATIONID] CHAR (36)      NOT NULL,
    [CAFEETEMPLATEFEEINPUTID] CHAR (36)      NOT NULL,
    [PROPERTYVALUE]           NVARCHAR (MAX) NULL,
    [TRANSLATEDVALUE]         NVARCHAR (MAX) NULL,
    CONSTRAINT [PK_CAFeeInputTranslation] PRIMARY KEY CLUSTERED ([CAFEEINPUTTRANSLATIONID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_Translate_Input] FOREIGN KEY ([CAFEETEMPLATEFEEINPUTID]) REFERENCES [dbo].[CAFEETEMPLATEFEEINPUT] ([CAFEETEMPLATEFEEINPUTID])
);


GO

CREATE TRIGGER [dbo].[TG_CAFEEINPUTTRANSLATION_DELETE]  ON  [dbo].[CAFEEINPUTTRANSLATION]
   AFTER DELETE
AS 
BEGIN
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
		[CAFEETEMPLATE].[CAFEETEMPLATEID],
		[CAFEETEMPLATE].[ROWVERSION],
		GETUTCDATE(),			
		(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
		'Fee Template, Fee/Child Template, Fee Translation Deleted',
        '',
        '',       
		'Fee Template (' + [CAFEETEMPLATE].[CAFEETEMPLATENAME] + '), Fee/Child Template (' + [CAFEETEMPLATEFEE].[FEENAME] +'), Fee Input Translation (' + [CAFEETEMPLATEFEEINPUT].[INPUTFRIENDLYNAME] +')',
		'CCA2295B-072A-494B-9C11-91424F59BC16',
		3,
		0,
		[CAFEETEMPLATE].[CAFEETEMPLATENAME]
    FROM [deleted]
	INNER JOIN [dbo].[CAFEETEMPLATEFEEINPUT] ON [CAFEETEMPLATEFEEINPUT].[CAFEETEMPLATEFEEINPUTID] = [deleted].[CAFEETEMPLATEFEEINPUTID]
	INNER JOIN [dbo].[CAFEETEMPLATEFEE] ON [CAFEETEMPLATEFEE].[CAFEETEMPLATEFEEID] = [CAFEETEMPLATEFEEINPUT].[CAFEETEMPLATEFEEID]
	INNER JOIN [dbo].[CAFEETEMPLATE] ON [CAFEETEMPLATEFEE].[CAFEETEMPLATEID] = [CAFEETEMPLATE].[CAFEETEMPLATEID]
END
GO

