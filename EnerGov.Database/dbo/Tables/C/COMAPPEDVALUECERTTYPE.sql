CREATE TABLE [dbo].[COMAPPEDVALUECERTTYPE] (
    [COMAPPEDVALUECERTTYPEID] CHAR (36)    NOT NULL,
    [MAPPEDTYPE]              VARCHAR (50) NULL,
    [COSIMPLELICCERTTYPEID]   CHAR (36)    NULL,
    [MAPPEDCATEGORY]          VARCHAR (50) NULL,
    [LASTCHANGEDBY]           CHAR (36)    NULL,
    [LASTCHANGEDON]           DATETIME     CONSTRAINT [DF_COMAPPEDVALUECERTTYPE_LastChangedOn] DEFAULT (getutcdate()) NOT NULL,
    [ROWVERSION]              INT          CONSTRAINT [DF_COMAPPEDVALUECERTTYPE_RowVersion] DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_COMappedValueCertType] PRIMARY KEY CLUSTERED ([COMAPPEDVALUECERTTYPEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_MappedValue_CertType] FOREIGN KEY ([COSIMPLELICCERTTYPEID]) REFERENCES [dbo].[COLICENSECERTIFICATIONTYPE] ([COSIMPLELICCERTTYPEID])
);


GO
CREATE TRIGGER [TG_COMAPPEDVALUECERTTYPE_INSERT] ON [dbo].[COMAPPEDVALUECERTTYPE]
   AFTER INSERT
AS 
BEGIN
	SET NOCOUNT ON;	
	-- Check if LASTCHANGEDBY contains VALID User Id and it Exists in USERS table, this is in replacement to foreign key reference of COMAPPEDVALUECERTTYPE table with USERS table.
	IF EXISTS (SELECT * FROM inserted 
		LEFT OUTER JOIN USERS WITH (NOLOCK) ON USERS.SUSERGUID = inserted.LASTCHANGEDBY
		WHERE inserted.LASTCHANGEDBY IS NOT NULL AND USERS.SUSERGUID IS NULL)
	BEGIN		
		RAISERROR ('The INSERT or UPDATE statement conflicted with the FOREIGN KEY to table USERS', 16, 0);
		ROLLBACK;
		RETURN;
	END

    INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT
			[inserted].[COMAPPEDVALUECERTTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Mapping Certification Type Added',
			'',
			'',
			ISNULL([dbo].[COLICENSECERTIFICATIONTYPE].[NAME],'[none]'),
			'6115EBA3-71FF-4CEA-AB43-759135A83E0F',
			1,
			0,
			ISNULL([dbo].[COLICENSECERTIFICATIONTYPE].[NAME],'[none]')
	FROM	[inserted]
	LEFT JOIN [dbo].[COLICENSECERTIFICATIONTYPE] ON [dbo].[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID] = [inserted].[COSIMPLELICCERTTYPEID]
END
