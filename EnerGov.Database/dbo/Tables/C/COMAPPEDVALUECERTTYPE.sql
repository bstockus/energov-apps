	WHERE	ISNULL([deleted].[COSIMPLELICCERTTYPEID],'') <> ISNULL([inserted].[COSIMPLELICCERTTYPEID],'')
	UNION ALL

	SELECT
			[inserted].[COMAPPEDVALUECERTTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Mapped Type',
			ISNULL([deleted].[MAPPEDTYPE],'[none]'),
			ISNULL([inserted].[MAPPEDTYPE],'[none]'),
			ISNULL([dbo].[COLICENSECERTIFICATIONTYPE].[NAME],'[none]'),
			'6115EBA3-71FF-4CEA-AB43-759135A83E0F',
			2,
			0,
			ISNULL([dbo].[COLICENSECERTIFICATIONTYPE].[NAME],'[none]')
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[COMAPPEDVALUECERTTYPEID] = [inserted].[COMAPPEDVALUECERTTYPEID]
			LEFT JOIN [dbo].[COLICENSECERTIFICATIONTYPE] ON [dbo].[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID] = [inserted].[COSIMPLELICCERTTYPEID]
	WHERE	ISNULL([deleted].[MAPPEDTYPE],'') <> ISNULL([inserted].[MAPPEDTYPE],'')
	UNION ALL

	SELECT
			[inserted].[COMAPPEDVALUECERTTYPEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Mapped Category',
			ISNULL([deleted].[MAPPEDCATEGORY] ,'[none]'),
