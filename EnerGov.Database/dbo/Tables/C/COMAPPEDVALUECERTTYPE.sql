			LEFT JOIN [dbo].[COLICENSECERTIFICATIONTYPE] [deleted_COLICENSECERTIFICATIONTYPE] ON [deleted_COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID] = [deleted].[COSIMPLELICCERTTYPEID]
