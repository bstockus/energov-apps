CREATE TRIGGER [dbo].[TG_CATEMPLATEFEEDISCOUNTXREF_UPDATE]   ON  [dbo].[CATEMPLATEFEEDISCOUNTXREF] 
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;
	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT 
			[CAFEETEMPLATE].[CAFEETEMPLATEID],
			[CAFEETEMPLATE].[ROWVERSION],
			GETUTCDATE(),			
			[CAFEETEMPLATE].[LASTCHANGEDBY],
			'Sort Order',			
			CASE WHEN [deleted].[SORTORDER] IS NULL THEN '[none]' ELSE CONVERT(NVARCHAR(MAX), [deleted].[SORTORDER]) END,
			CASE WHEN [inserted].[SORTORDER] IS NULL THEN '[none]' ELSE CONVERT(NVARCHAR(MAX), [inserted].[SORTORDER]) END,		
			'Fee Template (' + [CAFEETEMPLATE].[CAFEETEMPLATENAME] + '), Fee/Child Template (' + [CAFEETEMPLATEFEE].[FEENAME] +'), Fee Template Discount (' + [CADISCOUNT].[NAME] +')',
			'CCA2295B-072A-494B-9C11-91424F59BC16',
			2,
			0,
			[CAFEETEMPLATE].[CAFEETEMPLATENAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[CATEMPLATEFEEDISCOUNTXREFID] = [inserted].[CATEMPLATEFEEDISCOUNTXREFID]			
			INNER JOIN [dbo].[CAFEETEMPLATEFEE] ON [CAFEETEMPLATEFEE].[CAFEETEMPLATEFEEID] = [inserted].[CAFEETEMPLATEFEEID]
			INNER JOIN [dbo].[CAFEETEMPLATE] ON [CAFEETEMPLATE].[CAFEETEMPLATEID] = [CAFEETEMPLATEFEE].[CAFEETEMPLATEID]
			INNER JOIN [dbo].[CAFEETEMPLATEDISCOUNT] WITH (NOLOCK) ON [CAFEETEMPLATEDISCOUNT].CAFEETEMPLATEDISCOUNTID = [inserted].CAFEETEMPLATEDISCOUNTID
			LEFT JOIN [dbo].[CADISCOUNT] ON [CADISCOUNT].[CADISCOUNTID] = [CAFEETEMPLATEDISCOUNT].[CADISCOUNTID]
	WHERE	ISNULL([deleted].[SORTORDER],'') <> ISNULL([inserted].[SORTORDER],'')
END