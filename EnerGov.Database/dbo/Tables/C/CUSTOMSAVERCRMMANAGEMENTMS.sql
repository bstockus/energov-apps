CREATE TABLE [dbo].[CUSTOMSAVERCRMMANAGEMENTMS] (
    [ID]                        CHAR (36) NOT NULL,
    [CUSTOMFIELDID]             CHAR (36) NOT NULL,
    [CUSTOMFIELDPICKLISTITEMID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_CRMManagementMS] PRIMARY KEY NONCLUSTERED ([ID] ASC, [CUSTOMFIELDID] ASC, [CUSTOMFIELDPICKLISTITEMID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [CRMManagementMS_CRMManagement] FOREIGN KEY ([ID]) REFERENCES [dbo].[CUSTOMSAVERCRMMANAGEMENT] ([ID]),
    CONSTRAINT [CRMManagementMS_CustomField] FOREIGN KEY ([CUSTOMFIELDID]) REFERENCES [dbo].[CUSTOMFIELD] ([GCUSTOMFIELD]),
