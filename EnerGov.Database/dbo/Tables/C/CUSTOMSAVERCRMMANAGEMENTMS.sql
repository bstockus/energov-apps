);


GO
CREATE NONCLUSTERED INDEX [CRMManagementMS_ID]
    ON [dbo].[CUSTOMSAVERCRMMANAGEMENTMS]([ID] ASC) WITH (FILLFACTOR = 80);


GO

CREATE TRIGGER [TG_CUSTOMSAVERCRMMANAGEMENTMS_UPDATE_ELASTIC] ON  CUSTOMSAVERCRMMANAGEMENTMS
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[C].[CITIZENREQUESTID] ,
        'EnerGovBusiness.CitizenRequest.Requestmgmt' ,
        [C].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [CITIZENREQUEST] AS [C] WITH (NOLOCK) ON [C].[CITIZENREQUESTID] = [Inserted].[ID];

END