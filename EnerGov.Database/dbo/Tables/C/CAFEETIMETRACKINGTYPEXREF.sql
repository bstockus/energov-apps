CREATE TABLE [dbo].[CAFEETIMETRACKINGTYPEXREF] (
    [CAFEETIMETRACKINGTYPEXREFID] CHAR (36) NOT NULL,
    [CAFEEID]                     CHAR (36) NOT NULL,
    [TIMETRACKINGTYPEID]          CHAR (36) NOT NULL,
    CONSTRAINT [PK_CAFEETIMETRACKINGTYPEXREF] PRIMARY KEY CLUSTERED ([CAFEETIMETRACKINGTYPEXREFID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CAFEETTTYPE_CAFEE] FOREIGN KEY ([CAFEEID]) REFERENCES [dbo].[CAFEE] ([CAFEEID]),
    CONSTRAINT [FK_CAFEETTTYPE_TTTYPE] FOREIGN KEY ([TIMETRACKINGTYPEID]) REFERENCES [dbo].[TIMETRACKINGTYPE] ([TIMETRACKINGTYPEID])
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [IX_CAFEETIMETRACKINGTYPEXREF]
    ON [dbo].[CAFEETIMETRACKINGTYPEXREF]([CAFEEID] ASC, [TIMETRACKINGTYPEID] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_CAFEETIMETRACKINGTYPEXREF_CAFEEID]
    ON [dbo].[CAFEETIMETRACKINGTYPEXREF]([TIMETRACKINGTYPEID] ASC)
    INCLUDE([CAFEEID]);


GO

CREATE TRIGGER [TG_CAFEETIMETRACKINGTYPEXREF_DELETE] ON [CAFEETIMETRACKINGTYPEXREF]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[CAFEE].[CAFEEID],
			[CAFEE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Fee Time Tracking Type Deleted',
			'',
			'',
			'Fee (' + [CAFEE].[NAME] + '), Time Tracking Type (' + [TIMETRACKINGTYPE].[NAME] + ')',
			'A66F715D-7817-4C5C-8D32-D9B22581828C',
			3,
			0,
			[TIMETRACKINGTYPE].[NAME]
	FROM	[deleted]		
	INNER JOIN [CAFEE] ON [deleted].[CAFEEID] = [CAFEE].[CAFEEID]
	LEFT JOIN [TIMETRACKINGTYPE] ON [deleted].[TIMETRACKINGTYPEID] = [TIMETRACKINGTYPE].[TIMETRACKINGTYPEID]
END
GO
CREATE TRIGGER [TG_CAFEETIMETRACKINGTYPEXREF_INSERT] ON [CAFEETIMETRACKINGTYPEXREF]
	AFTER INSERT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
			[CAFEE].[CAFEEID],
			[CAFEE].[ROWVERSION],
			GETUTCDATE(),
			[CAFEE].[LASTCHANGEDBY],
			'Fee Time Tracking Type Added',
			'',
			'',
			'Fee (' + [CAFEE].[NAME] + '), Time Tracking Type (' + [TIMETRACKINGTYPE].[NAME] + ')',
			'A66F715D-7817-4C5C-8D32-D9B22581828C',
			1,
			0,
			[TIMETRACKINGTYPE].[NAME]
	FROM	[inserted]
	INNER JOIN [CAFEE] ON [inserted].[CAFEEID] = [CAFEE].[CAFEEID]
	LEFT JOIN [TIMETRACKINGTYPE] ON [inserted].[TIMETRACKINGTYPEID] = [TIMETRACKINGTYPE].[TIMETRACKINGTYPEID]	
END