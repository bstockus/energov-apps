CREATE TABLE [dbo].[CMCODECASEZONE] (
    [CMCODECASEZONEID] CHAR (36) NOT NULL,
    [CMCODECASEID]     CHAR (36) NOT NULL,
    [ZONEID]           CHAR (36) NOT NULL,
    [MAIN]             BIT       NOT NULL,
    CONSTRAINT [PK_CodeCaseZone] PRIMARY KEY CLUSTERED ([CMCODECASEZONEID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [FK_CodeCaseZone_CodeCase] FOREIGN KEY ([CMCODECASEID]) REFERENCES [dbo].[CMCODECASE] ([CMCODECASEID]),
    CONSTRAINT [FK_CodeCaseZone_Zone] FOREIGN KEY ([ZONEID]) REFERENCES [dbo].[ZONE] ([ZONEID])
);


GO
CREATE NONCLUSTERED INDEX [IX_CMCODECASE_ZONE]
    ON [dbo].[CMCODECASEZONE]([CMCODECASEID] ASC, [ZONEID] ASC) WITH (FILLFACTOR = 80);

