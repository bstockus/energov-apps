CREATE TABLE [dbo].[CUSTOMSAVERINSPECTIONS2] (
    [ID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_Inspections2] PRIMARY KEY NONCLUSTERED ([ID] ASC) WITH (FILLFACTOR = 80)
);


GO

CREATE TRIGGER [TG_CUSTOMSAVERINSPECTIONS2_UPDATE_ELASTIC] ON  CUSTOMSAVERINSPECTIONS2
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[C].[IMINSPECTIONID] ,
        'EnerGovBusiness.Inspections.Inspection' ,
        [C].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [IMINSPECTION] AS [C] WITH (NOLOCK) ON [C].[IMINSPECTIONID] = [Inserted].[ID]

	UNION ALL

	SELECT 
		NEWID(),
		[IMINSPECTIONCASE].[IMINSPECTIONCASEID],
		'EnerGovBusiness.Inspections.InspectionCase',
		[IMINSPECTIONCASE].[ROWVERSION],
		GETDATE() ,
        NULL ,
		2,
		(SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [IMINSPECTIONCASE] WITH (NOLOCK) ON [IMINSPECTIONCASE].[IMINSPECTIONCASEID] = [Inserted].[ID];

END
