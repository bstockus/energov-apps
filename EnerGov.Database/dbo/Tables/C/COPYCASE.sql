CREATE TABLE [dbo].[COPYCASE] (
    [PARENTID]      CHAR (36) NOT NULL,
    [COPYCASEID]    CHAR (36) NOT NULL,
    [MODULEID]      INT       NOT NULL,
    [LASTCHANGEDON] DATETIME  NULL,
    [LASTCHANGEDBY] CHAR (36) NULL,
    CONSTRAINT [PK_COPYCASES] PRIMARY KEY CLUSTERED ([PARENTID] ASC, [COPYCASEID] ASC, [MODULEID] ASC),
    CONSTRAINT [FK_COPYCASE_LASTCHANGEDBY_USER] FOREIGN KEY ([LASTCHANGEDBY]) REFERENCES [dbo].[USERS] ([SUSERGUID])
);


GO
CREATE NONCLUSTERED INDEX [IX_COPYCASE_PARENTID]
    ON [dbo].[COPYCASE]([PARENTID] ASC);

