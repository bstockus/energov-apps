    CONSTRAINT [FK_CACPIREFERENCEDATE_TMPTYPE] FOREIGN KEY ([DATEOBJECTTYPEID]) REFERENCES [dbo].[CAFEETEMPLATEINPUTTYPE] ([CAFEETEMPLATEINPUTTYPEID])
