CREATE TABLE [dbo].[CACPIREFERENCEDATE] (
    [CACPIREFERENCEDATEID]   CHAR (36)      NOT NULL,
    [CAMODULEID]             INT            NOT NULL,
    [DATEOBJECTTYPEID]       INT            NOT NULL,
    [DATEOBJECT]             NVARCHAR (255) NOT NULL,
    [DATEOBJECTFRIENDLYNAME] NVARCHAR (500) NOT NULL,
    [ISDEFAULT]              BIT            NOT NULL,
    [LASTCHANGEDBY]          CHAR (36)      NULL,
    [LASTCHANGEDON]          DATETIME       CONSTRAINT [DF_CACPIREFERENCEDATE_LastChangedOn] DEFAULT (getutcdate()) NOT NULL,
    [ROWVERSION]             INT            CONSTRAINT [DF_CACPIREFERENCEDATE_RowVersion] DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_CACPIREFERENCEDATE] PRIMARY KEY CLUSTERED ([CACPIREFERENCEDATEID] ASC) WITH (FILLFACTOR = 90),
