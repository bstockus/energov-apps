CREATE TRIGGER [dbo].[TG_CACPIREFERENCEDATE_UPDATE] ON [dbo].[CACPIREFERENCEDATE]
   AFTER UPDATE
AS 
BEGIN	
	SET NOCOUNT ON;

	-- Check if LASTCHANGEDBY contains VALID User Id and it Exists in USERS table, this is in replacement to foreign key reference of CACPIREFERENCEDATE table with USERS table.
	IF EXISTS (SELECT * FROM inserted 
		LEFT OUTER JOIN USERS WITH (NOLOCK) ON USERS.SUSERGUID = inserted.LASTCHANGEDBY
		WHERE inserted.LASTCHANGEDBY IS NOT NULL AND USERS.SUSERGUID IS NULL)
	BEGIN		
		RAISERROR ('The INSERT or UPDATE statement conflicted with the FOREIGN KEY to table USERS', 16, 0);
		ROLLBACK;
		RETURN;
	END


	INSERT INTO [HISTORYSYSTEMSETUP]
    (	[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
    )
	SELECT
			[inserted].[CACPIREFERENCEDATEID],
			[inserted].[ROWVERSION],
			GETUTCDATE(),
			[inserted].[LASTCHANGEDBY],
			'Default Flag',
			CASE [deleted].[ISDEFAULT] WHEN 1 THEN 'Yes' ELSE 'No' END,
			CASE [inserted].[ISDEFAULT] WHEN 1 THEN 'Yes' ELSE 'No' END,
			'Module (' + [CAMODULE].[NAME] + '), CPI Reference Date (' + [deleted].[DATEOBJECTFRIENDLYNAME] + ')',
			'D2215E33-94EF-4541-9CA6-DB5B9BF1BAE9',
			2,
			1,
			[inserted].[DATEOBJECTFRIENDLYNAME]
	FROM	[deleted]
			JOIN [inserted] ON [deleted].[CACPIREFERENCEDATEID] = [inserted].[CACPIREFERENCEDATEID]
			INNER JOIN CAMODULE ON [deleted].[CAMODULEID] = [CAMODULE].[CAMODULEID]
	WHERE	[deleted].[ISDEFAULT] <> [inserted].[ISDEFAULT]	
	
END
