CREATE TABLE [dbo].[COLICTYPECLASSREF] (
    [COLICTYPEID]      CHAR (36) NOT NULL,
    [CLASSIFICATIONID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_COLicTypeClassRef] PRIMARY KEY CLUSTERED ([COLICTYPEID] ASC, [CLASSIFICATIONID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CerTypeClassRef_CerT] FOREIGN KEY ([COLICTYPEID]) REFERENCES [dbo].[COLICENSECERTIFICATIONTYPE] ([COSIMPLELICCERTTYPEID]),
    CONSTRAINT [FK_CerTypeClassRef_Class] FOREIGN KEY ([CLASSIFICATIONID]) REFERENCES [dbo].[ILLICENSECLASSIFICATION] ([ILLICENSECLASSIFICATIONID])
);


GO

CREATE TRIGGER [TG_COLICTYPECLASSREF_INSERT] ON [COLICTYPECLASSREF]
	AFTER INSERT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
			[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID],
			[COLICENSECERTIFICATIONTYPE].[ROWVERSION],
			GETUTCDATE(),
			[COLICENSECERTIFICATIONTYPE].[LASTCHANGEDBY],
			'Certification Type Classification Added',
			'',
			'',
			'Certification Type (' + [COLICENSECERTIFICATIONTYPE].[NAME] + '), Classification (' + [ILLICENSECLASSIFICATION].[NAME] +')',
			'45F218A9-C018-4C83-9CA4-AC0538CA1BBE',
			1,
			0,
			[ILLICENSECLASSIFICATION].[NAME]
	FROM	[inserted]
	INNER JOIN COLICENSECERTIFICATIONTYPE ON [inserted].[COLICTYPEID] = [COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID]
	INNER JOIN ILLICENSECLASSIFICATION  WITH (NOLOCK) ON [inserted].[CLASSIFICATIONID] = [ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID]
END
GO

CREATE TRIGGER [TG_COLICTYPECLASSREF_DELETE] ON [COLICTYPECLASSREF]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID],
			[COLICENSECERTIFICATIONTYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Certification Type Classification Deleted',
			'',
			'',
			'Certification Type (' + [COLICENSECERTIFICATIONTYPE].[NAME] + '), Classification (' + [ILLICENSECLASSIFICATION].[NAME] +')',
			'45F218A9-C018-4C83-9CA4-AC0538CA1BBE',
			3,
			0,
			[ILLICENSECLASSIFICATION].[NAME]
	FROM	[deleted]	
	INNER JOIN COLICENSECERTIFICATIONTYPE ON [deleted].[COLICTYPEID] = [COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID]
	INNER JOIN ILLICENSECLASSIFICATION  WITH (NOLOCK) ON [deleted].[CLASSIFICATIONID] = [ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID]
END