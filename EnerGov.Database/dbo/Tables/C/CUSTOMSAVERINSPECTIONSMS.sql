CREATE TABLE [dbo].[CUSTOMSAVERINSPECTIONSMS] (
    [ID]                        CHAR (36) NOT NULL,
    [CUSTOMFIELDID]             CHAR (36) NOT NULL,
    [CUSTOMFIELDPICKLISTITEMID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_InspectionsMS] PRIMARY KEY NONCLUSTERED ([ID] ASC, [CUSTOMFIELDID] ASC, [CUSTOMFIELDPICKLISTITEMID] ASC) WITH (FILLFACTOR = 80),
    CONSTRAINT [InspectionsMS_CustomField] FOREIGN KEY ([CUSTOMFIELDID]) REFERENCES [dbo].[CUSTOMFIELD] ([GCUSTOMFIELD]),
    CONSTRAINT [InspectionsMS_CustomFieldPickL] FOREIGN KEY ([CUSTOMFIELDPICKLISTITEMID]) REFERENCES [dbo].[CUSTOMFIELDPICKLISTITEM] ([GCUSTOMFIELDPICKLISTITEM]),
    CONSTRAINT [InspectionsMS_Inspections] FOREIGN KEY ([ID]) REFERENCES [dbo].[CUSTOMSAVERINSPECTIONS] ([ID])
);


GO
CREATE NONCLUSTERED INDEX [InspectionsMS_ID]
    ON [dbo].[CUSTOMSAVERINSPECTIONSMS]([ID] ASC) WITH (FILLFACTOR = 80);


GO

CREATE TRIGGER [TG_CUSTOMSAVERINSPECTIONSMS_UPDATE_ELASTIC] ON  CUSTOMSAVERINSPECTIONSMS
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[C].[IMINSPECTIONID] ,
        'EnerGovBusiness.Inspections.Inspection' ,
        [C].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [IMINSPECTION] AS [C] WITH (NOLOCK) ON [C].[IMINSPECTIONID] = [Inserted].[ID];

END