CREATE TABLE [dbo].[CLIENTCREDENTIAL] (
    [CLIENTCREDENTIALID] INT            IDENTITY (1, 1) NOT NULL,
    [CLIENTID]           NVARCHAR (200) NOT NULL,
    [SECRET]             NVARCHAR (250) NOT NULL,
    [SCOPE]              NVARCHAR (200) NOT NULL,
    [DESCRIPTION]        NVARCHAR (MAX) NULL,
    [CLIENTKEY]          NVARCHAR (100) NULL,
    [NAME]               NVARCHAR (50)  NULL,
    [BASEURL]            NVARCHAR (MAX) NULL,
    [CLIENTIDENTITYURL]  NVARCHAR (MAX) NULL,
    [USERID]             CHAR (36)      NULL,
    CONSTRAINT [PK_CLIENTCREDENTIAL] PRIMARY KEY CLUSTERED ([CLIENTCREDENTIALID] ASC),
    CONSTRAINT [FK_CLIENTCREDENTIAL_USER] FOREIGN KEY ([USERID]) REFERENCES [dbo].[USERS] ([SUSERGUID]),
    CONSTRAINT [UC_CLIENTCREDENTIAL_CLIENTID] UNIQUE NONCLUSTERED ([CLIENTID] ASC)
);


GO
CREATE NONCLUSTERED INDEX [IX_CLIENTCREDENTIAL_CLIENTKEY]
    ON [dbo].[CLIENTCREDENTIAL]([CLIENTKEY] ASC);

