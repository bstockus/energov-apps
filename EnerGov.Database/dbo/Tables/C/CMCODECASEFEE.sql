CREATE TABLE [dbo].[CMCODECASEFEE] (
    [CMCODECASEFEEID] CHAR (36) NOT NULL,
    [CMCODECASEID]    CHAR (36) NOT NULL,
    [CACOMPUTEDFEEID] CHAR (36) NOT NULL,
    [CREATEDON]       DATETIME  NOT NULL,
    CONSTRAINT [PK_CMCodeCaseFee] PRIMARY KEY NONCLUSTERED ([CMCODECASEFEEID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CMCodeCaseFee_CMCodeCase] FOREIGN KEY ([CMCODECASEID]) REFERENCES [dbo].[CMCODECASE] ([CMCODECASEID]),
    CONSTRAINT [FK_CMCodeCaseFee_ComputedFee] FOREIGN KEY ([CACOMPUTEDFEEID]) REFERENCES [dbo].[CACOMPUTEDFEE] ([CACOMPUTEDFEEID])
);


GO
CREATE NONCLUSTERED INDEX [IX_CMCodeCaseFee_CodeCase]
    ON [dbo].[CMCODECASEFEE]([CMCODECASEID] ASC) WITH (FILLFACTOR = 90);


GO
CREATE NONCLUSTERED INDEX [IX_CMCODECASEFEE_COMP_FEE]
    ON [dbo].[CMCODECASEFEE]([CMCODECASEID] ASC, [CACOMPUTEDFEEID] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_CMCODECASEFEE_CACOMPUTEDFEEID]
    ON [dbo].[CMCODECASEFEE]([CACOMPUTEDFEEID] ASC)
    INCLUDE([CMCODECASEID]);

