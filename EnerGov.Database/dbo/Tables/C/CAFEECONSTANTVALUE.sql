CREATE TRIGGER [TG_CAFEECONSTANTVALUE_DELETE] ON [CAFEECONSTANTVALUE]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(		[ID],
			[ROWVERSION],
			[CHANGEDON],
			[CHANGEDBY],
			[FIELDNAME],
			[OLDVALUE],
			[NEWVALUE],
			[ADDITIONALINFO],
			[FORMID],
			[ACTION],
			[ISROOT],
			[RECORDNAME]
		)
	SELECT
			[CAFEECONSTANT].[CAFEECONSTANTID],
			[CAFEECONSTANT].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Fee Constant - Fee Constant Value Deleted',
			'',
			'',
			'Fee Constant (' + [CAFEECONSTANT].[NAME] + '), Value (' + [deleted].[VALUE] + ')',
			'B19A0B8D-ED51-4A4E-8075-E8EE0DEDE22C',
			3,
			0,
			[deleted].[VALUE]
	FROM	[deleted] 
			INNER JOIN CAFEECONSTANT ON [deleted].[CAFEECONSTANTID] = [CAFEECONSTANT].[CAFEECONSTANTID]			
END