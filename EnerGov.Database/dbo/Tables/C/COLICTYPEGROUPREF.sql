CREATE TABLE [dbo].[COLICTYPEGROUPREF] (
    [COLICTYPEID] CHAR (36) NOT NULL,
    [GROUPID]     CHAR (36) NOT NULL,
    CONSTRAINT [PK_COLicTypeGroupRef] PRIMARY KEY CLUSTERED ([COLICTYPEID] ASC, [GROUPID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_CerTypeGroupRef_CerT] FOREIGN KEY ([COLICTYPEID]) REFERENCES [dbo].[COLICENSECERTIFICATIONTYPE] ([COSIMPLELICCERTTYPEID]),
    CONSTRAINT [FK_CerTypeGroupRef_Gr] FOREIGN KEY ([GROUPID]) REFERENCES [dbo].[ILLICENSEGROUP] ([ILLICENSEGROUPID])
);


GO

CREATE TRIGGER [TG_COLICTYPEGROUPREF_INSERT] ON [COLICTYPEGROUPREF]
	AFTER INSERT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT 
			[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID],
			[COLICENSECERTIFICATIONTYPE].[ROWVERSION],
			GETUTCDATE(),
			[COLICENSECERTIFICATIONTYPE].[LASTCHANGEDBY],
			'Certification Type Allowed Group Added',
			'',
			'',
			'Certification Type (' + [COLICENSECERTIFICATIONTYPE].[NAME] + '), Allowed Group (' + [ILLICENSEGROUP].[NAME] +')',
			'45F218A9-C018-4C83-9CA4-AC0538CA1BBE',
			1,
			0,
			[ILLICENSEGROUP].[NAME]
	FROM	[inserted]
	INNER JOIN COLICENSECERTIFICATIONTYPE ON [inserted].[COLICTYPEID] = [COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID]
	INNER JOIN ILLICENSEGROUP  WITH (NOLOCK) ON [inserted].[GROUPID] = [ILLICENSEGROUP].[ILLICENSEGROUPID]
END
GO

CREATE TRIGGER [TG_COLICTYPEGROUPREF_DELETE] ON [COLICTYPEGROUPREF]
	AFTER DELETE
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO [HISTORYSYSTEMSETUP]
	(
		[ID],
		[ROWVERSION],
		[CHANGEDON],
		[CHANGEDBY],
		[FIELDNAME],
		[OLDVALUE],
		[NEWVALUE],
		[ADDITIONALINFO],
		[FORMID],
		[ACTION],
		[ISROOT],
		[RECORDNAME]
	)
	SELECT
			[COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID],
			[COLICENSECERTIFICATIONTYPE].[ROWVERSION],
			GETUTCDATE(),
			(SELECT dbo.UFN_GET_USERID_FROM_CONTEXT_INFO()),
			'Certification Type Allowed Group Deleted',
			'',
			'',
			'Certification Type (' + [COLICENSECERTIFICATIONTYPE].[NAME] + '), Allowed Group (' + [ILLICENSEGROUP].[NAME] +')',
			'45F218A9-C018-4C83-9CA4-AC0538CA1BBE',
			3,
			0,
			[ILLICENSEGROUP].[NAME]
	FROM	[deleted]	
	INNER JOIN COLICENSECERTIFICATIONTYPE ON [deleted].[COLICTYPEID] = [COLICENSECERTIFICATIONTYPE].[COSIMPLELICCERTTYPEID]
	INNER JOIN ILLICENSEGROUP  WITH (NOLOCK) ON [deleted].[GROUPID] = [ILLICENSEGROUP].[ILLICENSEGROUPID]
END