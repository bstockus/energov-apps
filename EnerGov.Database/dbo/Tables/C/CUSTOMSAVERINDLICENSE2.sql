CREATE TABLE [dbo].[CUSTOMSAVERINDLICENSE2] (
    [ID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_IndLicense2] PRIMARY KEY NONCLUSTERED ([ID] ASC) WITH (FILLFACTOR = 90)
);


GO

CREATE TRIGGER [TG_CUSTOMSAVERINDLICENSE2_UPDATE_ELASTIC] ON  CUSTOMSAVERINDLICENSE2
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[I].[ILLICENSEID] ,
        'EnerGovBusiness.IndividualLicense.License' ,
        [I].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [ILLICENSE] AS [I] WITH (NOLOCK) ON [I].[ILLICENSEID] = [Inserted].[ID];

END
