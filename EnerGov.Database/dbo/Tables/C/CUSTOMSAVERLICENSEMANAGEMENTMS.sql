CREATE TABLE [dbo].[CUSTOMSAVERLICENSEMANAGEMENTMS] (
    [ID]                        CHAR (36) NOT NULL,
    [CUSTOMFIELDID]             CHAR (36) NOT NULL,
    [CUSTOMFIELDPICKLISTITEMID] CHAR (36) NOT NULL,
    CONSTRAINT [PK_LicenseManagementMS] PRIMARY KEY NONCLUSTERED ([ID] ASC, [CUSTOMFIELDID] ASC, [CUSTOMFIELDPICKLISTITEMID] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [LicenseManagementMS_CFPLI] FOREIGN KEY ([CUSTOMFIELDPICKLISTITEMID]) REFERENCES [dbo].[CUSTOMFIELDPICKLISTITEM] ([GCUSTOMFIELDPICKLISTITEM]),
    CONSTRAINT [LicenseManagementMS_CustomFiel] FOREIGN KEY ([CUSTOMFIELDID]) REFERENCES [dbo].[CUSTOMFIELD] ([GCUSTOMFIELD]),
    CONSTRAINT [LicenseManagementMS_LicenseMan] FOREIGN KEY ([ID]) REFERENCES [dbo].[CUSTOMSAVERLICENSEMANAGEMENT] ([ID])
);


GO
CREATE NONCLUSTERED INDEX [LicenseManagementMS_ID]
    ON [dbo].[CUSTOMSAVERLICENSEMANAGEMENTMS]([ID] ASC) WITH (FILLFACTOR = 90);


GO

CREATE TRIGGER [TG_CUSTOMSAVERLICENSEMANAGEMENTMS_UPDATE_ELASTIC_LICENSE] ON  CUSTOMSAVERLICENSEMANAGEMENTMS
   AFTER UPDATE
AS 
BEGIN
	SET NOCOUNT ON;

    INSERT INTO [ELASTICSEARCHOBJECT]
    ( [ELASTICSEARCHOBJECTID] ,
        [OBJECTID] ,
        [OBJECTCLASSNAME] ,
        [ROWVERSION] ,
        [CREATEDATE] ,
        [PROCESSEDDATE] ,
        [OBJECTACTION] ,
        [INDEXNAME]
    )
	SELECT
		NEWID() ,
		[B].[BLLICENSEID] ,
        'EnerGovBusiness.BusinessLicense.BusinessLicense' ,
        [B].[ROWVERSION] ,
        GETDATE() ,
        NULL ,
        2 ,
        (SELECT STRINGVALUE FROM SETTINGS WITH (NOLOCK) WHERE NAME = 'ServiceBusTenant')
	FROM [Inserted]
	JOIN [BLLICENSE] AS [B] WITH (NOLOCK) ON [B].[BLLICENSEID] = [Inserted].[ID];

END