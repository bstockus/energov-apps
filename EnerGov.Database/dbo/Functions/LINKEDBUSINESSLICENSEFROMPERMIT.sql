CREATE FUNCTION DBO.LINKEDBUSINESSLICENSEFROMPERMIT
(
	@ENTITY_ID char(36),
	@USER_ID char(36),
	@BLLICENSETYPEMODULE_ID INT
)
RETURNS TABLE 
AS
RETURN 
(
		SELECT BLLICENSE.BLLICENSEID FROM PMPERMIT 
		INNER JOIN PMPERMITWFSTEP ON PMPERMIT.PMPERMITID = PMPERMITWFSTEP.PMPERMITID 
		INNER JOIN PMPERMITWFACTIONSTEP ON PMPERMITWFSTEP.PMPERMITWFSTEPID = PMPERMITWFACTIONSTEP.PMPERMITWFSTEPID
		INNER JOIN BLLICENSEACTIONREF ON PMPERMITWFACTIONSTEP.PMPERMITWFACTIONSTEPID = BLLICENSEACTIONREF.OBJECTID
		INNER JOIN BLLICENSE ON BLLICENSEACTIONREF.BLLICENSEID = BLLICENSE.BLLICENSEID 
		INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on BLLICENSE.BLLICENSETYPEID = u.RECORDTYPEID
		INNER JOIN BLLICENSETYPE ON BLLICENSETYPE.BLLICENSETYPEID= BLLICENSE.BLLICENSETYPEID
		WHERE (PMPERMIT.PMPERMITID = @ENTITY_ID AND BLLICENSETYPE.BLLICENSETYPEMODULEID=@BLLICENSETYPEMODULE_ID)
			UNION ALL
		SELECT license.BLLICENSEID from PMPERMITACTIONREF xref
		INNER JOIN BLLICENSEWFACTIONSTEP blActionStep on xref.OBJECTID = blActionStep.BLLICENSEWFACTIONSTEPID
		INNER JOIN BLLICENSEWFSTEP blStep on blActionStep.BLLICENSEWFSTEPID = blStep.BLLICENSEWFSTEPID
		INNER JOIN BLLICENSE license on blStep.BLLICENSEID = license.BLLICENSEID
		INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on license.BLLICENSETYPEID = u.RECORDTYPEID
		INNER JOIN BLLICENSETYPE ON BLLICENSETYPE.BLLICENSETYPEID= license.BLLICENSETYPEID
		where xref.PMPERMITID = @ENTITY_ID AND BLLICENSETYPE.BLLICENSETYPEMODULEID=@BLLICENSETYPEMODULE_ID
)