CREATE FUNCTION DBO.LINKEDREQUESTFROMPROPERTYMANAGEMENT
(	
	@ENTITY_ID char(36),
	@USER_ID as CHAR(36)
)
RETURNS TABLE 
AS
RETURN 
(
	Select CITIZENREQUESTPARCEL.CITIZENREQUESTID FROM CITIZENREQUESTPARCEL 
	INNER JOIN CITIZENREQUEST ON CITIZENREQUESTPARCEL.CITIZENREQUESTID = CITIZENREQUEST.CITIZENREQUESTID 
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on CITIZENREQUEST.CITIZENREQUESTTYPEID = u.RECORDTYPEID
	WHERE CITIZENREQUESTPARCEL.PARCELID = @ENTITY_ID
)