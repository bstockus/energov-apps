CREATE FUNCTION DBO.LINKEDPROFLICENSEFROMCODECASE
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
		select oneLicense.ILLICENSEID  from CMCODECASEACTIONREF xref  
		INNER JOIN ILLICENSE oneLicense on xref.PARENTCASEID = oneLicense.ILLICENSEID 
		INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on oneLicense.ILLICENSETYPEID = u.RECORDTYPEID
		where xref.CMCODECASEID = @ENTITY_ID	 
)


