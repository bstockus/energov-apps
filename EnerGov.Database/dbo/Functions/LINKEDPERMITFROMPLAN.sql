CREATE FUNCTION DBO.LINKEDPERMITFROMPLAN
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
	Select PMPERMIT.PMPERMITID, 1 AS ISCHILD, 0 AS ISPARENT FROM PLPLANWFSTEP  
	INNER JOIN PLPLANWFACTIONSTEP ON PLPLANWFSTEP.PLPLANWFSTEPID = PLPLANWFACTIONSTEP.PLPLANWFSTEPID  
	INNER JOIN PLPLAN ON  PLPLANWFSTEP.PLPLANID = PLPLAN.PLPLANID  
	INNER JOIN PMPERMITACTIONREF ON PLPLANWFACTIONSTEP.PLPLANWFACTIONSTEPID = PMPERMITACTIONREF.OBJECTID 
	INNER JOIN PMPERMIT ON PMPERMITACTIONREF.PMPERMITID = PMPERMIT.PMPERMITID 
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PMPERMIT.PMPERMITTYPEID = u.RECORDTYPEID
	WHERE(PLPLAN.PLPLANID  = @ENTITY_ID)  
	UNION ALL SELECT PMPERMIT.PMPERMITID, 0 AS ISCHILD, 1 AS ISPARENT FROM PLPLANACTIONREF  
	INNER JOIN PLPLAN on PLPLANACTIONREF.PLPLANID = PLPLAN.PLPLANID  
	INNER JOIN PMPERMITWFACTIONSTEP ON PLPLANACTIONREF.OBJECTID = PMPERMITWFACTIONSTEP.PMPERMITWFACTIONSTEPID  
	INNER JOIN PMPERMITWFSTEP ON PMPERMITWFACTIONSTEP.PMPERMITWFSTEPID = PMPERMITWFSTEP.PMPERMITWFSTEPID  
	INNER JOIN PMPERMIT ON PMPERMITWFSTEP.PMPERMITID = PMPERMIT.PMPERMITID  
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PMPERMIT.PMPERMITTYPEID = u.RECORDTYPEID
	WHERE(PLPLANACTIONREF.PLPLANID = @ENTITY_ID)
	 
)
