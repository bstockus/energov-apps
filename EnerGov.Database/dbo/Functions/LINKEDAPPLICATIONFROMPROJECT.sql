CREATE FUNCTION DBO.LINKEDAPPLICATIONFROMPROJECT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)	
)
RETURNS TABLE 
AS
RETURN 
(
	Select PRPROJECTAPPLICATION.PLAPPLICATIONID FROM PRPROJECTAPPLICATION 
	INNER JOIN PLAPPLICATION ON PRPROJECTAPPLICATION.PLAPPLICATIONID = PLAPPLICATION.PLAPPLICATIONID 
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PLAPPLICATION.PLAPPLICATIONTYPEID = u.RECORDTYPEID
	WHERE PRPROJECTAPPLICATION.PRPROJECTID = @ENTITY_ID
)