CREATE FUNCTION DBO.LINKEDCODECASEFROMPROPERTYMANAGEMENT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
	Select CMCODECASE.CMCODECASEID, 0 AS ISCHILD, 1 AS ISPARENT FROM CMCODECASEPARCEL
	INNER JOIN CMCODECASE ON CMCODECASEPARCEL.CMCODECASEID = CMCODECASE.CMCODECASEID 
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on CMCODECASE.CMCASETYPEID = u.RECORDTYPEID
	WHERE(CMCODECASEPARCEL.PARCELID = @ENTITY_ID)
)