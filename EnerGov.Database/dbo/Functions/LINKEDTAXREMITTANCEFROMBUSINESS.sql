CREATE FUNCTION DBO.LINKEDTAXREMITTANCEFROMBUSINESS
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
SELECT tax.TXREMITTANCEACCOUNTID FROM TXREMITTANCEACCOUNT tax 
 INNER JOIN BLGLOBALENTITYEXTENSION ext ON tax.BLGLOBALENTITYEXTENSIONID = ext.BLGLOBALENTITYEXTENSIONID
 INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u ON tax.TXREMITTANCETYPEID = u.RECORDTYPEID
 WHERE ext.BLGLOBALENTITYEXTENSIONID = @ENTITY_ID
 )