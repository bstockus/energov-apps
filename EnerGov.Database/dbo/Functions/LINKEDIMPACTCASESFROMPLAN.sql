CREATE FUNCTION DBO.LINKEDIMPACTCASESFROMPLAN
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(		
		SELECT oneImpactCase.IPCASEID FROM IPCASEPLANXREF xref
		INNER JOIN IPCASE oneImpactCase ON xref.IPCASEID = oneImpactCase.IPCASEID
		INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u ON oneImpactCase.IPCASETYPEID = u.RECORDTYPEID	
		where xref.PLPLANID = @ENTITY_ID
)