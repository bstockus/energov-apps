CREATE FUNCTION DBO.LINKEDINSPECTIONFROMPROPERTYMANAGEMENT
(	
	@ENTITY_ID char(36),
	@USER_ID as CHAR(36)	
)
RETURNS TABLE 
AS
RETURN 
(
	select DISTINCT IMINSPECTIONPARCEL.IMINSPECTIONID  from IMINSPECTIONPARCEL
	INNER JOIN IMINSPECTION ON IMINSPECTIONPARCEL.IMINSPECTIONID = IMINSPECTION.IMINSPECTIONID
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on IMINSPECTION.IMINSPECTIONTYPEID = u.RECORDTYPEID
	where IMINSPECTIONPARCEL.PARCELID  = @ENTITY_ID
)