CREATE FUNCTION DBO.LINKEDRENTALPROPERTIESFROMPROPERTYMANAGEMENT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
	SELECT RPPROPERTY.RPPROPERTYID FROM RPPROPERTY
	INNER JOIN RPPROPERTYPARCEL ON RPPROPERTY.RPPROPERTYID = RPPROPERTYPARCEL.RPPROPERTYID
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on RPPROPERTY.RPPROPERTYTYPEID = u.RECORDTYPEID
	WHERE RPPROPERTYPARCEL.PARCELID = @ENTITY_ID
)