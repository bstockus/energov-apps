CREATE FUNCTION DBO.LINKEDAPPLICATIONFROMPERMITPLAN
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)	
)
RETURNS TABLE 
AS
RETURN 
(
	select APPLICATIONID from PLAPPLICATIONATTACHEDCASELIST 
	INNER JOIN PLAPPLICATION ON PLAPPLICATIONATTACHEDCASELIST.APPLICATIONID = PLAPPLICATION.PLAPPLICATIONID	
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PLAPPLICATION.PLAPPLICATIONTYPEID = u.RECORDTYPEID
	where PLAPPLICATIONATTACHEDCASELIST.ATTACHEDCASEID = @ENTITY_ID
)

