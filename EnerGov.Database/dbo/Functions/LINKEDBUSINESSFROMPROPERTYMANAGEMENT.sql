CREATE FUNCTION DBO.LINKEDBUSINESSFROMPROPERTYMANAGEMENT
(	
	@ENTITY_ID char(36),
	@BLEXTCOMPANYTYPEMODULE_ID INT
)
RETURNS TABLE 
AS
RETURN 
(
		SELECT businessExt.BLGLOBALENTITYEXTENSIONID FROM BLGLOBALENTITYEXTENSIONPARCEL businessExtParcel 
		INNER JOIN BLGLOBALENTITYEXTENSION businessExt ON businessExtParcel.BLGLOBALENTITYEXTENSIONID = businessExt.BLGLOBALENTITYEXTENSIONID
		INNER JOIN BLEXTCOMPANYTYPE ON BLEXTCOMPANYTYPE.BLEXTCOMPANYTYPEID = businessExt.BLEXTCOMPANYTYPEID
		WHERE businessExtParcel.PARCELID = @ENTITY_ID AND BLEXTCOMPANYTYPE.BLEXTCOMPANYTYPEMODULEID= @BLEXTCOMPANYTYPEMODULE_ID	
)