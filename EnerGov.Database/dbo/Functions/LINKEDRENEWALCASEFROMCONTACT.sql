CREATE FUNCTION DBO.LINKEDRENEWALCASEFROMCONTACT
(	
	@ENTITY_ID char(36),
	@USER_ID as CHAR(36)	
)
RETURNS TABLE 
AS
RETURN 
(
	select PMRENEWALCASEID from PMRENEWALCASE 
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PMRENEWALCASE.PMRENEWALCASETYPEID = u.RECORDTYPEID
	where BILLINGCONTACTID = @ENTITY_ID
)

