CREATE FUNCTION DBO.LINKEDPERMITFROMPROFESSIONALLICENSE
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
	SELECT DISTINCT PMPERMIT.PMPERMITID, 1 AS ISCHILD, 0 AS ISPARENT FROM ILLICENSE 
	INNER JOIN ILLICENSEWFSTEP ON ILLICENSE.ILLICENSEID = ILLICENSEWFSTEP.ILLICENSEID
	INNER JOIN ILLICENSEWFACTIONSTEP ON ILLICENSEWFSTEP.ILLICENSEWFSTEPID = ILLICENSEWFACTIONSTEP.ILLICENSEWFSTEPID
	INNER JOIN PMPERMITACTIONREF ON ILLICENSEWFACTIONSTEP.ILLICENSEWFACTIONSTEPID = PMPERMITACTIONREF.OBJECTID
	INNER JOIN PMPERMIT ON PMPERMITACTIONREF.PMPERMITID = PMPERMIT.PMPERMITID
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PMPERMIT.PMPERMITTYPEID = u.RECORDTYPEID
	WHERE (ILLICENSE.ILLICENSEID = @ENTITY_ID)
)