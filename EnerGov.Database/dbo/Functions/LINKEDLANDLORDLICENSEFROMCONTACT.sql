CREATE FUNCTION DBO.LINKEDLANDLORDLICENSEFROMCONTACT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
 select llicense.RPLANDLORDLICENSEID from RPLANDLORDLICENSE llicense 
 INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) U ON llicense.RPLANDLORDLICENSETYPEID = u.RECORDTYPEID
 where llicense.GLOBALENTITYID = @ENTITY_ID
)