CREATE FUNCTION DBO.LINKEDPLANFROMPROJECT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
	Select PLPLAN.PLPLANID, 1 AS ISCHILD, 0 AS ISPARENT FROM PRPROJECTPLAN 
	INNER JOIN PLPLAN ON PRPROJECTPLAN.PLPLANID = PLPLAN.PLPLANID 
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PLPLAN.PLPLANTYPEID = u.RECORDTYPEID
	WHERE(PRPROJECTPLAN.PRPROJECTID = @ENTITY_ID)
)