CREATE FUNCTION DBO.LINKEDPROJECTFROMPROPERTYMANAGEMENT
(	
	@ENTITY_ID char(36),
	@USER_ID as CHAR(36)	
)
RETURNS TABLE 
AS
RETURN 
(
	select DISTINCT PRPROJECTPARCEL.PRPROJECTID from PRPROJECTPARCEL
	INNER JOIN PRPROJECT ON PRPROJECTPARCEL.PRPROJECTID = PRPROJECT.PRPROJECTID
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PRPROJECT.PRPROJECTTYPEID = u.RECORDTYPEID
	where PRPROJECTPARCEL.PARCELID  = @ENTITY_ID
)