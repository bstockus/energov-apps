CREATE FUNCTION [dbo].[GETENUMNAMEBYCOLUMNOPERATOR]
(
	@ENUMVALUE INT -- VALUE OF ENUM 
)
RETURNS NVARCHAR(MAX)
AS
BEGIN		
	DECLARE @ENUMNAME NVARCHAR(MAX)

SELECT @ENUMNAME= CASE @ENUMVALUE 
						WHEN 1
							THEN 'SUM' 
						WHEN 2
							THEN 'COUNT' 
						WHEN 3
							THEN 'AVG' 
					END
	RETURN @ENUMNAME
END