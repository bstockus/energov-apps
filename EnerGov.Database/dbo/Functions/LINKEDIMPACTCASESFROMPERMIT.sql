CREATE FUNCTION DBO.LINKEDIMPACTCASESFROMPERMIT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
		SELECT oneImpactCase.IPCASEID FROM IPCASEPERMITXREF xref
		INNER JOIN IPCASE oneImpactCase ON xref.IPCASEID = oneImpactCase.IPCASEID
		INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u ON oneImpactCase.IPCASETYPEID = u.RECORDTYPEID
		WHERE xref.PMPERMITID = @ENTITY_ID	 
)