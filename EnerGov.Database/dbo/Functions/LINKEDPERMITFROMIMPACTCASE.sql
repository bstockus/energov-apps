CREATE FUNCTION [dbo].[LINKEDPERMITFROMIMPACTCASE]
(
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS 
RETURN
(
	SELECT DISTINCT IPCASEPERMITXREF.PMPERMITID, 1 AS ISCHILD, 0 AS ISPARENT FROM IPCASEPERMITXREF
	INNER JOIN PMPERMIT ON IPCASEPERMITXREF.PMPERMITID = PMPERMIT.PMPERMITID
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u ON PMPERMIT.PMPERMITTYPEID = u.RECORDTYPEID	
	WHERE IPCASEPERMITXREF.IPCASEID = @ENTITY_ID
)