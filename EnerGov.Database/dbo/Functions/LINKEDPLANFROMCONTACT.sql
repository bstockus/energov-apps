CREATE FUNCTION DBO.LINKEDPLANFROMCONTACT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
	SELECT DISTINCT PLPLAN.PLPLANID, 0 AS ISCHILD, 1 AS ISPARENT from PLPLANCONTACT 
	INNER JOIN PLPLAN ON PLPLANCONTACT.PLPLANID = PLPLAN.PLPLANID
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PLPLAN.PLPLANTYPEID = u.RECORDTYPEID
	where PLPLANCONTACT.GLOBALENTITYID = @ENTITY_ID	
)