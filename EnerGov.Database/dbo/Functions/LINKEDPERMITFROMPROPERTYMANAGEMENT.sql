CREATE FUNCTION DBO.LINKEDPERMITFROMPROPERTYMANAGEMENT
(	
	@ENTITY_ID char(36),
	@USER_ID char(36)
)
RETURNS TABLE 
AS
RETURN 
(
	SELECT DISTINCT PMPERMIT.PMPERMITID, 0 AS ISCHILD, 1 AS ISPARENT FROM PMPERMITPARCEL
	INNER JOIN PMPERMIT ON  PMPERMITPARCEL.PMPERMITID = PMPERMIT.PMPERMITID
	INNER JOIN GETUSERVISIBLERECORDTYPES(@USER_ID) u on PMPERMIT.PMPERMITTYPEID = u.RECORDTYPEID
	WHERE (PMPERMITPARCEL.PARCELID = @ENTITY_ID)
)