CREATE PROCEDURE [inspection].[USP_RENTAL_PROPERTY_CONTACTS_BY_ID]
(
	@ID AS CHAR(36)
)
AS
SELECT [RPPROPERTYCONTACT].[GLOBALENTITYID], 
		[RPPROPERTYCONTACT].[RPCONTACTTYPEID], 
		[RPCONTACTTYPE].[NAME],
		[RPPROPERTYCONTACT].[ISBILLING]
FROM RPPROPERTYCONTACT INNER JOIN RPCONTACTTYPE 
	ON [RPPROPERTYCONTACT].[RPCONTACTTYPEID] = [RPCONTACTTYPE].[RPCONTACTTYPEID]
WHERE [RPPROPERTYCONTACT].[RPPROPERTYID] = @ID
UNION
SELECT [RPLANDLORDLICENSECONTACT].[GLOBALENTITYID], 
		[RPLANDLORDLICENSECONTACT].[RPCONTACTTYPEID], 
		[RPCONTACTTYPE].[NAME],
		[RPLANDLORDLICENSECONTACT].[ISBILLING]
FROM RPLANDLORDLICENSECONTACT INNER JOIN RPCONTACTTYPE 
	ON [RPLANDLORDLICENSECONTACT].[RPCONTACTTYPEID] = [RPCONTACTTYPE].[RPCONTACTTYPEID]
WHERE [RPLANDLORDLICENSECONTACT].[RPLANDLORDLICENSEID] = @ID