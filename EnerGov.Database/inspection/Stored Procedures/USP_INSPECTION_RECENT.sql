CREATE PROCEDURE [inspection].[USP_INSPECTION_RECENT]
(
  @USER_ID AS CHAR(36)
)
AS
BEGIN
	DECLARE @RECENTINSPECTIONLIST AS RecordIDs
	INSERT INTO @RECENTINSPECTIONLIST (RECORDID)

	SELECT TOP 15 RECENTHISTORYINSPECTION.INSPECTIONID 
	FROM RECENTHISTORYINSPECTION
	WHERE RECENTHISTORYINSPECTION.USERID = @USER_ID
	ORDER BY RECENTHISTORYINSPECTION.LOGGEDDATETIME DESC

	EXEC [inspection].[USP_INSPECTION_GETBYIDS] @RECENTINSPECTIONLIST, @USER_ID 
END