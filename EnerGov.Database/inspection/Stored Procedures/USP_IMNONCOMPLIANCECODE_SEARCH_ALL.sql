CREATE PROCEDURE [inspection].[USP_IMNONCOMPLIANCECODE_SEARCH_ALL]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1,
	@FILTER_FIELD_ID AS VARCHAR(36) = ''
)
AS
WITH RAW_DATA AS (
	SELECT
		[dbo].[IMNONCOMPLIANCECODE].[IMNONCOMPLIANCECODEID],
		[dbo].[IMNONCOMPLIANCECODE].[NAME],
		[dbo].[IMNONCOMPLIANCECODE].[DESCRIPTION],
		[dbo].[IMNONCOMPLIANCECATEGORY].[NAME] AS IMNONCOMPLIANCECATEGORY,
		[dbo].[IMNONCOMPLIANCECODE].[DEFAULTNONCOMPLIANCERISKID],
		[dbo].[IMNONCOMPLIANCECODE].[DEFAULTRESPONSIBLEPARTYID],
		[dbo].[IMNONCOMPLIANCECODEREVISION].[DEFAULTCOMMENTS],
		[dbo].[IMNONCOMPLIANCERESPPARTY].[NAME] AS IMNONCOMPLIANCERESPPARTY,
		[dbo].[IMNONCOMPLIANCECODE].[COMPLIANCEDAYSUNTILDUE],
		[dbo].[IMNONCOMPLIANCECODEREVISION].[IMNONCOMPLIANCECODEREVISIONID],
		(CASE WHEN @IS_ASCENDING = 1 THEN ROW_NUMBER() OVER(ORDER BY [dbo].[IMNONCOMPLIANCECODE].[NAME] ASC)
			  WHEN @IS_ASCENDING = 0 THEN ROW_NUMBER() OVER(ORDER BY [dbo].[IMNONCOMPLIANCECODE].[NAME] DESC)
		 END) AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[IMNONCOMPLIANCECODE] 
	INNER JOIN [dbo].[IMNONCOMPLIANCECATEGORY] ON [dbo].[IMNONCOMPLIANCECATEGORY].[IMNONCOMPLIANCECATEGORYID] = [dbo].[IMNONCOMPLIANCECODE].[IMNONCOMPLIANCECATEGORYID]
	LEFT OUTER JOIN [dbo].[IMNONCOMPLIANCERESPPARTY] ON [dbo].[IMNONCOMPLIANCERESPPARTY].[IMNONCOMPLIANCERESPPARTYID] = [dbo].[IMNONCOMPLIANCECODE].[DEFAULTRESPONSIBLEPARTYID]
	LEFT OUTER JOIN [dbo].[IMNONCOMPLIANCECODEREVISION] ON [dbo].[IMNONCOMPLIANCECODEREVISION].[IMNONCOMPLIANCECODEID] = [dbo].[IMNONCOMPLIANCECODE].[IMNONCOMPLIANCECODEID] AND [dbo].[IMNONCOMPLIANCECODEREVISION].[CURRENTREVISION] = 1
	WHERE (
			([dbo].[IMNONCOMPLIANCECODE].[NAME] LIKE '%'+ @SEARCH +'%') OR
			([dbo].[IMNONCOMPLIANCECODE].[DESCRIPTION] LIKE '%'+ @SEARCH +'%') OR
			([dbo].[IMNONCOMPLIANCECATEGORY].[NAME] LIKE '%'+ @SEARCH +'%')
		  ) AND (
			@FILTER_FIELD_ID = '' OR
			@FILTER_FIELD_ID = [dbo].[IMNONCOMPLIANCECATEGORY].[IMNONCOMPLIANCECATEGORYID]
		  )
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber