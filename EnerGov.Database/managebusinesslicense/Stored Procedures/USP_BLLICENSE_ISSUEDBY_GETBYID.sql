CREATE PROCEDURE [managebusinesslicense].[USP_BLLICENSE_ISSUEDBY_GETBYID]
(
	@ID CHAR(36)
)
AS
BEGIN
	SELECT 
		[BLLICENSE].[ISSUEDDATE],
		[USERS].[FNAME],
		[USERS].[LNAME]
	FROM 
		[dbo].[BLLICENSE] INNER JOIN [dbo].[USERS] ON [BLLICENSE].[ISSUEDBY] = [USERS].[SUSERGUID]
	WHERE 
			[BLLICENSE].[BLLICENSEID] = @ID 
		AND [BLLICENSE].[LASTRENEWALDATE] IS NOT NULL
END