CREATE PROCEDURE [managebusinesslicense].[USP_BLLICENSE_GETBYID]
(
	@ID CHAR(36)
)
AS
BEGIN
	SELECT 
		[BLLICENSE].[BLLICENSEID],
		[BLLICENSE].[LASTRENEWALDATE]
	FROM [dbo].[BLLICENSE] 
	WHERE
			[BLLICENSE].[BLLICENSEID] = @ID 
		AND [BLLICENSE].[LASTRENEWALDATE] IS NOT NULL

	EXEC [managebusinesslicense].[USP_BLLICENSE_ISSUEDBY_GETBYID] @ID
	EXEC [managebusinesslicense].[USP_BLLICENSEACTIVITY_GETBYBLLICENSEID] @ID
	EXEC [managebusinesslicense].[USP_CATRANSACTIONPAYMENT_GETBYBLLICENSEID] @ID
	EXEC [managebusinesslicense].[USP_BLLICENSEWFSTEP_GETBYBLLICENSEID] @ID
END