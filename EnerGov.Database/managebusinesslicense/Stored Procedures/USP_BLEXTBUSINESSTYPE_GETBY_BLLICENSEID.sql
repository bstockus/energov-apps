CREATE PROCEDURE [managebusinesslicense].[USP_BLEXTBUSINESSTYPE_GETBY_BLLICENSEID]
(
	@BLLICENSELIST RECORDIDS READONLY
)
AS
BEGIN	
	SELECT 
		 [BLLICENSEEXTBUSINESSTYPE].[BLLICENSEID],
		 [BLEXTBUSINESSTYPE].[NAME],
		 [BLEXTBUSINESSTYPE].[DESCRIPTION],
		 [BLEXTBUSINESSTYPE].[CODENUMBER]
	FROM 
		[dbo].[BLEXTBUSINESSTYPE] 
		INNER JOIN [dbo].[BLLICENSEEXTBUSINESSTYPE] ON [BLEXTBUSINESSTYPE].[BLEXTBUSINESSTYPEID] = [BLLICENSEEXTBUSINESSTYPE].[BLEXTBUSINESSTYPEID]
	WHERE 
			[BLLICENSEEXTBUSINESSTYPE].[MAIN] = 1 
		AND [BLLICENSEEXTBUSINESSTYPE].[BLLICENSEID] IN (SELECT [RECORDID] FROM @BLLICENSELIST)
END