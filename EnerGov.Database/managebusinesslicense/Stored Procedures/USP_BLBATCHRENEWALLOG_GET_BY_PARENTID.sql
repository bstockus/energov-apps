CREATE PROCEDURE [managebusinesslicense].[USP_BLBATCHRENEWALLOG_GET_BY_PARENTID]
(
	@BULKRENEWALID AS CHAR(36)
)
AS
BEGIN

	SELECT	[dbo].[BLBATCHRENEWALLOG].[BLBATCHRENEWALLOGID],
			[dbo].[BLBATCHRENEWALLOG].[BLLICENSEID],
			[dbo].[BLBATCHRENEWALLOG].[PROCESSEDDATE],
			[dbo].[BLBATCHRENEWALLOG].[REMARKS]
	FROM [dbo].[BLBATCHRENEWALLOG]
	WHERE [dbo].[BLBATCHRENEWALLOG].[BLBATCHRENEWALID] = @BULKRENEWALID
  	ORDER BY [dbo].[BLBATCHRENEWALLOG].[PROCESSEDDATE]

END