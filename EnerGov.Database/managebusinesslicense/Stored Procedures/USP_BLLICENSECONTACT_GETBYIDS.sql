CREATE PROCEDURE [managebusinesslicense].[USP_BLLICENSECONTACT_GETBYIDS]
(
	@BLLICENSELIST RECORDIDS READONLY
)
AS
BEGIN	
	SELECT 
		[BLLICENSECONTACT].[BLLICENSEID],
		 [GLOBALENTITY].[GLOBALENTITYID],
		 [GLOBALENTITY].[FIRSTNAME] ,
		 [GLOBALENTITY].[LASTNAME] ,
		 [GLOBALENTITY].[GLOBALENTITYNAME] ,
		 [GLOBALENTITY].[TITLE] ,
		 [BLLICENSECONTACT].[ISBILLING]
	FROM 
		[dbo].[BLLICENSECONTACT] 
		INNER JOIN [dbo].[GLOBALENTITY] ON [BLLICENSECONTACT].GLOBALENTITYID = [GLOBALENTITY].GLOBALENTITYID
	WHERE 
			[BLLICENSECONTACT].ISBILLING = 1 
		AND [BLLICENSECONTACT].[BLLICENSEID] IN (SELECT [RECORDID] FROM @BLLICENSELIST)
END