CREATE PROCEDURE [managebusinesslicense].[USP_HOLDS_GETBY_BLLICENSEIDS]
(
	@BLLICENSEIDS  RECORDIDS READONLY
)
AS
BEGIN
	SELECT 
		[BLLICENSEHOLD].[BLLICENSEID],
		[BLLICENSEHOLD].[SUSERGUID],
		[BLLICENSEHOLD].[HOLDSETUPID],
		[BLLICENSEHOLD].[BLLICENSEHOLDID],
		[BLLICENSEHOLD].[ACTIVE],
		[BLLICENSEHOLD].[CREATEDDATE],
		[HOLDTYPESETUPS].[NAME],
		[HOLDTYPESETUPS].[HOLDTYPEID],
		[HOLDTYPESETUPS].[ALLOWHOLDOVERRIDES],
		[HOLDTYPESETUPS].[ACTIVE],
		[HOLDTYPESETUPS].[BLRENEW]
	FROM [dbo].[BLLICENSEHOLD]
		INNER JOIN [dbo].[HOLDTYPESETUPS] ON [BLLICENSEHOLD].[HOLDSETUPID] = [HOLDTYPESETUPS].[HOLDSETUPID]
		WHERE 
			[BLLICENSEHOLD].[BLLICENSEID] IN (SELECT [RECORDID] FROM @BLLICENSEIDS) AND [BLLICENSEHOLD].[ACTIVE] = 1
END