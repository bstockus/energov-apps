CREATE PROCEDURE [managebusinesslicense].[USP_ZONE_GETBYBLLICENSEID]
(
	@ID CHAR(36)
)
AS
BEGIN
	SELECT 
			[ZONE].[ZONEID] ,
			[ZONE].[NAME] ,
			[ZONE].[DESCRIPTION] ,
			[ZONE].[ZONECODE] ,
			[BLLICENSEZONE].[MAIN]
	 FROM 
		[dbo].[BLLICENSEZONE] INNER JOIN [dbo].[ZONE] 
		ON [BLLICENSEZONE].[ZONEID] = [ZONE].[ZONEID]
		WHERE [BLLICENSEZONE].[BLLICENSEID] = @ID
END