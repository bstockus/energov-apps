CREATE PROCEDURE [report].[USP_RPTREPORTATTRIBUTE_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[RPTREPORTATTRIBUTE].[RPTREPORTATTRIBUTEID],
				[RPTREPORTATTRIBUTE].[RPTREPORTID],
				[RPTREPORTATTRIBUTE].[PARAMETERNAME],
				[RPTREPORTATTRIBUTE].[ISSTARTDATE]
	FROM		[dbo].[RPTREPORTATTRIBUTE]
	WHERE		[dbo].[RPTREPORTATTRIBUTE].[RPTREPORTID] = @PARENTID
	ORDER BY	[dbo].[RPTREPORTATTRIBUTE].[PARAMETERORDER]
END