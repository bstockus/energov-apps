CREATE PROCEDURE [report].[USP_RPTREPORT_GETBYID]
(
	@RPTREPORTID CHAR(36)
)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT	[RPTREPORT].[RPTREPORTID],
			[RPTREPORT].[REPORTNAME],
			[RPTREPORT].[SYSTEMREPORT],
			[RPTREPORT].[RPTREPORTTYPEID],
			[RPTREPORT].[REPORTVIRTUALPATH]
	FROM	[dbo].[RPTREPORT]
	WHERE	[dbo].[RPTREPORT].[RPTREPORTID] = @RPTREPORTID

	EXEC [report].[USP_RPTREPORTATTRIBUTE_GETBYPARENTID] @RPTREPORTID
END