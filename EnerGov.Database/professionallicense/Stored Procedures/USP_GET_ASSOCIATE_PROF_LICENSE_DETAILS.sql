CREATE PROCEDURE [professionallicense].[USP_GET_ASSOCIATE_PROF_LICENSE_DETAILS]
(
 @PROFESSIONALLICENSELIST RecordIDs READONLY
)
AS
BEGIN
SELECT PL.ILLICENSEID,PL.LICENSENUMBER,GE.FIRSTNAME,GE.LASTNAME,GE.GLOBALENTITYNAME
FROM ILLICENSE PL JOIN GLOBALENTITY GE ON PL.GLOBALENTITYID = GE.GLOBALENTITYID
JOIN @PROFESSIONALLICENSELIST PLL ON PL.ILLICENSEID = PLL.RECORDID
ORDER BY PL.APPLIEDDATE DESC
END