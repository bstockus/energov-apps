CREATE PROCEDURE [professionallicense].[USP_PROFESSIONALLICENSECONTACT_GETBYIDS]
(
 @PROFESSIONALLICENSELIST RecordIDs READONLY
)
AS
BEGIN
SELECT ILLICENSECONTACT.ILLICENSEID,ILLICENSECONTACT.GLOBALENTITYID,GLOBALENTITY.FIRSTNAME,GLOBALENTITY.LASTNAME,
GLOBALENTITY.GLOBALENTITYNAME,ILLICENSECONTACT.ISBILLING, GLOBALENTITY.TITLE,GLOBALENTITY.ISACTIVE,
BLCONTACTTYPE.BLCONTACTTYPEID,BLCONTACTTYPE.NAME
FROM ILLICENSECONTACT JOIN GLOBALENTITY ON ILLICENSECONTACT.GLOBALENTITYID = GLOBALENTITY.GLOBALENTITYID
JOIN BLCONTACTTYPE ON ILLICENSECONTACT.BLCONTACTTYPEID = BLCONTACTTYPE.BLCONTACTTYPEID
JOIN @PROFESSIONALLICENSELIST PLL ON ILLICENSECONTACT.ILLICENSEID = PLL.RECORDID
END