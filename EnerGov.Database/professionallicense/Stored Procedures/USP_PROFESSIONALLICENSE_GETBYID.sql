CREATE PROCEDURE [professionallicense].[USP_PROFESSIONALLICENSE_GETBYID]
(
  @ID AS CHAR(36)
)
AS
BEGIN
DECLARE @PROFESSIONALLICENSELIST AS RecordIDs
INSERT INTO @PROFESSIONALLICENSELIST (RECORDID) VALUES (@ID)
EXEC [professionallicense].[USP_PROFESSIONALLICENSE_GETBYIDS] @PROFESSIONALLICENSELIST
EXEC [professionallicense].[USP_PROFESSIONALLICENSEADDRESS_GETBYIDS] @PROFESSIONALLICENSELIST
EXEC [professionallicense].[USP_PROFESSIONALLICENSECONTACT_GETBYIDS] @PROFESSIONALLICENSELIST
END