CREATE PROCEDURE [professionallicense].[USP_PROFESSIONALLICENSE_RECENT]
(
  @USER_ID AS CHAR(36)
)
AS
BEGIN
DECLARE @RECENTPROFESSIONALLICENSELIST AS RecordIDs
INSERT INTO @RECENTPROFESSIONALLICENSELIST (RECORDID)
SELECT TOP 15 ILLICENSEID FROM RECENTHISTORYPROFESSIONALLICENSE
WHERE RECENTHISTORYPROFESSIONALLICENSE.USERID = @USER_ID
ORDER BY RECENTHISTORYPROFESSIONALLICENSE.LOGGEDDATETIME DESC
EXEC [professionallicense].[USP_PROFESSIONALLICENSE_GETBYIDS] @RECENTPROFESSIONALLICENSELIST, @USER_ID 
EXEC [professionallicense].[USP_PROFESSIONALLICENSEADDRESS_GETBYIDS] @RECENTPROFESSIONALLICENSELIST
EXEC [professionallicense].[USP_PROFESSIONALLICENSECONTACT_GETBYIDS] @RECENTPROFESSIONALLICENSELIST
END