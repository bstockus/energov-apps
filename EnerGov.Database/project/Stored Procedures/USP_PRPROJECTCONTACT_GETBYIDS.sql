CREATE PROCEDURE [project].[USP_PRPROJECTCONTACT_GETBYIDS]
(
	@PRPROJECTLIST RecordIDs READONLY
)
AS
BEGIN
	SELECT	PRPROJECTCONTACT.PRPROJECTID,
			PRPROJECTCONTACT.GLOBALENTITYID,
			GLOBALENTITY.FIRSTNAME,
			GLOBALENTITY.LASTNAME,
			GLOBALENTITY.GLOBALENTITYNAME,
			PRPROJECTCONTACT.ISBILLING, 
			GLOBALENTITY.TITLE,
			GLOBALENTITY.ISACTIVE,
			LANDMANAGEMENTCONTACTTYPE.LANDMANAGEMENTCONTACTTYPEID,
			LANDMANAGEMENTCONTACTTYPENAME = LANDMANAGEMENTCONTACTTYPE.NAME			
	FROM	PRPROJECTCONTACT
			JOIN GLOBALENTITY ON PRPROJECTCONTACT.GLOBALENTITYID = GLOBALENTITY.GLOBALENTITYID
			JOIN LANDMANAGEMENTCONTACTTYPE ON LANDMANAGEMENTCONTACTTYPE.LANDMANAGEMENTCONTACTTYPEID = PRPROJECTCONTACT.LANDMANAGEMENTCONTACTTYPEID
			JOIN @PRPROJECTLIST PRPROJECTLIST ON PRPROJECTCONTACT.PRPROJECTID = PRPROJECTLIST.RECORDID
END