CREATE PROCEDURE [project].[USP_PRPROJECT_RECENT]
(
  @USER_ID AS CHAR(36)
)
AS
BEGIN
	DECLARE @RECENTPROJECTLIST AS RecordIDs
	INSERT INTO @RECENTPROJECTLIST (RECORDID)

	SELECT		TOP 15 RECENTHISTORYPROJECT.PROJECTID 
	FROM		RECENTHISTORYPROJECT
	WHERE		RECENTHISTORYPROJECT.USERID = @USER_ID
	ORDER BY	RECENTHISTORYPROJECT.LOGGEDDATETIME DESC

	EXEC [project].[USP_PRPROJECT_GETBYIDS] @RECENTPROJECTLIST, @USER_ID

END