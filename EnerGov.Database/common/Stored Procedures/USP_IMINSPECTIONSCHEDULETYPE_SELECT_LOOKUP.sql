CREATE PROCEDURE [common].[USP_IMINSPECTIONSCHEDULETYPE_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[IMINSPECTIONSCHEDULETYPE].[IMINSPECTIONSCHEDULETYPEID],
				[dbo].[IMINSPECTIONSCHEDULETYPE].[NAME]
	FROM		[dbo].[IMINSPECTIONSCHEDULETYPE]
	ORDER BY	[dbo].[IMINSPECTIONSCHEDULETYPE].[NAME]
END