CREATE PROCEDURE [common].[USP_INSPECTIONCASES_GET_BY_LINKTYPE_ENTITYID]
	@ENTITYID char(36),
	@LINKTYPE int,
	@USERID AS CHAR(36)
AS
BEGIN
	DECLARE @INSPECTIONCASELIST RecordIDs
	INSERT INTO @INSPECTIONCASELIST
	SELECT IMINSPECTIONCASEID FROM [dbo].[IMINSPECTIONCASE] WHERE LINKID = @ENTITYID AND IMINSPECTIONLINKID = @LINKTYPE
	
	EXEC [common].[USP_IMINSPECTIONCASE_GETBYIDS] @INSPECTIONCASELIST, @USERID, 1
END