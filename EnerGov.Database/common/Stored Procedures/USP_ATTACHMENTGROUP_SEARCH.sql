CREATE PROCEDURE [common].[USP_ATTACHMENTGROUP_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1
)
AS
WITH RAW_DATA AS (
	SELECT
		[dbo].[ATTACHMENTGROUP].[ATTACHMENTGROUPID],
		[dbo].[ATTACHMENTGROUP].[NAME],
		[dbo].[ATTACHMENTGROUP].[DESCRIPTION],
		[dbo].[ATTACHMENTGROUP].[AVAILINPERMIT],
		[dbo].[ATTACHMENTGROUP].[AVAILINPLAN],
		[dbo].[ATTACHMENTGROUP].[AVAILINCODE],
		[dbo].[ATTACHMENTGROUP].[AVAILINCRM],
		[dbo].[ATTACHMENTGROUP].[AVAILINPROJECT],
		[dbo].[ATTACHMENTGROUP].[AVAILINWORKORDER],
		[dbo].[ATTACHMENTGROUP].[AVAILINASSET],
		[dbo].[ATTACHMENTGROUP].[AVAILININVOICE],
		[dbo].[ATTACHMENTGROUP].[AVAILINPAYMENT],
		[dbo].[ATTACHMENTGROUP].[AVAILININSPECTION],
		[dbo].[ATTACHMENTGROUP].[AVAILINCONTACT],
		[dbo].[ATTACHMENTGROUP].[AVAILINBUSINESSLICENSE],
		[dbo].[ATTACHMENTGROUP].[AVAILINBUSINESS],
		[dbo].[ATTACHMENTGROUP].[AVAILINPROLICENSE],
		[dbo].[ATTACHMENTGROUP].[AVAILINEXAMSITTING],
		[dbo].[ATTACHMENTGROUP].[AVAILINEXAMREQUEST],
		[dbo].[ATTACHMENTGROUP].[AVAILINEXAMLANDLORD],
		[dbo].[ATTACHMENTGROUP].[AVAILINEXAMRENTALPROPERTY],
		[dbo].[ATTACHMENTGROUP].[AVAILINEXAMTAXREMITTANCE],
		[dbo].[ATTACHMENTGROUP].[AVAILINCAP],
		[dbo].[ATTACHMENTGROUP].[AVAILINCAPCONTACTS],
		[dbo].[ATTACHMENTGROUP].[AVAILINOBJEC],
		[dbo].[ATTACHMENTGROUP].[AVAILINIMPACTCASE],
		[dbo].[ATTACHMENTGROUP].[AVAILINFIREOCCUPANCY],
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[ATTACHMENTGROUP].[NAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[ATTACHMENTGROUP].[NAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows,
		[dbo].[ATTACHMENTGROUP].[RECORDEDFILE]
	FROM [dbo].[ATTACHMENTGROUP]
	WHERE ([dbo].[ATTACHMENTGROUP].[NAME] LIKE '%'+ @SEARCH +'%') OR ([dbo].[ATTACHMENTGROUP].[DESCRIPTION] LIKE '%'+ @SEARCH +'%')
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber