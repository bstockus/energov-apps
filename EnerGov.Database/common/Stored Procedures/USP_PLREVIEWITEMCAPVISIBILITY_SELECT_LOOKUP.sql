CREATE PROCEDURE [common].[USP_PLREVIEWITEMCAPVISIBILITY_SELECT_LOOKUP]
AS
	SET NOCOUNT ON;
	SELECT 
		[dbo].[PLREVIEWITEMCAPVISIBILITY].[PLREVIEWITEMCAPVISIBILITYID],
		[dbo].[PLREVIEWITEMCAPVISIBILITY].[NAME]
	FROM [dbo].[PLREVIEWITEMCAPVISIBILITY]
	ORDER BY [dbo].[PLREVIEWITEMCAPVISIBILITY].[NAME]