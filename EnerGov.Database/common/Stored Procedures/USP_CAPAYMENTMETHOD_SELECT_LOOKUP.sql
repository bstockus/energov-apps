CREATE PROCEDURE [common].[USP_CAPAYMENTMETHOD_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[CAPAYMENTMETHOD].[CAPAYMENTMETHODID],
				[dbo].[CAPAYMENTMETHOD].[NAME],
				[dbo].[CAPAYMENTMETHOD].[CAPAYMENTTYPEID]
	FROM		[dbo].[CAPAYMENTMETHOD]
	WHERE		[dbo].[CAPAYMENTMETHOD].[ISACTIVE] = 1 -- Only Active Payment Methods
	ORDER BY	[dbo].[CAPAYMENTMETHOD].[NAME]
END