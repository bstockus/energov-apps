CREATE PROCEDURE [common].[USP_TASKTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[TASKTYPE].[TASKTYPEID],
	[dbo].[TASKTYPE].[NAME],
	[dbo].[TASKTYPE].[DESCRIPTION],
	[dbo].[TASKTYPE].[ACTIVE],
	[dbo].[TASKTYPE].[ISDEFAULT],
	[dbo].[TASKTYPE].[ISGLOBAL],
	[dbo].[TASKTYPE].[DAYSUNTILDUE],
	[dbo].[TASKTYPE].[TASKTEXT],
	[dbo].[TASKTYPE].[DEFAULTSUBJECT],
	[dbo].[TASKTYPE].[TASKSTATUSID],
	[dbo].[TASKTYPE].[TASKPRIORITYID],
	[dbo].[TASKTYPE].[LASTCHANGEDBY],
	[dbo].[TASKTYPE].[LASTCHANGEDON],
	[dbo].[TASKTYPE].[ROWVERSION]
FROM [dbo].[TASKTYPE] ORDER BY [dbo].[TASKTYPE].[NAME]

	EXEC [common].[USP_TASKTYPEASSIGNUSER_GETALL]

END