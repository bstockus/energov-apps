CREATE PROCEDURE [common].[USP_SETTINGS_GETALL_BY_CRITERIA]
(
	@SETTINGNAMELIST RecordNames READONLY
)
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[SETTINGS].[SETTINGID],
	[dbo].[SETTINGS].[NAME],
	[dbo].[SETTINGS].[BITVALUE],
	[dbo].[SETTINGS].[STRINGVALUE],
	[dbo].[SETTINGS].[INTVALUE],
	[dbo].[SETTINGS].[IMAGEVALUE],
	[dbo].[SETTINGS].[LASTCHANGEDBY],
	[dbo].[SETTINGS].[LASTCHANGEDON],
	[dbo].[SETTINGS].[ROWVERSION],
	[dbo].[SETTINGSMETADATA].[FRIENDLYNAME]
FROM [dbo].[SETTINGS]
	LEFT JOIN [dbo].[SETTINGSMETADATA] ON [dbo].[SETTINGSMETADATA].[SETTINGNAME] = [dbo].[SETTINGS].[NAME]
WHERE [dbo].[SETTINGS].[NAME] IN (SELECT [NAME] FROM @SETTINGNAMELIST)
ORDER BY [dbo].[SETTINGS].[NAME] ASC
END