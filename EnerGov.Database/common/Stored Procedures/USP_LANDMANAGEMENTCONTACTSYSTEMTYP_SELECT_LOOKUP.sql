CREATE PROCEDURE [common].[USP_LANDMANAGEMENTCONTACTSYSTEMTYP_SELECT_LOOKUP]	
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP].[LANDMANAGEMENTCONTACTSYSTEMTYP],
				[dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP].[NAME]
	FROM		[dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP]
	ORDER BY	[dbo].[LANDMANAGEMENTCONTACTSYSTEMTYP].[NAME]
END