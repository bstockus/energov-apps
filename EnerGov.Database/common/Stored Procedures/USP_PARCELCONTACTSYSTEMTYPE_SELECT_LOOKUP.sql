CREATE PROCEDURE [common].[USP_PARCELCONTACTSYSTEMTYPE_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[PARCELCONTACTSYSTEMTYPE].[PARCELCONTACTSYSTEMTYPEID],
				[dbo].[PARCELCONTACTSYSTEMTYPE].[NAME]
	FROM		[dbo].[PARCELCONTACTSYSTEMTYPE]
	ORDER BY	[dbo].[PARCELCONTACTSYSTEMTYPE].[NAME]
END