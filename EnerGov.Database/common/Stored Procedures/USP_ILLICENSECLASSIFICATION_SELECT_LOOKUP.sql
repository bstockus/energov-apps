CREATE PROCEDURE [common].[USP_ILLICENSECLASSIFICATION_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID],
				[dbo].[ILLICENSECLASSIFICATION].[NAME]
	FROM		[dbo].[ILLICENSECLASSIFICATION]
	ORDER BY	[dbo].[ILLICENSECLASSIFICATION].[NAME]
END