CREATE PROCEDURE [common].[USP_USERNOTIFICATION_INSERT]
(
	@USERID CHAR(36),
	@NOTIFICATIONSID CHAR(36),
	@DATECREATED DATETIME,
	@DATEREAD DATETIME
)
AS

INSERT INTO [dbo].[USERNOTIFICATION](
	[USERID],
	[NOTIFICATIONSID],
	[DATECREATED],
	[DATEREAD]
)

VALUES
(
	@USERID,
	@NOTIFICATIONSID,
	@DATECREATED,
	@DATEREAD
)