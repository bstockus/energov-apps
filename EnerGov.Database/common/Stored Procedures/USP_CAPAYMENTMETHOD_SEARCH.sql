CREATE PROCEDURE [common].[USP_CAPAYMENTMETHOD_SEARCH]
(
	@SEARCH				AS NVARCHAR(MAX)	=	'',
	@PAGE_NUMBER		AS INT				=	1,
	@PAGE_SIZE			AS INT				=	10,
	@IS_ASCENDING		AS BIT				=	1,
	@IS_ACTIVE			AS BIT				=	0

)
AS
WITH RAW_DATA AS (
	SELECT	[dbo].[CAPAYMENTMETHOD].[CAPAYMENTMETHODID],
			[dbo].[CAPAYMENTMETHOD].[NAME],
			CASE @IS_ASCENDING WHEN 1 THEN
				ROW_NUMBER() OVER(ORDER BY [dbo].[CAPAYMENTMETHOD].[NAME])
			ELSE
				ROW_NUMBER() OVER(ORDER BY [dbo].[CAPAYMENTMETHOD].[NAME] DESC)
			END AS RowNumber,
			COUNT(1) OVER() AS TotalRows
	FROM	[dbo].[CAPAYMENTMETHOD]
	WHERE	([dbo].[CAPAYMENTMETHOD].[NAME] LIKE '%'+ @SEARCH +'%')
			AND ([dbo].[CAPAYMENTMETHOD].[ISACTIVE] = 1 OR [dbo].[CAPAYMENTMETHOD].[ISACTIVE] = @IS_ACTIVE)
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber