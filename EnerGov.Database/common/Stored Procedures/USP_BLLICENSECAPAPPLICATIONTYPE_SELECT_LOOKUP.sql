CREATE PROCEDURE [common].[USP_BLLICENSECAPAPPLICATIONTYPE_SELECT_LOOKUP]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT		[dbo].[BLLICENSECAPAPPLICATIONTYPE].[BLLICENSECAPAPPLICATIONTYPEID],
				[dbo].[BLLICENSECAPAPPLICATIONTYPE].[NAME]
	FROM		[dbo].[BLLICENSECAPAPPLICATIONTYPE]
	ORDER BY	[dbo].[BLLICENSECAPAPPLICATIONTYPE].[NAME]
END