CREATE PROCEDURE [common].[USP_ILLICENSECLASSIFICATION_SEARCH]
(
	@SEARCH AS NVARCHAR(MAX) = '',
	@PAGE_NUMBER AS INT = 1,
	@PAGE_SIZE AS INT = 10,
	@IS_ASCENDING AS BIT = 1,
	@LICENSETYPEID AS char(36)
)
AS
WITH RAW_DATA AS (
	SELECT
		[dbo].[ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID],
		[dbo].[ILLICENSECLASSIFICATION].[NAME],
		CASE @IS_ASCENDING WHEN 1 THEN
			ROW_NUMBER() OVER(ORDER BY [dbo].[ILLICENSECLASSIFICATION].[NAME])
		ELSE
			ROW_NUMBER() OVER(ORDER BY [dbo].[ILLICENSECLASSIFICATION].[NAME] DESC)
		END AS RowNumber,
		COUNT(1) OVER() AS TotalRows
	FROM [dbo].[ILLICENSECLASSIFICATION]
	INNER JOIN 
	[dbo].[ILLICENSETYPELICENSECLASS] ON [dbo].[ILLICENSECLASSIFICATION].[ILLICENSECLASSIFICATIONID] =  [dbo].[ILLICENSETYPELICENSECLASS].[ILLICENSECLASSIFICATIONID]
	WHERE ([dbo].[ILLICENSECLASSIFICATION].[NAME] LIKE '%'+ @SEARCH +'%') 
	AND [dbo].[ILLICENSETYPELICENSECLASS].[ILLICENSETYPEID] = @LICENSETYPEID
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber