CREATE PROCEDURE [common].[USP_BLCONTACTTYPE_SEARCH]
(
	@SEARCH				AS NVARCHAR(MAX)	=	'',
	@PAGE_NUMBER		AS INT				=	1,
	@PAGE_SIZE			AS INT				=	10,
	@IS_ASCENDING		AS BIT				=	1,
	@SYSTEM_TYPE_ID		AS INT				=   0
)
AS
WITH RAW_DATA AS (
	SELECT	[dbo].[BLCONTACTTYPE].[BLCONTACTTYPEID],
			[dbo].[BLCONTACTTYPE].[NAME],
			[dbo].[BLCONTACTTYPE].[DESCRIPTION],
			[dbo].[BLCONTACTTYPESYSTEM].[NAME] AS [SYSTEMTYPE],
			[dbo].[BLCONTACTTYPE].[BLCONTACTTYPESYSTEMID],
			CASE @IS_ASCENDING WHEN 1 THEN
				ROW_NUMBER() OVER(ORDER BY [dbo].[BLCONTACTTYPE].[NAME])
			ELSE
				ROW_NUMBER() OVER(ORDER BY [dbo].[BLCONTACTTYPE].[NAME] DESC)
			END AS RowNumber,
			COUNT(1) OVER() AS TotalRows
	FROM	[dbo].[BLCONTACTTYPE]
			JOIN [dbo].[BLCONTACTTYPESYSTEM] WITH (NOLOCK) ON [dbo].[BLCONTACTTYPESYSTEM].[BLCONTACTTYPESYSTEMID] =  [dbo].[BLCONTACTTYPE].[BLCONTACTTYPESYSTEMID]
	WHERE	([dbo].[BLCONTACTTYPE].[NAME] LIKE '%'+ @SEARCH +'%' )	AND
			(@SYSTEM_TYPE_ID = 0 OR [dbo].[BLCONTACTTYPE].[BLCONTACTTYPESYSTEMID] = @SYSTEM_TYPE_ID)
)

SELECT * 
FROM RAW_DATA
WHERE
	RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) AND 
	RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY 
	RowNumber