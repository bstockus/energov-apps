CREATE PROCEDURE [gissetupapp].[USP_GEOFIELDMAPPING_GETBYPARENTID]
(
	@PARENTID CHAR(36)
)
AS
BEGIN
SET NOCOUNT ON;
SELECT		[dbo].[GEOFIELDMAPPING].[GEOFIELDMAPPINGID],
			[dbo].[GEOFIELDMAPPING].[GEORULEPROCESSID],
			[dbo].[GEOFIELDMAPPING].[ENTITYPROPERTY],
			[dbo].[GEOFIELDMAPPING].[GISATTRIBUTENAME],
			[dbo].[GEOFIELDMAPPING].[PROPERTYFRIENDLYNAME],
			[dbo].[GEOFIELDMAPPING].[ISCUSTOMFIELD],
			[dbo].[GEOFIELDMAPPING].[CUSTOMFIELDID]
FROM		[dbo].[GEOFIELDMAPPING]
INNER JOIN	[dbo].[GEORULEPROCESS]	ON [dbo].[GEORULEPROCESS].[GEORULEPROCESSID] = [dbo].[GEOFIELDMAPPING].[GEORULEPROCESSID]
INNER JOIN	[dbo].[GEORULE]			ON [dbo].[GEORULE].[GEORULEID] = [dbo].[GEORULEPROCESS].[GEORULEID]
WHERE		[dbo].[GEORULE].[GEORULEID] =  @PARENTID  

END