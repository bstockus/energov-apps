CREATE PROCEDURE [gissetupapp].[USP_GISMAPLAYER_GETALL]
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 
			[dbo].[GISMAPLAYER].[GISMAPLAYERID],
			[dbo].[GISMAPLAYER].[LAYERURL],
			[dbo].[GISMAPLAYER].[DESCRIPTION],
			[dbo].[GISMAPLAYER].[NAME],
			[dbo].[GISMAPLAYER].[LEGENDURL],
			[dbo].[GISMAPLAYER].[IS_KOOP_ENDPOINT],
			[dbo].[GISMAPLAYER].[LASTCHANGEDBY],
			[dbo].[GISMAPLAYER].[LASTCHANGEDON],
			[dbo].[GISMAPLAYER].[ROWVERSION]
	FROM	[dbo].[GISMAPLAYER]
	ORDER BY [dbo].[GISMAPLAYER].[NAME] ASC
END