CREATE PROCEDURE  [gissetupapp].[USP_GISZONEEXTERNALVALUE_GETALL]

AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[GISZONEEXTERNALVALUE].[GISZONEEXTERNALVALUEID],
	[dbo].[GISZONEEXTERNALVALUE].[GISZONEMAPPINGID],
	[dbo].[GISZONEEXTERNALVALUE].[VALUE],
	[dbo].[GISZONEEXTERNALVALUE].[IMINSPECTIONZONEID]
FROM [dbo].[GISZONEEXTERNALVALUE]
ORDER BY [dbo].[GISZONEEXTERNALVALUE].[VALUE] ASC

END