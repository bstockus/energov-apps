CREATE PROCEDURE [gissetupapp].[USP_GISLLMAPPINGFIELD_SEARCH]
(
	@SEARCH			AS VARCHAR(30)	=	'',
	@PAGE_NUMBER	AS INT			=	1,
	@PAGE_SIZE		AS INT			=	10,
	@IS_ASCENDING	AS BIT			=	1,
	@TABLENAME		AS VARCHAR(30)	=	''
)
AS

WITH RAW_DATA AS (
	SELECT		[dbo].[GISLLMAPPINGFIELD].[GISLLMAPPINGFIELDID],
				[dbo].[GISLLMAPPINGFIELD].[COLUMNNAME],
				CASE @IS_ASCENDING	WHEN 1 THEN ROW_NUMBER() OVER(ORDER BY [dbo].[GISLLMAPPINGFIELD].[COLUMNNAME])
										   ELSE ROW_NUMBER() OVER(ORDER BY [dbo].[GISLLMAPPINGFIELD].[COLUMNNAME] DESC)
				END AS RowNumber,
				COUNT(1) OVER() AS TotalRows
	FROM		[dbo].[GISLLMAPPINGFIELD]
	WHERE		[dbo].[GISLLMAPPINGFIELD].[TABLENAME] = @TABLENAME AND [dbo].[GISLLMAPPINGFIELD].[DISPLAYED] = 1
				AND [dbo].[GISLLMAPPINGFIELD].[COLUMNNAME] LIKE '%'+ @SEARCH +'%'
)

SELECT		* 
FROM		RAW_DATA
WHERE		RowNumber > @PAGE_SIZE * (@PAGE_NUMBER - 1) 
			AND RowNumber <= @PAGE_SIZE * @PAGE_NUMBER
ORDER BY	RowNumber