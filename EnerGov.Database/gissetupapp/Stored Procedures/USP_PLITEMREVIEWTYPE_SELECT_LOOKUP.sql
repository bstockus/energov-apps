CREATE PROCEDURE [gissetupapp].[USP_PLITEMREVIEWTYPE_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT		[dbo].[PLITEMREVIEWTYPE].[PLITEMREVIEWTYPEID],
			[dbo].[PLITEMREVIEWTYPE].[NAME],
			[dbo].[PLSUBMITTALTYPEITEMREVIEWTYPEX].[PLSUBMITTALTYPEID]
FROM		[dbo].[PLITEMREVIEWTYPE]
INNER JOIN	[dbo].[PLSUBMITTALTYPEITEMREVIEWTYPEX] ON [dbo].[PLSUBMITTALTYPEITEMREVIEWTYPEX].[PLITEMREVIEWTYPEID] = [dbo].[PLITEMREVIEWTYPE].[PLITEMREVIEWTYPEID]
WHERE		[dbo].[PLSUBMITTALTYPEITEMREVIEWTYPEX].[AUTOADD] != 1			
ORDER BY	[dbo].[PLITEMREVIEWTYPE].[NAME]