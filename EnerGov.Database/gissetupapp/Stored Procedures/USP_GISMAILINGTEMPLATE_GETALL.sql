CREATE PROCEDURE [gissetupapp].[USP_GISMAILINGTEMPLATE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[GISMAILINGTEMPLATE].[GISMAILINGTEMPLATEID],
	[dbo].[GISMAILINGTEMPLATE].[NAME],
	[dbo].[GISMAILINGTEMPLATE].[DESCRIPTION],
	[dbo].[GISMAILINGTEMPLATE].[MAPSERVICEURL],
	[dbo].[GISMAILINGTEMPLATE].[FEATURECLASSNAME],
	[dbo].[GISMAILINGTEMPLATE].[NAME1],
	[dbo].[GISMAILINGTEMPLATE].[NAME2],
	[dbo].[GISMAILINGTEMPLATE].[ADDRESS1],
	[dbo].[GISMAILINGTEMPLATE].[ADDRESS2],
	[dbo].[GISMAILINGTEMPLATE].[ADDRESS3],
	[dbo].[GISMAILINGTEMPLATE].[ADDRESS4],
	[dbo].[GISMAILINGTEMPLATE].[LASTCHANGEDBY],
	[dbo].[GISMAILINGTEMPLATE].[LASTCHANGEDON],
	[dbo].[GISMAILINGTEMPLATE].[ROWVERSION]
FROM [dbo].[GISMAILINGTEMPLATE]
ORDER BY 
	[dbo].[GISMAILINGTEMPLATE].[NAME]
END