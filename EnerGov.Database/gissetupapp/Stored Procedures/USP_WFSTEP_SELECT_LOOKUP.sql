CREATE PROCEDURE [gissetupapp].[USP_WFSTEP_SELECT_LOOKUP]
AS
SET NOCOUNT ON;
SELECT		[dbo].[WFSTEP].[WFSTEPID], 
			[dbo].[WFSTEP].[NAME],
			[dbo].[WFENTITYSTEPTYPE].[WFENTITYID],
			[dbo].[WFSTEPTYPE].[WFSTEPTYPEID]
FROM		[dbo].[WFSTEP]
INNER JOIN	[dbo].[WFSTEPTYPE]			ON [dbo].[WFSTEPTYPE].[WFSTEPTYPEID] =  [dbo].[WFSTEP].[WFSTEPTYPEID]
INNER JOIN	[dbo].[WFENTITYSTEPTYPE]	ON [dbo].[WFENTITYSTEPTYPE].[WFSTEPTYPEID] = [dbo].[WFSTEPTYPE].[WFSTEPTYPEID]
ORDER BY	[dbo].[WFSTEP].[NAME]