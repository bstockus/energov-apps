CREATE PROCEDURE [gissetupapp].[USP_MAILINGADDRESSTYPE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[MAILINGADDRESSTYPE].[MAILINGADDRESSTYPEID],
	[dbo].[MAILINGADDRESSTYPE].[MAILINGADDRESSTYPENAME],
	[dbo].[MAILINGADDRESSTYPE].[MAILINGADDRESSDESCRIPTION],
	[dbo].[MAILINGADDRESSTYPE].[SYSTEMACTIONID],
	[dbo].[MAILINGADDRESSTYPE].[ISDEFAULT],
	[dbo].[MAILINGADDRESSTYPE].[LASTCHANGEDBY],
	[dbo].[MAILINGADDRESSTYPE].[LASTCHANGEDON],
	[dbo].[MAILINGADDRESSTYPE].[ROWVERSION]
FROM 
	[dbo].[MAILINGADDRESSTYPE] 
ORDER BY 
	[dbo].[MAILINGADDRESSTYPE].[MAILINGADDRESSTYPENAME]
END