CREATE PROCEDURE [gissetupapp].[USP_IMINSPECTIONZONE_GETALL]
AS
BEGIN
SET NOCOUNT ON;
SELECT 
	[dbo].[IMINSPECTIONZONE].[IMINSPECTIONZONEID],
	[dbo].[IMINSPECTIONZONE].[NAME],
	[dbo].[IMINSPECTIONZONE].[DESCRIPTION],
	[dbo].[IMINSPECTIONZONE].[DEFAULTTRAVELTIME],
	[dbo].[IMINSPECTIONZONE].[LASTCHANGEDBY],
	[dbo].[IMINSPECTIONZONE].[LASTCHANGEDON],
	[dbo].[IMINSPECTIONZONE].[ROWVERSION]
FROM [dbo].[IMINSPECTIONZONE]
	ORDER BY [dbo].[IMINSPECTIONZONE].[NAME]
END