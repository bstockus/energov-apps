CREATE PROCEDURE [application].[USP_PLAPPLICATIONCONTACT_GETBYIDS]
(
	@PLAPPLICATIONLIST RecordIDs READONLY
)
AS
BEGIN
	SELECT 
		PLAPPLICATIONCONTACT.PLAPPLICATIONID,
		PLAPPLICATIONCONTACT.GLOBALENTITYID,
		GLOBALENTITY.FIRSTNAME,
		GLOBALENTITY.LASTNAME,
		GLOBALENTITY.GLOBALENTITYNAME,
		PLAPPLICATIONCONTACT.ISBILLING, 
		GLOBALENTITY.TITLE,
		GLOBALENTITY.ISACTIVE,
		LANDMANAGEMENTCONTACTTYPE.LANDMANAGEMENTCONTACTTYPEID,
		LANDMANAGEMENTCONTACTTYPE.NAME
	FROM  [dbo].[PLAPPLICATIONCONTACT]
	JOIN GLOBALENTITY ON PLAPPLICATIONCONTACT.GLOBALENTITYID = GLOBALENTITY.GLOBALENTITYID
	JOIN LANDMANAGEMENTCONTACTTYPE ON PLAPPLICATIONCONTACT.PLPLANCONTACTTYPEID = LANDMANAGEMENTCONTACTTYPE.LANDMANAGEMENTCONTACTTYPEID
	JOIN @PLAPPLICATIONLIST PLL ON PLAPPLICATIONCONTACT.PLAPPLICATIONID = PLL.RECORDID
END